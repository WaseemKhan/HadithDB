      <blockquote>
        <p> Adi b. Hatim reported: Allah's Messenger (way peace be upon him) said to me: When you let off your dog, recite the name of Allah, and if it catches (game for you) and you find it alive, then slaughter it; if you find it killed and that (your dog) has eaten nothing out of that, (even then) you may eat it; but if you find along with your dog another dog, and (the game an) dead, then don't eat, for you do not know which of the two has killed it. And if you shoot your arrow, recite the name of Allah, but if it (game) goes out of your sight for a day and you do not find on that but the mark of your arrow, then eat that it you so like, but if you find it drowned in water, then don't eat that.</p>
      </blockquote>
