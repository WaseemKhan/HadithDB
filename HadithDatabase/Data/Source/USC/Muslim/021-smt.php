      <p><strong> Chapter 4: IT IS PERMISSIBLE TO EAT THE ANIMALS OF WATER EVEN IF THEY ARE DEAD </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="021.4756"> Book 021, Number 4756: </a> </em>
      <blockquote>
        <p> Jabir reported: Allah's Messenger (may peace he upon him) sent us (on an expedition) and appointed Abu 'Ubaida our chief that we might intercept a caravan of the Quraish and provided us with a bag of dates. And he found for us nothing besides it. Abu Ubaida gave each of us one date (everyday). I (Abu Zubair, one of the narrators) said: What did you do with that? He said: We sucked that just as a baby sucks and then drank water over that, and it sufficed us for the day until night. We beat off leaves with the help of our staffs, then drenched them with water and ate them. We then went to the coast of the sea, and there rose before us on the coast of the sea something like a big mound. We came near that and we found that it was a beast, called al-'Anbar (spermaceti whale). Abu 'Ubaida said. It is dead. He then said: No (but it does not matter), we have been sent by the Messenger of Allah (may peace be upon him) in the path of Allah and you are hard pressed (on account of the scarcity of food), so you eat that. We three hundred in number stayed there for a month, until we grew bulky. He (Jabir) said: I saw how we extracted pitcher after pitcher full of fat from the cavity of its eye, and sliced from it compact piece of meat equal to a bull or like a bull. Abu 'Ubaida called forth thirteen men from us and he made them sit in the cavity of its eye, and he took hold of one of the ribs of its chest and made it stand and then saddled the biggest of the camels we had with us and it passed under it (the arched rib), and we provided ourselves with pieces of boiled meat (especially for use in our journey). When we came back to Medina, we went to Allah's Messenger (may peace be upon him) and made a mention of that to him, whereupon he said: That was a provision which Allah had brought forth for you. Is there any piece of meat (left) with you, so tnat you give to us that? He (Jabir) said: We sent to Allah's Messenger (may peace be upon him) tome of that (a piece of meat) and he ate it.</p>
      </blockquote>
