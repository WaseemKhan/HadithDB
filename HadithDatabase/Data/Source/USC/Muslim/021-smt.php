      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 21: </a> </em>
      <h1> <em> The Book of Games and the Animals which May be Slaughtered and the Aninals that Are to be Eaten (Kitab-us-Said wa'l-Dhaba'ih wa ma Yu'kalu min Al-Hayawan) </em> </h1>
      <br>
      <p><strong> Chapter 1: HUNTING WITH THE HELP OF TRAINED DOGS </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="021.4732"> Book 021, Number 4732: </a> </em>
      <blockquote>
        <p> 'Adi b. Hatim reported: I said: Messenger of Allah, I set off trained dogs and they catch for me (the game) and I recite the came of Allah over it (I slaughter the game by reciting Bismillah-i-Allah-o-Akbar), whereupon he said: When you set off your trained dogs and you recited the name of Allah (while setting them off), then eat (the game). I said: Even if them (the trained dogs) kill that (the game)? He (the Holy Prophet) said: Even if these kill, but (on the condition) that no other dog, which you did not set off (along with your dogs), participates (in catching the game). I said to him: I throw Mi'rad, a heavy featherless blunt arrow, for hunting and killing (the game). Thereupon he said: When you throw Mi'rad, and it pierces, then eat, but if it falls flatly (and beats the game to death), then do not eat that.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="021.4733"> Book 021, Number 4733: </a> </em>
      <blockquote>
        <p> 'Adi b. Hatim reported: I asked Allah's Messenger (may peace be upon him) saying: We are a people who hunt with these (trained) dogs, then (what should we do)? Thereupon he (the Holy Prophet) said: When you set of your trained dogs having recited the name of Allah, then eat what these (hounds) have caught for you, oven if it (the game) is killed, provided (the hunting dog) has not eaten (any part of the game). If it has eaten (the game), then you don't eat it as I fear that it might have caught for its own self. And do not eat in case other dogs have joined your trained dogs.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="021.4734"> Book 021, Number 4734: </a> </em>
      <blockquote>
        <p> 'Adi b. Hatim reported that he asked the Messenger of Allah (may peace be upon him) about (hunting) with the help of an arrow having a stub end. He said: If it strikes (the game) with its point, then eat, but if it strikes flatly and it dies, that is Waqidh (beaten into death), do not eat that. I asked the Messenger of Allah (may peace be upon him) about (hunting with the help of) dogs, whereupon he said. When you send your dog (for hunting) reciting the name of Allah, then eat (the game), but if some part of it is eaten (by the dogs, then do not eat that, for it (your dog) has caught that (the-game) for itself. I (again) said: If I find along with my dog another dog, and do not know which of (the dogs) has caught (the game). then (what should I do)? Thereupon he ('Allah's Messenger) said: Then don't eat that, for you recited the name of Allah on your dog and not on the other one.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="021.4735"> Book 021, Number 4735: </a> </em>
      <blockquote>
        <p> Adi b. Hatim reported: I asked Allah's Messenger (may peace be upon him) about Mi'rad (i. e. hunting with the help of arrow having a stub end, and he stated the same (as we find in the previous hadith).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
