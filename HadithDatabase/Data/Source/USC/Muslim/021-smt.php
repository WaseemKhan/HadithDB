      <blockquote>
        <p> 'Adi b. Hatim reported: I asked Allah's Messenger (may peace be upon him) about hunting the game with the help of Mi'rad, whereupon he said: If it strikes (the game) with its point, then eat it, but if it strikes flat, that is (the game is) beaten (into death), (then do not eat that) 'Adi further said: I asked him about hunting with the help of a dog, whereupon he said: If that (the dog) catches it (the game) for you and does not eat out of that, then you eat (the game) for Dhakat (slaughtering) of that is its being caught by it (by the dog). But if you find another dog besides it, and you fear that that dog (the second one) had caught it (the game) along with that (your dog) and killed it. then don't eat; for you recited the name of Allah on your dog and did not recite that on the other one (which joined your dog incidentally).</p>
      </blockquote>
