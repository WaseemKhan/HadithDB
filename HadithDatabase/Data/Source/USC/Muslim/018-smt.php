      <div class="content"><em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 18: </a> </em>
        <h1> <em> The Book Pertaining to Judicial Decisions (Kitab Al-Aqdiyya) </em> </h1>
        <br>
        <p><strong> Chapter 1: IT IS THE DEFENDANT WHO SHOULB SWEAR (TO PROVE HIS INNOCENCE) </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4244"> Book 018, Number 4244: </a> </em>
        <blockquote>
          <p> Ibn Abbas reported Allah's Apostle (may peace be upon him) as saying: If the people were given according to their claims, they would claim the lives of persons and their properties, but the oath must be taken by the defendant.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4245"> Book 018, Number 4245: </a> </em>
        <blockquote>
          <p> Ibn 'Abbas reported that Allah's Messenger (may peace be upon him) pronounced judgment on the basis of oath by the defendant.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4246"> Book 018, Number 4246: </a> </em>
        <blockquote>
          <p> Ibn 'Abbas reported that Allah's Messenger (may peace be upon him) pronounced judgment on the basis of an oath and a witness (by the plaintiff).</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 2: JUDGMENT IS TO BE PRONOUNCED ON EVIDENCE, AND ONE WHO IS ELOQUENT IN HIS PLEA (MAY WIN THE CASE) </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4247"> Book 018, Number 4247: </a> </em>
        <blockquote>
          <p> Umm Salama reported Allah's Messenger (may peace be upon him) as saying: You bring to me, for (judgment) your disputes, some of you perhaps being more eloquent in their plea than others, so I give judgment on their behalf according to what I hear from them. (Bear in mind, in my judgment) if I slice off anything for him from the right of his brother, he should not accept that, for I sliced off for him a portion from the Hell.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4248"> Book 018, Number 4248: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Hisham with the same chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4249"> Book 018, Number 4249: </a> </em>
        <blockquote>
          <p> Umm Salama, the wife of Allah's Apostle (may peace be upon him), reported that Allah's Messenger (may peace be upon him) heard the clamour of contenders at the door of his apartment. He went to them, and said: I am a human being and the claimants bring to me (the dispute) and perhaps some of them are more eloquent than the others. I judge him to be on the right, and thus decide in his favcur. So he whom I, by my judgment, (give the undue share) out of the right of a Muslim,. I give him a portion of Fire; he may burden himself with it or abandon it.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4250"> Book 018, Number 4250: </a> </em>
        <blockquote>
          <p> This hadith has been transmitted on the authority of Ma'mar with a slight variation of words.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4251"> Book 018, Number 4251: </a> </em>
        <blockquote>
          <p> A'isha reported: Hind. the daughter of 'Utba, wife of Abu Sufyan, came to Allah's Messenger (may peace be upon him) and said: Abu Sufyan is a miserly person. He does not give adequate maintenance for me and my children, but (I am constrained) to take from his wealth (some part of it) without his knowledge. Is there any sin for me? Thereupon Allah's Messenger (may peace be upon him) said: Take from his property what is customary which may suffice you and your children.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4252"> Book 018, Number 4252: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Hisham with the same chair of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4253"> Book 018, Number 4253: </a> </em>
        <blockquote>
          <p> A'isha reported that Hind came to Allah's Apostle (may peace be upon him) and said: Messenger of Allah, by Allah, there was no other household upon the surface of the earth than your household about which I cherished Allah bringing disgrace upon it, (and now) there is no other household upon the surface of the earth than your household about which I cherish Allah granting it honour. Allah's Apostle (may peace be upon him) said: It is so, by Him in Whose Hand is my life She said: Allah's Messenger, Abu Sufyan is a niggardly person. Is there any harm for me if I spend upon his children out of his wealth without his permission? Thereupon Allah's Apostle (may peace be upon him) said: There is no harm for you if you spend upon them what is reasonable.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4254"> Book 018, Number 4254: </a> </em>
        <blockquote>
          <p> A'isha reported that Hind, daughter of Utba h. Rabi', came to Allah's Messenger (may peace be upon him) and said: Allah's Messenger, by Allah, there was no household upon the surface of the earth than your household about which I cherished that it should be disgraced. But today there is no household on the surface of the earth than your household about which I cherish that it be honoured Thereupon Allah's Messenger (may peace be upon him) said. It will increase, by Him in Whose Hand is my life. She then said: Messenger of Allah, Abu Sufyan is a niggardly person; is there any harm for me if I spend out of that which belongs to him on our children? He said to her: No, but only that what is reasonable.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 3: IT IS FORBIDDEN TO ASK MANY QUESTIONS AND WASTING OF WEALTH </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4255"> Book 018, Number 4255: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Verily Allah likes three things for you and He disapproves three things for you. He is pleased with you that you worship Him and associate nor anything with Him, that you hold fast the rope of Allah, and be not scattered; and He disapproves for you irrelevant talk, persistent questioning ane the wasting of wealth.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4256"> Book 018, Number 4256: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Suhail with the same chain of transmitters, but with a slight variation of words.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4257"> Book 018, Number 4257: </a> </em>
        <blockquote>
          <p> Mughira b. Shu'ba reported Allah's Messenger (may peace be upon him) as saying: Verity Allah, the Glorious and Majestic, has forbidden for you: disobedience to mothers, and burying alive daughters, withholding the right of others in spite of having the power to return that to them and demanding that (which is not one's legitimate right). And He disapproved three things for you; irrelevant talk, persistent questioning and wasting of wealth.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4258"> Book 018, Number 4258: </a> </em>
        <blockquote>
          <p> A hadith like this has been trransmitted on the authority of Mansur with a slight vairiation of words.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4259"> Book 018, Number 4259: </a> </em>
        <blockquote>
          <p> Sha'bi reported that the scribe of al-Mughira b. Shu'ba said: Mu'awiya wrote to Mughira: Write for me something which you heard from Allah's Messenger (may peace be upon him) ; and he wrote: I heard Allah's Messenger (may peace be upon him) as saying. Verily Allah disapproves three thingq for you: irrelevant talk, wasting of wealth and persistent questioning.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4260"> Book 018, Number 4260: </a> </em>
        <blockquote>
          <p> Warrad reported that al-Mughira wrote to Mu'awiya: Peace be upon you, and then coming to the poirt (I should say) that I heard Allah's Messenger (may peace be upon him) as saying: Verily Allah has Prohibited three things and has forbidden three things. He has declared absolutely haram the disobedience of father, burying of daughters alive, and withholding that which you have power to return, and has forbidden three things: irrelevant talk, persistent questioning, and wasting of wealth.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 4: THE REWARD OF THE JUDGE WHEN HE TRIES TO ARRIVE AT A DECISION, WHETHER THAT DECISION IS CORRECT OR INCORRECT </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4261"> Book 018, Number 4261: </a> </em>
        <blockquote>
          <p> 'Amr b. al-'As reported that he heard Allah's Messenger (may peace be upon him) as saying: When a judge gives a decision, having tried his best to decide correctly and is right, there are two rewards for him; and if he gave a judgment after having tried his best (to arrive at a correct decision) but erred, there is one reward for him.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4262"> Book 018, Number 4262: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu Huraira with another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4263"> Book 018, Number 4263: </a> </em>
        <blockquote>
          <p> This hadith has been transmitted on the authority of Usama b. al-Had al-Laithi.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 5: IT IS NOT DESIRABLE TO GIVE JUDGMENT IN A FIT OF RAGE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4264"> Book 018, Number 4264: </a> </em>
        <blockquote>
          <p> Abd al-Rabmin b. Abu Bakra reported: My father dictated (and I wrote for him) to Ubaidullah b. Abu Bakra while he was the judge of Sijistan: Do not judge between two persons when you are angry, for I have heard Allah's Messenger (may peace be upon him) as saying: None of you should judge between two persons when he is angry.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4265"> Book 018, Number 4265: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of 'Abd al-Rahman b. Abu Bakra through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 6: REJECTING OF THE WRONG THINGS AND THE INNOVATIONS (IN RELIGION) </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4266"> Book 018, Number 4266: </a> </em>
        <blockquote>
          <p> 'A'isha reported Allah's Messenger (may peace be upon him) as saying: He who innovates things in our affairs for which there is no valid (reason) (commits sin) and these are to be rejected.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4267"> Book 018, Number 4267: </a> </em>
        <blockquote>
          <p> Sa'd b. Ibrahim reported: I asked Qasim b. Muhammad about a person who had three dwelling houses and he willed away the third part of every one of these houses; he (Qasim b. Muhammad) said: All of them could be combined in one house; and then said: 'A'isha informed me that Allah's Messenger (may peace be upon him) said: He who did any act for which there is no sanction from our behalf, that is to be rejected.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 7: AN EXCELLENT WITNESS </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4268"> Book 018, Number 4268: </a> </em>
        <blockquote>
          <p> Zaid b. Khalid al-Juhani reported Allah's Apostle (may peace be upon him) as saying: Should I not tell you of the best witnesses? He is the one who produces his evidence before he is asked for it.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 8: THE DIFFERENCE OF THOSE WHO ENDEAVOUR TO ARRIVE AT THE CORRECT DECISION </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4269"> Book 018, Number 4269: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Apostle (may peace be upon him) as saying: While two women had been going along witn their two sons, a wolf came and made away with the child of one of them. One of them said to her companion: It is with your child that it (the wolf) has run away The other one said: It has run away with your child. They brought the matter to (Hadrat) Dawud (David) for decision and he made a decision in favour of the elder one. They then went to Sulaiman b. Dawud (may there be peace upon both of them) and told them (the story). He said: Bring me a knife so that I may cut him (the child) (into two parts) for you. The younger one said: No, it can't be, may Allah have mercy upon you, he (the child) belongs to her (the elder). So he gave a decision in favour of the younger one. abu Huraira said: If ever I heard of the word as-sikin at all, it was that day. We called it by no other name but al-Mudya.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4270"> Book 018, Number 4270: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu az-Zinad with the same chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 9: IT IS MERITORIOUS FOR A JUDGE TO BRING ABOUT CONCILIATION BETWEEN TWO CONTENDING PERSONS OR PARTIES </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4271"> Book 018, Number 4271: </a> </em>
        <blockquote>
          <p> Hammim b. Munabbih said: Abu Huraira reported (so many) ahadith of Allah's Messenger (may peace be upon him), and one of them is this: A person bought from another person a piece of land, and the person who had, bought that land found in it an earthen ware which contained gold. The person who had bought the land said (to the seller of the land): Take your gold from me, for I bought only the land from you and not the gold. The man who had sold the land said: I sold the land to you and whatever was in it. They referred the matter to a person. One who was made as a judge said to them: Have you any issue? One of them said: I have a boy, and the other said: I have a young daughter He (the judge) said: Marry this young boy with the girl, and spend something on yourselves and also give (some) charity out of it.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 10: STRAY THING FOUND BY ANYONE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="018.4272"> Book 018, Number 4272: </a> </em>
        <blockquote>
          <p> Zaid b. Khalid al-Juhani reported: A man came to Allah's Apostle (may peace be upon him) and asked him about picking up of stray articles. He said: Recognise (well) its bag and the strap (by which it is tied) then make announcement of that for a year. If its owner comes (within this time return that to him), otherwise it is yours. He (again) said: (What about) the lost goat? Thereupon he (the Holy Prophet) said: It is yours or for your brother, or for the wolf. He said: (What about) the lost camel? Thereupon he said: You have nothing to do with it; it has a leather bag along with it, and its shoes also. It comes to the watering-place, eats (the leaves of the) trees until its master finds him.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4273"> Book 018, Number 4273: </a> </em>
        <blockquote>
          <p> Zaid b. Khalid al-Juhani reported that a person asked Allah's Apostle (may peace be upon him) about picking up of stray articles, whereupon he said: Make announcement about it for a year, and recognise well the strap and the bag (containing that) ; then spend that; and if its owner comes, make him the payment of that. He (the inquirer) said: Messenger of Allah, what about the lost goat? he said: Take it, for that is yours or for your brother, or for the wolf. He (again) said: (What about) the lost camel? The Messenger of Allah (may peace be upon him) was enraged until his cheeks became red (or his face became red) and then said: You have nothing to do about that; it has feet and a leather bag (to quench its thirst) until its owner finds it.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4274"> Book 018, Number 4274: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Rabi'a b. Abu Abd al-Rahman with the same chain of transmitters but with this addition:" There came a person to Allah's Messenger (may peace be upon him) while I was with him, and he asked him about picking up of a stray article, and he said: When none comes to demand it, then spend that."</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4275"> Book 018, Number 4275: </a> </em>
        <blockquote>
          <p> Zaid b. Khalid al-Juhani reported. There came to Allah's Messenger (may peace be upon him) a person, the rest of the hadith is the same but with the variation (of these words): His face became red, his forehead too, and he felt annoyed; and made an addition after the words: He should make announcement of that for a year, and if its owner does not turn up, then it is a trust with you.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="018.4276"> Book 018, Number 4276: </a> </em>
        <blockquote>
          <p> Zaid b. Khalid al-Juhani, the Companion ot Allah's Messenger (may peace be upon him), said that Allah's Messenger (may peace be upon him) was asked about the picking up of stray gold or silver, whereupon he said: Recognise well the strap and the bag (containing) that and then make an announcement regarding that for one year, but if none recognises it, then spend that and it would be a trust with you; and if someone comes one day to make demand of that, then pay that to him. He (the inquirer) asked about the lost camel, whereupon he said: You have nothing to do with that. Leave that alone, for it has feet and also a leather bag, it drinks water, and eats (the leaves) of the trees. He asked him about sheep, whereupon he said: Take it, it is for you, or for your brother, or for the wolf.</p>
        </blockquote>
