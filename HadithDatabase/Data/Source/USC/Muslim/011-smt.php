      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 11: </a> </em>
      <h1> <em> The Book Pertaining to the Rules of Inheritance (Kitab Al-Farai`d) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> Inheritance is the entry of living persons into possession of dead persons' property and exists in some form wherever the institution of private property is recognised as the basis of the social and economic system. The actual forms of inheritance and the laws governing it, however, differ according to the ideals of different societies. </p>
      <p> The law of inheritance in Islam is based upon five main considerations: </p>
      <p> To break up the concentration of wealth in individuals and spread it out in society. </p>
      <p> To respect the property right of ownership of an individual earned through honest means. </p>
      <p> To hammer in the consciousness of man the fact that man is not the absolute master of wealth he produces but he is its trustee and is not, therefore, authorised to pass it on to others as he likes. </p>
      <p> To consolidate the family system which is the social unit of an Islamic society. </p>
      <p> To give incentive to work and encourage economic activity as sanctioned by Islam. </p>
      <p> In the pre-Islamic world and even in modern societies the law of inheritance has so many evils in it, which may be summed up in the following points: </p>
      <p> Women had been completely denied the share of inheritance. They were rather regarded as part of the property of the deceased and, therefore, their right to property by inheritance was out of question. </p>
      <p> In pre-Islamic Arabia and other countries where there had been tribal societies not only women were deprived of the right of inheritance but even weak and sick persons and minor children were given no share in it, as the common principle of inheritance was that he alone is entitled to inherit who wields the sword. </p>
      <p> Then in certain societies there had been existing the law of primogeniture and it exists even today in some of the so-called civilised parts of the world which entitles only the eldest son to inherit the whole of the father's property or to get the lion's share. </p>
      <p> Islam introduced so many reforms in the laws of inheritance which can be succinctly summed up as follows. </p>
      <p> It defined and determined in clear-cut terms the share of each inheritor and imposed limits on the right of the property-owner to dispose of his property according to his whim and caprice. </p>
      <p> It made the female, who had been prevously thought a chattel, the co-sharer with the male and thus not only restored her dignity, but safeguarded her social and economic rights. </p>
      <p> It laid the rules for the break-up of the concentrated wealth in the society and helped in its proper and equitable distribution amongst a large number of persons. </p>
      <p> It gave a death-blow to the law of primogeniture and thus provided the democratic basis for the division of the property of the deceased. </p>
      <p> The above are some of the distinguishing features of the Islamic law of inheritance. While laying down the rules for the distribution of the estate of the deceased, the first principle to be observed is that the property both movable and immovable can be distributed after meeting the following obligations: </p>
      <p> funeral expenses; </p>
      <p> clearing off the debts incurred by the deceased; </p>
      <p> payment of bequest, if any, to the extent of one-third of the total assets. It may be remembered that the Mahr of the wife, if it had not been paid, is included in the debt. Moreover, it is not lawful to make a bequest in favour of a person who is entitled to a share in the inheritance. </p>
      <p> Four persons cannot get inheritance: </p>
      <p> (a) a fugitive slave who has fled away from his master, </p>
      <p> (b) one who has murdered one's predecessor intentionally or unintentionaly </p>
      <p> (c) one who professes a religion other than Islam, </p>
      <p> (d) one living in Dar-ul-Harb cannot inherit the property of one living in Dar-ul-Islam and vice versa. </p>
      <p> According to Islam, the heirs have been divided into three classes. </p>
      <p> (A) Dhaw-u'l-Fara'id are those persons who have a right to definite shares in assets left by the deceased. These sharers are twelve in number; four males: father, grandfather, uterine brothers and husband; and eight females: wife, single daughter, son's daughter, mother, grandmother, full sister, consanguine sister, uterine sister. </p>
      <p> Father's share is one-sixth when the deceased leaves a son or a son's son, but if the deceased is not survived by a son or grandson his father will, in addition to this share (one-sixth), also get a share of being 'Asaba. </p>
      <p> The grandfather's share is like that of father's share but in three conditions: </p>
      <p> According to Imam Bukhiri and Imam Muslim, the presence of father deprives even the brothers of their share in the inheritance. but this is not the case with the grandfather. Imam Abu Hanifa is of the opinion that the presence of grandfather deprives the brother of his share in the inheritance. </p>
      <p> If the father of the deceased is alive, then the share of the mother is of what is left from the share of the wife of the deceased. The presence of grandfather does not reduce the share of the mother of the deceased. </p>
      <p> The grandmother of the deceased has no share in the presence of the father of the deceased but she has a share in the presence of the grandfather. </p>
      <p> The third set of sharers are uterine brothers and sisters. They are entitled to one-sixth if their number is one, and one-third if they are more than one. </p>
      <p> The husband's share is one-half of the property of the deceased wife if she has no children, but in case of children it is one-fourth. </p>
      <p> The wife is entitled to one-fourth if the husband dies childless; otherwise it is one-eighth. </p>
      <p> Real daughter: one-half when alone, and two-thirds if more than one. If the deceased is survived by a male child also. the daughters are then treated as Asaba and the male child would get double of what falls to the lot of daughters. The granddaughters stand on the same level as daughters. But in case the deceased is survived by one real daughter and one or more than one granddaughter they would get one-sixth. The granddaughter is not entitled to any share if the deceased is survived by a son, but if he is survived by grandsons and granddaughters, they would be treated as 'Asaba and the male grandchild would get double of what goes to the female grandchild. </p>
      <p> Full sister gets one-half if she is alone, and two-thirds if they are more than one. </p>
      <p> Consanguine sister is entitled to one-half if one, and two thirds if more. </p>
      <p> Mother is entitled to one-sixth when she has a child or grandchild, and in case of being childless she gets one-third of the share. </p>
      <p> If the deceased is survived either by paternal grandmother or maternal grand- mother or even by both, they are entitled to one-sixth. The grandmother (maternal) is deprived of her share if the mother of the deceased is alive; and if father is alive the paternal grandmother is deprived of this share. </p>
      <p> (B) When the heirs of the first group have received the respective shares, the residue of the assets falls to the share of those relatives who are called Asaba which, according to the Shari'ah, implies those relatives in whose line of relationship no female enters. This is the second group of inheritors. </p>
      <p> There is no fixed share of the 'Asabat. If the deceased is not survived by any Dhaw-u'l-Fara'id, the whole of the property falls to their share; and If Dhaw-u'I. Fara'id are there to get their due share, the residue will be taken by the Asabat. The following are the 'Asabat: </p>
      <p> Son: He is the first to get the residue in order of succession. The daughters are entitled to half of the share as given to the son. The grandsons are not entitled to any share in the presence of the son. If the son is not living, then the grandson is entitled to a share in the inheritance. If there are more than one son, the inheritance will be distributed equally amongst them. </p>
      <p> The father, grandfather and the great-grandfather are included in the category of Dhaw-u'l-Fara'id. If, however, the deceased is not survived by category of a son, grandson of great-grandson, then the father will fall under the category of 'Asaba, and, in the absence of the father, the grandfather assumes that position. </p>
      <p> If the deceased is not survived by son, or grandson or father or grandfather, i. e. none amongst the 'Asabat, then the brother, and in the absence of brother his son, and in the absence of son, his grandson will be entitled to share in the inheritance as 'Asaba and the female would also join them in share claiming half of the share as compared with male. </p>
      <p> If unfortunately the deceased is survived by none of the above-mentioned relatives amongst the 'Asabat, then consanguine brother will be entitled to share in the inheritance and he will be preferred to full brother's son. </p>
      <p> Then comes the turn of full paternal uncle. </p>
      <p> (C) The last category of inheritors are known ad Dhaw-u'l Arham, i. e. relations connected through females, but it is in extremely rare cases that they get any share in the inheritance. The following relatives come under this category. </p>
      <p> The son of the daughter and daughter of the daughter. </p>
      <p> The son of the daughter of the son, and daughter of the daughter of the son and their children. </p>
      <p> Maternal grandfather, maternal grandfather of the father, the grandfather of the mother, maternal grandfather of the mother, the grandmother of the mother, the children of the sisters, the sisters of the father and those of the mother, etc. </p>
