      <blockquote>
        <p> Mu'awiya b. Suwaid b. Muqarrin reporxed: I visited al-Bara' b. 'Azib and heard him say: Allah's Messenger (may peace be upon him) commanded us to do seven things and forbade us to do seven (things). He commanded us to visit the sick, to follow the funeral procession, to answer the sneezer, to fulfil the vow, to help the poor, to accept the invitation and to greet everybody, and he forbade us to wear rings or gold rings, to drink in silver (vessels), and to use the saddle cloth made of red silk, and to wear garments made of Qassi material, or garments made of silk or brocade and velvet.</p>
      </blockquote>
