      <h1> <em> The Book Pertaining to Clothes and Decoration (Kitab Al-Libas wa'l-Zinah) </em> </h1>
      <br>
      <p><strong> Chapter 1: THE USE OF GOLD AND SILVER VESSELS IS FORBIDDEN TO MEN AND WOMEN AND IT IS ALSO PROHIBITED TO DRINK IN THEM </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="024.5126"> Book 024, Number 5126: </a> </em>
      <blockquote>
        <p> Umm Salama, the wife of Allah's Apostle (may peace be upon him), said: He who drinks in the vessel of silver in fact drinks down in his belly the fire of Hell.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="024.5127"> Book 024, Number 5127: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of 'Ubaidullah through other chains of transmitters: He who eats or drinks in the vessel of silver and gold, - but there is no mention in any one of them of the words pertaining to eating and gold.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="024.5128"> Book 024, Number 5128: </a> </em>
      <blockquote>
        <p> Abd al-Rahman reported on the authority of his mother's sister Umm Salama who said that Allah's Messenger (may peace be upon him) said: He who drank in vessels of gold or silver he in fact drank down in his belly the fire of Hell.</p>
      </blockquote>
