      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 29: </a> </em>
      <h1> <em> The Book of Vision (Kitab Al-Ruya) </em> </h1>
      <br>
      <p><strong> Chapter 1: </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="029.5613"> Book 029, Number 5613: </a> </em>
      <blockquote>
        <p> Abu Salama reported: I used to see dreams (and was so much perturbed) that I began to quiver and have temperature, but did not cover myself with a mantle. I met Abu Qatada and made a mention of that to him. He said: I heard Allah's Messenger (may peace be upon him) as saying: A good vision comes from Allah and a (bad) dream (hulm) from devil. So when one of you sees a bad dream (hulm) which he does not like, he should spit on his left side thrice and seek refuge with Allah from its evil; then it will not harm him.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5614"> Book 029, Number 5614: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Abu Qatada, but there is no mention of the words of Abu Salama:" I saw dreams (which perturbed me) but I did not cover myself with a mantle."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5615"> Book 029, Number 5615: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of Zuhri with the same chain of transmitters, but it does not contain the words:" I felt disturbed because of that," and there is an addition of these words in the hadith transmitted on the authority of Yunus:" Then spit thrice on the left side when you get up from sleep."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5616"> Book 029, Number 5616: </a> </em>
      <blockquote>
        <p> Abu Qatada reported Allah's Messenger (may peace be upon him) as saying: A good vision is from Allah and a bad dream (hulm) is from the satan; so if one of you sees anything (in a dream which he dislikes, he should spit on his left side thrice and seek refuge with Allah from its evil, and then it will never harm him. Abu Salama said: I used to see dreams weighing more heavily upon me than a mountain; but since I heard this hadith I don't care for it (its burden).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5617"> Book 029, Number 5617: </a> </em>
      <blockquote>
        <p> Abu Salama reported Allah's Messenger (may peace be upon him) as saying: I used to see dreams, but the hadith transmitted on the authority of Laith b. Nu'man, the words of Abu Salama at the concluding part of the hadith are not mentioned. Ibn Rumh has reported in the hadith:" He (one who sleeps) should change the side on which he had been lying before."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5618"> Book 029, Number 5618: </a> </em>
      <blockquote>
        <p> Abu Qatada reported Allah's Messenger (may peace be upon him) as saying: The good vision are from Allah and the evil dreams are from the satan. If one sees a dream which one does not like, one should spit on one's left side and seek the refuge of Allah from the satan; it will not do one any harm, and one should not disclose it to anyone and if one sees a good vision one should feel pleased but should not disclose it to anyone but whom one loves.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5619"> Book 029, Number 5619: </a> </em>
      <blockquote>
        <p> Abu Salama replied: I used to see (such horrible dreams) that I fell ill. I saw Abu Qatada who also said: I used to see dreams which made me sick until I heard Allah's Messenger (may peace be upon him) as saying: Good dreams are from Allah, so if any one of you sees which he likes he should not disclose it to one but whow he loves, but if he sees something which he does not like he should spit on his left side thrice and seek refuge with Allah from the mischief of the satan and its mischief (i. e. of the dream), and he should not relate it to anyone, then it would not harm him.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5620"> Book 029, Number 5620: </a> </em>
      <blockquote>
        <p> Jabir reported Allah's Messenger (may peace be upon him) as saying: If anyone sees a dream which he does not like, lie should spit on his left side.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5621"> Book 029, Number 5621: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: When the time draws near (when the Resurrection is near) a believer's dream can hardly be false. And the truest vision will be of one who is himself the most truthful in speech, for the vision of a Muslim is the forty-fifth part of Prophecy, and dreams are of three types: one good dream which is a sort of good tidings from Allah; the evil dream which causes pain is from the satan; and the third one is a suggestion of one's own mind; so if any one of you sees a dream which he does not like he should stand tip and offer prayer and he should not relate it to people, and he said: I would love to see fetters (in the dream), but I dislike wearing of necklace, for the fetters is (an indication of) one's steadfastness in religion. The narrator said: I do not know whether this is a part of the hadith or the words of Ibn Sirin.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5622"> Book 029, Number 5622: </a> </em>
      <blockquote>
        <p> Abu Huraira reported: I love to see fetters but I hate necklace (in a dream), for fetters signifies one's steadfastness in religion, and he also reported Allah's Messenger (may peace be upon him) as saying: The vision of a believer is forty-sixth part of Prophecy.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5623"> Book 029, Number 5623: </a> </em>
      <blockquote>
        <p> This hadith has been transmitted on the authority of Abu Huraira and the words are:" When the time draws near," the rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5624"> Book 029, Number 5624: </a> </em>
      <blockquote>
        <p> Muhammad b. Sirin reported from Abu Huraira a hadith from Allah's Apostle (may peace be upon him) and he mentioned in his hadith his words:" I dislike shackles," up to the end of his statement, but he made no mention of this:" A vision is a forty-sixth part of Prophecy."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5625"> Book 029, Number 5625: </a> </em>
      <blockquote>
        <p> Ubida b. as-Samit reported Allah's Messenger (may peace be upon him) as saying: The vision of a believer is the forty-sixth part of Prophecy.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5626"> Book 029, Number 5626: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Anas b. Malik through another chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5627"> Book 029, Number 5627: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Verily the vision of a believer is one of the forty-sixth part of Prophecy.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5628"> Book 029, Number 5628: </a> </em>
      <blockquote>
        <p> Allah's Messenger (may peace; be upon him) said: The vision of a Muslim which he sees or which is shown to him, and in the hadith transmitted on the authority of Ibn Alushir (the words are):" The pious dream is the forty-sixth part of Prophecy."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5629"> Book 029, Number 5629: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: The vision Qf a pious man is the forty-sixth part of Prophecy.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5630"> Book 029, Number 5630: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Yahyi b. Abu Kathir with the same chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5631"> Book 029, Number 5631: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Abu Huiaira through another chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5632"> Book 029, Number 5632: </a> </em>
      <blockquote>
        <p> Ibn 'Umar reported Allah's Messenger (may peace be upon him) as saying: The pious dreams are the seventieth part of Prophecy.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5633"> Book 029, Number 5633: </a> </em>
      <blockquote>
        <p> This hadith has been reported on tile authority of 'Ubaidullah with the same chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5634"> Book 029, Number 5634: </a> </em>
      <blockquote>
        <p> A hadith like this has been reported on the authority of Nafi' with the same chain of transmitters (and the words are):" I think Ibn 'Uniar said: The seventieth part from Prophecy."</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: THE SAYING OF THE HOLY PROPHET (MAY PEACE BE UPON HIM): HE WHO SEES ME IN A DREAM IN FACT SEES ME </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="029.5635"> Book 029, Number 5635: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: He who saw me in a dream in fact saw me, for the satan does not appear in my form.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5636"> Book 029, Number 5636: </a> </em>
      <blockquote>
        <p> Abu Huraira reported: I heard Allah's Messenger (may peace be upon him) as saying: He who saw me in a dream would soon see me in the state of wakefulness, or as if he saw me in a state of wakefulness, for the satan does not appear in my form.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5637"> Book 029, Number 5637: </a> </em>
      <blockquote>
        <p> Abu Qatada reported Allah's Messenger (may peace be upon him) as saying: He who saw me in dream in fact saw the truth (what is true).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5638"> Book 029, Number 5638: </a> </em>
      <blockquote>
        <p> Jabir reported Allah's Messenger (may peace be upon him) as saying: He who saw me in sleep in fact saw me, for it is not possible for the satan to appear in my form; and he also said: When any one of you sees a hulm he should not inform anyone, for it is a sort of vain sport of devil in the state of sleep.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: DO NOT INFORM THE VAIN SPORT OF DEVIL IN A DREAM </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="029.5639"> Book 029, Number 5639: </a> </em>
      <blockquote>
        <p> Jabir b. 'Abdullah reported Allah's Messenger (may peace be upon him) as saying: He who saw me in a dream in fact saw me, for the satan cannot assume my form.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5640"> Book 029, Number 5640: </a> </em>
      <blockquote>
        <p> Jabir reported Allah's Messenger (may peace be upon him) as saying: There came to him (the Holy Prophet) a desert Arab and said: I saw in a dream that I had been beheaded and I had been following it (the severed head). Allah's Apostle (may peace be upon him) reprimanded him saying: Do not inform about the vain sporting of devil with you during the night.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5641"> Book 029, Number 5641: </a> </em>
      <blockquote>
        <p> Jabir reported that there came to Allah's Apostle (may peace be upon him) a desert Arab and said: Allah's Messenger, I saw in the state of steep as if my head had been cut off and I had been moving on haltingly after it. Thereupon Allah's Messenger (may peace be upon him) said to that desert Arab: Do not narrate to the people the vain sporting of satan with you in your sleep and (the narrator) also said: I heard Allah's Messenger (may peace be upon him) in his subsequent address: None amongst you should narrate the vain sporting of devil with him in the dream.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5642"> Book 029, Number 5642: </a> </em>
      <blockquote>
        <p> Jabir reported that a'person came to Allah's Apostle (may peace be upon him) and said: Allah's Messenger, I have seen in the state of sleep as if my head had been cut off. Thereupon Allah's Apostle (may peace be upon him) laughed and said: When the satan plays with any one of you in the state of steep, do not inention it to the people; and in the hadith transmitted by Abu Bakr (the words are):" If one of you is played with, and he did not make any mention of the word:" Satan."</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 4: PERTAINING TO THE INTERPRETATION OF DREAMS </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="029.5643"> Book 029, Number 5643: </a> </em>
      <blockquote>
        <p> It is reported eithfr on the authority of Ibn Abbas or on the authority of Abu Huraira that a person came to Allah's Messenger (may peace be upon him) and said: Allah's Messenger, I saw while I was sleeping during the night (this vision) that there was a canopy from which butter and honey were trickling and I also saw people collecting them in the palms of their hands, some more, some less, and I also saw a rope connecting the earth with the sky and I saw you catching hold of it and rising towards the heaven; then another person after you catching hold of it and rising towards (Heaven) ; then another person catching hold of it, but it was broken while it was rejoined for him and he also climbed up. Abu Bakr said: Allah's Messenger, may my father be sacrificed for you, by Allah, allow me to interpret it. Allah's Messenger (may peace be upon him) sail: Well, give its interpretation. Thereupon Abu Bakr said: The canopy signifies the canopy of Islam and that what it trickles out of it in the form of butter and honey is the Holy Qur'an and its sweetness and softness and what the people get hold of it in their palms implies major portion of the Qur'an or the small portion; and so far as the rope joining the sky with the earth is concerned, it is the Truth by which you stood (in the worldly life) and by which Allah would raise you (to Heaven). Then the person after you would take hold of it and he would also climb up with the help of it. Then another person would take hold of it and climb up with the help of it. Then another person would take hold of it and it would be broken; then it would be rejoined for him and he would climb up with the help of it. Allah's Messenger, may my father be taken as a ransom for you, tell me whether I have interpreted it currectly or I have made an error. Allah's Messenger (may peace be upon him) said: You have interpreted a part of it correctly and you have erred in interpreting a part of it. Thereupon he said: Allah's Messenger, by Allah, tell me that part where I have committed an error. Thereupon he said: Don't take oath.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5644"> Book 029, Number 5644: </a> </em>
      <blockquote>
        <p> Ibn 'Abbas reported that there came to Allah's Apostle (may peace be upon him) a person as he was returning from Uhud and lie 'said: Allah's Messenger, I saw in sleep during the night a canopy trickling butter and honey; the rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5645"> Book 029, Number 5645: </a> </em>
      <blockquote>
        <p> It is reported either on the authority of Ibn 'Abbas or on that of Abu Huraira that a person came to Allah's Messenger (may peace be upon him) and said: Verily I saw during the night a canopy; the rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5646"> Book 029, Number 5646: </a> </em>
      <blockquote>
        <p> Ibn 'Abbas reported that Allah's Messenger (may peace be upon him) used to say to his Companions: He who amongst you sees a vision should narrate it and I would interpret it for him, and a person came and said: Allah's Messenger, I saw a canopy. The rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 5: THE VISION OF THE HOLY PROPHET (MAY PEACE BE UPON HIM) </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="029.5647"> Book 029, Number 5647: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported Allah's Messenger (may peace be upon him) as saying: I saw during the night that which a person sees during the sleep as if we are in the house of 'Uqba b. Rafi' that there was brought to us the fresh dates of Ibn Tab. I interpreted it as the sublimity for us in the world and good ending in the Hereafter and that our religion is good.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5648"> Book 029, Number 5648: </a> </em>
      <blockquote>
        <p> Abdullah b. 'Umar reported Allah's Messenger (may peace be upon him) as saying: I saw in a dream that I was using miswak and the two persons contended to get it from me, the one being older than the other one. I gave the miswak to the younger one. It was said to me to give that to the older one and I gave it to the older one.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="029.5649"> Book 029, Number 5649: </a> </em>
      <blockquote>
