      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 33: </a> </em>
      <h1> <em> The Book of Destiny (Kitab-ul-Qadr) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> Whether the fate of man is predestined or he himself is the architect of it, is a question which has been very often discussed by scholars of all times. This problem is significantly important as no sensible man. not even the man in the street, can afford to ignore it. Faith in Taqdir (Destiny) has a very deep impact upon our lives and we always find our lives oscillating between determinism and freewill. As a man looks around himself and looks to his own self and within himself, he finds that there are hundred and one things in shaping and reshaping of which he has no hand, e. g. in determining the climate of the land in which he is born, in canalising the courses of rivers which flow therein and in determining the nature of the soil thereof. He finds himself absolutely powerless. As he looks to himself he finds that there are so many things In him which are beyond his control, viz. the measure of intellect he has been endowed with, the shape and form of his physical structure with which he has been sent to this world, and the inclinations and so many other qualities of head and heart which are embedded in his very nature. In all these aspects of life he finds himself helpless before the Great and Mighty Power that created him. </p>
      <p> On the other hand, there are so many things in which man finds himself quite empowered. As he looks to the marvellous achievements of man despite all odds, he finds it difficult to believe that he is a mere puppet in the mighty hand of Nature. This problem of predestination and freewill, in which man finds his life hanging, has been adequately solved by the Qur'an and the Sunnah. We give below a brief summary of their elucidations. </p>
      <p> The first principle which Islam lays down in regard to Taqdir is that man is neither completely the master of his fate nor is he bound to the blind law of predestination. So far as the sovereignty of Allah's Will is concerned, it is all-pervading and nothing falls outside its orbit. Not even a leaf, therefore, stirs without His Will. </p>
      <p> It is His Will that prevails everywhere. To God belongs the sovereignty of Heavens and the Earth. He created what He pleaseth, giving to whom He pleaseth females and to whom He pleaseth males or conjoining them males and females, and He maketh whom He pleaseth barren, verify He hath knowledge and power (xlii. 48). </p>
      <p> Men are, therefore, completely subordinate to the overruling power of God, they cannot do anything unless God wills so. </p>
      <p> " Whom God guideth he is the rightly-guided. Whom he sendeth stray, thou wilt not find a patron to set him right (xviii. 16). </p>
      <p> His mighty grasp is, therefore, over everything. The Almighty Lord, Who has created everything and has determined its nature and course, has in His infinite wisdom and mercy conferred upon man a limited autonomy according to which a man is free to do or not to do a certain thing. It is because of this autonomy enjoyed by man that he is hold accountable for his deeds. The concept of human responsibility and that of his answerability for his deeds and misdeeds becomes meaningless if he is supposed to be deprived of this autonomy. There are, a large number of verses in the Holy Qur'an which make a pointed reference to the autonomy conferred upon man. </p>
      <p> Man shall have nothing but that what he strives for (liii. 39). </p>
      <p> Allah does not change the condition of a people until they change it themselves (viii. 53; xiii. 11). </p>
      <p> Those who strive in His path, are guided in the right path, while those who persist in denial and sinful livifig have their hearts sealed against faith (ii. 7, 26; iv. 155; v;. i. 102). </p>
      <p> Allah does not compel belief and leaves the people free to believe or disbelieve (vi. 35, 150; xvi-. 9). </p>
      <p> Whoever has done an atorn's weight of good shall meet with its reward and whoever has done an atom's weight of evil shall meet with its consequences (xcix. 7-8). </p>
      <p> It should be borne in mind in this connection that the word Taqdir used in the Qur'an does not always signify something predestined. It at times implies a measure or the latent potentialities or possibilities with which Allali created man and all things of Nature. For example: He created everything for its Destiny (or its Measure) (xxv. 2). In Sura 54, verse 9 (the words are): We created everything according to a Measure or Destiny. In both these verses Destiny impiies the inward reach of things, their latent potentialities or possibilities. </p>
      <p> The idea of Destiny as we find in the hadith that God wrote down the decrees regarding the created world fifty thousand years before He created the Heavens and the Earth does not in any way mean that God created a block Universe, finished off and complete, bound to the iron formulae of Nature. Here the idea behind Taqdir is that the creation of this universe is not accidental but something preplanned and pre-conceived and it was shaped according to the Grand Design of the Greatest Designer. There is no element of chance in the creation of this Universe. Everything is well-set and well-planned. </p>
      <p> The idea that Allah has a foreknowledge of everything that He created and the events unfold themselves exactly according to it, does not imply that human beings have been completely deprived of the freedom of action. The foreknowledge of God is an acknowledged fact, but it should not be interpreter in the sense of predestination, for if we do so we shall have to conceive of eternity as a storehouse of ready-made events, from which they drop one by one like particles of sand in a glass hour. If we take the foreknowledge to be a reflecting mirror we shall have to deprive the Creator and the Controller of the Universe of His Creative activity. </p>
      <p> Dr Muhammad Iqbal has shed a good deal of light over this problem. He says:" Divine knowledge must be conceived as a living creative activity to which the objects that appear to exist in their own right are organically related. By conceiving God's knowledge as a kind of reflecting mirror, we no doubt save His fore-knowledge of future events, but it is obvious that we do so at the expense of His freedom. The future certainly pre-exists in the organic whole of God's creative life, but it pre-exists as an open possibility, not as a fixed order of events with definite outlines." </p>
      <p> We should bear in mind that the idea of put, present and future is something relative and is conceived by the inite wind of man. It is. however, a great now in the eye of the All-Seeing God. The whole expanse of eternity lies before Him in the shape of now. Knowledge is, therefore, an act of creative activity and not the mere reflection of it. When He decrees a thing it happens and He knows it before it happens. God in Islam is not, therefore, a prisoner of necenity. He is a free Creator. </p>
      <p> The concept of predestination in Islam, therefore, does not in any way mean helpless abandonment of oneself to otherwise unwelcome fate. It means rather co-operation with God, studying His will and bringing oneself into unison with His Planning Will. Destiny as conceived by Islam is, thus, by no stretch of imagination, fatal to the freedom of conduct and unfoldment of one's inherent possibilities; it is a source of inspiration and encouragement and opens up vast fields of human activity. It is not a message of despondency and despair, but a source of solace, comfort and inspiration and a powerful means of evoking a sense of piety and humility and self-surrender to the Will of God. It does not inculcate in mind frustration and pessimism, making his life dark and dreary, devoid of hope and promise for the future, but it teaches him to put his heart and soul in the sublime work as assigned to him by his Master. </p>
