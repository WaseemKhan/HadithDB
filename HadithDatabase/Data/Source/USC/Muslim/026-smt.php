      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="026.5396"> Book 026, Number 5396: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Hisham with the same chain of transmitters, and the words are: She (Sauda) was a woman who looked to be significant amongst the people (so far as the bulk of her) body was concerned. The rest of the hadith is the same.</p>
