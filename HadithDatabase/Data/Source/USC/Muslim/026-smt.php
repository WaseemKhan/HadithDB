      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 26: </a> </em>
      <h1> <em> The Book on Salutations and Greetings (Kitab As-Salam) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> Just as the face is the index to the mind, in the same way the words of greetings which are exchanged amongst people indicate the spiritual role of a society. The gestures, and the expressions of salutations are, therefore, the indicators of the ideals for which a particular society stands. The common practice with the pre-Islamic Arabs and thi modern West is to greet one another with Good Morning and Good Evening accompanied by a gesture of the hand or the nodding of head. These words have no spiritual and moral significance, but just a wish that one should find one's mornings and evenings happy. The words of greeting which Islam has exhorted its followers to use at the time of meeting are as-Salam-u-'Alaikum which have a deep spiritual and moral significance behind them Since the predominant feature of Islamic faith and practice is peace within and peace without, it has, therefore, been made a watchword which should be repeated on every occasion when two Muslims meet so that the utmost importance of peace should be constantly inculcated in the mind of every Muslim, and he should have full consciousness of the great reality that he lives in this world as a messenger of peace for mankind. peace amongst individuals and nations. -The second important feature of words of greeting in Islam is that they convey the message of peace not only to the person who is greeted but to the whole of the Muslim society as it is the plural pronoun that is used for the blessing of peace. (The pronoun'Alaikum has a evry wide range and all the Muslims come within its orbit.) </p>
      <p> The two words as-Salam-u-'Alaikum may be called the irreducible minimum which are essential for greeting one another. The Muslims have been commanded to add to this expression some other words with a view to excelling one another in showering blessing upon the Muslims. The words as-Salam-u-'Alaikum are generally suffixed with these words Rahmat Allah wa Barakaatuh (mercy of Allah and His bleasings). These three words:" peace..., mercy" and" blessings of Allah" in fact form the sum and substance of Islam and the Muslims are asked to shower upon one another these benedictions. so that they may be able to live in this world and the Hereafter in perfect peace and tranquillity and blessings and mercy of Allah around him. </p>
      <p> According to Imam Nawawl, greeting with the words of as-Salam-u-'Alaikum is a Sunnah, and responding to it is Wajib (obligatory), but this essentiality is of the nature of Kifaya, meaning thereby that if a few persons out of the whole assembly or group respond to the greeting it will absolve all of the responsibility of response but if no response comes forth from any quarter all are held responsible for it. </p>
      <br>
      <p><strong> Chapter 1: THE RIDER SHOULD FIRST GREET THE PEDESTRIAN AND THE SMALL NUMBER SHOULD GREET THE LARGE NUMBER OF PERSONS </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="026.5374"> Book 026, Number 5374: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: The rider should first greet the pedestrian, and the pedestrian the one who is seated and a small group should greet a larger group (with as-Salam-u-'Alaikum).</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: THE DUTY THAT ONE OWES ON THE ROADSIDE IS TO RESPOND THE GREETING OF AS-SALAAM-U-'ALAIKUM </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="026.5375"> Book 026, Number 5375: </a> </em>
      <blockquote>
        <p> Abu Talha reported: While We were sitting in front of the houses and talking amongst ourselves, Allah's Messenger (may peace be upon him) happened to come there. He stood by us and said: What about you apd your meetings on the paths? Avoid these meetings on the paths. We said: We were sitting here without (any intention of doing harm to the passers-by) ; we are sitting to discuss matters and to hold conversation amongst ourselves. Thereupon he said: If there is no help (for you but to sit on these paths), then give the paths their rights and these are lowering of the gaze, exchanging of greetings and good conversation.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="026.5376"> Book 026, Number 5376: </a> </em>
      <blockquote>
        <p> Abu Sa'id Khudri reported Allah's Apostle (may peace be upon him) as saying: Avoid sitting on the paths. They (the Companions) said: Allah's Messenger, we cannot help but holding our meetings (in these paths) and discuss matters (there). Thereupon Allah's Messenger (may peace be upon him) said: If you insist on holding meetings, then give the path its due right. They said: What are its due rights? Upon this he said: Lowering the gaze, refraining from doing harm, exchanging of greetings. commanding of good and forbidding from evil.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="026.5377"> Book 026, Number 5377: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Zaid b. Aslam with the same chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: THE RIGHT OF A MUSLIM UPON ANOTHER MUSLIM IS THAT HIS GREETING SHOULD BE RESPONDED </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="026.5378"> Book 026, Number 5378: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Five are the rights of a Muslim over his brother: responding to salutation, saying Yarhamuk Allah when anybody sneezes and says al-Hamdulillah, visiting the sick. following the bier. ' Abd al-Razzaq said that this hadith has been transmitted as mursal hadith from Zuhri and he then substantiated it on the authority of Ibn Musayyib.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="026.5379"> Book 026, Number 5379: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Six are the rights of a Muslim over another Muslim. It was said to him: Allah's Messenger, what are these? Thereupon he said: When you meet him, offer him greetings;when he invites you to a feast accept it. when he seeks your council give him, and when he sneezes and says:" All praise is due to Allah," you say Yarhamuk Allah (may Allah show mercy to you) ; and when he fails ill visit him; and when he dies follow his bier.</p>
      </blockquote>
