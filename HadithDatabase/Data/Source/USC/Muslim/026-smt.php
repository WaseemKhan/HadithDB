      <!-- BH -->
      <hr>
      <em> <a name="026.5393"> Book 026, Number 5393: </a> </em>
      <blockquote>
        <p> Ibn Mas'ud reported: The Messenger of Allah (may peace be upon him) said to me: The sign that you have been permitted to come in is that the curtain is raised or that you hear me speaking quietly until I forbid you.</p>
