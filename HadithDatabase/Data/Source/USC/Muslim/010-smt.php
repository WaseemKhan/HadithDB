      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 10: </a> </em>
      <h1> <em> The Book of Transactions (Kitab Al-Buyu`) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> Honesty in commercial dealings is more strictly enjoined by Islam than by any other religion. It is because Islam is a religion which regulates and directs life in all its departments. It is not to be regarded, like the modern man's religion. as a personal, private affair, which has nothing to do with his economic and political life. It is not merely a body of dogmas or a bundle of rites and rituals; it is a practical code which governs life in all its spheres. Its laws are as effectively operative in our commerce and politics as in our domestic life and social relations. Islam censures political chicanery and economic exploitation as strongly as social excesses and individual dishonesty. Indeed, a true Islamic society is based upon honesty, justice and fraternity, and is absolutely intolerant of dishonesty in all its various forms. That is the reason why perfect honesty in business and truthfulness in trade are much emphasised by the Holy Prophet (may peace be upon him). It will not be an exaggeration to say that absolute honesty in business and commerce is really an Islamic concept. The Hindus and Jews were (and still, are) worshippers of the Mammon. Both of them have been mercenary nations, notorious for their greed. The pre-Islamic Christians. too, did not possess any high standard of business morality. It was Prophet Muhammad (may peace be upon him) who, on the one hand, urged his followers to adopt trade as their profession, and, on the other band, exhorted them to observe truthfulness and honesty in their business transactions. </p>
      <p> Islam lays the greatest emphasis on Qat Haldl (food earned through lawful means). The pious among us believe that just as nasty food spolis our physical health, similarly. food earned through unlawful means spoils our spiritual and moral health. A man who liver on income derived through illicit means and fraudulent practices cannot be morally advanced and spiritually elevated. If we try to comprehend the exact, implications of the term Haram (unlawful) we can form an idea of the high standard of morality on which Islam wants us to conduct our business. And, if business is conducted strictly in accordance with the Islamic principles of commerce, there can be absolutely no scope for any kind of commercial dishonesty varying from the simplest and most glaring type of business fraud to the most cunning and subtle type of profiteering which is often masked under a semblance of honesty. </p>
      <p> Islam is most vehement in its condemnation of commercial dishonesty. It denounced, in the strongest possible terms, all sorts of deceitful dealings and illegal profits. It has disallowed all transactions not based upon justice and fairplay The Holy Prophet (may peace be upon him), while reprimanding the dishonest dealer, said:" Laisa minna man gashshdna" (Whosoever deceives us is not one of us). </p>
      <p> According to Imam Ghazali, a Muslim who makes up his mind to adopt trade as a profession or to set up his own business should first acquire a thorough understanding of the rules of business transactions codified in the Islamic Shari'ah. Without such understanding he will go astray and fail into serious lapses making his earning unlawful. No people in the world have ever attached so much importance to lawful trading as did the early Muslims, nor has any other nation evinced such a dread of unlawful trading as they did. That is why al-Ghazali said stress on a clear understanding of the rules and laws governing business transactions as a necessary prerequisite to adopting trade or business as a profession. </p>
      <p> The Holy Qur'an has stressed the importance of fairness in business:" And, O my people, give full measure and weight justly, and defraud not men of their things, and act not corruptly in the land making mischief. What remains with Allah is better for you, if you are believers" (xi. 85-86). </p>
      <p> In these words addressed by Hadrat Shu'aib to his people, the Holy Qur'an enunciates the fundamental principles of commerce as follows </p>
      <p> To give just measure and weight. </p>
      <p> Not to withhold from the people the things that are their due. </p>
      <p> Not to commit evil on the earth with the intent of doing mischief. </p>
      <p> To be contented with the profit that is left with us by God after we have paid other people their due. </p>
      <p> We are told in these verses that commerce can flourish under conditions of peace and security. The people are, therefore, warned not to disturb the peace of the land so that there is a free and untrammelled trade between different parts of the world. In commercial relations we are expected to be absolutely just and honest, liberally giving other people their due. We are not to be guilty of selfish greed and not to indulge in profiteering; and we are told that the lawful profit which has God's blessings is the one that we are able to make through perfectly honest dealings with others. The injunctions contained in these Qur'anic verses and found elsewhere in the Holy Book close the door of all dishonest and unjust transactions. We should not forget that justice is a master virtue. If we give others just measure and just weight that tantamounts to saying that we should be fair and just in our dealings. </p>
      <p> A careful study of" Kitab al-Buyu`" (the book pertaining to business transactions) will reveal the fact that the Holy Prophet (may peace be upon him) based business dealings strictly on truth and justice. He has strongly disapproved all transactions which involve any kind of injustice or hardship to the buyer or the seller. He wanted that both, the buyer and the seller, should be truly sympathetic and considerate towards each other. One should not take undue advantage of the simplicity or ignorance of the other. The seller should not think that he has unrestricted liberty to extort as much as possible from the buyer. He has to be just; he should take his own due and give the buyer what is his. </p>
      <p> Islam, which condemns every kind of injustice and exploitation in human relations, wants its followers to conduct business in a sublime spirit of justice tempered with human kindness. The conduct of the seller in a transaction should be characterised not only by Insaf (justice), but also by Ihsan (magnanimity)." God will forgive the sins of a Muslim who absolves a fellow-Muslim from a sale-contract not liked by the latter," says the Holy Prophet (may peace be upon him). </p>
      <p> All transactions should be based on the fundamental principle of" Ta'auanu ala birri wa't-taqwa" (mutual co-operation for the cause of goodness or piety). A transaction not based upon this sound principle is not lawful. Unlawful transactions are motivated by lust for money and an ignoble desire to build up prestige. Islam strikes at the root of the passion for money and suggests a different yardstick to measure the prestige of a person. The Holy Qur'an, on the one hand, condemns hoarding and the excessive love for wealth, and, on the other, declares virtue and piety to be the criterion for determining a person's worth." Inna akramakum `ind-Allahi atqakum" (The noblest in the eyes of God is the most pious among you). Thus does Islam minimise in every possible way the temptation to illegal trade and traffic. Let us now take note of the forms of business transactions which have been prohibited in Islam. The Holy Prophet (may peace be upon him) has not only disapproved of certain forms of business transactions, but has also laid down some basic conditions that should be fulfilled in every transaction if it is to be lawful. </p>
      <p> The following are some of these basic conditions: </p>
      <p> Things sold and money offered as their price to be lawfully acquired. The things sold and the money to be offered as their price should both be lawfully acquired and clearly specified. This condition demands that the goods sold should have been lawfully obtained. One has no business to sell goods which one has stolen or which one has acquired in a fraudulent manner. nor should one purchase anything with the money which one has accepted as illegal gratification or has aceuired in some other deceitful way. This condition holds the buyer and the seller responsible for lawful possession of the goods on the partof one and of the money on the part of other. </p>
      <p> Goods not to be sold before obtaining their possession. The Holy Prophet (may peace be upon him) has warned the Muslims against indulging in forward transactions which means selling goods before obtaining their possession." Whoever buys cereals shall not tell them until he has obtained their possession," says the Holy Prophet (may peace be upon him). According to Ibn 'Abbas, what applies to cereals also applies to other categories of goods. On another occasion the Holy Prophet (may peace be upon him) has said:" Bargain not about that which is not with you." </p>
      <p> Goods to be bought in the open market. Goods and commodities for sale should go into the open market, and the seller or his agents must be aware of the state of the market before proposals are made for the purchase by the buyers. The seller should not be taken unawares lest the buyers should take undue advantage of his ignorance of the conditions and prices prevailing in the market. </p>
      <p> No trade and traffic in things, the use of which is prohibited by Islam. A Muslim can trade in those goods and commodities only the use of which has been declared to be Halal (lawful). There can be no trade and traffic in things the use of which is proliibited by Islam. For example, there can be no trade in wine, swine, dead bodies of animals and idols. A devout Muslim merchant would not even traffic in thin and transparent stuff for ladies because the use of such stuff by ladies is unlawful. One cannot sell the carcass of an animal. He can, however, flay its skin which can be used for making shoes and which can therefore, be sold, but not the flesh of the dead animal. What is true of the usable skin of animals is also true of the tusks of an elephant. </p>
      <p> Prohibited forms of Business </p>
      <p> Monopoly business. As monopoly means concentration of supply in one hand, it leads to exploitation of the consumers and the workers, it has, therefore, been declared unlawful by the Holy Prophet (may peace be upon him). Gigantic trusts. cartels and monopolies should not exist in the Islamic society. The monopoly-dominated economic order betrays lack of harmony between private and social good and is, thus, a negation of the principle of maximum social advantage which the Islamic society sets out to achieve. </p>
      <p> Speculative business basd on selfish interest. Speculation means buying something cheap in bulk at a time and selling it dear at another and, thus, controlling the whole market to achieve personal gains. A close observation will reveal that speculators are primarily interested in private gains regardless of the larger interest of the society. These speculators try to create artificial scarcity of goods and commodities and thereby create an inflationary pressure on the economy. As the poor masses have to pay for this. Islam has condemned such speculative business. </p>
      <p> Interest transactions. All transactions involving interest are forbidden in Islam. Some people find it hard to submit to the injunction prohibiting interest, because they think interest and profit earned in trade are similar. Capital invested in trade brings an excess called profit; invested in banking it brings interest. Why should one excess be considered lawful and the other unlawful? They fail to take note of the basic difference between the two. Trade involves risk of loss. Also in its case, it is not only the capital invested that brings profit which is equally the result of initiative, enterprise and efficiency of the entrepreneur. Hence its rate cannot be predetermined and fixed. Moreover, trade is productive. A person reaps a benefit after undergoing labour and hardship. It creates conditions of full employment and economic growth. It will also be noted that trade acts as one of the dominant factors in the process of building up civilisation through co-operation and mutual exchange of ideas. The spread of Islam and Islamic civilisation In the Far East has been mostly due to the efforts of Muslim traders. Interest has no redeeming feature at all. The fixed rate of profit which a person gets from a financial investment without any risk of loss and without augmenting it with human labour creates in man the undesirable weakness of miserliness and Shylockian selfishness and lack of sympathy. In the economic sphere it initiates and aggravates crisis. </p>
      <p> Rightly, therefore, has Islam strictly prohibited all transactions based on it or involving it in some form or other. </p>
      <p> Advancing money on interest, keeping deposits in a bank for the sake of earning interest, or getting concessions in rates of goods or commodities against advance payments of price, mortgaging and utilising an income-yielding property against a certain sum, to be returned in full when the property is redeemed and investing money in a trade against a predetermined and fixed rate of profit-are all unlawfnl business transactions because they involve Riba (interest) in some form or the other. </p>
      <p> Transactions similar (in nature) to gambling. The Arabic equivalent to gambling is Maisir which literarily means" getting something too easily"," getting a profit without working for it". The literal meaning of the term explains the principle on account of which gambling is prohibited in Islam. Any monetary gain which cornes too easily, so much so that one does not have to work for it, is unlawful. </p>
      <p> The most familiar form of gambling amang the Arabs in the days of the Holy Prophet (may peace be upon him) was gambling by casting of lots by means of arrows drawn from a bag. Some were blank and those who drew them got nothing. Others indicated prizes-big or small ones. Whether one got anything or nothing depended on pure luck. unless there was fraud on the part of someone concerned. The principle on which objection to gambling is based is that you gain what you have not earned, or lose on a mere chance. Dice, lottery, prize bonds and betting on horse races are to be held within the definition of gambling. </p>
      <p> Munabadha and Mulamasa. Islam recognises barter trade subject to the injunctions of the Qur'an and the Sunnah. In fact, Islam has closed all doors of dishonesty and deceit in business dealings. It has prohibited all forms of transactions which admit of fraud in the least degree. It has impressed on the traders that defective and worthless goods should not be given in exchange for good ones, and if there is a defect in the goods sold it must be pointed out and made manifest to the purchaser. The Messenger of Allah (may peace be upon him) said:" The buyer and the seller have the option of cancelling the contract as long as they have not separated; then. if they both speak the truth and make manifest, their transaction shall be blessed, and it they conceal and tell lies, the blessing of their transaction shall be obliterated". </p>
      <p> Besides issuing the instructions which govern all forms of trade, particularly barter trade, Islam has banned two forms of sale contract that were prevalent before Islam. These were Munabadha and Mulamasa. In neither of these was the purchaser offered an opportunity to examine the thing purchased. Munabadha means that the seller should throw the cloth to the buyer before he has carefully examined it. The very act of throwing the cloth will mean that the bargain has been struck. Mulamasa means touching the cloth without examining it, ie. the buyer was just supposed to touch the cloth to strike the bargain. Both these forms of transaction were prohibited because in either case the purchaser got no opportunity to examine the things sold to him, and the bargain was likely to prove unduly disadvantageous to one side. </p>
      <p> In fact, Islam demanlds that goods and commodities for we should go to the open market and the seller or his agents must be aware of the state of the market before proposals are made for the purchase of goods or communities in bulk. He should not be taken unawares lest advantage be taken of his ignorance of the state of the market, and the prevailing prices. All this is ver clearly laid down by the Prophet (may peace he upon him). </p>
      <p> As mentioned above, Islam tries to be fair to both parties to a transaction. Any step on the part of one, that is advantageous to him and disadvantageous to the other, is not permissible. The seller is expected to make the defects (if any) in the goods manifest to the buyer, nor is the buyer expected to take undue advantage of the ignorance of the seller. </p>
      <p> Mozabana. It is the exchange of fresh fruits for dry ones in a way that the quantity of the dry fruit is actually measured and fixed, but the quantity of the fresh fruit to be given in exchange is guessed while it is still on the trees (Mishkat, 2710). The Holy Prophet (may peace be upon him) has forbidden this exchange because the quantity of the fruit on the trees cannot be definitely Determined and the transaction is just a leap into the dark. </p>
      <p> Mu'awama. It consists in selling the fruit on the trees for a period of one, two or three years even before it has made its appearance. It is prohibited because like Muzabana it is also a leap into the dark. Such transactions may result in bitterness and frustration. </p>
      <p> Bai' al-Gharar. It is to sell a thing which one doesn't have in one's possession, nor expects to bring it under one's control, e g. fish in the river, or birds in the air. Possession is one of the basic conditions of a sale. One cannot sell a thing which is not in one's possession. </p>
      <p> Bai' al-'Uryan. It is getting a thing against a nominal advance on the condition that if the bargain is struck, the advance will be adjusted and if the bargain is cancelled, the seller will riot return the advance. The advance being nominal, the buyer has practically no liability. He will abide by the contract if he finds it advantageous to him and will withdraw himself from it otherwise. </p>
      <p> Bai' al-Mudtar. It is to buy a thing forcibly or to purchase a thing when its owner is compelled under stress of want to dispose it of. Instead of purchasing the thing, and taking undue advantage of the seller's helplessness, one should help him. Bai' alal-Bai' (sale over and above the sale of another). When one person has sold goods to another, a third Person should not upset the bargain trying to sell his own goods to the latter, offering them at lower rates or pointing out the defect in the goods already sold to him by the former." A Muslim should not purchase in opposition to his brother, nor should he send a marriage proposal over and above the proposal of another." </p>
      <p> Bai' al-Hast (i. e. sale by means of pebbles). The purchaser will tell the seller that when he will throw a pebble on his goods, the sale contract will be confirmed or the seller tell the purchaser that on whatever thing a pebble thrown by him falls will be sold to him. Sale contract is a serious matter and it should not be accomplished by such hit-and-miss methods like throwing the pebbles on the goods. A sale completed in this way may lead to injustice and hardship to one side and is consequently prohibited. </p>
      <p> Sale of unripe fruit and unripe corn. The Holy Prophet (may peace be upon him), according to Hadrat Anas (Allah be pleased with him), has prohibited the sale of grapes before they become dark and that of the corn before it ripens. Similarly, he has forbidden the sale of raw dates. The fruit of the date palms should not be sold until it becomes red or yellow. </p>
      <p> Here is a brief account of the sale transactions prohibited by Islam. If one ponders over these forms of transaction described above and described in greater detail in" Kitab al-Buyu," one can arrive at the following conclusions: </p>
      <p> Islam insists upon absolute justice and fairplay in business dealings. </p>
      <p> According to Islam, a person who sacrifices his faith, and loses the good pleasure of his Lord to make a monetary gain has not made a good bargain. A Muslim will not go in for such a bad bargain. A Muslim merchant is not a worshipper of the Mammon with an inordinate love for money. He prizes faith, piety and righteousness above all. </p>
      <p> Islam does not believe in the view that all is fair in business and that every kind of cleverness and deceit is justifiable in business transactions. Islam regards business or commerce as an economic activity to be carried on in a spirit of humanity. tarianism and justice. It does not approve of the cut-throat competition. Indeed, the very concept is un-Islamic. </p>
      <p> Islam expects the buyer and the seller to look upon each other as Muslim brethren or fellow human beings, each trying to go all his way to help and serve the other. It the seller happens to overcharge the buyer, he, instead of feeling proud of his cleverness in doing so, should somehow compensate him for the excessive payment received. </p>
      <p> All bargains that are clenched without giving the purchaser a fair chance of examining the things are prohibited because this amounts to denying him a right that was his due. </p>
      <p> Forcible transactions or transactions in which the buyer takes undue. advantage of the helplessness or misery of the seller are also discouraged. </p>
      <p> Islam has prohibited traffic in wine, swine, dead bodies of animals and other goods the use of which has been declared to be Haram (unlawful). </p>
      <p> It has also forbidden trading in things that have a debasing or vitiating influence on the Muslim society. </p>
