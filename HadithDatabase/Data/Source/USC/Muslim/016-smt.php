      <p><strong> Chapter 2: PERTAINING TO THE COMBATANTS AND APOSTATES </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="016.4130"> Book 016, Number 4130: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported that some people belonging (to the tribe) of 'Uraina came to Allah's Messenger (may peace be upon him) at Medina, but they found its climate uncogenial. So Allah's Messenger (may peace be upon him) said to them: If you so like, you may go to the camels of Sadaqa and drink their milk and urine. They did so and were all right. They then fell upon the shepherds and killed them and turned apostates from Islam and drove off the camels of the Prophet (may peace be upon him). This news reached Allah's Apostle (may peace be upon him) and he sent (people) on their track and they were (brought) and handed over to him. He (the Holy Prophet) got their hands cut off, and their feet, and put out their eyes, and threw them on the stony ground until they died.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="016.4131"> Book 016, Number 4131: </a> </em>
      <blockquote>
        <p> Anas reported: Eight men of the tribe of 'Ukl came to Allah's Messenger (may peace be upon him) and swore allegiance to him on Islam, but found the climate of that land uncogenial to their health and thus they became sick, and they made complaint of that to Allah's Messenger (may peace be upon him), and he said: Why don't you go to (the fold) of our camels along with our shepherd, and make use of their milk and urine. They said: Yes. They set out and drank their (camels') milk and urine and regained their health. They killed the shepherd and drove away the camels. This (news) reached Allah's Messenger (may peace be upon him) and he sent them on their track and they were caught and brought to him (the Holy Prophet). He commanded about them, and (thus) their hands and feet were cut off and their eyes were gouged and then they were thrown in the sun, until they died.</p>
        <p> This hadith has been narrated on the authority of Ibn al-Sabbah with a slight variation of words.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="016.4132"> Book 016, Number 4132: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported that some people of the tribe of 'Ukl or 'Uraina came to Allah's Messenger (may peace be upon him), and they found the climate of Medina uncogenial. Allah's Messenger (may peace be upon him) commanded them to the milch she-camels and commanded them to drink their urine and their milk. The rest of the hadith is the same (and the concluding words are):" Their eyes were pierced, and they were thrown on the stony ground. They were asking for water, but they were not given water."</p>
