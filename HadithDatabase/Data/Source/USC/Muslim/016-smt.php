      <br>
      <p><strong> Chapter 3: THE JUSTIFICATION OF QISAS (RETRIBUTION) WHEN ONE IS KILLED WITH STONE OR ANY OTHER HEAVY THING AND KILLING OF A MALE FOR THE MURDER OF A FEMALE </strong></p>
