      <br>
      <p><strong> Chapter 4: WHEN ANYONE ATTACKS THE LIFE OF A PERSON, OR HIS LIMB, AND THE VICTIM WARDS OFF THE ATTACK AND IN SELFDEFENCE EITHER THE LIFE OF THE ASSAILANT IS LOST OR HIS LIMB BROKEN, THERE WOULD BE NO PENALTY ON THE VICTIM </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="016.4143"> Book 016, Number 4143: </a> </em>
      <blockquote>
        <p> Imran b. Husain reported: Ya'la b. Munya or Ibn Umayya fought with a person, and the one bit the hand of the other. And he tried to draw his hand from his mouth and thus his foreteeth ware pulled out. They referred their dispute to Allah's Apostle (may peace be upon him), whereupon he said: Does any one of you bite as the camel bites? So there is no blood-wit for it.</p>
