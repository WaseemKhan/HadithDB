      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 27: </a> </em>
      <h1> <em> The Book Concerning the Use of Correct Words (Kitab Al-Alfaz min Al-Adab wa Ghairiha) </em> </h1>
      <br>
      <p><strong> Chapter 1: PROHIBITION OF ABUSING TIME (DAHR) </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="027.5580"> Book 027, Number 5580: </a> </em>
      <blockquote>
        <p> Abu Huraira reported: I heard Allah's Messenger (may peace be upon him) as saying: Allah, the Exalted and Glorious, said: The son of Adam abuses Dahr (the time), whereas I am Dahr since in My hand are the day and the night.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5581"> Book 027, Number 5581: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Allah, the Exalted and Glorious, said: The son of Adam displeases Me by abusing Dahr (time), whereas I am Dahr--I alternate the night and the day.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5582"> Book 027, Number 5582: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Allah, the Exalted and Glorious, said: The son of Adam causes Me pain as he says: Woe be upon the Time. None of you should say this: Woe be upon the Time, as I am the Time (because) I alternate the day and the night, and when I wish I can finish them up.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5583"> Book 027, Number 5583: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: None of you should say: Woe be upon the Time, for verily Allah is the Time.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5584"> Book 027, Number 5584: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Apostle (may peace be upon him) as saying: Do not abuse Time, for it is Allah Who is the Time.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: DISAPPROVAL OF NAMING THE VINE AS A KARM </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="027.5585"> Book 027, Number 5585: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: None of you should abuse Time for it is Allah Who is the Time, and none of you should call 'Inab (grape) as al-karm, for karm is a Muslim person.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5586"> Book 027, Number 5586: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Do not use the word karm (for wine) for worthy of respect is the heart of a believer.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5587"> Book 027, Number 5587: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Do not name grape as karm, for worthy of respect is a Muslim.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5588"> Book 027, Number 5588: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: None of you should use the word al-harin (for grape) for the heart of a believer is karm (worthy of respect).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5589"> Book 027, Number 5589: </a> </em>
      <blockquote>
        <p> Abu Huraira reported from Allah's Messenger (may peace be upon him) various ahadith, one of which is this that he said: None of you should use the word al-karm for 'Inab, for karm (worthy of respect) is a Muslim person.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5590"> Book 027, Number 5590: </a> </em>
      <blockquote>
        <p> 'Alqama b. Wa'il reported, from his father, Allah's Apostle (may peace be upon him) having said: Do not say al-karm (for the word vine) but say al-habala (that is grape). This hadith has been reported by Alqama b. Wa'il on the authority of his father with a different chain of transmitters and with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: THE PROPER USE OF THE WORDS: BONDMAN, SLAVE-GIRL, AL-MAULA AND AS-SAYYID </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="027.5591"> Book 027, Number 5591: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: None of you should say: My bondman and my slave-girl, for all of you are the bondmen of Allah, and all your women are the slave-girls of Allah; but say: My servant, my girl, and my young man and my young girl.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5592"> Book 027, Number 5592: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: None of you should say: My bondman, for all of you are the bondmen of Allah, but say: My young man, and the servant should not say: My Lord, but should say: My chief.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5593"> Book 027, Number 5593: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of al-A'mash with the same chain of transmitters, and the words are that the servant should not say to his chief: My Lord, and Abu Mu'awiya made an addition:" For it is Allah, the Exalted and Glorious, Who is your Lord."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5594"> Book 027, Number 5594: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) so many ahadith and one of them is this that Allah's Messenaer (may peace be upon him) said: None of you should say: Supply drink to your lord, feed your lord, hell) your lord in performing ablution, and none of you should say: My Lord. He should say: My chief, my patron; and none of you should say: My bondman, my slave-girl, but simply say: My boy, my girl, my servant.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 4: DISAPPROVAL OF USING THE PHRASE:" MY SOUL HAS BECOME EVIL" </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="027.5595"> Book 027, Number 5595: </a> </em>
      <blockquote>
        <p> 'A'isha reported Allah's Messenger (may peace be upon him) having said: None of you should say:" My soul has become evil," but he should say:" My soul has become remorseless." This hadith has been transmitted on the authority of Abfl Bakr with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5596"> Book 027, Number 5596: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Abia Mu'iwiya with the same chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5597"> Book 027, Number 5597: </a> </em>
      <blockquote>
        <p> Abu Umama b. Sahl b. Hunaif, on the authority of his father, reported Allah's Messenger (may peace be upon him) as saying: None of you should say:" My soul has become evil," but he should say:" My soul has become remorseless."</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 5: THE USE OF MUSK AND THAT IS THE BEST SCENT, AND THE DISAPPROVAL OF REJECTING THE GIFT OF SCENT AND FLOWER </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="027.5598"> Book 027, Number 5598: </a> </em>
      <blockquote>
        <p> Abd Sa'id Khudri reported Allah's Apostle (may peace be upon him) as saying: There was a woman from Bani Isra'il who was short-statured and she walked in the company of two tall women with wooden sandals in her feet and a ring of gold made of plates with musk filled in them and then looked up, and musk is the best of scents; then she walked between two women and they (the people) did not recognise her, and she made a gesture with her hand like this, and Shu'ba shook his hand in order to give an indication how she shook her hand.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5599"> Book 027, Number 5599: </a> </em>
      <blockquote>
        <p> Abu Sa'id Khudri reported that Allah's Messenger (may peace be upon him) made a mention of a woman of Bana Isra'il who had filled her ring with musk and musk is the most fragrant of the scents.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5600"> Book 027, Number 5600: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: He who is presented with a flower should not reject it, for it is light to carry and pleasant in odour.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="027.5601"> Book 027, Number 5601: </a> </em>
      <blockquote>
        <p> Nafi' reported that when Ibn Umar wanted fumigation he got it from aloeswood without mixing anything with it, or he put camphor along with aloeswood and then said: This is how Allah's Messenger (may peace be upon him) fumigated.</p>
      </blockquote>
      <!-- EH -->
