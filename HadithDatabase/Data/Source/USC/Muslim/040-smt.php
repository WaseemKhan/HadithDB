      <div class="content"><em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 40: </a> </em>
        <h1> <em> The Book Pertaining to Paradise, Its Description, Its Bounties and Its Intimates (Kitab Al-Jannat wa Sifat Na'imiha wa Ahliha) </em> </h1>
        <br>
        <p><strong> Chapter 1: </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6778"> Book 040, Number 6778: </a> </em>
        <blockquote>
          <p> Anas b. Malik reported: The Paradise is surrounded by hardships and the Hell-Fire is surrounded by temptations.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6779"> Book 040, Number 6779: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu Huraira through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6780"> Book 040, Number 6780: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Apostle (may peace be upon him) as saying that: Allah the Exalted and Glorious, said: I have prepared for My pious servants which no eye has ever seen, and no ear has ever heard, and no human heart has ever perceived but it is testified by the Book of Allah. He then recited:" No soul knows what comfort has been concealed from them, as a reward for what they did". (xxxii. 17)</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6781"> Book 040, Number 6781: </a> </em>
        <blockquote>
          <p> Abu Huraira reported that Allah's Apostle (may peace be upon him) said: Allah, the Exalted and Glorious, said: I have prepared for My pious servants which no eye (has ever) seen, no ear has (ever) heard and no human heart has ever perceived those bounties leaving apart (those bounties) about which Allah has informed you.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6782"> Book 040, Number 6782: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) said that Allah, the Exalted and Glorious, said: I have prepared for My pious servants which the eye has seen not, and the ear has heard not and no human heart has ever perceived such bounties leaving aside those about which Allah has informed you. He then recited:" No soul knows what comfort has been hidden for thein".</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6783"> Book 040, Number 6783: </a> </em>
        <blockquote>
          <p> Sahl b. Sa'd as-Sa'idi reported: I was in the company of Allah's Messenger (may peace be upon him) that he gave a description of Paradise and then Allah's Apostle (may peace be upon him) concluded with these words: There would be bounties which the eye has not seen and the ear has not heard and no human heart has ever perceived them. He then recited this verse:" They forsake (their) beds, calling upon their Lord in fear and in hope, and spend out of what We have given them. So no soul knows what refreshment of the eyes is hidden for them: a reward for what they did" (xxxii. 16-17)</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 2: THERE IS IN PARADISE A TREE UNDER THE SHADOW OF WHICH A RIDER CAN TRAVEL FOR A HUNDRED YEARS AND EVEN THEN HE WOULD NOT BE ABLE TO COVER IT </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6784"> Book 040, Number 6784: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: In Paradise, there is a tree under the shadow of which a rider can travel for a hundred years.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6785"> Book 040, Number 6785: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu Huraira through another chain of transmitters with the addition of these words:" He will not be able to cover this distance."</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6786"> Book 040, Number 6786: </a> </em>
        <blockquote>
          <p> Sahl b. Sa'd reported Allah's Messenger (may peace be upon him) as saying: In Paradise, there is a tree under the shadow of which a rider can travel for a hundred years without covering (the distance) completely. This hadith has also been transmitted on the authority of Abu Sa'id al-Khudri that Allah's Apostle (may peace be upon him) is reported to have said: In Paradise, there is a tree under the shadow of which a rider of a fine and swift-footed horse would travel for a hundred years without covering the distance completely. There would be the pleasure of Allah for the inmates of Paradise and He would never be annoyed with them.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6787"> Book 040, Number 6787: </a> </em>
        <blockquote>
          <p> Abu Sa'id al-Khudri reported that Allah's Apostle (may peace be upon him) said that Allah would say to the inmates of Paradise: O, Dwellers of Paradise, and they would say in response: At thy service and pleasure, our Lord, the good is in Thy Hand. He (the Lord) would say: Are you well pleased now? They would say: Why should we not be pleased, O Lord, when Thou hast given us what Thou hast not given to any of Thy creatures? He would, however, say: May I not give you (something) even more excellent than that? And they would say: 0 Lord, what thing can be more excellent than this? And He would say: I shall cause My pleasure to alight upon you and I shall never be afterwards annoyed with you.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 3: THE INMATES OF PARADISE WOULD SEE THE INMATES OF THE UPPER APARTMENT AS ARE SEEN THE PLANETS IN THE SKY </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6788"> Book 040, Number 6788: </a> </em>
        <blockquote>
          <p> Sahl b. Sa'd reported Allah's Messenger (may peace be upon him) as saying: The inmates of Paradise will look to the upper apartment of Paradise as you see the planets in the sky. I narrated this hadith to Nu'man b. Abi 'Ayyash and he said: I heard Abu Sa'id al-Khudri as saying: As you see the shining planets in the eastern and western (sides of) horizon.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6789"> Book 040, Number 6789: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu Hazim through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6790"> Book 040, Number 6790: </a> </em>
        <blockquote>
          <p> Abu Sa'id al-Khudri reported Allah's Messenger (may peace be upon him) as saying: The inmates of Paradise would see the inmates of the apartment over them just as you see the shining planets which remain in the eastern and the western horizon because of the superiority some have over others. They said: Allah's Messenger, would in these abodes of Apostles others besides them not be able to reach? He said: Yes, they will, by Him, in Whose hand is my life, those who believe in God and acknowledge the Truth, will reach them.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 4: HE WHO WOULD LOVE TO HAVE A GLIMPSE OF THE SACRED FACE OF THE HOLY PROPHET (MAY PEACE BE UPON HIM) EVEN AT THE COsT OF HIS WHOLE PROPERTY AND HIS FAMILY </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6791"> Book 040, Number 6791: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: The people most loved by me from amongst my Ummah would be those who would come after me but everyone amongst them would have the keenest desire to catch a glimpse of me even at the cost of his family and wealth.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 5: THERE IS A STREET IN PARADISE WHERE THE INMATES OF PARADISE WOULD GET FAVOUR AND GRACE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6792"> Book 040, Number 6792: </a> </em>
        <blockquote>
          <p> Anas b. Malik reported that Allah's Messenger (may peace be upon him) said: In Paradise there is a street to which they would come every Friday. The north wind will blow and would scatter fragrance on their faces and on their clothes and would add to their beauty and loveliness, and then they would go back to their family after having an added lustre to their beauty and loveliness, and their family would say to them: By Allah, you have been increased in beauty and loveliness after leaving us, and they would say: By Allah, you have also increased in beauty and loveliness after us.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 6: THE FIRST GROUP THAT WOULD BE ADMITTED TO PARADISE WOULD BE LIKE THE FACE OF THE FULL MOON AND THE DESCRIPTION OF THEIR QUALITIES AND THEIR SPOUSES </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6793"> Book 040, Number 6793: </a> </em>
        <blockquote>
          <p> Muhammad reported that some (persons) stated with a sense of pride and some discussed whether there would be more men in Paradise or more women. It was upon this that Abu Huraira reported that Abu'l Qasim (the Holy Prophet) (may peace be upon him) said: The (members) of the first group to get into Paradise would have their faces as bright as full moon during the night, and the next to this group would have their faces as bright as the shining stars in the sky, and every person would have two wives and the marrow of their shanks would glimmer beneath the flesh and there would be none without a wife in Paradise.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6794"> Book 040, Number 6794: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu Huraira through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6795"> Book 040, Number 6795: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Abu Huraira through another chain of transmitters that Allah's Messenger (may peace be upon him) said: The (members of the) first group which would get into Paradise will have their faces as bright as stars in the sky. They would neither pass water, nor void excrement, nor will they suffer from catarrh, nor will they spit, and their combs would be made of gold, and their sweat will be musk, the fuel of their brazier will be aloes, and their wives will be large-eyed maidens and their form would be alike as one single person after the form of their father (Adam) sixty cubits tall.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6796"> Book 040, Number 6796: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: The first group of my Ummah to get into Paradise would be like a full moon in the night. Then those who would be next to them; they would be like the most significantly glittering stars in regard to brightness, then after them (others) in ranks. They would neither void excrement, nor pass water, nor suffer from catarrh, nor would they spit. And their combs would be made of gold, and the fuel of their braziers would be aloes and their sweat would be musk and their form would be the form of one single person according to the length of their father sixty cubits tall. This hadith has been transmitted on the authority of Ibn Abi Shaiba with a slight variation of wording.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 7: THE DESCRIPTION OF PARADISE AND THE REMEMBRANCE OF ALLAH BY ITS INMATES MORNING AND EVENING </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6797"> Book 040, Number 6797: </a> </em>
        <blockquote>
          <p> Hammam b. Munabbih reported: These are some of the ahidith which Abu Huraira reported from Allah's Messenger (may peace be upon him) and one is this that he is reported to have said: The (members of the) first group that would be admitted to Paradise would have their faces as bright as full moon during the night. They would neither spit nor suffer catarrh, nor void excrement. They would have their utensils and their combs made of gold and silver and the fuel of their braziers would be aloes and their sweat would be musk and every one of them would have two spouses (so beautiful) that the marrow of their shanks would be visible through the flesh. There would be no dissension amongst them and no enmity in their hearts. Their hearts would be like one heart, glorifying Allah morning and evening.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6798"> Book 040, Number 6798: </a> </em>
        <blockquote>
          <p> Jabir reported: I heard Allah's Apostle (may peace be upon him) as saying that the inmates of Paradise would eat and drink but would neither spit, nor pass water, nor void excrement, nor suffer catarrah. It was said: Then, what would happen with food? Thereupon he said: They would belch and sweat (and it would be over with their food), and their sweat would be that of musk and they would glorify and praise Allah as easily as you breathe.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6799"> Book 040, Number 6799: </a> </em>
        <blockquote>
          <p> This hadith has been transmitted on the authority of A'mash with a slight variation of wording.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6800"> Book 040, Number 6800: </a> </em>
        <blockquote>
          <p> Jabir b. Abdullah reported that Allah's Messenger (may peace be upon him) said that the inmates of Paradise would eat therein and they would also drink, but they would neither void excrement, nor suffer catarrh, nor pass water, and their eating (would be digested) in the form of belching and their sweat would be musk aged they would glorify and praise Allah as easily ai you breathe.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6801"> Book 040, Number 6801: </a> </em>
        <blockquote>
          <p> This hadith has been transmitted on the authority of Jabir with a slight variation of wording.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 8: THE EVERLASTING BLISS FOR THE INMATES OF PARADISE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6802"> Book 040, Number 6802: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Apostle (may peace be upon him) as saying: He who would get into Paradise (would be made to enjoy such an everlasting) bliss that he would neither become destitute, nor would his clothes wear out, nor his youth would decline.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6803"> Book 040, Number 6803: </a> </em>
        <blockquote>
          <p> Abu Sa'id al-Khudri and Abu Huraira both reported Allah's Messenger (may peace be upon him) as saying: There would be an announcer (in Paradise) who would make this announcement: Verily I there is in store for you (everlasting) health and that you should never fall ill and that you live (for ever) and do not die at all. And that you would remain young and never grow old. And that you would always live in affluent circumstances and never become destitute, as words of Allah, the Exalted and Glorious, are:" And it would be announced to them: This is the Paradise. You have been made to inherit it for what you used to do". (VII; 43)</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 9: THE DESCRIPTION OF THE TENTS FOR THE INMATES OF PARADISE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6804"> Book 040, Number 6804: </a> </em>
        <blockquote>
          <p> Abu Bakr b. Abdullah b. Qais reported on the authority of his father that Allah's Messenger (may peace be upon him) said that in Paradise there would be for a believer a tent of a single hollowed pearl the breadth of which would be sixty miles. It would be meant for a believer and the believers would go around it and none would be able to see the others.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6805"> Book 040, Number 6805: </a> </em>
        <blockquote>
          <p> Abu Bakr b. Abdullah b. Qais reported on the authority of his father that Allah's Messenger (may peace be upon him) said that in Paradise there would be a tent made of a single hollowed pearl, the breadth of which would be sixty miles from all sides and there would live a family in each corner and the other would not be able to see the believer who goes around them.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6806"> Book 040, Number 6806: </a> </em>
        <blockquote>
          <p> This hadith has been transmitted on the authority of Abu Bakr b. Abu Musa b. Qais who, on the authority of his father, reported the Apostle (may peace be upon him) to have said that there would be a tent made of a pearl whose height towards the sky would be sixty miles. In each corner, there would be a fanaily of the believer, out of sight for the others.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 10: WHAT RIVERS OF THE WORLD WOULD BE FOUND IN PARADISE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6807"> Book 040, Number 6807: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Saihan, Jaihan, Euphrates and Nile are all among the rivers of Paradise.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 11: THERE WOULD ENTER PARADISE SOME PEOPLE WHOSE HEARTS WOULD BE'LIKE THOSE OF BIRDS </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="040.6808"> Book 040, Number 6808: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: There would enter Paradise people whose hearts would be like those of the hearts of birds.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="040.6809"> Book 040, Number 6809: </a> </em>
        <blockquote>
          <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: Allah, the Exalted and Glorious, created Adam in His own image with His length of sixty cubits, and as He created him He told him to greet that group, and that was a party of angels sitting there, and listen to the response that they give him, for it would form his greeting and that of his offspring. He then went away and said: Peace be upon you! They (the angels) said: May there be peace upon you and the Mercy of Allah, and they made an addition of" Mercy of Allah". So he who would get into Paradise would get in the form of Adarn, his length being sixty cubits, then the people who followed him continued to diminish in size up to this day.</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 12: THE DESCRIPTION OF HELL AND THE INTENSITY OF ITS HEAT AND TORMENTS </strong></p>
