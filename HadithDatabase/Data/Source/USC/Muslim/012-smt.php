      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 12: </a> </em>
      <h1> <em> The Book of Gifts (Kitab Al-Hibat) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> A Hiba is defined as the transfer of the possession of property, movable and immovable, from one person to the other willingly and without any reward. The one who makes this transfer is known as donor (Wahib), and the thing transferred is known as Mauhubah, and the one who is donated is known as Mauhub lahu (donee). The other words used in the language of the Shar'i'ah are Nihla or 'Atiyya. </p>
      <p> The act of Hiba is not complete unless the donor surrenders the possession of the thing donated. Moreover, the declaration of donation should be in clear terms without any ambiguity (around them). This act of donation is based on the following hadith of the Holy Prophet (may peace be upon him):" Give presents to one another for this would increase your mutual love." This not only implies the legality of gifts, but the exhortation to give these to one another. There is perfect agreement amongst all the jurists that Hiba is valid in Islam. </p>
      <p> The act of donation can be made verbally or in writing by any person capable of making a contract. A gift by a person involved in debt is invalid, and gift in death illness cannot take effect beyond one-third of the assets of the deceased after defraying all necessary expenses. The donation can be made to a living person and not to one who is dead. </p>
      <p> The Shari'ah imposes certain restrictions on the property which is to be donated. In the first place, the property must be in existence at the time when the gift is given; hence the gift of oil in the sesame or of butter in the milk would not be valid. The property once given as a gift cannot be revoked except in some extraordinary circumstances. The father is entitled to get back the donation given by him from his children if be finds it contrary to the laws and spirit of the Shari'ah. The right to revoke a gift is called Raj'. This act of revocation can be done with the decree of a competent court. </p>
