      <div class="content"><em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 38: </a> </em>
        <h1> <em> Pertaining To The Charateristics Of The Hypocrites And Command Concerning Them (Kitab Sifat Al-Munafiqin Wa Ahkamihim) </em> </h1>
        <br>
        <p><strong> Chapter 1: </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="038.6677"> Book 038, Number 6677: </a> </em>
        <blockquote>
          <p> Zaid b. Arqam reported: We set out on a journey along with Allah's Messenger (may peace be upon him) in which we faced many hardships. 'Abdullah b. Ubayy said to his friends: Do not give what you have in your possession to those who are with Allah's Messenger (may peace be upon him) until they desert him. Zubair said: That is the reciting of that person who recited as min haulahu (from around him) and the other reciting is man haulahia (who are around him). And in this case when we would return to Medina the honourable would drive out the meaner therefrom (lxiv. 8). I came to Allah's Apostle (may peace be upon him) and informed him about that and he sent someone to 'Abdullah b. Ubayy and he asked him whether he had said that or not. He took an oath to the fact that he had not done that and told that it was Zaid who had stated a lie to Allah's Messenger (may peace be upon him). Zaid said: I was much perturbed because of this until this verse was revealed attesting my truth:" When the hypocrites come" (lxiii. 1). Allah's Apostle (may peace be upon him) then called them in order to seek forgiveness for them, but they turned away their heads as if they were hooks of wood fixed in the wall (lxiii. 4), and they were in fact apparently good-looking persons.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6678"> Book 038, Number 6678: </a> </em>
        <blockquote>
          <p> Jabir reported Allah's Apostle (may peace be upon him) came to the grave of 'Abdullah b. Ubayy, brought him out from that, placed him on his knee and put his saliva in his mouth and shrouded him in his own shirt and Allah knows best.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6679"> Book 038, Number 6679: </a> </em>
        <blockquote>
          <p> Jabir b. 'Abdullah reported that Allah's Messenger (may peace be upon him) came to the grave of 'Abdullah b. Ubayy as he was placed in that. The rest of the hadith is the same.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6680"> Book 038, Number 6680: </a> </em>
        <blockquote>
          <p> Ibn 'Umar reported that when 'Abdullah b. Ubayy b. Salul died. His son 'Abdullah b. 'Abdullah (b. Ubayy) came to Allah's Messenger (may peace be upon him) and begged him that he should give him his shirt which he would use as a coffin for his father, he gave him that. He then begged that he should conduct funeral prayer for him. Allah's Messenger (may peace be upon him) had hardly got up to observe the prayer for him that 'Umar stood up and caught hold of the garment of Allah's Messenger (may peace be upon him) and said: Allah's Messenger, are you going to conduct prayer for this man, whereas Allah has forbidden you to offer prayer for him? Thereupon Allah's Messenger (may peace be upon him) said: Allah has given me an option as He has said:" You may beg pardon for them or you may not beg pardon for them, and even if you beg pardon for them, seventy times" (ix. 80), and I am going to make an addition to the seventy. He was a hypocrite and Allah's Messenger (may peace be upon him) offered prayer for him and Allah, the Exalted and Glorious, revealed this verse:" Do not offer prayer for any one of them at all and do not stand upon their graves for (offering prayer over them)" (ix. 84).</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6681"> Book 038, Number 6681: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of 'Abdullah with the same chain of transmitters but with this addition:" He then abandoned offering (funeral) prayer for them."</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6682"> Book 038, Number 6682: </a> </em>
        <blockquote>
          <p> Ibn Mas'ud reported that there gathered near the House three persons amongst whom two were Quraishi and one was a Thaqafi or two were Thaqafis and one was a Quraishi. They lacked understanding but wore more flesh. One of them said: Do you think that Allah hears as we speak? The other one said: He does hear when we speak loudly and He does not hear when we speak in undertones, and still the other one said: If He listens when we speak loudly, He also listens when we speak in undertones. It was on this occasion that this verse was revealed:" You did not conceal yourselves lest your ears, your eyes and your skins would stand witness against you" (xli. 22).</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6683"> Book 038, Number 6683: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of 'Abdullah through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6684"> Book 038, Number 6684: </a> </em>
        <blockquote>
          <p> Zaid b. Thabit reported that Allah's Apostle (may peace be upon him) set out for Uhud. Some of those persons who were with them came back. The Companions of Allah's Apostle (may peace be upon him) were divided in two groups. One group said: We would kill them, and the other one said: No, this should not be done, and it was on this occasion that this verse was revealed:" Why should you, then, be two parties in relation to hypocrites?" (iv. 88).</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6685"> Book 038, Number 6685: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Shu'ba with the same chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6686"> Book 038, Number 6686: </a> </em>
        <blockquote>
          <p> Abu Sa'id Khudri reported that during the lifetime of Allah's Messenger (may peace be upon him) the hypocrites behaved in this way that when Allah's Apostle (may peace be upon him) set out for a battle, they kept themselves behind, and they became happy that they had managed to sit in the house contrary to (the act of) Allah's Messenger (may peace be upon him), and when Allah's Apostle (may peace he upon him) came back, they put forward excuses and took oath and wished that people should laud them for the deeds which they had not done. It was on this occasion that this verse was revealed:" Think not that those who exult in what they have done, and love to be praised for what they have not done-think not them to be safe from the chastisement; and for them is a painful chastisement" (iii. 18).</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6687"> Book 038, Number 6687: </a> </em>
        <blockquote>
          <p> Humaid b. 'Abd al-Rahman b. 'Auf reported that Marwan said to Rafi', his chamberlain, that he should go to Ibn 'Abbas and ask him: If every one of us be punished for his being happy upon his deed and for his being praised for what he has not done, nobody would be saved from the torment. Ibn 'Abbas said: What you have to do with this verse? It has been in fact revealed in connection with the people of the Book." Then Ibn Abbas recited this verse:" When Allah took a covenant from those who had been given the Book: You shall explain it to people and shall not conceal this" (iii. 186), and then Ibn 'Abbas recited this verse:" Think not that those who exult in what they have done and love to be praised for what they have not done" (iii. 186). Ibn 'Abbas (further) said: Allah's Apostle (may peace be upon him) asked them about something and then they concealed that and they told him something else and they went out and they thought that they had informed him as lie had asked them and they felt happy of what they had concealed.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6688"> Book 038, Number 6688: </a> </em>
        <blockquote>
          <p> Qais reported: I said to 'Ammar: What is your opinion about that which you have done in case (of your siding with Hadrat 'Ali)? Is it your personal opinion or something you got from Allah's Messenger (may peace be upon him)? 'Ammar said: We have got nothing from Allah's Messenger (may peace be upon him) which people at large did not get, but Hudhaifa told me that Allah's Apostle (may peace be upon him) had especially told him amongst his Companion, that there would be twelve hypocrites out of whom eight would not get into Paradise, until a camel would be able to pass through the needle hole. The ulcer would be itself sufficient (to kill) eight. So far as four are concerned, I do not remember what Shu'ba said about them.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6689"> Book 038, Number 6689: </a> </em>
        <blockquote>
          <p> Qais b. 'Ubad reported: We said to 'Ammar: Was your fighting (on the side of 'Ali in the Battle of Siffin) a matter of your own choice or you got its hints from Allah's Messenger (may peace be upon him) for it, is likely for one to err in one's own discretion or was it because of any covenant that Allah's Messenger (may peace be upon him) got from you? He said: It was not because of any covenant that Allah's Messenger (may peace be upon him) got from us which he did get from other people, and he further said that Allah's Messenger (may peace be upon him) said:" In my Ummah." And I think that Hudhaifa reported to me and according to Ghundar (the words are) that he said: In my Ummah, there would be twelve hypocrites and they would not be admitted to Paradise and they would not smell its odour, until the camel would pass through a needle's hole. Dubaila (ulcer) would be enough to (torment them) -a kind of flame of Fire which would appear in their shoulders and it would protrude from their chest.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6690"> Book 038, Number 6690: </a> </em>
        <blockquote>
          <p> Abu Tufail reported that there was a dispute between Hudhaifa and one from the people of Aqaba as it happens amongst people. He said: I adjure you by Allah to tell me as to how many people from Aqaba were. The people said to him (Hudhaifa) to inform him as he had asked. We have been informed that they were fourteen and If you are to be counted amongst them, then they would be fifteen and I state by Allah that twelve amongst them were the enemies of Allah and of His Messenger (may peace be upon him) in this world. The rest of the three put forward this excuse: We did not hear the announcement of Allah's Messenger (may peace be upon him) and we were not aware of the intention of the people as he (the Holy Prophet) had been in the hot atmosphere. He (the Holy Prophet) then said: The water is small in quantity (at the next station). So nobody should go ahead of me, but he found people who had gone ahead of him and he cursed them on that day.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6691"> Book 038, Number 6691: </a> </em>
        <blockquote>
          <p> Jabir b. Abdullah reported that Allah's Messenger (may peace be upon him) said: He who climbed this hill, the hill of Murar, his sins would be obliterated as were obliterated the sins of Bani Isra'il. So the first to take their horses were the people of Banu Khazraj. Then there was a ceaseless flow of persons and Allah's Messenger (may peace be upon him) said to them: All of you are those who have been pardoned except the owner of a red camel. We came to him and said to him: You also come on, so that Allah's Messenger (may peace be upon him) may seek forgiveness for you. But he said: By Allah, so far as I am concerned, the finding of something lost is dearer to me than seeking of forgiveness for me by your companion (the Holy Prophet), and he remained busy in finding out his lost thing.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6692"> Book 038, Number 6692: </a> </em>
        <blockquote>
          <p> Jabir b. Abdullah reported Allah's Messenger (may peace be upon him) as saying: He who would climb this hill of Murar. The rest of the hadith is the same but with this variation that it was a desert Arab who was finding out his lost thing.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6693"> Book 038, Number 6693: </a> </em>
        <blockquote>
          <p> Anas b. Malik reported: There was a person amongst us who belonged to the tribe of Bani Najjar and he recited Sura al-Baqarah and Surat Al-i-'Imran and he used to transcribe for Allah's Messenger (may peace be upon him). He ran away as a rebel and joined the People of the Book. They gave it much importance and said: He is the person who used to transcribe for Muhammad and they were much pleased with him. Time rolled on that Allah caused his death. They dug the grave and buried him therein, but they found to their surprise that the earth had thrown him out over the surface. They again dug the grave for him and buried him but the earth again threw him out upon the surface. They again dug the grave for him and buried him but the earth again threw him out upon the surface. At last they left him unburied.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6694"> Book 038, Number 6694: </a> </em>
        <blockquote>
          <p> Jabir reported that Allah's Messenger (may peace be upon him) came back from a journey and as he was near Medina, there was such a violent gale that the mountain seemed to be pressed. Allah's Messenger (may peace be upon him) said: This wind has perhaps been made to blow for the death of a hypocrite, and as he reached Medina a notorious hypocrite from amongst the hypocrites had died.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6695"> Book 038, Number 6695: </a> </em>
        <blockquote>
          <p> Iyas reported on the authority of his father: We went along with Allah's Messenger (may peace be upon him) to visit a person suffering from fever. When I placed my hand upon him, I said: By Allah, I have never seen, till this day, a person running higher temperature than he. Thereupon Allah's Apostle (may peace be upon him), turning his face to his companions, said: May I not inform you of a severer temperature than this which these two persons would run on the Day of Resurrection? And they were two hypocrites riding upon the camel turning their back towards (the Muslims).</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6696"> Book 038, Number 6696: </a> </em>
        <blockquote>
          <p> Ibn Umar reported Allah's Apostle (may peace be upon him) as saying: The similitude of a hypocrite is that of a sheep which roams aimlessly between two flocks. She goes to one at one time and to the other at another time.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="038.6697"> Book 038, Number 6697: </a> </em>
        <blockquote>
          <p> Ibn Umar reported Allah's Apostle (may peace be upon him) saying like this but with this change of words:" She sometimes finds a way in one flock and then in another flock."</p>
        </blockquote>
        <!-- EH -->
