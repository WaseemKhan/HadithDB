      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 34: </a> </em>
      <h1> <em> The Book of Knowledge (Kitab Al-`Ilm) </em> </h1>
      <br>
      <p><strong> Chapter 1: PROHIBITION OF MAKING A HOT PURSUIT OF THE ALLEGORIES CONTAINED IN THE QUR'AN, AND AVOIDING THOSE WHO DO IT, AND OF DISPUTATION IN THE QUR'AN </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="034.6442"> Book 034, Number 6442: </a> </em>
      <blockquote>
        <p> 'A'isha reported that Allah's Messenger (may peace be upon him) recited (these verses of the Qur'an):" He it is Who revealed to thee (Muhammad) the Book (the Qur'an) wherein there are clear revelations-these are the substance of the Book and others are allegorical (verses). And as for those who have a yearning for error they go after the allegorical verses seeking (to cause) dissension, by seeking to explain them. And none knows their implications but Allah, and those who are sound in knowledge say: We affirm our faith in everything which is from our Lord. It is only the persons of understanding who really heed" (iii. 6). 'A'isha (further) reported that Allah's Messenger (may peace be upon him) said (in connection with these verses): When you see such verses, avoid them, for it is they whom Allah has pointed out (in the mentioned verses).</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: PERRAINING TO A DISPUTATIONIST </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="034.6443"> Book 034, Number 6443: </a> </em>
      <blockquote>
        <p> 'Abdullah b. 'Umar reported: I went to Allah's Messenger (may peace be upon him) in the morning and he heard the voice of two persons who had an argumenta- tion with each other about a verse. Allah's Apostle (may peace be upon him) came to us (and) the (signs) of anger could be seen on his face. He said: Verily, the (peoples) before you were ruined because of their disputation in the Book.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6444"> Book 034, Number 6444: </a> </em>
      <blockquote>
        <p> Jundub b. 'Abdullah al-Bajali reported Allah's Messenger (may peace be upon him) as saying: Recite the Qur'an as long as your hearts agree to do so, and when you feel variance between them (between your hearts and tongues), then get up (and leave its recital for the time being).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6445"> Book 034, Number 6445: </a> </em>
      <blockquote>
        <p> Jundub (i. e. Ibn 'Abdullah) reported that Allah's Messenger (may peace be upon him) said: Recite the Qur'an as long as your hearts agree to do so and when you find variance between them, then stand up.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6446"> Book 034, Number 6446: </a> </em>
      <blockquote>
        <p> Abu Imran reported that Jundub told us as we we-re young boys living in Kilfa, that Allah's Messenger (may peace be upon him) had said: Recite the Qur'an. The rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6447"> Book 034, Number 6447: </a> </em>
      <blockquote>
        <p> 'A'isha reported Allah's Messenger (may peace be upon him) as saying: The most despicable amongst persons in the eye of Allah is one who tries to fall into dispute with others (for nothing but only to display his knowledge and power of argumentation).</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: FOLLOWING THE FOOTSTEPS OF THE JEWS AND THE CHRISTIANS </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="034.6448"> Book 034, Number 6448: </a> </em>
      <blockquote>
        <p> Abu Sa'id al-Khudri reported Allah's Messenger (may peace be upon him) as saying: You would tread the same path as was trodden by those before you inch by inch and step by step so much so that if they had entered into the hole of the lizard, you would follow them in this also. We said: Allah's Messenger, do you mean Jews and Christians (by your words)" those before you"? He said: Who else (than those two religious groups)?</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6449"> Book 034, Number 6449: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of 'Ata' b. Yasir through another chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 4: THOSE WHO INDULGED IN HAIR-SPLITTING WERE RUINED </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="034.6450"> Book 034, Number 6450: </a> </em>
      <blockquote>
        <p> Abdullah reported Allah's Messenger (may peace be upon him) as saying: Ruined, were those who indulged in hair-splitting. He (the Holy Prophet) repeated this thrice.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 5: KNOWLEDGE WOULD BE TAKEN AWAY, AND IGNORANCE WOULD PREVAIL UPON PEOPLE AND THE TURMOIL AT THE END OF THE WORLD </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="034.6451"> Book 034, Number 6451: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported Allah's Messenger (may peace be upon him) as saying: It is from the conditions of the Last Hour that knowledge would be taken away and ignorance would prevail (upon the world), the liquor would be drunk, and adultery would become rainpant.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6452"> Book 034, Number 6452: </a> </em>
      <blockquote>
        <p> Qatida reported that Anas b. Malik said: May I not narrate to you a hadith which I heard from Allah's Messenger (may peace be upon him) which no one would narrate to you after me who would have personally heard it from him (the Holy Prophet) (as I have the good fortune to do so)? -" It is from the signs of the Last Hour that knowledge would be taken away, ignorance would prevail upon (the world), adultery would become common, wine would be drunk, the number of men will fall short and the women would survive (and thus such a disparity would arise in the number of men and women) that there would be one man to look after fifty women.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6453"> Book 034, Number 6453: </a> </em>
      <blockquote>
        <p> This hadith has been transmitted on the authority of Anas b. Malik through another chain of narrators, but with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6454"> Book 034, Number 6454: </a> </em>
      <blockquote>
        <p> Abu Wa'il reported: I was sitting with 'Abdullah and Abu Musa that they reported Allah's Messenger (may peace be upon him) having said: Prior to the Last Hour, there would be a time when knowledge would be taken away, and ignorance would take its place and there would be bloodshed on a large scale.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6455"> Book 034, Number 6455: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of 'Abdullah (b. Mas'ud) and Abu Musa (al-Asha'ri) through other chains of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6456"> Book 034, Number 6456: </a> </em>
      <blockquote>
        <p> A hadith like this has been narrated on the authority of Abu Musa through another chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6457"> Book 034, Number 6457: </a> </em>
      <blockquote>
        <p> Abu Wa'il reported: I was sitting with Abu Musa and 'Abdullah and they were conversing with each other and Abu Musa reported Allah's Messenger (may peace be upon him) as saying (that we find in the above-mentioned ahadith).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6458"> Book 034, Number 6458: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: (When) the time would draw close to the Last Hour, knowledge would be snatched away, turmoil would be rampant, miserliness would be put (in the hearts of the people) and therewould be much bloodshed. They said: What is al-harj? Thereupon he said: It is bloodshed.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6459"> Book 034, Number 6459: </a> </em>
      <blockquote>
        <p> This hadith has been transmitted on the authority of Abu Huraira with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6460"> Book 034, Number 6460: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) having said: The time would draw close to the Last Hour and knowledge would decrease. The rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6461"> Book 034, Number 6461: </a> </em>
      <blockquote>
        <p> This hadith has been transmitted on the authority of Abu Huraira through other chains of narrators and there is no mention of:" Miserliness would be put (in the hearts of the people)."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6462"> Book 034, Number 6462: </a> </em>
      <blockquote>
        <p> 'Abdullah b. 'Amr b. al-'As reported Allah's Messenger (may peace be upon him) as saying: Verily, Allah does not take away knowledge by snatching it from the people but He takes away knowledge by taking away the scholars, so that when He leaves no learned person, people turn to ignorant as their, leaders; then they are asked to deliver religious verdicts and they deliver them without knowledge, they go astray, and lead others astray.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6463"> Book 034, Number 6463: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of 'Abdullah b. Umar through other chains of transmitters, but in the hadith transmitted by Umar b. 'Ali there is an addition of these words:, I met 'Abdullah b. 'Amr at the end of the year and I asked him about it, and he narrated to us the hadith as he had narrated before that he had heard Allah's Messenger (may peace be upon him) as saying.... (The rest of the hadith is the same).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6464"> Book 034, Number 6464: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of 'Abdullah b. Amr b. al-'As through another chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6465"> Book 034, Number 6465: </a> </em>
      <blockquote>
        <p> 'Urwa b. Zubair reported that 'A'isha said to him: This news has reached me that 'Abdullah b. 'Amr al-'As would pass by us during the Hajj season, so you meet him and ask him (about religious matters) as he has acquired great knowledge from Allah's Messenger (may peace be upon him). I thus met him and asked him about things which he narrated from Allah's Messenger (may peace be upon him). And amongst these the one he mentioned was that Allah's Apostle (may peace be upon him) said: Verily, Allah does not take away knowledge from people directly but he takes away the scholars and consequently takes away (knowledge) along with them and leaves amongst persons the ignorant as their leaders who deliver religious verdicts without (adequate) knowledge and themselves go astray and lead others astray. 'Urwa said: When I narrated this to 'A'isha, she deemed it too much (to believe) and thus showed reluctance to accept that (as perfectly true) and said to, 'Urwa: Did he ('Abdullah b. 'Amr) say to you that he had heard Allah's Apostle (may peace be upon him) as saying: ('Urwa had forgotten to ask this from 'Abdullah b. 'Amr). So when it was the next year, she ('A'isha) said to him ('Urwa): Ibn Amr has come (for Hajj), so meet him. talk to him and ask him about this hadith that he narrated to You (last year on the occasion of the Hajj) pertaining to knowledge. He ('Urwa), said: So I met him, and asked about it and he narrated to me exactly like one that he had narrated (to me) for the first time. So when I informed her ('A'isha) about that, she said: I do not think but this that he has certainly told the truth and I find that be has neither made any addition to it, nor missed anything from it.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 6: PERTAINING TO ONE WHO INTRODUCED SOMETHING GOOD OR EVIL, OR HE WHO CALLED TO RIGHTEOUSNESS OR TO THE PATH OF ERROR </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="034.6466"> Book 034, Number 6466: </a> </em>
      <blockquote>
        <p> Jarir b. Abdullah reported that some desert Arabs clad in woollen clothes came to Allah's Messenger (may peace be upon him). He saw them in sad plight as they had been hard pressed by need. He (the Holy Prophet) exhorted people to give charity, but they showed some reluctance until (signs) of anger could be seen on his face. Then a person from the Ansar came with a purse containing silver. Then came another person and then other persons followed them in succession until signs of happiness could be seen on his (sacred) face. Thereupon Allah's Messenger (may peace be upon him) said: He who introduced some good practice in Islam which was followed after him (by people) he would be assured of reward like one who followed it, without their rewards being diminished in any respect. And he who introduced some evil practice in Islam which had been followed subsequently (by others), he would be required to bear the burden like that of one who followed this (evil practice) without their's being diminished in any respect.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6467"> Book 034, Number 6467: </a> </em>
      <blockquote>
        <p> Jarir reported that Allah's Messenger (may peace be upon him) delivered an address in which he exhorted people to give charity.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6468"> Book 034, Number 6468: </a> </em>
      <blockquote>
        <p> Jarir b. 'Abdullah reported Allah's Messenger (may peace be upon him) as saying: The servant does not introduce good practice which is followed after him.... The rest of the hadith is the same.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6469"> Book 034, Number 6469: </a> </em>
      <blockquote>
        <p> Jarir transmitted this hadith from his father through another chain of narrators.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="034.6470"> Book 034, Number 6470: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: He who called (people) to righteousness, there would be reward (assured) for him like the rewards of those who adhered to it, without their rewards being diminished in any respect. And he who called (people) to error, he shall have to carry (the burden) of its sin, like those who committed it, without their sins being diminished in any respect.</p>
      </blockquote>
