      <div class="content"><em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 36: </a> </em>
        <h1> <em> The Book of Heart-Melting Traditions (Kitab Al-Riqaq) </em> </h1>
        <br>
        <p><strong> Chapter 1: THE MAJORITY IN PARADISE WOULD CONSIST OF THE POOR PIOUS PERSONS AND THE MAJORITY OF THE DENIZENS OF HELL WOULD CONSIST OF WOMEN, AND THE TRIAL BY MEANS OF WOMEN </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="036.6596"> Book 036, Number 6596: </a> </em>
        <blockquote>
          <p> Usama b. Zaid reported that Allah's Messenger (way peace be upon him) said: I stood at the door of Paradise and I found that the overwhelming majority of those who entered therein was that of poor persons and the wealthy persons were detained to get into that. The denizens of Hell were commanded to get into Hell, and I stood upon the door of Fire and the majority amongst them who entered there was that of women.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6597"> Book 036, Number 6597: </a> </em>
        <blockquote>
          <p> Ibn Abbas reported that Allah's Messenger (may peace be upon him) said: I had a chance to look into the Paradise and I found that majority of the people was poor and I looked into the Fire and there I found the majority constituted by women.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6598"> Book 036, Number 6598: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Ibn 'Abbas through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6599"> Book 036, Number 6599: </a> </em>
        <blockquote>
          <p> Ibn 'Abbas reported that Allah's Messenger (may peace be upon him) stated like this: Re looked into the Fire of Hell. The rest of the hadith is the same.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6600"> Book 036, Number 6600: </a> </em>
        <blockquote>
          <p> Imran b. Husain reported that Allah's Messenger (may peace be upon him) said: Amongst the inmates of Paradise the women would form a minority.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6601"> Book 036, Number 6601: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Mutarrif through another chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6602"> Book 036, Number 6602: </a> </em>
        <blockquote>
          <p> 'Abdullah b. Umar reported that Allah's Messenger (may peace be upon him) supplicated in these words:" O Allah, I seek refuge in Thee from the withdrawal of Thine blessing and the change of Thine protection (from me) and from the sudden wrath of Thine, and from every displeasure of Thine."</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6603"> Book 036, Number 6603: </a> </em>
        <blockquote>
          <p> Usama b. Zaid reported Allah's Messenger (may peace be upon him) as saying: I have not left after me any (chance) of turmoil more injurious to men than the harm done to the men because of women.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6604"> Book 036, Number 6604: </a> </em>
        <blockquote>
          <p> Usama b. Zaid b. Harith and Sa'id b. Zaid b. 'Amr b. Naufal both reported Allah's Messenger (may peace be upon him) as saying: I have not left after me turmoil for the people but the harm done to men by women.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6605"> Book 036, Number 6605: </a> </em>
        <blockquote>
          <p> This hadith has been narrated on the authority of Sulaiman Taimi with the same chain of transmitters.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6606"> Book 036, Number 6606: </a> </em>
        <blockquote>
          <p> Abu Sa'id Khudri reported that Allah's Messenger (may peace be upon him) said: The world is sweet and green (alluring) and verily Allah is going to install you as vicegerent in it in order to see how you act. So avoid the allurement of women: verily, the first trial for the people of Isri'll was caused by women. And in the hadith transmitted on the authority of Ibn Bashshar the words are:" So that He should see how you act."</p>
        </blockquote>
        <!-- EH -->
        <br>
        <p><strong> Chapter 2: THE STORY OF THE THREE PERSONS OF THE CAVE AND THEIR MAKING GOOD DEEDS OF THEIRS AS THE MEANS OF RIDDANCE </strong></p>
        <!-- BH -->
        <hr>
        <em> <a name="036.6607"> Book 036, Number 6607: </a> </em>
        <blockquote>
          <p> 'Abdullah b. 'Umar reported that Allah's Messenger (may peace be upon him) said: Three persons set out on a journey. They were overtaken by rain and they had to find protection in a mountain cave where at its mouth there fell a rock of that mountain and thus blocked them altogether. One of them said to the others: Look to your good deeds that you performed for the sake of Allah and then supplicate Allah, the Exalted, that He might rescue you (from this trouble). One of them said: 0 Allah, I had my parents who were old and my wife and my small children also. I tended the flock and when I came back to them in the evening, I milked them (the sheep, goats, cows, etc.) and first served that milk to my parents. One day I was obliged to go out to a distant place in search of fodder and I could not come back before evening and found them (the parents) asleep. I milked the animals as I used to milk and brought milk to them and stood by their heads avoiding to disturb them from sleep and I did not deem it advisable to serve milk to my children before serving them. My children wept near my feet. I remained there in that very state and my parents too until it was morning. And (0 Allah) if Thou art aware that I did this in order to seek Thine pleasure, grant us riddance from this trouble. (The rock slipped a bit) that they could see the sky. The second one said: 0 Allah, I had a female cousin whom I loved more than the men love the women. I wanted to have sexual intercourse with her; she refused but on the condition of getting one hundred dinirs. It was with very great difficulty that I could collect one hundred dinirs and then paid them to her and when I was going to have a sexual intercourse with her, that she said: Servant of Allah, fear Allah and do not break the seal (of chastity) but by lawful means. I got up. 0 Allah, if Thou art aware that I did this in order to seek Thine pleasure, rid us from this trouble. The situation was somewhat eased for them. The third one said: Allah, I employed a workman for a measure of rice. After he had finished his work I gave him his dues (in the form of) a measure of rice, but he did not accept them. I used these rice as seeds, and that gave a bumper crop and I became rich enough to have cows and flocks (in my possession). He came to me and said: Fear Allah, and commit no crueltv upon me in regard to my dues. I said to him: Takeaway this flock of cows and sheep. He said: Fear Allah and do not make a fun of me. I said: I am not making a fun of you. You take the cows and the flocks. So he took them. 0 Allah, if Thou art aware that I did it for Thine pleasure, case the situation for us. And Allah relieved them from the rest of the trouble.</p>
        </blockquote>
        <!-- EH -->
        <!-- BH -->
        <hr>
        <em> <a name="036.6608"> Book 036, Number 6608: </a> </em>
        <blockquote>
          <p> This hadith has been transmitted on the authority of Musa b. 'Uqba but with a slight variation of wording.</p>
        </blockquote>
        <!-- EH -->
