        <hr>
        <em> <a name="036.6609"> Book 036, Number 6609: </a> </em>
        <blockquote>
          <p> Abdullah b 'Umar reported: I heard Allah's Alessenger (may peace be upon him) as saying: Three persons belonging to the earlier Ummahs set out on a journey until they had to spend a night in a cave. The rest of the hadith is the same and the additional words are:" A person amongst them said: 0 Allah, I had my aged parents and I served them milk before I (served that) to my wife, children and my servants." And in case of the second one, the words are:" She avoided me until she was hard pressed because of famine and she came to me and I gave her one hundred and twenty diiiirs" And in cease of the third one (the words are):" I invested his wages, and it brought profit and, as a result thereof, the merchandise increased and there was abundance of goods." And he (the narrator said) that they got out of the cave and began to walk.</p>
        </blockquote>
