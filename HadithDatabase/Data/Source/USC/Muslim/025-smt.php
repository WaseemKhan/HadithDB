      <em> <a name="025.5323"> Book 025, Number 5323: </a> </em>
      <blockquote>
        <p> Jabir b. 'Abdullah reported: A child was born in the house of a person amongst us, and he gave him the name of Qasim. We said: We will not allow you (to give the name) to your child as Qasim (and thus adopt the kunya of Abu'l-Qasim) and coal your eyes. He (that person) came to Allah's Apostle (may peace be upon him) and made a mention of that to him, whereupon he said: Call your son 'Abd al-Rahman.</p>
