      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 25: </a> </em>
      <h1> <em> The Book on General Behaviour (Kitab Al-Adab) </em> </h1>
      <br>
      <p><strong> Chapter 1: FORBIDDANCE IN CALLING ONE (OTHER THAN ALLAH'S APOSTLE) WITH THE KUNYA OF ABU'L-QASIM AND THE FACT PERTAINING TO GOOD NAMES </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="025.5314"> Book 025, Number 5314: </a> </em>
      <blockquote>
        <p> Anas reported that person at Baqi' called another person as" Abu'l- Qasim," and Allah's Messenger (may peace be upon him) turned towards him. He (the person who had uttered these words) said: Messenger of Allah, I did not mean you, but I called such and such (person), whereupon Allah's Messenger (may peace be upon him) said: You may call yourself by my name, but not by my kunya.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5315"> Book 025, Number 5315: </a> </em>
      <blockquote>
        <p> Ibn Umar reported that Allah's Messenger (may peace be upon him) said: The names dearest to Allah are 'Abdullah and 'Abd al-Rahman.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5316"> Book 025, Number 5316: </a> </em>
      <blockquote>
        <p> Jabir b. 'Abdullah reported that a child was born to a perfon amongst us and he gave him the name of Muhammad. Thereupon his people said: We will not allow You to give the name of Muhammad (to your child) after the name of Allah's Messenger (may peace be upon him). He set forth with his son carrying him on his back and came to Allah's Apostle (may peace be upon him', and said: Allah's Messenger a son has been born to me and I havegiven him the name after the name of Allah's Messenger (may peace be upon him), whereupon Allah's Messenger (may peace be upon him) said: Give him my name but do not give him my kunya, for I am Qasim in the sense that I distribute (the spoils of war) and the dues of Zakat amongst you.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5317"> Book 025, Number 5317: </a> </em>
      <blockquote>
        <p> Jabir b. 'Abdullah reported that a child was born to one of the persons amongst us and he decided to give him the name of Muhammad We said: We will not allow you to give the name after the name of Allah's Messenger (may peace be upon him) until you ask him (the Holy Prophet). So he (that person) came and said (to the Holy Prophet): A child was born in my house and I wanted to give him the name (of Muhammad) after the name of Allah's Messenger, whereas my people did not allow me that I should name him after that (sacred) name until I have asked Allah's Apostle (may peace be upon him) in this connection, whereupon he said: Give him the name after my name, but do not call him by my kunya, for I have been sent as a Qasim as I distribute amongst you.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5318"> Book 025, Number 5318: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of Husain With the same chain of transmitters but no mention is made of these words:" (I have been sent as a distributor), so I distribute amongst you."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5319"> Book 025, Number 5319: </a> </em>
      <blockquote>
        <p> Jabir b. Abdullah reported Allah's Messenger (may peace be upon him) as saying: Give the name after my name, but do not give (the kunya of Abu'l-Qasim after my) kunya, for I am Abu'l-Qasim (in the sense) that I distribute amongst you (the spoils of war) and disseminate the knowledge (of revelation). This hadith has been transmitted on the authority of Abu Bakr but with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
