      <em> <a name="025.5325"> Book 025, Number 5325: </a> </em>
      <blockquote>
        <p> Abu Huraira reported that Abu'l-Qasim (may peace be upon him) said: Give name (to your children) after my name but do not give the kunya (of Abu'l- Qasim) after my kunya. 'Amr reported from Abu Huraira that he did not say that he had heard it directly from Allah's Apostle (may peace be upon him).</p>
      </blockquote>
