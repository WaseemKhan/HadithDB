      <em> <a name="025.5326"> Book 025, Number 5326: </a> </em>
      <blockquote>
        <p> Mughira b. Shu'ba reported: When I came to Najran, they (the Christians of Najran) asked me: You read" O sister of Harun" (i. e. Hadrat Maryam) in the Qur'an, whereas Moses was born much before Jesus. When I came back to Allah's Messenger (may peace be upon him) I asked him about that, whereupon he said: The (people of the old age) used to give names (to their persons) after the names of Apostles and pious persons who had gone before them.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: DISAPPROVAL OF GIVING UGLY NAMES AND SUCH NAMES AS CAN BE TREATED AS ILL OMENS </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="025.5327"> Book 025, Number 5327: </a> </em>
      <blockquote>
        <p> Samura b. Jundub reported: Allah's Messenger (may peace be upon him) forbade us to give names to our servants as these four names: Aflah (Successful), Rabdh (Profit), Yasar (Wealth), and Nafi' (Beneficial).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5328"> Book 025, Number 5328: </a> </em>
      <blockquote>
        <p> Samura b. Jundub reported AUah's Messenger (may peace be upon him) as saying: Don't give names to your servants as Rabdh, 'Ya ar, Aflah and Nafi'.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5329"> Book 025, Number 5329: </a> </em>
      <blockquote>
        <p> Samura b. Jundub reprted: The dearest words to Allah are four: Subhan Allah (Hallowed be Allah), Al-Hamdulillah (Praise be to Allah), Wa la ilaha illa-Allah (There is no god but Allah), Allah-o-Akbar (God is the Greatest). There is no harm for you in which order you begin (them while remembering Allah), and he also said: Do not give these names to your servants, Yasar and Rabah and Nafi and Najth.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5330"> Book 025, Number 5330: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of Shu'ba and there is no mention but of the fact about giving the name to the servant and there is no mention of the four expressions (of remembrance) and he did not mention the four words</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5331"> Book 025, Number 5331: </a> </em>
      <blockquote>
        <p> Jabir b. 'Abdullah reported: Allah's Messenger (may peace be upon him) decided to forbid (his followers) to name persons as Ya'la (Elevated), Baraka (Blessing), Aflah (Successful), Yasar and Nafi', but I saw that he kept silent after that and he did not say anything until Allah's Messenger (may peace be upon him) died. And he did not forbid (his followers to do this), then 'Umar decided to prohibit (people) from giving these names, but later on gave up the idea.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: EXCELLENCE OF CHANGING UGLY NAMES TO GOOD NAMES </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="025.5332"> Book 025, Number 5332: </a> </em>
      <blockquote>
        <p> Ibn 'Umar reported that Allah's Messenger (may peace be upon him) changed the name of 'Asiya (Disobedient) and said: You are Jamila (i. e. good and handsome). Ahmad (one of the narrators) narrated it with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5333"> Book 025, Number 5333: </a> </em>
      <blockquote>
        <p> Ibn 'Umar reported that 'Umar had a daughter who was called 'Asiya. Allah's Messenger (may peace be upon him) gave her the name of Jamila.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5334"> Book 025, Number 5334: </a> </em>
      <blockquote>
        <p> Ibn Abbas reported that the name of Juwairlya (the wife of the Holy Prophet) was Barra (Pious). Allah's Messenger (may peace be upon him) changed her name to Juwairiya and said: I did not like that it should be said: He had come out from Barra (Pious). The hadith transmitted on the authority of Ibn Abi 'Umar is slightly different from it.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5335"> Book 025, Number 5335: </a> </em>
      <blockquote>
        <p> Abu Huraira reported that the name of Zainab was Barra. It was said of her: She presents herself to be innocent. Allah's Messenger (may peace be upon him) gave her the name of Zainab.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5336"> Book 025, Number 5336: </a> </em>
      <blockquote>
        <p> Zainab, daughter of Umm Salama, reported: My name first was Barra. Allah's Messenger (may peace be upon him) gave me the name of Zainab. Then there entered (into the house of Allah's Prophet as a wife) Zainab, daughter of Jahsh, and her name was also Barra, and he gave her the name of Zainab.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5337"> Book 025, Number 5337: </a> </em>
      <blockquote>
        <p> Muhammad b. 'Amr b. 'Ata' reported: I had given the name Barra to my daughter. Zainab, daughter of Abu Salama, told me that Allah's' Messenger (may peace be upon him) had forbidden me to give this name. (She said): I was also called Barra, but Allah's Messenger (may peace be upon him) said: Don't hold yourself to be pious. It is God alone who knows the people of piety among you. They (the Companions) said: Then, what name should we give to her? He said: Name her as Zainab.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 4: IT IS FORBIDDEN TO CALL THE RULER AS THE KING OF KINGS OR SHAHINSHAH </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="025.5338"> Book 025, Number 5338: </a> </em>
      <blockquote>
        <p> Abu Huraira reported Allah's Messenger (may peace be upon him) as saying: The vilest name in Allah's sight is Malik al-Amidh (King of Kings). The narration transmitted on the authority of Shaiba (contains these words): There is no king but Allah, the Exalted and Glorious. Sufyan said: Similarly, the word Shahinshah (is also the vilest appellation). Ahmad b. Hanbal said: I asked Abu 'Amr about the meaning of Akhna. He said: The vilest.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5339"> Book 025, Number 5339: </a> </em>
      <blockquote>
        <p> Abu Huraira reported from Allah's Messenger (may peace be upon him) so many ahadith and one of them was this that Allah's Messenger (may peace be upon him) said: The most wretched person in the sight of Allah on the Day of Resurrection and the worst person and target of His wrath would of the person who is called Malik al-Amlak (the King of Kings) for there is no king but Allah.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 5: THE EXCELLENCE OF TAHNIK OF THE NEW-BORN CHILD AT THE TIME OF ITS BIRTH. AND, OF CARRYING IT TO A PIOUS PERSON SO THAT HE MAY SOVTEN SOME DATES AND RUB ITS PALATE WITH THEM, AND PERMISSIBILITY OF GIVING THE NAME ON THE DAY OF ITS BIRTH AND THE EXCELLENCE OF SUCH NAMES AS 'ABDULLAH, IBRAHIM AND THE NAMES OF ALL THE APOSTLES OF ALLAH (PEACE BE UPON THEM) </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="025.5340"> Book 025, Number 5340: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported: I took 'Abdullah b. Abi Talha Ansari to Allah's Messenger (may peace be upon him) at the time of his birth. Allah's Messenger (may peace be upon him) was at that time wearing a woollen cloak and besmearing the camels with tar. He said: Have you got with you the dates? I said: Yes. He took hold of the dates and put them in his mouth and softened them, then opened the mouth of the infant and put that in it and the child began to lick it. Thereupon Allah's Messenger (may peace be upon him) said: The Ansar have a liking for the dates, and he (the Holy Prophet) gave him the name of 'Abdullah.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5341"> Book 025, Number 5341: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported that the son of Abu Talha had been ailing. Abu Talha set out (on a journey) and his son breathed his last (in his absence). When Abu Talha came back, he said (to his wife): What about my child? Umm Sulaim (the wife of Abu Talha) said: He is now in a more comfortable state than before. She served him the evening meal and he took it. He then came to her (and had sexual intercourse with her) and when it was all over she said: Make arrangements for the burial of the child. When it was morning. Abu Talha came to Allah's Messenger (may peace be upon him) and informed him, whereupon he said: Did you spend the night with her. He said: Yes. He (the Holy Prophet) then said: O Allah, bless both of them (and as a result of blessing) she gave birth to a child. Abu Talha said to me (Anas b. Malik) to take the child, (so I took him) and came to Allah's Messenger (may peace be upon him). She (Umm Sulaim) also had sent some dates (along with the child). Allah's Apostle (may peace be upon him) took him (the child) (in his lap) and said: Is there anything with you (for Tahnik). They (the Companions) said: Yes. Allah's Apostle (may peace be upon him) took hold of them (dates and chewed them). He then put them (the chewed dates) in the mouth of the child and then rubbed his palate and gave him the name of 'Abdullah.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5342"> Book 025, Number 5342: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of Anas through another chain of transmit ters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5343"> Book 025, Number 5343: </a> </em>
      <blockquote>
        <p> Abu Musa reported: A child was born in my house and I brought him to Allah's Apostle (may peace be upod him) and he gave him the name of Ibrahim and he rubbed his palate with dates.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="025.5344"> Book 025, Number 5344: </a> </em>
      <blockquote>
        <p> 'Urwa b. Zubair and Fatima daughter of Mandhir b. Zubair, reported that Asma' daughter of Abu Bakr was at the time of migration in the family way with 'Abdullah b. Zubair (in her womb). She came to Quba' and gave birth to 'Abdullah at that place and then sent him to Allah's Messenger (may peace be upon him) so that he should rub his palate with chewed dates. Allah's Messenger (may peace he upon him) took hold of him (the child) and he placed him in his lap and then called for dates. 'A'isha said: Some time was spent before we were able to find them. He (the Holy Prophet) chewed them and then put his saliva in his mouth. The first thing that entered his stomach, was the saliva of Allah's Messenger (may peace be upon him). Asma' said: He then rubbed him and blessed him and gave him the name of Abdullah. He ('Abdullah) went to him (the Holy Prophet) when he had attained the age of seven or eight years in order to pledge allegiance to Allah's Messenger (may peace be upon him) as Zubair had commanded him to do. Allah's Messenger (may peace be upon him) smiled when he saw him coming towards him and then accepted his allegiance.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
