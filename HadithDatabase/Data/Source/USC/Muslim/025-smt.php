      <em> <a name="025.5320"> Book 025, Number 5320: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of A'mash with the same chain of transmitters but there is a slight variation (of wording) that, instead of the word Bu'ithat (I have been sent), the word ju'ilat (I have been made) has been used.</p>
      </blockquote>
