      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 43: </a> </em>
      <h1> <em> The Book of Commentary (Kitab Al-Tafsir) </em> </h1>
      <br>
      <p><strong> Chapter 1: </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="043.7151"> Book 043, Number 7151: </a> </em>
      <blockquote>
        <p> Hammim b. Munabbih reported: This is what Abu Huraira reported to us from Allah's Messenger (may peace be upon him) and in this connection he narrated some of the ahadith and Allah's Messenger (may peace be upon him) said: It was said to people of Israel: Enter this land saying Hitta (Remove Thou from us the burden of our sins), whereupon We would forgive you your sins, but they twisted (this statement) and entered the gate dragging upon their breech and said: The" grain in the ear."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7152"> Book 043, Number 7152: </a> </em>
      <blockquote>
        <p> Anas b. Malik reported that Allah, the Exalted and Glorious, sent revelation to Allah's Messenger (may peace be upon him) just before his death in quick succession until he left for his heavenly home, and the day when he died, he received the revelation profusely.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7153"> Book 043, Number 7153: </a> </em>
      <blockquote>
        <p> Tariq b. Shihab reported that a Jew said to'Umar: You recite a verse which, if it had been revealed in relation to us, we would have taken that day as the day of rejoicing. Thereupon 'Umar said: I know where it was revealed and on the day when it was revealed and where Allah's Messenger (may peace be upon him) had been at that time when it was revealed. It was revealed on the day of 'Arafa (ninth of Dhu'l Hijjah) and Allah's Messenger (may peace be upon him) had been staying in 'Arafat. Sufyan said: I doubt, whether it was Friday or not (and the verse referred to) is this:" Today I have perfected your religion for you and completed My favours upon you" (v. 4).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7154"> Book 043, Number 7154: </a> </em>
      <blockquote>
        <p> Tariq b. Shihab reported that a Jew said to 'Umar: If this verse were revealed in relation to the Jews (i. e." This day I have perfected your religion for you and have completed My favours for you and have chosen for you al-Islam as religion" ) we would have taken the day of rejoicing on which this verse was revealed. Thereupon 'Umar said: I know the day on which it was revealed and the hour when it was revealed and where Allah's Messenger (may peace be upon him) had been when it was revealed. It was revealed on the night of Friday and we were in 'Arafat with Alleh's Messenger (may peace be upon him) at that time.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7155"> Book 043, Number 7155: </a> </em>
      <blockquote>
        <p> Tariq b. Shihab reported that a Jew came to 'Umar and said: Commander of the Faithful, there is a verse in your Book, which you recite. Had it been revealed in connection with the Jews, we would have taken it as the day of rejoicing. Thereupon he said: Which verse do you mean? He replied:" This day I have perfected your religion for you and I have completed My favours upon you and I have chosen al-Islam as religion for you." Umar said, I know the day when it was revealed and the place where it was revealed. It was revealed to Allah's Messenger (may peace be upon him) at 'Arafat on Friday.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7156"> Book 043, Number 7156: </a> </em>
      <blockquote>
        <p> 'Urwa b. Zubair reported that he asked 'A'isha about the words of Allah:" If you fear that you will not be able to maintain equity amongst the orphan girls, then marry (those) you like from amongst the women two, three or four." She said: O, the son of my sister, the orphan girl is one who is under the patronage of her guardian and she shares with him in his property and her property and beauty fascinate him and her guardian makes up his mind to marry her without giving her due share of the wedding money and is not prepared (to pay so much amount) which anyone else is prepared to pay and so Allah has forbidden to marry these girls but in case when equity is observed as regards the wedding money and they are prepared to pay them the full amount of the wedding money and Allah commanded to marry other women besides them according to the liking of their heart. 'Urwa reported that 'A'isha said that people began to seek verdict from Allah's Messenger (may peace be upon him) after the revelation of this verse about them (orphan girls) and Allah, the Exalted and Glorious, revealed this verse:" They asked thee verdict about women; say: Allah gives verdict to you in regard to them and what is recited to you in the Book about orphan woman, whom you give not what is ordained for them while you like to marry them" (iv. 126). She said: The wording of Allah" what is recited to you" in the Book means the first verse, i. e." if you fear that you may not be able to observe equity in case of an orphan woman, marry what you like in case of woman" (iv. 3). 'A'isha said: (And as for this verse [iv. 126], i. e. and you intend" to marry one of them from amongst the orphan girls" it pertains to one who is in charge (of orphans) having small amount of wealth and less beauty and they have been forbidden that they should marry what they like of her wealth and beauty out of the orphan girls, but with equity, because of their disliking for them.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7157"> Book 043, Number 7157: </a> </em>
      <blockquote>
        <p> 'Urwa reported that he asked 'A'isha about the words of Allah:" If you fear that you will not be able to observe equity in case of orphan girls" ; the rest of the hadith is the same but with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7158"> Book 043, Number 7158: </a> </em>
      <blockquote>
        <p> 'A'isha said that as for the words of Allah:" If you fear that you would not be able to observe equity in case of orphan girls)," it was revealed in reference to a person who had an orphan girl (as his ward) and he was her guardian, and her heir, and she possessed property, but there was none to contend on her behalf except her ownself. And he (her guardian) did not give her in marriage because of her property and he tortured her and ill-treated her, it was in relation to her that (Allah said: )" If you fear that you would not be able to observe equity in case of orphan girls, then marry whom you like among women," i. e. whatever I have made lawful for you and leave her whom you are putting to torture.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7159"> Book 043, Number 7159: </a> </em>
      <blockquote>
        <p> 'A'isha said in connection with His words (those of Allah):" What is recited to you in the Book about orphan women whom you give not what is ordained for them, while you like to marry them," these were revealed in connection with an orphan girl who was in the charge of the person and she shared with him in his property and he was reluctant to marry her himself and was also unwilling to marry her to someone else (fearing) that (that person) would share in his property (as the husband of that girl), preventing her to marry, neither marrying her himself nor marrying her to another person.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7160"> Book 043, Number 7160: </a> </em>
      <blockquote>
        <p> Hisham reported that 'A'isha said in connection with the words of Allah:" They ask thee the religious verdict about women, say: Allah gives you the verdict about them" (iv. 126), that these relate to an orphan girl who is in charge of the person and she shares with him in his property (as a heir) even in the date-palm trees and he is reluctant to give her hand in marriage to any other person lest he (her husband) should partake of his property, and thus keep her in a lingering state.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7161"> Book 043, Number 7161: </a> </em>
      <blockquote>
        <p> Hisham reported on the authority of his father that 'A'isha said in connection with His (Allah's) words:" And whoever is poor let him take reasonably (out of it)" that it was revealed in connection with the custodian of the property of an orphan, who is in charge of her and looks after her; In case he is poor, he is allowed to eat out of that.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7162"> Book 043, Number 7162: </a> </em>
      <blockquote>
        <p> 'A'isha reported in connection with the words of Allah, the Exalted:" He who is rich should abstain, and he who is poor may reasonably eat (out of it)" that this was revealed in relation to the guardian of an orphan who is poor; he may get out of that what is reasonable keeping in view his own status of solvency.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7163"> Book 043, Number 7163: </a> </em>
      <blockquote>
        <p> This hadith has been narrated on the authority of Hisham with the same chain of transmitters.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7164"> Book 043, Number 7164: </a> </em>
      <blockquote>
        <p> 'A'isha reported that these words of Allah:" When they came upon you from above you and from below you and when the eyes turned dull and the hearts rose up to the throats" (xxxiii. 10) pertain to the day of Ditch.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7165"> Book 043, Number 7165: </a> </em>
      <blockquote>
        <p> 'A'isha said in connection with the verse:" And if a woman has reason to fear ill-treatment from her husband or that he might turn away from her" (iv. 128) that it was revealed in case of a woman who had long association with a person (as his wife) and now he intends to divorce her and she says: Do not divorce me, but retain me (as wife in your house) and you are permitted to live with another wife. It is in this context that this verse was revealed.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7166"> Book 043, Number 7166: </a> </em>
      <blockquote>
        <p> 'A'isha said in connection with these words of Allah, the Exalted and Glorious:" And if a woman has reason to fear ill-treatment from her husband or that he might turn away from her" that it was revealed in case of a woman who lived with a person and perhaps he does not want to prolong (his relationship with her) whereas she has had sexual relationship with him (and as a result thereof) she got a child from him and she does not like that she should be divorced, so she says to him: I permit you to live with the other wife.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7167"> Book 043, Number 7167: </a> </em>
      <blockquote>
        <p> 'Urwa reported on the authority of his father that 'A'isha said to him: O, the son of my sister, the Muslims were commanded to seek forgiveness for the Companions of Allah's Apostle (may peace be upon him) but they reviled him.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7168"> Book 043, Number 7168: </a> </em>
      <blockquote>
        <p> This hadith has been transmitted on the authority of Abu Usama with the same chain of narrators.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7169"> Book 043, Number 7169: </a> </em>
      <blockquote>
        <p> Sa'id b. Jubair reported: The inhabitants of Kufa differed in regard to this verse:" But whoever slays another believer intentionally, his requital shall be Hell" (iv. 92), so I went to Ibn 'Abbas and asked him about it, whereupon he said: This has been revealed and nothing abrogated it.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7170"> Book 043, Number 7170: </a> </em>
      <blockquote>
        <p> This hadith has been transmitted on the authority of Shu'ba with the same chain of narrators but with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7171"> Book 043, Number 7171: </a> </em>
      <blockquote>
        <p> Sa'id b. Jubair reported: 'Abd al Rahman b. Abzi commanded me that I should ask Ibn 'Abbas about these two verses:" He who slays a believer intentionally his requital shall be Hell where he would abide for ever" (iv. 92). So, I asked him and he said: Nothing has abrogated it. And as for this verse:" And they who call not upon another god with Allah and slay not the soul which Allah has forbidden, except in the cause of justice" (xxv. 68), he (Ibn Abbas) said: This has been revealed in regard to the polytheists."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7172"> Book 043, Number 7172: </a> </em>
      <blockquote>
        <p> Ibn 'Abbas said: This verse was revealed in Mecca:" And they who call not upon another god with Allah and slay not the soul which Allah has forbidden except in the cause of justice" up to the word Muhdana (abased). Thereupon the polytheists said: Islam is of no avail to us for we have made peer with Allah and we killed the soul which Allah had forbidden to do and we committed debauchery, and it was (on this occasion) that Allah, the Exalted and Glorious, revealed this verse:" Except him who repents and believes and does good deeds" up to the end Ibn 'Abbis says: He who enters the fold of Islam and understands its command and then kills the soul there is no repentance for him.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7173"> Book 043, Number 7173: </a> </em>
      <blockquote>
        <p> Sa'id b. Jubair reported: I said to Ibn Abbas: Will the repentance of that person be accepted who kills a believer intentionally? He said: No. I recited to him this verse of Sura al-Furqan (xix.):" And those who call not upon another god with Allah and slay not the soul which Allah has forbidden except in the cause of justice" to the end of the verse. He said: This is a Meccan verse which has been abrogated by a verse revealed at Medina:" He who slays a believer intentionally, for him is the requital of Hell-Fire where he would abide for ever," and in the narration of Ibn Hisham (the words are): I recited to him this verse of Sura al-Furqan:" Except one who made repentance."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7174"> Book 043, Number 7174: </a> </em>
      <blockquote>
        <p> Ubaidullah b. 'Abdullah b. 'Utba reported: Ibn Abbas said to me: Do you know-and in the words of Harun (another narrator): Are you aware of-the last Sura which was revealed in the Qur'an as a whole? I said: Yes," When came the help from Allah and the victory" (cx.). Thereupon, he said: You have told the truth. And in the narration of Abu Shaiba (the words are): Do you know the Sura? And he did not mention the words" the last one".</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7175"> Book 043, Number 7175: </a> </em>
      <blockquote>
        <p> This hadith has been reported on the authority of Abu 'Umais through the same chain of transmitters but with a slight variation of wording.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7176"> Book 043, Number 7176: </a> </em>
      <blockquote>
        <p> Ibn Abbas reported that some Muslims met a person with a small flock of sheep. He said: As-Salam-o-'Alaikum. They caught hold of him and killed him and took possession of his flock. Then this verse was revealed:" He who meets you and extends you salutations, don't say: You are not a Muslim" (iv. 94). Ibn 'Abbas, however, recited the word as-Salam instead of" as-Salam".</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7177"> Book 043, Number 7177: </a> </em>
      <blockquote>
        <p> Bara' reported: When the Ansar performed the Pilgrimage, they did not enter their houses but from behind. A person from the Ansar came and he began to enter from his door but it was said to him (why he was doing something in contravention to the common practice of coming to the houses from behind). Then this verse was revealed." Piety is not that you come to the doors from behind" (ii. 189).</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: PERTAINING TO THE VERSE: 'HAS NOT THE TIME YET COME FOR THE BELIEVERS THAT THEIR HEARTS SHOULD BE HUMBLE FOR THE REMEMBRANCE OF ALLAH?" (lvii. 16) </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="043.7178"> Book 043, Number 7178: </a> </em>
      <blockquote>
        <p> Ibn Mas'ud said: Since our acceptance of Islam and the revelation of this verse in which Allah has shown annoyance to us:" Has not the time yet come for the believers that their hearts should be humble for the remembrance of Allah?" (lvii. 16), there was a gap of four years.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: PERTAINING TO THE WORDS OF ALLAH:" ADORN YOURSELF AT EVERY PLACE OF WORSHIP" (vii. 31) </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="043.7179"> Book 043, Number 7179: </a> </em>
      <blockquote>
        <p> Ibn Abbas reported: During the pre-Islamic days women circumambulated the Ka'ba nakedly, and said: Who would provide cloth to cover the one who is circumambulating the Ka'ba so that she would cover her private parts? And then she would say: Today will be exposed the whole or the part and what is exposed I shall not make it lawful. It was in this connection that the verse was revealed:" Adorn yourself at every place of worship" (vii. 31).</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 4: COMPEL NOT YOUR SLAVE-GIRLS TO PROSTITUTION </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="043.7180"> Book 043, Number 7180: </a> </em>
      <blockquote>
        <p> Jabir reported that 'Abdullah b. Ubayy b. Salul used to say to his slave-girl: Go and fetch something for us by committing prostitution. It was in this connection that Allah, the Exalted and Glorious, revealed this verse:" And compel not your slave-girls to prostitution when they desire to keep chaste in order to seek the frail goods of this world's life, and whoever compels them, then surely after their compulsion Allah is Forgiving, Merciful" (xxiv. 33).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="043.7181"> Book 043, Number 7181: </a> </em>
      <blockquote>
        <p> Jabir reported that 'Abdullah b. Ubayy b. Salul had two slave-girls; one was called Musaika and the other one was called Umaima and he compelled them to prostitution (for which'Abdullah b. Ubayy b. Salul compelled them). They made a complaint about this to Allah's Messenger (may peace be upon him) and it was upon this that this verse was revealed:" And compel not your slave-girls to prostitute" up to the words:" Allah is Forgiving, Merciful."</p>
      </blockquote>
