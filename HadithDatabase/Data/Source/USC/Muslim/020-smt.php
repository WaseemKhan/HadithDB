      <div class="content"><em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 20: </a> </em>
        <h1> <em> The Book on Government (Kitab Al-Imara) </em> </h1>
        <br>
        <p><strong> INTRODUCTION </strong></p>
        <p> Islam is a social religion, not in the sense that it is through society alone that religion breathes its sacred spirit in the individuals, but in the sense that it aims at inculcating God-consciousness both in individual and society. Jesus said: The Kingdom of Heaven is within you. Islam fully subscribes to this view, since no just kingdom can be founded on earth by unjust men, who have not first created the Kingdom of Heaven in their hearts, but Islam goes a step forward and says that the creation of the Kingdom of Heaven within heart is not enough; this Kingdom of Heaven within must be externalised into a Kingdom of Heaven on earth, so that the organised life of man may be built up on the basis of love, fraternity and justice. </p>
        <p> It thus becomes clear that the Islamic State is not an end in itself, but a means to an end, the end being the development of a community of people who stand up for equity and justice, for right against wrong or, to phrase it differently, for the creation of such conditions as would enable the greatest possible number of human beings to live spiritually, morally and physically in accordance with the teachings of Islam. </p>
        <p> Thus the founding of an Islamic State is not a sort of" worldly activity," something undesirable, as some people wrongly suppose, but the sine qua non of social justice, as demanded by Islam." The state," says Dr lqbal," from the Islamic standpoint, is an endeavour to transform these ideal principles into space-time forces, an aspiration to realize them in a definite human organization. It is in this sense alone that state in Islam is a theocracy, not in the sense that it is headed by a representative of God on earth who can always screen his despotic will behind his supposed infallibility. </p>
        <p> As this State is meant to enforce the law of the Shari'ah within its territorial jurisdiction, it is duty bound to make itself an efficient organ for transforming the high ideals of Islam into reality. The Qur'an, while enumerating the main functions of the Islamic State, says:" Those who, if We establish them in the land, observe worship, and pay the poor-due and enjoin good and forbid evil" (xxii. 41). </p>
        <p> This shows that the function of an Islamic State is not only to defend its citizens from external attack and internal disorder, but also to enable individual man and woman to realise the tenets of Islam and their beliefs. in the socioeconomic concerts of their practical life. </p>
        <p> So far as the nature of the Islamic State is concerned, it is theocracy with regard to God in the sense that the de jure sovereignty belongs to Allah Whose de facto sovereignty is inherent and manifest in the working of the entire universe and Who enjoys exclusively the sovereign prerogative over all the creation. The Holy Qur'an has stressed this point in so many verses: </p>
        <p> " The Command is for none but for Allah: He has commanded that ye obey </p>
        <p> none but Him: that is the right path" (xii. 40). </p>
        <p> " Follow the revelation sent unto you from your Lord, and do not follow the (so-called) guardians other than Him" (vii. 3). </p>
        <p> " And those who do not make their decisions in accordance with that revealed by Allah are (in fact) the deniers of Truth" (v. 44). It thus becomes quite clear that a State established on the basis of God's sovereignty cannot enforce any law in contravention of the Qur'an and the Sunnah even if all the citizens make a demand for it. An Islamic State is, therefore, theocratic in one aspect as it is run according to God-given laws, but it is altogether a differmt theocracy of which Europe has had the bitter experience and in which, a priestly clan is sharply marked off from the rest of the population and exercises an unchecked domination and enforces laws of its own making in the name of God, and thus imposes its own godhood upon the common people. The priest puts himself as a mediator between the masses and the unseen God. Such a system is quite un-Islamic. The theocracy built by Islam is not ruled by a particular religious class, but by the whole community of Muslims including the rank and file. as Allah has not appointed a particular individual, group, race or class as the representative of the Real Sovereign upon the earth, but the whole community. The Qur'an says:" Allah has promised to those among you who believe and do righteous deeds that He will assuredly make them to succeed (the present) rulers and grant them vicegerency in the land just as He made those before them to succeed (others)" (ixiv. 55). This verse makes it clear that all believers have been conferred upon the Caliphate and not a special class or dynasty. Moreover, the Caliphate granted by Allah to the faithful is the popular vicegerency and not a limited one and thus it is run with the consent of the people. The concepts of the Divine Right of Kingship and the infallibility of clergy are alien to the spirit of Islam. Every Muslim stands on equal level with the other Muslim as the vicegerent of the Lord upon the earth. None of them, therefore, enjoys any preference to the other by virtue of high birth or belonging to a special religious group. History is teplete with instances of Caliphs being brought to the court and publicly criticised by ordinary men and women. Even a lowliest villager could dare tell the Commander of the Faithful that he would set him straight like a spindle if he deviates an inch from the poth of righteousness. This shows that the rulers and the officials in the Islamic State are answerable both to God and man for their actions, and the people have every right to criticise. not only their public behaviour, but even their private activities. </p>
        <p> In the context of such heavy responsibilities the heading of the Islamic State or assumption of any other important portfolio in its set-up is a very difficult job so much so that that Holy Prophet (may peace be upon him) compared it to the slaughtering of one with a blunt knife. No person, therefore. who is conscious of the responsibilities of the job, covets to assume that; he rather shirks it and accepts it only when he is compelled to do so. The Holy Prophet (may peace be upon him) declared that person to be quite unfit for office in an Islamic State who is covetous of it, as he is either ignorant of its grave responsibilities or desires to take undue advantage of his official power. </p>
        <p> We enumerate below some of the important rights and duties of an Islamic State. </p>
        <p> As for the rights, the Islamic State can claim full loyalty from all its citizen wholehearted cooperation in all sectors of life and complete obedience to the law of the land and the commands of the rulers so long as they are in comformity with the Laws of the Shari'ah. But where the rulers transgress the limits of the citizens have the right, not only to withdraw co-operation, but also to check the rulers from acting against the Commands of Aliah and His Apostle (may peace be upon him). </p>
        <p> The citizens are also required to pay taxes due to the State quite willingly and not to misappropriate its funds, and to participate in Jihad in any capacity for which they are deemed fit by the authorities of the Islamic State. </p>
        <p> So far as the duties of the Islamic State are concerned, the first and foremost duty is to protect the life, property and honour of every citizen, irrespective of his caste, creed, colour, religion or social status. </p>
        <p> Moreover, it is also the duty of the Islamic State to protect the freedom and individual liberty of every citizen so long as there is no valid reason to curtail it. It cannot be done as an arbitrary act. The State is authorised to lay hands upon it through due process of law. </p>
        <p> Again, freedom of expression is one of the fundamental rights of the citizen which an Islamic State must respect. </p>
        <p> Then, it is also the duty of the Islamic State to see that no citizen remains unprovided for in respect of the basic necessities of life, viz, food, othing, shelter, medical aid and education, for the Holy Prophet (may peace be upon him) said:" The government is the guardian of those who have no guardian." </p>
        <p> Moreover, the State is bound to create such conditions in which the good reigns supreme in the society and evil is suppressed and exterminated, and its citizens learn to live as good Muslims. </p>
