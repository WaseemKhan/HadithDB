      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 22: </a> </em>
      <h1> <em> The Book of Sacrifices (Kitab Al-Adahi) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> Religion, at its highest and best, is the devotion of the total self, through service and adoration, to the Almighty Who controls the universe. In this sense all the manifold rites, consecrations, and purifications, offerings and sacred feasts, all the working of asceticism and morality are only the indirect expression of the inner experience of religion-the experience of trust, surrender, yearning and enthusiasm. Sacrifice, whether that of wealth or desires, is the practical proof of man's devotion to his Creator. It is in fact religion in action. </p>
      <p> The Holy Qur'an expresses this attitude of mind and heart in the following words:" Say: Truly, my prayer and my service of sacrifice, and my living and my dying are for God (alone), the Sustainer of all the worlds, in Whose Divinity none has a share. Thus I have been bidden-and I am foremost among those who surrender themselves unto Him" (vi. 162-163). </p>
      <p> The 'Id al-Adha is commemorative of that unparalleled act of devotion of that noble soul. Abraham (peace be upon him), who, in obedience to the Command of his Lord, readily offered the life of his son Isma'il. The Holy Qur'an narrates this soul- stirring event in these words:" And when he (Isma'il) attained the age to assist him in his (Abrahm's) work, he (Abraham) said: O my son! I see in vision that I offer thee in sacrifice. Now see what is thy view. The son said: O my father, do as thou art commanded. Thou wilt find me, if God so wills, patient. So when they both had surrendered themselves to (Allah), and he laid him down prostrate on his forehead (for sacrifice), We called out to him: O Abraham, thou hast indeed fulfilled the vision. Thus do We reward the doers of good. Surely this is a manifest trial. And We ransom- ed him with a great sacrifice. And We left (this blessing) for him among the later generations. Peace be upon Abraham I Thus indeed do We reward those who do good; for he was one of Our believing servants" (xxxvii. 102-111) </p>
      <p> In the above-quoted verses 'axim (great), the adjective qualifying" Sacrifice." may be understood both in literal and figurative sense. In literal sense it implies that a big ram was substituted. The figurative sense is even more important. It was indeed a great and momentous occasion, when two men with concentrated will ranged themselves in ranks of those to whom self-sacrifice in the service of God was the supreme thing in life. Similarly, the words" thou hast indeed fulfilled the vision" show that it was not in fact the act of slaughtering which was needed for the fulfilment of the vision, but it was the attitude of submission and surrender, an attitude of preparedness to sacrifice one's all in the path of Allah. that was demanded of Abraham and his illustrious son, and they eminently stood this test. </p>
      <p> Readiness to Sacrifice One's Life. In Islam the act of sacrifice is the symbol of a Muslim's readiness to lay down his life, and to sacrifice all his interests and desires in the cause of truth. The purpose of sacrifice is not fulfilled only by shedding the blood of an animal, but it is really fulfilled when a man submits himself completely to the command of Allah. This has been clearly laid down in Sura Hajj, verse 37;" Not their flesh, nor their blood reaches Allah, but it is the piety from you that reaches Him." This verse eloquently speaks of the fact that sacrifice in Islam is nothing else than a natural expression of homage and gratitude to the Creator. It is the spirit of willing devotion and cheerful obedience underlying sacrifices that is accepted by Allah Who is the Fountainhead of all morality. It is only piety of heart. nobility of soul and righteousness of conduct, that is acceptable to Him. It is essentially symbolic, an external symbol of dedication, devotion to Allah. Tafsir Ibn Kathir stresses this point:" The man who offers sacrifice should keep this fact uppermost in his mind that the most important motive behind this is the willing submission to Allah" ' (Vol. VI, p. 183). </p>
      <p> Such truths, so self-evident to the Muslim readers, needed a clear and emphatic enunciation in view of the horrible misconceptions which had crowded round the act of sacrifice before Islam. </p>
      <p> " Throughout the Semitic field," observes Robertson Smith, in his well known book. The Religion of the Semitics, the fundamental idea of sacrifice was that of communion between the God and his worshipper by joint participation in the living flesh and blood of a sacred victim" (p. 49). </p>
      <p> " The Greeks also looked upon sacrifice as a 'Communion feast' with the Divinity, in which the God and his people became of one flesh by partaking together of the flesh of the victim; the animal was regarded, as in some degree, divine, as having the divine spirit incarnate in it. Among the Babylonians the gods feast in heaven, they eat the offerings, they scent the savour, like flies do they gather themselves together with the offerers" (Hastings, Encyclopedia of Rdigion & Ethics, Article" Sacrifice" ). </p>
      <p> The Holy Qur'an strikes at the very root of such wrong concepts of sacrifice and asserts that" it is neither the flesh nor the blood of (animals) that reaches Allah, but it is your piety that reaches Him," for God does not stand In need of food or blood. What He, in fact, desires is the devotion and piety of our hearts. and, as a symbol of such offer, the visible institution of sacrifice has been instituted. The Holy Qur'an has further elucidated the main parpose of the institution of sacrifice." For every people did We appoint rites (of sacrifice) that they might celebrate the name of God over the sustenance He gave them from animals (fit for food). But your God is One God. Sub- mit then your wills to him (in Islam).... The sacrificial camels We have made for you as among the symbols from God. In them is (much) good for you So mention the name of Allah on them standing in a row. Then when they fall down on their sides, eat of them, feed the contented one and the beggar. Thus have We made them subservient to you that you may be grateful" (xxii. 34-38). </p>
      <p> The Qur'an testifies to the historical fact that whatever may be the outward symbols of sacrifice, it has been accepted in one form or another by all the nations of the world. It had been a fundamental element of both Jewish and Gentile religions, and Christianity. It had been corrupted by many wrong practices and been overlaid by many wrong notions before the advent of Islam. Islam purifies it from all wrong notions and practices connected with it, and makes it explicitly clear that the act of sacrifice is an outward symbol of man's readiness to lay down his life, if required, and to surrender all his interests in the cause of truth and righteousness. </p>
      <p> The words" We have made them (subservient) to you" have a very wide significance. A Muslim has been awakened to the realisation of the fact that if they offer as a sacrifice an animal over which they hold control, it is their bounden duty to lay down their lives in the way of Allah, Who is not only their Master, but also their Creator and Sustainer and Who. therefore, exercises a far greater authority over them than they do over the animals. This should be the true motive of sacrifice, and it is with this spirit that this act should be performed. </p>
      <p> The Qur'anic words" To Him is acceptable observance of duty on your part" make it abundantly clear that the prevalent idea of atonement that" it is the blood that maketh an atonement for the soul" (Leviticus; 17: 11) has no foundation in Islam. The expiation of sin in Islam rests entirely on the good deeds of men, repentance of the Winners and the Forgiving and Merciful nature of God 'This fact cuts the ground from under the feet of any theory of an atoning sacrifice. </p>
      <p> The opening verse" For every people did We appoint rites (of sacrifice) that they might celebrate the name of God over the beast cattle wherewith He bath provided them," speaks of the fact that the very idea of human sacrifice is repugnant to the true religion and Allah has never given it sanction. </p>
      <p> This practice of human sacrifice was not uncommon before Islam." Both on the mainland of Greece and in the Greek colonies human sacrifice was practised, usually as a means towards expulsion of evil" (Encydopaedia Britannica, Article'on" Sacrifice" ). it occupied a prominent place in the ritual of the mother goddesses of ancient times. </p>
      <p> " The ordinary form of sacrifice," says E. O. James, in his famous book, The origins of Sacrifice," consisted in stripping the victim of his ornaments, stretching him over the convex sacrificial stones and while fare priests held his arms, legs, head, the high priests or sacrificer cut open his breast with a flint or obsidian knife, and tore out the heart. This was held up to the sun to provide it with nourishment, before it was cast into a basin of Copal placed in a position to enable the blood and incense to ascend to the gods. The body was hurled down the steps of the temple to the court where it was seized by the priest or by the warrior who captured the victim. Some times a solemn feast was then held on the flesh, the skin having first been removed to be worn ceremonially by men who seem to have acquired thereby the fertilising nd health-giving qualities of the victim. Some of the blood was carried to certain temples and smeared on the hips of the images of gods" (pp. 84-6). </p>
      <p> Islam has not only exterminated the very idea of human sacrifice, but has completely ended all such inhuman practices which were very common with the people before Islam. The Holy Qur'an makes a pointed reference to the fact that this sacrifice of animals is commemorative of Abraham's offer of his son's life at the Command of Allah, who was substituted by a ram, and it has been perpetuated by Islam. It is narrated that once the Companions of the Holy Prophet (may peace be upon him) asked him about the sacrifice. He replied:" This is commemorative Sunnah of your father Abraham" (vide Ibn Kathir, Vol. III, p. 221). That this practice of sacrifice was already prevalent amongst the people before Islam can be well borne out by the fact that we find clear references to it in the poetry of pre-Islamic Arabia. The well. known poet Umayya observes: </p>
      <p> Abraham was one who would fulfil the pledges and offer sacrifices for Allah's sake. </p>
      <p> Thus he offered the life of his only son whose separation and whose risk of life, he could not bear. </p>
      <p> He said," O my son I have pledged you to Allah. </p>
      <p> May I sacrifice my life for you! </p>
      <p> Be steadfast and firm." </p>
      <p> He had hardly taken off the shirt of his son, when Allah substituted Isma'il by a stout ram. </p>
      <p> Not only this practice of sacrifice has been preserved in Islam, but even the way of Abraham's has been declared to he one of righteousness and truthfulness: </p>
      <p> " Say: Behold, my Lord has guided me to a way that is straight-a religion of Right Path-the Path (trodden) by Abraham, who was wholly devoted to God, and was not of those who ascribe divinity to any beside him" (vi. 161). </p>
      <p> Even the Millat has been assigned a name after the name of Abraham: </p>
      <p> " He hath selected you and hath not placed upon you any hardship in religion-the religion of your father, Abraham. He named you Muslims before this, and in this, that the Messenger may be a witness to you and you may he witnesses to mankind" (xxii. 78). </p>
      <p> Historical Continuity, The constant reference to the earlier Prophets and the Qur'anic testimony to their righteousness and the preservation of some of their religious practices have been done to awaken the people to the realisation of a fundamental fact, i. e. the fact of the historical continuity of religious experience. The Muslims have been asked to believe in that which has been revealed unto Prophet Muhammad (may peace be upon him) as well as in that which was revealed before him. Life-so the Qur'an teaches us-is not a series of disconnected parts but a continuous, organic process: and this law applies also to the law of the mind, of which man's religious experience (in its cumulative sense) is a part. To make religious experience more living, to set Allah the Ever-living with loving vividness before the eyes of living men, to make them feel Him as actually and eternally present in their lives, man needs a path, clear-cut path, lightened with glories of the Messengers of Allah-a path on which one should not feel lonely but the strength of comradeship of those noble souls upon whom Allah has bestowed His choicest blessings. </p>
      <p> A few words may be said about the way how an animal should be slaughtered according to the teachings of Islam. Three are the aims which should be kept before the mind while slaughtering the animal: </p>
      <p> It should be slaughtered by reciting the name of Allah and glorifying Him. </p>
      <p> It should be slaughtered with a sharp knife so that its jugular vein may be cut with the minimum possible pain and its skin should not be removed and limbs should not be cut so long as there is any sign of life in it. </p>
      <p> The head should not be removed from the body abruptly but only the jugular vein should be cut so that even the last drop of blood flows out of its body. If the animal is beheaded with a stroke, the blood congeals in its veins which makes the flesh distasteful and pernicious to health. </p>
