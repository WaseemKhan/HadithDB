      <em> <a name="002.0573"> Book 002, Number 0573: </a> </em>
      <blockquote>
        <p> Asma (daughter of Abu Bakr) reported: A woman came to the Apostle of Allah (may peace be upon him) and said: What should one do if the blood of menses smears the garment of one amongst us? He (the Holy Prophet) replied: She should scrape it, then rub it with water, then pour water over it and then offer prayer in it.</p>
      </blockquote>
