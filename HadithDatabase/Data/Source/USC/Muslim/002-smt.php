      <em> <a name="002.0443"> Book 002, Number 0443: </a> </em>
      <blockquote>
        <p> Abu Anas reported that Uthman performed ablution at Maqi'aid and said: Should I not show you the ablution performed by Allah's Messenger (may peace be upon him)? And he then washed (the different parts of the body) three times. 4" Qutaiba has added in his narration the words:" There were with him (with Uthman) Companions of Allah's Messenger (may peace be upon him)."</p>
