      <em> <a name="002.0441"> Book 002, Number 0441: </a> </em>
      <blockquote>
        <p> 'Amr b Sa'id b al-As reported: I was, with Uthman that he called for ablution water and said: I heard Allah's Messenger (may peace be upon him) say: When the time for a prescribed prayer comees, if any Muslim perform ablution well and offers his prayer) with humility and bowing, it will be an expiation for his past sins, so long as he has not committed a major sin; and this applies to for all times.</p>
      </blockquote>
