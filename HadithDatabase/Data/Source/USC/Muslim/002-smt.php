      <em> <a name="002.0571"> Book 002, Number 0571: </a> </em>
      <blockquote>
        <p> Abu Kuraib, Ibn al-Mubarak, Ibn Abu Za'ida all of them narrated from Amr b. Maimun with the same chain of transmitters. Ibn Abu Za'ida narrated as was transmitted from Ibn Bishr that the Messenger of Allah (may peace be upon him) washed semen, and in the hadith transmitted on the authority of Ibn Mabarak and Abdul Wahid the words are:" She (A'isha) reported: I used to wash it from the garment of the Messenger of Allah (may peace be upon him)."</p>
      </blockquote>
      <!-- EH -->
