      <blockquote>
        <p> Humran reported when 'Uthman performed ablution he said: By Allah, I am narrating to you a hadith had there not been this verse in the Book of Allah. I would not have narrated it to you. Verily I heard the Messenger of Allah (may peace be upon him) say: Not a person is there who performed ablution, and did it well, then offered prayer, but his sins (which he committed) were not pardoned between the prayer that he offered and the next one. 'Urwa said: The verse is this:" Those who suppress the clear proofs and the guidance which We have sent down"... to His words:" the Cursers" (ii. 15).</p>
      </blockquote>
