      <br>
      <p><strong> Chapter 24: WASHING AWAY OF'THE SEMEN FROM THE GARMENT AND ITS SCRAPING </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="002.0566"> Book 002, Number 0566: </a> </em>
      <blockquote>
        <p> Alqama and Aswad reported: A person stayed in the house of A'isha and in the morning began to wash his garment. A'isha said: In case you saw it (i. e. drop of semen), it would have served the purpose (of purifying the garment) if you had simply washed that spot; and in case you did not see it, it would have been enough to sprinkle water around it, for when I saw that on the garment of the Messenger of Allah (may peace be upon him). I simply scraped it off and he offered prayer, while putting that on.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0567"> Book 002, Number 0567: </a> </em>
      <blockquote>
        <p> Al-Aawad and Hammam reported A'isha as saying: I used to scrape off the (drop of) semen from the garment of the Messenger of Allah (may peace be upon him).</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0568"> Book 002, Number 0568: </a> </em>
      <blockquote>
        <p> Qutaiba b. Sa'id, Ishaq b. Ibrahim, Ibn Abi 'Aruba, Abu Ma'shar, Abu Bakr b. Abu Shaiba, Mansur and Mughira have all transmitted from Ibrahim, who transmitted it on the authority of A'isha's narration pertaining to the scraping off of the (drop) of semen from the garment of the Messenger of Allah (may peace be upon him) like the hadith of Khalid on the authority of Abu Ma'shar.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0569"> Book 002, Number 0569: </a> </em>
      <blockquote>
        <p> Hammam narrated the hadith from A'isha like the (above-mentioned) traditions.</p>
      </blockquote>
