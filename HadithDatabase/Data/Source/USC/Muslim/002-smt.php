      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 2: </a> </em>
      <h1> <em> The Book of Purification (Kitab Al-Taharah) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> We have read in the first Book that the love of Allah is the highest aim of a true believer. It is for the achievement of this single end that he affirms his faith in Him by renouncing all other types of godhood. </p>
      <p> The love of God is not something inert or lifeless; it is dynamic in the sense that it calls for a complete change in the life of man: change in his thoughts and ideas and change in his conduct and behaviour. One who claims to be a believer in Allah has to make a good deal of effort with a view to pleasing his: lord. He has to purify his soul from all evil thoughts and fancies so that the love of God should reside in it. Unless the soul is purged of all impurities one cannot achieve salvation. This is known as Tahdrah in Islam, and it is the foundation-stone of Imin. This high objective of the purification of the soul requires intentional and deliberate efforts and a good deal of sacrifice on the part of man, and the most elementary stage in this sacred path is the cleanliness of body. </p>
      <p> By enjoining cleanliness of body upon man Islam awakens him to the realisation of the fact that when impurities on the body of a man produce such unhealtby effects on his physical being and corrode his mental health, how miserable his life would be when his soul is polluted with impurities. The process of the purification of the soul should, therefore, start with the purification of the body. </p>
      <br>
      <p><strong> Chapter 1: MERIT OF WUDU' </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="002.0432"> Book 002, Number 0432: </a> </em>
      <blockquote>
        <p> Abu Malik at-Ash'ari reported: The Messenger of Allah (may peace be upon him) said: Cleanliness is half of faith and al-Hamdu Liliah (Praise be to Allah) fills the scale, and Subhan Allah (Glory be to Allah) and al-Hamdu Liliah (Praise be to Allah) fill upwhat is between the heavens and the earth, and prayer is a light, and charity is proof (of one's faith) and endurance is a brightness and the Holy Qur'an is a proof on your behalf or against you. All men go out early in the morning and sell themselves, thereby setting themselves free or destroying themselves.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 2: PURIFICATION IS ESSENTIAL FOR PRAYER </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="002.0433"> Book 002, Number 0433: </a> </em>
      <blockquote>
        <p> Mus'ab b. Sa'd reported: 'Abdullah son of Umar came to Ibn'Amir in order to inquire after his health as he was ailing. He said Ibn 'Umar, why don't you prayto Allah for me? He said: I heard of Allah's Messenger (may peace be upon him) say: Neither the prayer is accepted without parification nor is charity accepted out of the ill-gotten (wealth), and thou wert the (governor) of Basra.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0434"> Book 002, Number 0434: </a> </em>
      <blockquote>
        <p> A hadith like this is narrated from the Apostle (may peace be upon him) with the same chain of transmitters by Muhammad b. Muthanna, Ibn Bashshar, Muhammad b. Ja'far, Shu'ba.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0435"> Book 002, Number 0435: </a> </em>
      <blockquote>
        <p> Hammam b. Munabbih who is the brother of Wahb b. Munabbih. said: This is what has been transmitted to us by Abu Huraira from Muhammad, the Messenger of Allah (may peace be upon him) and then narrated a hadith out of them and observed that the Messenger of Allah (may peace be upon him) said: The prayer of none amongst you would be accepted in a state of impurity till he performs ablution.</p>
      </blockquote>
      <!-- EH -->
      <br>
      <p><strong> Chapter 3: HOW TO PERFORM ABLUTION </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="002.0436"> Book 002, Number 0436: </a> </em>
      <blockquote>
        <p> Humran, the freed slave of 'Uthman, said: Uthman b. 'Affan called for ablution water and this is how he performed the ablution. He washed his hands thrice. He then rinsed his mouth and cleaned his nose with water (three times). He then washed his face three times, then washed his right arm up to the elbow three times, then washed his left arm like that, then wiped his head; then washed his right foot up to the ankle three times, then washed his left foot like that, and then said: I saw the Messenger of Allah (may peace be upon him) perform ablution like this ablution of mine. Then the Messenger of Allah (may peace be upon him) said: He who performs ablution like this ablution of mine and then stood up (for prayer) and offered two rak'ahs of prayer without allowing his thoughts to be distracted, all his previous sins are expiated. Ibn Shihab said: Our scholars remarked: This is the most complete of the ablutions performed for prayer.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0437"> Book 002, Number 0437: </a> </em>
      <blockquote>
        <p> Humran, the freed slave of 'Uthman said: I saw Uthman call for a vessel (of water) and poured water over his hands three times and then washed them. Then he put his right hand in the vessel and rinsed his mouth and cleaned his nose. Then he washed his face three times and his hands up to the elbow three times; then wiped his head, then washed his feet three times. Then he said that the Messenger of Allah (may peace be upon him) had said: He who performed ablution like this ablution of mine and offered two raklahs of prayer without allowing his thoughts to be distracted, all his previous sins would be expiated.</p>
      </blockquote>
