      <em> <a name="002.0442"> Book 002, Number 0442: </a> </em>
      <blockquote>
        <p> Humran, the freed slave of 'Uthman reported: I brought for Uthman b. 'Affan the ablution water. He performed ablution and then said: Verily the people narrate from the Messenger of Allah (may peace be upon him) a hadith. I do not know what these are. but (I know this fact) that I saw the Messenger of Allah (may peace be upon him) perform ablution like this ablution of mine and then he said: He who performed ablution like this, all his previous sins would be expiated and his prayer and going towards the mosque would have an extra reward. In the tradition narrated by Ibn 'Abda (the words are):" I came to Uthman and he performed ablution."</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
