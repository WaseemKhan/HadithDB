      <em> <a name="002.0575"> Book 002, Number 0575: </a> </em>
      <blockquote>
        <p> Ibn Abbas reported: The Messenger of Allah (may peace be upon him) happened to pass by two graves and said: They (their occupants) are being tormented, but they are not tormented for a grievous sin. One of them carried tales and the other did not keep himself safe from being defiled by urine. He then called for a fresh twig and split it into two parts, and planted them on each grave and then said: Perhaps, their punishment way be mitigated as long as these twigs remain fresh.</p>
