      <hr>
      <em> <a name="002.0455"> Book 002, Number 0455: </a> </em>
      <blockquote>
        <p> Malik b. Anas narrated it from 'Amr b. Yahya with the same chain of transmitters, transmitters and mentioned the rinsing (of mouth) and snuffing (of water into the nostrils) three times, but he did not mention" from one palm," and made this addition: He moved them (his hands) for wiping to the front of his head and then the nape of his neck, then bringing them back till he reached the place from which he had begun, after which he washed his feet.</p>
      </blockquote>
      <!-- EH -->
