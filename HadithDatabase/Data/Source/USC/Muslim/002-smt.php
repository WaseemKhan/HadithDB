      <blockquote>
        <p> 'Amr b. Maimun said: I asked Sulaiman b. Yasir whether the semen that gets on to the garment of a person should be washed or not. He replied: A'isha told me: The Messenger of Allah (may peace be upon him) washed the semen, and then went out for prayer in that very garment and I saw the mark of washing on it.</p>
      </blockquote>
      <!-- EH -->
