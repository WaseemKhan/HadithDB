        <p> Ubaidullah b. Abdullah b. 'Utba b. Mas'ud said: Umm Qais, daughter of Mihsan, was among the earliest female emigrants who took the oath of allegiance to the Messenger of Allah (may peace be upon him), and she was the sister of 'Ukkasha b. Mihsan, one amongst the sons of Asad b. Khuzaima. He (the narrator) said: She (Umm Qais) told me that she came to the Messenger of Allah (may peace he upon him) with her son and he had not attained the age of eating food. He (the narrator, 'Ubaidullah), said: She told me that her son passed urine in the lap of the Messenger of Allah (may peace be upon him). The Messenger of Allah (may peace be upon him) sent for water and sprayed it over his garment (over that part which was contaminated with the urine of the child) and he did not wash it thoroughly.</p>
      </blockquote>
