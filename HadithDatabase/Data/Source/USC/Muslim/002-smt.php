      <!-- BH -->
      <hr>
      <em> <a name="002.0448"> Book 002, Number 0448: </a> </em>
      <blockquote>
        <p> Abu Huraira reported: The Messenger of Allah (may peace be upon him) said: Five prayers and from one Friday prayer to (the next) Friday prayer is an expiation (of the sins committed in between their intervals) if major sins are not committed.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0449"> Book 002, Number 0449: </a> </em>
      <blockquote>
        <p> Abu Huraira reported that the Messenger of Allah (way peace be upon him) said: Five prayers and one Friday prayer to (the next) Friday prayer are expiatious (for the sins committed in the intervals) between them.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0450"> Book 002, Number 0450: </a> </em>
      <blockquote>
        <p> Abu Huraira reported: Verily the Messenger of Allah (may peace be upon him) said: Five (daily) prayers and from one Friday prayer to the (next) Friday prayer, and from Ramadhan to Ramadhan are expiatious for the (sins) committed in between (their intervals) provided one shuns the major sins.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0451"> Book 002, Number 0451: </a> </em>
      <blockquote>
        <p> 'Uqba b. 'Amir reported: We were entrusted with the task of tending the camels. On my turn when I came back in the evening after grazing them in the pastures, I found Allah's Messenger (may peace be upon him) stand and address the people. I heard these words of his: If any Muslim performs ablution well, then stands and prays two rak'ahs setting about them with his heart as well as his face, Paradise would be guaranteed to him. I said: What a fine thing is this! And a narrator who was before me said: The first was better than even this. When I cast a glance, I saw that it was 'Umair who said: I see that you have just come and observed: If anyone amongst you performs the ablution, and then completes the ablution well and then says: I testify that there is no god but Allah and that Muhammad is the servant of Allah and His Messenger, the eight gates of Paradise would be opened for him and he may enter by whichever of them he wishes.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0452"> Book 002, Number 0452: </a> </em>
      <blockquote>
        <p> Uqba b. 'Amir al-Juhani reported: Verily the Messenger of Allah (may peace be upon him) said and then narrated (the hadith) like one (mentioned above) except (this) that he said: He who performed ablution and said: I testify that there is no god but Allah, the One, there is no associate with Him and I testify that Muhammad is His servant and His Messenger.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0453"> Book 002, Number 0453: </a> </em>
      <blockquote>
        <p> 'Abdullah b. Zaid b. 'Asim al-Ansari, who was a Companion (of the Holy Prophet), reported: It was said to him (by people): Perform for us the ablution (as it was performed) by the Messenger of Allah (way peace be upon him). He ('Abdullah b. Zaid) called for a vessel (of water), and poured water from it on his hands and washed them three times. Then he inserted his hand (in the vessel) and brought it (water) out, rinsed his mouth and snuffed up water from the palm of one hand doing that three times, He again inserted his hand and brought it out and washed his face three times, then inserted his hand and brought it out and washed each arm up to the elbow twice, then inserted his hand and brought it out and wiped his head both front and back with his hands. He then washed his feet up to the ankles, and then said: This is how God's Messenger (peace be upon him) performed ablution.</p>
      </blockquote>
      <!-- EH -->
      <!-- BH -->
      <hr>
      <em> <a name="002.0454"> Book 002, Number 0454: </a> </em>
      <blockquote>
        <p> This hadith is narrated by Amr b. Yahya with the same chain of transmitters, but there is no mention of ankles.</p>
      </blockquote>
      <!-- EH -->
