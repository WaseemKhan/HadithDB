      <p><strong> Chapter 4: THE MERIT OF WUDU AND THAT OF PRAYER AFTER IT </strong></p>
      <!-- BH -->
      <hr>
      <em> <a name="002.0438"> Book 002, Number 0438: </a> </em>
      <blockquote>
        <p> Humran. the freed slave of 'Uthman. said: I heard from 'Uthman b. 'Affan and he was in the courtyard of the mosque, when the Mu'adhdhin (announcer of the prayer) came to him at the time of afternoon prayer. So the ('Uthman) called for the ablution water and performed ablution and then said: By Allah, I am narrating to you a hadith. If there were not a verse in the Book of Allah, I would have never narrated it to you. I heard Allah's Messenger (may peace be upon him) say: If a Muslim performs ablution and does it well and offers prayer, all his (sins) daring the period from one prayer to another would be pardoned by Allah.</p>
      </blockquote>
