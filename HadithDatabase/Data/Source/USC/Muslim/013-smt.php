      <em> <a href="smtintro.php"> Translation of Sahih Muslim, Book 13: </a> </em>
      <h1> <em> The Book of Bequests (Kitab Al-Wasiyya) </em> </h1>
      <br>
      <p><strong> INTRODUCTION </strong></p>
      <p> A bequest (WasIyya) or will is defined as a transfer to come into operation after the testator's death. The testator is called Musi, and the legatee or devisee is called Musa lahu, and the executor is called Wasi. It is a spiritual testament of a man enabling him to make up his shortcomings in the worldly life and securing rewards in the Hereafter. </p>
      <p> According to the Shar'iah, one is entitled to make a will for one-third of one's property and not beyond that so that the rights of the legal heirs are not adversely affected. </p>
      <p> " Again, the principle on which the legality of a testamentary disposition is based being in defeance pro tanto of the rights of heirs generally the law requires that such disposition should be for the benefit of non-heirs alone. </p>
      <p> " A further reason why a bequest in favour of an heir is not allowed is that it would amount to giving preference to some heirs over others, thus defeating the spirit of the law which has fixed the portion of each in the inheritance and causing disputes among persons related to one another. If the other heirs consent to a bequest to one of them or to a bequest of more than one-third of the estate, the above reasons no longer hold good and the bequest as made will be valid" (Abdur Rahim, The Principles of Muhammadan jurisprudence, pp. 311-2). </p>
      <p> It is, however, preferable and most advisable not to will away the property if the legal heirs are poor, because it manifests benevolence to the heirs who have superior claim to it from the relations in which they stand. </p>
      <p> Another principle which is observed in case of bequest is that the proprietary right of a legatee in a bequest is establislied if he accepts it. </p>
