      <blockquote>
        <p> Narrated 'Abdullah bin Amr bin Al-As:
        <p> My father got me married to a lady of a noble family, and often used 
          to ask my wife about me, and she used to reply, "What a wonderful man 
          he is! He never comes to my bed, nor has he approached me since he 
          married me." When this state continued for a long period, my father 
          told the story to the Prophet who said to my father, "Let me meet 
          him." Then I met him and he asked me, "How do you fast?" I replied, "I
          fast daily," He asked, "How long does it take you to finish the 
          recitation of the whole Quran?" I replied, "I finish it every night." 
          On that he said, "Fast for three days every month and recite the 
          Qur'an (and finish it) in one month." I said, "But I have power to do 
          more than that." He said, "Then fast for three days per week." I said,
          "i have the power to do more than that." He said, "Therefore, fast the
          most superior type of fasting, (that is, the fasting of (prophet) 
          David who used to fast every alternate day; and finish the recitation 
          of the whole Qur'an In seven days." I wish I had accepted the 
          permission of Allah's Apostle as I have become a weak old man. It is 
          said that 'Abdullah used to recite one-seventh of the Qur'an during 
          the day-time to some of his family members, for he used to check his 
          memorization of what he would recite at night during the daytime so 
          that it would be easier for him to read at night. And whenever he 
          wanted to gain some strength, he used to give up fasting for some days
          and count those days to fast for a similar period, for he disliked to 
          leave those things which he used to do during the lifetime of the 
          Prophet.
