      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 61: </a> </em>
      <h1> <em> Virtues of the Qur'an </em> </h1>
      <hr>
      <p> <em> <a name="006.061.502"> Volume 6, Book 61, Number 502: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha and Ibn 'Abbas:
        <p> The Prophet remained in Mecca for ten years, during which the Qur'an 
          used to be revealed to him; and he stayed in Medina for ten years.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.503"> Volume 6, Book 61, Number 503: </a> </em>
      <blockquote>
        <p> Narrated Abu 'Uthman:
        <p> I was informed that Gabriel came to the Prophet while Um Salama was 
          with him. Gabriel started talking (to the Prophet). Then the Prophet 
          asked Um Salama, "Who is this?" She replied, "He is Dihya (al-Kalbi)."
          When Gabriel had left, Um Salama said, "By Allah, I did not take him 
          for anybody other than him (i.e. Dihya) till I heard the sermon of the
          Prophet wherein he informed about the news of Gabriel." The 
          subnarrator asked Abu 'Uthman: From whom have you heard that? Abu 
          'Uthman said: From Usama bin Zaid.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.504"> Volume 6, Book 61, Number 504: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "Every Prophet was given miracles because of which 
          people believed, but what I have been given, is Divine Inspiration 
          which Allah has revealed to me. So I hope that my followers will 
          outnumber the followers of the other Prophets on the Day of 
          Resurrection."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.505"> Volume 6, Book 61, Number 505: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> Allah sent down His Divine Inspiration to His Apostle continuously and
          abundantly during the period preceding his death till He took him unto
          Him. That was the period of the greatest part of revelation; and 
          Allah's Apostle died after that.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.506"> Volume 6, Book 61, Number 506: </a> </em>
      <blockquote>
        <p> Narrated Jundub:
        <p> Once the Prophet fell ill and did not offer the night prayer (Tahajjud
          prayer) for a night or two. A woman (the wife of Abu Lahab) came to 
          him and said, "O Muhammad ! I do not see but that your Satan has left 
          you." Then Allah revealed (Surat-Ad-Duha):
        <p> 'By the fore-noon, and by the night when it darkens (or is still); 
          Your Lord has not forsaken you, nor hated you.' (93)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.507"> Volume 6, Book 61, Number 507: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> (The Caliph 'Uthman ordered Zaid bin Thabit, Said bin Al-As, 'Abdullah
          bin Az-Zubair and 'Abdur-Rahman bin Al-Harith bin Hisham to write the 
          Quran in the form of a book (Mushafs) and said to them. "In case you 
          disagree with Zaid bin Thabit (Al-Ansari) regarding any dialectic 
          Arabic utterance of the Quran, then write it in the dialect of 
          Quraish, for the Quran was revealed in this dialect." So they did it.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.508"> Volume 6, Book 61, Number 508: </a> </em>
      <blockquote>
        <p> Narrated Safwan bin Ya'la bin Umaiya:
        <p> Ya'la used to say, "I wish I could see Allah's Apostle at the time he 
          is being inspired Divinely." When the Prophet was at Al-Ja'rana and 
          was shaded by a garment hanging over him and some of his companions 
          were with him, a man perfumed with scent came and said, "O Allah's 
          Apostle! What is your opinion regarding a man who assumes Ihram and 
          puts on a cloak after perfuming his body with scent?" The Prophet 
          waited for a while, and then the Divine Inspiration descended upon 
          him. 'Umar pointed out to Ya'la, telling him to come. Ya'la came and 
          pushed his head (underneath the screen which was covering the Prophet 
          ) and behold! The Prophet's face was red and he kept on breathing 
          heavily for a while and then he was relieved. Thereupon he said, 
          "Where is the questioner who asked me about 'Umra a while ago?" The 
          man was sought and then was brought before the Prophet who said (to 
          him), "As regards the scent which you perfumed your body with, you 
          must wash it off thrice, and as for your cloak, you must take it off; 
          and then perform in your 'Umra all those things which you perform in 
          Hajj."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.509"> Volume 6, Book 61, Number 509: </a> </em>
      <blockquote>
        <p> Narrated Zaid bin Thabit:
        <p> Abu Bakr As-Siddiq sent for me when the people! of Yamama had been 
          killed (i.e., a number of the Prophet's Companions who fought against 
          Musailama). (I went to him) and found 'Umar bin Al-Khattab sitting 
          with him. Abu Bakr then said (to me), "Umar has come to me and said: 
          "Casualties were heavy among the Qurra' of the! Qur'an (i.e. those who
          knew the Quran by heart) on the day of the Battle of Yalmama, and I am
          afraid that more heavy casualties may take place among the Qurra' on 
          other battlefields, whereby a large part of the Qur'an may be lost. 
          Therefore I suggest, you (Abu Bakr) order that the Qur'an be 
          collected." I said to 'Umar, "How can you do something which Allah's 
          Apostle did not do?" 'Umar said, "By Allah, that is a good project. 
          "Umar kept on urging me to accept his proposal till Allah opened my 
          chest for it and I began to realize the good in the idea which 'Umar 
          had realized." Then Abu Bakr said (to me). 'You are a wise young man 
          and we do not have any suspicion about you, and you used to write the 
          Divine Inspiration for Allah's Apostle. So you should search for (the 
          fragmentary scripts of) the Qur'an and collect it in one book)." By 
          Allah If they had ordered me to shift one of the mountains, it would 
          not have been heavier for me than this ordering me to collect the 
          Qur'an. Then I said to Abu Bakr, "How will you do something which 
          Allah's Apostle did not do?" Abu Bakr replied, "By Allah, it is a good
          project." Abu Bakr kept on urging me to accept his idea until Allah 
          opened my chest for what He had opened the chests of Abu Bakr and 
          'Umar. So I started looking for the Qur'an and collecting it from 
          (what was written on) palmed stalks, thin white stones and also from 
          the men who knew it by heart, till I found the last Verse of Surat 
          At-Tauba (Repentance) with Abi Khuzaima Al-Ansari, and I did not find 
          it with anybody other than him. The Verse is:
        <p> 'Verily there has come unto you an Apostle (Muhammad) from amongst 
          yourselves. It grieves him that you should receive any injury or 
          difficulty..(till the end of Surat-Baraa' (At-Tauba) (9.128-129) Then 
          the complete manuscripts (copy) of the Qur'an remained with Abu Bakr 
          till he died, then with 'Umar till the end of his life, and then with 
          Hafsa, the daughter of 'Umar.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.510"> Volume 6, Book 61, Number 510: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> Hudhaifa bin Al-Yaman came to Uthman at the time when the people of 
          Sham and the people of Iraq were Waging war to conquer Arminya and 
          Adharbijan. Hudhaifa was afraid of their (the people of Sham and Iraq)
          differences in the recitation of the Qur'an, so he said to 'Uthman, "O
          chief of the Believers! Save this nation before they differ about the 
          Book (Quran) as Jews and the Christians did before." So 'Uthman sent a
          message to Hafsa saying, "Send us the manuscripts of the Qur'an so 
          that we may compile the Qur'anic materials in perfect copies and 
          return the manuscripts to you." Hafsa sent it to 'Uthman. 'Uthman then
          ordered Zaid bin Thabit, 'Abdullah bin AzZubair, Said bin Al-As and 
          'AbdurRahman bin Harith bin Hisham to rewrite the manuscripts in 
          perfect copies. 'Uthman said to the three Quraishi men, "In case you 
          disagree with Zaid bin Thabit on any point in the Qur'an, then write 
          it in the dialect of Quraish, the Qur'an was revealed in their 
          tongue." They did so, and when they had written many copies, 'Uthman 
          returned the original manuscripts to Hafsa. 'Uthman sent to every 
          Muslim province one copy of what they had copied, and ordered that all
          the other Qur'anic materials, whether written in fragmentary 
          manuscripts or whole copies, be burnt. Said bin Thabit added, "A Verse
          from Surat Ahzab was missed by me when we copied the Qur'an and I used
          to hear Allah's Apostle reciting it. So we searched for it and found 
          it with Khuzaima bin Thabit Al-Ansari. (That Verse was): 'Among the 
          Believers are men who have been true in their covenant with Allah.' 
          (33.23)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.511"> Volume 6, Book 61, Number 511: </a> </em>
      <blockquote>
        <p> Narrated Zaid bin Thabit:
        <p> Abu Bakr sent for me and said, "You used to write the Divine 
          Revelations for Allah's Apostle : So you should search for (the Qur'an
          and collect) it." I started searching for the Qur'an till I found the 
          last two Verses of Surat At-Tauba with Abi Khuzaima Al-Ansari and I 
          could not find these Verses with anybody other than him. (They were):
        <p> 'Verily there has come unto you an Apostle (Muhammad) from amongst 
          yourselves. It grieves him that you should receive any injury or 
          difficulty ...' (9.128-129)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.512"> Volume 6, Book 61, Number 512: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara:
        <p> There was revealed: 'Not equal are those believers who sit (at home) 
          and those who strive and fight in the Cause of Allah.' (4.95)
        <p> The Prophet said, "Call Zaid for me and let him bring the board, the 
          inkpot and the scapula bone (or the scapula bone and the ink pot)."' 
          Then he said, "Write: 'Not equal are those Believers who sit..", and 
          at that time 'Amr bin Um Maktum, the blind man was sitting behind the 
          Prophet . He said, "O Allah's Apostle! What is your order For me (as 
          regards the above Verse) as I am a blind man?" So, instead of the 
          above Verse, the following Verse was revealed:
        <p> 'Not equal are those believers who sit (at home) except those who are 
          disabled (by injury or are blind or lame etc.) and those who strive 
          and fight in the cause of Allah.' (4.95)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.513"> Volume 6, Book 61, Number 513: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Abbas:
        <p> Allah's Apostle said, "Gabriel recited the Qur'an to me in one way. 
          Then I requested him (to read it in another way), and continued asking
          him to recite it in other ways, and he recited it in several ways till
          he ultimately recited it in seven different ways."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.514"> Volume 6, Book 61, Number 514: </a> </em>
      <blockquote>
        <p> Narrated 'Umar bin Al-Khattab:
        <p> I heard Hisham bin Hakim reciting Surat Al-Furqan during the lifetime 
          of Allah's Apostle and I listened to his recitation and noticed that 
          he recited in several different ways which Allah's Apostle had not 
          taught me. I was about to jump over him during his prayer, but I 
          controlled my temper, and when he had completed his prayer, I put his 
          upper garment around his neck and seized him by it and said, "Who 
          taught you this Sura which I heard you reciting?" He replied, "Allah's
          Apostle taught it to me." I said, "You have told a lie, for Allah's 
          Apostle has taught it to me in a different way from yours." So I 
          dragged him to Allah's Apostle and said (to Allah's Apostle),
        <p> "I heard this person reciting Surat Al-Furqan in a way which you 
          haven't taught me!" On that Allah's Apostle said, "Release him, (O 
          'Umar!) Recite, O Hisham!" Then he recited in the same way as I heard 
          him reciting. Then Allah's Apostle said, "It was revealed in this 
          way," and added, "Recite, O 'Umar!" I recited it as he had taught me. 
          Allah's Apostle  then said, "It was revealed in this way. This Qur'an 
          has been revealed to be recited in seven different ways, so recite of 
          it whichever (way) is easier for you (or read as much of it as may be 
          easy for you)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.515"> Volume 6, Book 61, Number 515: </a> </em>
      <blockquote>
        <p> Narrated Yusuf bin Mahk:
        <p> While I was with Aisha, the mother of the Believers, a person from 
          Iraq came and asked, "What type of shroud is the best?" 'Aisha said, 
          "May Allah be merciful to you! What does it matter?" He said, "O 
          mother of the Believers! Show me (the copy of) your Qur'an," She said,
          "Why?" He said, "In order to compile and arrange the Qur'an according 
          to it, for people recite it with its Suras not in proper order." 
          'Aisha said, "What does it matter which part of it you read first? (Be
          informed) that the first thing that was revealed thereof was a Sura 
          from Al-Mufassal, and in it was mentioned Paradise and the Fire. When 
          the people embraced Islam, the Verses regarding legal and illegal 
          things were revealed. If the first thing to be revealed was: 'Do not 
          drink alcoholic drinks.' people would have said, 'We will never leave 
          alcoholic drinks,' and if there had been revealed, 'Do not commit 
          illegal sexual intercourse, 'they would have said, 'We will never give
          up illegal sexual intercourse.' While I was a young girl of playing 
          age, the following Verse was revealed in Mecca to Muhammad: 'Nay! But 
          the Hour is their appointed time (for their full recompense), and the 
          Hour will be more grievous and more bitter.'  (54.46)  Sura Al-Baqara 
          (The Cow) and Surat An-Nisa (The Women) were revealed while I was with
          him." Then 'Aisha took out the copy of the Qur'an for the man and 
          dictated to him the Verses of the Suras (in their proper order) .
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.516"> Volume 6, Book 61, Number 516: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Mas'ud:
        <p> Surat Bani-lsrael, Al-Kahf (The Cave), Maryam, Taha, Al-Anbiya' (The 
          prophets) are amongst my first earnings and my old property, and (in 
          fact) they are my old property.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.517"> Volume 6, Book 61, Number 517: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara':
        <p> I learnt, 'Glorify the Name of your Lord the Most High' (Surat 
          al-A'la) No 87, before the Prophet came (to Medina) .
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.518"> Volume 6, Book 61, Number 518: </a> </em>
      <blockquote>
        <p> Narrated Shaqiq:
        <p> Abdullah said, "I learnt An-Naza'ir which the Prophet used to recite 
          in pairs in each Rak'a." Then Abdullah got up and Alqama accompanied 
          him to his house, and when Alqama came out, we asked him (about those 
          Suras). He said, "They are twenty Suras that start from the beginning 
          of Al-Mufassal, according to the arrangement done be Ibn Mas'ud, and 
          end with the Suras starting with Ha Mim, e.g. Ha Mim (the Smoke). and 
          "About what they question one another?" (78.1)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.519"> Volume 6, Book 61, Number 519: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The Prophet was the most generous person, and he used to become more 
          so (generous) particularly in the month of Ramadan because Gabriel 
          used to meet him every night of the month of Ramadan till it elapsed. 
          Allah's Apostle used to recite the Qur'an for him. When Gabriel met 
          him, he used to become more generous than the fast wind in doing 
          good.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.520"> Volume 6, Book 61, Number 520: </a> </em>
      <blockquote>
        <p> Narrated Abu-Huraira: Gabriel used to repeat the recitation of the 
          Qur'an with the Prophet once a year, but he repeated it twice with him
          in the year he died. The Prophet used to stay in I'tikaf for ten days 
          every year (in the month of Ramadan), but
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.521"> Volume 6, Book 61, Number 521: </a> </em>
      <blockquote>
        <p> Narrated Masriq:
        <p> 'Abdullah bin 'Amr mentioned 'Abdullah bin Masud and said, "I shall 
          ever love that man, for I heard the Prophet saying, 'Take (learn) the 
          Qur'an from four: 'Abdullah bin Masud, Salim, Mu'adh and Ubai bin 
          Ka'b.' "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="006.061.522"> Volume 6, Book 61, Number 522: </a> </em>
      <blockquote>
        <p> Narrated Shaqiq bin Salama:
        <p> Once 'Abdullah bin Mas'ud delivered a sermon before us and said, "By 
          Allah, I learnt over seventy Suras direct from Allah's Apostle . By 
          Allah, the companions of the Prophet came to know that I am one of 
          those who know Allah's Book best of all of them, yet I am not the best
          of them." Shaqiq added: I sat in his religious gathering and I did not
          hear anybody opposing him (in his speech).
        <p>        
      </blockquote>
      <hr>
