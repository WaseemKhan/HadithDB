          Apostle used to move his tongue and lips, and that used to be hard for
          him, and one could easily recognize that he was being inspired 
          Divinely. So Allah revealed the Verse which occurs in the Surah 
          starting with "I do swear by the Day of Resurrection.'  (75.1)  i.e. 
          'Move not your tongue concerning (the Qur'an) to make haste then with.
          It is for Us to collect it (in your mind) (O Muhammad) an give you the
          ability to recite it 'by heart.'   (75.16-17) which means: It is for 
          us to collect it (in your mind) and give you the ability to recite it 
          by heart. And when We have recited it to you (O Muhammad) through 
          Gabriel then follow you its recital. (75.18)  means: 'When We reveal 
          it (the Qur'an) to you, Listen to it.' for then: It is for Us to 
          explain it and make it clear to you'    (75.19) i.e. It is up to Us to
          explain it through your tongue. So, when Gabriel came to him, Allah's 
          Apostle would listen to him attentively, and as soon as Gabriel left, 
          he would recite the Revelations, as Allah had promised him.
