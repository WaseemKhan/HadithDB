          me, and you have taught me Surat-al-Furqan." The Prophet said, "O 
          Hisham, recite!" So he recited in the same way as I heard him recite 
          it before. On that Allah's Apostle said, "It was revealed to be 
          recited in this way." Then Allah's Apostle said, "Recite, O 'Umar!" So
          I recited it as he had taught me. Allah's Apostle then said, "It was 
          revealed to be recited in this way." Allah" Apostle added, "The Quran 
          has been revealed to be recited in several different ways, so recite 
          of it that which is easier for you."
        <p>        
