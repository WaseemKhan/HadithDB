          <p> Narrated Abu Huraira:
          <p> Allah's Apostle said, "There will be three types of people whom Allah 
            will neither speak to them on the Day of Resurrection nor will purify 
            them from sins, and they will have a painful punishment: They are, (1)
            a man possessed superfluous water (more than he needs) on a way and he
            withholds it from the travelers. (2) a man who gives a pledge of 
            allegiance to an Imam (ruler) and gives it only for worldly benefits, 
            if the Imam gives him what he wants, he abides by his pledge, 
            otherwise he does not fulfill his pledge; (3) and a man who sells 
            something to another man after the 'Asr prayer and swears by Allah (a 
            false oath) that he has been offered so much for it whereupon the 
            buyer believes him and buys it although in fact, the seller has not 
            been offered such a price." (See Hadith No. 838, Vol. 3)
          <p>        
        </blockquote>
