      <div class="content"><em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 89: </a> </em>
        <h1> <em> Judgments (Ahkaam) </em> </h1>
        <hr>
        <p> <em> <a name="009.089.251"> Volume 9, Book 89, Number 251: </a> </em>
        <blockquote>
          <p> Narrated Abu Huraira:
          <p> Allah's Apostle said, "Whoever obeys me, obeys Allah, and whoever 
            disobeys me, disobeys Allah, and whoever obeys the ruler I appoint, 
            obeys me, and whoever disobeys him, disobeys me."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.252"> Volume 9, Book 89, Number 252: </a> </em>
        <blockquote>
          <p> Narrated 'Abdullah bin 'Umar:
          <p> Allah's Apostle said, "Surely! Everyone of you is a guardian and is 
            responsible for his charges: The Imam (ruler) of the people is a 
            guardian and is responsible for his subjects; a man is the guardian of
            his family (household) and is responsible for his subjects; a woman is
            the guardian of her husband's home and of his children and is 
            responsible for them; and the slave of a man is a guardian of his 
            master's property and is responsible for it. Surely, everyone of you 
            is a guardian and responsible for his charges."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.253"> Volume 9, Book 89, Number 253: </a> </em>
        <blockquote>
          <p> Narrated Muhammad bin Jubair bin Mut'im:
          <p> That while he was included in a delegation of Quraish staying with 
            Muawiya, Muawiya heard that 'Abdullah bin 'Amr had said that there 
            would be a king from Qahtan tribe, whereupon he became very angry. He 
            stood up, and after glorifying and praising Allah as He deserved, 
            said, "To proceed, I have come to know that some of you men are 
            narrating things which are neither in Allah's Book, nor has been 
            mentioned by Allah's Apostle . Such people are the ignorant among you.
            Beware of such vain desires that mislead those who have them. I have 
            heard Allah's Apostle saying, 'This matter (of the caliphate) will 
            remain with the Quraish, and none will rebel against them, but Allah 
            will throw him down on his face as long as they stick to the rules and
            regulations of the religion (Islam).'"
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.254"> Volume 9, Book 89, Number 254: </a> </em>
        <blockquote>
          <p> Narrated Ibn 'Umar:
          <p> Allah's Apostle said, "This matter (caliphate) will remain with the 
            Quraish even if only two of them were still existing."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.255"> Volume 9, Book 89, Number 255: </a> </em>
        <blockquote>
          <p> Narrated 'Abdullah:
          <p> Allah's Apostle said, "Do not wish to be like anyone, except in two 
            cases: (1) A man whom Allah has given wealth and he spends it 
            righteously. (2) A man whom Allah has given wisdom (knowledge of the 
            Quran and the Hadith) and he acts according to it and teaches it to 
            others."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.256"> Volume 9, Book 89, Number 256: </a> </em>
        <blockquote>
          <p> Narrated Anas bin Malik:
          <p> Allah's Apostle said, "You should listen to and obey, your ruler even 
            if he was an Ethiopian (black) slave whose head looks like a raisin."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.257"> Volume 9, Book 89, Number 257: </a> </em>
        <blockquote>
          <p> Narrated Ibn 'Abbas:
          <p> The Prophet said, "If somebody sees his Muslim ruler doing something 
            he disapproves of, he should be patient, for whoever becomes separate 
            from the Muslim group even for a span and then dies, he will die as 
            those who died in the Pre-lslamic period of ignorance (as rebellious 
            sinners). (See Hadith No. 176 and 177)
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.258"> Volume 9, Book 89, Number 258: </a> </em>
        <blockquote>
          <p> Narrated 'Abdullah:
          <p> The Prophet said, "A Muslim has to listen to and obey (the order of 
            his ruler) whether he likes it or not, as long as his orders involve 
            not one in disobedience (to Allah), but if an act of disobedience (to 
            Allah) is imposed one should not listen to it or obey it. (See 
            Hadith No. 203, Vol. 4)
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.259"> Volume 9, Book 89, Number 259: </a> </em>
        <blockquote>
          <p> Narrated 'Ali:
          <p> The Prophet sent an army unit (for some campaign) and appointed a man 
            from the Ansar as its commander and ordered them (the soldiers) to 
            obey him. (During the campaign) he became angry with them and said, 
            "Didn't the Prophet order you to obey me?" They said, "Yes." He said, 
            "I order you to collect wood and make a fire and then throw yourselves
            into it." So they collected wood and made a fire, but when they were 
            about to throw themselves into, it they started looking at each other,
            and some of them said, "We followed the Prophet to escape from the 
            fire. How should we enter it now?" So while they were in that state, 
            the fire extinguished and their commander's anger abated. The event 
            was mentioned to the Prophet and he said, "If they had entered it (the
            fire) they would never have come out of it, for obedience is required 
            only in what is good." (See Hadith No. 629. Vol. 5)
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.260"> Volume 9, Book 89, Number 260: </a> </em>
        <blockquote>
          <p> Narrated 'Abdur-Rahman bin Samura:
          <p> The Prophet said, "O 'Abdur-Rahman! Do not seek to be a ruler, for if 
            you are given authority on your demand then you will be held 
            responsible for it, but if you are given it without asking (for it), 
            then you will be helped (by Allah) in it. If you ever take an oath to 
            do something and later on you find that something else is better, then
            you should expiate your oath and do what is better."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.261"> Volume 9, Book 89, Number 261: </a> </em>
        <blockquote>
          <p> Narrated 'Abdur-Rahman bin Samura:
          <p> Allah's Apostle said, "O 'Abdur-Rahman bin Samura! Do not seek to be a
            ruler, for if you are given authority on your demand, you will be held
            responsible for it, but if you are given it without asking for it, 
            then you will be helped (by Allah) in it. If you ever take an oath to 
            do something and later on you find that something else is better, then
            do what is better and make expiation for your oath."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.262"> Volume 9, Book 89, Number 262: </a> </em>
        <blockquote>
          <p> Narrated Abu Huraira:
          <p> The Prophet said, "You people will be keen to have the authority of 
            ruling which will be a thing of regret for you on the Day of 
            Resurrection. What an excellent wet nurse it is, yet what a bad 
            weaning one it is!"
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.263"> Volume 9, Book 89, Number 263: </a> </em>
        <blockquote>
          <p> Narrated Abu Musa:
          <p> Two men from my tribe and I entered upon the Prophet. One of the two 
            men said to the Prophet, "O Allah's Apostle! Appoint me as a 
            governor," and so did the second. The Prophet said, "We do not assign 
            the authority of ruling to those who ask for it, nor to those who are 
            keen to have it."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.264"> Volume 9, Book 89, Number 264: </a> </em>
        <blockquote>
          <p> Narrated Ma'qil:
          <p> I heard the Prophet saying, "Any man whom Allah has given the 
            authority of ruling some people and he does not look after them in an 
            honest manner, will never feel even the smell of Paradise."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.265"> Volume 9, Book 89, Number 265: </a> </em>
        <blockquote>
          <p> Narrated Ma'qil:
          <p> Allah's Apostle said, "If any ruler having the authority to rule 
            Muslim subjects dies while he is deceiving them, Allah will forbid 
            Paradise for him."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.266"> Volume 9, Book 89, Number 266: </a> </em>
        <blockquote>
          <p> Narrated Tarif Abi Tamima:
          <p> I saw Safwan and Jundab and Safwan's companions when Jundab was 
            advising. They said, "Did you hear something from Allah's Apostle?" 
            Jundab said, "I heard him saying, 'Whoever does a good deed in order 
            to show off, Allah will expose his intentions on the Day of 
            Resurrection (before the people), and whoever puts the people into 
            difficulties, Allah will put him into difficulties on the Day of 
            Resurrection.'" The people said (to Jundab), "Advise us." He said, 
            "The first thing of the human body to purify is the abdomen, so he who
            can eat nothing but good food (Halal and earned lawfully) should do 
            so, and he who does as much as he can that nothing intervene between 
            him and Paradise by not shedding even a handful of blood, (i.e. 
            murdering) should do so."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.267"> Volume 9, Book 89, Number 267: </a> </em>
        <blockquote>
          <p> Narrated Anas bin Malik:
          <p> Apostle." The Prophet said, "You will be with the one whom you love."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.268"> Volume 9, Book 89, Number 268: </a> </em>
        <blockquote>
          <p> Narrated Thabit Al-Bunani:
          <p> Anas bin Malik said to a woman of his family, "Do you know 
            such-and-such a woman?" She replied, "Yes." He said, "The Prophet 
            passed by her while she was weeping over a grave, and he said to her, 
            'Be afraid of Allah and be patient.' The woman said (to the Prophet). 
            'Go away from me, for you do not know my calamity.'" Anas added, "The 
            Prophet left her and proceeded. A man passed by her and asked her, 
            'What has Allah's Apostle said to you?' She replied, 'I did not 
            recognize him.' The man said, 'He was Allah's Apostle."' Anas added, 
            "So that woman came to the gate of the Prophet and she did not find a 
            gate-keeper there, and she said, 'O Allah's Apostle! By Allah. I did 
            not recognize you!' The Prophet said, 'No doubt, patience is at the 
            first stroke of a calamity.'"
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.269"> Volume 9, Book 89, Number 269: </a> </em>
        <blockquote>
          <p> Narrated Anas:
          <p> Qais bin Sa'd was to the Prophet like a chief police officer to an 
            Amir (chief).
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.270"> Volume 9, Book 89, Number 270: </a> </em>
        <blockquote>
          <p> Narrated Abu Musa:
          <p> that the Prophet sent him and sent Mu'adh after him (as rulers to 
            Yemen).
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.271"> Volume 9, Book 89, Number 271: </a> </em>
        <blockquote>
          <p> Narrated Abu Musa:
          <p> A man embraced Islam and then reverted back to Judaism. Mu'adh bin 
            Jabal came and saw the man with Abu Musa. Mu'adh asked, "What is wrong
            with this (man)?" Abu Musa replied, "He embraced Islam and then 
            reverted back to Judaism." Mu'adh said, "I will not sit down unless 
            you kill him (as it is) the verdict of Allah and His Apostle
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.272"> Volume 9, Book 89, Number 272: </a> </em>
        <blockquote>
          <p> Narrated 'Abdur Rahman bin Abi Bakra:
          <p> Abu Bakra wrote to his son who was in Sijistan: 'Do not judge between 
            two persons when you are angry, for I heard the Prophet saying, "A 
            judge should not judge between two persons while he is in an angry 
            mood."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.273"> Volume 9, Book 89, Number 273: </a> </em>
        <blockquote>
          <p> Narrated Abu Mas'ud Al-Ansari:
          <p> A man came to Allah's Apostle and said, "O Allah's Apostle! By Allah, 
            I fail to attend the morning congregational prayer because so-and-so 
            (i.e., Muadh bin Jabal) prolongs the prayer when he leads us for it." 
            I had never seen the Prophet more furious in giving advice than he was
            on that day. He then said, "O people! some of you make others dislike 
            (good deeds, i.e. prayers etc). So whoever among you leads the people 
            in prayer, he should shorten it because among them there are the old, 
            the weak and the busy (needy having some jobs to do).  (See 
            Hadith No. 90, Vol. 1)
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.274"> Volume 9, Book 89, Number 274: </a> </em>
        <blockquote>
          <p> Narrated 'Abdullah bin 'Umar:
          <p> That he had divorced his wife during her menses. 'Umar mentioned that 
            to the Prophet. Allah's Apostle became angry and said, "He must take 
            her back (his wife) and keep her with him till she becomes clean from 
            her menses and then to wait till she gets her next period and becomes 
            clean again from it and only then, if he wants to divorce her, he may 
            do so."
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.275"> Volume 9, Book 89, Number 275: </a> </em>
        <blockquote>
          <p> Narrated 'Aisha:
          <p> Hind bint 'Utba bin Rabia came and said. "O Allah's Apostle! By Allah,
            there was no family on the surface of the earth, I like to see in 
            degradation more than I did your family, but today there is no family 
            on the surface of the earth whom I like to see honored more than 
            yours." Hind added, "Abu Sufyan is a miser. Is it sinful of me to feed
            our children from his property?" The Prophet said, "There is no blame 
            on you if you feed them (thereof) in a just and reasonable manner.
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.276"> Volume 9, Book 89, Number 276: </a> </em>
        <blockquote>
          <p> Narrated Anas bin Malik:
          <p> When the Prophet intended to write to the Byzantines, the people said,
            "They do not read a letter unless it is sealed (stamped)." Therefore 
            the Prophet took a silver ring----as if I am looking at its glitter 
            now----and its engraving was: 'Muhammad, Apostle of Allah'
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.277"> Volume 9, Book 89, Number 277: </a> </em>
        <blockquote>
          <p> Narrated 'Abdullah bin As-Sa'di:
          <p> That when he went to 'Umar during his Caliphate. 'Umar said to him, 
            "Haven't I been told that you do certain jobs for the people but when 
            you are given payment you refuse to take it?" 'Abdullah added: I said,
            "Yes." 'Umar said, "Why do you do so?" I said, "I have horses and 
            slaves and I am living in prosperity and I wish that my payment should
            be kept as a charitable gift for the Muslims." 'Umar said, "Do not do 
            so, for I intended to do the same as you do. Allah's Apostles used to 
            give me gifts and I used to say to him, 'Give it to a more needy one 
            than me.' Once he gave me some money and I said, 'Give it to a more 
            needy person than me,' whereupon the Prophet said, 'Take it and keep 
            it in your possession and then give it in charity. Take what ever 
            comes to you of this money if you are not keen to have it and not 
            asking for it; otherwise (i.e., if it does not come to you) do not 
            seek to have it yourself.' "
          <p> Narrated 'Abdullah bin 'Umar: I have heard 'Umar saying, "The Prophet 
            used to give me some money (grant) and I would say (to him), 'Give it 
            to a more needy one than me.' Once he gave me some money and I said, 
            'Give it to a more needy one than me.' The Prophet said (to me), 'Take
            it and keep it in your possession and then give it in charity. Take 
            whatever comes to you of this money while you are not keen to have it 
            and not asking for it; take it, but you should not seek to have what 
            you are not given. ' "
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.278"> Volume 9, Book 89, Number 278: </a> </em>
        <blockquote>
          <p> Narrated Sahl bin Sa'd:
          <p> I witnessed a husband and a wife who were involved in a case of Lian. 
            Then (the judgment of) divorce was passed. I was fifteen years of age,
            at that time.
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.279"> Volume 9, Book 89, Number 279: </a> </em>
        <blockquote>
          <p> Narrated Sahl:
          <p> (the brother of Bani Sa'ida) A man from the Ansar came to the Prophet 
            and said, "If a man finds another man sleeping with his wife, should 
            he kill him?" That man and his wife then did Lian in the mosque while 
            I was present.
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.280"> Volume 9, Book 89, Number 280: </a> </em>
        <blockquote>
          <p> Narrated Abu Huraira:
          <p> A man came to Allah's Apostle while he was in the mosque, and called 
            him, saying, "O Allah's Apostle! I have committed illegal sexual 
            intercourse." The Prophet turned his face to the other side, but when 
            the man gave four witnesses against himself, the Prophet said to him, 
            "Are you mad?" The man said, "No." So the Prophet said (to his 
            companions), "Take him away and stone him to death. "
          <p>        
        </blockquote>
        <hr>
        <em> <a name="009.089.281"> Volume 9, Book 89, Number 281: </a> </em>
