      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 82: </a> </em>
      <h1> <em> Punishment of Disbelievers at War with Allah and His Apostle </em> </h1>
      <hr>
      <p> <em> <a name="008.082.794"> Volume 8, Book 82, Number 794: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> Some people from the tribe of 'Ukl came to the Prophet and embraced 
          Islam. The climate of Medina did not suit them, so the Prophet ordered
          them to go to the (herd of milch) camels of charity and to drink, 
          their milk and urine (as a medicine). They did so, and after they had 
          recovered from their ailment (became healthy) they turned renegades 
          (reverted from Islam) and killed the shepherd of the camels and took 
          the camels away. The Prophet sent (some people) in their pursuit and 
          so they were (caught and) brought, and the Prophets ordered that their
          hands and legs should be cut off and that their eyes should be branded
          with heated pieces of iron, and that their cut hands and legs should 
          not be cauterized, till they die.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.795"> Volume 8, Book 82, Number 795: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> The Prophet cut off the hands and feet of the men belonging to the 
          tribe of 'Uraina and did not cauterise (their bleeding limbs) till 
          they died.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.796"> Volume 8, Book 82, Number 796: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> A group of people from 'Ukl (tribe) came to the Prophet and they were 
          living with the people of As-Suffa, but they became ill as the climate
          of Medina did not suit them, so they said, "O Allah's Apostle! Provide
          us with milk." The Prophet said, I see no other way for you than to 
          use the camels of Allah's Apostle." So they went and drank the milk 
          and urine of the camels, (as medicine) and became healthy and fat. 
          Then they killed the shepherd and took the camels away. When a 
          help-seeker came to Allah's Apostle, he sent some men in their 
          pursuit, and they were captured and brought before mid day. The 
          Prophet ordered for some iron pieces to be made red hot, and their 
          eyes were branded with them and their hands and feet were cut off and 
          were not cauterized. Then they were put at a place called Al-Harra, 
          and when they asked for water to drink they were not given till they 
          died. (Abu Qilaba said, "Those people committed theft and murder and 
          fought against Allah and His Apostle.")
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.797"> Volume 8, Book 82, Number 797: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> A group of people from 'Ukl (or 'Uraina) tribe ----but I think he said
          that they were from 'Ukl came to Medina and (they became ill, so) the 
          Prophet ordered them to go to the herd of (Milch) she-camels and told 
          them to go out and drink the camels' urine and milk (as a medicine). 
          So they went and drank it, and when they became healthy, they killed 
          the shepherd and drove away the camels. This news reached the Prophet 
          early in the morning, so he sent (some) men in their pursuit and they 
          were captured and brought to the Prophet before midday. He ordered to 
          cut off their hands and legs and their eyes to be branded with heated 
          iron pieces and they were thrown at Al-Harra, and when they asked for 
          water to drink, they were not given water. (Abu Qilaba said, "Those 
          were the people who committed theft and murder and reverted to 
          disbelief after being believers (Muslims), and fought against Allah 
          and His Apostle").
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.798"> Volume 8, Book 82, Number 798: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "Seven (people) will be shaded by Allah by His Shade
          on the Day of Resurrection when there will be no shade except His 
          Shade. (They will be), a just ruler, a young man who has been brought 
          up in the worship of Allah, a man who remembers Allah in seclusion and
          his eyes are then flooded with tears, a man whose heart is attached to
          mosques (offers his compulsory congregational prayers in the mosque), 
          two men who love each other for Allah's Sake, a man who is called by a
          charming lady of noble birth to commit illegal sexual intercourse with
          her, and he says, 'I am afraid of Allah,' and (finally), a man who 
          gives in charity so secretly that his left hand does not know what his
          right hand has given."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.799"> Volume 8, Book 82, Number 799: </a> </em>
      <blockquote>
        <p> Narrated Sahl bin Sa'd:
        <p> The Prophet said, "Whoever guarantees me (the chastity of) what is 
          between his legs (i.e. his private parts), and what is between his 
          jaws (i.e., his tongue), I guarantee him Paradise."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.800i"> Volume 8, Book 82, Number 800i: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> I will narrate to you a narration which nobody will narrate to you 
          after me. I heard that form the Prophet. I heard the Prophet saying, 
          "The Hour sill not be established" or said: "From among the portents 
          of the Hour is that the religious knowledge will betaken away (by the 
          death of religious Scholars) and general ignorance (of religion) will 
          appear; and the drinking of alcoholic drinks will be very common, and 
          (open) illegal sexual intercourse will prevail, and men will decrease 
          in number while women will increase so much so that, for fifty women 
          there will only be one man to look after them." 
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.800e"> Volume 8, Book 82, Number 800e: </a> </em>
      <blockquote>
        <p> Narrated 'Ikrima from Ibn 'Abbas:
        <p> Allah's Apostles said, "When a slave (of Allah) commits illegal sexual
          intercourse, he is not a believer at the time of committing it; and if
          he steals, he is not a believer at the time of stealing; and if he 
          drinks an alcoholic drink, when he is not a believer at the time of 
          drinking it; and he is not a believer when he commits a murder," 
          'Ikrima said: I asked Ibn Abbas, "How is faith taken away from him?" 
          He said, Like this," by clasping his hands and then separating them, 
          and added, "But if he repents, faith returns to him like this, by 
          clasping his hands again.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.801"> Volume 8, Book 82, Number 801: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "The one who commits an illegal sexual intercourse 
          is not a believer at the time of committing illegal sexual intercourse
          and a thief is not a believer at the time of committing theft and a 
          drinker of alcoholic drink is not a believer at the time of drinking. 
          Yet, (the gate of) repentance is open thereafter."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.802"> Volume 8, Book 82, Number 802: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Mas'ud:
        <p> I said, "O Allah's Apostle! Which is the biggest sin?" He said, "To 
          set up rivals to Allah by worshipping others though He alone has 
          created you." I asked, "What is next?" He said, "To kill your child 
          lest it should share your food." I asked, "What is next?" He said, "To
          commit illegal sexual intercourse with the wife of your neighbor."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.803"> Volume 8, Book 82, Number 803: </a> </em>
      <blockquote>
        <p> Narrated Ash-Sha'bi:
        <p> from 'Ali when the latter stoned a lady to death on a Friday. 'Ali 
          said, "I have stoned her according to the tradition of Allah's 
          Apostle."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.804"> Volume 8, Book 82, Number 804: </a> </em>
      <blockquote>
        <p> Narrated Ash Shaibani:
        <p> I asked 'Abdullah bin Abi Aufa, 'Did Allah's Apostle carry out the 
          Rajam penalty ( i.e., stoning to death)?' He said, "Yes." I said, 
          "Before the revelation of Surat-ar-Nur or after it?" He replied, "I 
          don't Know."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.805"> Volume 8, Book 82, Number 805: </a> </em>
      <blockquote>
        <p> Narrated Jabir bin Abdullah Al-Ansari:
        <p> A man from the tribe of Bani Aslam came to Allah's Apostle and 
          Informed him that he had committed illegal sexual intercourse and bore
          witness four times against himself. Allah's Apostle ordered him to be 
          stoned to death as he was a married Person.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.806"> Volume 8, Book 82, Number 806: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> A man came to Allah's Apostle while he was in the mosque, and he 
          called him, saying, "O Allah's Apostle! I have committed illegal 
          sexual intercourse.'" The Prophet turned his face to the other side, 
          but that man repeated his statement four times, and after he bore 
          witness against himself four times, the Prophet called him, saying, 
          "Are you mad?" The man said, "No." The Prophet said, "Are you 
          married?" The man said, "Yes." Then the Prophet said, 'Take him away 
          and stone him to death." Jabir bin 'Abdullah said: I was among the 
          ones who participated in stoning him and we stoned him at the Musalla.
          When the stones troubled him, he fled, but we over took him at 
          Al-Harra and stoned him to death.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.807"> Volume 8, Book 82, Number 807: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Sa'd bin Abi Waqqas and 'Abd bin Zam'a quarrelled with each other 
          (regarding a child). The Prophet said, "The boy is for you, O 'Abd bin
          Zam'a, for the boy is for (the owner) of the bed. O Sauda ! Screen 
          yourself from the boy." The sub-narrator, Al-Laith added (that the 
          Prophet also said), "And the stone is for the person who commits an 
          illegal sexual intercourse."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.808"> Volume 8, Book 82, Number 808: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "The boy is for (the owner of) the bed and the stone
          is for the person who commits illegal sexual intercourse.'
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.809"> Volume 8, Book 82, Number 809: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> A Jew and a Jewess were brought to Allah's Apostle on a charge of 
          committing an illegal sexual intercourse. The Prophet asked them. 
          "What is the legal punishment (for this sin) in your Book (Torah)?" 
          They replied, "Our priests have innovated the punishment of blackening
          the faces with charcoal and Tajbiya."  'Abdullah bin Salam said, "O 
          Allah's Apostle, tell them to bring the Torah." The Torah was brought,
          and then one of the Jews put his hand over the Divine Verse of the 
          Rajam (stoning to death) and started reading what preceded and what 
          followed it. On that, Ibn Salam said to the Jew, "Lift up your hand." 
          Behold! The Divine Verse of the Rajam was under his hand. So Allah's 
          Apostle ordered that the two (sinners) be stoned to death, and so they
          were stoned. Ibn 'Umar added: So both of them were stoned at the Balat
          and I saw the Jew sheltering the Jewess.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.810"> Volume 8, Book 82, Number 810: </a> </em>
      <blockquote>
        <p> Narrated Jabir:
        <p> A man from the tribe of Aslam came to the Prophet and confessed that 
          he had committed an illegal sexual intercourse. The Prophet turned his
          face away from him till the man bore witness against himself four 
          times. The Prophet said to him, "Are you mad?" He said "No." He said, 
          "Are you married?" He said, "Yes." Then the Prophet ordered that he be
          stoned to death, and he was stoned to death at the Musalla. When the 
          stones troubled him, he fled, but he was caught and was stoned till he
          died. The Prophet spoke well of him and offered his funeral prayer.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.811d"> Volume 8, Book 82, Number 811d: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> A person had sexual relation with his wife in the month of Ramadan 
          (while he was fasting), and he came to Allah's Apostle seeking his 
          verdict concerning that action. The Prophet said (to him), "Can you 
          afford to manumit a slave?" The man said, "No." The Prophet said, "Can
          you fast for two successive months?" He said, "No." The Prophet said, 
          "Then feed sixty poor persons."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.811e"> Volume 8, Book 82, Number 811e: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> A man came to the Prophet in the mosque and said, "I am burnt 
          (ruined)!" The Prophet asked him, "With what (what have you done)?" He
          said, "I have had sexual relation with my wife in the month of Ramadan
          (while fasting)." The Prophet said to him, "Give in charity." He said,
          "I have nothing." The man sat down, and in the meantime there came a 
          person driving a donkey carrying food to the Prophet ..... (The 
          sub-narrator, 'Abdur Rahman added: I do not know what kind of food it 
          was). On that the Prophet said, "Where is the burnt person?" The man 
          said, "Here I am." The Prophet said to him, "Take this (food) and give
          it in charity (to someone)." The man said, "To a poorer person than l?
          My family has nothing to eat." Then the Prophet  said to him, "Then 
          eat it yourselves."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.812"> Volume 8, Book 82, Number 812: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> While I was with the Prophet a man came and said, "O Allah's Apostle! 
          I have committed a legally punishable sin; please inflict the legal 
          punishment on me'.' The Prophet did not ask him what he had done. Then
          the time for the prayer became due and the man offered prayer along 
          with the Prophet , and when the Prophet had finished his prayer, the 
          man again got up and said, "O Allah's Apostle! I have committed a 
          legally punishable sin; please inflict the punishment on me according 
          to Allah's Laws." The Prophet said, "Haven't you prayed with us?' He 
          said, "Yes." The Prophet said, "Allah has forgiven your sin." or said,
          "....your legally punishable sin."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.813"> Volume 8, Book 82, Number 813: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> When Ma'iz bin Malik came to the Prophet (in order to confess), the 
          Prophet said to him, "Probably you have only kissed (the lady), or 
          winked, or looked at her?" He said, "No, O Allah's Apostle!" The 
          Prophet said, using no euphemism, "Did you have sexual intercourse 
          with her?" The narrator added: At that, (i.e. after his confession) 
          the Prophet ordered that he be stoned (to death).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.814"> Volume 8, Book 82, Number 814: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> A man from among the people, came to Allah's Apostle while Allah's 
          Apostle was sitting in the mosque, and addressed him, saying, "O 
          Allah's Apostle! I have committed an illegal sexual intercourse." The 
          Prophet turned his face away from him. The man came to that side to 
          which the Prophet had turned his face, and said, "O Allah's Apostle! I
          have committed an illegal intercourse." The Prophet turned his face to
          the other side, and the man came to that side, and when he confessed 
          four times, the Prophet called him and said, "Are you mad?" He said, 
          "No, O Allah's Apostle!" The Prophet said, "Are you married?" He said,
          "Yes, O Allah's Apostle." The Prophet said (to the people), "Take him 
          away and stone him to death." Ibn Shihab added, "I was told by one who
          heard Jabir, that Jabir said, 'I was among those who stoned the man, 
          and we stoned him at the Musalla ('Id praying Place), and when the 
          stones troubled him, he jumped quickly and ran away, but we overtook 
          him at Al-Harra and stoned him to death (there).' "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.082.815"> Volume 8, Book 82, Number 815: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira and Zaid bin Khalid:
        <p> While we were with the Prophet , a man stood up and said (to the 
          Prophet ), "I beseech you by Allah, that you should judge us according
          to Allah's Laws." Then the man's opponent who was wiser than him, got 
          up saying (to Allah's Apostle) "Judge us according to Allah's Law and 
          kindly allow me (to speak)." The Prophet said, "'Speak." He said, "My 
          son was a laborer working for this man and he committed an illegal 
          sexual intercourse with his wife, and I gave one-hundred sheep and a 
          slave as a ransom for my son's sin. Then I asked a learned man about 
          this case and he informed me that my son should receive one hundred 
          lashes and be exiled for one year, and the man's wife should be stoned
          to death." The Prophet said, "By Him in Whose Hand my soul is, I will 
          judge you according to the Laws of Allah. Your one-hundred sheep and 
          the slave are to be returned to you, and your son has to receive 
          one-hundred lashes and be exiled for one year. O Unais! Go to the wife
          of this man, and if she confesses, then stone her to death." Unais 
          went to her and she confessed. He then stoned her to death.
        <p>        
      </blockquote>
      <hr>
