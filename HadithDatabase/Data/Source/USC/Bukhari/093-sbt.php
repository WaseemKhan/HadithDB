      <em> <a name="009.093.647"> Volume 9, Book 93, Number 647: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> The Prophet said, "The painters of these pictures will be punished on 
          the Day of Resurrection, and it will be said to them, 'Make alive what
          you have created."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.648"> Volume 9, Book 93, Number 648: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> I heard the Prophet saying, "Allah said, 'Who are most unjust than 
          those who try to create something like My creation? I challenge them 
          to create even a smallest ant, a wheat grain or a barley grain.' "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.649"> Volume 9, Book 93, Number 649: </a> </em>
      <blockquote>
        <p> Narrated Abu Musa:
        <p> The Prophet said, 'The example of a believer who recites the Qur'an is
          that of a citron (a citrus fruit) which is good in taste and good in 
          smell. And the believer who does not recite the Quran is like a date 
          which has a good taste but no smell. And the example of an impious 
          person who recites the Qur'an is that of Ar-Rihana (an aromatic plant)
          which smells good but is bitter in taste. And the example of an 
          impious person who does not recite the Quran is that of a colocynth 
          which is bitter in taste and has no smell."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.650"> Volume 9, Book 93, Number 650: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Some people asked the Prophet regarding the soothsayers. He said, 
          "They are nothing." They said, "O Allah's Apostle! Some of their talks
          come true." The Prophet said, "That word which happens to be true is 
          what a Jinn snatches away by stealth (from the Heaven) and pours it in
          the ears of his friend (the foreteller) with a sound like the cackling
          of a hen. The soothsayers then mix with that word, one hundred lies."
        <p>        
      </blockquote>
