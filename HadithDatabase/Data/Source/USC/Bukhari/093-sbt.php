      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 93: </a> </em>
      <h1> <em> ONENESS, UNIQUENESS OF ALLAH (TAWHEED) </em> </h1>
      <hr>
      <p> <em> <a name="009.093.469"> Volume 9, Book 93, Number 469: </a> </em>
      <blockquote>
        <p> Narrated Ibn Abbas:
        <p> When the Prophet sent Muadh to Yemen, he said to him, "You are going 
          to a nation from the people of the Scripture, so let the first thing 
          to which you will invite them, be the Tauhid of Allah.  If they learn 
          that, tell them that Allah has enjoined on them, five prayers to be 
          offered in one day and one night.  And if they pray, tell them that 
          Allah  has enjoined on them Zakat of their properties and it is to be 
          taken from the rich among them and given to the poor. And if they 
          agree to that, then take from them Zakat but avoid the best property 
          of the people."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.470"> Volume 9, Book 93, Number 470: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh bin Jabal:
        <p> The Prophet  said, "O Mu'adh! Do you know what Allah's Right upon His 
          slaves is?" I said, "Allah and His Apostle know best." The Prophet 
          said, "To worship Him (Allah) Alone and to join none in worship with 
          Him (Allah). Do you know what their right upon Him is?" I replied, 
          "Allah and His Apostle know best." The Prophet said, "Not to punish 
          them (if they do so)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.471"> Volume 9, Book 93, Number 471: </a> </em>
      <blockquote>
        <p> Narrated Abu Said Al-Khudri:
        <p> A man heard another man reciting (in the prayers): 'Say (O Muhammad): 
          "He is Allah, the One."   (112.1) And he recited it repeatedly. When 
          it was morning, he went to the Prophet and informed him about that as 
          if he considered that the recitation of that Sura by itself was not 
          enough. Allah's Apostle said, "By Him in Whose Hand my life is, it is 
          equal to one-third of the Quran."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.472"> Volume 9, Book 93, Number 472: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet sent (an army unit) under the command of a man who used to
          lead his companions in the prayers and would finish his recitation 
          with (the Sura 112): 'Say (O Muhammad): "He is Allah, the One." ' 
          (112.1) When they returned (from the battle), they mentioned that to 
          the Prophet. He said (to them), "Ask him why he does so." They asked 
          him and he said, "I do so because it mentions the qualities of the 
          Beneficent and I love to recite it (in my prayer)." The Prophet; said 
          (to them), "Tell him that Allah loves him"
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.473"> Volume 9, Book 93, Number 473: </a> </em>
      <blockquote>
        <p> Narrated Jarir bin 'Abdullah:
        <p> Allah's Apostle said, "Allah will not be merciful to those who are not
          merciful to mankind."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.474"> Volume 9, Book 93, Number 474: </a> </em>
      <blockquote>
        <p> Narrated Usama bin Zaid:
        <p> We were with the Prophet when suddenly there came to him a messenger 
          from one of his daughters who was asking him to come and see her son 
          who was dying. The Prophet said (to the messenger), "Go back and tell 
          her that whatever Allah takes is His, and whatever He gives is His, 
          and everything with Him has a limited fixed term (in this world). So 
          order her to be patient and hope for Allah's reward." But she sent the
          messenger to the Prophet again, swearing that he should come to her. 
          So the Prophets got up, and so did Sa'd bin 'Ubada and Mu'adh bin 
          Jabal (and went to her). When the child was brought to the Prophet his
          breath was disturbed in his chest as if it were in a water skin. On 
          that the eyes of the Prophet . became flooded with tears, whereupon 
          Sa'd said to him, "O Allah's Apostle! What is this?" The Prophet said,
          "This is mercy which Allah has put in the heart of His slaves, and 
          Allah bestows His mercy only on those of His slaves who are merciful 
          (to others)." (See Hadith No. 373, Vol. 2)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.475"> Volume 9, Book 93, Number 475: </a> </em>
      <blockquote>
        <p> Narrated Abu Musa Al-Ashari:
        <p> The Prophet said, "None is more patient than Allah against the harmful
          and annoying words He hears (from the people): They ascribe children 
          to Him, yet He bestows upon them health and provision .
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.476"> Volume 9, Book 93, Number 476: </a> </em>
      <blockquote>
        <p> Narrated Ibn Umar:
        <p> The Prophet said, "The keys of the unseen are five and none knows them
          but Allah: (1) None knows what is in the womb, but Allah: 
          (2) None knows what will happen tomorrow,  but Allah; (3) None knows 
          when it will rain, but Allah; (4) None knows where he will die, but 
          Allah (knows that); (5) and none knows when the Hour will be 
          established, but Allah."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.477"> Volume 9, Book 93, Number 477: </a> </em>
      <blockquote>
        <p> Narrated Masruq:
        <p> 'Aisha said, "If anyone tells you that Muhammad has seen his Lord, he 
          is a liar, for Allah says: 'No vision can grasp Him.' (6.103) And if 
          anyone tells you that Muhammad has seen the Unseen, he is a liar, for 
          Allah says: "None has the knowledge of the Unseen but Allah."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.478"> Volume 9, Book 93, Number 478: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> We used to pray behind the Prophet and used to say: "As-Salamu 
          'Al-Allah. The Prophet said, "Allah himself is As-Salam (Name of 
          Allah), so you should say: 'At-Tahiyatu lil-laihi was- sala-watu 
          wat-taiyibatu, as-sallamu 'Alayka aiyuha-n-nabiyyu wa rahrmatu-l-lahi 
          wa barak-atuhu, As-salamu 'alaina wa 'ala 'ibaldi-l-lahi as-salihin. 
          Ashhadu an la ilaha il-lallah, wa ash-hadu anna Muhammadan 'abduhu wa 
          rasuluhu."'
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.479"> Volume 9, Book 93, Number 479: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "On the Day of Resurrection Allah will hold the 
          whole earth and fold the heaven with His right hand and say, 'I am the
          King: where are the kings of the earth?" '
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.480"> Volume 9, Book 93, Number 480: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The Prophet used to say, "I seek refuge (with YOU) by Your 'Izzat, 
          None has the right to be worshipped but You Who does not die while the
          Jinns and the human beings die."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.481"> Volume 9, Book 93, Number 481: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> The Prophet said, "(The people will be thrown into Hell ( Fire) and it
          will keep on saying, 'Is there any more?' till the Lord of the worlds 
          puts His Foot over it, whereupon its different sides will come close 
          to each other, and it will say, 'Qad! Qad! (enough! enough!) By Your 
          'Izzat (Honor and Power) and YOUR KARAM (Generosity)!' Paradise will 
          remain spacious enough to accommodate more people until Allah will 
          create some more people and let them dwell in the superfluous space of
          Paradise. "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.482"> Volume 9, Book 93, Number 482: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The Prophet used to invoke Allah at night, saying, "O Allah: All the 
          Praises are for You: You are the Lord of the Heavens and the Earth. 
          All the Praises are for You; You are the Maintainer of the Heaven and 
          the Earth and whatever is in them. All the Praises are for You; You 
          are the Light of the Heavens and the Earth. Your Word is the Truth, 
          and Your Promise is the Truth, and the Meeting with You is the Truth, 
          and Paradise is the Truth, and the (Hell) Fire is the Truth, and the 
          Hour is the Truth. O Allah! I surrender myself to You, and I believe 
          in You and I depend upon You, and I repent to You and with You (Your 
          evidences) I stand against my opponents, and to you I leave the 
          judgment (for those who refuse my message). O Allah! Forgive me my 
          sins that I did in the past or will do in the future, and also the 
          sins I did in secret or in public. You are my only God (Whom I 
          worship) and there is no other God for me (i.e. I worship none but 
          You)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.483"> Volume 9, Book 93, Number 483: </a> </em>
      <blockquote>
        <p> Narrated Sufyan:
        <p> (regarding the above narration) that the Prophet added, "You are the 
          Truth, and Your Word is the Truth."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.484"> Volume 9, Book 93, Number 484: </a> </em>
      <blockquote>
        <p> Narrated Abu Musa:
        <p> We were with the Prophet on a journey, and whenever we ascended a high
          place, we used to say, "Allahu Akbar." The Prophet said, "Don't 
          trouble yourselves too much! You are not calling a deaf or an absent 
          person, but you are calling One Who Hears, Sees, and is very near." 
          Then he came to me while I was saying in my heart, "La hawla wala 
          quwwatta illa billah (There is neither might nor power but with 
          Allah)." He said, to me, "O 'Abdullah bin Qais! Say, 'La hawla wala 
          quwwata illa billah (There is neither might nor power but with Allah),
          for it is one of the treasures of Paradise." Or said, "Shall I tell 
          you of it?"
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.485"> Volume 9, Book 93, Number 485: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Amr:
        <p> Abu Bakr As-Siddiq said to the Prophet "O Allah's Apostle! Teach me an
          invocation with which I may invoke Allah in my prayers." The Prophet 
          said, "Say: O Allah! I have wronged my soul very much (oppressed 
          myself), and none forgives the sins but You; so please bestow Your 
          Forgiveness upon me. No doubt, You are the Oft-Forgiving, Most 
          Merciful."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.486"> Volume 9, Book 93, Number 486: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet said, "Gabriel called me and said, 'Allah has heard the 
          statement of your people and what they replied to you.'"
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.487"> Volume 9, Book 93, Number 487: </a> </em>
      <blockquote>
        <p> Narrated Jabir bin Abdullah:
        <p> As-Salami: Allah's Apostle used to teach his companions to perform the
          prayer of Istikhara for each and every matter just as he used to teach
          them the Suras from the Quran He used to say, "If anyone of you 
          intends to do some thing, he should offer a two rakat prayer other 
          than the compulsory prayers, and after finishing it, he should say: O 
          Allah! I consult You, for You have all knowledge, and appeal to You to
          support me with Your Power and ask for Your Bounty, for You are able 
          to do things while I am not, and You know while I do not; and You are 
          the Knower of the Unseen. O Allah If You know It this matter (name 
          your matter) is good for me both at present and in the future, (or in 
          my religion), in my this life and in the Hereafter, then fulfill it 
          for me and make it easy for me, and then bestow Your Blessings on me 
          in that matter. O Allah! If You know that this matter is not good for 
          me in my religion, in my this life and in my coming Hereafter (or at 
          present or in the future), then divert me from it and choose for me 
          what is good wherever it may be, and make me be pleased with it." (See
          Hadith No. 391, Vol. 8)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.488"> Volume 9, Book 93, Number 488: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> The Prophet frequently used to swear, "No, by the One Who turns the 
          hearts."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.489"> Volume 9, Book 93, Number 489: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Allah's Apostle said, "Allah has ninety-nine Names, one-hundred less 
          one; and he who memorized them all by heart will enter Paradise." To 
          count something means to know it by heart.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.490"> Volume 9, Book 93, Number 490: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "When anyone of you goes to bed, he should dust it 
          off thrice with the edge of his garment, and say: Bismika Rabbi 
          wada'tu janbi, wa bika arfa'hu. In amsakta nafsi faghfir laha, wa in 
          arsaltaha fahfazha bima tahfaz bihi 'ibadaka-s-salihin."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.491"> Volume 9, Book 93, Number 491: </a> </em>
      <blockquote>
        <p> Narrated Juhaifa:
        <p> When the Prophet went to bed, he used to say, "Allhumma bismika ahya 
          wa amut." And when he got Up in the mornings he used to say, "Alhamdu 
          lillahi al-ladhi ahyana ba'da ma amatana wa ilaihi-n-nushur."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.492"> Volume 9, Book 93, Number 492: </a> </em>
      <blockquote>
        <p> Narrated Abu Dharr:
        <p> When the Prophet went to bed at night, he used to say: "Bismika namutu
          wa nahya." And when he got up in the morning, he used to say, "Alhamdu
          lillahi al-ladhi ahyana ba'da ma amatana, wa ilaihi-n-nushur."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.493"> Volume 9, Book 93, Number 493: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> Allah's Apostle said, "If anyone of you, when intending to have a 
          sexual relation (sleep) with his wife, says: Bismillah, Allahumma 
          jannibna ashShaitan, wa Jannib ash-Shaitana ma razaqtana,  Satan would
          never harm that child, should it be ordained that they will have one. 
          (Because of that sleep)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.494"> Volume 9, Book 93, Number 494: </a> </em>
      <blockquote>
        <p> Narrated 'Adi bin Hatim:
        <p> I asked the Prophet, "I send off (for a game) my trained hunting dogs;
          (what is your verdict concerning the game they hunt?" He said, "If you
          send off your trained hunting dogs and mention the Name of Allah, 
          then, if they catch some game, eat (thereof). And if you hit the game 
          with a mi'rad (a hunting tool) and it wounds it, you can eat (it)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.495"> Volume 9, Book 93, Number 495: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The people said to the Prophet , "O Allah's Apostle! Here are people 
          who have recently embraced Islam and they bring meat, and we do not 
          know whether they had mentioned Allah's Name while slaughtering the 
          animals or not." The Prophet said, "You should mention Allah's Name 
          and eat."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.496"> Volume 9, Book 93, Number 496: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> The Prophet slaughtered two rams as sacrifice and mentioned Allah's 
          Name and said, "Allahu-Akbar" while slaughtering).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.497"> Volume 9, Book 93, Number 497: </a> </em>
      <blockquote>
        <p> Narrated Jundab:
        <p> That he witnessed the Prophet on the Day of Nahr. The Prophet offered 
          prayer and then delivered a sermon saying, "Whoever slaughtered his 
          sacrifice before offering prayer, should slaughter another animal in 
          place of the first; and whoever has not yet slaughtered any, should 
          slaughter a sacrifice and mention Allah's Name while doing so."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.498"> Volume 9, Book 93, Number 498: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> The Prophet said, "Do not swear by your fathers; and whoever wants to 
          swear should swear by Allah."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.499"> Volume 9, Book 93, Number 499: </a> </em>
      <blockquote>
