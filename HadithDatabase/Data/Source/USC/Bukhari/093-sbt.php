          something, but on finding something else which is better, I do that 
          which is better and make the expiation for my oath.' "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.645"> Volume 9, Book 93, Number 645: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The delegates of 'Abdul Qais came to Allah's Apostle and said, "The 
          pagans of the tribe of Mudar intervene between you and us therefore we
          cannot come to you except in the Holy months. So please order us to do
          something good (Religious deeds) by which we may enter Paradise (by 
          acting on them) and we may inform our people whom we have left behind 
          to observe it." The Prophet said, "I order you to do four things and 
          forbid you from four things: I order you to believe in Allah. Do you 
          know what is meant by belief in Allah? It is to testify that none has 
          the right to be worshipped except Allah, to offer prayers perfectly, 
          to give Zakat, and to give Al-Khumus (one-fifth of the war booty) (in 
          Allah's Cause). And I forbid you four things, (i.e., Do not drink 
          alcoholic drinks) Ad-Dubba, An-Naqir, (pitched water skins), Az-Zuruf,
          Al-Muzaffat and Al--Hantam (names of utensils used for the preparation
          of alcoholic drinks)." (See Hadith No. 50, Vol. 1)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.093.646"> Volume 9, Book 93, Number 646: </a> </em>
      <blockquote>
        <p> Narrated Aisha:
        <p> Allah's Apostle said, "The painter of these pictures will be punished 
          on the Day of Resurrection, and it will be said to them, Make alive 
          what you have created.' "
        <p>        
      </blockquote>
