      <em> <a name="009.086.109"> Volume 9, Book 86, Number 109: </a> </em>
      <blockquote>
        <p> Narrated Abu Rafi':
        <p> The Prophet said, "The neighbor has more right to be taken care of by 
          his neighbor (than anyone else)." Some men said, "If one wants to buy 
          a house for 20,000 Dirhams then there is no harm to play a trick to 
          deprive somebody of preemption by buying it (just on paper) with 
          20,000 Dirhams but paying to the seller only 9,999 Dirhams in cash and
          then agree with the seller to pay only one Dinar in cash for the rest 
          of the price (i.e. 10,001 Dirhams). If the preemptor offers 20,000 
          Dirhams for the house, he can buy it otherwise he has no right to buy 
          it (by this trick he got out of preemption). If the house proves to 
          belong to somebody else other than the seller, the buyer should take 
          back from the seller what he has paid, i.e., 9,999 Dirhams and one 
          Dinar, because if the house proves to belong to somebody else, so the 
          whole bargain (deal) is unlawful. If the buyer finds a defect in the 
          house and it does not belong to somebody other than the seller, the 
          buyer may return it and receive 20,000 Dirhams (instead of 9999 Dirham
          plus one Dinar) which he actually paid.' Abu 'Abdullah said, "So that 
          man allows (some people) the playing of tricks amongst the Muslims 
          (although) the Prophet said, 'In dealing with Muslims one should not 
          sell them sick (animals) or bad things or stolen things."
        <p>        
      </blockquote>
