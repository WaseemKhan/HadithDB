      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 63: </a> </em>
      <h1> <em> Divorce </em> </h1>
      <hr>
      <p> <em> <a name="007.063.178"> Volume 7, Book 63, Number 178: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> that he had divorced his wife while she was menstruating during the 
          lifetime of Allah's Apostle . 'Umar bin Al-Khattab asked Allah's 
          Apostle about that. Allah's Apostle said, "Order him (your son) to 
          take her back and keep her till she is clean and then to wait till she
          gets her next period and becomes clean again, whereupon, if he wishes 
          to keep her, he can do so, and if he wishes to divorce her he can 
          divorce her before having sexual intercourse with her; and that is the
          prescribed period which Allah has fixed for the women meant to be 
          divorced."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.179"> Volume 7, Book 63, Number 179: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Sirin:
        <p> Ibn 'Umar said: "I divorced my wife while she was menstruating. 'Umar 
          mentioned that to the Prophet . The Prophet said, (to my father), "Let
          your son take her back." I asked (Ibn 'Umar), "Is such a divorce 
          counted (i.e. as one legal divorce)?" Ibn 'Umar said, "Of course." 
          Narrated Yunus bin Jubair:  Ibn 'Umar said, "The Prophet said to 
          'Umar, 'Order him (Ibn 'Umar) to take her back.' " I asked, "Is such a
          divorce counted (as one legal divorce)?" Ibn 'Umar said, "What do you 
          think if someone becomes helpless and foolish?" 
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.180"> Volume 7, Book 63, Number 180: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> (Divorcing my wife during her menses) was counted as one legal 
          divorce.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.181"> Volume 7, Book 63, Number 181: </a> </em>
      <blockquote>
        <p> Narrated Al-Awza:
        <p> I asked Az-Zuhri, "Which of the wives of the Prophet sought refuge 
          with Allah from him?" He said "I was told by 'Ursa that 'Aisha said, 
          'When the daughter of Al-Jaun was brought to Allah's Apostle (as his 
          bride) and he went near her, she said, "I seek refuge with Allah from 
          you." He said, "You have sought refuge with The Great; return to your 
          family."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.182"> Volume 7, Book 63, Number 182: </a> </em>
      <blockquote>
        <p> Narrated Abu Usaid:
        <p> We went out with the Prophet to a garden called Ash-Shaut till we 
          reached two walls between which we sat down. The Prophet said, "Sit 
          here," and went in (the garden). The Jauniyya (a lady from Bani Jaun) 
          had been brought and lodged in a house in a date-palm garden in the 
          home of Umaima bint An-Nu'man bin Sharahil, and her wet nurse was with
          her. When the Prophet entered upon her, he said to her, "Give me 
          yourself (in marriage) as a gift." She said, "Can a princess give 
          herself in marriage to an ordinary man?" The Prophet raised his hand 
          to pat her so that she might become tranquil. She said, "I seek refuge
          with Allah from you." He said, "You have sought refuge with One Who 
          gives refuge. Then the Prophet came out to us and said, "O Abu Usaid! 
          Give her two white linen dresses to wear and let her go back to her 
          family." Narrated Sahl and Abu Usaid: The Prophet married Umaima bint 
          Sharahil, and when she was brought to him, he stretched his hand 
          towards her. It seemed that she disliked that, whereupon the Prophet 
          ordered Abu Usaid to prepare her and to provide her with two white 
          linen dresses. (See Hadith No. 541).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.183"> Volume 7, Book 63, Number 183: </a> </em>
      <blockquote>
        <p> Narrated Sahl bin Sad:
        <p> similarly as above (182).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.184"> Volume 7, Book 63, Number 184: </a> </em>
      <blockquote>
        <p> Narrated Abi Ghallab Yunus bin Jubair:
        <p> I asked Ibn 'Umar,"(What is said regarding) a man divorces his wife 
          during her period?" He said, "Do you know Ibn 'Umar? Ibn 'Umar 
          divorced his wife while she was menstruating. 'Umar then went to the 
          Prophet and mentioned that to him. The Prophet ordered him to take her
          back and when she became clean, he could divorce her if he wanted." I 
          asked (Ibn 'Umar), "Was that divorce counted as one legal divorce?"  
          He said, "If one becomes helpless and foolish (will he be excused? Of 
          course not). "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.185"> Volume 7, Book 63, Number 185: </a> </em>
      <blockquote>
        <p> Narrated Sahl bin Sad As-Sa'idi:
        <p> Uwaimir Al-'Ajlani came to 'Asim bin Adi Al-Ansari and asked, "O 
          'Asim! Tell me, if a man sees his wife with another man, should he 
          kill him, whereupon you would kill him in Qisas, or what should he do?
          O 'Asim! Please ask Allah's Apostle about that." 'Asim asked Allah's 
          Apostle about that. Allah's Apostle disliked that question and 
          considered it disgraceful. What 'Asim heard from Allah's Apostle was 
          hard on him. When he returned to his family, 'Uwaimir came to him and 
          said "O 'Asim! What did Allah's Apostle say to you?" 'Asim said, "You 
          never bring me any good. Allah's Apostle disliked to hear the problem 
          which I asked him about." 'Uwaimir said, "By Allah, I will not leave 
          the matter till I ask him about it." So 'Uwaimir proceeded till he 
          came to Allah's Apostle who was in the midst of the people and said, 
          "O Allah's Apostle! If a man finds with his wife another man, should 
          he kill him, whereupon you would kill him (in Qisas): or otherwise, 
          what should he do?" Allah's Apostle said, "Allah has revealed 
          something concerning the question of you and your wife. Go and bring 
          her here." So they both carried out the judgment of Lian, while I was 
          present among the people (as a witness). When both of them had 
          finished, 'Uwaimir said, "O Allah's Apostle! If I should now keep my 
          wife with me, then I have told a lie". Then he pronounced his decision
          to divorce her thrice before Allah's Apostle ordered him to do so. 
          (Ibn Shihab said, "That was the tradition for all those who are 
          involved in a case of Lian."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.186"> Volume 7, Book 63, Number 186: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The wife of Rifa'a Al-Qurazi came to Allah's Apostle and said, "O 
          Allah's Apostle! Rifa'a divorced me irrevocably. After him I married 
          'Abdur-Rahman bin Az-Zubair Al-Qurazi who proved to be impotent." 
          Allah's Apostle said to her, "Perhaps you want to return to Rifa'a? 
          Nay (you cannot return to Rifa'a) until you and 'Abdur-Rahman 
          consummate your marriage."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.187"> Volume 7, Book 63, Number 187: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> A man divorced his wife thrice (by expressing his decision to divorce 
          her thrice), then she married another man who also divorced her. The 
          Prophet was asked if she could legally marry the first husband (or 
          not). The Prophet replied, "No, she cannot marry the first husband 
          unless the second husband consummates his marriage with her, just as 
          the first husband had done."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.188"> Volume 7, Book 63, Number 188: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Allah's Apostle gave us the option (to remain with him or to be 
          divorced) and we selected Allah and His Apostle . So, giving us that 
          option was not regarded as divorce.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.189"> Volume 7, Book 63, Number 189: </a> </em>
      <blockquote>
        <p> Narrated Musruq:
        <p> I asked 'Aisha about the option: She said, "The Prophet gave us the 
          option. Do you think that option was considered as a divorce?" I said,
          "It matters little to me if I give my wife the option once or a 
          hundred times after she has chosen me."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.190"> Volume 7, Book 63, Number 190: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> A man divorced his wife and she married another man who proved to be 
          impotent and divorced her. She could not get her satisfaction from 
          him, and after a while he divorced her. Then she came to the Prophet 
          and said, "O Allah's Apostle! My first husband divorced me and then I 
          married another man who entered upon me to consummate his marriage but
          he proved to be impotent and did not approach me except once during 
          which he benefited nothing from me. Can I remarry my first husband in 
          this case?" Allah's Apostle said, "It is unlawful to marry your first 
          husband till the other husband consummates his marriage with you."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.191"> Volume 7, Book 63, Number 191: </a> </em>
      <blockquote>
        <p> Narrated Said bin Jubair:
        <p> that he heard Ibn 'Abbas saying, "If a man makes his wife unlawful for
          him,  it does not mean that she is divorced." He added, "Indeed in the
          Apostle of Allah , you have a good example to follow."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.192"> Volume 7, Book 63, Number 192: </a> </em>
      <blockquote>
        <p> Narrated 'Ubaid bin 'Umar:
        <p> I heard 'Aisha saying, "The Prophet used to stay for a long while with
          Zanab bint Jahsh and drink honey at her house. So Hafsa and I decided 
          that if the Prophet came to anyone of us, she should say him, "I 
          detect the smell of Maghafir (a nasty smelling gum) in you. Have you 
          eaten Maghafir?' " So the Prophet visited one of them and she said to 
          him similarly. The Prophet said, "Never mind, I have taken some honey 
          at the house of Zainab bint Jahsh, but I shall never drink of it 
          anymore." So there was revealed: 'O Prophet ! Why do you ban (for you)
          that which Allah has made lawful for you . . . If you two (wives of 
          Prophet) turn in repentance to Allah,' (66.1-4) addressing Aisha and 
          Hafsa. 'When the Prophet disclosed a matter in confidence to some of 
          his wives.'  (66.3) namely his saying: But I have taken some honey."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.193"> Volume 7, Book 63, Number 193: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Allah's Apostle was fond of honey and sweet edible things and (it was 
          his habit) that after finishing the 'Asr prayer he would visit his 
          wives and stay with one of them at that time. Once he went to Hafsa, 
          the daughter of 'Umar and stayed with her more than usual. I got 
          jealous and asked the reason for that. I was told that a lady of her 
          folk had given her a skin filled with honey as a present, and that she
          made a syrup from it and gave it to the Prophet to drink (and that was
          the reason for the delay). I said, "By Allah we will play a trick on 
          him (to prevent him from doing so)." So I said to Sada bint Zam'a "The
          Prophet will approach you, and when he comes near you, say: 'Have you 
          taken Maghafir (a bad-smelling gum)?' He will say, 'No.' Then say to 
          him: 'Then what is this bad smell which i smell from you?' He will say
          to you, 'Hafsa made me drink honey syrup.' Then say: Perhaps the bees 
          of that honey had sucked the juice of the tree of Al-'Urfut.' I shall 
          also say the same. O you, Safiyya, say the same." Later Sada said, "By
          Allah, as soon as he (the Prophet ) stood at the door, I was about to 
          say to him what you had ordered me to say because I was afraid of 
          you." So when the Prophet came near Sada, she said to him, "O Allah's 
          Apostle! Have you taken Maghafir?" He said, "No." She said. "Then what
          is this bad smell which I detect on you?" He said, "Hafsa made me 
          drink honey syrup." She said, "Perhaps its bees had sucked the juice 
          of Al-'Urfut tree." When he came to me, I also said the same, and when
          he went to Safiyya, she also said the same. And when the Prophet again
          went to Hafsa, she said, 'O Allah's Apostle! Shall I give you more of 
          that drink?" He said, "I am not in need of it." Sada said, "By Allah, 
          we deprived him (of it)." I said to her, "Keep quiet." '
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.194"> Volume 7, Book 63, Number 194: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "Allah has forgiven my followers the evil thoughts 
          that occur to their minds, as long as such thoughts are not put into 
          action or uttered."  And Qatada said, "If someone divorces his wife 
          just in his mind, such an unuttered divorce has no effect.:
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.195"> Volume 7, Book 63, Number 195: </a> </em>
      <blockquote>
        <p> Narrated Jabir:
        <p> A man from the tribe of Bani Aslam came to the Prophet while he was in
          the mosque and said, "I have committed illegal sexual intercourse." 
          The Prophet turned his face to the other side. The man turned towards 
          the side towards which the Prophet had turned his face, and gave four 
          witnesses against himself. On that the Prophet called him and said, 
          "Are you insane?" (He added), "Are you married?" The man said, 'Yes." 
          On that the Prophet ordered him to be stoned to the death in the 
          Musalla  (a praying place). When the stones hit him with their sharp 
          edges and he fled, but he was caught at Al-Harra and then killed
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.196"> Volume 7, Book 63, Number 196: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> A man from Bani Aslam came to Allah's Apostle while he was in the 
          mosque and called (the Prophet ) saying, "O Allah's Apostle! I have 
          committed illegal sexual intercourse." On that the Prophet turned his 
          face from him to the other side, whereupon the man moved to the side 
          towards which the Prophet had turned his face, and said, "O Allah's 
          Apostle! I have committed illegal sexual intercourse." The Prophet 
          turned his face (from him) to the other side whereupon the man moved 
          to the side towards which the Prophet had turned his face, and 
          repeated his statement. The Prophet turned his face (from him) to the 
          other side again. The man moved again (and repeated his statement) for
          the fourth time. So when the man had given witness four times against 
          himself, the Prophet called him and said, "Are you insane?" He 
          replied, "No." The Prophet then said (to his companions), "Go and 
          stone him to death." The man was a married one. Jabir bin 'Abdullah 
          Al-Ansari said: I was one of those who stoned him. We stoned him at 
          the Musalla ('Id praying place) in Medina. When the stones hit him 
          with their sharp edges, he fled, but we caught him at Al-Harra and 
          stoned him till he died.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.197"> Volume 7, Book 63, Number 197: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The wife of Thabit bin Qais came to the Prophet and said, "O Allah's 
          Apostle! I do not blame Thabit for defects in his character or his 
          religion, but I, being a Muslim, dislike to behave in un-Islamic 
          manner (if I remain with him)." On that Allah's Apostle said (to her),
          "Will you give back the garden which your husband has given you (as 
          Mahr)?" She said, "Yes." Then the Prophet said to Thabit, "O Thabit! 
          Accept your garden, and divorce her once."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.198"> Volume 7, Book 63, Number 198: </a> </em>
      <blockquote>
        <p> Narrated 'Ikrima:
        <p> The sister of 'Abdullah bin Ubai narrated (the above narration, 197) 
          with the addition that the Prophet said to Thabit's wife, "Will you 
          return his garden?" She said, "Yes," and returned it, and (then) the 
          Prophet ordered Thabit to divorce her. Narrated Ibn 'Abbas:  The wife 
          of Thabit bin Qais came to Allah's Apostle and said, "O Allah's 
          Apostle! I do not blame Thabit for any defects in his character or his
          religion, but I cannot endure to live with him." On that Allah's 
          Apostle said, "Will you return his garden to him?" She said, "Yes."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.199"> Volume 7, Book 63, Number 199: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The wife of Thabit bin Qais bin Shammas came to the Prophet and said, 
          "O Allah's Apostle! I do not blame Thabit for any defects in his 
          character or his religion, but I am afraid that I (being a Muslim) may
          become unthankful for Allah's Blessings." On that, Allah's Apostle 
          said (to her), 'Will you return his garden to him?" She said, "Yes." 
          So she returned his garden to him and the Prophet told him to divorce 
          her.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.200"> Volume 7, Book 63, Number 200: </a> </em>
      <blockquote>
        <p> Narrated 'Ikrima:
        <p> that Jamila... Then he related the whole ,Hadith, (i.e. 199).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="007.063.201"> Volume 7, Book 63, Number 201: </a> </em>
