      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 11: </a> </em>
      <h1> <em> Call to Prayers (Adhaan) </em> </h1>
      <hr>
      <p> <em> <a name="001.011.577"> Volume 1, Book 11, Number 577: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> The people mentioned the fire and the bell (they suggested those as 
          signals to indicate the starting of prayers), and by that they 
          mentioned the Jews and the Christians. Then Bilal was ordered to 
          pronounce Adhan for the prayer by saying its wordings twice, and for 
          the Iqama (the call for the actual standing for the prayers in rows) 
          by saying its wordings once. (Iqama is pronounced when the people are 
          ready for the prayer).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.578"> Volume 1, Book 11, Number 578: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> When the Muslims arrived at Medina, they used to assemble for the 
          prayer, and used to guess the time for it. During those days, the 
          practice of Adhan for the prayers had not been introduced yet. Once 
          they discussed this problem regarding the call for prayer. Some people
          suggested the use of a bell like the Christians, others proposed a 
          trumpet like the horn used by the Jews, but 'Umar was the first to 
          suggest that a man should call (the people) for the prayer; so Allah's
          Apostle ordered Bilal to get up and pronounce the Adhan for prayers.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.579"> Volume 1, Book 11, Number 579: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> Bilal was ordered to repeat the wording of the Adhan for prayers 
          twice, and to pronounce the wording of the Iqamas once except 
          "Qad-qamat-is-Salat".
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.580"> Volume 1, Book 11, Number 580: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> When the number of Muslims increased they discussed the question as to
          how to know the time for the prayer by some familiar means. Some 
          suggested that a fire be lit (at the time of the prayer) and others 
          put forward the proposal to ring the bell. Bilal was ordered to 
          pronounce the wording of Adhan twice and of the Iqama once only.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.581"> Volume 1, Book 11, Number 581: </a> </em>
      <blockquote>
        <p> Narrated Abu Qilaba:
        <p> Anas said, "Bilal was ordered to pronounce the wording of Adhan twice 
          and of Iqama once only." The sub narrator Isma'li said, "I mentioned 
          that to Aiyub and he added (to that), "Except Iqama (i.e. 
          Qad-Qamatis-Salat which should be said twice)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.582"> Volume 1, Book 11, Number 582: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Allah's Apostle said, "When the Adhan is pronounced Satan takes to his
          heels and passes wind with noise during his flight in order not to 
          hear the Adhan. When the Adhan is completed he comes back and again 
          takes to his heels when the Iqama is pronounced and after its 
          completion he returns again till he whispers into the heart of the 
          person (to divert his attention from his prayer) and makes him 
          remember things which he does not recall to his mind before the prayer
          and that causes him to forget how much he has prayed."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.583"> Volume 1, Book 11, Number 583: </a> </em>
      <blockquote>
        <p> Narrated 'Abdul Rahman:
        <p> Abu Sa'id Al-Khudri told my father, "I see you liking sheep and the 
          wilderness. So whenever you are with your sheep or in the wilderness 
          and you want to pronounce Adhan for the prayer raise your voice in 
          doing so, for whoever hears the Adhan, whether a human being, a jinn 
          or any other creature, will be a witness for you on the Day of 
          Resurrection." Abu Said added, "I heard it (this narration) from 
          Allah's Apostle."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.584"> Volume 1, Book 11, Number 584: </a> </em>
      <blockquote>
        <p> Narrated Humaid:
        <p> Anas bin Malik said, "Whenever the Prophet went out with us to fight 
          (in Allah's cause) against any nation, he never allowed us to attack 
          till morning and he would wait and see: if he heard Adhan he would 
          postpone the attack and if he did not hear Adhan he would attack 
          them." Anas added, "We reached Khaibar at night and in the morning 
          when he did not hear the Adhan for the prayer, he (the Prophet ) rode 
          and I rode behind Abi Talha and my foot was touching that of the 
          Prophet.
        <p> The inhabitants of Khaibar came out with their baskets and spades and 
          when they saw the Prophet they shouted 'Muhammad! By Allah, Muhammad 
          and his army.' When Allah's Apostle saw them, he said, "Allahu-Akbar! 
          Allahu-Akbar! Khaibar is ruined. Whenever we approach a (hostile) 
          nation (to fight), then evil will be the morning of those who have 
          been warned."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.585"> Volume 1, Book 11, Number 585: </a> </em>
      <blockquote>
        <p> Narrated Abu Said Al-Khudri:
        <p> Allah's Apostle said, "Whenever you hear the Adhan, say what the 
          Mu'adhdhin is saying.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.586"> Volume 1, Book 11, Number 586: </a> </em>
      <blockquote>
        <p> Narrated 'Isa bin Talha:
        <p> that he had heard Muawiya repeating the words of Adhan up to "Wa 
          ash-hadu Anna Muhammadan Rasulul-lah (and I testify that Muhammad is 
          Allah's Apostle.)"
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.587"> Volume 1, Book 11, Number 587: </a> </em>
      <blockquote>
        <p> Narrated Yahya as above (586) and added:
        <p> "Some of my companions told me that Hisham had said, "When the 
          Mu'adhdhin said, "Haiya alas-sala(t) (come for the prayer)." Muawiya 
          said, "La hawla wala quwata illa billah (There is neither might nor 
          any power except with Allah)" and added, "We heard your Prophet saying
          the same."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.588"> Volume 1, Book 11, Number 588: </a> </em>
      <blockquote>
        <p> Narrated Jabir bin 'Abdullah:
        <p> Allah's Apostle said, "Whoever after listening to the Adhan says, 
          'Allahumma Rabba hadhihi-d-da' watit-tammati was-salatil qa'imati, ati
          Muhammadan al-wasilata wal-fadilata, wab' athhu maqaman 
          mahmudan-il-ladhi wa' adtahu (O Allah! Lord of this perfect call (of 
          not ascribing partners to You) and of the regular prayer which is 
          going to be established! Kindly give Muhammad the right of 
          intercession and superiority and send him (on the Day of Judgment) to 
          the best and the highest place in Paradise which You promised him)', 
          then intercession for me will be permitted for him on the Day of 
          Resurrection").
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.589"> Volume 1, Book 11, Number 589: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Allah's Apostle said, "If the people knew the reward for pronouncing 
          the Adhan and for standing in the first row (in congregational 
          prayers) and found no other way to get that except by drawing lots 
          they would draw lots, and if they knew the reward of the Zuhr prayer 
          (in the early moments of its stated time) they would race for it (go 
          early) and if they knew the reward of 'Isha' and Fajr (morning) 
          prayers in congregation, they would come to offer them even if they 
          had to crawl."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.590"> Volume 1, Book 11, Number 590: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Al-Harith:
        <p> Once on a rainy muddy day, Ibn 'Abbas delivered a sermon in our 
          presence and when the Mu'adhdhin pronounced the Adhan and said, "Haiya
          ala-s-sala(t) (come for the prayer)" Ibn 'Abbas ordered him to say 
          'Pray at your homes.' The people began to look at each other 
          (surprisingly). Ibn 'Abbas said. "It was done by one who was much 
          better than I (i.e. the Prophet or his Mu'adhdhin), and it is a 
          license.'
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.591"> Volume 1, Book 11, Number 591: </a> </em>
      <blockquote>
        <p> Narrated Salim bin Abdullah:
        <p> My father said that Allah s Apostle said, "Bilal pronounces 'Adhan at 
          night, so keep on eating and drinking (Suhur) till Ibn Um Maktum 
          pronounces Adhan." Salim added, "He was a blind man who would not 
          pronounce the Adhan unless he was told that the day had dawned."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.592"> Volume 1, Book 11, Number 592: </a> </em>
      <blockquote>
        <p> Narrated Hafsa:
        <p> When the Muadh-dhin pronounced the Adhan for Fajr prayer and the dawn 
          became evident the Prophet ordered a two Rakat light prayer (Sunna) 
          before the Iqama of the compulsory (congregational) prayer.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.593"> Volume 1, Book 11, Number 593: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet used to offer two light Rakat between the Adhan and the 
          Iqama of the Fajr prayer.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.594"> Volume 1, Book 11, Number 594: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> Allah's Apostle said, "Bilal pronounces the Adhan at night, so keep on
          eating and drinking (Suhur) till Ibn Um Maktum pronounces the Adhan."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.595"> Volume 1, Book 11, Number 595: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Mas'ud:
        <p> The Prophet said, "The Adhan pronounced by Bilal should not stop you 
          from taking Suhur, for he pronounces the Adhan at night, so that the 
          one offering the late night prayer (Tahajjud) from among you might 
          hurry up and the sleeping from among you might wake up. It does not 
          mean that dawn or morning has started." Then he (the Prophet) pointed 
          with his fingers and raised them up (towards the sky) and then lowered
          them (towards the earth) like this (Ibn Mas'ud imitated the gesture of
          the Prophet). Az-Zuhri gestured with his two index fingers which he 
          put on each other and then stretched them to the right and left. These
          gestures illustrate the way real dawn appears. It spreads left and 
          right horizontally. The dawn that appears in the high sky and lowers 
          down is not the real dawn) .
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.596"> Volume 1, Book 11, Number 596: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet said, "Bilal pronounces the Adhan at night, so eat and 
          drink (Suhur) till Ibn Um Maktum pronounces the Adhan."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.597"> Volume 1, Book 11, Number 597: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Mughaffal Al-Muzani:
        <p> Allah's Apostle said thrice, "There is a prayer between the two Adhans
          (Adhan and Iqama)," and added, "For the one who wants to pray."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.598"> Volume 1, Book 11, Number 598: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> "When the Mu'adhdhin pronounced the Adhan, some of the companions of 
          the Prophet would proceed to the pillars of the mosque (for the 
          prayer) till the Prophet arrived and in this way they used to pray two
          Rakat before the Maghrib prayer. There used to be a little time 
          between the Adhan and the Iqama." Shu'ba said, "There used to be a 
          very short interval between the two (Adhan and Iqama)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.599"> Volume 1, Book 11, Number 599: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Allah's Apostle used to pray two light Rakat before the morning 
          (compulsory) prayer after the day dawned and the Mu'adhdhin had 
          finished his Adhan. He then would lie on his right side till the 
          Mu'adhdhin came to pronounce the Iqama.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.600"> Volume 1, Book 11, Number 600: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Mughaffal:
        <p> The prophet said, "There is a prayer between the two Adhans (Adhan and
          Iqama), there is a prayer between the two Adhans." And then while 
          saying it the third time he added, "For the one who wants to (pray)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.601"> Volume 1, Book 11, Number 601: </a> </em>
      <blockquote>
        <p> Narrated Malik bin Huwairth:
        <p> I came to the Prophet with some men from my tribe and stayed with him 
          for twenty nights. He was kind and merciful to us. When he realized 
          our longing for our families, he said to us, "Go back and stay with 
          your families and teach them the religion, and offer the prayer and 
          one of you should pronounce the Adhan for the prayer when its time is 
          due and the oldest one amongst you should lead the prayer."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.602"> Volume 1, Book 11, Number 602: </a> </em>
      <blockquote>
        <p> Narrated Abu Dhar:
        <p> We were in the company of the Prophet on a journey and the Mu'adhdhin 
          wanted to pronounce the Adhan for the (Zuhr) prayer. The Prophet said 
          to him, "Let it become cooler." Then he again wanted to pronounce the 
          Adhan but the Prophet; said to him, "Let it become cooler." The 
          Mu'adh-dhin again wanted to pronounce the Adhan for the prayer but the
          Prophet said, "Let it become cooler," till the shadows of the hillocks
          become equal to their sizes. The Prophet added, "The severity of the 
          heat is from the raging of Hell."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.603"> Volume 1, Book 11, Number 603: </a> </em>
      <blockquote>
        <p> Narrated Malik bin Huwairth:
        <p> Two men came to the Prophet with the intention of a journey. The 
          Prophet said, "When (both of) you set out, pronounce Adhan and then 
          Iqama and the oldest of you should lead the prayer."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.604"> Volume 1, Book 11, Number 604: </a> </em>
      <blockquote>
        <p> Narrated Malik:
        <p> We came to the Prophet and stayed with him for twenty days and nights.
          We were all young and of about the same age. The Prophet was very kind
          and merciful. When he realized our longing for our families, he asked 
          about our homes and the people there and we told him. Then he asked us
          to go back to our families and stay with them and teach them (the 
          religion) and to order them to do good things. He also mentioned some 
          other things which I have (remembered or) forgotten. The Prophet then 
          added, "Pray as you have seen me praying and when it is the time for 
          the prayer one of you should pronounce the Adhan and the oldest of you
          should lead the prayer.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.605"> Volume 1, Book 11, Number 605: </a> </em>
      <blockquote>
        <p> Narrated Nafi:
        <p> Once in a cold night, Ibn 'Umar pronounced the Adhan for the prayer at
          ,Dajnan (the name of a mountain) and then said, "Pray at your homes", 
          and informed us that Allah's Apostle used to tell the Mu'adhdin to 
          pronounce Adhan and say, "Pray at your homes" at the end of the Adhan 
          on a rainy or a very cold night during the journey."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.606"> Volume 1, Book 11, Number 606: </a> </em>
      <blockquote>
        <p> Narrated 'Aun bin Abi Juhaifa:
        <p> My father said, "I saw Allah's Apostle at a place called Al-Abtah. 
          Bilal came and informed him about the prayer and then came out with an
          Anza and planted it in front of Allah's Apostle at Al-Abtah and 
          pronounced the Iqama."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.607"> Volume 1, Book 11, Number 607: </a> </em>
      <blockquote>
        <p> Narrated 'Aun bin Abi Juhaifa:
        <p> My father said, "I saw Bilal turning his face from side to side while 
          pronouncing the Adhan for the prayer."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.608"> Volume 1, Book 11, Number 608: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Abi Qatada:
        <p> My father said, "While we were praying with the Prophet he heard the 
          noise of some people. After the prayer he said, 'What is the matter?' 
          They replied 'We were hurrying for the prayer.' He said, 'Do not make 
