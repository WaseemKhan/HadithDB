      <em> <a name="001.011.692"> Volume 1, Book 11, Number 692: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> The Prophet said, "Straighten your rows for I see you from behind my 
          back." Anas added, "Everyone of us used to put his shoulder with the 
          shoulder of his companion and his foot with the foot of his 
          companion."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.693"> Volume 1, Book 11, Number 693: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> I prayed with the Prophet one night and stood on his left side. 
          Allah's Apostle caught hold of my head from behind and drew me to his 
          right and then offered the prayer and slept. Later the Mu'adh-dhin 
          came and the Prophet stood up for prayer without performing ablution.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.694"> Volume 1, Book 11, Number 694: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> One night an orphan and I offered the prayers behind the Prophet in my
          house and my mother (Um Sulaim) was standing behind us (by herself 
          forming a row).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.695"> Volume 1, Book 11, Number 695: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> One night I stood to the left of the Prophet in the prayer but he 
          caught hold of me by the hand or by the shoulder (arm) till he made me
          stand on his right and beckoned with his hand (for me) to go from 
          behind (him). (Al-Kashmaihani-Fateh al-Bari).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.011.696"> Volume 1, Book 11, Number 696: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Allah's Apostle used to pray in his room at night. As the wall of the 
          room was LOW, the people saw him and some of them stood up to follow 
          him in the prayer. In the morning they spread the news. The following 
          night the Prophet stood for the prayer and the people followed him. 
          This went on for two or three nights. Thereupon Allah's Apostle did 
          not stand for the prayer the following night, and did not come out. In
          the morning, the people asked him about it. He replied, that he way 
          afraid that the night prayer might become compulsory.
        <p>        
      </blockquote>
