          says "Sami'a-l-lahu Liman hamida"; and if he prostrates, prostrate 
          (after him), and if he prays sitting, pray sitting all together, and 
          straighten the rows for the prayer, as the straightening of the rows 
          is amongst those things which make your prayer a correct and perfect 
          one. (See Hadith No. 657).
        <p>        
      </blockquote>
