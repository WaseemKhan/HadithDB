      <em> <a name="001.011.691"> Volume 1, Book 11, Number 691: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> I arrived at Medina and was asked whether I found any change since the
          days of Allah's Apostle. I said, "I have not found any change except 
          that you do not stand in alignment in your prayers."
        <p>        
      </blockquote>
