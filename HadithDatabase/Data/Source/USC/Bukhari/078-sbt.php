      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 78: </a> </em>
      <h1> <em> Oaths and Vows </em> </h1>
      <hr>
      <p> <em> <a name="008.078.618"> Volume 8, Book 78, Number 618: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Abu Bakr As-Siddiq had never broken his oaths till Allah revealed the 
          expiation for the oaths. Then he said, "If I take an oath to do 
          something and later on I find something else better than the first 
          one, then I do what is better and make expiation for my oath."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.619"> Volume 8, Book 78, Number 619: </a> </em>
      <blockquote>
        <p> Narrated 'Abdur-Rahman bin Samura:
        <p> The Prophet said, "O 'Abdur-Rahman bin Samura! Do not seek to be a 
          ruler, because if you are given authority for it, then you will be 
          held responsible for it, but if you are given it without asking for 
          it, then you will be helped in it (by Allah): and whenever you take an
          oath to do something and later you find that something else is better 
          than the first, then do the better one and make expiation for your 
          oath."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.620"> Volume 8, Book 78, Number 620: </a> </em>
      <blockquote>
        <p> Narrated Abu Musa:
        <p> I went to the Prophet along with a group of Al-Ash'ariyin in order to 
          request him to provide us with mounts. He said, "By Allah, I will not 
          provide you with mounts and I haven't got anything to mount you on." 
          Then we stayed there as long as Allah wished us to stay, and then 
          three very nice looking she-camels were brought to him and he made us 
          ride them. When we left, we, or some of us, said, "By Allah, we will 
          not be blessed, as we came to the Prophet asking him for mounts, and 
          he swore that he would not give us any mounts but then he did give us.
          So let us go back to the Prophet and remind him (of his oath)." When 
          we returned to him (and reminded him of the fact), he said, "I did not
          give you mounts, but it is Allah Who gave you. By Allah, Allah 
          willing, if I ever take an oath to do something and then I find 
          something else than the first, I will make expiation for my oath and 
          do the thing which is better (or do something which is better and give
          the expiation for my oath)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.621"> Volume 8, Book 78, Number 621: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "We (Muslims) are the last in the world, but will be
          foremost on the Day of Resurrection." Allah's Apostle also said, "By 
          Allah, if anyone of you insists on fulfilling an oath by which he may 
          harm his family, he commits a greater sin in Allah's sight than that 
          of dissolving his oath and making expiation for it."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.622"> Volume 8, Book 78, Number 622: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Allah's Apostle said, "Anyone who takes an oath through which his 
          family may be harmed, and insists on keeping it, he surely commits a 
          sin greater (than that of dissolving his oath). He should rather 
          compensate for that oath by making expiation."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.623"> Volume 8, Book 78, Number 623: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> Allah's Apostle sent an army detachment and made Usama bin Zaid its 
          commander. Some people criticized (spoke badly of) Usama's leadership.
          So Allah's Apostle got up saying, "If you people are criticizing 
          Usama's leadership, you have already criticized the leadership of his 
          father before. But Wa-aimullah (i.e., By Allah), he (i.e. Zaid) 
          deserved the leadership, and he was one of the most beloved persons to
          me; and now this (his son Usama) is one of the dearest persons to me 
          after him." (See Hadith No. 745, Vol. 5)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.624"> Volume 8, Book 78, Number 624: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> The oath of the Prophet used to be: "No, by Him who turns the 
          hearts."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.625"> Volume 8, Book 78, Number 625: </a> </em>
      <blockquote>
        <p> Narrated Jabir bin Samura:
        <p> The Prophet said, "If Caesar is ruined, there will be no Caesar after 
          him; and if Khosrau is ruined, there will be no Khosrau, after him; 
          and, by Him in Whose Hand my soul is, surely you will spend their 
          treasures in Allah's Cause."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.626"> Volume 8, Book 78, Number 626: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Allah's Apostle said, "If Khosrau is ruined, there will be no Khosrau 
          after him; and if Caesar is ruined, there will be no Caesar after him.
          By Him in Whose Hand Muhammad's soul is, surely you will spend their 
          treasures in Allah's Cause."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.627"> Volume 8, Book 78, Number 627: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet said, "O followers of Muhammad! By Allah, if you knew what
          I know, you would weep much and laugh little."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.628"> Volume 8, Book 78, Number 628: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin Hisham:
        <p> We were with the Prophet and he was holding the hand of 'Umar bin 
          Al-Khattab. 'Umar said to Him, "O Allah's Apostle! You are dearer to 
          me than everything except my own self." The Prophet said, "No, by Him 
          in Whose Hand my soul is, (you will not have complete faith) till I am
          dearer to you than your own self." Then 'Umar said to him, "However, 
          now, by Allah, you are dearer to me than my own self." The Prophet 
          said, "Now, O 'Umar, (now you are a believer)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.629"> Volume 8, Book 78, Number 629: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira and Zaid bin Khalid:
        <p> Two men had a dispute in the presence of Allah's Apostle. One of them 
          said, "O Allah's Apostle! Judge between us according to Allah's Laws."
          The other who was wiser, said, "Yes, O Allah's Apostle! Judge between 
          us according to Allah's Laws and allow me to speak. The Prophet said, 
          "Speak." He said, "My son was a laborer serving this (person) and he 
          committed illegal sexual intercourse with his wife, The people said 
          that my son is to be stoned to death, but I ransomed him with 
          one-hundred sheep and a slave girl. Then I asked the learned people, 
          who informed me that my son should receive one hundred lashes and will
          be exiled for one year, and stoning will be the lot for the man's 
          wife." Allah's Apostle said, "Indeed, by Him in Whose Hand my soul is,
          I will judge between you according to Allah's Laws: As for your sheep 
          and slave girl, they are to be returned to you." Then he scourged his 
          son one hundred lashes and exiled him for one year. Then Unais 
          Al-Aslami was ordered to go to the wife of the second man, and if she 
          confessed (the crime), then stone her to death. She did confess, so he
          stoned her to death.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.630"> Volume 8, Book 78, Number 630: </a> </em>
      <blockquote>
        <p> Narrated Abu Bakra:
        <p> The Prophet said, "Do you think if the tribes of Aslam, Ghifar, 
          Muzaina and Juhaina are better than the tribes of Tamim, 'Amir bin 
          Sa'sa'a, Ghatfan and Asad, they (the second group) are despairing and 
          losing?" They (the Prophet's companions) said, "Yes, (they are)." He 
          said, "By Him in Whose Hand my soul is, they (the first group) are 
          better than them (the second group)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.631"> Volume 8, Book 78, Number 631: </a> </em>
      <blockquote>
        <p> Narrated Abu Humaid As-Sa'idi:
        <p> Allah's Apostle employed an employee (to collect Zakat). The employee 
          returned after completing his job and said, "O Allah's Apostle! This 
          (amount of Zakat) is for you, and this (other amount) was given to me 
          as a present." The Prophet said to him, "Why didn't you stay at your 
          father's or mother's house and see if you would be given presents or 
          not?" Then Allah's Apostle got up in the evening after the prayer, and
          having testified that none has the right to be worshipped but Allah 
          and praised and glorified Allah as He deserved, he said, "Now then ! 
          What about an employee whom we employ and then he comes and says, 
          'This amount (of Zakat) is for you, and this (amount) was given to me 
          as a present'? Why didn't he stay at the house of his father and 
          mother to see if he would be given presents or not? By Him in Whose 
          Hand Muhammad's soul is, none of you will steal anything of it (i.e. 
          Zakat) but will bring it by carrying it over his neck on the Day of 
          Resurrection. If it has been a camel, he will bring it (over his neck)
          while it will be grunting, and if it has been a cow, he will bring it 
          (over his neck), while it will be mooing; and if it has been a sheep, 
          he will bring it (over his neck) while it will be bleeding." The 
          Prophet added, "I have preached you (Allah's Message)." Abu Humaid 
          said, "Then Allah's Apostle raised his hands so high that we saw the 
          whiteness of his armpits."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.632"> Volume 8, Book 78, Number 632: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Abu-l-Qasim (the Prophet) said, "By Him in Whose Hand Muhammad's soul 
          is, if you know that which I know, you would weep much and laugh 
          little."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.633"> Volume 8, Book 78, Number 633: </a> </em>
      <blockquote>
        <p> Narrated Abu Dhar:
        <p> I reached him (the Prophet ) while in the shade of the Ka'ba; he was 
          saying, "They are the losers, by the Lord of the Ka'ba! They are the 
          losers, by the Lord of the Ka'ba!" I said (to myself ), "What is wrong
          with me? Is anything improper detected in me? What is wrong with me? 
          Then I sat beside him and he kept on saying his statement. I could not
          remain quiet, and Allah knows in what sorrowful state I was at that 
          time. So I said, ' Who are they (the losers)? Let My father and mother
          be sacrificed for you, O Allah's Apostle!" He said, "They are the 
          wealthy people, except the one who does like this and like this and 
          like this (i.e., spends of his wealth in Allah's Cause)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.634"> Volume 8, Book 78, Number 634: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Allah's Apostle said, "(The Prophet) Solomon once said, 'Tonight I 
          will sleep with ninety women, each of whom will bring forth a 
          (would-be) cavalier who will fight in Allah's Cause." On this, his 
          companion said to him, "Say: Allah willing!" But he did not say Allah 
          willing. Solomon then slept with all the women, but none of them 
          became pregnant but one woman who later delivered a half-man. By Him 
          in Whose Hand Muhammad's soul is, if he (Solomon) had said, 'Allah 
          willing' (all his wives would have brought forth boys) and they would 
          have fought in Allah's Cause as cavaliers. "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.635"> Volume 8, Book 78, Number 635: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara 'bin 'Azib:
        <p> A piece of silken cloth was given to the Prophet as a present and the 
          people handed it over amongst themselves and were astonished at its 
          beauty and softness. Allah's Apostle said, "Are you astonished at it?"
          They said, "Yes, O Allah's Apostle!" He said, "By Him in Whose Hand my
          soul is, the handkerchiefs of Sa'd in Paradise are better than it."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.636"> Volume 8, Book 78, Number 636: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Hind bint 'Utba bin Rabi 'a said, "O Allah 's Apostle! (Before I 
          embraced Islam), there was no family on the surface of the earth, I 
          wish to have degraded more than I did your family. But today there is 
          no family whom I wish to have honored more than I did yours." Allah's 
          Apostle said, "I thought similarly, by Him in Whose Hand Muhammad's 
          soul is!" Hind said, "O Allah's Apostle! (My husband) Abu Sufyan is a 
          miser. Is it sinful of me to feed my children from his property?" The 
          Prophet said, "No, unless you take it for your needs what is just and 
          reasonable."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.637"> Volume 8, Book 78, Number 637: </a> </em>
      <blockquote>
        <p> Narrated Abdullah bin Masud:
        <p> While Allah's Apostle was sitting, reclining his back against a 
          Yemenite leather tent he said to his companions, "Will you be pleased 
          to be one-fourth of the people of Paradise?" They said, 'Yes.' He said
          "Won't you be pleased to be one-third of the people of Paradise" They 
          said, "Yes." He said, "By Him in Whose Hand Muhammad's soul is, I hope
          that you will be one-half of the people of Paradise."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.638"> Volume 8, Book 78, Number 638: </a> </em>
      <blockquote>
        <p> Narrated Abu Sa'id Al-Khudri:
        <p> A man heard another man reciting: Surat-ul-Ikhlas (The Unity) 'Say: He
          is Allah, the One (112) and he was repeating it. The next morning he 
          came to Allah's Apostle and mentioned the whole story to him as if he 
          regarded the recitation of that Sura as insufficient On that, Allah's 
          Apostle said, "By Him in Whose Hand my soul is! That (Sura No. 112) 
          equals one-third of the Qur'an."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.639"> Volume 8, Book 78, Number 639: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> I heard the Prophet saying, "Perform the bowing and the prostration 
          properly (with peace of mind), for, by Him in Whose Hand my soul is, I
          see you from behind my back when you bow and when you prostrate."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.640"> Volume 8, Book 78, Number 640: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> An Ansari woman came to the Prophet in the company of her children, 
          and the Prophet said to her, "By Him in Whose Hand my soul is, you are
          the most beloved people to me!" And he repeated the statement thrice.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.641"> Volume 8, Book 78, Number 641: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> Allah's Apostle met 'Umar bin Al-Khattab while the latter was going 
          with a group of camel-riders, and he was swearing by his father. The 
          Prophet said, "Lo! Allah forbids you to swear by your fathers, so 
          whoever has to take an oath, he should swear by Allah or keep quiet."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.642"> Volume 8, Book 78, Number 642: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p>        
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.643"> Volume 8, Book 78, Number 643: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> Allah's Apostle said, "Do not swear by your fathers."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.644"> Volume 8, Book 78, Number 644: </a> </em>
      <blockquote>
        <p> Narrated Zahdam:
        <p> There was a relation of love and brotherhood between this tribe of 
          Jarm and Al-Ash'ariyin. Once we were with Abu Musa Al-Ash'ari, and 
          then a meal containing chicken was brought to Abu Musa, and there was 
          present, a man from the tribe of Taimillah who was of red complexion 
          as if he were from non-Arab freed slaves. Abu Musa invited him to the 
          meal. He said, "I have seen chickens eating dirty things, so I deemed 
          it filthy and took an oath that I would never eat chicken." On that, 
          Abu Musa said, "Get up, I will narrate to you about that. Once a group
          of the Ash'ariyin and I went to Allah's Apostle and asked him to 
          provide us with mounts; he said, 'By Allah, I will never give you any 
          mounts nor do I have anything to mount you on.' Then a few camels of 
          war booty were brought to Allah's Apostle , and he asked about us, 
          saying, 'Where are the Ash-'ariyin?' He then ordered five nice camels 
          to be given to us, and when we had departed, we said, 'What have we 
          done? Allah's Apostle had taken the oath not to give us any mounts, 
          and that he had nothing to mount us on, and later he gave us that we 
          might ride? Did we take advantage of the fact that Allah's Apostle had
