        <p> The Prophet used to stay (for a period) in the house of Zainab bint 
          Jahsh (one of the wives of the Prophet ) and he used to drink honey in
          her house. Hafsa and I decided that when the Prophet entered upon 
          either of us, she would say, "I smell in you the bad smell of Maghafir
          (a bad smelling raisin). Have you eaten Maghafir?" When he entered 
          upon one of us, she said that to him. He replied (to her), "No, but I 
          have drunk honey in the house of Zainab bint Jahsh, and I will never 
          drink it again." Then the following verse was revealed: 'O Prophet ! 
          Why do you ban (for you) that which Allah has made lawful for you?. 
          ..(up to)  If you two (wives of the Prophet turn in repentance to 
          Allah.'  (66.1-4) The two were 'Aisha and Hafsa And also the Statement
          of Allah: 'And (Remember) when the Prophet disclosed a matter in 
          confidence to one of his wives!'  (66.3) i.e., his saying, "But I have
          drunk honey." Hisham said: It also meant his saying, "I will not drink
          anymore, and I have taken an oath, so do not inform anybody of that '
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.683"> Volume 8, Book 78, Number 683: </a> </em>
      <blockquote>
        <p> Narrated Sa'id bin Al-Harith:
        <p> that he heard Ibn 'Umar saying, "Weren't people forbidden to make 
          vows?" The Prophet said, 'A vow neither hastens nor delays anything, 
          but by the making of vows, some of the wealth of a miser is taken 
          out."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.684"> Volume 8, Book 78, Number 684: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> The Prophet forbade the making of vows and said, "It (a vow) does not 
          prevent anything (that has to take place), but the property of a miser
          is spent (taken out) with it."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.685"> Volume 8, Book 78, Number 685: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, "Allah says, 'The vow, does not bring about for the 
          son of Adam anything I have not decreed for him, but his vow may 
          coincide with what has been decided for him, and by this way I cause a
          miser to spend of his wealth. So he gives Me (spends in charity) for 
          the fulfillment of what has been decreed for him what he would not 
          give Me before but for his vow."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.686"> Volume 8, Book 78, Number 686: </a> </em>
      <blockquote>
        <p> Narrated Zahdam bin Mudarrab:
        <p> 'Imran bin Hussain said, "The Prophet said, 'The best of you (people) 
          are my generation, and the second best will be those who will follow 
          them, and then those who will follow the second generation." Imran 
          added, "I do not remember whether he mentioned two or three 
          (generations) after his generation. He added, 'Then will come some 
          people who will make vows but will not fulfill them; and they will be 
          dishonest and will not be trustworthy, and they will give their 
          witness without being asked to give their witness, and fatness will 
          appear among them.' "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.687"> Volume 8, Book 78, Number 687: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet said, "Whoever vows that he will be obedient to Allah, 
          should remain obedient to Him; and whoever made a vow that he will 
          disobey Allah, should not disobey Him."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.688"> Volume 8, Book 78, Number 688: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> 'Umar said "O Allah's Apostle! I vowed to perform I'tikaf for one 
          night in Al-Masjid-al-Haram, during the Pre-Islamic Period of 
          ignorance (before embracing Islam). "The Prophet said, "Fulfill your 
          vow." Ibn 'Umar said to the lady, "Pray on her behalf." Ibn 'Abbas 
          said the same.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.689"> Volume 8, Book 78, Number 689: </a> </em>
      <blockquote>
        <p> Narrated Sa'id bin 'Ubada Al-Ansari:
        <p> that he consulted the Prophet about a vow that had been made by his 
          mother who died without fulfilling it. The Prophet gave his verdict 
          that he should fulfill it on her behalf. The verdict became Sunna 
          (i.e. the Prophet's tradition).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.690"> Volume 8, Book 78, Number 690: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> A man came to the Prophet and said to him, "My sister vowed to perform
          the Hajj, but she died (before fulfilling it)." The Prophet said, 
          "Would you not have paid her debts if she had any?" The man said, 
          "Yes." The Prophet said, "So pay Allah's Rights, as He is more 
          entitled to receive His rights."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.691"> Volume 8, Book 78, Number 691: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet said, "Whoever vowed to be obedient to Allah, must be 
          obedient to Him; and whoever vowed to be disobedient to Allah, should 
          not be disobedient to Him."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.692"> Volume 8, Book 78, Number 692: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> The Prophet said, "Allah is not in need of this man) torturing 
          himself," when he saw the man walking between his two sons (who were 
          supporting him).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.693"> Volume 8, Book 78, Number 693: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The Prophet saw a man performing Tawaf around the Ka'ba, tied with a 
          rope or something else (while another person was holding him). The 
          Prophet cut that rope off.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.694"> Volume 8, Book 78, Number 694: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> While performing the Tawaf around the Ka'ba, the Prophet passed by a 
          person leading another person by a hair-rope nose-ring in his nose. 
          The Prophet cut the hair-rope nose-ring off with his hand and ordered 
          the man to lead him by the hand.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.695"> Volume 8, Book 78, Number 695: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> While the Prophet was delivering a sermon, he saw a man standing, so 
          he asked about that man. They (the people) said, "It is Abu Israil who
          has vowed that he will stand and never sit down, and he will never 
          come in the shade, nor speak to anybody, and will fast.'' The Prophet 
          said, "Order him to speak and let him come in the shade, and make him 
          sit down, but let him complete his fast."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.696"> Volume 8, Book 78, Number 696: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> that he was asked about a man who had vowed that he would fast all the
          days of his life then the day of 'Id al Adha or 'Id-al-Fitr came. 
          'Abdullah bin 'Umar said: You have indeed a good example in Allah's 
          Apostle. He did not fast on the day of 'Id al Adha or the day of 
          'Id-al-Fitr, and we do not intend fasting on these two days.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.697"> Volume 8, Book 78, Number 697: </a> </em>
      <blockquote>
        <p> Narrated Ziyad bin Jubair:
        <p> I was with Ibn 'Umar when a man asked him, "I have vowed to fast every
          Tuesday or Wednesday throughout my life and if the day of my fasting 
          coincided with the day of Nahr (the first day of 'Id-al-Adha), (What 
          shall I do?)" Ibn 'Umar said, "Allah has ordered the vows to be 
          fulfilled, and we are forbidden to fast on the day of Nahr." The man 
          repeated his question and Ibn 'Umar repeated his former answer, adding
          nothing more.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="008.078.698"> Volume 8, Book 78, Number 698: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> We went out in the company of Allah's Apostle on the day of (the 
          battle of) Khaibar, and we did not get any gold or silver as war 
          booty, but we got property in the form of things and clothes. Then a 
          man called Rifa'a bin Zaid, from the tribe of Bani Ad-Dubaib, 
          presented a slave named Mid'am to Allah's Apostle. Allah's Apostle 
          headed towards the valley of Al-Qura, and when he was in the valley of
          Al-Qura an arrow was thrown by an unidentified person, struck and 
          killed Mid'am who was making a she-camel of Allah's Apostle kneel 
          down. The people said, "Congratulations to him (the slave) for gaining
          Paradise." Allah's Apostle said, "No! By Him in Whose Hand my soul is,
          for the sheet which he stole from the war booty before its 
          distribution on the day of Khaibar, is now burning over him." When the
          people heard that, a man brought one or two Shiraks (leather straps of
          shoes) to the Prophet. The Prophet said, "A Shirak of fire, or two 
          Shiraks of fire."
        <p>        
      </blockquote>
