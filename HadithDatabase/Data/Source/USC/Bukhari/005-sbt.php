      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 5: </a> </em>
      <h1> <em> Bathing (Ghusl) </em> </h1>
      <hr>
      <p> <em> <a name="001.005.248"> Volume 1, Book 5, Number 248: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Whenever the Prophet took a bath after Janaba he started by washing 
          his hands and then performed ablution like that for the prayer. After 
          that he would put his fingers in water and move the roots of his hair 
          with them, and then pour three handfuls of water over his head and 
          then pour water all over his body.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.249"> Volume 1, Book 5, Number 249: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> (the wife of the Prophet) Allah's Apostle performed ablution like that
          for the prayer but did not wash his feet. He washed off the discharge 
          from his private parts and then poured water over his body. He 
          withdrew his feet from that place (the place where he took the bath) 
          and then washed them. And that was his way of taking the bath of 
          Janaba.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.250"> Volume 1, Book 5, Number 250: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet and I used to take a bath from a single pot called 
          'Faraq'.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.251"> Volume 1, Book 5, Number 251: </a> </em>
      <blockquote>
        <p> Narrated Abu Salama:
        <p> 'Aisha's brother and I went to 'Aisha and he asked her about the bath 
          of the Prophet. She brought a pot containing about a Sa' of water and 
          took a bath and poured it over her head and at what time there was a 
          screen between her and us.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.252"> Volume 1, Book 5, Number 252: </a> </em>
      <blockquote>
        <p> Narrated Abu Ja'far:
        <p> While I and my father were with Jabir bin 'Abdullah, some People asked
          him about taking a bath He replied, "A Sa' of water is sufficient for 
          you." A man said, "A Sa' is not sufficient for me." Jabir said, "A Sa 
          was sufficient for one who had more hair than you and was better than 
          you (meaning the Prophet)." And then Jabir (put on) his garment and 
          led the prayer.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.253"> Volume 1, Book 5, Number 253: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The Prophet and Maimuna used to take a bath from a single pot.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.254"> Volume 1, Book 5, Number 254: </a> </em>
      <blockquote>
        <p> Narrated Jubair bin Mutim:
        <p> Allah's Apostle said, "As for me, I pour water three times on my 
          head." And he pointed with both his hands.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.255"> Volume 1, Book 5, Number 255: </a> </em>
      <blockquote>
        <p> Narrated Jabir bin 'Abdullah:
        <p> The Prophet used to pour water three times on his head.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.256"> Volume 1, Book 5, Number 256: </a> </em>
      <blockquote>
        <p> Narrated Abu Ja'far:
        <p> Jabir bin Abdullah said to me, "Your cousin (Hasan bin Muhammad bin 
          Al-Hanafiya) came to me and asked about the bath of Janaba. I replied,
          'The Prophet uses to take three handfuls of water, pour them on his 
          head and then pour more water over his body.' Al-Hasan said to me, 'I 
          am a hairy man.' I replied, 'The Prophet had more hair than you'. "
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.257"> Volume 1, Book 5, Number 257: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> I placed water for the bath of the Prophet. He washed his hands twice 
          or thrice and then poured water on his left hand and washed his 
          private parts. He rubbed his hands over the earth (and cleaned them), 
          rinsed his mouth, washed his nose by putting water in it and blowing 
          it out, washed his face and both forearms and then poured water over 
          his body. Then he withdrew from that place and washed his feet.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.258"> Volume 1, Book 5, Number 258: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Whenever the Prophet took the bath of Janaba (sexual relation or wet 
          dream) he asked for the Hilab or some other scent. He used to take it 
          in his hand, rub it first over the right side of his head and then 
          over the left and then rub the middle of his head with both hands.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.259"> Volume 1, Book 5, Number 259: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> I placed water for the bath of the Prophet and he poured water with 
          his right hand on his left and washed them. Then he washed his private
          parts and rubbed his hands on the ground, washed them with water, 
          rinsed his mouth and washed his nose by putting water in it and 
          blowing it out, washed his face and poured water on his head. He 
          withdrew from that place and washed his feet. A piece of cloth (towel)
          was given to him but he did not use it.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.260"> Volume 1, Book 5, Number 260: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> The Prophet took the bath of Janaba. (sexual relation or wet dream). 
          He first cleaned his private parts with his hand, and then rubbed 
          it(that hand) on the wall (earth) and washed it. Then he performed 
          ablution like that for the prayer, and after the bath he washed his 
          feet.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.261"> Volume 1, Book 5, Number 261: </a> </em>
      <blockquote>
        <p> Narrated Aisha:
        <p> The Prophet and I used to take a bath from a single pot of water and 
          our hands used to go in the pot after each other in turn.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.262"> Volume 1, Book 5, Number 262: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> Whenever Allah's Apostle took a bath of Janaba, he washed his hands 
          first.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.263"> Volume 1, Book 5, Number 263: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The Prophet and I used to take a bath from a single pot of water after
          Janaba.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.264"> Volume 1, Book 5, Number 264: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> the Prophet and one of his wives used to take a bath from a single pot
          of water. (Shu'ba added to Anas's Statement "After the Janaba")
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.265"> Volume 1, Book 5, Number 265: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> I placed water for the bath of Allah's Apostle and he poured water 
          over his hands and washed them twice or thrice; then he poured water 
          with his right hand over his left and washed his private parts (with 
          his left hand). He rubbed his hand over the earth and rinsed his mouth
          and washed his nose by putting water in it and blowing it out. After 
          that he washed his face, both fore arms and head thrice and then 
          poured water over his body. He withdrew from that place and washed his
          feet.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.266"> Volume 1, Book 5, Number 266: </a> </em>
      <blockquote>
        <p> Narrated Maimuna bint Al-Harith:
        <p> I placed water for the bath of Allah's Apostle and put a screen. He 
          poured water over his hands, and washed them once or twice. (The 
          subnarrator added that he did not remember if she had said thrice or 
          not). Then he poured water with his right hand over his left one and 
          washed his private parts. He rubbed his hand over the earth or the 
          wall and washed it. He rinsed his mouth and washed his nose by putting
          water in it and blowing it out. He washed his face, forearms and head.
          He poured water over his body and then withdrew from that place and 
          washed his feet. I presented him a piece of cloth (towel) and he 
          pointed with his hand (that he does not want it) and did not take it.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.267"> Volume 1, Book 5, Number 267: </a> </em>
      <blockquote>
        <p> Narrated Muhammad bin Al-Muntathir:
        <p> on the authority of his father that he had asked 'Aisha (about the 
          Hadith of Ibn 'Umar). She said, "May Allah be Merciful to Abu 
          'Abdur-Rahman. I used to put scent on Allah's Apostle and he used to 
          go round his wives, and in the morning he assumed the Ihram, and the 
          fragrance of scent was still coming out from his body."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.268"> Volume 1, Book 5, Number 268: </a> </em>
      <blockquote>
        <p> Narrated Qatada:
        <p> Anas bin Malik said, "The Prophet used to visit all his wives in a 
          round, during the day and night and they were eleven in number." I 
          asked Anas, "Had the Prophet the strength for it?" Anas replied, "We 
          used to say that the Prophet was given the strength of thirty (men)." 
          And Sa'id said on the authority of Qatada that Anas had told him about
          nine wives only (not eleven).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.269"> Volume 1, Book 5, Number 269: </a> </em>
      <blockquote>
        <p> Narrated 'Ali:
        <p> I used to get emotional urethral discharge frequently. Being the 
          son-in-law of the Prophet I requested a man to ask him about it. So 
          the man asked the Prophet about it. The Prophet replied, "Perform 
          ablution after washing your organ (penis)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.270"> Volume 1, Book 5, Number 270: </a> </em>
      <blockquote>
        <p> Narrated Muhammad bin Al-Muntathir:
        <p> on the authority of his father that he had asked 'Aisha about the 
          saying of Ibn 'Umar(i.e. he did not like to be a Muhrim while the 
          smell of scent was still coming from his body). 'Aisha  said, "I 
          scented Allah's Apostle and he went round (had sexual intercourse 
          with) all his wives, and in the morning he was Muhrim (after taking a 
          bath)."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.271"> Volume 1, Book 5, Number 271: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> It is as if I am just looking at the glitter of scent in the parting 
          of the Prophet's head hair while he was a Muhrim.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.272"> Volume 1, Book 5, Number 272: </a> </em>
      <blockquote>
        <p> Narrated Hisham bin 'Urwa:
        <p> (on the authority of his father) 'Aisha said, "Whenever Allah's 
          Apostle took the bath of Janaba, he cleaned his hands and performed 
          ablution like that for prayer and then took a bath and rubbed his 
          hair, till he felt that the whole skin of the head had become wet, 
          then he would pour water thrice and wash the rest of the body." 'Aisha
          further said, "I and Allah's Apostle used to take a bath from a single
          water container, from which we took water simultaneously."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.273"> Volume 1, Book 5, Number 273: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> Water was placed for the ablution of Allah's Apostle after Janaba. He 
          poured water with his right hand over his left twice or thrice and 
          then washed his private parts and rubbed his hand on the earth or on a
          wall twice or thrice and then rinsed his mouth, washed his nose by 
          putting water in it and then blowing it out arid then washed his face 
          and forearms and poured water over his head and washed his body. Then 
          he shifted from that place and washed his feet. I brought a piece of 
          cloth, but he did not take it and removed the traces of water from his
          body with his hand."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.274"> Volume 1, Book 5, Number 274: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> Once the call (Iqama) for the prayer was announced and the rows were 
          straightened. Allah's Apostle came out; and when he stood up at his 
          Musalla, he remembered that he was Junub. Then he ordered us to stay 
          at our places and went to take a bath and then returned with water 
          dropping from his head. He said, "Allahu-Akbar", and we all offered 
          the prayer with him.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.275"> Volume 1, Book 5, Number 275: </a> </em>
      <blockquote>
        <p> Narrated Maimuna:
        <p> I placed water for the bath of the Prophet and screened him with a 
          garment. He poured water over his hands and washed them. After that he
          poured water with his right hand over his left and washed his private 
          parts, rubbed his hands with earth and washed them, rinsed his mouth, 
          washed his nose by putting water in it and then blowing it out and 
          then washed his face and forearms. He poured water over his head and 
          body. He then shifted from that place and washed his feet. I gave him 
          a piece of cloth but he did not take it and came out removing the 
          water (from his body) with both his hands.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.276"> Volume 1, Book 5, Number 276: </a> </em>
      <blockquote>
        <p> Narrated Aisha:
        <p> Whenever any one of us was Junub, she poured water over her head 
          thrice with both her hands and then rubbed the right side of her head 
          with one hand and rubbed the left side of the head with the other 
          hand.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="001.005.277"> Volume 1, Book 5, Number 277: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> The Prophet said, 'The (people of) Bani Israel used to take bath naked
          (all together) looking at each other. The Prophet Moses used to take a
          bath alone. They said, 'By Allah! Nothing prevents Moses from taking a
          bath with us except that he has a scrotal hernia.' So once Moses went 
