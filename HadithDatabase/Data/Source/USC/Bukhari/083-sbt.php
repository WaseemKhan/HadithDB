          house so that the people might gather before him. Then he admitted 
          them and (when they came in), he said, "What do you think of 
          Al-Qasama?" They said, "We say that it is lawful to depend on 
          Al-Qasama in Qisas, as the previous Muslim Caliphs carried out Qisas 
          depending on it." Then he said to me, "O Abu Qilaba! What do you say 
          about it?" He let me appear before the people and I said, "O Chief of 
          the Believers! You have the chiefs of the army staff and the nobles of
          the Arabs. If fifty of them testified that a married man had committed
          illegal sexual intercourse in Damascus but they had not seen him 
          (doing so), would you stone him?" He said, "No." I said, "If fifty of 
          them testified that a man had committed theft in Hums, would you cut 
          off his hand though they did not see him?" He replied, "No." I said, 
          "By Allah, Allah's Apostle never killed anyone except in one of the 
          following three situations: (1) A person who killed somebody unjustly,
          was killed (in Qisas,) (2) a married person who committed illegal 
          sexual intercourse and (3) a man who fought against Allah and His 
          Apostle and deserted Islam and became an apostate." Then the people 
          said, "Didn't Anas bin Malik narrate that Allah's Apostle cut off the 
          hands of the thieves, branded their eyes and then, threw them in the 
          sun?" I said, "I shall tell you the narration of Anas. Anas said: 
          "Eight persons from the tribe of 'Ukl came to Allah's Apostle and gave
          the Pledge of allegiance for Islam (became Muslim). The climate of the
          place (Medina) did not suit them, so they became sick and complained 
          about that to Allah's Apostle. He said (to them ), "Won't you go out 
          with the shepherd of our camels and drink of the camels' milk and 
          urine (as medicine)?" They said, "Yes." So they went out and drank the
          camels' milk and urine, and after they became healthy, they killed the
          shepherd of Allah's Apostle and took away all the camels. This news 
          reached Allah's Apostle , so he sent (men) to follow their traces and 
          they were captured and brought (to the Prophet). He then ordered to 
          cut their hands and feet, and their eyes were branded with heated 
          pieces of iron, and then he threw them in the sun till they died." I 
          said, "What can be worse than what those people did? They deserted 
          Islam, committed murder and theft." 
        <p> Then 'Anbasa bin Said said, "By Allah, I never heard a narration like 
          this of today." I said, "O 'Anbasa! You deny my narration?" 'Anbasa 
          said, "No, but you have related the narration in the way it should be 
          related. By Allah, these people are in welfare as long as this Sheikh 
          (Abu Qilaba) is among them." I added, "Indeed in this event there has 
          been a tradition set by Allah's Apostle. The narrator added: Some 
          Ansari people came to the Prophet and discussed some matters with him,
          a man from amongst them went out and was murdered. Those people went 
          out after him, and behold, their companion was swimming in blood. They
          returned to Allah's Apostle and said to him, "O Allah's Apostle, we 
          have found our companion who had talked with us and gone out before 
          us, swimming in blood (killed)." Allah's Apostle went out and asked 
          them, "Whom do you suspect or whom do you think has killed him?" They 
          said, "We think that the Jews have killed him." The Prophet sent for 
          the Jews and asked them, "Did you kill this (person)?" They replied, 
          "No." He asked the Al-Ansars, "Do you agree that I let fifty Jews take
          an oath that they have not killed him?" They said, "It matters little 
          for the Jews to kill us all and then take false oaths." He said, "Then
          would you like to receive the Diya after fifty of you have taken an 
          oath (that the Jews have killed your man)?" They said, "We will not 
          take the oath." Then the Prophet himself paid them the Diya 
          (Blood-money)." The narrator added, "The tribe of Hudhail repudiated 
          one of their men (for his evil conduct) in the Pre-lslamic period of 
          Ignorance.
        <p> Then, at a place called Al-Batha' (near Mecca), the man attacked a 
          Yemenite family at night to steal from them, but a. man from the 
          family noticed him and struck him with his sword and killed him. The 
          tribe of Hudhail came and captured the Yemenite and brought him to 
          'Umar during the Hajj season and said, "He has killed our companion." 
          The Yemenite said, "But these people had repudiated him (i.e., their 
          companion)." 'Umar said, "Let fifty persons of Hudhail swear that they
          had not repudiated him." So forty-nine of them took the oath and then 
          a person belonging to them, came from Sham and they requested him to 
          swear similarly, but he paid one-thousand Dirhams instead of taking 
          the oath. They called another man instead of him and the new man shook
          hands with the brother of the deceased. Some people said, "We and 
          those fifty men who had taken false oaths (Al-Qasama) set out, and 
          when they reached a place called Nakhlah, it started raining so they 
          entered a cave in the mountain, and the cave collapsed on those fifty 
          men who took the false oath, and all of them died except the two 
          persons who had shaken hands with each other. They escaped death but a
          stone fell on the leg of the brother of the deceased and broke it, 
          whereupon he survived for one year and then died." I further said, 
