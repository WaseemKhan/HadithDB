      <em> <a href="sbtintro.php"> Translation of Sahih Bukhari, Book 83: </a> </em>
      <h1> <em> Blood Money (Ad-Diyat) </em> </h1>
      <hr>
      <p> <em> <a name="009.083.001"> Volume 9, Book 83, Number 1: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> A man said, "O Allah's Apostle! Which sin is the greatest in Allah's 
          Sight?" The Prophet said, "To set up a rival unto Allah though He 
          Alone created you . " The man said, "What is next?" The Prophet said, 
          "To kill your son lest he should share your food with you." The man 
          said, "What is next?" The Prophet said, "To commit illegal sexual 
          intercourse with the wife of your neighbor." So Allah revealed in 
          confirmation of this narration:--
        <p> 'And those who invoke not with Allah, any other god. Nor kill, such 
          life as Allah has forbidden except for just cause nor commit illegal 
          sexual intercourse. And whoever does this shall receive the 
          punishment.'   (25.68)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.002"> Volume 9, Book 83, Number 2: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Umar:
        <p> Allah's Apostle said, "A faithful believer remains at liberty 
          regarding his religion unless he kills somebody unlawfully."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.003"> Volume 9, Book 83, Number 3: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> One of the evil deeds with bad consequence from which there is no 
          escape for the one who is involved in it is to kill someone 
          unlawfully.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.004"> Volume 9, Book 83, Number 4: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> The Prophet said, "The first cases to be decided among the people (on 
          the Day of Resurrection) will be those of blood-shed."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.005"> Volume 9, Book 83, Number 5: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdad bin 'Amr Al-Kindi:
        <p> An ally of Bani Zuhra who took part in the battle of Badr with the 
          Prophet, that he said, "O Allah's Apostle! If I meet an unbeliever and
          we have a fight, and he strikes my hand with the sword and cuts it 
          off, and then takes refuge from me under a tree, and says, 'I have 
          surrendered to Allah (i.e. embraced Islam),' may I kill him after he 
          has said so?" Allah's Apostle said, "Do not kill him." Al-Miqdad said,
          "But O Allah's Apostle! He had chopped off one of my hands and he said
          that after he had cut it off. May I kill him?" The Prophet said. "Do 
          not kill him for if you kill him, he would be in the position in which
          you had been before you kill him, and you would be in the position in 
          which he was before he said the sentence." The Prophet also said to 
          Al-Miqdad, "If a faithful believer conceals his faith (Islam) from the
          disbelievers, and then when he declares his Islam, you kill him, (you 
          will be sinful). Remember that you were also concealing your faith 
          (Islam) at Mecca before."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.006"> Volume 9, Book 83, Number 6: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> The Prophet said, "No human being is killed unjustly, but a part of 
          responsibility for the crime is laid on the first son of Adam who 
          invented the tradition of killing (murdering) on the earth. (It is 
          said that he was Qabil).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.007"> Volume 9, Book 83, Number 7: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Umar:
        <p> The Prophet said, "After me (i.e. after my death), do not become 
          disbelievers, by striking (cutting) the necks of one another.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.008"> Volume 9, Book 83, Number 8: </a> </em>
      <blockquote>
        <p> Narrated Abu Zur'a bin 'Amr bin Jarir:
        <p> The Prophet said during Hajjat-al-Wada', "Let the people be quiet and 
          listen to me. After me, do not become disbelievers, by striking 
          (cutting) the necks of one another."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.009"> Volume 9, Book 83, Number 9: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah bin 'Amr:
        <p> The Prophet said, "Al-Kaba'ir (the biggest sins) are: To join others 
          (as partners) in worship with Allah, to be undutiful to one's 
          parents," or said, "to take a false oath." (The sub-narrator, Shu'ba 
          is not sure) Mu'adh said: Shu'ba said, "Al-kaba'ir (the biggest sins) 
          are: (1) Joining others as partners in worship with Allah, (2) to take
          a false oath (3) and to be undutiful to one's parents," or said, "to 
          murder (someone unlawfully).
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.010"> Volume 9, Book 83, Number 10: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> The Prophet said, "The biggest of Al-Kaba'ir (the great sins) are (1) 
          to join others as partners in worship with Allah, (2) to murder a 
          human being, (3) to be undutiful to one's parents (4) and to make a 
          false statement," or said, "to give a false witness."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.011"> Volume 9, Book 83, Number 11: </a> </em>
      <blockquote>
        <p> Narrated Usama bin Zaid bin Haritha:
        <p> Allah's Apostle sent us (to fight) against Al-Huraqa (one of the 
          sub-tribes) of Juhaina. We reached those people in the morning and 
          defeated them. A man from the Ansar and I chased one of their men and 
          when we attacked him, he said, "None has the right to be worshipped 
          but Allah." The Ansari refrained from killing him but I stabbed him 
          with my spear till I killed him. When we reached (Medina), this news 
          reached the Prophet. He said to me, "O Usama! You killed him after he 
          had said, 'None has the right to be worshipped but Allah?"' I said, "O
          Allah's Apostle! He said so in order to save himself." The Prophet 
          said, "You killed him after he had said, 'None has the right to be 
          worshipped but Allah." The Prophet kept on repeating that statement 
          till I wished I had not been a Muslim before that day.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.012"> Volume 9, Book 83, Number 12: </a> </em>
      <blockquote>
        <p> Narrated 'Ubada bin As-Samat:
        <p> I was among those Naqibs (selected leaders) who gave the Pledge of 
          allegiance to Allah's Apostle. We gave the oath of allegiance, that we
          would not join partners in worship besides Allah, would not steal, 
          would not commit illegal sexual intercourse, would not kill a life 
          which Allah has forbidden, would not commit robbery, would not disobey
          (Allah and His Apostle), and if we fulfilled this pledge we would have
          Paradise, but if we committed any one of these (sins), then our case 
          will be decided by Allah.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.013"> Volume 9, Book 83, Number 13: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> The Prophet said, "Whoever carries arms against us, is not from us."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.014"> Volume 9, Book 83, Number 14: </a> </em>
      <blockquote>
        <p> Narrated Al-Ahnaf bin Qais:
        <p> I went to help that man (i.e., 'Ali), and on the way I met Abu Bakra 
          who asked me, "Where are you going?" I replied, "I am going to help 
          that man." He said, "Go back, for I heard Allah's Apostle saying, 'If 
          two Muslims meet each other with their swords then (both) the killer 
          and the killed one are in the (Hell) Fire.' I said, 'O Allah's 
          Apostle! It is alright for the killer, but what about the killed one?'
          He said, 'The killed one was eager to kill his opponent."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.015"> Volume 9, Book 83, Number 15: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> A Jew crushed the head of a girl between two stones, and the girl was 
          asked, "Who has done that to you, so-and-so or so and so?" (Some names
          were mentioned for her) till the name of that Jew was mentioned 
          (whereupon she agreed). The Jew was brought to the Prophet and the 
          Prophet kept on questioning him till he confessed, whereupon his head 
          was crushed with stones.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.016"> Volume 9, Book 83, Number 16: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> A girl wearing ornaments, went out at Medina. Somebody struck her with
          a stone. She was brought to the Prophet while she was still alive. 
          Allah's Apostle asked her, "Did such-and-such a person strike you?" 
          She raised her head, denying that. He asked her a second time, saying,
          "Did so-and-so strike you?" She raised her head, denying that. He said
          for the third time, "Did so-and-so strike you?" She lowered her head, 
          agreeing. Allah's Apostle then sent for the killer and killed him 
          between two stones.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.017"> Volume 9, Book 83, Number 17: </a> </em>
      <blockquote>
        <p> Narrated 'Abdullah:
        <p> Allah's Apostle said, "The blood of a Muslim who confesses that none 
          has the right to be worshipped but Allah and that I am His Apostle, 
          cannot be shed except in three cases: In Qisas for murder, a married 
          person who commits illegal sexual intercourse and the one who reverts 
          from Islam (apostate) and leaves the Muslims."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.018"> Volume 9, Book 83, Number 18: </a> </em>
      <blockquote>
        <p> Narrated Anas:
        <p> A Jew killed a girl so that he may steal her ornaments. He struck her 
          with a stone, and she was brought to the Prophet while she was still 
          alive. The Prophet asked her, "Did such-and-such person strike you?" 
          She gestured with her head, expressing denial. He asked her for the 
          second time, and she again gestured with her head, expressing denial. 
          When he asked her for the third time, she beckoned, "Yes." So the 
          Prophet killed him (the Jew) with two stones.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.019"> Volume 9, Book 83, Number 19: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> In the year of the Conquest of Mecca, the tribe of Khuza'a killed a 
          man from the tribe of Bam Laith in revenge for a killed person 
          belonging to them in the Pre-lslamic Period of Ignorance. So Allah's 
          Apostle got up saying, "Allah held back the (army having) elephants 
          from Mecca, but He let His Apostle and the believers overpower the 
          infidels (of Mecca). Beware! (Mecca is a sanctuary)! Verily! Fighting 
          in Mecca was not permitted for anybody before me, nor will it be 
          permitted for anybody after me; It was permitted for me only for a 
          while (an hour or so) of that day. No doubt! It is at this moment a 
          sanctuary; its thorny shrubs should not be uprooted; its trees should 
          not be cut down; and its Luqata (fallen things) should not be picked 
          up except by the one who would look for its owner. And if somebody is 
          killed, his closest relative has the right to choose one of two 
          things, i.e., either the Blood money or retaliation by having the 
          killer killed." Then a man from Yemen, called Abu Shah, stood up and 
          said, "Write that) for me, O Allah's Apostle!" Allah's Apostle said 
          (to his companions), "Write that for Abu Shah." Then another man from 
          Quraish got up, saying, "O Allah's Apostle! Except Al-Idhkhir (a 
          special kind of grass) as we use it in our houses and for graves." 
          Allah's Apostle said, "Except Al-idhkkir."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.020"> Volume 9, Book 83, Number 20: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> For the children of Israel the punishment for crime was Al-Qisas only 
          (i.e., the law of equality in punishment) and the payment of Blood 
          money was not permitted as an alternate. But Allah said to this nation
          (Muslims): 'O you who believe! Qisas is prescribed for you in case of 
          murder, .....(up to) ...end of the Verse. (2.178)
        <p> Ibn 'Abbas added: Remission (forgiveness) in this Verse, means to 
          accept the Blood-money in an intentional murder. Ibn 'Abbas added: The
          Verse: 'Then the relatives should demand Blood-money in a reasonable 
          manner.'   (2.178) means that the demand should be reasonable and it 
          is to be compensated with handsome gratitude.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.021"> Volume 9, Book 83, Number 21: </a> </em>
      <blockquote>
        <p> Narrated Ibn 'Abbas:
        <p> The Prophet said, "The most hated persons to Allah are three: (1) A 
          person who deviates from the right conduct, i.e., an evil doer, in the
          Haram (sanctuaries of Mecca and Medina); (2) a person who seeks that 
          the traditions of the Pre-lslamic Period of Ignorance, should remain 
          in Islam (3) and a person who seeks to shed somebody's blood without 
          any right."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.022"> Volume 9, Book 83, Number 22: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> The pagans were defeated on the day (of the battle) of Uhud. Satan 
          shouted among the people on the day of Uhud, "O Allah's worshippers! 
          Beware of what is behind you!" So the front file of the army attacked 
          the back files (mistaking them for the enemy) till they killed 
          Al-Yaman. Hudhaifa (bin Al-Yaman) shouted, "My father!" My father! But
          they killed him. Hudhaifa said, "May Allah forgive you." (The narrator
          added: Some of the defeated pagans fled till they reached Taif.)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.023"> Volume 9, Book 83, Number 23: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
        <p> A Jew crushed the head of a girl between two stones. It was said to 
          her. "Who has done this to you, such-and-such person, such-and-such 
          person?" When the name of the Jew was mentioned, she nodded with her 
          head, agreeing. So the Jew was brought and he confessed. The Prophet 
          ordered that his head be crushed with the stones. (Hammam said, "with 
          two stones.")
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.024"> Volume 9, Book 83, Number 24: </a> </em>
      <blockquote>
        <p> Narrated Anas bin Malik:
          The Prophet killed a Jew for killing a girl in order to take her orna
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.025"> Volume 9, Book 83, Number 25: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> We poured medicine into the mouth of the Prophet during his ailment. 
          He said, "Don't pour medicine into my mouth." (We thought he said 
          that) out of the aversion a patient usually has for medicines. When he
          improved and felt better he said, "There is none of you but will be 
          forced to drink medicine, except Al-'Abbas, for he did not witness 
          your deed."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.026"> Volume 9, Book 83, Number 26: </a> </em>
      <blockquote>
        <p> Narrated Abu Huraira:
        <p> That he heard Allah's Apostle saying, "We (Muslims) are the last (to 
          come) but (will be) the foremost (on the Day of Resurrection)." And 
          added, "If someone is peeping (looking secretly) into your house 
          without your permission, and you throw a stone at him and destroy his 
          eyes, there will be no blame on you."
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.027"> Volume 9, Book 83, Number 27: </a> </em>
      <blockquote>
        <p> Narrated Yahya:
        <p> Humaid said, "A man peeped into the house of the Prophet and the 
          Prophet aimed an arrow head at him to hit him." I asked, "Who told you
          that?" He said, "Anas bin Malik" (See Hadith No. 258 and 259, Vol. 8)
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.028"> Volume 9, Book 83, Number 28: </a> </em>
      <blockquote>
        <p> Narrated 'Aisha:
        <p> "When it was the day of (the battle of) Uhud, the pagans were 
          defeated. Then Satan shouted, "O Allah's worshipers! Beware of what is
          behind you!" So the front files attacked the back files of the army. 
          Hudhaifa looked, and behold, there was his father, Al-Yaman (being 
          attacked) ! He shouted (to his companions), "O Allah's worshipers, my 
          father, my father!" But by Allah, they did not stop till they killed 
          him (i.e., Hudhaifa's father). Hudhaifa said, "May Allah forgive you."
          ('Urwa said, Hudhaifa continued asking Allah's Forgiveness for the 
          killer of his father till he died.
        <p>        
      </blockquote>
      <hr>
      <em> <a name="009.083.029"> Volume 9, Book 83, Number 29: </a> </em>
      <blockquote>
        <p> Narrated Salama:
        <p> We went out with the Prophet to Khaibar. A man (from the companions) 
