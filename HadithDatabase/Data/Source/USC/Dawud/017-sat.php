      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 17: </a> </em>
      <h1> <em> Wills (Kitab Al-Wasaya) </em> </h1>
      <hr>
      <em> <a name="017.2860"> Book 17, Number 2860: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: A man giving a dirham as sadaqah (charity) during his life is better than giving one hundred dirhams as sadaqah (charity) at the moment of his death. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2861"> Book 17, Number 2861: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: A man or a woman acts in obedience to Allah for sixty years, then when they are about to die they cause injury by their will, so they must go to Hell. Then AbuHurayrah recited: "After a legacy which you bequeath or a debt, causing no injury...that will be the mighty success. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2864"> Book 17, Number 2864: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Allah has appointed for everyone who has a right what is due to him, and no bequest must be made to an heir. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2865"> Book 17, Number 2865: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> When Allah, Most High, revealed the verses: "Come not nigh to the orphan's property except to improve it". And "Those who unjustly eat up the property of orphans", everyone who had an orphan with him went and separated his food from his (orphan's) food, and his drink from his drink, and began to detain the remaining food which he (the orphan) himself ate or spoiled. </p>
        <p> This fell heavy on them, and they mentioned this to the Apostle of Allah (peace_be_upon_him). So Allah, Most High, revealed the verse: "They ask thee concerning orphans. Say: The best thing to do is what is for their good; if ye mix their affairs with yours, they are your brethren." Then they mixed their food with his food and their drink with his drink. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2866"> Book 17, Number 2866: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and said: I am poor, I have nothing (with me), and I have an orphan. He said: Use the property of your orphan without spending it lavishly, hurrying and taking it as your own property. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2867"> Book 17, Number 2867: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> I memorised (a tradition) from the Apostle of Allah (peace_be_upon_him): There is no orphanhood after puberty, and there is no silence for the whole day till the night. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2869"> Book 17, Number 2869: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> A Companion of the Prophet (peace_be_upon_him) said: A man asked him (the Prophet): Apostle of Allah, what are the grave sins? He replied: They are nine. He then mentioned the tradition to the same effect. This version adds: "And disobedience to the Muslim parents, and to violate the sacred House, your qiblah (direction of prayer), in your life and after death. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2875"> Book 17, Number 2875: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> A woman said: Apostle of Allah, my mother suddenly died; if it had not happened, she would have given sadaqah (charity) and donated (something). Will it suffice if I give sadaqah on her behalf? The Prophet (peace_be_upon_him) said: Yes, give sadaqah on her behalf. </p>
      </blockquote>
      <hr>
      <em> <a name="017.2877"> Book 17, Number 2877: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> Al-'As ibn Wa'il left his will that a hundred slaves should be emancipated on his behalf. His son Hisham emancipated fifty slaves and his son Amr intended to emancipate the remaining fifty on his behalf, but he said: I should ask first the Apostle of Allah (peace_be_upon_him). He, therefore, came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, my father left in his will that a hundred slaves should be emancipated on his behalf and Hisham has emancipated fifty on his behalf and fifty remain. Shall I emancipate them on his behalf? The Apostle of Allah (peace_be_upon_him) said: Had he been a Muslim and you had emancipated slaves on his behalf, or given sadaqah on his behalf, or performed the pilgrimage, that would have reached him. </p>
      </blockquote>
