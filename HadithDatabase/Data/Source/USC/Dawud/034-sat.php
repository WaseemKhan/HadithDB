      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 34: </a> </em>
      <h1> <em> Signet-Rings (Kitab Al-Khatam) </em> </h1>
      <hr>
      <em> <a name="034.4210"> Book 34, Number 4210: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet of Allah (peace_be_upon_him) disliked ten things: Yellow colouring, meaning khaluq, dyeing grey hair, trailing the lower garment, wearing a gold signet-ring, a woman decking herself before people who are not within the prohibited degrees, throwing dice, using spells except with the Mu'awwidhatan, wearing amulets, withdrawing the penis before the semen is discharged, in the case of a woman who is wife or not a wife, and having intercourse with a woman who is suckling a child; but he did not declare them to be prohibited. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4211"> Book 34, Number 4211: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and he was wearing a signet-ring of yellow copper. He said to him: How is it that I notice the odour of idols in you? So he threw it away, and came wearing an iron signet ring. He (the Prophet) said: What is it that I see you wearing the adornment of the inhabitants of Hell? So he threw it away. He asked: Apostle of Allah, what material I must use? He said: Make it of silver, but do not weigh it as much as a mithqal, </p>
      </blockquote>
      <hr>
      <em> <a name="034.4212"> Book 34, Number 4212: </a> </em>
      <blockquote>
        <p> Narrated AbuDhubab: </p>
        <p> The signet-ring of the Prophet (peace_be_upon_him) was of iron polished with silver. Sometimes it remained in my possession. Al-Mu'ayqib was in charge of the signet-ring of the Prophet (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="034.4214"> Book 34, Number 4214: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) used to wear the signet-ring on his right hand. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4215"> Book 34, Number 4215: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) used to wear the signet-ring on his left hand, and put its stone next the palm of his hand. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4217"> Book 34, Number 4217: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Muhammad ibn Ishaq said: I saw as-Salt ibn Abdullah ibn Nawfal ibn AbdulMuttalib wearing the signet-ring on his right small finger. I asked: What is this? He replied: I saw Ibn Abbas wearing his ring in this manner. He put its stone towards the upper part of his palm. Ibn Abbas also mentioned that the Apostle of Allah (peace_be_upon_him) used to wear his signet-ring in his manner. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4218"> Book 34, Number 4218: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Ibn az-Zubayr told that a woman client of theirs took az-Zubayr's daughter to Umar ibn al-Khattab wearing bells on her legs. Umar cut them off and said that he had heard the Apostle of Allah (peace_be_upon_him) say: There is a devil along with each bell. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4220"> Book 34, Number 4220: </a> </em>
      <blockquote>
        <p> Narrated Arfajah ibn As'ad: </p>
        <p> AbdurRahman ibn Tarafah said that his grandfather Arfajah ibn As'ad who had his nose cut off at the battle of al-Kilab got a silver nose, but it developed a stench, so the Prophet (peace_be_upon_him) ordered him to get a gold nose. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4221"> Book 34, Number 4221: </a> </em>
      <blockquote>
        <p> Narrated Arfajah ibn As'ad: </p>
        <p> The tradition mentioned above (No. 4220) has also been transmitted by Arfajah ibn As'ad through a different chain to the same effect. </p>
        <p> Yazid said: I asked AbulAshhab: Did AbdurRahman ibn Tarafah meet his grandfather Arfajah? He replied: Yes. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4223"> Book 34, Number 4223: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) got some ornaments presented by Negus as a gift to him. They contained a gold ring with an Abyssinian stone. The Apostle of Allah (peace_be_upon_him) turning his attention from it took it by means of a stick or his finger, then called Umamah, daughter of Abul'As and daughter of his daughter Zaynab, and said: Wear it, my dear daughter. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4224"> Book 34, Number 4224: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone wants to put a ring of fire on one he loves, let him put a gold ring on him: if anyone wants to put a necklace of fire on one he loves, let him put a gold necklace on him, and if anyone wants to put a bracelet of fire on one he loves let him put a gold bracelet on him. Keep to silver and amuse yourselves with it. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4225"> Book 34, Number 4225: </a> </em>
      <blockquote>
        <p> Narrated A sister of Hudhayfah: </p>
        <p> The Prophet (peace_be_upon_him) said: You women folk, have in silver something with which you adorn yourselves. I assure you that any woman of you who adorns herself with gold which she displays will be punished for it. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4226"> Book 34, Number 4226: </a> </em>
      <blockquote>
        <p> Narrated Asma' daughter of Yazid: </p>
        <p> The Prophet (peace_be_upon_him) said: Any woman who wears a gold necklace will have a similar one of fire put on her neck on the Day of Resurrection, and any woman who puts a gold earring in her ear will have a similar one of fire put in her ear on the Day of Resurrection. </p>
      </blockquote>
      <hr>
      <em> <a name="034.4227"> Book 34, Number 4227: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn AbuSufyan: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to ride on panther skins and to wear gold except a little. </p>
      </blockquote>
