      <div class="content"><em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 39: </a> </em>
        <h1> <em> Types of Blood-Wit (Kitab Al-Diyat) </em> </h1>
        <hr>
        <em> <a name="039.4479"> Book 39, Number 4479: </a> </em>
        <blockquote>
          <p> Narrated Abdullah Ibn Abbas: </p>
          <p> Qurayzah and Nadir (were two Jewish tribes). An-Nadir were nobler than Qurayzah. When a man of Qurayzah killed a man of an-Nadir, he would be killed. But if a man of an-Nadir killed a man of Qurayzah, a hundred wasq of dates would be paid as blood-money. When Prophethood was bestowed upon the Prophet (peace_be_upon_him), a man of an-Nadir killed a man of Qurayzah. </p>
          <p> They said: Give him to us, we shall kill him.  They replied: We have the Prophet (peace_be_upon_him) between you and us. So they came to him. </p>
          <p> Thereupon the following verse was revealed: "If thou judge, judge in equity between them." "In equity" means life for a life. </p>
          <p> The following verse was then revealed: "Do they seek of a judgment of (the days) ignorance?" </p>
        </blockquote>
        <hr>
        <em> <a name="039.4480"> Book 39, Number 4480: </a> </em>
        <blockquote>
          <p> Narrated AbuRimthah: </p>
          <p> I went to the Prophet (peace_be_upon_him) with my father. The Apostle of Allah (peace_be_upon_him) then asked my father: Is this your son? He replied: Yes, by the Lord of the Ka'bah. He again said: Is it true? He said: I bear witness to it. The Apostle of Allah (peace_be_upon_him) then smiled for my resemblance with my father, and for the fact that my father took an oath upon me. He then said: He will not bring evil on you, nor will you bring evil on him. The Apostle of Allah (peace_be_upon_him) recited  the verse: "No bearer of burdens can bear the burden of another." </p>
        </blockquote>
        <hr>
        <em> <a name="039.4481"> Book 39, Number 4481: </a> </em>
        <blockquote>
          <p> Narrated AbuShurayh al-Khuza'i: </p>
          <p> The Prophet (peace_be_upon_him) said: If a relative of anyone is killed, or if he suffers khabl, which means a wound, he may choose one of the three things: he may retaliate, or forgive, or receive compensation. But if he wishes a fourth (i.e. something more), hold his hands. After this whoever exceeds the limits shall be in grave penalty. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4482"> Book 39, Number 4482: </a> </em>
        <blockquote>
          <p> Narrated Anas ibn Malik: </p>
          <p> I never saw the Apostle of Allah (peace_be_upon_him) that some dispute which involved retaliation was brought to him but he commanded regarding it for remission. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4483"> Book 39, Number 4483: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> A man was killed in the lifetime of the Prophet (peace_be_upon_him). The matter was brought to the Prophet (peace_be_upon_him). He entrusted him to the legal guardian of the slain. The slayer said: Apostle of Allah, I swear by Allah, I did not intend to kill him. The Apostle of Allah (peace_be_upon_him) said to the legal guardian: Now if he is true and you kill him, you will enter Hell-fire. So he let him go. His hands were tied with a strap. He came out pulling his strap. Hence he was called Dhu an-Nis'ah (possessor of strap). </p>
        </blockquote>
        <hr>
        <em> <a name="039.4484"> Book 39, Number 4484: </a> </em>
        <blockquote>
          <p> Narrated Wa'il ibn Hujr: </p>
          <p> I was with the Prophet (peace_be_upon_him) when a man who was a murderer and had a strap round his neck was brought to him. </p>
          <p> He then called the legal guardian of the victim and asked him: Do you forgive him? </p>
          <p> He said: No.  He asked: Will you accept the blood-money?  He said: No.  He asked: Will you kill him?  He said: Yes.  He said: Take him. When he turned his back, he said: Do you forgive him?  He said: No.  He said: Will you accept the blood-money?  He said: No.  He said: Will you kill him?  He said: Yes.  He said: Take him. After repeating all this a fourth time, he said: If you forgive him, he will bear the burden of his own sin and the sin of the victim. He then forgave him.  He (the narrator) said: I saw him pulling the strap. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4487"> Book 39, Number 4487: </a> </em>
        <blockquote>
          <p> Narrated Uthman ibn Affan: </p>
          <p> AbuUmamah ibn Sahl said: We were with Uthman when he was besieged in the house. There was an entrance to the house. He who entered it heard the speech of those who were in the Bilat. Uthman then entered it. He came out to us, looking pale. </p>
          <p> He said: They are threatening to kill me now.  We said: Allah will be sufficient for you against them, Commander of the Faithful!  He asked: Why kill me? I heard the Apostle of Allah (peace_be_upon_him) say: It is not lawful to kill a man who is a Muslim except for one of the three reasons: Kufr (disbelief) after accepting Islam, fornication after marriage, or wrongfully killing someone, for which he may be killed. </p>
          <p> I swear by Allah, I have not committed fornication before or after the coming of Islam, nor did I ever want another religion for me instead of my religion since Allah gave guidance to me, nor have I killed anyone. So for what reason do you want to kill me? </p>
        </blockquote>
        <hr>
        <em> <a name="039.4488"> Book 39, Number 4488: </a> </em>
        <blockquote>
          <p> Narrated Sa'd ibn Dumayrah al-Aslami ; Dumayrah al-Aslami: </p>
          <p> Ziyad ibn Sa'd ibn Dumayrah as-Sulami said on the authority of his father (Sa'd) and his grandfather (Dumayrah) (according to Musa's version) who were present in the battle of Hunayn with the Apostle of Allah (peace_be_upon_him): After the advent of Islam, Muhallam ibn Jaththamah al-Laythi killed a man of Ashja'. </p>
          <p> That was the first blood-money decided by the Apostle of Allah (peace_be_upon_him) (for payment). Uyaynah spoke about the killing of al-Ashja'i, for he belonged to Ghatafan, and al-Aqra' ibn Habis spoke on behalf of Muhallam, for he belonged to Khunduf. The voices rose high, and the dispute and noise grew. </p>
          <p> So the Apostle of Allah (peace_be_upon_him) said: Do you not accept blood-money, Uyaynah? </p>
          <p> Uyaynah then said: No, I swear by Allah, until I cause his women to suffer the same fighting and grief as he caused my women to suffer. Again the voices rose high, and the dispute and noise grew. </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: Do you not accept the blood-money Uyaynah? Uyaynah gave the same reply as before, and a man of Banu Layth called Mukaytil stood up. He had a weapon and a skin shield in his hand. </p>
          <p> He said: I do not find in the beginning of Islam any illustration for what he has done except the one that "some sheep came on, and those in the front were shot; hence those in the rear ran away". (The other example is that) "make a law today and change it." </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: Fifty (camels) here immediately and fifty when we return to Medina. This happened during some of his journeys. Muhallam was a tall man of dark complexion. He was with the people. They continued (to make effort for him) until he was released. He sat before the Apostle of Allah (peace_be_upon_him), with his eyes flowing. </p>
          <p> He said: Apostle of Allah! I have done (the act) of which you have been informed. I repent to Allah, the Exalted, so ask Allah's forgiveness for me. Apostle of Allah! </p>
          <p> The Apostle of Allah (peace_be_upon_him) then said: Did you kill him with your weapon at the beginning of Islam. O Allah! do not forgive Muhallam. He said these words loudly. </p>
          <p> AbuSalamah added: He (Muhallam) then got up while he was wiping his tears with the end of his garment. </p>
          <p> Ibn Ishaq said: His people alleged that the Apostle of Allah (peace_be_upon_him) asked forgiveness for him after that. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4489"> Book 39, Number 4489: </a> </em>
        <blockquote>
          <p> Narrated AbuShurayb al-Ka'bi: </p>
          <p> The Prophet (peace_be_upon_him) said: Then you, Khuza'ah, have killed this man of Hudhayl, but I will pay his blood-wit. After these words of mine if a man of anyone is killed, his people will have a choice to accept blood-wit or to kill him. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4491"> Book 39, Number 4491: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Prophet (peace_be_upon_him) said: A believer will not be killed for an infidel. If anyone kills a man deliberately, he is to be handed over to the relatives of the one who has been killed. If they wish, they may kill, but if they wish, they may accept blood-wit </p>
        </blockquote>
        <hr>
        <em> <a name="039.4492"> Book 39, Number 4492: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Prophet (peace_be_upon_him) said: I will not forgive anyone who kills after accepting blood-wit </p>
        </blockquote>
        <hr>
        <em> <a name="039.4495"> Book 39, Number 4495: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> Ibn Shihab said: Jabir ibn Abdullah used to say that a Jewess from the inhabitants of Khaybar poisoned a roasted sheep and presented it to the Apostle of Allah (peace_be_upon_him) who took its foreleg and ate from it. A group of his companions also ate with him. </p>
          <p> The Apostle of Allah (peace_be_upon_him) then said: Take your hands away (from the food). The Apostle of Allah (peace_be_upon_him) then sent someone to the Jewess and he called her. </p>
          <p> He said to her: Have you poisoned this sheep?  The Jewess replied: Who has informed you? He said: This foreleg which I have in my hand has informed me.  She said: Yes.  He said: What did you intend by it?  She said: I thought if you were a prophet, it would not harm you; if you were not a prophet, we should rid ourselves of him (i.e. the Prophet). The Apostle of Allah (peace_be_upon_him) then forgave her, and did not punish her. But some of his companions who ate it, died. The Apostle of Allah (peace_be_upon_him) had himself cupped on his shoulder on account of that which he had eaten from the sheep. AbuHind cupped him with the horn and knife. He was a client of Banu Bayadah from the Ansar. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4496"> Book 39, Number 4496: </a> </em>
        <blockquote>
          <p> Narrated AbuSalamah: </p>
          <p> A Jewess presented a roasted sheep to the Apostle of Allah (peace_be_upon_him) at Khaybar. </p>
          <p> He then mentioned the rest of the tradition like that of Jabir (No. 4495). He said: Then Bashir ibn al-Bara' ibn Ma'rur al-Ansari died. He sent someone to call on the Jewess, and said to her (when she came): What motivated you to do the work you have done?  He then mentioned the rest of the tradition similar to the one mentioned by Jabir (No. 4495). </p>
          <p> The Apostle of Allah (peace_be_upon_him) then ordered regarding her and she was killed. But he (AbuSalamah) did not mention the matter of cupping. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4498"> Book 39, Number 4498: </a> </em>
        <blockquote>
          <p> Narrated AbuSalamah: </p>
          <p> Muhammad ibn Amr said on the authority of AbuSalamah, and he did not mention the name of AbuHurayrah: The Apostle of Allah (peace_be_upon_him) used to accept presents but not alms (sadaqah). </p>
          <p> This version adds: So a Jewess presented him at Khaybar with a roasted sheep which she had poisoned. The Apostle of Allah (peace_be_upon_him) ate of it and the people also ate. </p>
          <p> He then said: Take away your hands (from the food), for it has informed me that it is poisoned. Bishr ibn al-Bara' ibn Ma'rur al-Ansari died. </p>
          <p> So he (the Prophet) sent for the Jewess (and said to her): What motivated you to do the work you have done? </p>
          <p> She said: If you were a prophet, it would not harm you; but if you were a king, I should  rid the people of you. The Apostle of Allah (peace_be_upon_him) then ordered regarding her and she was killed. He then said about the pain of which he died: I continued to feel pain from the morsel which I had eaten at Khaybar. This is the time when it has cut off my aorta. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4499"> Book 39, Number 4499: </a> </em>
        <blockquote>
          <p> Narrated Umm Mubashshir: </p>
          <p> Umm Mubashshir said to the Prophet (peace_be_upon_him) during the sickness of which he died: What do you think about your illness, Apostle of Allah (peace_be_upon_him)? I do not think about the illness of my son except the poisoned sheep of which he had eaten with you at Khaybar. The Prophet (peace_be_upon_him) said: And I do not think about my illness except that. This is the time when it cut off my aorta. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4501"> Book 39, Number 4501: </a> </em>
        <blockquote>
          <p> Narrated Samurah: </p>
          <p> The Prophet (peace_be_upon_him) Said: If anyone kills his slave, we shall kill him, and if anyone cuts off the nose of his slave, we shall cut off his nose. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4504"> Book 39, Number 4504: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> A man came to the Prophet (peace_be_upon_him) crying for help. He said: His slave-girl, Apostle of Allah! He said: Woe to you, what happened with you ? He said that it was an evil one. He saw the slave-girl of his master; he became jealous of him, and cut off his penis. The Apostle of Allah (peace_be_upon_him) said: Bring the man to me. The man was called, but people could not get control over him. The Apostle of Allah (peace_be_upon_him) then said: Go away, you are free. He asked: Apostle of Allah! upon whom does my help lie? He replied: On every believer, or he said: On every Muslim. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4507"> Book 39, Number 4507: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) killed a man of Banu Nadr ibn Malik at Harrah ar-Righa' at the bank of Layyat al-Bahrah. The transmitter Mahmud (ibn Khalid) also mentioned the words along with the words "at Bahrah" "the slayer and the slain were from among them". Mahmud alone transmitted in this tradition the words "at the bank of Layyah". </p>
        </blockquote>
        <hr>
        <em> <a name="039.4509"> Book 39, Number 4509: </a> </em>
        <blockquote>
          <p> Narrated Rafi' ibn Khadij: </p>
          <p> A man of the Ansar was killed at Khaybar and his relatives went to the Prophet (peace_be_upon_him) and mentioned that to him. He asked: Have you two witnesses who can testify to the murderer of your friend? They replied: Apostle of Allah! there was not a single Muslim present, but only Jews who sometimes have the audacity to do even greater crimes than this. He said: Then choose fifty of them and demand that they take an oath; but they refused and the Prophet (peace_be_upon_him) paid the blood-wit himself. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4511"> Book 39, Number 4511: </a> </em>
        <blockquote>
          <p> Narrated Some men: </p>
          <p> The Prophet (peace_be_upon_him) said to the Jews and started with them: Fifty of you should take the oaths. But they refused (to take the oaths). He then said to the Ansar: Prove your claim. They said: Do we take the oaths without seeing, Apostle of Allah? The Apostle of Allah (peace_be_upon_him) then imposed the blood-wit on the Jews because he (the slain) was found among them. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4515"> Book 39, Number 4515: </a> </em>
        <blockquote>
          <p> Narrated Ali ibn AbuTalib: </p>
          <p> Qays ibn Abbad and Ashtar went to Ali and said to him: Did the Apostle of Allah (peace_be_upon_him) give you any instruction about anything for which he did not give any instruction to the people in general? </p>
          <p> He said: No, except what is contained in this document of mine. Musaddad said: He then took out a document. Ahmad said: A document from the sheath of his sword. </p>
          <p> It contained: The lives of all Muslims are equal; they are one hand against others; the lowliest of them can guarantee their protection. Beware, a Muslim must not be killed for an infidel, nor must one who has been given a covenant be killed while his covenant holds. If anyone introduces an innovation, he will be responsible for it. If anyone introduces an innovation or gives shelter to a man who introduces an innovation (in religion), he is cursed by Allah, by His angels, and by all the people. </p>
          <p> Musaddad said: Ibn AbuUrubah's version has: He took out a document. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4516"> Book 39, Number 4516: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said, mentioning the tradition similar to the one transmitted by Ali. This version adds: The most distant of them gives protection as from all, those who are strong among them send back (spoil) to those who are weak among them, and their expeditions sending it back to those who are at home. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4519"> Book 39, Number 4519: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) sent AbuJahm ibn Hudhayfah as a collector of zakat. A man quarrelled with him about his sadaqah (i.e. zakat), and AbuJahm struck him and wounded his head. His people came to the Prophet (peace_be_upon_him) and said: Revenge, Apostle of Allah! </p>
          <p> The Prophet (peace_be_upon_him) said: You may have so much and so much. But they did not agree. He again said: You may have so much and so much. But they did not agree. He again said: You may have so much and so much. So they agreed. </p>
          <p> The Prophet (peace_be_upon_him) said: I am going to address the people in the afternoon and tell them about your consent. </p>
          <p> They said: Yes.  Addressing (the people), the Apostle of Allah (peace_be_upon_him) said: These people of faith came to me asking for revenge. I presented them with so much and so much and they agreed. Do you agree? </p>
          <p> They said: No. The immigrants (muhajirun) intended (to take revenge) on them. But the Apostle of Allah (peace_be_upon_him) commanded them to refrain and they refrained. </p>
          <p> He then called them and increased (the amount), and asked: Do you agree?  They replied: Yes.  He said: I am going to address the people and tell them about your consent.  They said: Yes.  The Prophet (peace_be_upon_him) addressed and said: Do you agree?  They said: Yes. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4521"> Book 39, Number 4521: </a> </em>
        <blockquote>
          <p> Narrated AbuSa'id al-Khudri: </p>
          <p> When the Apostle of Allah (peace_be_upon_him) was distributing something, a man came towards him and bent down on him. The Apostle of Allah (peace_be_upon_him) struck him with a bough and his face was wounded. The Apostle of Allah (peace_be_upon_him) said to him: Come and take retaliation. He said: no, I have forgiven, Apostle of Allah!. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4522"> Book 39, Number 4522: </a> </em>
        <blockquote>
          <p> Narrated Umar ibn al-Khattab: </p>
          <p> I did not send my collectors (of zakat) so that they strike your bodies and that they take your property. If that is done with someone and he appeals to me, I shall take retaliation on him. Amr ibn al-'As said: If any man (i.e. governor) inflicts disciplinary punishment on his subjects, would you take retaliation on him too? He said: Yes, by Him in Whose hand my soul is, I shall take retaliation on him. I saw that the Apostle of Allah (peace_be_upon_him) has given retaliation on himself. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4523"> Book 39, Number 4523: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) Said: The disputants should refrain from taking retaliation. The one who is nearer should forgive first and then the one who is next to him, even if (the one who forgives) were a woman. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4526"> Book 39, Number 4526: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) gave judgment that if anyone is killed accidentally, his blood-wit should be one hundred camels: thirty she-camels which had entered their second year, thirty she-camels which had entered their third year, thirty she-camels which had entered their fourth year, and ten male camels which had entered their third year. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4527"> Book 39, Number 4527: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The value of the blood-money at the time of the Apostle of Allah (peace_be_upon_him) was eight hundred dinars or eight thousand dirhams, and the blood-money for the people of the Book was half of that for Muslims. </p>
          <p> He said: This applied till Umar (Allah be pleased with him) became caliph and he made a speech in which he said: Take note! Camels have become dear. So Umar fixed the value for those who possessed gold at one thousand dinars, for those who possessed silver at twelve thousand (dirhams), for those who possessed cattle at two hundred cows, for those who possessed sheep at two thousand sheep, and for those who possessed suits of clothing at two hundred suits. He left the blood-money for dhimmis (protected people) as it was, not raising it in proportion to the increase he made in the blood-wit. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4528"> Book 39, Number 4528: </a> </em>
        <blockquote>
          <p> Narrated Ata' ibn AbuRabah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) gave judgment that blood-wit for those who possessed camels should be one hundred camels, and for those who possessed cattle two hundred cows, and for those who possessed sheep  one thousand sheep, and for those who possessed suits of clothing two hundred suits, and for those who possessed wheat something which the narrator Muhammad (ibn Ishaq) did not remember. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4528A"> Book 39, Number 4528A: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) fixed; and he mentioned the tradition like that of Musa; he said: And those who possess corn food should pay something which I do not remember. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4529"> Book 39, Number 4529: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Mas'ud: </p>
          <p> The Prophet (peace_be_upon_him) said: The blood-wit for accidental killing should be twenty she-camels which had entered their fourth year, twenty she-camels which had entered their fifth year, twenty she-camels which had entered their second year, twenty she-camels which had entered their third year, and twenty male camels which had entered their second year. It does not beyond Ibn Mas'ud. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4530"> Book 39, Number 4530: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> A man of Banu Adi was killed. The Prophet (peace_be_upon_him) fixed his blood-wit at the rate of twelve thousand (dirhams). </p>
        </blockquote>
        <hr>
        <em> <a name="039.4531"> Book 39, Number 4531: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr: </p>
          <p> (Musaddad's version has): The Apostle of Allah (peace_be_upon_him) made a speech on the day of the conquest of Mecca, and said: Allah is Most Great, three times. He then said: There is no god but Allah alone: He fulfilled His promise, helped His servant, and alone defeated the companies. </p>
          <p> (The narrator said:) I have remembered from Musaddad up to this. </p>
          <p> Then the agreed version has: Take note! All the merits mentioned in pre-Islamic times, and the claim made for blood or property are under my feet, except the supply of water to the pilgrims and the custody of the Ka'bah. He then said: The blood-money for unintentional murder which appears intentional, such as is done with a whip and a stick, is one hundred camels, forty of which are pregnant. Musaddad's version is more accurate. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4540"> Book 39, Number 4540: </a> </em>
        <blockquote>
          <p> Narrated AbuMusa: </p>
          <p> The Prophet (peace_be_upon_him) said: The fingers are equal: ten camels for each finger. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4541"> Book 39, Number 4541: </a> </em>
        <blockquote>
          <p> Narrated AbuMusa al-Ash'ari: </p>
          <p> The Prophet (peace_be_upon_him) said: The fingers are equal. I asked: Ten camels for each? He replied: Yes. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4543"> Book 39, Number 4543: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Prophet (peace_be_upon_him) said: The fingers are equal and the teeth are equal. The front tooth and the molar tooth are equal, this and that are equal. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4544"> Book 39, Number 4544: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Prophet (peace_be_upon_him) said: The teeth are equal, and the fingers are equal. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4545"> Book 39, Number 4545: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) treated the fingers and toes as equal. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4546"> Book 39, Number 4546: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Prophet (peace_be_upon_him) said in his address while he was leaning against the Ka'bah: (The blood-wit) for each finger is ten camels. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4547"> Book 39, Number 4547: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Prophet (peace_be_upon_him) said: For each tooth are ten camels. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4547A"> Book 39, Number 4547A: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) would fix the blood-money for accidental killing at the rate of four hundred dinars or their equivalent in silver for townsmen, and he would fix it according to the price of camels. So when they were dear, he increased the amount to be paid, and when cheap prices prevailed he reduced the amount to be paid. In the time of the Apostle of Allah (peace_be_upon_him) they reached between four hundred and eight hundred dinars, their equivalent in silver being eight thousand dirhams. </p>
          <p> He said: The Apostle of Allah (peace_be_upon_him) gave judgment that those who possessed cattle should pay two hundred cows, and those who possessed sheep two thousand sheep. </p>
          <p> He said: The Apostle of Allah (peace_be_upon_him) said: The blood-money is to be treated as something to be inherited by the heirs of the one who has been killed, and the remainder should be divided among the agnates. </p>
          <p> He said: The Apostle of Allah (peace_be_upon_him) gave judgment that for cutting off a nose completely there was full blood-money, one hundred (camels) were to be paid. If the tip of the nose was cut off, half of the blood-money,i.e. fifty camels were to be paid, or their equivalent in gold or in silver, or a hundred cows, or one thousand sheep. For the hand, when it was cut of,f half of the blood-money was to be paid; for one foot of half, the blood-money was to be paid. For a wound in the head, a third of the blood-money was due, i.e. thirty-three camels and a third of the blood-money, or their equivalent in gold, silver, cows or sheep. For a head thrust which reaches the body, the same blood-money was to be paid. Ten camels were to be paid for every finger, and five camels for every tooth. </p>
          <p> The Apostle of Allah (peace_be_upon_him) gave judgment that the blood-money for a woman should be divided among her relatives on her father's side, who did not inherit anything from her except the residence of her heirs. If she was killed, her blood-money should be distributed among her heirs, and they would have the right of taking revenge on the murderer. </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: There is nothing for the murderer; and if he (the victim) has no heir, his heir will be the one who is nearest to him among the people, but the murderer should not inherit anything. </p>
          <p> Muhammad said: All this has been transmitted to me by Sulayman ibn Musa on the authority of Amr ibn Shu'ayb who, on his father's authority, said that his grandfather heard it from the Prophet (peace_be_upon_him). </p>
        </blockquote>
        <hr>
        <em> <a name="039.4548"> Book 39, Number 4548: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Prophet (peace_be_upon_him) said: Blood-wit for what resembles intentional murder is to be made as severe as that for intentional murder, but the culprit is not to be killed. Khalid gave us some additional information on the authority of Ibn Rashid: That (unintentional murder which resembles intentional murder) means that Satan jumps among the people and then the blood is shed blindly without any malice and weapon. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4549"> Book 39, Number 4549: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Prophet (peace_be_upon_him) said: Blood-wit for every wound which lays bare a bone is five camels. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4550"> Book 39, Number 4550: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) gave judgment that a third of the blood-wit should be paid for an eye fixed in its place. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4555"> Book 39, Number 4555: </a> </em>
        <blockquote>
          <p> Narrated Umar ibn al-Khattab: </p>
          <p> Ibn Abbas said: Umar asked about the decision of the Prophet (peace_be_upon_him) about that (i.e. abortion) Haml ibn Malik ibn an-Nabighah got up and said: I was between two women. One of them struck another with a rolling-pin killing both her and what was in her womb. So the Apostle of Allah (peace_be_upon_him) gave judgment that the blood-wit for the unborn child should be a male or a female slave of the best quality and that she should be killed. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4556"> Book 39, Number 4556: </a> </em>
        <blockquote>
          <p> Narrated Umar ibn al-Khattab: </p>
          <p> Tawus said: Umar stood on the pulpit. He then mentioned the rest of the tradition to the same effect as mentioned before. He did not mention "that she should be killed". This version adds: "a male or a female slave". Umar then said: Allah is Most Great. Had I not heard it, we would have decided about it something else. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4557"> Book 39, Number 4557: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> About the story of Haml ibn Malik, Ibn Abbas said: She aborted a child who had grown hair and was dead, and the woman also died. He (the Prophet) gave judgment that the blood-wit was to be paid by the woman's relatives on the father's side. Her uncle said: Apostle of Allah! She has aborted a child who had grown hair. The father of the woman who had slain said: He is a liar: I swear by Allah, he did not raise his voice, or drink or eat. No compensation is to be paid for an offence like this. The Prophet (peace_be_upon_him) said: is it a rhymed prose of pre-Islamic Arabia and its soothsaying? Pay a male or female slave of the best quality in compensation for the child. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4558"> Book 39, Number 4558: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> One of the two women of Hudhayl killed the other, Each of them had husband and sons. The Apostle of Allah (peace_be_upon_him) fixed the blood-wit for the slain woman to be paid by the woman's relatives on the father's side. He declared her husband and the child innocent. The relatives of the woman who killed said: We shall inherit from her. The Apostle of Allah (peace_be_upon_him) said: No, her sons and her husband should inherit from her. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4561"> Book 39, Number 4561: </a> </em>
        <blockquote>
          <p> Narrated Buraydah ibn al-Hasib: </p>
          <p> A woman threw a stone at another woman and she aborted. The dispute was brought to the Apostle of Allah (peace_be_upon_him). He gave judgment that five hundred sheep should be paid for her (unborn) child, and forbade throwing stones. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4562"> Book 39, Number 4562: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) gave judgment that a male or a female slave, or a horse or a mule should be paid for a miscarriage. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4564"> Book 39, Number 4564: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) gave judgment about the slave who had made an agreement to purchase his freedom (mukatab) and he had been killed that blood-wit is paid for him at the rate paid for a free man so far as he has paid the purchase money, and at the rate paid for a slave as the remainder is concerned. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4565"> Book 39, Number 4565: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Prophet (peace_be_upon_him) said: When a mukatab (a slave who has made an agreement to purchase his freedom) gifts blood-money or an inheritance, he can inherit in accordance with the extent to which he has been emancipated. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4566"> Book 39, Number 4566: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him)  said: The blood-wit for a man who makes a covenant is half of the blood-wit for a free man. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4569"> Book 39, Number 4569: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: Anyone who practises medicine when he is not known as a practitioner will be held responsible. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4570"> Book 39, Number 4570: </a> </em>
        <blockquote>
          <p> Narrated Some people: </p>
          <p> AbdulAziz ibn Umar ibn AbdulAziz said: Some people of the deputation which came to my father reported the Apostle of Allah (peace_be_upon_him) as saying: Any physician who practises medicine when he was not known as a practitioner before that and he harms (the patients) he will be held responsible. AbdulAziz said: Here physician does not refer to a man by qualification. it means opening a vein, incision and cauterisation. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4571"> Book 39, Number 4571: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Prophet (peace_be_upon_him) said: Musaddad's version has: He addressed on the day of Conquest. The agreed version then goes: Beware! Every object of pride of pre-Islamic times, whether it is blood-vengeance or property, mentioned or claimed, has been put under my feet except supply of water to the pilgrims and custody of the House (the Ka'bah). He then said: Beware! The blood-wit for unintentional murder, such as is done with a whip and stick, is one hundred camels, forty of which are pregnant. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4573"> Book 39, Number 4573: </a> </em>
        <blockquote>
          <p> Narrated Imran ibn Husayn: </p>
          <p> A servant of some poor people cut off the ear of the servant of some rich people. His people came to the Prophet (peace_be_upon_him) and said: Apostle of Allah! we are poor people. So he imposed no compensation on them. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4574"> Book 39, Number 4574: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Prophet (peace_be_upon_him) said: If anyone is killed blindly or, when people are throwing stones, by a stone or a whip, his blood-wit is the blood-wit for an accidental murder. But if anyone is killed intentionally, retaliation is due. If anyone tries to prevent it, the curse of Allah, of angels, and of all the people will rest on him. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4575"> Book 39, Number 4575: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: No recompense is to be demanded if one is kicked by an animal. </p>
        </blockquote>
        <hr>
        <em> <a name="039.4577"> Book 39, Number 4577: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: No recompense may be demanded if the fire spreads. </p>
        </blockquote>
