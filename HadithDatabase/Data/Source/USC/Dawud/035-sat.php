      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 35: </a> </em>
      <h1> <em> Trials and Fierce Battles (Kitab Al-Fitan Wa Al-Malahim) </em> </h1>
      <hr>
      <em> <a name="035.4229"> Book 35, Number 4229: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet (peace_be_upon_him) said: four (majestic) trials (fitnahs) will take place among this community, and in their end there will be destruction. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4230"> Book 35, Number 4230: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> When we were sitting with the Apostle of Allah (peace_be_upon_him), he talked about periods of trial (fitnahs), mentioning many of them. </p>
        <p> When he mentioned the one when people should stay in their houses, some asked him: Apostle of Allah, what is the trial (fitnah) of staying at home? </p>
        <p> He replied: It will be flight and plunder. Then will come a test which is pleasant. Its murkiness is due to the fact that it is produced by a man from the people of my house, who will assert that he belongs to me, whereas he does not, for my friends are only the God-fearing. Then the people will unite under a man who will be like a hip-bone on a rib. Then there will be the little black trial which will leave none of this community without giving him a slap, and when people say that it is finished, it will be extended. During it a man will be a believer in the morning and an infidel in the evening, so that the people will be in two camps: the camp of faith which will contain no hypocrisy, and the camp of hypocrisy which will contain no faith. When that happens, expect the Antichrist (Dajjal) that day or the next. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4231"> Book 35, Number 4231: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah ibn al-Yaman: </p>
        <p> I swear by Allah, I do not know whether my companions have forgotten or have pretended to forgot. I swear by Allah that the Apostle of Allah (peace_be_upon_him) did not omit a leader of a wrong belief (fitnah)--up to the end of the world--whose followers reach the number of three hundred and upwards but he mentioned to us his name, his father's name and the name of his tribe. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4232"> Book 35, Number 4232: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah ibn al-Yaman: </p>
        <p> Subay' ibn Khalid said: I came to Kufah at the time when Tustar was conquered. I took some mules from it. When I entered the mosque (of Kufah), I found there some people of moderate stature, and among them was a man whom you could recognize when you saw him that he was from the people of Hijaz. </p>
        <p> I asked: Who is he?  The people frowned at me and said: Do you not recognize him? This is Hudhayfah ibn al-Yaman, the companion of the Apostle of Allah (peace_be_upon_him). </p>
        <p> Then Hudhayfah said: People used to ask the Apostle of Allah (peace_be_upon_him) about good, and I used to ask him about evil. Then the people stared hard at him. </p>
        <p> He said: I know the reason why you dislike it. I then asked: Apostle of Allah, will there be evil as there was before, after this good which Allah has bestowed on us? </p>
        <p> He replied: Yes.  I asked: Wherein does the protection from it lie?  He replied: In the sword.  I asked: Apostle of Allah, what will then happen? </p>
        <p> He replied: If Allah has on Earth a caliph who flays your back and takes your property, obey him, otherwise die holding onto the stump of a tree. </p>
        <p> I asked: What will come next?  He replied: Then the Antichrist (Dajjal) will come forth accompanied by a river and fire. He who falls into his fire will certainly receive his reward, and have his load taken off him, but he who falls into his river will have his load retained and his reward taken off him. </p>
        <p> I then asked: What will come next?  He said: The Last Hour will come. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4234"> Book 35, Number 4234: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah: </p>
        <p> The tradition mentioned above (No. 4232) has also been transmitted through a different chain of narrators by Nasr ibn Asim al-Laythi who said: We came to al-Yashkuri with a group of the people of Banu Layth. </p>
        <p> He asked: Who are these people?  We replied: Banu Layth. We have come to you to ask you about the tradition of Hudhayfah. He then mentioned the tradition and said: I asked: Apostle of Allah, will there be evil after this good? </p>
        <p> He replied: There will be trial (fitnah) and evil.  I asked: Apostle of Allah, will there be good after this evil?  He replied: Learn the Book of Allah, Hudhayfah, and adhere to its contents. He said it three times. </p>
        <p> I asked: Apostle of Allah, will there be good after this evil?  He replied: An illusory truce and a community with specks in its eye.  I asked: Apostle of Allah, what do you mean by an illusory community? </p>
        <p> He replied: The hearts of the people will not return to their former condition.  I asked: Apostle of Allah, will there be evil after this good?  He replied: There will be wrong belief which will blind and deafen men to the truth in which there will be summoners at the gates of Hell. If you, Hudhayfah, die adhering to a stump, it will be better for you than following any of them. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4237A"> Book 35, Number 4237A: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: The Muslims will soon be besieged up to Medina, so that their most distant frontier outpost will be Salah. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4240"> Book 35, Number 4240: </a> </em>
      <blockquote>
        <p> Narrated AbuMalik al-Ash'ari: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah  has protected you from three things: that your Prophet should not invoke a curse on you and should all perish, that those who follow what is false should not prevail over those who follow the truth, and that you should not all agree in an error. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4241"> Book 35, Number 4241: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet (peace_be_upon_him) said: The mill of Islam will go round till the year thirty-five, or thirty-six, or thirty-seven; then if they perish, they will have followed the path of those who perished before them, but if their religion is maintained, it will be maintained for seventy years. I asked: Does it mean seventy years which remain or seventy years which are gone by? He replied: It means (seventy years) that are gone by. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4243"> Book 35, Number 4243: </a> </em>
      <blockquote>
        <p> Narrated AbuBakrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: There will be a period of commotion in which the one who lies down will be better than the one who sits, and the one who sits is better than the one who stands, and the one who stands is better than the one who walks, and the one who walks is better than the one who runs (to it). </p>
        <p> He asked: What do you command me to do, Apostle of Allah?  He replied: He who has camels should remain with his camels, he who has sheep should remain with his sheep, and he who has land should remain with his land. </p>
        <p> He asked: If anyone has more of these, (what should he do)? </p>
        <p> He replied: He should take his sword, strike its edge on a stone, and then escape if he can. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4244"> Book 35, Number 4244: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> I asked: Apostle of Allah! tell me if someone enters my house and extends his hands to kill me (what should I do?) The Apostle of Allah (peace_be_upon_him) replied: Be like the two sons of Adam. The narrator Yazid (ibn Khalid) then recited the verse: "If thou dost stretch they hand against me to slay me." </p>
      </blockquote>
      <hr>
      <em> <a name="035.4245"> Book 35, Number 4245: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud ; Khuraym ibn Fatik: </p>
        <p> The tradition mentioned above (No. 4243) has also been transmitted by Ibn Mas'ud through a different chain of narrators. </p>
        <p> Ibn Mas'ud said: I heard the Prophet (peace_be_upon_him) say: He then mentioned a portion of the tradition narrated by AbuBakrah (No. 4243). </p>
        <p> This version adds: He (the Prophet) said: All their slain will go to Hell.  I (Wabisah) asked: When will this happen Ibn Mas'ud? </p>
        <p> He replied: This is the period of turmoil (harj) when a man will not be safe from his associates. </p>
        <p> I asked: What do you command me (to do) if I happen to live during that period?  He replied: You should restrain your tongue and hand and stay at home. </p>
        <p> When Uthman was slain, I recollected this tradition. I then rode (on a camel) and came to Damascus. There I met Khuraym ibn Fatik and mentioned this tradition to him. He swore by Allah, there was no god but He, he had heard it from the Apostle of Allah (peace_be_upon_him), as Ibn Mas'ud transmitted it to me (Wabisah). </p>
      </blockquote>
      <hr>
      <em> <a name="035.4246"> Book 35, Number 4246: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa al-Ash'ari: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Before the Last Hour there will be commotions like pieces of a dark night in which a man will be a believer in the morning and an infidel in the evening, or a believer in the evening and infidel in the morning. He who sits during them will be better than he who gets up and he who walks during them is better than he who runs. So break your bows, cut your bowstrings and strike your swords on stones. If people then come in to one of you, let him be like the better of Adam's two sons. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4247"> Book 35, Number 4247: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> AbdurRahman ibn Samurah said: I was holding the hand of Ibn Umar on one of the ways of Medina. He suddenly came to a hanging head. He said: Unhappy is the one who killed him. When he proceeded, he said: I do not consider him but unfortunate. I heard the Apostle of Allah (peace_be_upon_him) say: If anyone goes to a man of my community in order to kill him, he should say in this way, the one who kills will go to Hell and the one who is killed will go to Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4248"> Book 35, Number 4248: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to me: O AbuDharr.  I replied: At thy service and at thy pleasure, Apostle of Allah.  He then mentioned the tradition in which he said: What will you do when there the death of the people (in Medina) and a house will reach the value of a slave (that is, a grave will be sold for a slave). </p>
        <p> I replied: Allah and His Apostle know best. Or he said: What Allah and His Apostle choose for me. </p>
        <p> He said: You must show endurance. Or he said; you may endure. He then said to me: What will you do, AbuDharr, when you see the Ahjar az-Zayt covered with blood? </p>
        <p> I replied: What Allah and His Apostle choose for me. </p>
        <p> He said: You must go to those who are like-minded. </p>
        <p> I asked: Should I not take my sword and put it on my shoulder?  He replied: you would then associate yourself with the people.  I then asked: What do you order me to do?  You must stay at home.  I asked: (What should I do) if people enter my house and find me? </p>
        <p> He replied: If you are afraid the gleam of the sword may dazzle you, put the end of your garment over your face in order that (the one who kills you) may bear the punishment of your sins and his. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4249"> Book 35, Number 4249: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa al-Ash'ari: </p>
        <p> The Prophet (peace_be_upon_him) said: Before you there will be commotions like pieces of a dark night in which a man will be a believer in the morning and an infidel in the evening. He who sits during them will be better than he who gets up, and he who gets up during them is better than he who walks, and he who walks during them is better than he who runs. They (the people) said: What do you order us to do? He replied: Keep to your houses. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4250"> Book 35, Number 4250: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdad ibn al-Aswad: </p>
        <p> I swear by Allah, I heard the Apostle of Allah (peace_be_upon_him) say: The happy man is he who avoids dissensions: happy is the man who avoids dissensions; happy is the man who avoids dissensions: but how fine is the man who is afflicted and shows endurance. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4251"> Book 35, Number 4251: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: There will be civil strife (fitnah) which will render people deaf, dumb and blind regarding what is right. Those who contemplate it will be drawn by it, and giving rein to the tongue during it will be like smiting with the sword. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4252"> Book 35, Number 4252: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: There will be civil strife which wipe out the Arabs, and their slain will go to Hell. During it the tongue will be more severe than blows of the sword. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4257"> Book 35, Number 4257: </a> </em>
      <blockquote>
        <p> Narrated AbudDarda' and Ubadah ibn as-Samit: </p>
        <p> Khalid ibn Dihqan said: When we were engaged in the battle of Constantinople at Dhuluqiyyah, a man of the people of Palestine, who was one of their nobility and elite and whose rank was known to them, came forward. He was called Hani ibn Kulthum ibn Sharik al-Kinani. He greeted Abdullah ibn Zakariyya who knew his rank. </p>
        <p> Khalid said to us: Abdullah ibn AbuZakariyya told us: I heard Umm ad-Darda' say: I heard AbudDarda' say: I heard the Apostle of Allah (peace_be_upon_him) say: It is hoped that Allah may forgive every sin, except in the case of one who dies a polytheist, or one who purposely kills a believer. </p>
        <p> Hani ibn Kulthum ar-Rabi' then said: I heard Mahmud ibn ar-Rabi' transmitting a tradition from Ubadah ibn as-Samit who transmitted from the Apostle of Allah (peace_be_upon_him) who said: If a man kills a believer unjustly, Allah will not accept any action or duty of his, obligatory or supererogatory. </p>
        <p> Khalid then said to us: Ibn AbuZakariyya transmitted a tradition to us from Umm ad-Darda' on the authority of AbudDarda' from the Apostle of Allah (peace_be_upon_him) who said: A believer will continue to go on quickly and well so long as he does not shed unlawful blood; when he sheds unlawful blood, he becomes slow and heavy-footed. </p>
        <p> A similar tradition has been transmitted by Hani ibn Kulthum from Mahmud ibn ar-Rabi' on the authority of Ubadah ibn as-Samit from the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="035.4259"> Book 35, Number 4259: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> The verse "If a man kills a believer intentionally, his recompense is Hell to abide therein for ever" was revealed six months after the verse "And those who invoke not with Allah any other god, nor slay such life as Allah has made sacred, except for just cause in Surat al-Furqan. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4264"> Book 35, Number 4264: </a> </em>
      <blockquote>
        <p> Narrated Sa'id ibn Zayd: </p>
        <p> We were with the Prophet (peace_be_upon_him). He mentioned civil strife (fitnah) and expressed its gravity. We or the people said: Apostle of Allah, if this happens to us it will destroy us. The Apostle of Allah (peace_be_upon_him) said; No. It is enough for you that you would be killed. Sa'id said: I saw that my brethren were killed. </p>
      </blockquote>
      <hr>
      <em> <a name="035.4265"> Book 35, Number 4265: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa: </p>
        <p> The Prophet (peace_be_upon_him) said: This people of mine is one to which mercy is shown. It will have no punishment in the next world, but its punishment in this world will be trials, earthquakes and being killed. </p>
      </blockquote>
