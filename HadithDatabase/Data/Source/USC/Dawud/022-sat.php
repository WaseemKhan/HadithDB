      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 22: </a> </em>
      <h1> <em> Commercial Transactions (Kitab Al-Buyu) </em> </h1>
      <hr>
      <em> <a name="022.3320"> Book 22, Number 3320: </a> </em>
      <blockquote>
        <p> Narrated Qays ibn AbuGharazah: </p>
        <p> In the time of the Apostle of Allah (peace_be_upon_him) we used to be called brokers, but the Prophet (peace_be_upon_him) came upon us one day, and called us by a better name than that, saying: O company of merchants, unprofitable speech and swearing takes place in business dealings, so mix it with sadaqah (alms). </p>
      </blockquote>
      <hr>
      <em> <a name="022.3322"> Book 22, Number 3322: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A man seized his debtor who owed ten dinars to him. He said to him: I swear by Allah, I shall not leave you until you pay off (my debt) to me or bring a surety. The Prophet (peace_be_upon_him) stood as a surety for him. </p>
        <p> He then brought as much (money) as he promised. The Prophet (peace_be_upon_him) asked: From where did you acquire this gold? He replied: From a mine. He said: We have no need of it; there is no good in it. Then the Apostle of Allah (peace_be_upon_him) paid (the debt) on his behalf. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3325"> Book 22, Number 3325: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: A time is certainly coming to mankind when only the receiver of usury will remain, and if he does not receive it, some of its vapour will reach him. Ibn Isa said: Some of its dust will reach him. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3326"> Book 22, Number 3326: </a> </em>
      <blockquote>
        <p> Narrated One of the Ansar: </p>
        <p> Asim ibn Kulayb quoted his father's authority for the following statement by one of the Ansar: We went out with the Apostle of Allah (peace_be_upon_him) to a funeral, and I saw the Apostle of Allah (peace_be_upon_him) at the grave giving this instruction to the grave-digger: </p>
        <p> Make it wide on the side of his feet, and make it wide on the side of his head. When he came back, he was received by a man who conveyed an invitation from a woman. So he came (to her), to it food was brought, and he put his hand (i.e. took a morsel in his hand); the people did the same and they ate. Our fathers noticed that the Apostle of Allah (peace_be_upon_him) was moving a morsel around his mouth. </p>
        <p> He then said: I find the flesh of a sheep which has been taken without its owner's permission. </p>
        <p> The woman sent a message to say: Apostle of Allah, I sent (someone) to an-Naqi' to have a sheep bought for me, but there was none; so I sent (a message) to my neighbour who had bought a sheep, asking him to send it to me for the price (he had paid), but he could not be found. I, therefore, sent (a message) to his wife and she sent it to me. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Give this food to the prisoners. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3327"> Book 22, Number 3327: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) cursed the one who accepted usury, the one who paid it, the witness to it, and the one who recorded it. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3328"> Book 22, Number 3328: </a> </em>
      <blockquote>
        <p> Narrated Amr ibn al-Ahwas al-Jushami: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say in the Farewell Pilgrimage: "Lo, all claims to usury of the pre-Islamic period have been abolished. You shall have your capital sums, deal not unjustly and you shall not be dealt with unjustly. </p>
        <p> Lo, all claims for blood-vengeance belonging to the pre-Islamic period have been abolished. The first of those murdered among us whose blood-vengeance I remit is al-Harith ibn AbdulMuttalib, who suckled among Banu Layth and killed by Hudhayl." </p>
        <p> He then said: O Allah, have I conveyed the message? They said: Yes, saying it three times. He then said: O Allah, be witness, saying it three times. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3330"> Book 22, Number 3330: </a> </em>
      <blockquote>
        <p> Narrated Suwayd ibn Qays: </p>
        <p> I and Makhrafah al-Abdi imported some garments from Hajar, and brought them to Mecca. The Apostle of Allah (peace_be_upon_him) came to us walking, and after he had bargained with us for some trousers, we sold them to him. There was a man who was weighing for payment. The Apostle of Allah (peace_be_upon_him) said to him: Weigh out and give overweight. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3331"> Book 22, Number 3331: </a> </em>
      <blockquote>
        <p> Narrated AbuSafwan ibn Umayrah: </p>
        <p> (The tradition mentioned above (No. 3330) has also been transmitted by AbuSafwan ibn Umayrah through a different chain of narrators.) </p>
        <p> This version has: AbuSafwan said: I came to the Apostle of Allah (peace_be_upon_him) at Mecca before his immigration. He then narrated the rest of the tradition, but he did not mention the words "who was weighing for payment". </p>
      </blockquote>
      <hr>
      <em> <a name="022.3334"> Book 22, Number 3334: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: (The standard) weight is the weight of the people of Mecca, and the (standard) measure is the measure of the people of Medina. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3335"> Book 22, Number 3335: </a> </em>
      <blockquote>
        <p> Narrated Samurah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) addressed us and said: Is here any one of such and such tribe present? But no one replied. </p>
        <p> He again asked: Is here any one of such and such tribe present? But no one replied. </p>
        <p> He again asked: Is here any one of such and such tribe? </p>
        <p> Then a man stood and said: I am (here), Apostle of Allah. </p>
        <p> He said: What prevented you from replying the first two times? I wish to tell you something good. </p>
        <p> Your companion has been detained (from entering Paradise) on account of his debt. Then I saw him that he paid off all his debt on his behalf and there remained no one to demand from him anything. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3336"> Book 22, Number 3336: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa al-Ash'ari: </p>
        <p> The Prophet (peace_be_upon_him) said: After the grave sins which Allah has prohibited the greatest sin is that a man dies while he has debt due from him and does not leave anything to pay it off, and meets Him with it. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3337"> Book 22, Number 3337: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) would not say funeral prayer over a person who died while the debt was due from him. A dead Muslim was brought to him and he asked: Is there any debt due from him? They (the people)  said: Yes, two dirhams. He said: Pray yourselves over your companion. </p>
        <p> Then AbuQatadah al-Ansari said: I shall pay them, Apostle of Allah. The Apostle of Allah (peace_be_upon_him) then prayed over him. </p>
        <p> When Allah granted conquests to the Apostle of Allah (peace_be_upon_him), he said: I am nearer to every believer than himself, so if anyone (dies and) leaves a debt, I shall be responsible for paying it; and if anyone leaves property, it goes to his heirs. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3341"> Book 22, Number 3341: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) owed me a debt and gave me something extra when he paid it. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3343"> Book 22, Number 3343: </a> </em>
      <blockquote>
        <p> Narrated Ubadah ibn as-Samit: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Gold is to be paid for with gold, raw and coined, silver with silver, raw and coined (in equal weight), wheat with wheat in equal measure, barley with barley in equal measure, dates with dates in equal measure, salt by salt with equal measure; if anyone gives more or asks more, he has dealt in usury. But there is no harm in selling gold for silver and silver (for gold), in unequal weight, payment being made on the spot. Do not sell them if they are to be paid for later. There is no harm in selling wheat for barley and barley (for wheat) in unequal measure, payment being made on the spot. If the payment is to be made later, then do not sell them. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3345"> Book 22, Number 3345: </a> </em>
      <blockquote>
        <p> Narrated Fudalah ibn Ubayd: </p>
        <p> The Prophet (peace_be_upon_him) was brought a necklace in which there were gold and pearls. </p>
        <p> (The narrators AbuBakr and (Ahmad) Ibn Mani' said: The pearls were set with gold in it, and a man bought it for nine or seven dinars.) </p>
        <p> The Prophet (peace_be_upon_him) said: (It must not be sold) till the contents are considered separately. The narrator said: He returned it till the contents were considered separately. The narrator Ibn Asa said: By this I intended trade. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3348"> Book 22, Number 3348: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> I used to sell camels at al-Baqi for dinars and take dirhams for them, and sell for dirhams and take dinars for them. I would take these for these and give these for these. I went to the Apostle of Allah (peace_be_upon_him) who was in the house of Hafsah. I said: Apostle of Allah , take it easy, I shall ask you (a question): I sell camels at al-Baqi'. I sell (them) for dinars and take dirhams and I sell for dirhams and take dinars. I take these for these, and give these for these. The Apostle of Allah (peace_be_upon_him) then said: There is no harm in taking them at the current rate so long as you do not separate leaving something to be settled. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3350"> Book 22, Number 3350: </a> </em>
      <blockquote>
        <p> Narrated Samurah (ibn Jundub): </p>
        <p> The Prophet (peace_be_upon_him) forbade selling animals for animals when payment was to be made at a later date. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3351"> Book 22, Number 3351: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) commanded him to equip an army, but the camels were insufficient. So he commanded him to keep back the young camels of sadaqah, and he was taking a camel to be replaced by two when the camels of sadaqah came. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3353"> Book 22, Number 3353: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> Zayd AbuAyyash asked Sa'd ibn AbuWaqqas about the sale of the soft and white kind of wheat for barley. Sa'd said: Which of them is better? He replied: Soft and white kind of wheat. So he forbade him from it and said: I heard the Apostle of Allah (peace_be_upon_him) say, when he was asked about buying dry dates for fresh. The Apostle of Allah (peace_be_upon_him) said: Are fresh dates diminished when they become dry? The (the people) replied: Yes. So the Apostle of Allah (peace_be_upon_him) forbade that. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3354"> Book 22, Number 3354: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to sell fresh dates for dry dates when payment is made at a later date. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3363"> Book 22, Number 3363: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to sell spoils of war till they are appointed, and to sell palm trees till they are safe from every blight, and a man praying without tying belt. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3365"> Book 22, Number 3365: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) forbade the sale of grapes till they became black and the sale of grain till it had become hard. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3366"> Book 22, Number 3366: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> Yunus said: I asked AbuzZinad about the sale of fruits before they were clearly in good condition, and what was said about it. </p>
        <p> He replied: Urwah ibn az-Zubayr reports a tradition from Sahl ibn AbuHathmah on the authority of Zayd ibn Thabit who said: The people used to sell fruits before they were clearly in good condition. When the people cut off the fruits, and were demanded to pay the price, the buyer said: The fruits have been smitten by duman, qusham and murad fruit diseases on which they used to dispute. When their disputes which were brought to the Prophet (peace_be_upon_him) increased, the Apostle of Allah (peace_be_upon_him) said to them as an advice: No, do not sell fruits till they are in good condition, due to a large number of their disputes and differences. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3367"> Book 22, Number 3367: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) forbade the sale of fruits till they were clearly in good condition , and (ordered that) they should not be sold but for dinar or dirham except Araya. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3368"> Book 22, Number 3368: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) forbade selling fruits years ahead, and commanded that unforeseen loss be remitted in respect of what is affected by blight. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3376"> Book 22, Number 3376: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> A time is certainly coming to mankind when people will bite each other and a rich man will hold fast, what he has in his possession (i.e. his property), though he was not commanded for that. Allah, Most High, said: "And do not forget liberality between yourselves." The men who are forced will contract sale while the Prophet (peace_be_upon_him) forbade forced contract, one which involves  some uncertainty, and the sale of fruit before it is ripe. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3377"> Book 22, Number 3377: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) having said: Allah, Most High, says: "I make a third with two partners as long as one of them does not cheat the other, but when he cheats him, I depart from them." </p>
      </blockquote>
      <hr>
      <em> <a name="022.3378"> Book 22, Number 3378: </a> </em>
      <blockquote>
        <p> Narrated Urwah ibn AbulJa'd al-Bariqi: </p>
        <p> The Prophet (peace_be_upon_him) gave him a dinar to buy a sacrificial animal or a sheep. He bought two sheep, sold one of them for a dinar, and brought him a sheep and dinar. So he invoked a blessing on him in his business dealing, and he was such that if had he bought dust he would have made a profit from it. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3380"> Book 22, Number 3380: </a> </em>
      <blockquote>
        <p> Narrated Hakim ibn Hizam: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sent with him a dinar to buy a sacrificial animal for him. He bought a sheep for a dinar, sold it for two and then returned and bought a sacrificial animal for a dinar for him and brought the (extra) dinar to the Prophet (peace_be_upon_him). The Prophet (peace_be_upon_him) gave it as alms (sadaqah) and invoked blessing on him in his trading. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3382"> Book 22, Number 3382: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> I Ammar, and Sa'd became partners in what we would receive on the day of Badr. Sa'd then brought two prisoners, but I and Ammar did not bring anything. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3383"> Book 22, Number 3383: </a> </em>
      <blockquote>
        <p> Narrated Ibn Abbad: </p>
        <p> Amr ibn Dinar said: I heard Ibn Umar say: We did not see any harm in sharecropping till I heard Rafi' ibn Khadij say: The Apostle of Allah (peace_be_upon_him) has forbidden it. So I mentioned it to Tawus. He said: Ibn Abbas told me that the Apostle of Allah (peace_be_upon_him) had not forbidden it, but said: It is better for one of you to lend to his brother than to take a prescribed sum from him. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3384"> Book 22, Number 3384: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> Zayd ibn Thabit said: May Allah forgive Rafi' ibn Khadij. I swear by Allah, I have more knowledge of Hadith than him. Two persons of the Ansar (according to the version of Musaddad) came to him who were disputing with each other. The Apostle of Allah (peace_be_upon_him) said: If this is your position, then do not lease the agricultural land. The version of Musaddad has: So he (Rafi' ibn Khadij) heard his statement: Do not lease agricultural lands. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3385"> Book 22, Number 3385: </a> </em>
      <blockquote>
        <p> Narrated Sa'd: </p>
        <p> We used to lease land for what grew by the streamlets and for what was watered from them.  The Apostle of Allah (peace_be_upon_him) forbade us to do that, and commanded us to lease if for gold or silver. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3387"> Book 22, Number 3387: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> Hanzalah ibn Qays said that he asked Rafi' ibn Khadij about the lease of land. He replied: The Apostle of Allah (peace_be_upon_him) forbade the leasing of land. I asked: (Did he forbid) for gold and silver (i.e. dinars and dirhams)? He replied: If it is against  gold and silver, then there is no harm in it. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3391"> Book 22, Number 3391: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> AbuRafi' came to us from the Apostle of Allah (peace_be_upon_him) said: The Apostle of Allah (peace_be_upon_him) forbade us from a work which benefited us; but obedience to Allah and His Apostle (peace_be_upon_him) is more beneficial to us. He forbade that one of us cultivates land except the one which he owns or the land which a man lends him (to cultivate). </p>
      </blockquote>
      <hr>
      <em> <a name="022.3391a"> Book 22, Number 3391a: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> Usayd ibn Zubayr said: Rafi' ibn Khadij came to us and said: The Apostle of Allah (peace_be_upon_him) forbids you from a work which is beneficial to you; and obedience to Allah and His Prophet (peace_be_upon_him) is more beneficial to you. The Apostle of Allah (peace_be_upon_him) forbids you from renting land for a share of its produce and he said: If anyone is not in need of his land he should lend it to his brother or leave it. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3393"> Book 22, Number 3393: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> AbuJa'far al-Khatmi said: My uncle sent me and his slave to Sa'id ibn al-Musayyab. We said to him, there is something which has reached us about sharecropping. He replied: Ibn Umar did not see any harm in it until a tradition reached him from Rafi' ibn Khadij. He then came to him and Rafi'  told him that the Apostle of Allah (peace_be_upon_him) came to Banu Harithah and saw crop in the land of Zuhayr. He said: What an excellent crop of Zuhayr is! They said: It does not belong to Zuhayr. He asked: Is this not the land of Zuhayr? They said: Yes, but the crop belongs to so-and-so. He said: Take your crop and give him the wages. Rafi' said: We took our crop and gave him the wages. Sa'id (ibn al-Musayyab) said: Lend your brother or employ him for dirhams. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3394"> Book 22, Number 3394: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade muhaqalah and muzabanah. Those who cultivate land are three: a man who has (his own) land and he tills it: a man who has been lent land and he tills the one lent to him; a man who employs another man to till land against gold (dinars) or silver (dirhams). </p>
      </blockquote>
      <hr>
      <em> <a name="022.3395"> Book 22, Number 3395: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> Rafi' had cultivated a land. The Prophet (peace_be_upon_him) passed him when he was watering it. So he asked him: To whom does the crop belong, and to whom does the land belong? He replied: The crop is mine for my seed and labour. The half (of the crop) is mine and the half for so-and-so. He said: You conducted usurious transaction. Return the land to its owner and take your wages and cost. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3396"> Book 22, Number 3396: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone sows in other people's land without their permission, he has no right to any of the crop, but he may have what it cost him. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3406"> Book 22, Number 3406: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) used to send Abdullah ibn Rawahah (to Khaybar), and he would assess the amount of dates when they began to ripen before they were eaten (by the Jews). He would then give choice to the Jews that they have them (on their possession) by that assessment or could assign to them (Muslims) by that assignment, so that the (amount of) zakat could be calculated before the fruit became eatable and distributed (among the people). </p>
      </blockquote>
      <hr>
      <em> <a name="022.3407"> Book 22, Number 3407: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> When Allah bestowed Khaybar on His Prophet (peace_be_upon_him) as fay' (as a result of conquest without fighting), the Apostle of Allah (peace_be_upon_him) allowed (them) to remain there as they were before, and apportioned it between him and them. He then sent Abdullah ibn Rawahah who assessed (the amount of dates) upon them. </p>
      </blockquote>
      <hr>
      <em> <a name="022.3408"> Book 22, Number 3408: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> Ibn Rawahah assessed them (the amount of dates) at forty thousand wasqs, and when Ibn Rawahah gave them option, the Jews took the fruits in their possession and twenty thousand wasqs of dates were due from them. </p>
      </blockquote>
