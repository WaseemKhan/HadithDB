      <div class="content"><em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 28: </a> </em>
        <h1> <em> Medicine (Kitab Al-Tibb) </em> </h1>
        <hr>
        <em> <a name="028.3846"> Book 28, Number 3846: </a> </em>
        <blockquote>
          <p> Narrated Usamah ibn Sharik: </p>
          <p> I came to the Prophet (peace_be_upon_him) and his Companions were sitting as if they had birds on their heads. I saluted and sat down. The desert Arabs then came from here and there. They asked: Apostle of Allah, should we make use of medical treatment? He replied: Make use of medical treatment, for Allah has not made a disease without appointing a remedy for it, with the exception of one disease, namely old age. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3847"> Book 28, Number 3847: </a> </em>
        <blockquote>
          <p> Narrated Umm al-Mundhar bint Qays al-Ansariyyah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) came to visit me, accompanied by Ali who was convalescing. We had some ripe dates hung up. The Apostle of Allah (peace_be_upon_him) got up and began to eat from them. </p>
          <p> Ali also got up to eat, but the Apostle of Allah (peace_be_upon_him) said repeatedly to Ali: Stop, Ali, for you are convalescing, and Ali stopped. </p>
          <p> She said: I then prepared some barley and beer-root and brought it.  The Apostle of Allah (peace_be_upon_him) then said: Take some of this, Ali, for it will be more beneficial for you.  AbuDawud said: The narrator Harun said: al-Adawiyyah (i.e. Umm al-Mundhar). </p>
        </blockquote>
        <hr>
        <em> <a name="028.3848"> Book 28, Number 3848: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: The best medical treatment you apply is cupping. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3849"> Book 28, Number 3849: </a> </em>
        <blockquote>
          <p> Narrated Salmah: </p>
          <p> the maid-servant of the Apostle of Allah (peace_be_upon_him), said: No one complained to the Apostle of Allah (peace_be_upon_him) of a headache but he told him to get himself cupped, or of a pain in his legs but he told him to dye them with henna. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3850"> Book 28, Number 3850: </a> </em>
        <blockquote>
          <p> Narrated AbuKabshah al-Ansari: </p>
          <p> The Apostle of Allah (peace_be_upon_him) used to have himself cupped on the top of his head and between his shoulders, and that he used to say: If anyone pours out any of his blood, he will not suffer if he applies no medical treatment for anything. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3851"> Book 28, Number 3851: </a> </em>
        <blockquote>
          <p> Narrated Anas ibn Malik: </p>
          <p> The Prophet (peace_be_upon_him) had himself cupped three times in the veins at the sides of the neck and on the shoulder. Ma'mar said: I got myself cupped, and I lost my memory so much so that I was instructed Surat al-Fatihah by others in my prayer. He had himself cupped at the top of his head. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3852"> Book 28, Number 3852: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: If anyone has himself cupped on the 17th, 19th and 21st it will be a remedy for every disease. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3853"> Book 28, Number 3853: </a> </em>
        <blockquote>
          <p> Narrated Kabshah daughter of AbuBakrah: </p>
          <p> (the narrator other than Musa said that Kayyisah daughter of AbuBakrah) She  said that her father used to forbid his family to have themselves cupped on a Tuesday, and used to assert on the authority of the Apostle of Allah (peace_be_upon_him) that Tuesday is the day of blood in which there is an hour when it does not stop. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3854"> Book 28, Number 3854: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) had himself cupped above the thigh for a contusion from which he suffered. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3855"> Book 28, Number 3855: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Prophet (peace_be_upon_him) sent a physician to Ubayy (ibn Ka'b), and he cut his vein. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3856"> Book 28, Number 3856: </a> </em>
        <blockquote>
          <p> Narrated Imran ibn Husayn: </p>
          <p> The Prophet (peace_be_upon_him) forbade to cauterise; we cauterised but they (cauterisation) did not benefit us, nor proved useful for us. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3859"> Book 28, Number 3859: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) was asked about a charm for one who is possessed (nashrah). He replied: It pertains to the work of the devil. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3860"> Book 28, Number 3860: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) say: If I drink an antidote, or tie an amulet, or compose poetry, I am the type who does not care what he does. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3861"> Book 28, Number 3861: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) prohibited unclean medicine. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3862"> Book 28, Number 3862: </a> </em>
        <blockquote>
          <p> Narrated AbdurRahman ibn Uthman: </p>
          <p> When a physician consulted the Prophet (peace_be_upon_him) about putting frogs in medicine, he forbade him to kill them. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3864"> Book 28, Number 3864: </a> </em>
        <blockquote>
          <p> Narrated Tariq ibn Suwayd or Suwayd ibn Tariq: </p>
          <p> Wa'il said: Tariq ibn Suwayd or Suwayd ibn Tariq asked the Prophet (peace_be_upon_him) about wine, but he forbade it. He again asked him, but he forbade him. He said to him: Prophet of Allah, it is a medicine. The Prophet (peace_be_upon_him) said: No it is a disease. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3865"> Book 28, Number 3865: </a> </em>
        <blockquote>
          <p> Narrated AbudDarda: </p>
          <p> The Prophet (peace_be_upon_him) said: Allah has sent down both the disease and the cure, and He has appointed a cure for every disease, so treat yourselves medically, but use nothing unlawful. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3866"> Book 28, Number 3866: </a> </em>
        <blockquote>
          <p> Narrated Sa'd: </p>
          <p> I suffered from an illness. The Apostle of Allah (peace_be_upon_him) came to pay a visit to me. He put his hands between my nipples and I felt its coolness at my heart. He said: You are a man suffering from heart sickness. Go to al-Harith ibn Kaladah, brother of Thaqif. He is a man who gives medical treatment. He should take seven ajwah dates of Medina and grind them with their kernels, and then put them into your mouth. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3869"> Book 28, Number 3869: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Prophet (peace_be_upon_him) said: Wear your white garments, for they are among your best garments, and shroud your dead in them. Among the best types of collyrium you use is antimony (ithmid): it clears the vision and makes the hair sprout. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3871"> Book 28, Number 3871: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The man casting evil would be commanded to perform ablution, and then the man affected was washed with it. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3872"> Book 28, Number 3872: </a> </em>
        <blockquote>
          <p> Narrated Asma', daughter of Yazid ibn as-Sakan,: </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) as saying: Do not kill your children secretly, for the milk, with which a child is suckled while his mother is pregnant, overtakes the horseman and throws him from his horse. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3874"> Book 28, Number 3874: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Mas'ud: </p>
          <p> Zaynab, the wife of Abdullah ibn Mas'ud, told that Abdullah said: I heard the Apostle of Allah (peace_be_upon_him) saying: spells, charms and love-potions are polytheism. </p>
          <p> I asked: Why do you say this? I swear by Allah, when my eye was discharging I used to go to so-and-so, the Jew, who applied a spell to me. When he applied the spell to me, it calmed down. Abdullah said: </p>
          <p> That was just the work of the Devil who was picking it with his hand, and when he uttered the spell on it, he desisted. All you need to do is to say as the Apostle of Allah (peace_be_upon_him) used to say: Remove the harm, O Lord of men, and heal. Thou art the Healer. There is no remedy but Thine which leaves no disease behind. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3875"> Book 28, Number 3875: </a> </em>
        <blockquote>
          <p> Narrated Imran ibn Husayn: </p>
          <p> The Prophet (peace_be_upon_him) said: No spell is to be used except for the evil eye or a scorpion sting. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3876"> Book 28, Number 3876: </a> </em>
        <blockquote>
          <p> Narrated Thabit ibn Qays ibn Shammas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) entered upon Thabit ibn Qays. The version of Ahmad (ibn Salih) has: When he was ill He (the Prophet) said: Remove the harm, O Lord of men, from Thabit ibn Qays ibn Shammas. He then took some dust of Bathan, and put it in a bowel, and then mixed it with water and blew in it, and poured it on him. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3878"> Book 28, Number 3878: </a> </em>
        <blockquote>
          <p> Narrated Ash-Shifa', daughter of Abdullah,: </p>
          <p> The Apostle of Allah (peace_be_upon_him) entered when I was with Hafsah, and he said to me: Why do you not teach this one the spell for skin eruptions as you taught her writing. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3879"> Book 28, Number 3879: </a> </em>
        <blockquote>
          <p> Narrated Sahl ibn Hunayf: </p>
          <p> I passed by a river. I entered it and took a bath in it. When I came out, I had fever. The Apostle of Allah (peace_be_upon_him) was informed about it. He said: Ask AbuThabit to seek refuge in Allah from that I asked: O my Lord, will the spell be useful? He replied: No, the spell is to be used except for the evil eye or a snake bite or a scorpion sting. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3883"> Book 28, Number 3883: </a> </em>
        <blockquote>
          <p> Narrated AbudDarda': </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) say: If any of you is suffering from anything or his brother is suffering, he should say: Our Lord is Allah Who is in the heaven, holy is Thy name, Thy command reigns supreme in the heaven and the earth, as Thy mercy in the heaven, make Thy mercy  in the earth; forgive us our sins, and our errors; Thou art the Lord of good men; send down mercy from Thy mercy, and remedy, and remedy from Thy remedy on this pain so that it is healed up. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3884"> Book 28, Number 3884: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
          <p> The Apostle of Allah (peace_be_upon_him) sued to teach them the following words in the case of alarm: I seek refuge in Allah's perfect words from His anger, the evil of His servants, the evil suggestions of the devils and their presence. Abdullah ibn Amr used to teach them to those of his children who had reached puberty, and he wrote them down (on some material) and hung on the child who had not reached puberty. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3887"> Book 28, Number 3887: </a> </em>
        <blockquote>
          <p> Narrated Alaqah ibn Sahar at-Tamimi: </p>
          <p> Alaqah came to the Apostle of Allah (may peace be upon him) and embraced Islam. He then came back from him and passed some people who had a lunatic fettered in chains. </p>
          <p> His people said: We are told that your companion has brought some good. Have you something with which you can cure him? I then recited Surat al-Fatihah and he was cured. They gave me one hundred sheep. I then came to the Apostle of Allah (peace_be_upon_him) and informed him of it. </p>
          <p> He asked: Is it only this? The narrator, Musaddad, said in his other version: Did you say anything other than this?  I said: No.  He said: Take it, for by my life, some accept if for a worthless chain, but you have done so for a genuine one. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3889"> Book 28, Number 3889: </a> </em>
        <blockquote>
          <p> Narrated AbuSalih Zakwan as-Samman: </p>
          <p> A man from Aslam tribe said: I was sitting with the Apostle of Allah (peace_be_upon_him). A man from among his Companions came and said: Apostle of Allah! I have been stung last night, and I could not sleep till morning. He asked: What was that? He replied: A scorpion. </p>
          <p> He said: Oh, had you said in the evening: "I take refuge in the perfect words of Allah from the evil of what He created," nothing would have harmed you, Allah willing. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3890"> Book 28, Number 3890: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> A man who was stung by a scorpion was brought to the Prophet (peace_be_upon_him). He said: Had he said the word: "I seek refuge in the perfect words of Allah from the evil of what He created, "he would not have been stung, or he said, "It would not have harmed him." </p>
        </blockquote>
        <hr>
        <em> <a name="028.3892"> Book 28, Number 3892: </a> </em>
        <blockquote>
          <p> Narrated Alaqah ibn Sahar at-Tamimi: </p>
          <p> We proceeded from the Apostle of Allah (peace_be_upon_him) and came to a clan of the Arabs. </p>
          <p> They said: We have been told that you have brought what is good from this man. Have you any medicine or a charm, for we have a lunatic in chains? </p>
          <p> We said: Yes. Then they brought a lunatic in chains.  He said: I recited Surat al-Fatihah over him for three days, morning and evening. Whenever I finished it, I would collect my saliva and spit it out, and he seemed as if he were set free from a bond. He said: They gave me some payment, but I said: No, not until I ask the Apostle of Allah (peace_be_upon_him). </p>
          <p> He (the Prophet) said: Accept it, for, by my life, some accept it for a worthless charm, but you have done so for a genuine one. </p>
        </blockquote>
        <hr>
        <em> <a name="028.3894"> Book 28, Number 3894: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> My mother intended to make me fat to send me to the (house of) the Apostle of Allah (peace_be_upon_him). But nothing which he desired benefited me till she gave me cucumber with fresh dates to eat. Then I became fat as good (as she desired). </p>
        </blockquote>
