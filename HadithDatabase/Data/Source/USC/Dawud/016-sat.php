      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 16: </a> </em>
      <h1> <em> Game (Kitab Al-Said) </em> </h1>
      <hr>
      <em> <a name="016.2839"> Book 16, Number 2839: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mughaffal: </p>
        <p> The Prophet (peace_be_upon_him) said: Were dogs not a species of creature I should command that they all be killed; but kill every pure black one. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2840"> Book 16, Number 2840: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet of Allah (peace_be_upon_him) ordered to kill dogs, and we were even killing a dog which a woman brought with her from the desert. Afterwards he forbade to kill them, saying: Confine yourselves to the type which is black. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2845"> Book 16, Number 2845: </a> </em>
      <blockquote>
        <p> Narrated Adi ibn Hatim: </p>
        <p> The Prophet (peace_be_upon_him) said: Eat what ever is caught for you by a dog or a hawk you have trained and set off when you have mentioned Allah's name. I said: (Does this apply) if it killed (the animal)? He said: When it kills it without eating any of it, for it caught it only for you. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2846"> Book 16, Number 2846: </a> </em>
      <blockquote>
        <p> Narrated AbuTha'labah al-Khushani: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said about the game hunted by a dog: If you set off your dog and have mentioned Allah's name, eat (it),  even if it eats any of it; and eat what your hands return you. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2847"> Book 16, Number 2847: </a> </em>
      <blockquote>
        <p> Narrated Adi ibn Hatim: </p>
        <p> Apostle of Allah, one of us shoots at the game, and follows its mark for two or three days, and then finds it dead, and there is his arrow (pierced) in it, may he eat it? He said: Yes, if he wishes, or he said: he may eat if he wishes. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2850"> Book 16, Number 2850: </a> </em>
      <blockquote>
        <p> Narrated AbuTha'labah al-Khushani: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to me: AbuTha'labah, eat what returns to you by your bow and your dog. </p>
        <p> Ibn Harb's version adds: "The trained (dog), and your hand, then eat, whether it has been slaughtered or not slaughtered". </p>
      </blockquote>
      <hr>
      <em> <a name="016.2851"> Book 16, Number 2851: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> There was a bedouin called AbuTha'labah. He said: Apostle of Allah, I have trained dogs, so tell me your opinion about (eating) the animal they hunt. The Prophet (peace_be_upon_him) said: If you have trained dogs, then eat what they catch for you. He asked: Whether it is slaughtered or not? He replied: Yes. He asked: Does it apply even if it eats any of it? He replied: Even if it eats any of it. He again asked: Apostle of Allah, tell me your opinion about my bow (i.e. the game hunted by arrow). He said: Eat what your bow returns to you, whether it is slaughtered or not. He asked: If it goes out of my sight? He replied: Even if it goes out of your sight, provided it has no stench, or you find a mark on it other than the mark of your arrow. </p>
        <p> He asked: Tell me about the use of the vessels of the Magians when we are forced to use them. He replied: Wash them and eat in them. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2852"> Book 16, Number 2852: </a> </em>
      <blockquote>
        <p> Narrated AbuWaqid: </p>
        <p> The Prophet (peace_be_upon_him) said: Whatever is cut off of an animal when it is alive is dead. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2853"> Book 16, Number 2853: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: (the narrator Sufyan said: I do not know but that it [the tradition] has been transmitted from the Prophet (peace_be_upon_him): He who lives in the desert will become rude; he who pursues the game will be negligent, and he who visits a king will be perverted. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2854"> Book 16, Number 2854: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He said: He who sticks to a king is perverted. This version adds: The nearer a servant (of Allah) goes to a king, the farther he keeps away from Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="016.2855"> Book 16, Number 2855: </a> </em>
      <blockquote>
        <p> Narrated AbuTha'labah al-Khushani: </p>
        <p> The Prophet (peace_be_upon_him) said: When you shoot your arrow (and the animal goes out of your sight) and you come three days later on it, and in it there is your arrow, then eat provided it has not stench. </p>
      </blockquote>
