      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 8: </a> </em>
      <h1> <em> Prayer (Kitab Al-Salat): Detailed Injunctions about Witr </em> </h1>
      <hr>
      <em> <a name="008.1411"> Book 8, Number 1411: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah is single (witr) and loves what is single, so observe the witr, you who follow the Qur'an. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1413"> Book 8, Number 1413: </a> </em>
      <blockquote>
        <p> Narrated Kharijah ibn Hudhafah al-Adawi: </p>
        <p> The Apostle of Allah (peace_be_upon_him) came out to us and said: Allah the Exalted has given you an extra prayer which is better for you then the red camels (i.e. high breed camels). This is the witr which Allah has appointed for you between the night prayer and the daybreak. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1414"> Book 8, Number 1414: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: The witr is a duty, so he who does not observe it does not belong to us; the witr is a duty, so he who does not observe it does not belong to us; the witr is a duty, so he who does not observe it does not belong to us. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1415"> Book 8, Number 1415: </a> </em>
      <blockquote>
        <p> Narrated Ubadah ibn as-Samit: </p>
        <p> Ibn Muhayriz said: A man from Banu Kinanah, named al-Makhdaji, heard a person called AbuMuhammad in Syria, saying: The witr is a duty (wajib). </p>
        <p> Al-Makhdaji said: So I went to Ubadah ibn as-Samit and informed him. </p>
        <p> Ubadah said: AbuMuhammad told a lie. I heard the Apostle of Allah  (peace_be_upon_him) say: There are five prayers which Allah has prescribed on His servants. If anyone offers them, not losing any of them, and not treating them lightly, Allah guarantees that He will admit him to Paradise. If anyone does not offer them, Allah does not take any responsibility for such a person. He may either punish him or admit him to Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1417"> Book 8, Number 1417: </a> </em>
      <blockquote>
        <p> Narrated AbuAyyub al-Ansari: </p>
        <p> The Prophet (peace_be_upon_him) said: The witr is a duty for every Muslim so if anyone wishes to observe it with five rak'ahs, he may do so; if anyone wishes to observe it with three, he may do so, and if anyone wishes to observe it with one, he may do so. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1418"> Book 8, Number 1418: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to observe witr with (reciting) "Glorify the name of thy Lord, the most High" (Surah 87), "Say O disbelievers" (Surah 109), and "Say, He is Allah, the One, Allah, the eternally besought of all" (112). </p>
      </blockquote>
      <hr>
      <em> <a name="008.1419"> Book 8, Number 1419: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> AbdulAziz ibn Jurayj said: I asked Aisha, mother of the believers: With which (surah) the Apostle of Allah (peace_be_upon_him) used to observe witr? (She reported same as in the Hadith of Ubayy ibn Ka'b, No. 1418) </p>
        <p> This version adds: In the third rak'ah he would recite: "Say, He is Allah, the One" (Surah 112), and  "Say, I seek refuge in the Lord of daybreak" (Surah 113), and "Say, I seek refuge in the Lord of mankind" (Surah 114). </p>
      </blockquote>
      <hr>
      <em> <a name="008.1420"> Book 8, Number 1420: </a> </em>
      <blockquote>
        <p> Narrated Al-Hasan ibn Ali: </p>
        <p> The Apostle of Allah (peace_be_upon_him) taught me some words that I say during the witr. (The version of Ibn Jawwas has: I say them in the supplication of the witr.) They were: "O Allah, guide me among those Thou hast guided, grant me security among those Thou hast granted security, take me into Thy charge among those Thou hast taken into Thy charge, bless me in what Thou hast given, guard me from the evil of what Thou hast decreed, for Thou dost decree, and nothing is decreed for Thee. He whom Thou befriendest is not humbled. Blessed and Exalted art Thou, our Lord." </p>
      </blockquote>
      <hr>
      <em> <a name="008.1422"> Book 8, Number 1422: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to say at the end of his witr: "O Allah, I seek refuge in Thy good pleasure from Thy anger, and in Thy forgiveness from Thy punishment, and I seek refuge in Thy mercy from Thy wrath. I cannot reckon the praise due to Thee. Thou art as Thou hast praised Thyself." </p>
      </blockquote>
      <hr>
      <em> <a name="008.1424"> Book 8, Number 1424: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> Al-Hasan reported: Umar ibn al-Khattab gathered the people (in tarawih prayer) behind Ubayy ibn Ka'b (who led them). He used to lead them for twenty days (during Ramadan, and would not recite the supplication except in the second half of it (i.e. Ramadan). When the last ten days remained, he kept away from them, and prayed in his house. They used to say: Ubayy ran away. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1425"> Book 8, Number 1425: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) offered salutation in the witr prayer, he said: Glorify be to the king most holy. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1426"> Book 8, Number 1426: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone oversleeps and misses the witr, or forgets it, he should pray when he remembers. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1429"> Book 8, Number 1429: </a> </em>
      <blockquote>
        <p> Narrated AbuQatadah: </p>
        <p> The Prophet (peace_be_upon_him) asked AbuBakr: When do you observe the witr? </p>
        <p> He replied: I observe the witr prayer in the early hours of the night. </p>
        <p> The Prophet (peace_be_upon_him) asked Umar: When do you observe the witr? </p>
        <p> He replied: At the end of the night. </p>
        <p> He then said to AbuBakr: This has followed it with care; and he said to Umar: He has followed it with strength. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1434"> Book 8, Number 1434: </a> </em>
      <blockquote>
        <p> Narrated Talq ibn Ali: </p>
        <p> Qays ibn Talq said: Talq ibn Ali visited us on a certain day during Ramadan. He remained with us till evening and broke fast with us. He then stood up and led us in the witr prayer. </p>
        <p> He then went to his mosque and led them in prayer. When the witr remained, he put forward another man and said: Lead your companions in the witr prayer, for I heard the Apostle of Allah (peace_be_upon_him) as saying: There are no two witrs during one night. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1438"> Book 8, Number 1438: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) recited the supplication (Qunut) daily for a month at the noon, afternoon, sunset, night and morning prayers. When he said: "Allah listens to him who praises Him" in the last rak'ah, invoking a curse on some clans of Banu Sulaym, Ri'l, Dhakwan and Usayyah, and those who were standing behind him said: Amen. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1441"> Book 8, Number 1441: </a> </em>
      <blockquote>
        <p> Narrated Someone who prayed with the Prophet: </p>
        <p> Muhammad ibn Sirin said: Someone who prayed the morning prayer along with the Prophet (peace_be_upon_him) narrated to me: When he raised his head after the second rak'ah, he remained standing for a short while. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1445"> Book 8, Number 1445: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: May Allah show mercy to a man who gets up during the night and prays, who wakens his wife and she prays; if she refuses, he sprinkles water on her face. May Allah show mercy to a woman who gets up during the night and prays, who wakens her husband and he prays; if he refuses she sprinkles water on his face. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1446"> Book 8, Number 1446: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id ; AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When a man himself wakes at night and wakens his wife and they pray two rak'ahs together, they are recorded among the men and women who make much mention of Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1448"> Book 8, Number 1448: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh al-Juhani: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone recites the Qur'an and acts according to its contents, on the Day of Judgment his parents will be given to wear a crown whose light is better than the light of the sun in the dwellings of this world if it were among you. So what do you think of him who acts according to his. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1454"> Book 8, Number 1454: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was given seven repeated long surahs, while Moses was given six, When he threw the tablets, two of them were withdrawn and four remained. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1457"> Book 8, Number 1457: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> I was driving the she-camel of the Apostle of Allah (peace_be_upon_him) during a journey. He said to me: Uqbah, should I not teach you two best surahs ever recited? He then taught me: "Say, I seek refuge in the Lord of the dawn," and "Say, I seek refuge in the Lord of men." He did not see me much pleased (by these two surahs). </p>
        <p> When he alighted for prayer, he led the people in the morning prayer and recited them in prayer. When the Apostle of Allah (peace_be_upon_him) finished his prayer, he turned to me and said: O Uqbah, how did you see. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1458"> Book 8, Number 1458: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> White I was travelling with the Apostle of Allah (peace_be_upon_him) between al-Juhfah and al-Abwa', a wind and intense darkness enveloped us, whereupon the Apostle of Allah (peace_be_upon_him) began to seek refuge in Allah, reciting: "I seek  refuge in the Lord of the dawn," and "I seek refuge in the Lord of men." </p>
        <p> He then said: Uqbah, use them when seeking refuge in Allah, for no one can use anything to compare with them for the purpose. </p>
        <p> Uqbah added: I heard him reciting them when he led the people in prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1459"> Book 8, Number 1459: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: One who was devoted to the Qur'an will be told to recite, ascend and recite carefully as he recited carefully when he was in the world, for he will reach his abode when he comes to the last verse he recites. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1461"> Book 8, Number 1461: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> Ya'la ibn Mumallak said that he asked Umm Salamah about the recitation and prayer of the Apostle of Allah (peace_be_upon_him). </p>
        <p> She said: What have you to do with his prayer? He would pray, then sleep as long as he had prayed, till morning. She then described his recitation and did so with an exposition word by word. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1463"> Book 8, Number 1463: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> The Prophet (peace_be_upon_him) said: Beautify the Qur'an with your voices. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1464"> Book 8, Number 1464: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: He who does not chant the Qur'an is not one of us. </p>
        <p> (Sub-narrator Qutaybah said: This Hadith according to my collection is on the authority of Sa'id ibn AbuSa'id.) </p>
      </blockquote>
      <hr>
      <em> <a name="008.1466"> Book 8, Number 1466: </a> </em>
      <blockquote>
        <p> Narrated AbuLubabah: </p>
        <p> Ubaydullah ibn Yazid said: AbuLubabah passed by us and we followed him till he entered his house, and we also entered it. </p>
        <p> There was a man in a rusty house and in shabby condition. I heard him say: I heard the Apostle of Allah (peace_be_upon_him) say: He is not one of us who does not chant the Qur'an. </p>
        <p> I (the narrator AbdulJabbar) said to Ibn AbuMulaykah: AbuMuhammad, what do you think if a person does not have pleasant voice? He said: He should recite with pleasant voice as much as possible. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1469"> Book 8, Number 1469: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn Ubadah: </p>
        <p> The Prophet (peace_be_upon_him) said: No man recites the Qur'an, then forgets it, but will meet Allah on the Day of Judgment in a maimed condition (or empty-handed, or with no excuse). </p>
      </blockquote>
      <hr>
      <em> <a name="008.1474"> Book 8, Number 1474: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> The Prophet (peace_be_upon_him) said: Supplication (du'a') is itself the worship. </p>
        <p> (He then recited:) "And your Lord said: Call on Me, I will answer you" (xI.60). </p>
      </blockquote>
      <hr>
      <em> <a name="008.1475"> Book 8, Number 1475: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> Ibn Sa'd said: My father (Sa'd ibn AbuWaqqas) heard me say: O Allah, I ask Thee for Paradise, its blessings, its pleasure and such-and-such, and such-and-such; I seek refuge in Thee from Hell, from its chains, from its collars, and from such-and-such, and from such-and-such. He said: I heard the Apostle of Allah (peace_be_upon_him) say: There will be people who will exaggerate in supplication. You should not be one of them. If you are granted Paradise, you will be granted all what is good therein; if you are protected from Hell, you will be protected from what is evil therein. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1476"> Book 8, Number 1476: </a> </em>
      <blockquote>
        <p> Narrated Fudalah ibn Ubayd,: </p>
        <p> The Apostle of Allah (peace_be_upon_him) heard a person supplicating during prayer. He did not mention the greatness of Allah, nor did he invoke blessings on the Prophet (peace_be_upon_him). </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: He made haste. </p>
        <p> He then called him and said either to him or to any other person: If any of you prays, he should mention the exaltation of his Lord in the beginning and praise Him; he should then invoke blessings on the Prophet (peace_be_upon_him); thereafter he should supplicate Allah for anything he wishes. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1480"> Book 8, Number 1480: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not cover the walls. He who sees the letter of his brother without his permission, sees Hell-fire. </p>
        <p> Supplicate Allah with the palms of your hands; do not supplicate Him with their backs upwards. When you finish supplication, wipe your faces with them. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1481"> Book 8, Number 1481: </a> </em>
      <blockquote>
        <p> Narrated Malik ibn Yasar as-Sakuni, al-Awfi: </p>
        <p> The Prophet (peace_be_upon_him) said: When you make requests to Allah, do so with the palms of your hands, and not backs, upwards. </p>
      </blockquote>
      <hr>
      <em> <a name="008.1482"> Book 8, Number 1482: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> I saw the Apostle of Allah (peace_be_upon_him) supplicating Allah in this manner with the palms of his hands and also with their backs upwards. </p>
