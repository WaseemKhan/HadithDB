      <em> <a name="002.0673"> Book 2, Number 0673: </a> </em>
      <blockquote>
        <p> Narrated AbdulHamid ibn Mahmud: </p>
        <p> I offered the Friday prayer along with Anas ibn Malik.  We were pushed to the pillars (due to the crowd of people). We, therefore, stopped forward and backward. Anas then said: We used to avoid it (setting a row between the pillars) during the time of the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0676"> Book 2, Number 0676: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Allah and His angels bless those who are on the right flanks of the rows. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0677"> Book 2, Number 0677: </a> </em>
      <blockquote>
        <p> Narrated AbuMalik al-Ash'ari: </p>
        <p> Should I not tell you how the Apostle of Allah (peace_be_upon_him) led the prayer? He said: He had the iqamah announced, drew the men up in line and drew up the youths behind them, then led them in prayer. He then mentioned how he conducted it. and said: Thus is the prayer of......AbdulA'la said: I think he must have said: My people. </p>
      </blockquote>
