      <em> <a name="002.0653"> Book 2, Number 0653: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> I saw the Apostle of Allah (peace_be_upon_him) praying both  barefooted and wearing sandals. </p>
      </blockquote>
