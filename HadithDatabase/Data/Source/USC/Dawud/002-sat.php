        <p> The Apostle of Allah (peace_be_upon_him) used to pass through the row from one side to the other; he used to set out chests and shoulders in order, and say: Do not be irregular. And he would say: Allah and His angels bless those who are near the first rows. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0665"> Book 2, Number 0665: </a> </em>
      <blockquote>
