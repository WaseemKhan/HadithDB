      <em> <a name="002.0672"> Book 2, Number 0672: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: The best of you are those whose shoulders are soft in prayer. </p>
      </blockquote>
