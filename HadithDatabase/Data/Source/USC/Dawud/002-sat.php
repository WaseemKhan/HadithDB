      <em> <a name="002.0671"> Book 2, Number 0671: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: Complete the front row, then the one that comes next, and if there is any incompleteness, let it be in the last row. </p>
      </blockquote>
