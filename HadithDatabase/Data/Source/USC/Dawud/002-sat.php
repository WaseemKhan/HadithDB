      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 2: </a> </em>
      <h1> <em> Prayer (Kitab Al-Salat) </em> </h1>
      <hr>
      <em> <a name="002.0393"> Book 2, Number 0393: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Gabriel (peace_be_upon_him) led me in prayer at the House (i.e. the Ka'bah). He prayed the noon prayer with me when the sun had passed the meridian to the extent of the thong of a sandal; he prayed the afternoon prayer with me when the shadow of everything was as long as itself; he prayed the sunset prayer with me when one who is fasting breaks the fast; he prayed the night prayer with me when the twilight had ended; and he prayed the dawn prayer with me when food and drink become forbidden to one who is keeping the fast. </p>
        <p> On the following day he prayed the noon prayer with me when his shadow was as long as himself; he prayed the afternoon prayer with me when his shadow was twice as long as himself; he prayed the sunset prayer at the time when one who is fasting breaks the fast; he prayed the night prayer with me when about the third of the night had passed; and he prayed the dawn prayer with me when there was a fair amount of light. </p>
        <p> Then turning to me he said: Muhammad, this is the time observed by the prophets before you, and the time is anywhere between two times. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0399"> Book 2, Number 0399: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> I would offer my noon prayer with the Apostle of Allah (peace_be_upon_him) and took a handful of gravels so that they might become cold in my hand and I placed them (before me) so that I may put my forehead on them at the time when I would prostrate. I did this due to the intensity of heat. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0400"> Book 2, Number 0400: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The extent of the shadow when the Apostle of Allah prayed (the noon prayer) was three to five feet in summer and five to seven feet in winter. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0408"> Book 2, Number 0408: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn Shayban: </p>
        <p> We came upon the Apostle of Allah (peace_be_upon_him) in Medina. He would postpone the afternoon prayer as long as the sun remained white and clear. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0418"> Book 2, Number 0418: </a> </em>
      <blockquote>
        <p> Narrated AbuAyyub: </p>
        <p> Marthad ibn Abdullah said: When AbuAyyub came upon us to fight the infidels and in those days Uqbah ibn Amir was the Governor of Egypt, he (Uqbah) delayed the sunset prayer. Hence AbuAyyub stood and said: What kind of prayer is this, Uqbah? He said: We were busy. He said: Did you not hear the Apostle of Allah (peace_be_upon_him) say: My community will remain well, or he said: will remain on its natural condition, so long as it would not delay the evening prayer until the stars shine brightly just like a network. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0419"> Book 2, Number 0419: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> I am the one who is best informed of the time of this prayer, i.e. the night prayer. The Apostle of Allah (peace_be_upon_him) used to offer it at the hour when the moon went down on its third night. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0421"> Book 2, Number 0421: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> We waited for the Prophet (peace_be_upon_him) to offer the night prayer. He delayed until people thought that he would not come out and some of us said that he had offered the prayer. At the moment when we were in this condition the Prophet (peace_be_upon_him) came out. People said to him as they were already saying. He said: Observe this prayer when it is dark, for by it you have been made superior to all the peoples, no people having observed it before you. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0422"> Book 2, Number 0422: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> We observed the prayer after nightfall with the Apostle of Allah (peace_be_upon_him), and he did not come out till about half the night had passed. He then said: Take your places. We then took our places. Then he said: The people have prayed and gone to bed, but you are still engaged in prayer as long as you wait for the prayer. Were it not for the weakness of the weak and for  the sickness of the sick. I would delay this prayer till half the night had gone. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0425"> Book 2, Number 0425: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Sunabihi: </p>
        <p> AbuMuhammad fancies that witr prayer is essential. (Hearing this) Ubadah ibn as-Samit said: AbuMuhammad was wrong. I bear witness that I heard the Apostle of Allah (peace_be_upon_him) say: Allah, the Exalted, has made five prayers obligatory. If anyone performs ablution for them well, offers them at their (right) time, and observes perfectly their bowing and submissiveness in them, it is the guarantee of Allah that He will pardon him; if anyone does not do so, there is no guarantee for him on the part of Allah; He may pardon him if He wills, and punish him if He wills. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0426"> Book 2, Number 0426: </a> </em>
      <blockquote>
        <p> Narrated Umm Farwah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was asked: Which of the actions is best? He replied: Observing prayer early in its period. </p>
        <p> Al-Khuza'i narrated in his version from his aunt named Umm Farwah who took the oath of allegiance to the Prophet (peace_be_upon_him): He was questioned. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0427"> Book 2, Number 0427: </a> </em>
      <blockquote>
        <p> Narrated Umarah ibn Ruwaybah: </p>
        <p> A man from Basrah said: Tell me what you heard from the Apostle of Allah (peace_be_upon_him). He said: I heard the Apostle of Allah (peace_be_upon_him) say: No one will enter Hell who has prayed before the rising of the sun and before its setting (meaning the dawn and the afternoon prayers). He said three times: Have you heard it from him? He replied: Yes, each time saying: My ears heard it and my heart memorised it. The man then said: And I heard him (the Prophet) say that. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0428"> Book 2, Number 0428: </a> </em>
      <blockquote>
        <p> Narrated Fudalah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) taught me and what he taught me is this: Observe the five prayers regularly. He said: I told (him): I have many works at these times; so give me a comprehensive advice which, if I follow, should be enough for me. He said: Observe the two afternoon prayers (al-asrayn). But the term al-asrayn (two afternoon prayers) was not used in our language. Hence I said: What is al-asrayn?  He said: A prayer before the sunrise and a prayer before the sunset (i.e. the dawn and the afternoon prayers). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0430"> Book 2, Number 0430: </a> </em>
      <blockquote>
        <p> Narrated AbuQatadah ibn Rab'i: </p>
        <p> Allah , the Exalted said: I made five times' prayers obligatory on your people, and I took a guarantee that if anyone observes them regularly at their times, I shall admit him to Paradise; if anyone does not offer them regularly, there is no such guarantee of Mine for him. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0432"> Book 2, Number 0432: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Amr ibn Maymun al-Awdi said: Mu'adh ibn Jabal, the Messenger of the Apostle of Allah (peace_be_upon_him) came to us in Yemen, I heard his takbir (utterance of AllahuAkbar) in the dawn prayer. He was a man with loud voice. I began to love him. I did depart from him until I buried him dead in Syria (i.e. until his death). </p>
        <p> Then I searched for a person who had deep understanding in religion amongst the people after him. So I came to Ibn Mas'ud and remained in his company until his death. He (Ibn Mas'ud) said: The Apostle of Allah (peace_be_upon_him) said to me: How will you act when you are ruled by rulers who say prayer beyond its proper time? I said: What do you command me, Apostle of Allah, if I witness such a time? He replied: Offer the prayer at its proper time and also say your prayer along with them as a supererogatory prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0433"> Book 2, Number 0433: </a> </em>
      <blockquote>
        <p> Narrated Ubadah ibn as-Samit: </p>
        <p> After me you will come under rulers who will be detained from saying prayer at its proper time by (their) works until its time has run out, so offer prayer at its proper time. A man asked him: Apostle of Allah, may I offer prayer with them? He replied: Yes, if you wish (to do so). </p>
        <p> Sufyan (another narrator through a different chain)said: May I offer prayer with them if I get it with them? He said: Yes, if you wish to do so. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0434"> Book 2, Number 0434: </a> </em>
      <blockquote>
        <p> Narrated Qabisah ibn Waqqas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: After me you will be ruled by rulers who will delay the prayer and it will be to your credit but to their discredit. So pray with them so long as they pray facing the qiblah. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0444"> Book 2, Number 0444: </a> </em>
      <blockquote>
        <p> Narrated Amr ibn Umayyah ad-Damri: </p>
        <p> We were in the company of the Apostle of Allah (peace_be_upon_him) during one of his journeys. He overslept abandoning the morning prayer until the sun had arisen. The Apostle of Allah (peace_be_upon_him) awoke and said: Go away from this place. </p>
        <p> He then commanded Bilal to call for prayer. He called for prayer. They (the people) performed ablution and offered two rak'ahs of the morning prayer (sunnah prayer). He then commanded Bilal (to utter the iqamah, i.e. to summon the people to attend the prayer). He announced the prayer (i.e. uttered the iqamah) and he led them in the morning prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0447"> Book 2, Number 0447: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> We proceeded with the Apostle of Allah (peace_be_upon_him) on the occasion of al-Hudaybiyyah. The Apostle of Allah (peace_be_upon_him) said: Who will keep watch for us? Bilal said: I (shall do). The overslept till the sun arose. The Prophet (peace_be_upon_him) awoke and said: Do as you used to do (i.e. offer prayer as usual). Then we did accordingly. He said: Anyone who oversleeps or forgets (prayer) should do similarly. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0448"> Book 2, Number 0448: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> I was not commanded to build high mosques. Ibn Abbas said: You will certainly adorn them as the Jews and Christians did. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0449"> Book 2, Number 0449: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: The Last Hour will not come until people vie with one another about mosques. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0450"> Book 2, Number 0450: </a> </em>
      <blockquote>
        <p> Narrated Uthman ibn Abul'As: </p>
        <p> The Prophet (nay peace be upon him) commanded him to build a mosque at Ta'if where the idols were placed. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0455"> Book 2, Number 0455: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) commanded us to build mosques in different localities (i.e. in the locality of each tribe separately)  and that they should be kept clean and be perfumed. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0457"> Book 2, Number 0457: </a> </em>
      <blockquote>
        <p> Narrated Maymunah ibn Sa'd: </p>
        <p> I said: Apostle of Allah, tell us the legal injunction about (visiting) Bayt al-Muqaddas (the dome of the Rock at Jerusalem). The Apostle of Allah (peace_be_upon_him) said: go and pray there. All the cities at that time were effected by war. If you cannot visit it and pray there, then send some oil to be used in the lamps. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0461"> Book 2, Number 0461: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: The rewards of my people were presented before me, so much so that even the reward for removing a mote by a person from the mosque was presented to me. The sins of my people were also presented before me. I did not find a sin greater than that of a person forgetting the Qur'anic chapter or verse memorised by him. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0478"> Book 2, Number 0478: </a> </em>
      <blockquote>
        <p> Narrated Abdullah al-Muharibi: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: When a man stands with the intention of saying prayer, or if any of you says prayer, he should not spit before him, nor at his right side; but he should do so at his left side, if there is a place for it; or he should spit under his left foot and then rub it off. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0481"> Book 2, Number 0481: </a> </em>
      <blockquote>
        <p> Narrated Ubadah ibn as-Samit: </p>
        <p> We came to Jabir ibn Abdullah  who was sitting in his mosque. He said: The Apostle of Allah (peace_be_upon_him) came to us in this mosque and he had a twig of date-palm of the kind of Ibn Tab. He looked and saw phlegm on the wall towards qiblah. He turned to it and scraped it with the twig. </p>
        <p> He then said: Who of you likes that Allah turns His face from him? He further said: When any of you stands for praying, Allah faces him. So he should not spit before him, nor on his right side. He should spit on his left side under his left foot. If he is in a hurry (i.e. forced to spit immediately), he should do with his cloth in this manner. He then placed the cloth on his mouth and rubbed it off. </p>
        <p> He then said: Bring perfume. A young man of the tribe stood and hurried to his house and returned with perfume in his palm. The Apostle of Allah (peace_be_upon_him) took it and put it at the end of the twig. He then stained the mark of phlegm with it. </p>
        <p> Jabir said: This is the reason you use perfume in your mosques. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0482"> Book 2, Number 0482: </a> </em>
      <blockquote>
        <p> Narrated AbuSahlah as-Sa'ib ibn Khallad: </p>
        <p> A man led the people in prayer. He spat towards qiblah while the Apostle of Allah (peace_be_upon_him) was looking at him. The Apostle of Allah said to the people when he finished his prayer: He should not lead you in prayer (henceforth). </p>
        <p> Thenceforth he intended to lead them in prayer, but they forbade him and informed him of the prohibition of the Apostle of Allah (peace_be_upon_him). He mentioned it to the Apostle of Allah (peace_be_upon_him) who said to him: Yes. </p>
        <p> The narrator said: I think he (the Prophet) said: You did harm to Allah and His Apostle. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0485"> Book 2, Number 0485: </a> </em>
      <blockquote>
        <p> Narrated Wathilah ibn al-Asqa': </p>
        <p> AbuSa'id said: I saw Wathilah ibn al-Asqa' in the mosque of Damascus. He spat at the mat and then rubbed it with his foot. He was asked: Why did you do so? He said: Because I saw the Apostle of Allah (peace_be_upon_him) doing so. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0489"> Book 2, Number 0489: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The earth has been made for me purifying and as a mosque (place for prayer). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0490"> Book 2, Number 0490: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> AbuSalih al-Ghifari reported: Ali (once) passed by Babylon during his travels. The mu'adhdhin (the person who calls for prayer) came to him to call for the afternoon prayer. When he passed by that place, he commanded to announce for the prayer. After finishing the prayer he said: My affectionate friend (i.e. the Prophet) prohibited me to say prayer in the graveyard. He also forbade me to offer prayer in Babylon because it is accursed. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0492"> Book 2, Number 0492: </a> </em>
      <blockquote>
        <p> Narrated Sa'id: </p>
        <p> and the narrator Musa said: As far as Amr thinks, the Prophet (peace_be_upon_him) said: The whole earth is a place of prayer except public baths and graveyards. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0494"> Book 2, Number 0494: </a> </em>
      <blockquote>
        <p> Narrated As-Saburah: </p>
        <p> The Prophet (peace_be_upon_him) said: Command a boy to pray when he reaches the age of seven years. When he becomes ten years old, then beat him for prayer. </p>
      </blockquote>
      <hr>
