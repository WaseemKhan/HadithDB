        <p> We again visited him to inquire about his illness. He offered the obligatory prayer sitting. We, therefore, stood (praying) behind him; he made a sign to us and we sat down. When he finished the prayer, he said: When the imam prays sitting, pray sitting; and when the imam prays standing, pray standing, and do not act as the people of Persia used to act with their chiefs (i.e. the people stood and they were sitting). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0603"> Book 2, Number 0603: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The imam is appointed only to be followed; when he says "Allah is most great," say "Allah is most great" and do not say "Allah is most great" until he says "Allah is most great." When he bows; bow; and do not bow until he bows. And when he says "Allah listens to him who praise Him," say "O Allah, our Lord, to Thee be the praise." </p>
        <p> The version recorded by Muslim goes: "And to Thee be the praise:  And when he prostrate; and do not prostrate until he prostrates. When he prays standing, pray standing, and when he prays sitting, all of you pray sitting. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0604"> Book 2, Number 0604: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The imam is appointed only to be followed. This version adds: When he recites (the Qur'an), keep silent." </p>
      </blockquote>
      <hr>
      <em> <a name="002.0613"> Book 2, Number 0613: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Alqamah and al-Aswad sought permission from Abdullah (ibn Mas'ud) for admission, and we remained sitting at his door for a long time. A slave-girl came out and gave them permission (to enter). He (Ibn Mas'ud) then got up and prayed (standing) between me (al-Aswad) and him (Alqamah). He then said: I witnessed the Apostle of Allah (peace_be_upon_him) doing similarly. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0614"> Book 2, Number 0614: </a> </em>
      <blockquote>
        <p> Narrated Yazid ibn al-Aswad: </p>
        <p> Jabir ibn Yazid ibn al-Aswad reported on the authority of his father: I prayed behind the Apostle of Allah (peace_be_upon_him). When he finished the prayer, he would turn (his face from the direction of the Ka'bah). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0615"> Book 2, Number 0615: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> When we would pray behind the Apostle of Allah (peace_be_upon_him), we liked to be on his right side. He (the Prophet) would sit turning his face towards us (and back towards the Ka'bah) (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0616"> Book 2, Number 0616: </a> </em>
      <blockquote>
        <p> Narrated Mughirah ibn Shu'bah: </p>
        <p> The Prophet (peace_be_upon_him) said: The Imam should not pray at the place where he led the prayer until he removes  (from there). </p>
      </blockquote>
      <hr>
      <em> <a name="002.0617"> Book 2, Number 0617: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: When the Imam completes the prayer and sits (for reciting tashahhud), and then becomes defiled (i.e. his ablution becomes void) before he speaks (to someone), his prayer becomes complete. And those who prayed behind him also complete the prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0618"> Book 2, Number 0618: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: The key of prayer is purification; takbir (saying "Allah is most great") makes (all acts which break prayer) unlawful and taslim (uttering the salutation) makes (all such acts) lawful. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0619"> Book 2, Number 0619: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn AbuSufyan: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not try to outstrip me in bowing and prostrating because however earlier I bow you will join me when I raise (my head from bowing); I have become bulky. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0629"> Book 2, Number 0629: </a> </em>
      <blockquote>
        <p> Narrated Talq ibn Ali al-Hanafi: </p>
        <p> We came to the Prophet (peace_be_upon_him), and a man came and said: Prophet of Allah, what do you say if one prays in a single garment? The Apostle of Allah (peace_be_upon_him) then took off his wrapper and combined it with his sheet, and put it on them. He got up and the Prophet of Allah (peace_be_upon_him) led us in prayer. When he finished the prayer, he said: Does every one of you have two garments? </p>
      </blockquote>
      <hr>
      <em> <a name="002.0632"> Book 2, Number 0632: </a> </em>
      <blockquote>
        <p> Narrated Salamah ibn al-Akwa': </p>
        <p> I said: Apostle of Allah, I am a man who goes out hunting; may I pray in a single shirt? He replied: Yes, but fasten it even if it should be with a thorn. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0636"> Book 2, Number 0636: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) prohibited us to pray in a sheet of cloth without crossing both its ends, and he also prohibited us to pray in a wrapper without putting on a sheet. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0637"> Book 2, Number 0637: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: He who lets his garment trail during prayer out of pride, Allah, the Almighty, has nothing to do with pardoning him and protecting him from Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0641"> Book 2, Number 0641: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah does not accept the prayer of a woman who has reached puberty unless she wears a veil. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0643"> Book 2, Number 0643: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade trailing garments during prayer and that a man should cover his mouth. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0646"> Book 2, Number 0646: </a> </em>
      <blockquote>
        <p> Narrated AbuRafi': </p>
        <p> Sa'id ibn AbuSa'id al-Maqburi reported on the authority of his father that he saw AbuRafi' the freed slave of the Prophet (peace_be_upon_him), passing by Hasan ibn Ali (Allah be pleased with them) when he was standing offering his prayer. He had tied the back knot of his hair. AbuRafi' untied it. Hasan turned to him with anger, AbuRafi' said to him: Concentrate on your prayer and do not be angry: I heard the Apostle of Allah (peace_be_upon_him)  say: This is the seat of the devil, referring to the back knot of the hair. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0647"> Book 2, Number 0647: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Kurayb the freed slave of Ibn Abbas reported: Abdullah ibn Abbas saw Abdullah ibn al-Harith praying having the back knot of the hair. He stood behind him and began to untie it. He remained standing unmoved (stationary). When he finished his prayer he came to Ibn Abbas and said to him: What were you doing with my head? He said: I heard the Apostle of Allah (peace_be_upon_him) say: A man who prays with the black knot of hair tied is the one praying pinioned. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0648"> Book 2, Number 0648: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn as-Sa'ib: </p>
        <p> I saw the Prophet (peace_be_upon_him) praying on the day of the conquest of Mecca and he had placed his shoe at his left side. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0650"> Book 2, Number 0650: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> While the Apostle of Allah (peace_be_upon_him) was leading his Companions in prayer, he took off his sandals and laid them on his left side; so when the people saw this, they removed their sandals. When the Apostle of Allah (peace_be_upon_him) finished his prayer, he asked: What made you remove your sandals? The replied: We saw you remove your sandals, so we removed our sandals. </p>
        <p> The Apostle of Allah (peace_be_upon_him) then said: Gabriel came to me and informed me that there was filth in them. When any of you comes to the mosque, he should see; if he finds filth on his sandals, he should wipe it off and pray in them. </p>
      </blockquote>
      <hr>
      <em> <a name="002.0652"> Book 2, Number 0652: </a> </em>
      <blockquote>
        <p> Narrated Aws ibn Thabit al-Ansari: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Act differently from the Jews, for they do not pray in their sandals or their shoes. </p>
      </blockquote>
