      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 33: </a> </em>
      <h1> <em> Combing the Hair (Kitab Al-Tarajjul) </em> </h1>
      <hr>
      <em> <a name="033.4147"> Book 33, Number 4147: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mughaffal: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade combing the hair except every second day. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4148"> Book 33, Number 4148: </a> </em>
      <blockquote>
        <p> Narrated Fudalah ibn Ubayd: </p>
        <p> Abdullah ibn Buraydah said: A man from the companions of the Prophet (peace_be_upon_him) travelled to Fudalah ibn Ubayd when he was in Egypt. </p>
        <p> He came to him and said: I have not come to you to visit you. But you and I heard a tradition from the Apostle of Allah (peace_be_upon_him). I hope you may have some knowledge of it. </p>
        <p> He asked: What is it?  He replied: So and so.  He said: Why do I see you dishevelled when you are the ruler of this land? </p>
        <p> He said: The Apostle of Allah (peace_be_upon_him) has forbidden us to indulge much in luxury. </p>
        <p> He said: Why do I see you unshod?  He replied: The Prophet (peace_be_upon_him) used to command us to go barefoot at times. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4149"> Book 33, Number 4149: </a> </em>
      <blockquote>
        <p> Narrated AbuUmamah Ilyas ibn Tha'labah: </p>
        <p> The Companions of the Apostle of Allah (peace_be_upon_him) mentioned this word before him. The Apostle of Allah (peace_be_upon_him) said: Listen, listen! Wearing old clothes is a part of faith, wearing old clothes is a part of faith. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4150"> Book 33, Number 4150: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him)  had sikkah with which he perfumed himself. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4151"> Book 33, Number 4151: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who has hair should honour it. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4152"> Book 33, Number 4152: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Karimah, daughter of Hammam, told that a woman came to Aisha (Allah be pleased with her) and asked her about dyeing with henna. She replied: There is no harm, but I do not like it. My beloved, the Apostle of Allah (peace_be_upon_him), disliked its odour. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4153"> Book 33, Number 4153: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> When Hind, daughter of Utbah, said: Prophet of Allah, accept my allegiance, he replied; I shall not accept your allegiance till you make a difference to the palms of your hands; for they look like the paws of a beast of prey. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4154"> Book 33, Number 4154: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> A woman made a sign from behind a curtain to indicate that she had a letter for the Apostle of Allah (peace_be_upon_him). The Prophet (peace_be_upon_him) closed his hand, saying: I do not know this is a man's or a woman's hand. She said: No, a woman. He said: If you were a woman, you would make a difference to your nails, meaning with henna. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4161"> Book 33, Number 4161: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa: </p>
        <p> The Prophet (peace_be_upon_him) said: If a woman uses perfume and passes the people so that they may get its odour, she is so-and-so, meaning severe remarks. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4162"> Book 33, Number 4162: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> A woman met him and he found the odour of perfume in her. Her clothes were fluttering in the air. He said: O maid-servant of the Almighty, are you coming from the mosque? She replied: Yes. He said: For it did you use perfume? She replied: Yes. He said: I heard my beloved AbulQasim (peace_be_upon_him) say: The prayer of a woman who uses perfume for this mosque is not accepted until she returns and takes a bath like that of sexual defilement (perfectly). </p>
      </blockquote>
      <hr>
      <em> <a name="033.4163"> Book 33, Number 4163: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If a woman fumigates herself with perfume, she must not attend the night prayer with us. Ibn Nufayl said: Isha' means night prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4164"> Book 33, Number 4164: </a> </em>
      <blockquote>
        <p> Narrated Ammar ibn Yasir: </p>
        <p> I came to my family at night (after a journey) with my hands chapped and they perfumed me with saffron. In the morning I went to the Prophet (peace_be_upon_him) and gave him a greeting, but he did not respond to me nor did he welcome me. </p>
        <p> He said: Go away and wash this off yourself. I then went away and washed it off me. I came to him but there remained a spot of it on me. I give him a greeting, but he did not respond to me nor did he welcome me. </p>
        <p> He said: Go away and wash it off yourself. I then went away and washed it off me. I then came and gave him a greeting. </p>
        <p> He responded to me and welcomed me, saying: The angels do not attend the funeral of an unbeliever bringing good to it, nor a man who smears himself with saffron, nor a man who is sexually defiled. He said: He permitted the man who was sexually defiled to perform ablution when he slept, ate or drank. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4165"> Book 33, Number 4165: </a> </em>
      <blockquote>
        <p> Narrated Ammar ibn Yasir: </p>
        <p> The tradition mentioned above (No. 4164) has also been transmitted by Ammar ibn Yasir through a different chain of narrators. </p>
        <p> This version has: Ammar said: I used khaluq. The first version is more perfect; it mentioned "taking a bath". Ibn Jurayj said: I said to Umar (a transmitter): They might be wearing ihram (robe of pilgrim)? He replied: No, they were residents. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4166"> Book 33, Number 4166: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Allah does not  accept the prayer of a man who has any khaluq (perfume composed of saffron) on his body. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4168"> Book 33, Number 4168: </a> </em>
      <blockquote>
        <p> Narrated Ammar ibn Yasir: </p>
        <p> The Prophet (peace_be_upon_him) said: The angels do not come near three: the dead body of the unbeliever, one who smears himself with khaluq, and the one who is sexually defiled except that he performs ablution. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4169"> Book 33, Number 4169: </a> </em>
      <blockquote>
        <p> Narrated Al-Walid ibn Uqbah: </p>
        <p> When the Prophet of Allah (peace_be_upon_him) conquered Makkah. The people of Makkah began to bring their boys and he would invoke a blessing on them and rub their heads. I was brought, but as I had been perfumed with khaluq, he did not touch me because of the khaluq. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4170"> Book 33, Number 4170: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> A man came to the Apostle of Allah (peace_be_upon_him) and he had the mark of yellowness (of saffron). The Prophet (peace be upon him rarely mentioned a thing which he disliked before a man. When he went away, he said: Would that you tell this man that he should wash this off him. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4173"> Book 33, Number 4173: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The hair of the Apostle of Allah (peace_be_upon_him) were up to the lobes of his ears. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4175"> Book 33, Number 4175: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The hair of the Apostle of Allah (peace_be_upon_him) were above wafrah and below jummah. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4177"> Book 33, Number 4177: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> When I parted the hair of the Apostle of Allah (peace_be_upon_him) I made a parting from the crow of his head and let his forelock hang between his eyes. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4178"> Book 33, Number 4178: </a> </em>
      <blockquote>
        <p> Narrated Wa'il  ibn Hujr: </p>
        <p> I came to the Prophet (peace_be_upon_him) and I had long hair. When the Apostle of Allah (peace_be_upon_him) saw me, he said: Evil, evil! He said: I then returned and cut them off. I then came to him in the morning. He said (to me): I did not intend to do evil to you. This is much better. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4179"> Book 33, Number 4179: </a> </em>
      <blockquote>
        <p> Narrated Umm Hani: </p>
        <p> The Prophet (peace_be_upon_him) came to Mecca and he had four plaits of hair. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4180"> Book 33, Number 4180: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Ja'far: </p>
        <p> The Prophet (peace_be_upon_him) gave the children of Ja'far three day' time to visit them. He then came to visit them, and said: Do not weep over my brother after this day. He said: Call to me the children of my brother. We were brought to him as if we were chicken. He said: Call a barber to me. He then ordered and our heads were shaved. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4182"> Book 33, Number 4182: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> the Prophet (peace_be_upon_him) forbade qaza' which means that the head of a boy is shaved and a lock is left. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4183"> Book 33, Number 4183: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) saw a boy with part of his head shaved and part left unshaven. He forbade them to do that, saying: Shave it all or leave it all. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4184"> Book 33, Number 4184: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> I had a hanging lock of hair. My mother said to me: I shall not cut it, for the Apostle of Allah (peace_be_upon_him) used to stretch it our and hold it. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4185"> Book 33, Number 4185: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> Al-Hajjaj ibn Hassan said: We entered upon Anas ibn Malik. My sister al-Mughirah said: You were a boy in those days and you had two locks of hair. He (Anas) rubbed your head and invoked blessing on you. He said: Shave them (i.e. the locks) or clip them, for this is the fashion of the Jews. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4190"> Book 33, Number 4190: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Do not pluck out grey hair. If any believer grows a grey hair in Islam, he will have light on the Day of Resurrection. (This is Sufyan's version). Yahya's version says: Allah will record on his behalf a good deed for it, and will blot out a sin for it. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4193"> Book 33, Number 4193: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The Prophet (peace_be_upon_him) said: The best things with which grey hair are changed are henna and katam. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4194"> Book 33, Number 4194: </a> </em>
      <blockquote>
        <p> Narrated AbuRimthah: </p>
        <p> I went with my father to the Prophet (peace_be_upon_him). He had locks hanging down as far as the lobes of the ears stained with henna, and he was wearing two green garments. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4195"> Book 33, Number 4195: </a> </em>
      <blockquote>
        <p> Narrated AbuRimthah: </p>
        <p> This version adds (to the previous hadith No 4194): My father said to him (the Prophet): Show me what is on your back, for I am a physician. He (the Prophet) said: You are only a soother. Its physician is He Who has credit it. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4196"> Book 33, Number 4196: </a> </em>
      <blockquote>
        <p> Narrated AbuRimthah: </p>
        <p> I and my father came to the Prophet (peace_be_upon_him). He said to a man or to my father: Who is this? He replied: He is my son. He said: Do not commit a crime on him. He had stained his beard with henna. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4198"> Book 33, Number 4198: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) used to wear tanned leather sandals and dye his beard yellow with wars and saffron. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4199"> Book 33, Number 4199: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> When a man who had dyed himself with henna passed by the Prophet (peace_be_upon_him), he said: How fine this is! When another man who had dyed himself with henna and katam passed by, he said: This is better than that. Then another man who had dyed himself with yellow dye, passed by, he said: This is better than all that. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4200"> Book 33, Number 4200: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: At the end of time there will be people who will use this black dye like the crops of doves who will not experience the fragrance of Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="033.4201"> Book 33, Number 4201: </a> </em>
      <blockquote>
        <p> Narrated Thawban: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) went on a journey, the last member of his family he saw was Fatimah, and the first he visited on his return was Fatimah. Once when he returned from an expedition she had hung up a hair-cloth, or a curtain, at her door, and adorned al-Hasan and al-Husayn with silver bracelets. So when he arrived, he did not enter. Thinking that he had been prevented from entering by what he had seen, she tore down the curtain, unfastened the bracelets from the boys and cut them off. </p>
        <p> They went weeping to the Apostle of Allah (peace_be_upon_him), and when he had taken them from them, he said: Take this to so and so's family. Thawban. In Medina, these are my family, and I did not like them to enjoy their good things in the present life. Buy Fatimah a necklace or asb, Thawban, and two ivory bracelets. </p>
      </blockquote>
