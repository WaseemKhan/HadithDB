        <p> The Prophet (peace_be_upon_him) said: There are forty characteristics; the highest of them is to give a goat on loan (for benefiting from its milk). If any man carries out any of those characteristics with the hope of getting a reward and testifying to the promise for it, Allah will admit him to Paradise for it. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1687"> Book 9, Number 1687: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) commanded to give sadaqah. A man said: Apostle of Allah, I have a dinar. He said: Spend it on yourself. He again said: I have another. He said: Spend it on your children. He again said: I have another. He said: Spend it on your wife. He again said: I have another. He said: Spend it on your servant. He finally said: I have another. He replied: You know best (what to do with it). </p>
      </blockquote>
      <hr>
      <em> <a name="009.1690"> Book 9, Number 1690: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Awf: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Allah the Exalted has said: I am Compassionate, and this has been derived from mercy. I have derived its name from My name. If anyone joins it, I shall join him, and if anyone cuts it off, I shall cut him off. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1694"> Book 9, Number 1694: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) preached and said: Abstain from avarice, for those who had been before you were annihilated due to avarice. It (avarice) commanded them to show niggardliness; it commanded them to cut off their relationship with their nearest relatives, so they cut off. It commanded them to show profligacy, so they showed it. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1696"> Book 9, Number 1696: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> AbuMulaykah reported: Aisha counted a number of indigents. AbuDawud said: The other version has: She counted a number of sadaqahs. The Apostle of Allah (peace_be_upon_him) said: Give and do not calculate, so calculation will be made against you. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1697"> Book 9, Number 1697: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> Suwayd ibn Ghaflah said: I fought along with Zayd ibn Suhan and Sulayman ibn Rabi'ah. I found a whip. They said to me: Throw it away. I said: No; if I find its owner (I shall give it to him); if not, I shall use it. Then I performed hajj; and when I reached Medina, I asked Ubayy ibn Ka'b. </p>
        <p> He said: I found a purse which contained one hundred dinars; so I came to the Prophet (peace_be_upon_him). He said to me: Make the matter known for a year. I made it known for a year and then came to him. He then said to me: Make the matter known for a year. So I made it known for a year. I then (again) came to him. He said to me: Make the matter known for a year. Then I came to him and said: I did not find anyone who realises it. He said: Remember, its number, its container and its tie. If its owner comes, (give it to him), otherwise use it yourself. </p>
        <p> He (the narrator Shu'bah) said: I do not know whether he said the word "make the matter known" three times or once. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1702"> Book 9, Number 1702: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Khalid al-Juhani: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was asked about a find. He said: Make it known for a year. If its seeker comes, deliver it to him, otherwise note its container and its string. Then use it; if its seeker comes, deliver it to him. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1705"> Book 9, Number 1705: </a> </em>
      <blockquote>
        <p> Narrated Iyad ibn Himar: </p>
        <p> The Prophet (peace_be_upon_him) said: He who finds something should call one or two trusty persons as witnesses and not conceal it or cover it up; then if he finds its owner he should return it to him, otherwise it is Allah's property which He gives to whom He will. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1706"> Book 9, Number 1706: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was asked about the hanging fruit. He replied: If a needy person takes some and does not take a supply away in his garment, he is not to be blamed, but he who carries any of it away is to be find twice the value and punished, and he who steals any of it after it has been put in the place where dates are dried is to have his hand cut off if its value reaches the price of a shield. Regarding stray camels and sheep he mentioned the same as others have done. He said: He was asked about finds and replied: If it is in a frequented road and a large town, make the matter known for a year, and if its owner comes, give it to him, but if he does not, it belongs to you. If it is in a place which has been a waste from ancient time, or if it is a hidden treasure (belonging to the Islamic period), it is subject to the payment of the fifth. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1710"> Book 9, Number 1710: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> Ali ibn AbuTalib found a dinar and he took it to Fatimah. She asked the Apostle of Allah (peace_be_upon_him) about it. He said: This is Allah's provision. Then the Apostle of Allah (peace_be_upon_him) ate out of the food (bought with it), and Ali and Fatimah also ate out of that food. But afterwards a woman came crying out about the dinar. The Prophet (peace_be_upon_him) said: Pay the dinar, Ali. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1711"> Book 9, Number 1711: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Bilal ibn Yahya al-Absi said: Ali found a dinar and purchased some flour with it. The seller of the flour recognised him and returned the dinar to him. Ali took it, deducted two qirat (carat) from it, and purchased meat with it. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1713"> Book 9, Number 1713: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) gave us licence to use (for our purpose) a stick, a rope, a whip and things of that type which a man picked up; he may benefit from them. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1714"> Book 9, Number 1714: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who hides a stray camel shall pay a fine, and a like compensation with it. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1716"> Book 9, Number 1716: </a> </em>
      <blockquote>
        <p> Narrated Al-Mundhir ibn Jarir: </p>
        <p> I accompanied Jarir at Bawazij. The shepherd brought the cows. Among them there was a cow that was not one of them. Jarir asked him: What is this? He replied: This was mixed with the cows and we do not know to whom it belongs. Jarir said: Take it out. I heard the Apostle of Allah (peace_be_upon_him) say: No one mixes a stray animal (with his animals) but a man who strays from right path. </p>
      </blockquote>
