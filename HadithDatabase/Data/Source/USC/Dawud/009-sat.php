      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 9: </a> </em>
      <h1> <em> Zakat (Kitab Al-Zakat) </em> </h1>
      <hr>
      <em> <a name="009.1554"> Book 9, Number 1554: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: There is no zakat payable (on grain or dates) on less than five camel-loads. The wasq (one camel-load) measures sixty sa' in weight. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1556"> Book 9, Number 1556: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> Habib al-Maliki said: A man said to Imran ibn Husayn: AbuNujayd, you narrate to us traditions whose basis we do not find in the Qur'an. </p>
        <p> Thereupon, Imran got angry and said to the man: Do you find in the Qur'an that one dirham is due on forty dirhams (as Zakat), and one goat is due on such-and-such number of goats, and one camel will be due on such-and-such number of camels? </p>
        <p> He replied: No. </p>
        <p> He said: From whom did you take it? You took it from us, from the Apostle of Allah (peace_be_upon_him). </p>
        <p> He mentioned many similar things. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1557"> Book 9, Number 1557: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to order us to pay the sadaqah (zakat) on what we prepared for trade. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1558"> Book 9, Number 1558: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> A woman came to the Apostle of Allah (peace_be_upon_him) and she was accompanied by her daughter who wore two heavy gold bangles in her hands. He said to her: Do you pay zakat on them? She said: No. He then said: Are you pleased that Allah may put two bangles of fire on your hands? </p>
        <p> Thereupon she took them off and placed them before the Prophet (peace_be_upon_him) saying: They are for Allah and His Apostle. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1559"> Book 9, Number 1559: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> I used to wear gold ornaments. I asked: Is that a treasure (kanz), Apostle of Allah? He replied: whatever reaches a quantity on which zakat is payable is not a treasure (kanz) when the zakat is paid. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1563"> Book 9, Number 1563: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) wrote a letter about sadaqah (zakat) but he died before he could send it to his governors. He had kept it  with his sword. So AbuBakr acted upon it till he died, and then Umar acted upon it till he died. </p>
        <p> It contained: "For five camels one goat is to be given; for ten camels two goats are to be given; for fifteen camels three goats are to be given; for twenty camels four goats are to be given; for twenty-five to thirty-five camels a she-camel in her second year is to be given. If the number exceeds by one up to seventy camels, a she-camel in her fourth year is to be given; if they exceed by one up to seventy-five camels, a she-camel in her fifth year is to be given; if they exceed by one up to ninety camels, two she-camels in their third year are to be given; if they exceed by one up to one hundred and twenty, two she-camels in their fourth year are to be given. If the camels are more than this, a she-camel in her fourth year is to be given for every fifty camels, and a she-camel in her third year is to be given for every forty camels. </p>
        <p> For forty to one hundred and twenty goats one goat is to be given; if they exceed by one up to two hundred, two goats are to be given. If they exceed by one up to three hundred, three goats are to be given; if the goats are more than this, one goat for every hundred goats is to be given. Nothing is payable until they reach one hundred. Those which are in one flock are not to be separated, and those which are in separate flocks are not be brought together from fear of sadaqah (zakat). Regarding that which belongs to two partners, they can make claims for restitution from each other with equity. An old goat and a defective one are not to be accepted as sadaqah (zakat)." </p>
        <p> Az-Zuhri said: When the collector comes, the goats will be apportioned into three flocks: one containing bad, the second good, and the third moderate. The collector will take zakat from the moderate. Az-Zuhri did not mention the cows (to be apportioned in three flocks). </p>
      </blockquote>
      <hr>
      <em> <a name="009.1567"> Book 9, Number 1567: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Al-Harith al-A'war reported from Ali. Zuhayr said: I think, the Prophet (peace_be_upon_him) said: "Pay a fortieth. A dirham is payable on every forty, but you are not liable for payment until you have accumulated two hundred dirhams. When you have two hundred dirhams, five dirhams are payable, and that proportion is applicable to larger amounts. </p>
        <p> "Regarding sheep, for every forty sheep up to one hundred and twenty, one sheep is due. But if you possess only thirty-nine, nothing is payable on them." He further narrated the tradition about the sadaqah (zakat) on sheep like that of az-Zuhri. </p>
        <p> "Regarding cattle, a yearling bull calf is payable for every thirty, and a cow in her third year for forty, and nothing is payable on working animals. </p>
        <p> Regarding (the zakat on) camels, he mentioned the rates that az-Zuhri mentioned in his tradition. He said: "For twenty-five camels, five sheep are to be paid. If they exceed by one, a she-camel in her second year is to be given. If there is no she-camel in her second year, a male camel in its third year is to be given, up to thirty-five. If they exceed by one a she-camel in her third year is to be given, up to forty-five. If they exceed by one, a she-camel in her fourth year which is ready to be covered by a bull-camel is to be given." He then transmitted the rest of the tradition like that of az-Zuhri. </p>
        <p> He continued: If they exceed by one, i.e. they are ninety-one to hundred and twenty, two she-camels in their fourth year, which are ready to be covered by a bull-camel, are to be given. If there are more camels than that, a she-camel in her fourth year is to be given for every fifty. Those which are in one flock are not to be separated, and those which are separate are not to be brought together. An old sheep, one with a defect in the eye, or a billy goat is not to be accepted as a sadaqah unless the collector is willing. </p>
        <p> As regards agricultural produce, a tenth is payable on that which is watered by rivers or rain, and a twentieth on that which is watered by draught camels." </p>
        <p> The version of Asim and al-Harith says: "Sadaqah (zakat) is payable every year." Zuhayr said: I think he said "Once a year". </p>
        <p> The version of Asim has the words: "If a she-camel in her second year is not available among the camels, nor is there a bull-camel in its third year, ten dirhams or two goats are to be given." </p>
      </blockquote>
      <hr>
      <em> <a name="009.1568"> Book 9, Number 1568: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: "When you possess two hundred dirhams and one year passes on them, five dirhams are payable. Nothing is incumbent on you, that is, on gold, till it reaches twenty dinars. When you possess twenty dinars and one year passes on them, half a dinar is payable. Whatever exceeds, that will be reckoned properly." </p>
        <p> (The narrator said: I do not remember whether the words "that will be reckoned properly" were uttered by All himself or he attributed them to the Prophet (peace_be_upon_him). </p>
        <p> No zakat is payable on property till a year passes on it. </p>
        <p> But Jarir said: Ibn Wahb (sub-narrator) added to this tradition from the Prophet (peace_be_upon_him): "No zakat is payable on property until a year passes away on it." </p>
      </blockquote>
      <hr>
      <em> <a name="009.1569"> Book 9, Number 1569: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: I have given exemption regarding horses and slaves; with regard to coins, however, you must pay a dirham for every forty (dirhams), but nothing is payable on one hundred and ninety. When the total reaches two hundred, five dirhams are payable. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1570"> Book 9, Number 1570: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn Haydah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: For forty pasturing camels, one she-camel in her third year is to be given. The camels are not to be separated from reckoning. He who pays zakat with the intention of getting reward will be rewarded. If anyone evades zakat, we shall take half the property from him as a due from the dues of our Lord, the Exalted. There is no share in it (zakat) of the descendants of Muhammad (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="009.1571"> Book 9, Number 1571: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> When the Prophet (peace_be_upon_him) sent him to the Yemen, he ordered him to take a male or a female calf a year old for every thirty cattle and a cow in its third year for every forty, and one dinar for every adult (unbeliever as a poll-tax) or cloths of equivalent value manufactured in the Yemen. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1574"> Book 9, Number 1574: </a> </em>
      <blockquote>
        <p> Narrated Someone who accompanied the collector of the Prophet: </p>
        <p> Suwayd ibn Ghaflah said: I went myself or someone who accompanied the collector of the Prophet (peace_be_upon_him) told me: It was recorded in the document written by the Apostle of Allah (peace_be_upon_him) not to accept a milking goat or she-camel or a (suckling) baby (as zakat on animals); and those which are in separate flocks are not to be brought together, and those which are in one flock are not to be separated. </p>
        <p> The collector used to visit the water-hole when the sheep went there and say: Pay the sadaqah (zakat) on your property.  The narrator said: A man wanted to give him his high-humped camel (kawma').  The narrator (Hilal) asked: What is kawma', AbuSalih?  He said: A camel a high hump. </p>
        <p> The narrator continued: He (the collector) refused to accept it. He said: I wish you could take the best of my camels. He refused to accept it. He then brought another camel lower in quality than the previous one. He refused to accept it too. He then brought another camel lower in quality than the previous one. He accepted it, saying: I shall take it, but I am afraid the Apostle of Allah (peace_be_upon_him) might be angry with me, saying to me: You have purposely taken from a man a camel of your choice. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1576"> Book 9, Number 1576: </a> </em>
      <blockquote>
        <p> Narrated Sa'r ibn Disam: </p>
        <p> Muslim ibn Shu'bah said: Nafi' ibn Alqamah appointed my father as charge d'affaires of his tribe, and commanded him to collect sadaqah (zakat) from them. My father sent me to a group of them; so I came to an aged man called Sa'r ibn Disam </p>
        <p> I said: My father has sent me to you to collect zakat from you.  He asked: What kind of animals will you take, my nephew?  I replied: We shall select the sheep and examine their udders.  He said: My nephew, I shall narrate a tradition to you. I lived on one of these steppes during the time of the Apostle of Allah (peace_be_upon_him) along with my sheep. Two people riding a camel came to me. </p>
        <p> They said to me: We are messengers of the Apostle of Allah (peace_be_upon_him), sent to you so that you may pay the sadaqah (zakat) on your sheep. </p>
        <p> I asked: What is due from me for them? </p>
        <p> They said: One goat. I went to a goat which I knew was full of milk and fat, and I brought it to them. </p>
        <p> They said: This is a pregnant goat. The Apostle of Allah (peace_be_upon_him) prohibited us to accept a pregnant goat. </p>
        <p> I asked: What will you take then?  They said: A goat in its second year or a goat in its third year. I then went to a goat which had not given birth to any kid, but it was going to do so. I brought it to them. </p>
        <p> They said: Give it to us. They took it on the camel and went away. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1577"> Book 9, Number 1577: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mu'awiyah al-Ghadiri: </p>
        <p> AbuDawud said: I read in a document possessed by Abdullah ibn Salim at Hims: Abdullah ibn Mu'awiyah al-Ghadiri reported the Prophet (peace_be_upon_him) as saying: He who performs three things will have the taste of the faith. (They are:) One who worships Allah alone and one believes that there is no god but Allah; and one who pays the zakat on his property agreeably every year. One should not give an aged animal, nor one suffering from itch or ailing, and one most condemned, but one should give animals of medium quality, for Allah did not demand from you the best of your animals, nor did He command you to give the animals of worst quality. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1578"> Book 9, Number 1578: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> The Apostle of Allah (peace_be_upon_him) commissioned me as a collector of zakat. I visited a man. When he had collected his property of camels, I found that a she-camel in her second year was due from him. </p>
        <p> I said to him: Pay a she-camel in her second year, for she is to be paid as sadaqah (zakat) by you. </p>
        <p> He said: That one is not worthy of milking and riding. Here is another she-camel which is young, grand and fat. So take it. </p>
        <p> I said to him: I shall not take an animal for which I have not been commanded. The Apostle of Allah (peace_be_upon_him) is here near to you. If you like, go to him, and present to him what you presented to me. Do that; if he accepts it from you, I shall accept it; if he rejects it, I shall reject it. </p>
        <p> He said: I shall do it. He accompanied me and took with him the she-camel which he had presented to me. We came to the Apostle of Allah (peace_be_upon_him). He said to him: Prophet of Allah, your messenger came to me to collect zakat on my property. By Allah, neither the Apostle of Allah nor his messenger has ever seen my property before. I gathered my property (camels), and he estimated that a she-camel in her second year would be payable by me. But that has neither milk nor is it worth riding. So I presented to him a grand young she-camel for acceptance as  zakat. But he has refused to take her. Look, she is here; I have brought her to you, Apostle of Allah. Take her. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: That is what is due from you. If you give voluntarily a better (animal) Allah will give a reward to you for it. We accept her from you. </p>
        <p> She is here, Apostle of Allah; I have brought her to you. So take her. The Apostle of Allah (peace_be_upon_him) then ordered me to take possession of it, and he prayed for a blessing on his property. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1580"> Book 9, Number 1580: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: He who collects more sadaqah than is due is like him who refuses to pay it. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1581"> Book 9, Number 1581: </a> </em>
      <blockquote>
        <p> Narrated Bashir ibn al-Khasasiyyah: </p>
        <p> (Ibn Ubayd said in the version of his tradition that his name was not Bashir, but (it was) the Apostle of Allah (peace_be_upon_him) (who had) named him Bashir) </p>
        <p> We said: (to the Apostle of Allah): The collectors of sadaqah collect more than is due; can we hide our property to that proportion? He replied: "No." </p>
      </blockquote>
      <hr>
      <em> <a name="009.1583"> Book 9, Number 1583: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Atik: </p>
        <p> The Prophet (peace_be_upon_him) said: Riders who are objects of dislike to you will come to you, but you must welcome them when they come to you, and give them a free hand regarding what they desire. If they are just, they will receive credit for it, but if they are unjust, they will be held responsible. Please them, for the perfection of your zakat consists in their good pleasure, and let them ask a blessing for you . </p>
      </blockquote>
      <hr>
      <em> <a name="009.1587"> Book 9, Number 1587: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: There is to be no collecting of sadaqah (zakat) from a distance, nor must people who own property remove it far away, and their sadaqahs are to be received in their dwelling. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1590"> Book 9, Number 1590: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: No sadaqah is due on a horse or a slave except that given at the breaking of the fast (at the end of Ramadan). </p>
      </blockquote>
      <hr>
      <em> <a name="009.1595"> Book 9, Number 1595: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) sent him to the Yemen, he said (to him): Collect corn from the corn, sheep from the sheep, camel from the camels, and cow from the cows. </p>
      </blockquote>
      <hr>
      <em> <a name="009.1596"> Book 9, Number 1596: </a> </em>
      <blockquote>
