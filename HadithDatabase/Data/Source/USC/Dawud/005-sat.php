      <div class="content"><em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 5: </a> </em>
        <h1> <em> Prayer (Kitab Al-Salat): Prayer at Night </em> </h1>
        <hr>
        <em> <a name="005.1299"> Book 5, Number 1299: </a> </em>
        <blockquote>
          <p> Narrated Abdullah Ibn Abbas: </p>
          <p> In Surat al-Muzzammil (73), the verse: "Keep vigil at night but a little, a half thereof" (2-3) has been abrogated by the following verse: "He knoweth that ye count it not, and turneth unto you in mercy. Recite then of the Qur'an that which is easy for you" (v.20). The phrase "the vigil of the night" (nashi'at al-layl) means the early hours of the night. They (the companions) would pray (the tahajjud prayer) in the early hours of the night. </p>
          <p> He (Ibn Abbas) says: It is advisable to offer the prayer at night (tahajjud), prescribed by Allah for you (in the early hours of the night). This is because when a person sleeps, he does not know when he will awake. The words "speech more certain" (aqwamu qilan) means that this time is more suitable for the understanding of the Qur'an. He says: The verse: "Lo, thou hast by day a chain of business" (v.7) means engagement for long periods (in the day's work). </p>
        </blockquote>
        <hr>
        <em> <a name="005.1302"> Book 5, Number 1302: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Do not give up prayer at night, for the Apostle of Allah (peace_be_upon_him) would not leave it. Whenever he fell ill or lethargic, he would offer it sitting. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1303"> Book 5, Number 1303: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: May Allah have mercy on a man who gets up at night and prays, and awakens his wife; if she refuses, he should sprinkle water on her face. May Allah have mercy on a woman who gets up at night and prays, and awakens her husband; if he refuses, she would sprinkle water on his face. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1304"> Book 5, Number 1304: </a> </em>
        <blockquote>
          <p> Narrated AbuSa'id ; AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: If a man awakens his wife at night, and then both pray or both offer two rak'ahs together, the (name of the )man will be recorded among those who mention the name of Allah, and the (name of the) woman will be recorded among those who mention the name of Allah. Ibn Kathir did not narrate this tradition as a statement of the Prophet (peace_be_upon_him), but he reported it as a statement of AbuSa'id. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1309"> Book 5, Number 1309: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) said: Any person who offers prayer at night regularly but (on a certain night) he is dominated by sleep will be given the reward of praying. His sleep will be almsgiving. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1320"> Book 5, Number 1320: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Habashi al-Khath'ami: </p>
          <p> The Prophet (peace_be_upon_him) was asked: which is the best action? He replied: To stand in prayer for a long time. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1322"> Book 5, Number 1322: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Prophet's (peace_be_upon_him) recitation was loud enough for one who was in the inner chamber to hear it when he was in the house. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1324"> Book 5, Number 1324: </a> </em>
        <blockquote>
          <p> Narrated AbuQatadah: </p>
          <p> The Prophet (peace_be_upon_him) went out at night and found AbuBakr praying in a low voice, and he passed Umar ibn al-Khattab who was raising his voice while praying. </p>
          <p> When they both met the Prophet (peace_be_upon_him) together, the Prophet (peace_be_upon_him) said: I passed by you, AbuBakr, when you were praying in a low voice.  He replied: I made Him hear with Whom I was holding intimate converse, Apostle of Allah.  He (the Prophet) said to Umar: I passed by you when you were praying in a loud voice.  He replied: Apostle of Allah, I was awakening the drowsy and driving away the Devil. </p>
          <p> Al-Hasan added in his version: The Prophet (peace_be_upon_him) said: Raise your voice a little, AbuBakr, and he said to Umar: Lower your voice a little. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1327"> Book 5, Number 1327: </a> </em>
        <blockquote>
          <p> Narrated AbuSa'id al-Khudri: </p>
          <p> The Apostle of Allah (peace_be_upon_him) retired to the mosque. He heard them (the people) reciting the Qur'an in a loud voice. He removed the curtain and said: Lo! every one of you is calling his Lord quietly. One should not trouble the other and one should not raise the voice in recitation or in prayer over the voice of the other. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1328"> Book 5, Number 1328: </a> </em>
        <blockquote>
          <p> Narrated Uqbah ibn Amir al-Juhani: </p>
          <p> The Prophet (peace_be_upon_him) said: One who recites the Qur'an in a loud voice is like one who gives alms openly; and one who recites the Qur'an quietly is one who gives alms secretly. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1341"> Book 5, Number 1341: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Zurarah ibn Awfa said that Aisha was asked about the midnight prayer of the Apostle of Allah (peace_be_upon_him). </p>
          <p> She said: He used to offer his night prayer in congregation and then return to his family (in his house) and pray four rak'ahs. Then he would go to his bed and sleep, but the water for his ablution was placed covered near his head and his tooth-stick was also kept there until Allah awakened him at night. </p>
          <p> He then used the tooth-stick, performed ablution perfectly then came to the place of prayer and would pray eight rak'ahs, in which he would recite Surah al-Fatihah, and a surah from the Qur'an as Allah willed. He would not sit during any of them but sit after the eighth rak'ah, and would not utter the salutation, but recite (the Qur'an) during the ninth rak'ah. Then he would sit and supplicate as long as Allah willed, and beg Him and devote his attention to Him; He would utter the salutation once in such a loud voice that the inmates of the house were almost awakened by his loud salutation. He would then recite Surah al-Fatihah while sitting, bow while sitting, and then recite the Qur'an during the second rak'ah, and would bow and prostrate while sitting. He would supplicate Allah as long as He willed, then utter the salutation and turn away. </p>
          <p> This amount of prayer of the Apostle of Allah (peace_be_upon_him) continued till he put a weight. During that period he retrenched two rak'ahs from nine and began to pray six and seven rak'ahs standing and two rak'ahs sitting. This continued till he died. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1347"> Book 5, Number 1347: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Sa'd ibn Hisham said: I came to Medina and called upon Aisha, and said to her: Tell me about the prayer of the Apostle of Allah (peace_be_upon_him). </p>
          <p> She said: The Apostle of Allah (peace_be_upon_him) used to lead the people in the night prayer, and then go to his bed and sleep. When midnight came he got up, went to answer the call of nature and to perform ablution with water. Having performed ablution, he entered the mosque and prayed eight rak'ahs. </p>
          <p> To my mind he performed the recitation of the Qur'an, bowing and prostrating equally. He then observed witr with one rak'ah and prayed two rak'ahs sitting. Then he lay down on the ground. Sometimes Bilal came to him and called him for prayer. He then dozed, and sometimes I doubted whether he dozed or not, till he (Bilal) called him for prayer. </p>
          <p> This is the prayer he offered till he grew old or put on weight. She then mentioned how he put on weight as Allah wished. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1358"> Book 5, Number 1358: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Al-Aswad ibn Yazid said that he entered upon Aisha and asked her about the prayer of the Apostle of Allah (peace_be_upon_him) during the night. She said: He used to pray thirteen rak'ahs during the night. Then he began to pray eleven rak'ahs and left two rak'ahs. When he died, he would pray nine rak'ahs during the night. His last prayer during the night was witr. </p>
        </blockquote>
        <hr>
        <em> <a name="005.1360"> Book 5, Number 1360: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> I spent a night with my maternal aunt Maymunah. The Prophet (peace_be_upon_him) got up to pray at night. He prayed thirteen rak'ahs including two rak'ahs of the dawn prayer. I guessed that he stood in every rak'ah as long as one could recite Surah al-Muzzammil (73). </p>
        </blockquote>
