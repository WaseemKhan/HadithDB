      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 31: </a> </em>
      <h1> <em> Hot Baths (Kitab Al-Hammam) </em> </h1>
      <hr>
      <em> <a name="031.3998"> Book 31, Number 3998: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to enter the hot baths. He then permitted men to enter them in lower garments. </p>
      </blockquote>
      <hr>
      <em> <a name="031.3999"> Book 31, Number 3999: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> AbulMalih said: Some women of Syria came to Aisha. She asked them: From whom are you? They replied: From the people of Syria. She said: Perhaps you belong to the place where women enter hot baths (for washing ). The said: Yes. She said: I heard the Apostle of Allah (peace_be_upon_him) say: If a woman puts off her clothes in a place other than her house, she tears the veil between her and Allah, the Exalted. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4000"> Book 31, Number 4000: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: After some time the lands of the non-Arabs will be conquered for you, and there you will find houses called hammamat (hot baths). so men should not enter them (to wash) except in lower garments, and forbid the women to enter them except a sick or one who is in a child-bed. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4001"> Book 31, Number 4001: </a> </em>
      <blockquote>
        <p> Narrated Ya'la: </p>
        <p> The Apostle of Allah (peace_be_upon_him) saw a man washing in a public place without a lower garment. So he mounted the pulpit, praised and extolled Allah and said: Allah is characterised by modesty and concealment. So when any of you washes, he should conceal himself. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4003"> Book 31, Number 4003: </a> </em>
      <blockquote>
        <p> Narrated Jarhad: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sat with us and my thigh was uncovered. He said: Do you not know that thigh is a private part. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4004"> Book 31, Number 4004: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not uncover you thigh, and do not look at the thigh of the living and the dead. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4006"> Book 31, Number 4006: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn Haydah: </p>
        <p> I said: Apostle of Allah, from whom should we conceal our private parts and to whom can we show? He replied: conceal your private parts except from your wife and from whom your right hands possess (slave-girls). </p>
        <p> I then asked: Apostle of Allah, (what should we do), if the people are assembled together? </p>
        <p> He replied: If it is within your power that no one looks at it, then no one should look at it. </p>
        <p> I then asked: Apostle of Allah if one of us is alone, (what should he do)? </p>
        <p> He replied: Allah is more entitled than people that bashfulness should be shown to him. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4007"> Book 31, Number 4007: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: A man should not look at the private parts of another man, and a woman should not look at the private parts of another woman. A man should not lie with another man without wearing lower garment under one cover; and a woman should not be lie with another woman without wearing lower garment under one cover. </p>
      </blockquote>
      <hr>
      <em> <a name="031.4008"> Book 31, Number 4008: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: A man should not lie with another man and a woman should not lie with another woman without covering their private parts except a child or a father. He also mentioned a third thing which I forgot. </p>
      </blockquote>
