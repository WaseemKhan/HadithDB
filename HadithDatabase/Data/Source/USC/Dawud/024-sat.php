      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 24: </a> </em>
      <h1> <em> The Office of the Judge (Kitab Al-Aqdiyah) </em> </h1>
      <hr>
      <em> <a name="024.3564"> Book 24, Number 3564: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who has been appointed a judge has been killed without a knife. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3565"> Book 24, Number 3565: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who has been appointed a judge among the people has been killed without a knife. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3566"> Book 24, Number 3566: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Prophet (peace_be_upon_him) said: Judges are of three types, one of whom will go to Paradise and two to Hell. The one who will go to Paradise is a man who knows what is right and gives judgment accordingly; but a man who knows what is right and acts tyrannically in his judgment will go to Hell; and a man who gives judgment for people when he is ignorant will go to Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3568"> Book 24, Number 3568: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone seeks the office of judge among Muslims till he gets it and his justice prevails over his tyranny, he will go to Paradise; but the man whose tyranny prevails over his justice will go to Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3570"> Book 24, Number 3570: </a> </em>
      <blockquote>
        <p> Narrated AbuMas'ud al-Ansari: </p>
        <p> AbdurRahman ibn Bishr al-Ansari al-Azraq said: Two men from the locality of Kindah came while AbuMas'ud al-Ansari was sitting n a circle. They said: Is there any man who decides between us. A man from the circle said: I, AbuMas'ud took a handful of pebbles and threw at him, saying: Hush! It is disapproved to make haste in decision. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3571"> Book 24, Number 3571: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone desires the office of Judge and seeks help for it, he will be left to his own devices; if anyone does not desire it, nor does he seek help for it, Allah will send down an angel who will direct him aright. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3573"> Book 24, Number 3573: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) cursed the one who bribes and the one who takes bribe. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3574"> Book 24, Number 3574: </a> </em>
      <blockquote>
        <p> Narrated Adi ibn Umayrah al-Kindi: </p>
        <p> The Prophet (peace_be_upon_him) said: O people, if any of you is put in an administrative post on our behalf and conceals from us a needle or more, he is acting unfaithfully, and will bring it on the Day of Resurrection. A black man from the Ansar, as if I am seeing him, stood and said: Apostle of Allah, take back from me my post. He asked: What is that? He replied: I heard you say such and such. He said: And I say that. If we appoint anyone to an office, he must bring what is connected with it, both little and much. What he is given, he may take, and he must refrain from what is kept away from him. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3575"> Book 24, Number 3575: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sent me to the Yemen as judge, and I asked: Apostle of Allah, are you sending me when I am young and have no knowledge of the duties of a judge? He replied: Allah will guide your heart and keep your tongue true. When two litigants sit in front of you, do not decide till you hear what the other has to say as you heard what the first had to say; for it is best that you should have a clear idea of the best decision. He said: I had been a judge (for long); or he said (the narrator is doubtful): I have no doubts about a decision afterwards. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3579"> Book 24, Number 3579: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Umar said while he was (sitting) on the pulpit: O people, the opinion from the Apostle of Allah (peace_be_upon_him) was right, because Allah showed (i.e. inspired) him; but from us it is sheer conjecture and artifice. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3581"> Book 24, Number 3581: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn az-Zubayr: </p>
        <p> The Apostle of Allah (peace_be_upon_him) gave the decision that the two adversaries should be made to sit in front of the judge. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3584"> Book 24, Number 3584: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> When this verse was revealed: "If they do come to thee, either judge between them, or decline to interfere....If thou judge, judge in equity between them." Banu an-Nadir used to pay half blood-money if they killed any-one from Banu Qurayzah. When Banu Qurayzah killed anyone from Banu an-Nadir, they would pay full blood-money. So the Apostle of Allah (peace_be_upon_him) made it equal between them. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3585"> Book 24, Number 3585: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> Some companions of Mu'adh ibn Jabal said: When the Apostle of Allah (peace_be_upon_him) intended to send Mu'adh ibn Jabal to the Yemen, he asked: How will you judge when the occasion of deciding a case arises? </p>
        <p> He replied: I shall judge in accordance with Allah's Book.  He asked: (What will you do) if you do not find any guidance in Allah's Book?  He replied: (I shall act) in accordance with the Sunnah of the Apostle of Allah (peace_be_upon_him). </p>
        <p> He asked: (What will you do) if you do not find any guidance in the Sunnah of the Apostle of Allah (peace_be_upon_him) and in Allah's Book? </p>
        <p> He replied: I shall do my best to form an opinion and I shall spare no effort. </p>
        <p> The Apostle of Allah (peace_be_upon_him) then patted him on the breast and said: Praise be to Allah Who has helped the messenger of the Apostle of Allah to find something which pleases the Apostle of Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3587"> Book 24, Number 3587: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Conciliation between Muslims is permissible. The narrator Ahmad added in his version: "except the conciliation which makes lawful unlawful and unlawful lawful." Sulayman ibn Dawud added: The Apostle of Allah (peace_be_upon_him) said: Muslims are on (i.e. stick to) their conditions. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3590"> Book 24, Number 3590: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Yahya ibn Rashid said: We were sitting waiting for Abdullah ibn Umar who came out to us and sat. He then said: I heard the Apostle of Allah (peace_be_upon_him) as saying: If anyone's intercession intervenes as an obstacle to one of the punishments prescribed by Allah, he has opposed Allah; if anyone disputes knowingly about something which is false, he remains in the displeasure of Allah till he desists, and if anyone makes an untruthful accusation against a Muslim, he will be made by Allah to dwell in the corrupt fluid flowing from the inhabitants of Hell till he retracts his statement. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3592"> Book 24, Number 3592: </a> </em>
      <blockquote>
        <p> Narrated Khuraym Ibn Fatik: </p>
        <p> The Apostle of Allah (peace_be_upon_him) offered the morning prayer. When he finished it, he stood up and said three times: False witness has been made equivalent to attributing a partner to Allah. He then recited: "So avoid the abomination of idols and avoid speaking falsehood as people pure of faith to Allah, not associating anything with Him. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3593"> Book 24, Number 3593: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) rejected the testimony of a deceitful man and woman, of one who harbours rancour against his brother, and he rejected the testimony of one who is dependent on a family, and he allowed his testimony for other. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3594"> Book 24, Number 3594: </a> </em>
      <blockquote>
        <p> Narrated Sulayman ibn Musa: </p>
        <p> The tradition mentioned above (No. 3593) has also been transmitted by Sulayman ibn Musa through a different chain of narrators. </p>
        <p> This version has: The Apostle of Allah (peace_be_upon_him) said: The testimony of a deceitful man or woman, of an adulterer and adulteress, and of one who harbours rancour against his brother is not allowable. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3595"> Book 24, Number 3595: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The testimony of a nomad Arab against a townsman is not allowable. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3597"> Book 24, Number 3597: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: The proviso of a loan combined with a sale is not allowable, nor two conditions relating to one transaction, nor the profit arising from something which is not in one's charge, nor selling what is not in your possession. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3598"> Book 24, Number 3598: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa al-Ash'ari: </p>
        <p> Ash-Sha'bi said: A Muslim was about to die at Daquqa', but he did not find any Muslim to call him for witness to his will. So he called two men of the people of the Book for witness. Then they came to Kufah, and approaching AbuMusa al-Ash'ari they informed him (about his) will. They brought his inheritance and will. Al-Ash'ari said: This is an incident (like) which happened in the time of the Apostle of Allah (peace_be_upon_him) and never occurred after him. So he made them to swear by Allah after the afternoon prayer to the effect that they had not misappropriated, nor  told a lie, nor changed, nor concealed, nor altered, and that it was the will of the man and his inheritance. He then executed their witness. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3599"> Book 24, Number 3599: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Abbas: </p>
        <p> A man from Banu Sahm went out with Tamim ad-Dari and Adi ibn Badda'. The man of Banu Sahm died in the land where no Muslim was present. When they returned with his inheritance, they (the heirs) did not find a silver cup with lines of gold (in his property). The Apostle of Allah (peace_be_upon_him) administered on oath to them. The cup was then found (with someone) at Mecca. They said: We have bought it from Tamim and Adi. </p>
        <p> Then two men from the heirs of the man of Banu Sahm got up and swore saying: Our witness is more reliable than their witness. They said that the cup belonged to their man. </p>
        <p> He (Ibn Abbas) said: The following verse was revealed about them: "O ye who believe! when death approaches any of you....." </p>
      </blockquote>
      <hr>
      <em> <a name="024.3600"> Book 24, Number 3600: </a> </em>
      <blockquote>
        <p> Narrated Uncle of Umarah ibn Khuzaymah: </p>
        <p> The Prophet (peace_be_upon_him) bought a horse from a Bedouin. The Prophet (peace_be_upon_him) took him with him to pay him the price of his horse. The Apostle of Allah (peace_be_upon_him) walked quickly and the Bedouin walked slowly. The people stopped the Bedouin and began to bargain with him for the horse as and they did not know that the Prophet (peace_be_upon_him) had bought it. </p>
        <p> The Bedouin called the Apostle of Allah (peace_be_upon_him) saying: If you want this horse, (then buy it), otherwise I shall sell it. The Prophet (peace_be_upon_him) stopped when he heard the call of the Bedouin, and said: Have I not bought it from you?  The Bedouin said: I swear by Allah, I have not sold it to you.  The Prophet (peace_be_upon_him) said: Yes, I have bought it from you.  The Bedouin began to say: Bring a witness.  Khuzaymah ibn Thabit then said: I bear witness that you have bought it.  The Prophet (peace_be_upon_him) turned to Khuzaymah and said: On what (grounds) do you bear witness? </p>
        <p> He said: By considering you trustworthy, Apostle of Allah (peace_be_upon_him)! The Prophet (peace_be_upon_him) made the witness of Khuzaymah equivalent to the witness of two people. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3603"> Book 24, Number 3603: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) gave a decision on the basis of an oath and a single witness. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3605"> Book 24, Number 3605: </a> </em>
      <blockquote>
        <p> Narrated Zubayb ibn Tha'labah al-Anbari: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sent an army to Banu al-Anbar. They captured them at Rukbah in the suburbs of at-Ta'if and drove them to the Holy Prophet (peace_be_upon_him). </p>
        <p> I rode hurriedly to the Holy Prophet (peace_be_upon_him) and said: Peace be on you, Apostle of Allah, and the mercy of Allah and His blessings. Your contingent came to us and arrested us, but we had already embraced Islam and cut the sides of the ears of our cattle. </p>
        <p> When Banu al-Anbar arrived, the Holy Prophet (peace_be_upon_him) said to me: Have you any evidence that you had embraced Islam before you were captured today? </p>
        <p> I said: Yes.  He said: Who is your witness? I said: Samurah, a man from Banu al-Anbar, and another man whom he named. The man testified but Samurah refused to testify.  The Holy Prophet (peace_be_upon_him) said: He (Samurah) has refused to testify for you, so take an oath with your other witness.  I said: Yes. He then dictated an oath to me and I swore to the effect that we had embraced Islam on a certain day, and that we had cut the sides of the ears of the cattle. </p>
        <p> The Holy Prophet (peace_be_upon_him) said: Go and divide half of their property, but do not touch their children. Had Allah not disliked the wastage of action, we should not have taxed you even a rope. </p>
        <p> Zubayb said: My mother called me and said: This man has taken my mattress. I then went to the Holy Prophet (peace_be_upon_him) and informed him. </p>
        <p> He said to me: Detain him. So I caught him with a garment around his neck, and stood there with him . Then the Holy Prophet (peace_be_upon_him) looked at us standing there. He asked: What do you intend (doing) with your captive? </p>
        <p> I said: I shall let him go free if he returns to this (man) the mattress of his mother which he has taken from her. </p>
        <p> He said: Prophet of Allah (peace_be_upon_him), I no longer have it. </p>
        <p> He said: The Holy Prophet (peace_be_upon_him) took the sword of the man and gave it to me, and said to him: Go and give him some sa's of cereal. So he gave me some sa's of barley. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3606"> Book 24, Number 3606: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa al-Ash'ari: </p>
        <p> Two men claimed a camel or an animal and brought the case to the Holy Prophet (peace_be_upon_him). But as neither of them produced any proof, the Holy Prophet (peace_be_upon_him) declared that they should share it equally. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3609"> Book 24, Number 3609: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> Two men disputed about some property and brought the case to the Holy Prophet (peace_be_upon_him), but neither of them could produce any proof. So the Holy Prophet (peace_be_upon_him) said: Cast lots about the oath whatever it may be, whether they like it or dislike it. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3613"> Book 24, Number 3613: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Holy Prophet (peace_be_upon_him) said to a man whom he asked to take an oath: Swear by Allah except whom there is no god that you have nothing belonging to him, i.e. the plaintiff. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3619"> Book 24, Number 3619: </a> </em>
      <blockquote>
        <p> Narrated Ikrimah: </p>
        <p> The Holy Prophet (peace_be_upon_him) said to Ibn Suriya': I remind you by Allah Who saved you from the people of Pharaoh, made you cover the sea, gave you the shade of clouds, sent down to you manna and quails, sent down you Torah to Moses, do you find stoning (for adultery) in your Book? He said: You have reminded me by the Great. It is not possible for me to belie you. He then transmitted the rest of the tradition. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3620"> Book 24, Number 3620: </a> </em>
      <blockquote>
        <p> Narrated Awf ibn Malik: </p>
        <p> The Holy Prophet (peace_be_upon_him) gave a decision between two men, and the one against whom the decision was given turned away and said: For me Allah sufficeth, and He is the best dispenser of affairs. The Holy Prophet (peace_be_upon_him) said: Allah, Most High, blames for falling short, but apply intelligence, and when the matter gets the better of you, say; For me Allah sufficeth, and He is the best disposer of affairs. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3621"> Book 24, Number 3621: </a> </em>
      <blockquote>
        <p> Narrated Ash-Sharid: </p>
        <p> The Prophet (peace_be_upon_him) said: Delay in payment on the part of one who possesses means makes it lawful to dishonour and punish him. Ibn al-Mubarak said that "dishonour" means that he may be spoken to roughly and "punish" means he may be imprisoned for it. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3622"> Book 24, Number 3622: </a> </em>
      <blockquote>
        <p> Narrated Grandfather of Hirmas ibn Habib: </p>
        <p> I brought my debtor to the Holy Prophet (peace_be_upon_him). He said to me: Stick to him. He again said to me: O brother of Banu Tamim, what do you want to do with your prisoner. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3623"> Book 24, Number 3623: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah al-Qushayri: </p>
        <p> The Prophet (peace_be_upon_him) imprisoned a man on suspicion. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3624"> Book 24, Number 3624: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah al-Qushayri: </p>
        <p> Bahz ibn Hakim reported from his grandfather: (Ibn Qudamah's version has: His grandfather's brother or uncle reported:) - the narrator Mu'ammal said: - He (his grandfather Mu'awiyah) got up before the Holy Prophet (peace_be_upon_him) who was giving sermon: and he said: Why have your companions arrested my neighbours? He turned away from him twice. He (his grandfather Mu'awiyah) then mentioned something. The Holy Prophet (peace_be_upon_him) then said: Let his neighbours go. </p>
        <p> (Mu'ammal did not mention the words "He was giving sermon.") </p>
      </blockquote>
      <hr>
      <em> <a name="024.3625"> Book 24, Number 3625: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> I intended to go (on expedition) to Khaybar. So I came to the Holy Prophet (peace_be_upon_him), greeted him and said: I am intending to go to Khaybar. He said: When you come to my agent, you should take from him fifteen wasqs (of dates). If he asks you for a sign, then place your hand on his collar-bone. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3626"> Book 24, Number 3626: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If you dispute over a pathway, leave the margin of seven yards. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3628"> Book 24, Number 3628: </a> </em>
      <blockquote>
        <p> Narrated AbuSirmah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone harms (others), Allah will harm him, and if anyone shows hostility to others, Allah will show hostility to him. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3629"> Book 24, Number 3629: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> Samurah had a row of palm-trees in the garden of a man of the Ansar. The man had his family with him. Samurah used to visit his palm-trees, and the man was annoyed by that and felt it keenly. So he asked him (Samurah) to sell them to him, but he refused. He then asked him to take something else in exchange, but he refused. </p>
        <p> So he came to the Holy Prophet (peace_be_upon_him) and mentioned it to him. The Holy Prophet (peace_be_upon_him) asked him to sell it to him, but he refused. He asked him to take something else in exchange, but he refused. </p>
        <p> He then said: Give it to him and you can have such and such, mentioning something with which he tried to please him, but he refused. He then said: You are a nuisance. The Apostle of Allah (peace_be_upon_him) then said to the Ansari: Go and uproot his palm-trees. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3630"> Book 24, Number 3630: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn az-Zubayr: </p>
        <p> A man disputed with az-Zubayr about streamlets in the lava plain which was irrigated by them. The Ansari said: Release the water and let it run, but az-Zubayr refused. The Holy Prophet (peace_be_upon_him) said to az-Zubayr: Water (your ground), Zubayr, then let the water run to your neighbour. The Ansari then became angry and said: Apostle of Allah! it is because he is your cousin! Thereupon the face of the Apostle of Allah (peace_be_upon_him) changed colour and he said: Water (your ground), then keep back the water till it returns to the embankment. Az-Zubayr said: By Allah! I think this verse came down about that: "But no , by thy Lord! they can have no (real) faith, until they make thee judge....." </p>
      </blockquote>
      <hr>
      <em> <a name="024.3631"> Book 24, Number 3631: </a> </em>
      <blockquote>
        <p> Narrated Tha'labah ibn AbuMalik: </p>
        <p> Tha'labah heard his elders say that a man from the Quraysh had his share with Banu Qurayzah (in water). He brought the dispute to the Apostle of Allah (peace_be_upon_him) about al-Mahzur, a stream whose water they shared together. The Apostle of Allah (peace_be_upon_him) then decided that when water reached the ankles waters should not be held back to flow to the lower. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3632"> Book 24, Number 3632: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) decided regarding the stream al-Mahzur that its water should be held back till it reached the ankles, and that the upper waters should then be allowed to flow to the lower. </p>
      </blockquote>
      <hr>
      <em> <a name="024.3633"> Book 24, Number 3633: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> Two men brought their dispute about the precincts of a palm-tree to the Apostle of Allah (peace_be_upon_him). According to a version of this tradition, he ordered to measure and it was measured. It was found seven yards. According to another version, it was found five yards. He made a decision according to that. AbdulAziz said: He ordered to measure with a branch of its branches. It was then measured. </p>
      </blockquote>
