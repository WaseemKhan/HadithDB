        <p> They said: Bier of Abdullah ibn Umayr. So I followed it. Suddenly I saw a man, who had a thin garment on riding his small mule. He had a piece of cloth on his head to protect himself from the sun.  I asked: Who is this important man? People said: This is Anas ibn Malik. </p>
        <p> When the bier was placed, Anas stood and led the funeral prayer over him while I was just behind him, and there was no obstruction between me and him. He stood near his head, and uttered four takbirs (Allah is Most Great). He neither lengthened the prayer nor hurried it. He then went to sit down. They said: AbuHamzah, (here is the bier of) an Ansari woman. They brought her near him and there was a green cupola-shaped structure over her bier. He stood opposite her hips and led the funeral prayer over her as he had led it over the man. He then sat down. </p>
        <p> Al-Ala' ibn Ziyad asked: AbuHamzah, did the Apostle of Allah (peace_be_upon_him) say the funeral prayer over the dead as you have done, uttering four takbirs (Allah is Most Great) over her, and standing opposite the head of a man and the hips of a woman? </p>
        <p> He replied: Yes.  He asked: AbuHamzah, did you fight with the Apostle of Allah?  He replied: Yes. I fought with him in the battle of Hunayn. The polytheists came out and invaded us so severely that we saw our horses behind our backs. Among the people (i.e. the unbelievers) there was a man who was attacking us, and striking and wounding us (with his sword). Allah then defeated them. They were then brought and began to take the oath of allegiance to him for Islam. </p>
        <p> A man from among the companions of the Prophet (peace_be_upon_him) said: I make a vow to myself that if Allah brings the man who was striking us (with his sword) that day, I shall behead him. The Apostle of Allah (peace_be_upon_him) kept silent and the man was brought (as a captive). </p>
        <p> When he saw the Apostle of Allah (peace_be_upon_him), he said: Apostle of Allah, I have repented to Allah. The Apostle of Allah (peace_be_upon_him) stopped (for a while) receiving his oath of allegiance, so that the other man might fulfil his vow. But the man began to wait for the order of the Apostle of Allah (peace_be_upon_him) for his murder. He was afraid of the Apostle of Allah (peace_be_upon_him) to kill him. When the Apostle of Allah (peace_be_upon_him) saw that he did not do anything, he received his oath of allegiance.  The man said: Apostle of Allah, what about my vow?  He said: I stopped (receiving his oath of allegiance) today so that you might fulfil your vow.  He said: Apostle of Allah, why did you not give any signal to me?  The Prophet (peace_be_upon_him) said: It is not worthy of a Prophet to give a signal. </p>
