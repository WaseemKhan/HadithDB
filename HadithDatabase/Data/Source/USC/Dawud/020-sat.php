        <p> A cry was again raised (for his death).  He came to the Apostle of Allah (peace_be_upon_him) and said: He has died.  The Prophet (peace_be_upon_him) said: He has not died. He then returned. </p>
        <p> A cry was again raised over him. His wife said: Go to the Apostle of Allah (peace_be_upon_him) and inform him.  The man said: O Allah, curse him. </p>
        <p> He said: The man then went and saw that he had killed himself with an arrowhead. So he went to the Prophet (peace_be_upon_him) and informed him that he had died. </p>
        <p> He asked: Who told you?  He replied: I myself saw that he had killed himself with arrowheads.  He asked: Have you seen him?  He replied: Yes.  He then said: Then I shall not pray over him. </p>
