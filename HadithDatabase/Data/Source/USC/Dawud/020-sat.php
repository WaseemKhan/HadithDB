      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 20: </a> </em>
      <h1> <em> Funerals (Kitab Al-Jana'iz) </em> </h1>
      <hr>
      <em> <a name="020.3083"> Book 20, Number 3083: </a> </em>
      <blockquote>
        <p> Narrated Amir ar-Ram: </p>
        <p> We were in our country when flags and banners were raised. I said: What is this? </p>
        <p> The (the people) said: This is the banner of the Apostle of Allah (peace_be_upon_him). So I came to him. He was (sitting) under a tree. A sheet of cloth was spread for him and he was sitting on it. His Companions were gathered around him. I sat with them. </p>
        <p> The Apostle of Allah (peace_be_upon_him) mentioned illness and said: When a believer is afflicted by illness and Allah cures him of it, it serves as an atonement for his previous sins and a warning to him for the future. </p>
        <p> But when a hypocrite becomes ill and is then cured, he is like a camel which has been tethered and then let loose by its owners, but does not know why they tethered it and why they let it loose. </p>
        <p> A man from among those around him asked: Apostle of Allah, what are illnesses? I swear by Allah, I never fell ill. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Get up and leave us. You do not belong to our number. When we were with him, a man came to him. He had a sheet of cloth and something in his hand. </p>
        <p> He turned his attention to him and said: Apostle of Allah, when I saw you, I turned towards you. I saw a group of trees and heard the sound of fledglings. I took them and put them in my garment. Their mother then came and began to hover round my head. I showed them to her, and she fell on them. I wrapped them with my garment. They are now with me. </p>
        <p> He said: Put them away from you. So I put them away, but their mother stayed with them. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to his companions: Are you surprised at the affection of the mother for her young? </p>
        <p> They said: Yes, Apostle of Allah.  He said: I swear by Him Who has sent me with the Truth, Allah is more affectionate to His servants than a mother to her young ones. Take them back put them and where you took them from when their mother should have been with them. So he took them back. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3084"> Book 20, Number 3084: </a> </em>
      <blockquote>
        <p> Narrated Muhammad ibn Khalid as-Sulami: </p>
        <p> As-Sulami's grandfather, who was a Companion of the Apostle of Allah (peace_be_upon_him) said: I heard the Apostle of Allah (peace_be_upon_him) say: When Allah has previously decreed for a servant a rank which he has not attained by his action, He afflicts him in his body, or his property or his children. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3086"> Book 20, Number 3086: </a> </em>
      <blockquote>
        <p> Narrated Umm al-Ala: </p>
        <p> The Apostle of Allah (peace_be_upon_him) visited me while I was sick. He said: Be glad, Umm al-Ala' for Allah removes the sins of a Muslim for his illness as fire removes the dross of gold and silver. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3091"> Book 20, Number 3091: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone performs ablution well and pays a sick-visit to his brother Muslim seeking his reward from Allah, he will be removed a distance of sixty years (kharif) from Hell. I asked: What is kharif, AbuHamzah? He replied: A year. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3096"> Book 20, Number 3096: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Arqam: </p>
        <p> The Apostle of Allah (peace_be_upon_him) visited me while I was suffering from pain in my eyes. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3100"> Book 20, Number 3100: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone visits a sick whose time (of death) has not come, and says with him seven times: I ask Allah, the Mighty, the Lord of the mighty Throne, to cure you, Allah will cure him from that disease. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3101"> Book 20, Number 3101: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: When a man comes to visit a sick person, he should say: O Allah, cure Thy servant, who may then wreak havoc on an enemy for Thy sake, or walk at a funeral for Thy sake. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3104"> Book 20, Number 3104: </a> </em>
      <blockquote>
        <p> Narrated Ubayd ibn Khalid as-Sulami,: </p>
        <p> A man from the Companions of the Prophet (peace_be_upon_him), said: The narrator Sa'd ibn Ubaydah narrated sometimes from the Prophet (peace_be_upon_him) and sometimes as a statement of Ubayd (ibn Khalid): The Prophet (peace_be_upon_him) said: Sudden death is a wrathful catching. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3105"> Book 20, Number 3105: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Atik: </p>
        <p> The Apostle of Allah (peace_be_upon_him) came to visit Abdullah ibn Thabit who was ill. He found that he was dominated (by the divine decree). The Apostle of Allah (peace_be_upon_him) called him loudly, but he did not respond. </p>
        <p> He uttered the Qur'anic verse "We belong to Allah and to Him do we return" and he said: We have been dominated against you, AburRabi'. Then the women cried and wept, and Ibn Atik began to silence them. The Apostle of Allah (peace_be_upon_him) said: Leave them, when the divine decree is made, no woman should weep. </p>
        <p> They (the people) asked: What is necessary happening, Apostle of Allah?  He replied: Death.  His daughter said: I hope you will be a martyr, for you have completed your preparations for jihad. The Apostle of Allah (peace_be_upon_him) said: Allah Most High gave him a reward according to his intentions. What do you consider martyrdom? </p>
        <p> They said: Being killed in the cause of Allah. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: There are seven types of martyrdom in addition to being killed in Allah's cause: one who dies of plague is a martyr; one who is drowned is a martyr; one who dies of pleurisy is a martyr; one who dies of an internal complaint is a martyr; one who is burnt to death is a martyr; who one is killed by a building falling on him is a martyr; and a woman who dies while pregnant is a martyr. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3108"> Book 20, Number 3108: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> When the time of his death came, he called for new clothes and put on them. He then said: I heard the Apostle of Allah (peace_be_upon_him) say: A deceased will be raised in the clothes in which he died. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3115"> Book 20, Number 3115: </a> </em>
      <blockquote>
        <p> Narrated Ma'qil ibn Yasar: </p>
        <p> The Prophet (peace_be_upon_him) said: Recite Surah Ya-Sin over your dying men. This is the version of Ibn al-Ala' </p>
      </blockquote>
      <hr>
      <em> <a name="020.3117"> Book 20, Number 3117: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> We buried a deceased person in the company of the Apostle of Allah (peace_be_upon_him). When we had finished, the Apostle of Allah (peace_be_upon_him) returned and we also returned with him. When he approached his door, he stopped, and we saw a woman coming towards him. </p>
        <p> He (the narrator) said: I think he recognized her. When she went away, we came to know that she was Fatimah. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to her: What brought you out of your house, Fatimah? </p>
        <p> She replied: I came to the people of this house, Apostle of Allah, and I showed pity and expressed my condolences to them for their deceased relation. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: You might have gone to the graveyard with them. </p>
        <p> She replied: I seek refuge in Allah! I heard you referring to what you mentioned. </p>
        <p> He said: If you had gone to the graveyard...He then mentioned severe words about it. </p>
        <p> I then asked Rabi'ah (a narrator of this tradition) about al-kuda (stony land). He replied: I think it means the graves. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3122"> Book 20, Number 3122: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Apostle of Allah (peace_be_upon_him) cursed the wailing woman and the woman who listens to her. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3124"> Book 20, Number 3124: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa: </p>
        <p> Yazid ibn Aws said: I entered upon AbuMusa while he was at the point of death. His wife began to weep or was going to weep. AbuMusa said to her: Did you not hear what the Apostle of Allah (peace_be_upon_him) said? She said: Yes. The narrator said: She then kept silence. When AbuMusa died, Yazid said: I met the woman and asked her: What did AbuMusa mean when he said to you: Did you not hear what the Apostle of Allah (peace_be_upon_him) and the you kept silence? She replied: The Apostle of Allah (peace_be_upon_him) said: He who shaves (his head), shouts and tears his clothing does not belong to us. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3125"> Book 20, Number 3125: </a> </em>
      <blockquote>
        <p> Narrated A woman who took oath of allegiance: </p>
        <p> Usayd ibn AbuUsayd, reported on the authority of a woman who took oath of allegiance (to the Prophet): One of the oaths which the Apostle of Allah (peace_be_upon_him) received from us about the virtue was that we would not disobey him in it (virtue): that we would not scratch the face, nor wail, nor tear the front of the garments nor dishevel the hair. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3126"> Book 20, Number 3126: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Ja'far: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Prepare food for the family of Ja'far for there came upon them an incident which has engaged them. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3127"> Book 20, Number 3127: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> A man had a shot of arrow in his chest or throat (the narrator is doubtful). So he died. He was shrouded in his clothes as he was. The narrator said: We were with the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="020.3128"> Book 20, Number 3128: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) commanded to remove weapons and skins from the martyrs of Uhud, and that they should be buried with their blood and clothes. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3129"> Book 20, Number 3129: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The martyrs of Uhud were not washed, and they were buried with their blood. No prayer was offered over them. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3130"> Book 20, Number 3130: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Apostle of Allah (peace_be_upon_him) passed Hamzah who was killed and disfigured. He said: If Safiyyah were not grieved, I would have left him until the birds and beasts of prey would have eaten him, and he would have been resurrected from their bellies. The garments were scanty and the slain were in great number. So one, two and three persons were shrouded in one garment. The narrator Qutaybah added: They were then buried in one grave. The Apostle of Allah (peace_be_upon_him) asked: Which of the two learnt the Qur'an more? He then advanced him toward the qiblah (direction of prayer). </p>
      </blockquote>
      <hr>
      <em> <a name="020.3131"> Book 20, Number 3131: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) passed by Hamzah who was disfigured (after being killed). He did not offer prayer over any martyr except him. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3134"> Book 20, Number 3134: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not unveil your thigh, and do not look at the thigh of the living and the dead. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3135"> Book 20, Number 3135: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> By Allah, we did not know whether we should take off the clothes of the Apostle of Allah (peace_be_upon_him) as we took off the clothes of our dead, or wash him while his clothes were on him. When they (the people) differed among themselves, Allah cast slumber over them until every one of them had put his chin on his chest. </p>
        <p> Then a speaker spoke from a side of the house, and they did not know who he was: Wash the Prophet (peace_be_upon_him) while his clothes are on him. So they stood round the Prophet (peace_be_upon_him) and washed him while he had his shirt on him. They poured water on his shirt, and rubbed him with his shirt and not with their hands. Aisha used to say: If I had known beforehand about my affair what I found out later, none would have washed him except his wives. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3144"> Book 20, Number 3144: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: When one of you dies, and he possesses something, he should be shrouded in the garment of the Yemeni stuff. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3145"> Book 20, Number 3145: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was shrouded in three garments of white Yemeni stuff, among which was neither a shirt nor a turban. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3146"> Book 20, Number 3146: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) was shrouded in three garments of cotton. </p>
        <p> The narrator said: Aisha was told that the people said that he was shrouded in two garments and one cloak. She replied: A cloak was brought but they returned it and did not shroud him in it. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3147"> Book 20, Number 3147: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was shrouded in three garments made in Najran: two garments and one shirt in which he died. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3148"> Book 20, Number 3148: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Do not be extravagant in shrouding, for I heard the Apostle of Allah (peace_be_upon_him) say: Do not be extravagant in shrouding, for it will be quickly decayed. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3150"> Book 20, Number 3150: </a> </em>
      <blockquote>
        <p> Narrated Ubadah ibn as-Samit: </p>
        <p> The Prophet (peace_be_upon_him) said: The best shroud is a lower garment and one which covers the whole body, and the best sacrifice is a horned ram. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3151"> Book 20, Number 3151: </a> </em>
      <blockquote>
        <p> Narrated Layla daughter of Qa'if ath-Thaqafiyyah: </p>
        <p> I was one of those who washed Umm Kulthum, daughter of the Prophet (peace_be_upon_him), when she died. The Apostle of Allah (peace_be_upon_him) first gave us lower garment, then shirt, then head-wear, then cloak (which covers the whole body), and then she was shrouded in another garment. She said: The Apostle of Allah (peace_be_upon_him) was sitting at the door, and he had shroud with him. He gave us the garments one by one. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3153"> Book 20, Number 3153: </a> </em>
      <blockquote>
        <p> Narrated Al-Husayn ibn Wahwah: </p>
        <p> Talhah ibn al-Bara' fell ill and the Prophet (peace_be_upon_him) came to pay him a sick-visit. He said: I think Talhah has died; so tell me (about his death), and make haste, for it is not advisable that the corpse of a Muslim should remain withheld among his family. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3154"> Book 20, Number 3154: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) used to take a bath on account of sexual defilement, on Friday, for cupping and washing the dead. </p>
      </blockquote>
      <hr>
      <em> <a name="020.3155"> Book 20, Number 3155: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
