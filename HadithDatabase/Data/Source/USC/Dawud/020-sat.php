      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> We asked the Prophet (peace_be_upon_him) about walking with the funeral. He replied: Not running (but walking quickly). If he (the dead person) was good, send him to it quickly; if he was otherwise, keep away the people of Hell. The bier should be followed and should not follow. Those who go in front of it are not accompanying it. </p>
      </blockquote>
