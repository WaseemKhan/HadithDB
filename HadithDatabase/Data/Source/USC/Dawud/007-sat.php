      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 7: </a> </em>
      <h1> <em> Prayer (Kitab Al-Salat): Prostration while reciting the Qur'an </em> </h1>
      <hr>
      <em> <a name="007.1396"> Book 7, Number 1396: </a> </em>
      <blockquote>
        <p> Narrated Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) taught me fifteen prostrations while reciting the Qur'an, including three in al-Mufassal and two in Surah al-Hajj. </p>
      </blockquote>
      <hr>
      <em> <a name="007.1397"> Book 7, Number 1397: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> I said to the Apostle of Allah (peace_be_upon_him): Are there two prostrations in Surah al-Hajj? He replied: Yes; if anyone does not make two prostrations, he should not recite them. </p>
      </blockquote>
      <hr>
      <em> <a name="007.1398"> Book 7, Number 1398: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) did not make a prostration at any verse in al-Mufassal from the time he moved to Medina. </p>
      </blockquote>
      <hr>
      <em> <a name="007.1405"> Book 7, Number 1405: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> When the Prophet (peace_be_upon_him) uttered the salutation at the end of the prayer, he used to say: "O Allah, forgive me my former and latter sins, what I have kept secret and what I have done openly, and what I have done extravagance; and what Thou knowest better than I do. Thou art the Advancer, the Delayer, there is no god but Thou" </p>
      </blockquote>
      <hr>
      <em> <a name="007.1406"> Book 7, Number 1406: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> In the year of Conquest the Apostle of Allah (peace_be_upon_him) recited a verse at which a prostration should be made and all the people prostrated themselves. Some were mounted, and some were prostrating themselves on the ground, and those who were mounted prostrated themselves on their hands. </p>
      </blockquote>
      <hr>
      <em> <a name="007.1408"> Book 7, Number 1408: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to recite the Qur'an to us. When he came upon the verse containing prostration, he would utter the takbir (Allah is most great) and we would prostrate ourselves along with him. </p>
      </blockquote>
      <hr>
      <em> <a name="007.1409"> Book 7, Number 1409: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) prostrated himself at night when reciting the Qur'an. He said repeatedly: My face prostrates itself to Him Who created it and brought forth its hearing and seeing by His might and power. </p>
      </blockquote>
      <hr>
      <em> <a name="007.1410"> Book 7, Number 1410: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> AbuTamimah al-Hujaymi said: When we came to Medina accompanying the caravan, I used to preach after the dawn prayer, and prostrate on account of the recitation of the Qur'an. Ibn Umar prohibited me three times, but I did not cease  doing that. He then repeated (his prohibition) saying: I prayed behind the Apostle of Allah (peace_be_upon_him), AbuBakr, Umar and Uthman, they would not prostrate (on account of the recitation of the Qur'an) till the sun had risen. </p>
      </blockquote>
