      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 15: </a> </em>
      <h1> <em> Sacrifice (Kitab Al-Dahaya) </em> </h1>
      <hr>
      <em> <a name="015.2782"> Book 15, Number 2782: </a> </em>
      <blockquote>
        <p> Narrated Mikhnaf ibn Sulaym: </p>
        <p> We were staying with the Apostle of Allah (peace_be_upon_him) at Arafat; he said: O people, every family must offer a sacrifice and an atirah. Do you know what  the atirah is? It is what you call the Rajab sacrifice. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2783"> Book 15, Number 2783: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: I have been commanded to celebrate festival ('Id) on the day of sacrifice, which Allah, Most High, has appointed for this community. A man said: If I do not find except a she-goat or a she-camel borrowed for milk or other benefits, should I sacrifice it? He said: No, but you should clip your hair , and nails, trim your moustaches, and shave your pubes. This is all your sacrifice in the eyes of Allah, Most High. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2784"> Book 15, Number 2784: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Hanash said: I saw Ali sacrificing two rams; so I asked him: What is this? He replied. The Apostle of Allah (peace_be_upon_him) enjoined upon me to sacrifice on his behalf, so that is what I am doing. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2789"> Book 15, Number 2789: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) sacrificed two horned rams which were white with black markings and had been castrated. When he made them face the qiblah, he said: I have turned my face towards Him. Who created the heavens and the earth, following Abraham's religion, the true in faith, and I am not one of the polytheists. My prayer, and my service of sacrifice, my life and my death are all for Allah, the Lord of the Universe, Who has no partner. That is what I was commanded to do, and I am one of the Muslims. O Allah it comes from Thee and is given to Thee from Muhammad and his people. In the name of Allah, and Allah is Most Great. He then made sacrifice. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2790"> Book 15, Number 2790: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to sacrifice a choice, horned ram with black round the eyes, the mouth and the feet. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2792"> Book 15, Number 2792: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Khalid al-Juhani: </p>
        <p> The Apostle of Allah (peace_be_upon_him) distributed sacrificial animals among his Companions. He gave me a kid (of less than a year). I took it to him and said: This is a kid. He said: Sacrifice it. so I sacrificed it. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2795"> Book 15, Number 2795: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> A maternal uncle of mine called AbuBurdah sacrificed before the prayer (for 'Id). The Apostle of Allah (peace_be_upon_him) said: Your goat is meant for flesh. He said: Apostle of Allah, I have a domestic kid with me. He said: Sacrifice it, but it is not valid for any man other than you. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2796"> Book 15, Number 2796: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> Ubayd ibn Firuz said: I asked al-Bara' ibn Azib: What should be avoided in sacrificial animals? He said: The Apostle of Allah (peace_be_upon_him)  stood among us, and my fingers are smaller than his fingers, and my fingertips are smaller than his fingertips. He said (pointing with his fingers): Four (types of animals) should be avoided in sacrifice: A One-eyed animal which has obviously lost the sight of one eye, a sick animal which is obviously sick, a lame animal which obviously limps and an animal with a broken leg with no marrow. I also detest an animal which has defective teeth. He said: Leave what you detest, but do not make it illegal for anyone. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2797"> Book 15, Number 2797: </a> </em>
      <blockquote>
        <p> Narrated Utbah ibn AbdusSulami: </p>
        <p> Yazid Dhu Misr said: I came to Utbah ibn AbdusSulami and said: AbulWalid, I went out seeking sacrificial animals. I did not find anything which attracted me except an animal whose teeth have fallen. So I abominated it. What do you say (about it)? He said: Why did you not bring it to me? He said: Glory be to Allah: Is if lawful for you and not lawful for me? He said: Yes, you doubt and I do not doubt. The Apostle of Allah (peace_be_upon_him) has forbidden an animal whose ear has been uprooted so much so that its hole appears (outwardly), and an animal whose horn has broken from the root, and an animal which has totally lost the sight of its eye, and an animal which is so thin and weak that it cannot go with the herd, and an animal with a broken leg. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2798"> Book 15, Number 2798: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) enjoined upon us to pay great attention to the eye and both ears, and not to sacrifice a one-eyed animal, and an animal with a slit which leaves something hanging at the front or back of the ear, or with a lengthwise slit with a perforation in the ear. I asked AbuIshaq: Did he mention an animal with broken horns and uprooted ears? He said: No. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2799"> Book 15, Number 2799: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) prohibited to sacrifice an animal with a slit ear and broken horn. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2802"> Book 15, Number 2802: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: A cow serves for seven, and a camel serves for seven. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2803"> Book 15, Number 2803: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> We sacrificed along with the Apostle of Allah (peace_be_upon_him) at al-Hudaybiyyah a camel for seven and a cow for seven people. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2804"> Book 15, Number 2804: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> I witnessed sacrificing along with the Apostle of Allah (peace_be_upon_him) at the place of prayer. When he finished his sermon, he descended from his pulpit, and a ram was brought to him. The Apostle of Allah (peace_be_upon_him) slaughtered it with his hand, and said: In the name of Allah, Allah, is Most Great. This is from me and from those who did not sacrifice from my community. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2807"> Book 15, Number 2807: </a> </em>
      <blockquote>
        <p> Narrated Nubayshah: </p>
        <p> The Prophet (peace_be_upon_him) said: We forbade you to eat their meat for more than three days in order that you  might have abundance; now Allah has produced abundance, so you may eat, store up and seek reward. Beware, these days are days of eating, drinking and remembrance of Allah, Most High. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2812"> Book 15, Number 2812: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> explaining the verse "But the evil ones ever inspire their friend to contend with you" They used to say: Do not eat which Allah killed, but eat which you slaughtered. So Allah revealed the verse: "Eat not of (meats) on which Allah's name hath not been pronounced"...to the end of the verse. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2813"> Book 15, Number 2813: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Jews came to the Prophet (peace_be_upon_him) and said: We eat which we kill but we do not eat which Allah kills? So Allah revealed: "Eat not of (meats) on which Allah's name hath not been pronounced." to the end of the verse. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2814"> Book 15, Number 2814: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to eat (the meat of animals) slaughtered by the bedouins for vainglory and pride. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2816"> Book 15, Number 2816: </a> </em>
      <blockquote>
        <p> Narrated Muhammad ibn Safwan or Safwan ibn Muhammad: </p>
        <p> I hunted two hares and slaughtered them with a flint. I asked the Apostle of Allah (peace_be_upon_him) about them. He permitted me to eat them. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2817"> Book 15, Number 2817: </a> </em>
      <blockquote>
        <p> Narrated Ata' ibn Yasar: </p>
        <p> A man of Banu Harith was pasturing a pregnant she-camel in one of the ravines of Uhud, (he saw that) it was about to die; he could find nothing to slaughter it; he took a stake and stabbed it in the upper part of its breast until he made its blood flow. </p>
        <p> He then came to the Prophet (peace_be_upon_him) and informed him about that, and he ordered him to eat it. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2818"> Book 15, Number 2818: </a> </em>
      <blockquote>
        <p> Narrated Adi ibn Hatim: </p>
        <p> I said: Apostle of Allah, tell me when one of us catches game and has no knife; may he slaughter with a flint and a splinter of stick. He said: Cause the blood to flow with whatever you like and mention Allah's name. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2819"> Book 15, Number 2819: </a> </em>
      <blockquote>
        <p> Narrated AbulUshara' (Usamah ibn Malik?): </p>
        <p> AbulUshara' reported on the authority of his father: He asked: Apostle of Allah, is the slaughtering to be done only in the upper part of the breast and the throat? The Apostle of Allah (peace_be_upon_him) replied: If you pierced its thigh, it would serve you. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2820"> Book 15, Number 2820: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Ibn Isa added: (Ibn Abbas) and AbuHurayrah said: The Apostle of Allah (peace_be_upon_him) forbade the devil's sacrifice. AbuIsa added in his version: This refers to the slaughtered animal whose skin cut off, and is then left to die without its jugular veins being severed. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2821"> Book 15, Number 2821: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> I asked the Apostle of Allah (peace_be_upon_him) about the embryo. He replied: Eat it if you wish. </p>
        <p> Musaddad's version says: we said: Apostle of Allah, we slaughter a she-camel, a cow and a sheep, and we find an embryo in its womb. Shall we throw it away or eat it? He replied: Eat it if you wish for the slaughter of its mother serves its slaughter. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2822"> Book 15, Number 2822: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: The slaughter of embryo is included when its mother is slaughtered. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2824"> Book 15, Number 2824: </a> </em>
      <blockquote>
        <p> Narrated Nubayshah: </p>
        <p> A man called the Apostle of Allah (peace_be_upon_him): We used to sacrifice Atirah in pre-Islamic days during Rajab; so what do you command us? He said: Sacrifice for the sake of Allah in any month whatever; obey Allah, Most High, and feed(the people). He said: We used to sacrifice a Fara' in pre-Islamic days, so what do you command us? He said: On every pasturing animal there is a Fara' which is fed by your cattle till it becomes strong and capable of carrying load. </p>
        <p> The narrator Nasr said (in his version): When it becomes capable of carrying load of the pilgrims, you may slaughter it and give its meat as charity (sadaqah). </p>
        <p> The narrator Khalid's version says: You (may give it) to the travellers, for it is better. Khalid said: I asked AbuQilabah: How many pasturing animals? He replied: One hundred. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2828"> Book 15, Number 2828: </a> </em>
      <blockquote>
        <p> Narrated Umm Kurz al-Ka'biyyah: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Two resembling sheep are to be sacrificed for a boy and one for a girl. </p>
        <p> AbuDawud said: I heard Ahmad (ibn Hanbal) say: The Arabic word mukafi'atani means equal (in age) or resembling each other. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2829"> Book 15, Number 2829: </a> </em>
      <blockquote>
        <p> Narrated Umm Kurz: </p>
        <p> I heard the Prophet (nay peace be upon him) say: Let the birds stay in their roosts. She said: I also heard him say: Two sheep are to be sacrificed for a boy and one for a girl, but it does you no harm whether they are male or female. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2830"> Book 15, Number 2830: </a> </em>
      <blockquote>
        <p> Narrated Umm Kurz: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Two sheep which resemble each other are to be sacrificed for a boy and one for a girl. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2831"> Book 15, Number 2831: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> The Prophet (peace_be_upon_him) said: A boy is in pledge for his Aqiqah. Sacrifice is made for him on the seventh day, his head is shaved and is smeared with blood. </p>
        <p> When Qatadah was asked about smearing with blood, how that should be done, he said: When you cut the head (i.e. throat) of the animal (meant for Aqiqah), you may take a few hair of it, place them on its veins, and then place them in the middle of the head of the infant, so that the blood flows on the hair (of the infant) like a threat. Then its head may be washed and shaved off. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2832"> Book 15, Number 2832: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> The Prophet (peace_be_upon_him) said: A boy is in pledge for his Aqiqah, Sacrifice is made for him on the seventh day, his head is shaved and he is given name. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2835"> Book 15, Number 2835: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sacrificed a ram for both al-Hasan and al-Husayn each (Allah be pleased with them). </p>
      </blockquote>
      <hr>
      <em> <a name="015.2836"> Book 15, Number 2836: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was asked  about the aqiqah. He replied: Allah does not like the breaking of ties (uquq), as though he disliked the name. And he said: If anyone has a child born to him and wishes to offer a sacrifice on its behalf, he may offer two resembling sheep for a boy and one for a girl. And he was asked about fara'. He replied: Fara' is right. If you leave it (i.e. let it grow till it becomes a healthy camel of one year or two years, then you give it to a widow or give it in the path of Allah for using it as a riding beast, it is better than slaughtering it at the age when its meat is stuck to its hair, and you turn over your milking vessel and annoy your she-camel. </p>
      </blockquote>
      <hr>
      <em> <a name="015.2837"> Book 15, Number 2837: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> When a boy was born to one of us in the pre-Islamic period, we sacrificed a sheep and smeared his head with its blood; but when Allah brought Islam, we sacrificed a sheep, shaved his head and smeared his head with saffron. </p>
      </blockquote>
