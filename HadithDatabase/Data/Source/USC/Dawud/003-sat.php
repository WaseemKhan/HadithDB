      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 3: </a> </em>
      <h1> <em> Prayer (Kitab Al-Salat): Details of Commencing Prayer </em> </h1>
      <hr>
      <em> <a name="003.0725"> Book 3, Number 0725: </a> </em>
      <blockquote>
        <p> Narrated Wa'il ibn Hujr: </p>
        <p> I purposely looked at the prayer of the Apostle of Allah (peace_be_upon_him), how he offered it. The Apostle of Allah (peace_be_upon_him) stood up, faced the direction of the qiblah and uttered the takbir (Allah is most great) and then raised his hands in front of his ears, then placed his right hand on his left (catching each other). </p>
        <p> When he was about to bow, he raised them in the same manner. He then placed his hands on his knees. When he raised his head after bowing, he raised them in the like manner. When he prostrated himself he placed his forehead between his hands. </p>
        <p> He then sat down and spread his left foot and placed his left hand on his left thigh, and kept his right elbow aloof from his  right thigh. He closed his two fingers and made a circle (with the fingers). </p>
        <p> I (Asim ibn Kulayb) saw him (Bishr ibn al-Mufaddal) say in this manner. Bishr made the circle with the thumb and the middle finger and pointed with the forefinger. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0727"> Book 3, Number 0727: </a> </em>
      <blockquote>
        <p> Narrated Wa'il ibn Hujr: </p>
        <p> I witnessed the Prophet (peace_be_upon_him) raise his hands in front of his ears when he began to pray. I then came back and saw them (the people) raising their hands up to their chest when they began to pray. They wore long caps and blankets. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0736"> Book 3, Number 0736: </a> </em>
      <blockquote>
        <p> Narrated Wa'il ibn Hujr: </p>
        <p> I saw the Apostle of Allah (peace_be_upon_him) raising his thumbs in prayer up to the lobes of his ears. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0737"> Book 3, Number 0737: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) uttered the takbir (Allah is most great) for prayer (in the beginning), he raised his hands opposite to his shoulders; and when he bowed, he did like that; and when he raised his head to prostrate, he did like that; and when he got up at the end of two rak'ahs, he did like that. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0738"> Book 3, Number 0738: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Maymun al-Makki said: that he saw Abdullah ibn az-Zubayr leading in prayer. He pointed with his hands (i.e. raised his hands opposite to the shoulders) when he stood up, when he bowed and when he prostrated, and when he got up after prostration, he pointed with his hands (i.e. raised his hands). </p>
        <p> The I went to Ibn Abbas and said (to him) I saw Ibn az-Zubayr praying that I never saw anyone praying. I then told him about the pointing with his hands (raising his hands). He said: If you like to see the prayer of the Apostle of Allah (peace_be_upon_him) follower the prayer as offered by Abdullah ibn az-Zubayr. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0739"> Book 3, Number 0739: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Nadr ibn Kathir as-Sa'di said: Abdullah ibn Tawus prayed at my side in the mosque of al-Khayf. When he made the first prostration, he raised his head after it and raised his hands opposite to his face. This came as something strange for me. I, therefore, said it to Wuhayb ibn Khalid. </p>
        <p> Then Wuhayb ibn Khalid said to him: You are doing a thing that I did not see anyone do. Ibn Tawus then replied: I saw my father doing it, and my father said: I saw Ibn Abbas doing it. I do not know but he said: The Prophet (peace_be_upon_him) used to do it. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0743"> Book 3, Number 0743: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) stood for offering the obligatory prayer, he uttered the takbir (Allah is most great) and raised his hands opposite to his shoulders; and he did like that when he finished recitation (of the Qur'an) and was about to bow; and he did like that when he rose after bowing; and he did not raise his hands in his prayer while he was in his sitting position. </p>
        <p> When he stood up from his prostrations (at the end of two rak'ahs), he raised his hands likewise and uttered the takbir (Allah is most great) and raised his hands so as to bring them up to his shoulders, as he uttered the takbir in the beginning of the prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0745"> Book 3, Number 0745: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> If I were in front of the Prophet (peace_be_upon_him), I would see his armpits. Ibn Mu'adh added that Lahiq said: Do you not see, AbuHurayrah could not stand in front of the Prophet (peace_be_upon_him) while he was praying. Musa added: When he uttered the takbir, he raised his hands. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0746"> Book 3, Number 0746: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) taught us how to pray. He then uttered the takbir (Allah is most great) and raised his hands; when he bowed, he joined his hands and placed them between his knees. When this (report) reached Sa'd, he said: My brother said truly. We used to do this; then we were later on commanded to do this, that is, to place the hands on the knees. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0747"> Book 3, Number 0747: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Alqamah said: Abdullah ibn Mas'ud said: Should I pray in the way the Apostle of Allah (peace_be_upon_him) had performed it? He said: He prayed, raising his hands only once. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0749"> Book 3, Number 0749: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) began prayer, he raised his hands up to his ears, then he did not repeat. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0751"> Book 3, Number 0751: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> I saw that the Apostle of Allah (peace_be_upon_him) raised his hands when he began prayer, but he did not raise them until he finished (prayer). </p>
      </blockquote>
      <hr>
      <em> <a name="003.0752"> Book 3, Number 0752: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) began his prayer, he raised his hands extensively. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0754"> Book 3, Number 0754: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> AbuUthman an-Nahdi said: When Ibn Mas'ud prayed he placed his left hand on the right. The Prophet (peace_be_upon_him) saw him and placed his right hand on his left one. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0755"> Book 3, Number 0755: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> AbuJuhayfah said: Ali said that it is a sunnah to place one hand on the other in prayer below the navel. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0756"> Book 3, Number 0756: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Jarir ad-Dabbi reported: I saw Ali (Allah be pleased with him) catching hold of his left hand) by his right hand on the wrist above the navel. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0757"> Book 3, Number 0757: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> (The established way of folding hands is) to hold the hands by the hands in prayer below the navel. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0758"> Book 3, Number 0758: </a> </em>
      <blockquote>
        <p> Narrated Tawus: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to place his right hand on his left hand, then he folded them strictly on his chest in prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0763"> Book 3, Number 0763: </a> </em>
      <blockquote>
        <p> Narrated Jubayr ibn Mut'im: </p>
        <p> Jabir saw the Apostle of Allah (peace_be_upon_him) observing prayer. (The narrator Amr said: I do not know which prayer he was offering.) </p>
        <p> He (the Prophet) said: Allah is altogether great; Allah is altogether great; Allah is altogether great; and praise be to Allah in abundance; and praise be to Allah is abundance; and praise be to Allah in abundance. Glory be to Allah in the morning and after (saying it three times). I seek refuge in Allah from the accursed devil, from his puffing up (nafkh), his spitting (nafth) and his evil suggestion (hamz). </p>
        <p> He (Amr) said: His nafth it poetry, his nafkh is pride, and his hamz is madness. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0765"> Book 3, Number 0765: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Asim ibn Humayd said: I asked Aisha: By what words the Apostle of Allah (peace_be_upon_him) would begin his supererogatory prayer at night? </p>
        <p> She replied: You ask me about a thing of which no one asked me before you. When he stood up, be uttered the takbir (Allah is most great) ten times, and uttered "Praise be to Allah" ten times, and uttered "Glory be to Allah " ten times, and uttered "There is no god but Allah" ten times, and sought forgiveness ten times, and said: O Allah, forgive me, and guide me, and give me sustenance, and keep me well, and he sought refuge in Allah from the hardship of standing before Allah on the Day of Judgment. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0772"> Book 3, Number 0772: </a> </em>
      <blockquote>
        <p> Narrated Rifa'ah ibn Rafi': </p>
        <p> I offered prayer behind the Apostle of Allah (peace_be_upon_him). Rifa'ah sneezed. The narrator Qutaybah did not mention the name Rifa'ah (but he said: I sneezed). So I said: Praise be to Allah, praise much, good and blessed therein, blessed thereupon, as our Lord likes and is pleased. When the Apostle of Allah (peace_be_upon_him) finished his prayer, he turned and said: Who was the speaker in prayer? He then narrated the rest of the tradition like that of Malik and completed it. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0774"> Book 3, Number 0774: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) got up to pray at night (for tahajjud prayer) he uttered the takbir and then said: "Glory be to Thee, O Allah," and "Praise be to Thee" and "Blessed is Thy name," and Exalted is Thy greatness." and "There is no god but Thee." He then said: "There is no god but Allah" three times; he then said: "Allah is altogether great" three times: "I seek refuge in Allah, All-Hearing and All-Knowing from the accursed devil, from his evil suggestion (hamz), from his puffing up (nafkh), and from his spitting (nafth)" He then recited (the Qur'an). </p>
      </blockquote>
      <hr>
      <em> <a name="003.0775"> Book 3, Number 0775: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) began his prayer, he said: "Glory be to Thee, O Allah," and "Praise be to Thee" and "Blessed is Thy name, and Exalted is Thy greatness, sand there is no god but Allah." </p>
      </blockquote>
      <hr>
      <em> <a name="003.0776"> Book 3, Number 0776: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> I remember two period of silence in prayer, one when the imam said the takbir; and one when he finished reciting the Fatihah and the surah when he was about to bow. But Imran ibn Husayn took it as something strange. So they wrote about it to Ubayy (ibn Ka'b) in Medina. He verified the statement of Samurah. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0778"> Book 3, Number 0778: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub; Ubayy ibn Ka'b: </p>
        <p> Samurah ibn Jundub and Imran ibn Husayn had a discussion (about the periods of silence in prayer). Samurah then said that he remembered two periods of silence from the Apostle of Allah (peace_be_upon_him); one when he uttered the takbir and the other when he finished reciting: "Not of those with whom Thou art angry, nor of those who go astray" (i.7). </p>
        <p> Samurah remembered that, but Imran ibn Husayn rejected it. </p>
        <p> Then they wrote about it to Ubayy ibn Ka'b. He wrote a letter to them and gave a reply to them that Samurah remembered correctly. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0779"> Book 3, Number 0779: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> I remember from the Apostle of Allah (peace_be_upon_him) two periods of silence. Sa'id said: We asked Qatadah: What are those two periods of silence? He said: (one) when he began his prayer, and (one) when he finished the recitation. Then he added: When he finished reciting (the closing verse of the Fatihah): "Not of those with whom Thou art angry, nor of who go astray." </p>
      </blockquote>
      <hr>
      <em> <a name="003.0785"> Book 3, Number 0785: </a> </em>
      <blockquote>
        <p> Narrated Uthman ibn Affan:: </p>
        <p> Yazid al-Farisi said: I heard Ibn Abbas say: I asked Uthman ibn Affan: What moved you to put the (Surah) al-Bara'ah which belongs to the mi'in (surahs) (containing one hundred verses) and the (Surah) al-Anfal which belongs to the mathani (Surahs) in the category of as-sab'u at-tiwal (the first long surah or chapters of the Qur'an), and you did not write "In the name of Allah, the Compassionate, the Merciful" between them? </p>
        <p> Uthman replied: When the verses of the Qur'an were revealed to the Prophet (peace_be_upon_him), he called someone to write them down for him and said to him: Put this verse in the surah in which such and such has been mentioned; and when one or two verses were revealed, he used to say similarly (regarding them). (Surah) al-Anfal is the first surah that was revealed at Medina, and (Surah) al-Bara'ah was revealed last in the Qur'an, and its contents were similar to those of al-Anfal. I, therefore, thought that it was a part of al-Anfal. Hence I put them in the category of as-sab'u at-tiwal (the seven lengthy surahs), and I did not write "In the name of Allah, the Compassionate, the Merciful" between them. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0792"> Book 3, Number 0792: </a> </em>
      <blockquote>
        <p> Narrated Some Companions of the Prophet: </p>
        <p> AbuSalih reported on the authority of some Companions of the Prophet (peace_be_upon_him): The Prophet (peace_be_upon_him) said to a person: what do you say in prayer? </p>
        <p> He replied: I first recite tashahhud (supplication recited in sitting position), and then I say: O Allah, I ask Thee for Paradise, and I seek refuge in Thee from Hell-Fire, but I do not understand your sound and the sound of Mu'adh (what you say or he says in prayer). The Prophet (peace_be_upon_him) said: We too go around it (paradise and Hell-fire). </p>
      </blockquote>
      <hr>
      <em> <a name="003.0804"> Book 3, Number 0804: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Samurah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to recite in the noon and afternoon prayer: "By the Heaven and the Morning Star" (Surah 86) and "By the Heaven , holding mansions of the stars" (Surah 85) and similar surahs of equal length. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0807"> Book 3, Number 0807: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Abdullah ibn Ubaydullah said: I went to Ibn Abbas accompanying some youths of Banu Hashim. We said to one of them: Ask Ibn Abbas: Did the Apostle of Allah (peace_be_upon_him) recite (the Qur'an) in the noon and afternoon prayers? He replied: No. People said to him: Perhaps he might recite the Qur'an quietly. He said: May your face be scratched (a kind of curse)!  This (statement) is worse than the former. </p>
        <p> He was only a servant (of Allah) receiving Commands from Him. He preached (the divine) message which he brought with him. He did not command anything to us (Banu Hashim) specially excluding other people except three points: he commanded us to perform ablution perfectly, and not to accept charity (sadaqah) and not to make pairing of donkey with horse. </p>
      </blockquote>
      <hr>
      <em> <a name="003.0808"> Book 3, Number 0808: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> I do not know whether the Apostle of Allah (peace_be_upon_him) would recite the Qur'an at the noon and afternoon prayer or not. </p>
