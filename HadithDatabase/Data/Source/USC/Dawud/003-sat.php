        <p> Ishaq ibn Abdullah ibn Kinanah reported: Al-Walid ibn Utbah or (according to the version of Uthman) al-Walid ibn Uqbah, the then governor of Medina, sent me to Ibn Abbas to ask him about the prayer for rain offered by the Apostle of Allah (peace_be_upon_him). He said: The Apostle of Allah (peace_be_upon_him) went out wearing old clothes in a humble and lowly manner until he reached the place of prayer. He then ascended the pulpit, but he did not deliver the sermon as you deliver (usually).  He remained engaged in making supplication, showing humbleness (to Allah) and uttering the takbir (Allah is most great). He then offered two rak'ahs of prayer as done on the 'Id (festival). </p>
      </blockquote>
      <hr>
      <em> <a name="003.1164"> Book 3, Number 1164: </a> </em>
      <blockquote>
        <p> Narrated Umayr, the client of AbulLahm: </p>
        <p> Umayr saw the Prophet (peace_be_upon_him) praying for rain at Ahjar az-Zayt near az-Zawra', standing, making supplication, praying for rain and raising his hands in front of his face, but not lifting them above his head. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1165"> Book 3, Number 1165: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The people came to the Prophet (peace_be_upon_him) weeping (due to drought). He said (making supplication): O Allah! give us rain which will replenish us, abundant, fertilising and profitable, not injurious, granting it now without delay. He (the narrator) said: Thereupon the sky became overcast. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1169"> Book 3, Number 1169: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The people complained to the Apostle of Allah (peace_be_upon_him) of the lack of rain, so he gave an order for a pulpit. It was then set up for him in the place of prayer. He fixed a day for the people on which they should come out. </p>
        <p> Aisha said: The Apostle of Allah (peace_be_upon_him), when the rim of the sun appeared, sat down on the pulpit, and having pronounced the greatness of Allah and expressed His praise, he said: You have complained of drought in your homes, and of the delay in receiving rain at the beginning of its season. Allah has ordered you to supplicate Him has and promised that He will answer your prayer. </p>
        <p> Then he said: Praise be to Allah, the Lord of the Universe, the Compassionate, the Merciful, the Master of the Day of Judgment. There is no god but Allah Who does what He wishes. O Allah, Thou art Allah, there is no deity but Thou, the Rich, while we are the poor. Send down the rain upon us and make what Thou sendest down a strength and satisfaction for a time. </p>
        <p> He then raised his hands, and kept raising them till the whiteness under his armpits was visible. He then turned his back to the people and inverted or turned round his cloak while keeping his hands aloft. He then faced the people, descended and prayed two rak'ahs. </p>
        <p> Allah then produced a cloud, and the storm of thunder and lightning came on. Then the rain fell by Allah's permission, and before he reached his mosque streams were flowing. When he saw the speed with which the people were seeking shelter, he (peace_be_upon_him) laughed till his back teeth were visible. </p>
        <p> Then he said: I testify that Allah is Omnipotent and that I am Allah's servant and apostle. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1170"> Book 3, Number 1170: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The people of Medina had a drought during the time of the Prophet (peace_be_upon_him). </p>
        <p> While he was preaching on a Friday, a man stood up and said: Apostle of Allah, the horses have perished, the goats have perished, pray to Allah to give us water. He spread his hands and prayed. </p>
        <p> Anas said: The sky was like a mirror (there was no cloud). Then the wind rose; a cloud appeared (in the sky) and it spread : the sky poured down the water. We came out (from the mosque after the prayer) passing through the water till we reached our homes. The rain continued till the following Friday. The same or some other person stood up and said: Apostle of Allah, the houses have been demolished, pray to Allah to stop it. </p>
        <p> The Apostle of Allah (peace_be_upon_him) smiled and said: (O Allah), the rain may fall around us but not upon us. Then I looked at the cloud which dispersed around Medina just like a crown. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1172"> Book 3, Number 1172: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) prayed for rain, he said: O Allah! Provide water for Thy servants and Thy cattle, display Thy mercy and give life to Thy dead land. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1180"> Book 3, Number 1180: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> When, a boy from the Ansar and I were shooting (arrows) towards two of our targets, the sun was sighted by the people at the height of two or three lances above the horizon. It became black like the black herb called tannumah. </p>
        <p> One of us said to his companion: Let us go to the mosque; by Allah, this incident of the sun will surely bring something new in the community of the Apostle of Allah (peace_be_upon_him). </p>
        <p> As we reached it, we suddenly saw that he (the Prophet) had already come out (of his house). He stepped forward for a long time as much as he could do so in the prayer. But we did not hear his voice. He then performed a bowing and prolonged it as much as he could do in the prayer. But we did not hear his voice. He then prostrated himself with us and prolonged it which he never did in the prayer before. But we did not hear his voice. He then did similarly in the second rak'ah. The sun became bright when he sat after the second rak'ah. Then he uttered the salutation. He then stood up, praised Allah, and extolled Him, and testified that there was no god but Allah and testified that he was His servant and apostle. Ahmad ibn Yunus then narrated the address of the Prophet (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="003.1181"> Book 3, Number 1181: </a> </em>
      <blockquote>
        <p> Narrated Qabisah al-Hilali: </p>
        <p> There was an eclipse of the sun in the time of the Apostle of Allah (peace_be_upon_him). He came out bewildered pulling his garment, and I was in his company at Medina. He prayed two rak'ahs and stood for a long time in them. He then departed and the sun became bright. He then said: There are the signs by means of which Allah, the Exalted, produces dread (in His servants). When you see anything of this nature, then pray as you are praying a fresh obligatory prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1189"> Book 3, Number 1189: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> There was an eclipse of the sun in the time of the Prophet (peace_be_upon_him). He began to pray a series of pairs of rak'ahs enquiring about the sun (at the end of them) till it became clear. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1190"> Book 3, Number 1190: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> There was an eclipse of the sun in the time of the Apostle of Allah (peace_be_upon_him). The Apostle of Allah (peace_be_upon_him) stood up and he was not going to perform bowing till he bowed; and he was not going to raise his head till he raised (after bowing); and he was not going to prostrate himself till he prostrated himself; and he was not going to raise his head till he raised (at the end of prostration); he did similarly in the second rak'ah, he then puffed in the last prostration saying; Fie, Fie! He then said: My Lord, didst Thou not promise me that Thou wouldst not punish them so long as I will remain among them? Didst Thou not promise me that Thou will not punish them so long as they continue to beg pardon of Thee. The Apostle of Allah (peace_be_upon_him) finished the prayer, and the sun was clear. The narrator then narrated the tradition (in full). </p>
      </blockquote>
      <hr>
      <em> <a name="003.1192"> Book 3, Number 1192: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> Ubaydullah ibn an-Nadr reported on the authority of his father: Darkness prevailed in the time of Anas ibn Malik, I came to Anas and said (to him): AbuHamzah, did anything like this happen to you in the time of the Apostle of Allah (peace_be_upon_him)? He replied: Take refuge in Allah. If the wind blew violently, we would run quickly towards the mosque for fear of the coming of the Day of Judgment. </p>
      </blockquote>
      <hr>
      <em> <a name="003.1193"> Book 3, Number 1193: </a> </em>
      <blockquote>
        <p> Narrated Ibn Abbas: </p>
        <p> Ikrimah said: Ibn Abbas was informed that so-and-so, a certain wife of the Prophet (peace_be_upon_him), had died. He fell down prostrating himself. He was questioned: Why do you prostrate yourself this moment? He said: The Apostle of Allah (peace_be_upon_him) said: When you see a portent (an accident), prostrate yourselves. And which portent (accident) can be greater than the death of a wife of the Prophet (peace_be_upon_him). </p>
      </blockquote>
