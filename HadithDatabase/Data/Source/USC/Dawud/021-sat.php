      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 21: </a> </em>
      <h1> <em> Oaths and Vows (Kitab Al-Aiman Wa Al-Nudhur) </em> </h1>
      <hr>
      <em> <a name="021.3236"> Book 21, Number 3236: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone swears a false oath in confinement, he should make his seat in Hell on account of his (act). </p>
      </blockquote>
      <hr>
      <em> <a name="021.3237"> Book 21, Number 3237: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: He who swears an oath in which he tells a lie to take the property of a Muslim by unfair means, will meet Allah while He is angry with him. </p>
        <p> Al-Ash'ath said: I swear by Allah, he said this about me. There was some land between me and a Jew, but he denied it to me; so I presented him to the Prophet (peace_be_upon_him). </p>
        <p> The Prophet (peace_be_upon_him) asked me: Have you any evidence?  I replied: No.  He said to the Jew: Take an oath.  I said: Apostle of Allah, now he will take an oath and take my property.  So Allah, the Exalted, revealed the verse, "As for those who sell the faith they owe to Allah and their own plighted word for a small price, they shall have no portion in the hereafter." </p>
      </blockquote>
      <hr>
      <em> <a name="021.3238"> Book 21, Number 3238: </a> </em>
      <blockquote>
        <p> Narrated Al-Ash'ath ibn Qays: </p>
        <p> A man of Kindah and a man of Hadramawt brought their dispute to the Prophet (peace_be_upon_him) about a land in the Yemen. Al-Hadrami said: Apostle of Allah, the father of this (man) usurped my land and it is in his possession. </p>
        <p> The Prophet asked: Have you any evidence? </p>
        <p> Al-Hadrami replied: No, but I make him swear (that he should say) that he does not know that it is my land which his father usurped from me. </p>
        <p> Al-Kindi became ready to take the oath. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: If anyone usurps the property by taking an oath, he will meet Allah while his hand is mutilated. </p>
        <p> Al-Kindi then said: It is his land. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3240"> Book 21, Number 3240: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: One should not take a false oath at this pulpit of mine even on a green tooth-stick; otherwise he will make his abode in Hell, or Hell will be certain for him. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3242"> Book 21, Number 3242: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not swear by your fathers, or by your mothers, or by rivals to Allah; and swear by Allah only, and swear by Allah only when you are speaking the truth. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3245"> Book 21, Number 3245: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Sa'id ibn Ubaydah said: Ibn Umar heard a man swearing: No, I swear by the Ka'bah. Ibn Umar said to him: I heard the Apostle of Allah (peace_be_upon_him) say: He who swears by anyone but Allah is polytheist. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3247"> Book 21, Number 3247: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Prophet (peace_be_upon_him) said: He who swears by Amanah (faithfulness) is not one of our number. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3248"> Book 21, Number 3248: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said about the futile oath: It is man's speech in his house: No, by Allah, and Yes, by Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3250"> Book 21, Number 3250: </a> </em>
      <blockquote>
        <p> Narrated Suwayd ibn Hanzalah: </p>
        <p> We went out intending (to visit) the Apostle of Allah (peace_be_upon_him) and Wa'il ibn Hujr was with us. His enemy caught him. The people desisted from swearing an oath, but I took an oath that he was my brother. So he left him. We then came to the Apostle of Allah (peace_be_upon_him), and I informed him that the people desisted from taking the oath, but I swore that he was my brother. He said: You spoke the truth: A Muslim is a brother of a Muslim. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3252"> Book 21, Number 3252: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone takes an oath and says: I am free from Islam; now if he is a liar (in his oath), he will not return to Islam with soundness. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3253"> Book 21, Number 3253: </a> </em>
      <blockquote>
        <p> Narrated Yusuf ibn Abdullah ibn Salam: </p>
        <p> I saw that the Prophet (peace_be_upon_him) put a date on a loaf and said: This is a thing eaten with bread (condiments). </p>
      </blockquote>
      <hr>
      <em> <a name="021.3255"> Book 21, Number 3255: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone says when swearing an oath: "If Allah wills," he makes an exception. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3256"> Book 21, Number 3256: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone swears an oath and makes an exception, he may fulfil it if he wishes and break it if he wishes without any accountability for breaking. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3258"> Book 21, Number 3258: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) swore an oath strongly, he said: No, by Him in Whose hand is the soul of AbulQasim. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3259"> Book 21, Number 3259: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) swore an oath, it was: No, and I beg forgiveness of Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3260"> Book 21, Number 3260: </a> </em>
      <blockquote>
        <p> Narrated Laqit ibn Amir: </p>
        <p> We came to the Apostle of Allah (peace_be_upon_him) in a delegation. The Prophet (peace_be_upon_him) then said: By the age of thy god. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3261"> Book 21, Number 3261: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> AbuBakr adjured the Prophet (peace_be_upon_him). The Prophet (peace_be_upon_him) said: Do not adjure an oath. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3266"> Book 21, Number 3266: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Sa'id ibn al-Musayyab said: There were two brothers among the Ansar who shared an inheritance. When one of them asked the other for the portion due to him, he replied: If you ask me again for the portion due to you, all my property will be devoted to the decoration of the Ka'bah. </p>
        <p> Umar said to him: The Ka'bah does not need your property. Make atonement for your oath and speak to your brother. I heard the Apostle of Allah (peace_be_upon_him) say: An oath or vow to disobey the Lord, or to break ties of relationship or about something over which one has no control is not binding on you. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3267"> Book 21, Number 3267: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: A vow is binding in those things by which the pleasure of Allah is sought, and an oath to break ties of relationship is not binding. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3268"> Book 21, Number 3268: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him)  said: An oath or a vow about something over which a human being has no control, and to disobey Allah, and to break ties of relationship is not binding. If anyone takes an oath and then considers something else better than it, he should give it up, and do what is better, for leaving it is its atonement. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3269"> Book 21, Number 3269: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Two men brought their dispute to the Prophet (peace_be_upon_him). The Prophet (peace_be_upon_him) asked the plaintiff to produce evidence, but he had no evidence. So he asked the defendant to swear. He swore by Allah "There is no god but He." </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Yes, you have done it, but you have been forgiven for the sincerity of the statement: "There is no god but Allah." </p>
      </blockquote>
      <hr>
      <em> <a name="021.3273"> Book 21, Number 3273: </a> </em>
      <blockquote>
        <p> Narrated Safiyyah bint Huyayy: </p>
        <p> Ibn Harmalah said: Umm Habib gave us a sa' and told us narration from the nephew of Safiyyah on the authority of Safiyyah that it was the sa' of the Prophet (peace_be_upon_him). </p>
        <p> Anas ibn Ayyad said: I tested it and found its capacity two and half mudd according to the mudd of Hisham. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3277"> Book 21, Number 3277: </a> </em>
      <blockquote>
        <p> Narrated Ash-Sharid ibn Suwayd ath-Thaqafi: </p>
        <p> Sharid's mother left a will to emancipate a believing slave on her behalf. So he came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, my mother left a will that I should emancipate a believing slave for her, and I have a black Nubian slave-girl. He mentioned a tradition about the test of the girl. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3279"> Book 21, Number 3279: </a> </em>
      <blockquote>
        <p> Narrated Ikrimah ibn AbuJahl: </p>
        <p> The Prophet (peace_be_upon_him) said: I swear by Allah, I shall fight against the Quraysh; I swear by Allah, I shall fight against the Quraysh; I swear by Allah, I shall fight against the Quraysh. He then said: "If Allah wills." </p>
      </blockquote>
      <hr>
      <em> <a name="021.3285"> Book 21, Number 3285: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) as saying: No vow must be taken to do an act of disobedience, and the atonement for it is the same as for an oath. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3287"> Book 21, Number 3287: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: No vow must be taken to do an act of disobedience, and the atonement for it is the same as for an oath. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3288"> Book 21, Number 3288: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> Uqbah consulted the Prophet (peace_be_upon_him) about his sister who took a vow to perform hajj barefooted and bareheaded. So he said: Command her to cover her head and to ride, and to fast three days. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3290"> Book 21, Number 3290: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A man came to Prophet (peace_be_upon_him) and said: Apostle of Allah, my sister has taken a vow to perform hajj on foot. The Prophet (peace_be_upon_him) said: Allah gets no good from the affliction your sister imposed on herself, so let her perform hajj riding and make atonement for her oath. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3297"> Book 21, Number 3297: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The sister of Uqbah ibn Amir took a vow that she would perform hajj on foot, and she was unable to do so. The Prophet (peace_be_upon_him) said: Allah is not in need of the walking of your sister. She must ride and offer a sacrificial camel. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3298"> Book 21, Number 3298: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir al-Juhani: </p>
        <p> Uqbah said to the Prophet (peace_be_upon_him): My sister has taken a vow that she will walk to the House of Allah (the Ka'bah). Thereupon he said: Allah will not do anything of the walking of your sister to the House of Allah (i.e. the Ka'bah). </p>
      </blockquote>
      <hr>
      <em> <a name="021.3299"> Book 21, Number 3299: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> A man stood on the day of Conquest (of Mecca) and said: Apostle of Allah, I have vowed to Allah that if He grants conquest of Mecca at your hands, I shall pray two rak'ahs in Jerusalem. </p>
        <p> He replied: Pray here. </p>
        <p> He repeated (his statement) to him and he said: Pray here. </p>
        <p> He again repeated (his statement) to him. </p>
        <p> He (the Prophet) replied: Pursue your own course, then. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3300"> Book 21, Number 3300: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Awf: </p>
        <p> The tradition mentioned above (No.3299) has also been transmitted by Umar ibn AbdurRahman ibn Awf on the authority of his father and the Companions of the Prophet (peace_be_upon_him). </p>
        <p> This version has: "The Prophet (peace_be_upon_him) said: By Him Who sent Muhammad with truth, if you prayed here, this would be sufficient for you like the prayer in Jerusalem." </p>
      </blockquote>
      <hr>
      <em> <a name="021.3302"> Book 21, Number 3302: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A woman made a voyage and vowed that she would fast one month if Allah made her reach her destination with peace and security. Allah made her reach her destination with security but she died before she could fast. Her daughter or sister (the narrator doubted) came to the Apostle of Allah (peace_be_upon_him). So he commanded to fast on her behalf. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3306"> Book 21, Number 3306: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> A woman came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, I have taken a vow to play the tambourine over you. </p>
        <p> He said: Fulfil your vow. </p>
        <p> She said: And I have taken a vow to perform a sacrifice in such a such a place, a place in which people had performed sacrifices  in pre-Islamic times. </p>
        <p> He asked: For an Idol? </p>
        <p> She replied: No. </p>
        <p> He asked: For an image? </p>
        <p> She replied: No. </p>
        <p> He said: Fulfil your vow. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3307"> Book 21, Number 3307: </a> </em>
      <blockquote>
        <p> Narrated Thabit ibn ad-Dahhak: </p>
        <p> In the time of the Prophet (peace_be_upon_him) a man took a vow to slaughter a camel at Buwanah. So he came to the Prophet (peace_be_upon_him) and said: I have taken a vow to sacrifice a camel at Buwanah. </p>
        <p> The Prophet (peace_be_upon_him) asked: Did the place contain any idol worshipped in pre-Islamic times? </p>
        <p> They (the people) said: No. </p>
        <p> He asked: Was any pre-Islamic festival observed there? </p>
        <p> They replied: No. </p>
        <p> The Prophet (peace_be_upon_him) said: Fulfil your vow, for a vow to do an act of disobedience to Allah must not be fulfilled, neither must one do something over which a human being has no control. </p>
      </blockquote>
      <hr>
      <em> <a name="021.3308"> Book 21, Number 3308: </a> </em>
      <blockquote>
        <p> Narrated Maymunah, daughter of Kardam: </p>
        <p> I went out with my father to see the hajj performed by the Apostle of Allah (peace_be_upon_him). I saw the Apostle of Allah (peace_be_upon_him). I fixed my eyes on him. My father came near him while he was riding his she-camel. He had a whip like the whip of scribes. I heard the bedouin and the people say: The whip, the whip. My father came near him and held his foot. She said: He admitted his Prophethood and stood and listened to him. </p>
        <p> He said: Apostle of Allah, I have made a vow that if a son is born to me, I shall slaughter a number of sheep at the end of Buwanah in the dale of hill. </p>
        <p> The narrator said: I do not know (for certain) that she said: Fifty (sheep). </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Does it contain any idol? </p>
        <p> He said: No. Then he said: Fulfil your vow that you have taken for Allah. He then gathered them (i.e. the sheep) and began to slaughter them. A sheep ran away from them. </p>
        <p> He searched for it saying: O Allah, fulfil my vow on my behalf. So he succeeded (in finding it) and slaughtered it. </p>
      </blockquote>
