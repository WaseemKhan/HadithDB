      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 27: </a> </em>
      <h1> <em> Foods (Kitab Al-At'imah) </em> </h1>
      <hr>
      <em> <a name="027.3732"> Book 27, Number 3732: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: He who does not accept an invitation which he receives has disobeyed Allah and His Apostle of, and he who enters without invitation enters as a thief and goes out as a raider. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3735"> Book 27, Number 3735: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) held a wedding feast) for Safiyyah with meal and dates. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3736"> Book 27, Number 3736: </a> </em>
      <blockquote>
        <p> Narrated Zubayr ibn Uthman: </p>
        <p> The Prophet (peace_be_upon_him) said: The wedding feast on the first day is a duty, that on the second is a good practice, but that on the third day is to make men hear of it and show it to them. Qatadah said: A man told me that Sa'id ibn al-Musayyab was invited (to a wedding feast on the first day and he accepted it. He was again invited on the second day, and he accepted. When he was invited on the third day, he did not accept; he said: They are the people who make men hear of it and show it to them. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3738"> Book 27, Number 3738: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> When the Prophet (peace_be_upon_him) returned to Medina, he would slaughter a camel or a cow. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3740"> Book 27, Number 3740: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Hospitality extend for three days, and what goes beyond that is sadaqah (charity). </p>
      </blockquote>
      <hr>
      <em> <a name="027.3741"> Book 27, Number 3741: </a> </em>
      <blockquote>
        <p> Narrated AbuKarimah: </p>
        <p> The Prophet (peace_be_upon_him) said: It is a duty of every Muslim (to provide hospitality) to a guest for a night. If anyone comes in the morning to his house, it is a debt due to him. If he wishes, he may fulfil it, and if he wishes he may leave it. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3742"> Book 27, Number 3742: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam AbuKarimah: </p>
        <p> The Prophet (peace_be_upon_him) said: If any Muslim is a guest of people and is given nothing, it is the duty of every Muslim to help him to the extent of taking for him from their crop and property for the entertainment of one night. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3744"> Book 27, Number 3744: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Abbas: </p>
        <p> When the verse: "O ye who believe! eat not up your property among yourselves in vanities, but let there be amongst you traffic and trade by mutual good will" was revealed, a man thought it a sin to eat in the house of another man after the revelation of this verse. </p>
        <p> Then this (injunction) was revealed by the verse in Surat an-Nur: "No blame on you whether you eat in company or separately." </p>
        <p> When a rich man (after revelation) invited a man from his people to eat food in his house, he would say: I consider it a sin to eat from it, and he said: a poor man is more entitled to it than I. The Arabic word tajannah means sin or fault. It was then declared lawful to eat something on which the name of Allah was mentioned, and it was made lawful to eat the flesh of an animal slaughtered by the people of the Book. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3745"> Book 27, Number 3745: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) forbade that the food of two people who were rivalling on another should be eaten </p>
      </blockquote>
      <hr>
      <em> <a name="027.3746"> Book 27, Number 3746: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Safinah AbuAbdurRahman said that a man prepared food for Ali ibn AbuTalib who was his guest, and Fatimah said: I wish we had invited the Apostle of Allah (peace_be_upon_him) and he had eaten with us. They invited him, and when he came he put his hands on the side-ports of the door, but when he saw the figured curtain which had been put at the end of the house, he went away. So Fatimah said to Ali: Follow him and see what turned him back. I (Ali) followed him and asked: What turned you back, Apostle of Allah? He replied: It is not fitting for me or for any Prophet to enter a house which is decorated. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3747"> Book 27, Number 3747: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman al-Himyari: </p>
        <p> A companion of the Prophet (peace_be_upon_him) reported him as saying: When two people come together to issue an invitation, accept that of the one whose door is nearer in neighbourhood, but if one of them comes before the other accept the invitation of the one who comes first. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3749"> Book 27, Number 3749: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: Prayer should not be postponed for taking meals nor for any other thing. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3750"> Book 27, Number 3750: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Abdullah ibn Ubaydullah ibn Umayr said: I was with my father in the time of Ibn az-Zubayr sitting beside Abdullah ibn Umar. Then Abbad ibn Abdullah ibn az-Zubayr said: We have heard that the evening meal is taken just before the night prayer. Thereupon Abdullah ibn Umar said: Woe to you! what was their evening meal? Do you think it was like the meal of your father? </p>
      </blockquote>
      <hr>
      <em> <a name="027.3751"> Book 27, Number 3751: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) came out from the privy and was presented to him. They (the people) asked: Should we bring you water for ablution? He replied: I have been commanded to perform ablution when I get up for prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3752"> Book 27, Number 3752: </a> </em>
      <blockquote>
        <p> Narrated Salman al-Farsi: </p>
        <p> I read in the Torah that the blessing of food consists in ablution before it. So I mentioned it to the Prophet (peace_be_upon_him). He said: The blessing of food consists in ablution before it and ablution after it. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3753"> Book 27, Number 3753: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) came out from the valley of a mountain where he had eased himself. There were some dried dates on a shield before us. We called him and he ate with us. He did not touch water. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3755"> Book 27, Number 3755: </a> </em>
      <blockquote>
        <p> Narrated Wahshi ibn Harb: </p>
        <p> The Companions of the Prophet (peace_be_upon_him) said: Apostle of Allah (peace_be_upon_him) we eat but we are not satisfied. He said: Perhaps you eat separately. They replied: Yes. He said: If you gather together at your food and mention Allah's name, you will be blessed in it. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3758"> Book 27, Number 3758: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: When one of you eats, he should mention Allah's name; if he forgets to mention  Allah's name at the beginning, he should say: "In the name of Allah at the beginning and at the end of it." </p>
      </blockquote>
      <hr>
      <em> <a name="027.3759"> Book 27, Number 3759: </a> </em>
      <blockquote>
        <p> Narrated Umayyah ibn Makhshi: </p>
        <p> Umayyah was sitting and a man was eating. He did not mention Allah's name until there remained the last morsel. When he raised it to his mouth, he said: In the name of Allah at the beginning and at the end of it. The Prophet (peace_be_upon_him) laughed and said: The devil kept eating along with him, but when he mentioned Allah's name, he vomited what was in his belly. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3761"> Book 27, Number 3761: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was never seen reclining while eating, nor walking with two men at his heels. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3763"> Book 27, Number 3763: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: When one of you eats, he must not eat from the top of the dish, but should eat from the bottom; for the blessing descends from the top of it. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3764"> Book 27, Number 3764: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Busr: </p>
        <p> The Prophet (peace_be_upon_him) had a bowl called gharra'. It was carried by four persons. When the sun rose high, and they performed the forenoon prayer, the bowl in which tharid was prepared was brought, and the people gathered round it. When they were numerous, the Apostle of Allah (peace_be_upon_him) said: Allah has made me a respectable servant, and He did not make me an obstinate tyrant. The Apostle of Allah (peace_be_upon_him) said: Eat from it sides and leave its top, the blessing will be conferred on it </p>
      </blockquote>
      <hr>
      <em> <a name="027.3765"> Book 27, Number 3765: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade two kinds of food: to sit at cloth on which wine is drunk, and to eat by a man while lying on his stomach. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3768"> Book 27, Number 3768: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn AbuSalamah: </p>
        <p> The Prophet (peace_be_upon_him) said: Come near, my son, mention Allah's name, eat with your right hand and eat from what is next to you. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3769"> Book 27, Number 3769: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Do not eat meat with a knife, for it is a foreign practice, but bite it, for it is more beneficial and wholesome. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3770"> Book 27, Number 3770: </a> </em>
      <blockquote>
        <p> Narrated Safwan ibn Umayyah: </p>
        <p> I was eating with the Prophet (peace_be_upon_him) and snatching the meat from the bone with my hand. He said: bring the bone near your mouth, for it is more beneficial and wholesome. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3771"> Book 27, Number 3771: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The bone dearer to the Apostle of Allah (peace_be_upon_him) was the bone of sheep. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3772"> Book 27, Number 3772: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The tradition mentioned above (No. 3771) has also been narrated by Ibn Mas'ud  with a different chain of narrators. </p>
        <p> This version has: The Prophet (peace_be_upon_him) liked the foreleg (of a sheep). Once the foreleg was poisoned, and he thought that the Jews had poisoned it. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3774"> Book 27, Number 3774: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The food the Apostle of Allah (peace_be_upon_him) liked best was tharid made from bread and tharid made from Hays. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3775"> Book 27, Number 3775: </a> </em>
      <blockquote>
        <p> Narrated Qabisah ibn Halb: </p>
        <p> A man asked the Apostle of Allah (peace_be_upon_him): Is there any food from which I should keep myself away? I heard the Apostle of Allah (peace_be_upon_him) say: Anything which creates doubt should not occur in your mind by which you resemble Christianity. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3776"> Book 27, Number 3776: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) prohibited eating the animal which feeds on filth and drinking its milk. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3777"> Book 27, Number 3777: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) prohibited to drink the milk of the animal which feeds on filth. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3778"> Book 27, Number 3778: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade riding the camel which feeds on filth and drinking its milk. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3780"> Book 27, Number 3780: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> On the day of Khaybar we slaughtered horses, mules, and assess. The Apostle of Allah (peace_be_upon_him) forbade us (to eat) mules and asses, but he did not forbid horse-flesh. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3781"> Book 27, Number 3781: </a> </em>
      <blockquote>
        <p> Narrated Khalid ibn al-Walid: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade us to eat horse-flesh, the flesh of mules and of asses. The narrator Haywah added: Every beast of prey with a fang. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3786"> Book 27, Number 3786: </a> </em>
      <blockquote>
        <p> Narrated Thabit ibn Wadi'ah: </p>
        <p> We were in an army with the Apostle of Allah (peace_be_upon_him). We got some lizards. I roasted one lizard and brought it to the Apostle of Allah (peace_be_upon_him) and placed it before him. He took a stick and counted its fingers. He then said: A group from the children of Isra'il was transformed into an animal of the land, and I do not know which animal it was. He did not eat it nor did he forbid (its eating). </p>
      </blockquote>
      <hr>
      <em> <a name="027.3787"> Book 27, Number 3787: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Shibl: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to eat the flesh of lizard. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3788"> Book 27, Number 3788: </a> </em>
      <blockquote>
        <p> Narrated Safinah: </p>
        <p> I ate the flesh of a bustard along with the Prophet (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="027.3789"> Book 27, Number 3789: </a> </em>
      <blockquote>
        <p> Narrated at-Talabb ibn Tha'labah at-Tamimi: </p>
        <p> I accompanied the Apostle of Allah (peace_be_upon_him), but I did not hear about the prohibition of (eating) insects and little creatures of land. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3790"> Book 27, Number 3790: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Numaylah  said: I was with Ibn Umar. He was asked about eating hedgehog. He recited: "Say: I find not in the message received by me by inspiration any (meat) forbidden." An old man who was with him said: I heard AbuHurayrah say: It was mentioned to the Apostle of Allah (peace_be_upon_him). Noxious of the noxious. Ibn Umar said: If the Apostle of Allah (peace_be_upon_him) had said it, it is as he said that we did not know. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3791"> Book 27, Number 3791: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The people of pre-Islamic times used to eat some things and leave others alone, considering them unclean. Then Allah sent His Prophet (peace_be_upon_him) and sent down His Book, marking some things lawful and others unlawful; so what He made lawful is lawful, what he made unlawful is unlawful, and what he said nothing about is allowable. And he recited: "Say: I find not in the message received by me by inspiration any (meat) forbidden to be eaten by one who wishes to eat it...." up to the end of the verse. </p>
      </blockquote>
      <hr>
