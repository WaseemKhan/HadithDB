      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> I asked the Apostle of Allah (peace_be_upon_him) about the hyena. He replied: It is game, and if one who is wearing ihram (pilgrim's robe) hunts it, he should give a sheep as atonement. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3795"> Book 27, Number 3795: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam ibn Ma'dikarib: </p>
        <p> The Prophet (peace_be_upon_him) said: Beware, the fanged beast of prey is not lawful, nor the domestic asses, nor the find from the property of a man with whom treaty has been concluded, except that he did not need it. If anyone is a guest of people who provide no hospitality for him, he is entitled to take from them the equivalent of the hospitality due to him. </p>
      </blockquote>
