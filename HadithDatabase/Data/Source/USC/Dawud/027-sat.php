      <em> <a name="027.3806"> Book 27, Number 3806: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: What the sea throws up and is left by the tide you may eat, but what dies in the sea and floats you must not eat. </p>
      </blockquote>
