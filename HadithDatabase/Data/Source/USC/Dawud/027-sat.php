      <blockquote>
        <p> Narrated Khalid ibn al-Walid: </p>
        <p> I went with the Apostle of Allah (peace_be_upon_him) to fight at the battle of Khaybar, and the Jews came and complained that the people had hastened to take their protected property (as a booty), so the Apostle of Allah (peace_be_upon_him) said: The property of those who have been given a mules, every fanged beast of prey, and every bird with a talon are forbidden for you. </p>
      </blockquote>
