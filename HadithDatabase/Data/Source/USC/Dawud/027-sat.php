      </blockquote>
      <hr>
      <em> <a name="027.3815"> Book 27, Number 3815: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah ibn al-Yaman: </p>
        <p> Zirr ibn Hubaysh said: Hudhayfah traced, I think, to the Apostle of Allah (peace_be_upon_him) the saying: He who spits in the direction of the qiblah will come on the Day of Resurrection in the state that his saliva will be between his eyes; and he who eats from this noxious vegetable should not come near our mosque, saying it three times. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3816"> Book 27, Number 3816: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: He who eats from this plant should not come near the mosques. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3817"> Book 27, Number 3817: </a> </em>
      <blockquote>
        <p> Narrated Al-Mughirah ibn Shu'bah: </p>
        <p> I ate garlic and came to the place where the Prophet (peace_be_upon_him) was praying; one rak'ah of prayer had been performed when I joined. When I entered the mosque, the Prophet (peace_be_upon_him) noticed the odour of garlic. </p>
        <p> When the Apostle of Allah (peace_be_upon_him) finished his prayer, he said: He who eats from this plant should not come near us until its odour has gone away. </p>
        <p> When I finished the prayer, I came to the Apostle of Allah (peace_be_upon_him) and said: Apostle of Allah, do give me your hand. Then I put his hand in the sleeve of my shirt, carrying it to my chest to show that my chest was fastened with a belt.  He said: You have a (valid) excuse. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3818"> Book 27, Number 3818: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn Qurrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade these two plants (i.e. garlic and onions), and he said: He who eats them should not come near our mosque. If it is necessary to eat them, make them dead by cooking, that is, onions and garlic. </p>
      </blockquote>
