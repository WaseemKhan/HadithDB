      <em> <a name="027.3809"> Book 27, Number 3809: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: I wish I had a white loaf made from tawny and softened with clarified butter and milk. A man from among the people got up and getting one brought it. He asked: In which had it been? He replied: In a lizard skin. He said: Take it away. </p>
      </blockquote>
      <hr>
      <em> <a name="027.3810"> Book 27, Number 3810: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) was brought a piece of cheese in Tabuk. He called for a knife, mentioned Allah's name and cut it. </p>
      </blockquote>
