        <p> We faced a famine, and I had nothing from my property which I could feed my family ex except a few asses, and the Prophet (peace_be_upon_him) forbade the flesh of domestic asses. So I came to the Prophet (peace_be_upon_him) and said: Apostle of Allah (may peace be upon) , we are suffering from famine, and I have no property which I feed my family except some fat asses, and you have forbidden the flesh of domestic asses. He said: Feed your family on the fat asses of yours, for I forbade them on account of the animal which feeds on the filth of the town, that is, the animal which feeds on filth. </p>
      </blockquote>
      <hr>
