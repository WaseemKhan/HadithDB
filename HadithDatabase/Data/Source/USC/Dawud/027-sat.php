      <em> <a name="027.3807"> Book 27, Number 3807: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Samurah: </p>
        <p> A man alighted at Harrah with his wife and children.  A man said (to him): My she-camel has strayed; if you find it, detain it. </p>
        <p> He found it, but did not find its owner, and it fell ill. His wife said: Slaughter it. But he refused and it died. </p>
        <p> She said: Skin it so that we may dry its fat and flesh and then eat them. </p>
        <p> He said: Let me ask the Apostle of Allah (peace_be_upon_him). So he came to him (the Prophet) and asked him.  He said: Have you sufficient for your needs?  He replied: No.  He then said: Then eat it. </p>
        <p> Then its owner came and he told him the story.  He said: Why did you not slaughter it? He replied: I was ashamed (or afraid) of you. </p>
      </blockquote>
