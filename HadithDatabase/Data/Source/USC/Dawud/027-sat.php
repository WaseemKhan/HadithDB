      <em> <a name="027.3796"> Book 27, Number 3796: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> On the day of Khaybar the Apostle of Allah (peace_be_upon_him) prohibited eating every beast of prey, and every bird with a talon. </p>
