      <blockquote>
        <p> Narrated Salman al-Farsi: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was asked about (eating) locusts. He replied: They are the most numerous of Allah's hosts. I neither eat them nor declare them unlawful. </p>
      </blockquote>
