      <blockquote>
        <p> Narrated Al-Faji' ibn Abdullah al-Amiri: </p>
        <p> Al-Faji' came to the Apostle of Allah (peace_be_upon_him) and asked: Is not dead meat lawful for us? He said: What is your food? We said: Some food in the evening and some in the morning. AbuNu'aym said: Uqbah explained it to me saying: a cup (of milk) in the morning and a cup in the evening; this does not satisfy the hunger. So made the carrion lawful for them in this condition. </p>
      </blockquote>
