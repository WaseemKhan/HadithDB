      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 11: </a> </em>
      <h1> <em> Marriage (Kitab Al-Nikah) </em> </h1>
      <hr>
      <em> <a name="011.2044"> Book 11, Number 2044: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A man came to the Prophet (peace_be_upon_him), and said: My wife does not prevent the hand of a man who touches her. He said: Divorce her. He then said: I am afraid my inner self may covet her. He said: Then enjoy her. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2045"> Book 11, Number 2045: </a> </em>
      <blockquote>
        <p> Narrated Ma'qil ibn Yasar: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and said: I have found a woman of rank and beauty, but she does not give birth to children. Should I marry her? He said: No. He came again to him, but he prohibited him. He came to him third time, and he (the Prophet) said: Marry women who are loving and very prolific, for I shall outnumber the peoples by you. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2046"> Book 11, Number 2046: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> Marthad ibn AbuMarthad al-Ghanawi used to take prisoners (of war) from Mecca (to Medina). At Mecca there was a prostitute called Inaq who had illicit relations with him. (Marthad said:) I came to the Prophet (peace_be_upon_him) and said to him: May I marry Inaq, Apostle of Allah? The narrator said: He kept silence towards me. Then the verse was revealed:"....and the adulteress none shall marry save and adulterer or an idolater." He called me and recited this (verse) to me, and said: Do not marry her. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2047"> Book 11, Number 2047: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The adulterer who has been flogged shall not marry save the one like him. AbuMa'mar said: Habib al-Mu'allim narrated (this tradition) to us on the authority of Amr ibn Shu'ayb. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2050"> Book 11, Number 2050: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) said: What is unlawful by reason of consanguinity is unlawful by reason of fosterage. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2052"> Book 11, Number 2052: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Aflah ibn AbulQu'ays entered upon me. I hid myself from him. He said: You are hiding yourself from me while I am your paternal uncle. She said: I said: From where? He said: The wife of my brother suckled you. She said: The woman suckled me and not the man. Thereafter the Apostle of Allah (peace_be_upon_him) entered upon me and I told him this matter. He said: He is your paternal uncle; he may enter upon you. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2059"> Book 11, Number 2059: </a> </em>
      <blockquote>
        <p> Narrated Hajjaj ibn Malik al-Aslami: </p>
        <p> I asked: Apostle of Allah, what will remove from me the obligation due for fostering a child? He said: A slave or a slave-woman. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2062"> Book 11, Number 2062: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) abominated the combination of paternal and maternal aunts and the combination of two maternal aunts and two paternal aunts in marriage. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2068"> Book 11, Number 2068: </a> </em>
      <blockquote>
        <p> Narrated Saburah ibn Ma'bad al-Juhani: </p>
        <p> The Apostle of Allah (peace_be_upon_him) prohibited temporary marriage with women. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2070"> Book 11, Number 2070: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah: </p>
        <p> AbdurRahman ibn Hurmuz al-A'raj said: Al-Abbas ibn Abdullah ibn al-Abbas married his daughter to AbdurRahman ibn al-Hakam, and AbdurRahman married his daughter to him. And they made this (exchange) their dower. Mu'awiyah wrote to Marwan commanding him to separate them. He wrote in his letter: This is the shighar which the Apostle of Allah (peace_be_upon_him) has forbidden. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2071"> Book 11, Number 2071: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> (The narrator Isma'il said: I think ash-Sha'bi attributed this tradition to the Prophet) </p>
        <p> The Prophet (peace_be_upon_him) said: Curse be upon the one who marries a divorced woman with the intention of making her lawful for her former husband and upon the one for whom she is made lawful. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2073"> Book 11, Number 2073: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If any slave marries without the permission of his masters, he is a fornicator. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2076"> Book 11, Number 2076: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: One of you must not ask a woman in marriage when his brother has done so already, and one of you must not sell (his own goods) when his brother has already sold (his goods) except with his permission. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2077"> Book 11, Number 2077: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: When one of you asked a woman in marriage, if he is able to look at what will induce him to marry her, he should do so. He (Jabir) said: I asked a girl in marriage,  I used to look at her secretly, until I looked at what induced me to marry her. I, therefore, married her. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2078"> Book 11, Number 2078: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: The marriage of a woman who marries without the consent of her guardians is void. (He said these words) three times. If there is cohabitation, she gets her dower for the intercourse her husband has had. If there is a dispute, the sultan (man in authority) is the guardian of one who has none. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2080"> Book 11, Number 2080: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa: </p>
        <p> The Prophet (peace_be_upon_him) said: There is no marriage without the permission of a guardian. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2081"> Book 11, Number 2081: </a> </em>
      <blockquote>
        <p> Narrated Umm Habibah: </p>
        <p> Ibn Az-Zubayr reported on the authority of Umm Habibah that she was the wife of Ibn Jahsh, but he died, He was among those who migrated to Abyssinia. Negus then married her to the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2083"> Book 11, Number 2083: </a> </em>
      <blockquote>
        <p> Narrated Samurah: </p>
        <p> The Prophet (peace_be_upon_him) said: Any woman who is married by two guardians (to two different men) belongs to the first woman who is married by two guardians (to two different men) belongs to the first of them and anything sold by a man to two persons belongs to the first of them. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2084"> Book 11, Number 2084: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> About the Qur'anic verse: "It is not lawful for you forcibly to inherit the woman (of your deceased kinsmen), nor (that) ye should put constraint upon them. When a man died, his relatives had more right to his wife then her own guardian. If any one of them wanted to marry her, he did so; or they married her (to some other person), and if they did not want to marry her, they did so. So this verse was revealed about the matter. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2088"> Book 11, Number 2088: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: An orphan virgin girl should be consulted about herself; if she says nothing that indicates her permission, but if she refuses, the authority of the guardian cannot be exercised against her will. The full information rest with the tradition narrated by Yazid. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2090"> Book 11, Number 2090: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: Consult women about (the marriage of) their daughters. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2091"> Book 11, Number 2091: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A virgin came to the Prophet (peace_be_upon_him) and mentioned that her father had married her against her will, so the Prophet (peace_be_upon_him) allowed her to exercise her choice. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2095"> Book 11, Number 2095: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: A guardian has no concern with a woman previously married and has no husband, and an orphan girl (i.e. virgin) must be consulted, her silence being her acceptance. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2097"> Book 11, Number 2097: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> AbuHind cupped the Prophet (peace_be_upon_him) in the middle of his head. The Prophet (peace_be_upon_him) said: Banu Bayadah, marry AbuHind (to your daughter), and ask him to marry (his daughter) to you. He said: The best thing by which you treat yourself is cupping. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2098"> Book 11, Number 2098: </a> </em>
      <blockquote>
        <p> Narrated Maymunah, daughter of Kardam: </p>
        <p> I went out along with my father during the hajj performed by the Apostle of Allah (peace_be_upon_him). I saw the Apostle of Allah (peace_be_upon_him). My father came near him; he was riding his she-camel. He stopped there and listened to him. He had a whip like the whip of the teachers. I heard the Bedouin and the people saying: Keep away from the whip. My father came up to him. He caught hold of his foot and acknowledged him (his Prophethood). He stopped and listened to him. </p>
        <p> He then said: I participated in the army of Athran (in the pre-Islamic days). </p>
        <p> The narrator, Ibn al-Muthanna, said: Army of Gathran.  Tariq ibn al-Muraqqa' said: Who will give me a lance and get a reward? </p>
        <p> I asked: What is its reward?  He replied: I shall marry him to my first daughter born to me. So I gave him my lance and then disappeared from him till I knew that a daughter was born to him and she came of age. </p>
        <p> I then came to him and said: Send my wife to me. He swore that he would not do that until I fixed a dower afresh other than that agreed between me and him, and I swore that I should not give him the dower other than that I had given him before. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: How old is she now? </p>
        <p> He said: She has grown old.  He said: I think you should leave her. He said: This put awe and fear into me, and I looked at the Apostle of Allah (peace_be_upon_him). </p>
        <p> When he felt this in me, he said: You will not be sinful, nor will your companion be sinful. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2101"> Book 11, Number 2101: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> AbulAjfa' as-Sulami said: Umar (Allah be pleased with him) delivered a speech to us and said: Do not go to extremes in giving women their dower, for if it represented honour in this world and piety in Allah's sight, the one of you most entitled to do so would have been the Prophet (peace_be_upon_him). The Apostle of Allah (peace_be_upon_him) did not marry any of his wives or gave any of his daughters in marriage for more than twelve uqiyahs. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2102"> Book 11, Number 2102: </a> </em>
      <blockquote>
        <p> Narrated Umm Habibah: </p>
        <p> Urwah reported on the authority of Umm Habibah that she was married to Abdullah ibn Jahsh who died in Abyssinia, so the Negus married her to the Prophet (peace_be_upon_him) giving her on his behalf a dower of four thousand (dirhams). He sent her to the Apostle of Allah (peace_be_upon_him) with Shurahbil ibn Hasanah. AbuDawud said: Hasanah is his mother. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2103"> Book 11, Number 2103: </a> </em>
      <blockquote>
        <p> Narrated Umm Habibah daughter of AbuSufyan: </p>
        <p> Az-Zuhri said: The Negus married Umm Habibah daughter of AbuSufyan to the Apostle of Allah (peace_be_upon_him) for a dower of four thousand dirhams. He wrote it to the Apostle of Allah (peace_be_upon_him) who accepted it. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2105"> Book 11, Number 2105: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone gives as a dower to his wife two handfuls of flour or dates he has made her lawful for him. </p>
        <p> AbuDawud said: This tradition has been narrated by AbdurRahman ibn Mahdi, from Salih ibn Ruman, from AbuzZubayr on the authority of Jabir as his own statement (not going back to the Prophet). It has also been transmitted by AbuAsim from Salih ibn Ruman , from AbuzZubayr on the authority of Jabir who said: During the lifetime of the Apostle of Allah (peace_be_upon_him) we used to contract temporary marriage for a handful of grain. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2109"> Book 11, Number 2109: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Masruq said on the authority of Abdullah ibn Mas'ud: Abdullah (ibn Mas'ud ) was asked about a man who had married a woman without cohabiting with her or fixing any dower for her till he died. Ibn Mas'ud said: She should receive the full dower (as given to women of her class), observe the waiting period ('Iddah), and have her share of inheritance. Thereupon Ma'qil ibn Sinan said: I heard the Apostle of Allah (peace_be_upon_him) giving the same decision regarding Birwa' daughter of Washiq (as the decision you have given). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2111"> Book 11, Number 2111: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Abdullah ibn Utbah ibn Mas'ud said: Abdullah ibn Mas'ud was informed of this story of a man. The people continued to visit him for a month or visited him many times (the narrator was not sure). </p>
        <p> He said: In this matter I hold the opinion that she should receive the type of dower given to women of her class with no diminution or excess, observe the waiting period ('iddah) and have her share of inheritance. If it is erroneous, that is from me and from Satan. Allah and His Apostle are free from its responsibility. Some people from Ashja' got up; among them were al-Jarrah and AbuSinan. </p>
        <p> They said: Ibn Mas'ud, we bear witness that the Apostle of Allah (peace_be_upon_him) gave a decision for us regarding Birwa', daughter of Washiq, to the same effect as the decision you have given. Her husband was Hilal ibn Murrah al-Ashja'i. Thereupon Abdullah ibn Mas'ud was very pleased when his decision agreed with the decision of the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2112"> Book 11, Number 2112: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> The Prophet (peace_be_upon_him) said to a man: Would you like me to marry you to so-and-so? </p>
        <p> He said: Yes.  He also said to the woman: Would you like me to marry you to so-and-so? </p>
        <p> She said: Yes. He then married one to the other. The man had sexual intercourse with her, but he did not fix any dower for her, nor did he give anything to her. He was one of those who participated in the expedition to al-Hudaybiyyah. One part of the expedition to al-Hudaybiyyah had a share in Khaybar. </p>
        <p> When he was nearing his death, he said: The Apostle of Allah (peace_be_upon_him) married me to so-and-so, and I did not fix a dower for her, nor did I give anything to her. I call upon you as witness that I have given my share in Khaybar as her dower. So she took the share and sold it for one lakh (of dirhams). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2113"> Book 11, Number 2113: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) taught us the address in case of some need: </p>
        <p> Praise be to Allah from Whom we ask help and pardon, and in Whom we take refuge from the evils within ourselves. He whom Allah guides has no one who can lead him astray, and he whom He leads astray has no one to guide him. I testify that there is no god but Allah, and I testify that Muhammad is His servant and Apostle. </p>
        <p> "You who believe,...fear Allah by Whom you ask your mutual rights, and reverence the wombs. Allah has been watching you." ..."you who believe, fear Allah as He should be feared, and die only as Muslims" ...."you who believe, fear Allah as He should be feared, and die only as Muslims"....."you who believe, fear Allah and say what is true. He will make your deeds sound, and forgive your sins. He who obeys Allah and His Apostle has achieved a mighty success." </p>
        <p> The narrator, Muhammad ibn Sulayman, did mention the word "inna" (verily). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2114"> Book 11, Number 2114: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) recited the tashahhud....He then narrated the same tradition. In this version after the word "and His Apostle" he added the words: "He has sent him in truth as a bearer of glad tidings and a warner before the Hour. He who obeys Allah and His Prophet is on the right path, and he who disobeys them does not harm anyone except  himself, and he does not harm Allah to the least. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2120"> Book 11, Number 2120: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> When Ali married Fatimah, the Prophet (peace_be_upon_him) said to him: Give her something. He said: I have nothing with me. He said: Where is your Hutamiyyah (coat of mail). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2121"> Book 11, Number 2121: </a> </em>
      <blockquote>
        <p> Narrated A man from the Companion of the Prophet: </p>
        <p> Muhammad ibn AbdurRahman ibn Thawban reported on the authority of a man from the Companions of the Prophet (peace_be_upon_him): When Ali married Fatimah, daughter of the Apostle of Allah (peace_be_upon_him), he intended to have intercourse with her. The Apostle of Allah (peace_be_upon_him) prohibited him to do so until he gave her something. Ali said: I have nothing with me, Apostle of Allah. The Prophet (peace_be_upon_him) said: Give her your coat of mail. So he gave her his coat of mail, and then cohabited with her. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2123"> Book 11, Number 2123: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) commanded me to send a woman to her husband before he gave something to her. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2124"> Book 11, Number 2124: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: A woman who marries on a dower or a reward or a promise before the solemnisation of marriage is entitled to it; and whatever is fixed for her after solemnisation of marriage belongs to whom it is given. A man is more entitled to receive a thing given as a gift on account of his daughter or sister (than other kinds of gifts). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2125"> Book 11, Number 2125: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> When the Prophet (peace_be_upon_him) congratulated a man on his marriage, he said: May Allah bless for you, and may He bless on you, and combine both of you in good (works). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2126"> Book 11, Number 2126: </a> </em>
      <blockquote>
        <p> Narrated Basrah: </p>
        <p> A man from the Ansar called Basrah said: I married a virgin woman in her veil. When I entered upon her, I found her pregnant. (I mentioned this to the Prophet). The Prophet (peace_be_upon_him) said: She will get the dower, for you made her vagina lawful for you. The child will be your slave. When she has begotten (a child), flog her (according to the version of al-Hasan). The version of Ibn AbusSari has: You people, flog her, or said: inflict hard punishment on him. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2128"> Book 11, Number 2128: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When a man has two wives and he is inclined to one of them, he will come on the Day of resurrection with a side hanging down. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2129"> Book 11, Number 2129: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to divide his time equally and said: O Allah, this is my division concerning what I possess, so do not blame me concerning what Thou possessest and I do not. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2135"> Book 11, Number 2135: </a> </em>
      <blockquote>
        <p> Narrated Qays ibn Sa'd: </p>
        <p> I went to al-Hirah and saw them (the people) prostrating themselves before a satrap of theirs, so I said: The Apostle of Allah (peace_be_upon_him) has most right to have prostration made before him. When I came to the Prophet (peace_be_upon_him), I said: I went to al-Hirah and saw them prostrating themselves before a satrap of theirs, but you have most right, Apostle of Allah, to have (people) prostrating themselves before you. He said: Tell me , if you were to pass my grave, would you prostrate yourself before it? I said: No. He then said: Do not do so. If I were to command anyone to make prostration before another I would command women to prostrate themselves before their husbands, because of the special right over them given to husbands by Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2137"> Book 11, Number 2137: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah al-Qushayri: </p>
        <p> Mu'awiyah asked: Apostle of Allah, what is the right of the wife of one of us over him? He replied: That you should give her food when you eat, clothe her when you clothe yourself, do not strike her on the face, do not revile her or separate yourself from her except in the house. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2138"> Book 11, Number 2138: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn Haydah: </p>
        <p> I said: Apostle of Allah, how should we approach our wives and how should we leave them? He replied: Approach your tilth when or how you will, give her (your wife) food when you take food, clothe when you clothe yourself, do not revile her face, and do not beat her. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2139"> Book 11, Number 2139: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah al-Qushayri: </p>
        <p> I went to the Apostle of Allah (peace_be_upon_him) and asked him: What do you say (command) about our wives? He replied: Give them food what you have for yourself, and clothe them by which you clothe yourself, and do not beat them, and do not revile them. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2141"> Book 11, Number 2141: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn AbuDhubab: </p>
        <p> Iyas ibn Abdullah ibn AbuDhubab reported the Apostle of Allah (peace_be_upon_him) as saying: Do not beat Allah's handmaidens, but when Umar came to the Apostle of Allah (peace_be_upon_him) and said: Women have become emboldened towards their husbands, he (the Prophet) gave permission to beat them. Then many women came round the family of the Apostle of Allah (peace_be_upon_him) complaining against their husbands. So the Apostle of Allah (peace_be_upon_him) said: Many women have gone round Muhammad's family complaining against their husbands. They are not the best among you. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2142"> Book 11, Number 2142: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> The Prophet (peace_be_upon_him) said: A man will not be asked as to why he beat his wife. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2144"> Book 11, Number 2144: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Prophet (peace_be_upon_him) said: to Ali: Do not give a second look, Ali, (because) while you are not to blame for the first, you have no right to the second. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2153"> Book 11, Number 2153: </a> </em>
      <blockquote>
        <p> Narrated Ruwayfi' ibn Thabit al-Ansari: </p>
        <p> Should I tell you what I heard the Apostle of Allah (peace_be_upon_him) say on the day of Hunayn: It is not lawful for a man who believes in Allah and the last day to water what another has sown with his water (meaning intercourse with women who are pregnant); it is not lawful for a man who believes in Allah and the Last Day to have intercourse with a captive woman till she is free from a menstrual course; and it is not lawful for a man who believes in Allah and the Last Day to sell spoil till it is divided. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2155"> Book 11, Number 2155: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him)  said: If one of you marries a woman or buys a slave, he should say: "O Allah, I ask Thee for the good in her, and in the disposition Thou hast given her; I take refuge in Thee from the evil in her, and in the disposition Thou hast given her." When he buys a camel, he should take hold of the top of its hump and say the same kind of thing. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2157"> Book 11, Number 2157: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who has intercourse with his wife through her anus is accursed. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2159"> Book 11, Number 2159: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Abbas: </p>
        <p> Ibn Umar misunderstood (the Qur'anic verse, "So come to your tilth however you will")--may Allah forgive him. The fact is that this clan of the Ansar, who were idolaters, lived in the company of the Jews who were the people of the Book. They (the Ansar) accepted their superiority over themselves in respect of knowledge, and they followed most of their actions. The people of the Book (i.e. the Jews) used to have intercourse with their women on one side alone (i.e. lying on their backs). This was the most concealing position for (the vagina of) the women. This clan of the Ansar adopted this practice from them. But this tribe of the Quraysh used to uncover their women completely, and seek pleasure with them from in front and behind and laying them on their backs. </p>
        <p> When the muhajirun (the immigrants) came to Medina, a man married a woman of the Ansar. He began to do the same kind of action with her, but she disliked it, and said to him: We were approached on one side (i.e. lying on the back); do it so, otherwise keep away from me. This matter of theirs spread widely, and it reached the Apostle of Allah (peace_be_upon_him). </p>
        <p> So Allah, the Exalted, sent down the Qur'anic verse: "Your wives are a tilth to you, so come to your tilth however you will," i.e. from in front, from behind or lying on the back. But this verse meant the place of the delivery of the child, i.e. the vagina. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2161"> Book 11, Number 2161: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> I and the Apostle of Allah (peace_be_upon_him) used to lie in one cloth at night while I was menstruating. If anything from me smeared him, he washed the same place (that was smeared), and did not wash beyond it. If anything from him smeared his clothe, he washed the same place and did not wash beyond that, and prayed with it (i.e. the clothe). </p>
      </blockquote>
      <hr>
      <em> <a name="011.2163"> Book 11, Number 2163: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said about a man who has sexual intercourse with a menstruating woman: He should give one or half dinar as sadaqah. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2164"> Book 11, Number 2164: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> If a man has sexual intercourse (with menstruating woman) during her bleeding, he should give one dinar as sadaqah, and if he does so when bleeding has stopped, he should give half a dinar as sadaqah. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2166"> Book 11, Number 2166: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> A man said: Apostle of Allah, I have a slave-girl and I withdraw the penis from her (while having intercourse), and I dislike that she becomes pregnant. I intend (by intercourse) what the men intend by it. </p>
        <p> The Jews say that withdrawing the penis (azl) is burying the living girls on a small scale. He (the Prophet) said: The Jews told a lie. If Allah intends to create it, you cannot turn it away. </p>
      </blockquote>
      <hr>
      <em> <a name="011.2169"> Book 11, Number 2169: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> AbuNadrah reported: An old man of Tufawah said to me: I was a guest of AbuHurayrah at Medina. I did not find any one of the companions of the Prophet (peace_be_upon_him) more devoted to worship and more hospitable than AbuHurayrah. </p>
        <p> One day I was with him when he was sitting on his bed. He had a purse which contained pebbles or kernels. A black slave-girl of his was sitting below. Counting them he was glorifying Allah. When the pebbles or the kernels in the purse were finished, she gathered them and put them again in the purse, and gave it to him. He said: Should I not tell you about me and about the Apostle of Allah (peace_be_upon_him)? </p>
        <p> I said: Yes.  He said: Once when I was laid up with fever in the mosque, the Apostle of Allah (peace_be_upon_him) came and entered the mosque, and said: Who saw the youth of ad-Daws. He said this three times. </p>
        <p> A man said: Apostle of Allah, there he is, laid up with fever on one side of the mosque. He moved, walking forward till he reached me. He placed his hand on me. He had a kind talk with me, and I rose. He then began to walk till he reached the place where he used to offer his prayer. He paid his attention to the people. There were two rows of men and one row of women, or two rows of women and one row of men (the narrator is doubtful). </p>
        <p> He then said: If Satan makes me forget anything during the prayer, the men should glorify Allah, and the women should clap their hands. The Apostle of Allah (peace_be_upon_him) then prayed and he did not forget anything during the prayer. </p>
        <p> He said: Be seated in your places, be seated in your places. The narrator, Musa, added the word "here". He then praised Allah and exalted Him, and said: Now to our topic. </p>
        <p> The agreed version begins: He then said: Is there any man among you who approaches his wife, closes the door, covers himself with a curtain, and he is concealed with the curtain of Allah? </p>
        <p> They replied: Yes.  He said: later he sits and says: I did so-and-so; I did so-and-so. The people kept silence. He then turned to the women and said (to them): Is there any woman among you who narrates it? They kept silence. Then a girl fell on one of her knees. The narrator, Mu'ammil, said in his version: a buxom girl. She raised her head before the Apostle of Allah (peace_be_upon_him) so that he could see her and listen to her. </p>
        <p> She said: Apostle of Allah, they (the men) describe the secrets (of intercourse) and they (the women) also describe the secrets (of intercourse) to the people. </p>
        <p> He said: Do you know what the similitude is? He said: The likeness of this act is the likeness of a female Satan who meets the male Satan on the roadside; he fulfils his desire with her while the people are looking at him. Beware! The perfume of men is that whose smell becomes visible and its colour does not appear. Beware! The perfume of women is that whose colour becomes visible and whose smell is not obvious. </p>
        <p> AbuDawud said: From here I remembered this tradition from Mu'ammil and Musa: Beware! No man should lie with another man, no woman should lie with another woman except with one's child or father. He also mentioned a third which I have forgotten. This has been mentioned in the version of Musaddad, but I do not remember it as precisely as I like. </p>
        <p> The narrator, Musa, said: Hammad narrated this tradition from al-Jarir from AbuNadrah from at-Tufawi. </p>
      </blockquote>
