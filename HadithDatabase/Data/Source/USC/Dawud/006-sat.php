      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 6: </a> </em>
      <h1> <em> Prayer (Kitab Al-Salat): Detailed Injunctions about Ramadan </em> </h1>
      <hr>
      <em> <a name="006.1370"> Book 6, Number 1370: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> We fasted with the Apostle of Allah (peace_be_upon_him) during Ramadan, but he did not make us get up at night for prayer at any time during the month till seven nights remained; then he made us get up for prayer till a third of the night had passed. When the sixth remaining night came, he did not make us get up for prayer. When the fifth remaining night came, he made us stand in prayer till a half of the night had gone. </p>
        <p> So I said: Apostle of Allah, I wish you had led us in supererogatory prayers during the whole of tonight. </p>
        <p> He said: When a man prays with an imam till he goes he is reckoned as having spent a whole night in prayer. On the fourth remaining night he did not make us get up. When the third remaining night came, he gathered his family, his wives, and the people and prayed with us till we were afraid we should miss the falah (success). </p>
        <p> I said: What is falah?  He said: The meal before daybreak. Then he did not make us get up for prayer during the remainder of the month. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1374"> Book 6, Number 1374: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Unays: </p>
        <p> I was present at the gathering of Banu Salamah, and I was the youngest of them. </p>
        <p> They (the people) said: Who will ask the Apostle of Allah (peace_be_upon_him) for us about Laylat al-Qadr? That was the twenty-first of Ramadan. I went out and said the sunset prayer along with the Apostle of Allah (peace_be_upon_him). I then stood at the door of his house. </p>
        <p> He passed by me and said: Come in. I entered (the house) and dinner was brought for him. I was prevented from taking food as it was scanty. </p>
        <p> When he finished his dinner, he said to me: Give me my shoes. He then stood up and I also stood up with him. He said: Perhaps you have some business with me. </p>
        <p> I said: Yes. Some people of Banu Salamah have sent me to you to ask you about Laylat al-Qadr. He asked: Which night: Is it tonight? </p>
        <p> I said: Twenty-second.  He said: This is the very night. He then withdrew and said: Or the following night, referring to the twenty-third night. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1375"> Book 6, Number 1375: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Unays al-Juhani: </p>
        <p> I said to the Apostle of Allah: I have a place in the desert where I live and in which I pray, with the praise of Allah; but give me command about a night when I come to this mosque. </p>
        <p> He replied: Come on the twenty third night. </p>
        <p> I (a sub-narrator, Muhammad ibn Ibrahim) said to his (Abdullah ibn Unays's) son: How would your father act? </p>
        <p> He replied: He used to enter the mosque when he had offered the afternoon prayer, and did not leave it for any purpose till he prayed the morning prayer. Then when he had prayed the morning prayer, he found his riding beast at the door of the mosque, mounted it and got back to his desert. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1378"> Book 6, Number 1378: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: Seek it (laylat al-Qadr) in the last ten days of Ramadan. Seek it on the ninth, seventh and fifth night. I (AbuNadrah) said: You know counting better than us, AbuSa'id. He said: Yes. I asked: What do you mean by the ninth, seventh and fifth night? He said: When the twenty-first night passes, the night which follows it is the night; when the twenty-third night passes, the night which follows it is the seventh; when the twenty-fifth passes, the night which follows it is the fifth. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1379"> Book 6, Number 1379: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to us: Seek it (laylat al-Qadr) on the seventeenth night of Ramadan, and on the twenty first night, and on the twenty-third night. He then kept silence. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1385"> Book 6, Number 1385: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> Yazid ibn Abdullah said that Abdullah ibn Amr asked the Prophet (peace_be_upon_him): In how many days should I complete the recitation of the whole Qur'an, Apostle of Allah? </p>
        <p> He replied: In one month. </p>
        <p> He said: I am more energetic to complete it in a period less than this. He kept on repeating these words and lessening the period until he said: Complete its recitation in seven days. </p>
        <p> He again said: I am more energetic to complete it in a period less than this. </p>
        <p> The Prophet (peace_be_upon_him) said: He who finishes the recitation of the Qur'an in less than three days does not understand it. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1388"> Book 6, Number 1388: </a> </em>
      <blockquote>
        <p> Narrated Aws ibn Hudhayfah: </p>
        <p> We came upon the Apostle of Allah (peace_be_upon_him) in a deputation of Thaqif. The signatories of the pact came to al-Mughirah ibn Shu'bah as his guests. The Apostle of Allah (peace_be_upon_him) made Banu-Malik stay in a tent of his. </p>
        <p> Musaddad's version says: He was in the deputation of Thaqif which came to the Apostle of Allah (peace_be_upon_him). He used to visit and have a talk with us every day after the night prayer. </p>
        <p> The version of AbuSa'id says: He remained standing for such a long time (talking to us) that he put his weight sometimes on one leg and sometimes on the other due to his long stay. He mostly told us how his people, the Quraysh, behaved with him. </p>
        <p> He would say: We were not equal; we were weak and degraded at Mecca (according to Musaddad's version). When we came over to Medina the fighting began between us; sometimes we overcome them and at other times they overcome us. One night he came late and did not come at the time he used to come. </p>
        <p> We asked him: You came late tonight?  He said: I could not recite the fixed part of the Qur'an that I used to recite every day. I disliked to come till I had completed it. </p>
        <p> Aws said: I asked the companions of the Apostle of Allah (peace_be_upon_him): How do you divide the Qur'an for daily recitation?  They said: Three surahs, five surahs, eleven surahs, thirteen surahs' mufassal surahs. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1389"> Book 6, Number 1389: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: He who recites the Qur'an in a period less than three days does not understand it. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1390"> Book 6, Number 1390: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> Wahb ibn Munabbih said: Abdullah ibn Amr asked the Prophet (peace_be_upon_him); In how many days should one complete the recitation of the Qur'an? He said: In forty days. He then said: In one month. He again said: In twenty days. He then said: In fifteen days. He then said: In ten days. Finally he said: In seven days. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1391"> Book 6, Number 1391: </a> </em>
      <blockquote>
        <p> Narrated Ibn Mas'ud: </p>
        <p> Alqamah and al-Aswad said: A man came to Ibn Mas'ud. He said: I recite the mufassal surahs in one rak'ah. You might recite it quickly as one recites verse (poetry) quickly, or as the dried dates fall down (from the tree). </p>
        <p> But the Prophet (peace_be_upon_him) used to recite two equal surahs in one rak'ah; he would recite (for instance) surahs an-Najm (53) and ar-Rahman (55) in one rak'ah, surahs Iqtarabat (54) and al-Haqqah (69) in one rak'ah, surahs at-Tur (52) and adh-Dhariyat (51) in one rak'ah, surahs al-Waqi'ah (56) and Nun (68) in one rak'ah, surahs al-Ma'arij (70) and  an-Nazi'at (79) in one rak'ah, surahs al-Mutaffifin (83) and Abasa (80) in one rak'ah, surahs al-Muddaththir (74) and al-Muzzammil (73) in one rak'ah, surahs al-Insan (76) and al-Qiyamah (75) in one rak'ah, surahs an-Naba' (78) and  al-Mursalat (77) in one rak'ah, and surahs ad-Dukhan (44) and at-Takwir (81) in one rak'ah. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1393"> Book 6, Number 1393: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone prays at night reciting regularly ten verses, he will not be recorded among the negligent; if anyone prays at night and recites a hundred verses, he will be recorded among those who are obedient to Allah; and if anyone prays at night reciting one thousand verses, he will be recorded among those who receive huge rewards. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1394"> Book 6, Number 1394: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr: </p>
        <p> A man came to the Apostle of Allah (peace_be_upon_him) and said: Teach me to read the Qur'an, Apostle of Allah. </p>
        <p> He said: Read three surahs which begin with A.L.R.  He said: My age is advanced, my mind has become dull (i.e. memory has grown weak), and my tongue has grown heavy). So he said: Then read three surahs which begin with H.M. He repeated the same words.  So he said: Read three surahs which begin with the "Glorification of Allah". But he repeated the same excuse.  The man then said: Teach me a comprehensive surah, Apostle of Allah. The Prophet (peace_be_upon_him) taught him Surah (99). "When the Earth is shaken with her earthquake". When he finished it, the man said: By Him Who sent you with truth, I shall never add anything to it. Then man then went away. </p>
        <p> The Prophet (peace_be_upon_him) said twice:  The man received salvation. </p>
      </blockquote>
      <hr>
      <em> <a name="006.1395"> Book 6, Number 1395: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: A surah of the Qur'an containing thirty verses will intercede its reader till he will be forgiven. That is: "Blessed is He in Whose Hand is the sovereignty" (Surah 67). </p>
      </blockquote>
