      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 23: </a> </em>
      <h1> <em> Wages (Kitab Al-Ijarah) </em> </h1>
      <hr>
      <em> <a name="023.3409"> Book 23, Number 3409: </a> </em>
      <blockquote>
        <p> Narrated Ubaydah ibn as-Samit: </p>
        <p> I taught some persons of the people of Suffah writing and the Qur'an. A man of them presented to me a bow. I said: It cannot be reckoned  property; may I shoot with it in Allah's path? I must come to the Apostle of of Allah (peace_be_upon_him) and ask him (about it). So I came to him and said : Apostle of Allah (peace_be_upon_him), one of those whom I have been teaching writing and the Qur'an has presented me a bow, and as it cannot be reckoned property, may I shoot with it in Allah's path? He said: If you want to have a necklace of fire on you, accept it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3413"> Book 23, Number 3413: </a> </em>
      <blockquote>
        <p> Narrated Alaqah ibn Sahar at-Tamimi: </p>
        <p> Alaqah passed by a clan (of the Arab) who came to him and said: You have brought what is good from this man. Then they brought a lunatic in chains. He recited Surat al-Fatihah over him three days, morning and evening. When he finished, he collected his saliva and then spat it out, (he felt relief) as if he were set free from a bond. They gave him something (as wages). He then came to the Prophet (peace_be_upon_him) and mentioned it to him. The Apostle of Allah (peace_be_upon_him) said: Accept it, for by my life, some accept it for a worthless charm, but you have done so far a genuine one. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3414"> Book 23, Number 3414: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> The Prophet (peace_be_upon_him) said: The earnings of a cupper are impure, the price paid for a dog is impure, and the hire paid to a prostitute is impure. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3415"> Book 23, Number 3415: </a> </em>
      <blockquote>
        <p> Narrated Muhayyisah ibn Ka'b: </p>
        <p> Muhayyisah asked permission of the Apostle of Allah (peace_be_upon_him) regarding hire of the cupper, but he forbade him. He kept on asking his permission, and at last he said to him: Feed your watering camel with it and feed your slave with it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3419"> Book 23, Number 3419: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Rifa'ah: </p>
        <p> Tariq ibn AbdurRahman al-Qarashi said: Rafi' ibn Rifa'ah came to a meeting of the Ansar and said: The Prophet of Allah (peace_be_upon_him) forbade us (from some things) today, and he mentioned some things. He forbade the earning of a slave-girl except what she earned with her hand. He indicated (some things) with his fingers such as baking, spinning, and ginning. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3421"> Book 23, Number 3421: </a> </em>
      <blockquote>
        <p> Narrated AbuMas'ud: </p>
        <p> The Prophet (peace_be_upon_him) forbade the price paid for a dog, the hire paid to a prostitute, and the gift given to a soothsayer. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3422"> Book 23, Number 3422: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade (taking hire for) a stallion's covering. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3423"> Book 23, Number 3423: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> AbuMajidah said: I cut the ear of a boy, or he cut my ear (the narrator is doubtful). AbuBakr then came to us to perform hajj and we got together with him. But he referred us to Umar ibn al-Khattab. Umar (ibn al-Khattab) said: This reached the extent of retaliation. Call a cupper to me so that he may retaliate. When the cupper was called, he (Umar) said: I heard the Apostle of Allah (peace_be_upon_him) say: I gave a boy to my maternal aunt, and I hope that she will be blessed in respect of him. I said to her: Do not entrust him to a supper, nor to a goldsmith, nor to a butcher. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3428"> Book 23, Number 3428: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone buys a slave who possesses property, his property belongs to the seller unless the buyer makes a proviso. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3433"> Book 23, Number 3433: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: A townsman must not sell for a man from the desert, even if he is his brother or father. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3434"> Book 23, Number 3434: </a> </em>
      <blockquote>
        <p> Narrated Talhah ibn Ubaydullah: </p>
        <p> Salim al-Makki said that a bedouin told him that he brought a milch she-camel in the time of the Apostle of Allah (peace_be_upon_him). He alighted with Talhah ibn Ubaydullah (and wanted to sell his milch animal to him). He said: The Prophet (peace_be_upon_him) forbade a townsman to sell for a man from the desert. But go to the market and see who buys from you. consult me thereafter, and then I shall ask you (to sell) or forbid you. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3437"> Book 23, Number 3437: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) forbade payment for cat. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3439"> Book 23, Number 3439: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone buys a sheep whose udders have been tied up, he has option for three days (for decision). If he returns it, he should return with it wheat equal to its milk or double of it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3442"> Book 23, Number 3442: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to break the coins of the Muslims current among them except for some defect. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3443"> Book 23, Number 3443: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> A man came and said: Apostle of Allah, fix prices. He said: (No), but I shall pray. Again the man came and said: Apostle of Allah, fix prices. He said: It is but Allah Who makes the prices low and high. I hope that when I meet Allah, none of you has any claim on me for doing wrong regarding blood or property. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3444"> Book 23, Number 3444: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The people said: Apostle of Allah , prices have shot up, so fix prices for us. Thereupon the Apostle of Allah (peace_be_upon_him) said: Allah is the one Who fixes prices, Who withholds, gives lavishly and provides, and I hope that when I meet Allah, none of you will have any claim on me for an injustice regarding blood or property. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3449"> Book 23, Number 3449: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: Both parties in a business transaction have a right to annul it so long as they have not separated unless it is a bargain with the option to annul is attached to it; and it is not permissible for one of them to separate from the other for fear that one may demand that the bargain be rescinded. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3450"> Book 23, Number 3450: </a> </em>
      <blockquote>
        <p> Narrated AbuBarzah: </p>
        <p> AbulWadi', Abbad ibn Nasib said: We fought one of our battle, and encamped at a certain place. One of our companions sold a horse for a slave. After that they remained there for the rest of day and night. When the next morning came, they prepared themselves for departure. The buyer of the horse began to saddle it, but the seller was ashamed (of the transaction). He went to the man (buyer) and asked him to annul the transaction. The man refused to hand it over (the horse) to him. </p>
        <p> He said: AbuBarzah, the companion of the Prophet (peace_be_upon_him), is to decide between me and you. They went to AbuBarzah in the corner of the army. They related this story to him. </p>
        <p> He said: Do you agree that I make a decision between you on the basis of the decision of the Apostle of Allah (peace_be_upon_him)? </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Both parties in a business transaction have an option (right) to annul it so long as they have not separated. </p>
        <p> Hisham to Hassan said that Jamil said in his version: "I do not think that you separated." </p>
      </blockquote>
      <hr>
      <em> <a name="023.3451"> Book 23, Number 3451: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> When AbuZur'ah made a business transaction with a man, he gave him the right of option. He then would tell him: Give me the right of option (to annul the bargain). He said: I heard AbuHurayrah say: The Apostle of Allah (peace_be_upon_him) said: Two people must separate only by mutual consent. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3453"> Book 23, Number 3453: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone rescinds a sale with a Muslim, Allah will cancel his slip, on the Day of Resurrection. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3454"> Book 23, Number 3454: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone makes two transactions combined in one bargain, he should have the lesser of the two or it will involve usury. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3455"> Book 23, Number 3455: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> I heard the Apostle of Allah, (peace_be_upon_him) say: When you enter into the inah transaction, hold the tails of oxen, are pleased with agriculture, and give up conducting jihad (struggle in the way of Allah). Allah will make disgrace prevail over you, and will not withdraw it until you return to your original religion. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3459"> Book 23, Number 3459: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn AbuAwfa ibn AbuAwfa al-Aslami: </p>
        <p> We made a journey to Syria on an expedition along with the Apostle of Allah (peace_be_upon_him). The Nabateans of Syria came to us and we paid in advance to them (in a salam contract) in wheat and olive oil at a specified rate and for a specified time. He asked (by the people): you might have contracted with him who had these things in his possession? He replied: We did not ask them. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3460"> Book 23, Number 3460: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> A man paid in advance for a palm-tree. It did not bear fruit that year. They brought their case for decision to the Prophet (peace_be_upon_him). He said: for which do you make his property lawful? He then said: Do not pay in advance for a palm-tree till they (the fruits) were clearly in good condition. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3461"> Book 23, Number 3461: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone pays in advance he must not transfer it to someone else before he receives it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3470"> Book 23, Number 3470: </a> </em>
      <blockquote>
        <p> Narrated A man: </p>
        <p> A man from the immigrants of the Companions of the Prophet (peace_be_upon_him) said: I participated in battle three times along with the Prophet (peace_be_upon_him). I heard him say: Muslims have common share in three (things). grass, water and fire. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3471"> Book 23, Number 3471: </a> </em>
      <blockquote>
        <p> Narrated Iyas ibn Abd: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade the sale of excess water. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3472"> Book 23, Number 3472: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) forbade payment for dog and cat. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3475"> Book 23, Number 3475: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade the price paid for a dog; if someone comes to ask for the price of a dog, fill his hand-palm with dust. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3477"> Book 23, Number 3477: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The price paid for a dog, the price given to a soothsayer, and the hire paid to a prostitute are not lawful. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3482"> Book 23, Number 3482: </a> </em>
      <blockquote>
        <p> Narrated Al-Mughirah ibn Shu'bah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who sold wine should shear the flesh of swine. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3488"> Book 23, Number 3488: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to sell grain which one buys by measurement until one receives it in full. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3492"> Book 23, Number 3492: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> Ibn Umar said: I bought olive oil in the market. When I became its owner, a man met me and offered good profit for it. I intended to settle the bargain with him, but a man caught hold of my hand from behind. When I turned I found that he was Zayd ibn Thabit. He said: Do not sell it on the spot where you have bought it until you take it to your house, for the Apostle of Allah (peace_be_upon_him) forbade to sell the goods where they are bought until the tradesmen take them to their houses. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3494"> Book 23, Number 3494: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> During the time of the Apostle of Allah (peace_be_upon_him) a man used to buy (goods), and he was weak in his intellect. His people came to the Prophet of Allah (peace_be_upon_him) and said: Prophet of Allah, stop so-and-so (to make a bargain) for he buys (goods), but he is weak in his intellect. So the Prophet (peace_be_upon_him) called on him and forbade him to make a bargain. He said: Prophet of Allah, I cannot keep away myself from business transactions. Thereupon the Apostle of Allah (peace_be_upon_him) said: If you cannot give up making a bargain , then say: Take , and give, and there is no attempt to deceive. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3495"> Book 23, Number 3495: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade the type of transactions in which earnest money was paid. Malik said: This means, as we think--Allah better knows-that a man buys a slave or hires an animal, and he says: I give you a dinar on condition that if I give up the transaction or hire, what I gave you is yours. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3496"> Book 23, Number 3496: </a> </em>
      <blockquote>
        <p> Narrated Hakim ibn Hizam: </p>
        <p> Hakim asked (the Prophet): Apostle of Allah, a man comes to me and wants me to sell him something which is not in my possession. Should I buy it for him from the market? He replied: Do not sell what you do not possess. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3499"> Book 23, Number 3499: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> The Prophet (peace_be_upon_him) said: The contractual obligation of a slave is three days. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3501"> Book 23, Number 3501: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Profit follows responsibility. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3502"> Book 23, Number 3502: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Makhlad ibn Khufaf al-Ghifari said: I and some people were partners in a slave. I employed him on some work in the absence of one of the partners. He got earnings for me. He disputed me and the case of his claim to his share in the earnings to a judge, who ordered me to return the earnings (i.e. his share) to him. I then came to Urwah ibn az-Zubayr, and related the matter to him. Urwah then came to him and narrated to him a tradition from the Apostle of Allah (peace_be_upon_him) on the authority of Aisha: Profit follows responsibility. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3503"> Book 23, Number 3503: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> A man bought a slave, and he remained with him as long as Allah wished him to remain. He then found defect in him. He brought his dispute with him to the Prophet (peace_be_upon_him) and he returned him to him. The man said: Apostle of Allah, my slave earned some wages. The Apostle of Allah (peace_be_upon_him) then said: Profit follows responsibility. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3504"> Book 23, Number 3504: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Muhammad ibn al-Ash'ath said: Al-Ash'ath bought slaves of booty from Abdullah ibn Mas'ud for twenty thousand (dirhams. Abdullah asked him for payment of their price. He said: I bought them for ten thousand (dirhams). Abdullah said: Appoint a man who may adjudicate between me and you. Al-Ash'ath said: (I appoint) you between me and yourself. Abdullah said: I heard the Apostle of Allah (peace_be_upon_him) say: If both parties in a business transaction differ (on the price of an article), and they have witness between them, the statement of the owner of the article will be accepted (as correct) or they may annul the transaction. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3508"> Book 23, Number 3508: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When land has been divided and boundaries have been set up, there is no right of pre-emption in it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3510"> Book 23, Number 3510: </a> </em>
      <blockquote>
        <p> Narrated Samurah: </p>
        <p> The Prophet (peace_be_upon_him) said: A neighbour has the best claim to the house or land of the neighbour. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3511"> Book 23, Number 3511: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: The neighbour is most entitled to the right of pre-emption, and he should wait for its exercise even if he is absent, when the two properties have one road. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3513"> Book 23, Number 3513: </a> </em>
      <blockquote>
        <p> Narrated AbuBakr ibn AbdurRahman ibn al-Harith ibn Hisham: </p>
        <p> The Prophet (peace_be_upon_him) said: If a man sells (his) property and the man who buys it becomes insolvent, and the seller does not receive the price of the property he had sold, but finds his very property with him (i.e. the buyer), he is more entitled to it (than others). If the buyer dies, then the owner of the property is equal to the creditors. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3515"> Book 23, Number 3515: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> A similar tradition (to the No. 3513) has been transmitted by AbuHurayrah from the Prophet (peace_be_upon_him). </p>
        <p> This version has: If he paid something from its price, then he will be equal to the creditors in the remaining price. If a man dies and he has the very property of a man (i.e. seller), he is equal to the creditors whether he (the buyer) pays him (the price) or not. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3516"> Book 23, Number 3516: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> Umar ibn Khaldah said: We came to AbuHurayrah who had become insolvent. He said: I shall decide between you on the basis of the decision of the Apostle of Allah (peace_be_upon_him): If anyone becomes insolvent or dies and the man (the seller) finds his very property with him, he is more entitled to it (than others). </p>
      </blockquote>
      <hr>
      <em> <a name="023.3517"> Book 23, Number 3517: </a> </em>
      <blockquote>
        <p> Narrated Amir ash-Sha'bi: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone finds an animal whose owners were helpless to provide fodder to it and so they turned it out (of their house), and he took it and looked after it, it will belong to him. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3518"> Book 23, Number 3518: </a> </em>
      <blockquote>
        <p> Narrated ash-Sha'bi: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone leaves an animal  at a place of perishing and another man brings it to life, it belongs to him who brings it to life. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3520"> Book 23, Number 3520: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> reported the Prophet (peace_be_upon_him) as saying: There are people from the servants of Allah who are neither prophets nor martyrs; the prophets and martyrs will envy them on the Day of Resurrection for their rank from Allah, the Most High. </p>
        <p> They (the people) asked: Tell us, Apostle of Allah, who are they?  He replied: They are people who love one another for the spirit of Allah (i.e. the Qur'an), without having any mutual kinship and giving property to one. I swear by Allah, their faces will glow and they will be (sitting) in (pulpits of) light. They will have no fear (on the Day) when the people will have fear, and they will not grieve when the people will grieve. </p>
        <p> He then recited the following Qur'anic verse: "Behold! Verily for the friends of Allah there is no fear, nor shall they grieve." </p>
      </blockquote>
      <hr>
      <em> <a name="023.3521"> Book 23, Number 3521: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The aunt of Umarah ibn Umayr asked Aisha: I have an orphan in my guardianship. May I enjoy from his property? She said: The Apostle of Allah (peace_be_upon_him) said: The pleasantest things a man enjoys come from what he earns, and his child comes from what he earns. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3522"> Book 23, Number 3522: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) Said: The children of a man come from what he earns, rather they are his pleasantest earning; so enjoy from their property. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3523"> Book 23, Number 3523: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, I have property and children, and my father finishes my property. He replied; You and your property belong to your father; your children come from the pleasantest of what you earn; so enjoy from the earning of your children. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3524"> Book 23, Number 3524: </a> </em>
      <blockquote>
        <p> Narrated Samurah ibn Jundub: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone finds his very property with a man, he is more entitled to it (than anyone else), and the buyer should pursue the one who sold it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3527"> Book 23, Number 3527: </a> </em>
      <blockquote>
        <p> Narrated Yusuf ibn Malik al-Makki: </p>
        <p> I used to write (the account of) the expenditure incurred on orphans who were under the guardianship of so-and-so. They cheated him by one thousand dirhams and he paid these (this amount) to them. I then got double the property which they deserved. I said (to the man: Take one thousand (dirhams) which they have taken from you (by cheating). He said: No, my father has told me that he heard the Apostle of Allah (peace_be_upon_him) say: Pay the deposit to him who deposited it with you, and do not betray him who betrays you. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3528"> Book 23, Number 3528: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Pay the deposit to him who deposited it with you, and do not betray him who betrayed you. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3530"> Book 23, Number 3530: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: I swear by Allah, I shall not accept gift from anyone after this day except from an immigrant Qarashi, an Ansari a Dawsi or a Thaqafi. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3532"> Book 23, Number 3532: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Umar ;  Abdullah Ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: It is not lawful for a man to make a donation or give a gift and then take it back, except a father regarding what he gives his child. One who gives a gift and then takes it back is like a dog which eats and vomits when it is full, then returns to its vomit. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3533"> Book 23, Number 3533: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: The similitude of the one who takes back what he gifted is like that of a dog which vomits and then it eats vomit. When a donor seeks to take back (his gift), it should be made known and he informed why he sought to take it back. Then whatever he donated should be returned to him. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3534"> Book 23, Number 3534: </a> </em>
      <blockquote>
        <p> Narrated AbuUmamah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone intercedes for his brother and he presents a gift to him for it and he accepts it, he approaches a great door of the doors of usury. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3537"> Book 23, Number 3537: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> The Prophet (peace_be_upon_him) said: Act equally between your children; Act equally between your sons. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3540"> Book 23, Number 3540: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: It is not permissible for a woman to present a gift (from her husband's property) except with the permission of her husband. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3544"> Book 23, Number 3544: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone is given life-tenancy, it belongs to him and to his descendants. His descendants who inherit him will inherit from it. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3549"> Book 23, Number 3549: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not give property to go to the survivor and do not give life-tenancy. If anyone is given something to the survivor or given life-tenancy, it goes to his heirs. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3550"> Book 23, Number 3550: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah  (peace_be_upon_him) decided a case of a woman from the Ansar to whom an orchard of date-palms was given by her son. She then died. Her son said: I gave it to her for her life, and she has brothers. Thereupon the Apostle of Allah (peace_be_upon_him) said: It belongs to her during her life and after death. He then said: I gave a sadaqah (charity to her. He replied: It is more unexpected from you. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3551"> Book 23, Number 3551: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: Life-tenancy is lawful for the one to whom it is given and donation of property to go to the survivor is lawful to whom it is given. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3552"> Book 23, Number 3552: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone gives something in life-tenancy, it belongs to the one to whom it is given, in his life and after his death; and do not give property to go to the survivor, for if anyone gives something to to to the survivor, it belongs to him. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3555"> Book 23, Number 3555: </a> </em>
      <blockquote>
        <p> Narrated Samurah: </p>
        <p> The Prophet (peace_be_upon_him) said: The hand which takes is responsible till it pays. Then Al-Hasan forgot and said: (If you give something on loan to a man), he is your depositor; there is no compensation (for it) on him. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3555a"> Book 23, Number 3555a: </a> </em>
      <blockquote>
        <p> Narrated Safwan ibn Umayyah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) borrowed coats of mail from him on the day of (the battle of) Hunayn. He asked: Are you taking them by force. Muhammad? He replied: No, it is a loan with a guarantee of their return. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3556"> Book 23, Number 3556: </a> </em>
      <blockquote>
        <p> Narrated Some people: </p>
        <p> AbdulAziz ibn Rufay' narrated on the authority of some people from the descendants of Abdullah ibn Safwan who reported the Apostle of Allah (peace_be_upon_him) as saying: Have you weapons, Safwan? He asked: On loan or by force? He replied: No, but on loan. So he lent him coats of mail numbering between thirty and forty! The Apostle of Allah (peace_be_upon_him) fought the battle of Hunayn. When the polytheists were defeated, the coats of mail of Safwan were collected. Some of them were lost. The Apostle of Allah (peace_be_upon_him) said to Safwan: We have lost some coats of mail from your coats of mail. Should we pay compensation to you? He replied: No. Apostle of Allah, for I have in my heart today what I did not have that day. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3558"> Book 23, Number 3558: </a> </em>
      <blockquote>
        <p> Narrated AbuUmamah: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) Said: Allah , Most Exalted, has appointed for everyone who has a right what is due to him, and no will be made to an heir, and a woman should not spend anything from her house except with the permission of her husband. He was asked: Even foodgrain, Apostle of Allah? He replied: That is the best of our property. He then said: A loan must be paid back, a she-camel lent for a time for milking must be returned, a debt must be discharged, one who stands surety is held responsible. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3559"> Book 23, Number 3559: </a> </em>
      <blockquote>
        <p> Narrated Ya'la ibn Umayyah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to me: When my messengers come to you, give them thirty coats of mail, and thirty camels. I asked: Apostle of Allah, is it a loan with a guarantee of its return, or a loan to be paid back? He replied : It is a loan to be paid back. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3561"> Book 23, Number 3561: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> I saw no one cooking food like Safiyyah. She cooked food for the Apostle of Allah (peace_be_upon_him) and sent it. I became angry and broke the vessel. I then asked: Apostle of Allah, what is the atonement for what I have done? He replied: A vessel like (this) vessel and food like (this) food. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3562"> Book 23, Number 3562: </a> </em>
      <blockquote>
        <p> Narrated Muhayyisah: </p>
        <p> The she-camel of Bara' ibn Azib entered the garden of a man and did damage to it. The Apostle of Allah (peace_be_upon_him) gave decision that the owners of properties are responsible for guarding them by day, and the owners of animals are responsible for guarding them by night. </p>
      </blockquote>
      <hr>
      <em> <a name="023.3563"> Book 23, Number 3563: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> Al-Bara' had a she-camel which was accustomed to graze the standing crop belonging to the people. She entered a garden and did damage to it. The Apostle of Allah (peace_be_upon_him) was informed about it. </p>
        <p> So he gave decision that the owners of gardens are responsible for guarding them by day, and the owners of the animals are responsible for guarding them by night. Any damage done by animals during the night is a responsibility lying on their owners. </p>
      </blockquote>
