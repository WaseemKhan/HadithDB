      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 32: </a> </em>
      <h1> <em> Clothing (Kitab Al-Libas) </em> </h1>
      <hr>
      <em> <a name="032.4009"> Book 32, Number 4009: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) put on a new garment he mentioned it by name, turban or shirt, and would then say: O Allah, praise be to Thee! as Thou hast clothed me with it, I ask Thee for its good and the good of that for which it was made, and I seek refuge in Thee from its evil and the evil of that for which it was made. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4012"> Book 32, Number 4012: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Anas: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone eats food and then says: "Praise be to Allah Who has fed me with this food and provided me with it through no might and power on my part," he will be forgiven his former and later sins. If anyone puts on a garment and says: "Praise be to Allah Who has clothed me with this and provided me with it through no might and power on my part," he will be forgiven his former and later sins. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4014"> Book 32, Number 4014: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> The clothing which the Apostle of Allah (peace_be_upon_him) liked best was shirt. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4015"> Book 32, Number 4015: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> No clothing was dearer to be Apostle of Allah (peace_be_upon_him) than shirt. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4016"> Book 32, Number 4016: </a> </em>
      <blockquote>
        <p> Narrated Asma', daughter of Yazid,: </p>
        <p> The sleeve of the shirt of the Apostle of Allah (peace_be_upon_him) came to the wrist. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4018"> Book 32, Number 4018: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) as saying: If anyone wears a garment for gaining fame, Allah will clothe him in a similar garment on the Day of Resurrection. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4020"> Book 32, Number 4020: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: He who copies any people is one of them. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4021A"> Book 32, Number 4021A: </a> </em>
      <blockquote>
        <p> Narrated Utbah ibn AbdusSulami: </p>
        <p> I asked the Apostle of Allah (peace_be_upon_him) to clothe me. He clothed me with two coarse clothes of linen. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4022"> Book 32, Number 4022: </a> </em>
      <blockquote>
        <p> Narrated AbuMusa al-Ash'ari: </p>
        <p> AbuBurdah said: My father said to me: My son, if you had seen us while we were with the Apostle of Allah (peace_be_upon_him) and the rain had fallen on us, you would have thought that our smell was the smell of the sheep. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4023"> Book 32, Number 4023: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The King Dhu Yazan presented to the apostle of Allah (peace_be_upon_him) a suit of clothes which he had purchased for thirty-three camels or thirty-three she-camels. He accepted it. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4024"> Book 32, Number 4024: </a> </em>
      <blockquote>
        <p> Narrated Ishaq ibn Abdullah ibn al-Harith: </p>
        <p> The Apostle of Allah (peace_be_upon_him) purchased a suit of clothes for twenty she-camels and some more and he presented it to Dhu Yazan. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4026"> Book 32, Number 4026: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> When the Haruriyyah made a revolt, I came to Ali (may Allah be pleased with him). He said: Go to these people. I then put on the best suit of the Yemen. AbuZumayl (a transmitter) said: Ibn Abbas was handsome and of imposing countenance. Ibn Abbas said: I then came to them and they said: Welcome to you, Ibn Abbas! what is this suit of clothes? I said: Why are you objecting to me? I saw over the Apostle of Allah (peace_be_upon_him) the best suit of clothes. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4027"> Book 32, Number 4027: </a> </em>
      <blockquote>
        <p> Narrated Sa'd: </p>
        <p> I saw a man riding on a white mule and he had a black turban of silk and wool. He said: The Apostle of Allah (peace_be_upon_him) put it on me. This is the version of Uthman, and there is the word akhbara in his tradition. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4028"> Book 32, Number 4028: </a> </em>
      <blockquote>
        <p> Narrated AbuAmir or AbuMalik: </p>
        <p> AbdurRahman ibn Ghanam al-Ash'ari said: AbuAmir or AbuMalik told me--I swear by Allah another oath that he did not believe me that he heard the Apostle of Allah (peace_be_upon_him) say: There will be among my community people who will make lawful (the use of) khazz and silk. Some of them will be transformed into apes and swine. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4036"> Book 32, Number 4036: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The king of Rome presented a fur of silk brocade to the Prophet (peace_be_upon_him) and he wore it. The scene that his hands were moving (while wearing the robe) is before my eyes. He then sent it to Ja'far who wore it and came to him. The Prophet (peace_be_upon_him) said: I did not send it to you to wear. He asked: What should I do with it? He replied: Send it to your brother Negus. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4037"> Book 32, Number 4037: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> The Prophet (peace_be_upon_him) said: I do not ride on purple, or wear a garment dyed with saffron, or wear shirt hemmed with silk. Pointing to the collar of his shirt al-Hasan (al-Basri) said: The perfume used by men should have an odour but no colour, and the perfume used by women should have a colour but no odour. </p>
        <p> Sa'id said: I think he said: They interpreted his tradition about perfume used by women as applying when she comes out. But when she is with her husband, she may use any perfume she wishes. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4038"> Book 32, Number 4038: </a> </em>
      <blockquote>
        <p> Narrated AbuRayhanah: </p>
        <p> AbulHusayn, al-Haytham ibn Shafi said: I and a companion of mine called AbuAmir, a man from al-Ma'afir went to perform prayer in Bayt al-Maqdis (Jerusalem). Their preacher was a man of Azd called AbuRayhanah, who was a companion of the Prophet (peace_be_upon_him). </p>
        <p> AbulHusayn said: my companion went to the mosque before me. I went there after him and sat beside him.  He asked me: Did you hear the preaching of AbuRayhanah?  I said: No.  He said: I heard him say: The Apostle of Allah (peace_be_upon_him) forbade ten things: Sharpening the ends of the teeth, tattooing, plucking hair, men sleeping together without an under garment, women sleeping together without an under-garment, men putting silk at the hem of their garments like the Persians, or putting silk on their shoulders like the Persians, plundering, riding on panther skins, wearing signet rings, except in the case of one in authority. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4040"> Book 32, Number 4040: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade me to wear a gold ring, or a Qassi garment or the use purple saddle-cloths. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4041"> Book 32, Number 4041: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) once prayed wearing a garment having marks. He looked at its marks. When he saluted, he said: Take this garment of mine to AbuJahm, for it turned my attention just now in my prayer, and bring a simple garment without marks. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4043"> Book 32, Number 4043: </a> </em>
      <blockquote>
        <p> Narrated Asma: </p>
        <p> Abdullah AbuUmar, client of Asma', daughter of AbuBakr, said: I saw Ibn Umar buying a Syrian garment in the market. When he saw that it had red warp, he returned it. I then came to Asma' and mentioned it to her. She said: Bring me, slave-girl, the mantle of the Apostle of Allah (peace_be_upon_him). She brought out a mantle of a course ornamented cloth, with its collar, sleeves, front, and back were hemmed with brocade. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4046"> Book 32, Number 4046: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet of Allah (peace_be_upon_him) took silk and held it in his right hand, and took gold and held it in his left hand and said: both of these are prohibited to the males of my community. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4048"> Book 32, Number 4048: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> We used to take it away (i.e. silk) from boys, and leave it for girls. Mis'ar said: I asked Amr ibn Dinar about it, but he did not know it. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4050"> Book 32, Number 4050: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Wear your white garments, for they are among your best garments, and shroud your dead in them. Among the best types of collyrium you use is antimony (ithmid) for it clears the vision and makes the hair sprout. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4051"> Book 32, Number 4051: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) paid visit to us, and saw a dishevelled man whose hair was disordered. He said: Could this man not find something to make his hair lie down? He saw another man wearing dirty clothes and said: Could this man not find something to wash his garments with. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4052"> Book 32, Number 4052: </a> </em>
      <blockquote>
        <p> Narrated AbulAhwas Awf ibn Malik: </p>
        <p> I came to the Prophet (peace_be_upon_him) wearing a poor garment and he said (to me): Have you any property? He replied: Yes. He asked: What kind is it? He said: Allah has given me camels. Sheep, horses and slaves. He then said: When Allah gives you property, let the mark of Allah's favour and honour to you be seen. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4053"> Book 32, Number 4053: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Zayd ibn Aslam said: Ibn Umar used to dye his beard with yellow colour so much so that his clothes were filled (dyed) with yellowness. He was asked: Why do you dye  with yellow colour? He replied: I saw the Apostle of Allah (peace_be_upon_him) dyeing with yellow colour, and nothing was dearer to him than it. He would dye all his clothes with it, even his turban. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4054"> Book 32, Number 4054: </a> </em>
      <blockquote>
        <p> Narrated AbuRimthah: </p>
        <p> I went with my father to the Prophet (peace_be_upon_him) and saw two green garments over him. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4055"> Book 32, Number 4055: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> We came down with the Apostle of Allah (peace_be_upon_him) from a turning of a valley. He turned his attention to me and I was wearing a garment dyed with a reddish yellow dye. He asked: What is this garment over you? I recognised what he disliked. I then came to my family who were burning their oven. I threw it (the garment) in it and came to him the next day. He asked: Abdullah, what have you done  with the garment? I informed him about it. He said: Why did you not give it to one of your family to wear, for there is no harm in it for women. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4057"> Book 32, Number 4057: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah (peace_be_upon_him) saw me. The version of AbuAli al-Lula' has: I think I wore a garment dyed with a reddish yellow colour. He asked: What is this? So I went and burnt it. The Prophet (peace_be_upon_him) said: What have you done with your garment?  I replied: I burnt it. He said: Why did you not give it to one of your women to wear? </p>
      </blockquote>
      <hr>
      <em> <a name="032.4058"> Book 32, Number 4058: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> A man wearing two red garments passed the Prophet (peace_be_upon_him) and gave him a greeting, but he did not respond to his greeting. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4059"> Book 32, Number 4059: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> We went out with the Apostle of Allah (peace_be_upon_him) on a journey, and we had on our saddles and camels garments consisting of red warp of wool. The Apostle of Allah (peace_be_upon_him) said: Do I not see that red colour has dominated you. We then got up quickly on account of this saying of the Apostle of Allah (peace_be_upon_him) and some of our camels ran away. We then took the garments and withdrew them. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4060"> Book 32, Number 4060: </a> </em>
      <blockquote>
        <p> Narrated Zaynab: </p>
        <p> Hurayth ibn al-Abajj as-Sulayhi said: that a woman of Banu Asad: One day I was with Zaynab, the wife of the Apostle of Allah (peace_be_upon_him), and we were dyeing her clothes with red ochre. In the meantime the Apostle of Allah (peace_be_upon_him) peeped us. When he saw the red ochre, he returned. When Zaynab saw this, she realised that the Apostle of Allah (peace_be_upon_him) disapproved of what she had done. She then took and washed her clothes and concealed all redness. The Apostle of Allah (peace_be_upon_him) then returned and peeped, and when he did not see anything, he entered. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4062"> Book 32, Number 4062: </a> </em>
      <blockquote>
        <p> Narrated Amir: </p>
        <p> I saw the Apostle of Allah (peace_be_upon_him) at Mina giving sermon on a  mule and wearing a red garment, while Ali was announcing. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4063"> Book 32, Number 4063: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> I made a black cloak for the Prophet (peace_be_upon_him) and he put it on; but when he sweated in it and noticed the odour of the wool, he threw it away. The narrator said: I think he said: He liked good smell. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4064"> Book 32, Number 4064: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> When I came to the Prophet (peace_be_upon_him), he was sitting with his hands round his knees wearing the cloak the fringe of which was over his feet. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4067"> Book 32, Number 4067: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn Rukanah: </p>
        <p> Ali quoting his father said: Rukanah wrestled  with the Prophet (peace_be_upon_him) and the Prophet (peace_be_upon_him) threw him on the ground. Rukanah said: I heard the Prophet (peace_be_upon_him) say: The difference between us and the polytheists is that we wear turbans over caps. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4068"> Book 32, Number 4068: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Awf: </p>
        <p> The Apostle of Allah (peace_be_upon_him) put a turban on me and let the ends hang in front of him and behind me. </p>
      </blockquote>
      <hr>
      <em> <a name="032.4069"> Book 32, Number 4069: </a> </em>
      <blockquote>
