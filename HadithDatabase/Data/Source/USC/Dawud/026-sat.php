      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 26: </a> </em>
      <h1> <em> Drinks (Kitab Al-Ashribah) </em> </h1>
      <hr>
      <em> <a name="026.3662"> Book 26, Number 3662: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> When the prohibition of wine (was yet to be) declared, Umar said: O Allah, give us a satisfactory explanation about wine. </p>
        <p> So the following verse of Surat al-Baqarah revealed; "They ask thee concerning wine and gambling. Say: In them is great sin...." Umar was then called and it was recited to him. </p>
        <p> He said: O Allah, give us a satisfactory explanation about wine. </p>
        <p> Then the following verse of Surat an-Nisa' was revealed: "O ye who believe! approach not prayers with a mind befogged...." Thereafter the herald of the Apostle of Allah (peace_be_upon_him) would call when the (congregational) prayer was performed: Beware, one who is drunk should not come to prayer. Umar was again called and it was recited to him). </p>
        <p> He said: O Allah, give us a satisfactory explanation about wine. This verse was revealed: "Will ye not then abstain?"  Umar said: We abstained. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3663"> Book 26, Number 3663: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> A man of the Ansar called him and AbdurRahman ibn Awf and supplied them wine before it was prohibited. Ali then led them in the evening prayer, and he recited; "Say: O ye who reject faith." He was confused in it. Then the following verse came down: "O ye who believe! approach not prayers with a mind befogged until you can understand all that ye say. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3665"> Book 26, Number 3665: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> I was serving wine to the people in the house of AbuTalhah when it was prohibited and that day our wine was made from unripe dates. A man entered upon us and said: The wine has been prohibited, and the herald of the Apostle of Allah (peace_be_upon_him) made an announcement. We then said: This is the herald of the Apostle of Allah (peace_be_upon_him) </p>
      </blockquote>
      <hr>
      <em> <a name="026.3666"> Book 26, Number 3666: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah has cursed wine, its drinker, its server, its seller, its buyer, its presser, the one for whom it is pressed, the one who conveys it, and the one to whom it is conveyed. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3668"> Book 26, Number 3668: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> The Prophet (peace_be_upon_him) said: from grapes wine is made, from dried dates wine is made, from honey wine is made, from wheat wine is made, from barley wine is made. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3669"> Book 26, Number 3669: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Wine is made from grape-syrup, raisins, dried dates, wheat, barley, millet, and I forbid you from every intoxicant. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3672"> Book 26, Number 3672: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Every intoxicant is khamr (wine) and every intoxicant is forbidden. If anyone drinks wine, Allah will not accept prayer from him for forty days, but if he repents, Allah will accept his repentance. If he repeats it a fourth time, it is binding on Allah that He will give him tinat al-khabal to drink. </p>
        <p> He was asked: What is tinat al-khabal, Apostle of Allah?  He replied: Discharge of wounds, flowing from the inhabitants of Hell. If anyone serves it to a minor who does not distinguish between the lawful and the unlawful, it is binding on Allah that He will give him to drink the discharge of wounds, flowing from the inhabitants of Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3673"> Book 26, Number 3673: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If a large amount of anything causes intoxication, a small amount of it is prohibited. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3675"> Book 26, Number 3675: </a> </em>
      <blockquote>
        <p> Narrated Daylam al-Himyari: </p>
        <p> I asked the Prophet (peace_be_upon_him) and said: Apostle of Allah! we live in a cold land in which we do heavy work and we make a liquor from wheat to get strength from if for our work and to stand the cold of our country.  He asked: Is it intoxicating? I replied: Yes. He said: You must avoid it. I said: The people will not abandon it. He said: If they do not abandon it, fight with them. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3677"> Book 26, Number 3677: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) forbade wine (khamr), game of chance (maysir), drum (kubah), and wine made from millet (ghubayrah), saying: Every intoxicant is forbidden. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3678"> Book 26, Number 3678: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade every intoxicant and everything which produces languidness. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3679"> Book 26, Number 3679: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Every intoxicant is forbidden; if a faraq of anything causes intoxication, a handful of it is forbidden. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3680"> Book 26, Number 3680: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Ghanam: </p>
        <p> Malik ibn AbuMaryam said: AbdurRahman ibn Ghanam entered upon us and we discussed tila' and he said: AbuMalik al-Ash'ari told me that he heard the Apostle of Allah (peace_be_upon_him) say: Some of my people will assuredly drink wine calling it by another name. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3685"> Book 26, Number 3685: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> In the story of the deputation of AbdulQays Ibn Abbas said: They (the people) asked: In which should we drink, Prophet of Allah? The Prophet (peace_be_upon_him) said: You should use those skin vessels that are tied at their mouths. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3686"> Book 26, Number 3686: </a> </em>
      <blockquote>
        <p> Narrated Qays ibn an-Nu'man: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not drink from hollowed stumps, vessel smeared with pitch, pumpkins, and green jars, but drink from a skin which is tied with string. If the drink ferments, lighten it by infusing water. If you are helpless, then pour it away. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3688"> Book 26, Number 3688: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade us the use of pumpkins, green jars, hollow stumps and wine made from barley. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3689"> Book 26, Number 3689: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Prophet (peace_be_upon_him) said: I forbade you three things, and now I command (permit) you for them. I forbade you to visit graves, now you may visit them, for in visiting them there is admonition. I forbade you drinks except from skin vessels, but now you may drink from any kind of vessels, but do not drink an intoxicant. I forbade you to eat the meat of sacrificial animals after three days, but now you may eat and enjoy it during your journeys. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3696"> Book 26, Number 3696: </a> </em>
      <blockquote>
        <p> Narrated A man: </p>
        <p> A man from among the Companions of the Prophet (peace_be_upon_him) said: The Prophet (peace_be_upon_him) forbade (mixing) unripe dates and dried dates, and (mixing) raisins and dried dates. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3697"> Book 26, Number 3697: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> Kabshah, daughter of AbuMaryam, asked Umm Salamah (Allah be pleased with her): What did the Prophet (peace_be_upon_him) prohibit? She replied: He forbade us to boil dates so much so that the kernels are spoiled, and to mix raisins and dried dates. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3698"> Book 26, Number 3698: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Raisins were steeped for the Apostle of Allah (peace_be_upon_him) and then dried dates were infused in them, or dried dates were steeped and then raisins were infused in them. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3699"> Book 26, Number 3699: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Safiyyah, daughter of Atiyyah, said: I entered upon Aisha with some women of AbdulQays, and asked her about mixing dried dates and raisins (for drink). She replied: I used to take a handful of dried dates and a handful or raisins and put them in a vessel, and then crush them (and soak in water). Then I would give it to the Prophet (peace_be_upon_him) to drink. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3701"> Book 26, Number 3701: </a> </em>
      <blockquote>
        <p> Narrated Ad-Daylami: </p>
        <p> We came to the Prophet (peace_be_upon_him) and said to him: Apostle of Allah, you know who we are, from where we are and to whom we have come. He said: To Allah and His Apostle. We said: Apostle of Allah, we have grapes; what should we do with them? He said: Make them raisins. We then asked: What should we do with raisins? He replied: Steep them in the morning and drink in the evening, and steep them in the evening and drink in the morning. Steep them in skin vessels and do not steep them in earthen jar, for it it is delayed in pressing, it becomes vinegar. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3703"> Book 26, Number 3703: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Amrah said on the authority of Aisha that she would steep dates for the Apostle of Allah (peace_be_upon_him) in the morning. When the evening came, he took his dinner and drank it after his dinner. If anything remained, she poured it out. She then would steep for him at night. When the morning came, he took his morning meal and drank it after his morning meal. She said: The skin vessel was washed in the morning and in the evening. My father (Hayyan) said to her: Twice a day? She said: Yes. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3707"> Book 26, Number 3707: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> I knew that the Apostle of Allah (peace_be_upon_him) used to keep fast. I waited for the day when he did not fast to present him the drink (nabidh) which I made in a pumpkin. I then brought it to him while it fermented. He said: Throw it to this wall, for this is a drink of the one who does not believe in Allah and the Last Day. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3708"> Book 26, Number 3708: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) forbade that a man should drink while standing. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3712"> Book 26, Number 3712: </a> </em>
      <blockquote>
        <p> Narrated A man of the Ansar: </p>
        <p> The Prophet (peace_be_upon_him) called for a skin-vessel on the day of the battle of Uhud. He then said: Invert the head of the vessel and he drank from its mouth. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3716"> Book 26, Number 3716: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn AbuAwfa: </p>
        <p> The Prophet (peace_be_upon_him) said: The supplier of the people is the last (man) to drink. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3719"> Book 26, Number 3719: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade blowing or breathing into a vessel. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3721"> Book 26, Number 3721: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> I was in the house of Maymunah. The Apostle of Allah (peace_be_upon_him) accompanied by Khalid ibn al-Walid entered. Two roasted long-tailed lizards (dabb) placed on the sticks were brought to him. The Apostle of Allah (peace_be_upon_him) spat. Khalid said: I think that you abominate it, Apostle of Allah. He said: Yes. Then the Apostle of Allah (peace_be_upon_him) was brought milk, and he drank (it). The Apostle of Allah  (peace_be_upon_him) then said: When one of you eats food, he should say: O Allah, bless us in it, and give us food (or nourishment) better than it. When he is given milk to drink he should say: O Allah! bless us in it and give us more of it, for no food or drink satisfies like milk. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3722"> Book 26, Number 3722: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: Shut your door and make mention of Allah's name, for the devil does not open a door which has been shut; extinguish your lamp and make mention of Allah's name, cover up your vessel even by a piece of wood that you just put on it and make mention of Allah's name, and tie up your water-skin mentioning Allah's name. </p>
      </blockquote>
      <hr>
      <em> <a name="026.3726"> Book 26, Number 3726: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The water from as-Suqya' was considered sweetest by the Prophet (peace_be_upon_him). Qutaybah said: it was a well on two days' journey from Medina. </p>
      </blockquote>
