      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 36: </a> </em>
      <h1> <em> The Promised Deliverer (Kitab Al-Mahdi) </em> </h1>
      <hr>
      <em> <a name="036.4266"> Book 36, Number 4266: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Samurah: </p>
        <p> The Prophet (peace_be_upon_him) said: The religion will continue to be established till there are twelve caliphs over you, and the whole community will agree on each of them. I then heard from the Prophet (peace_be_upon_him) some remarks which I could not understand. I asked my father: What is he saying: He said: all of them will belong to Quraysh. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4269"> Book 36, Number 4269: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet (peace_be_upon_him) said: If only one day of this world remained. Allah would lengthen that day (according to the version of Za'idah), till He raised up in it a man who belongs to me or to my family whose father's name is the same as my father's, who will fill the earth with equity and justice as it has been filled with oppression and tyranny (according to the version of Fitr). Sufyan's version says: The world will not pass away before the Arabs are ruled by a man of my family whose name will be the same as mine. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4270"> Book 36, Number 4270: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: If only one day of this time (world) remained, Allah would raise up a man from my family who would fill this earth with justice as it has been filled with oppression. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4271"> Book 36, Number 4271: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) said: The Mahdi will be of my family, of the descendants of Fatimah. Abdullah ibn Ja'far said: I heard AbulMalih praising Ali ibn Nufayl and describing his good qualities. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4272"> Book 36, Number 4272: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: The Mahdi will be of my stock, and will have a broad forehead a prominent nose. He will fill the earth will equity and justice as it was filled with oppression and tyranny, and he will rule for seven years. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4273"> Book 36, Number 4273: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) said: Disagreement will occur at the death of a caliph and a man of the people of Medina will come flying forth to Mecca. Some of the people of Mecca will come to him, bring him out against his will and swear allegiance to him between the Corner and the Maqam. An expeditionary force will then be sent against him from Syria but will be swallowed up in the desert between Mecca and Medina. When the people see that, the eminent saints of Syria and the best people of Iraq will come to him and swear allegiance to him between the Corner and the Maqam. </p>
        <p> Then there will arise a man of Quraysh whose maternal uncles belong to Kalb and send against them an expeditionary force which will be overcome by them, and that is the expedition of Kalb. Disappointed will be the one who does not receive the booty of Kalb. He will divide the property, and will govern the people by the Sunnah of their Prophet (peace_be_upon_him) and establish Islam on Earth. He will remain seven years, then die, and the Muslims will pray over him. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4276A"> Book 36, Number 4276A: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> AbuIshaq told that Ali looked at his son al-Hasan and said: This son of mine is a sayyid (chief) as named by the Prophet (peace_be_upon_him), and from his loins will come forth a man who will be called by the name of your Prophet (peace_be_upon_him) and resemble him in conduct but not in appearance. He then mentioned the story about his filling the earth with justice. </p>
      </blockquote>
      <hr>
      <em> <a name="036.4277"> Book 36, Number 4277: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Prophet (peace_be_upon_him) said: A man called al-Harith ibn Harrath will come forth from Ma Wara an-Nahr. His army will be led by a man called Mansur who will establish or consolidate things for Muhammad's family as Quraysh consolidated them for the Apostle of Allah (peace_be_upon_him). Every believer must help him, or he said: respond to his sermons. </p>
      </blockquote>
