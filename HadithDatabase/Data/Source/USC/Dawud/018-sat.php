      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 18: </a> </em>
      <h1> <em> Shares of Inheritance (Kitab Al-Fara'id) </em> </h1>
      <hr>
      <em> <a name="018.2879"> Book 18, Number 2879: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: Knowledge has three categories; anything else is extra; a precise verse, or an established sunnah (practice), or a firm obligatory duty. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2881"> Book 18, Number 2881: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> I fell ill, and I had seven sisters. The Apostle of Allah (peace_be_upon_him) came to me and blew on my face. So I became conscious. I said: Apostle of Allah, may I not bequeath one-third of my property to my sisters? He replied: Do good. I asked: Half? He replied: Do good. He then went out and left me, and said: I do not think, Jabir, you will die of this disease. Allah has revealed (verses) and described the share of your sisters. He appointed two-thirds for them. Jabir used to say: This verse was revealed about me: "They ask thee for a legal decision. Say: Allah directs (thus) about those who leave no descendants or ascendants as heirs. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2883"> Book 18, Number 2883: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, they ask thee for a legal decision about a kalalah. What is meant by kalalah? He replied: The verse revealed in summer is sufficient for you. </p>
        <p> I asked AbuIshaq: Does it mean a person who dies and leaves neither children nor father? He said: This is so. The people think it is so. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2885"> Book 18, Number 2885: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> We went out with the Apostle of Allah (peace_be_upon_him) and came to a woman of the Ansar in al-Aswaf. The woman brought her two daughters, and said: Apostle of Allah, these are the daughters of Thabit ibn Qays who was killed as a martyr when he was with you at the battle of Uhud, their paternal uncle has taken all their property and inheritance, and he has not left anything for them. What do you think, Apostle of Allah? They cannot be married unless they have some property. The Apostle of Allah (peace_be_upon_him) said: Allah will decide regarding the matter. Then the verse of Surat an-Nisa was revealed: "Allah (thus) directs you as regards your children's (inheritance)." Apostle of Allah (peace_be_upon_him) said: Call to me the woman and her husband's brother. He then said to their paternal uncle: Give them two-thirds and their mother an eighth, and what remains is yours. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2888"> Book 18, Number 2888: </a> </em>
      <blockquote>
        <p> Narrated al-Mughirah ibn Shu'bah: </p>
        <p> Qabisah ibn Dhuwayb said: A grandmother came to AbuBakr asking him for her share of inheritance. He said: There is nothing prescribed for you in Allah's Book, nor do I know anything for you in the Sunnah of the Prophet of Allah (peace_be_upon_him) Go home till I question the people. He then questioned the people, and al-Mughirah ibn Shu'bah said: I had been present with the Apostle of Allah  (peace_be_upon_him) when he gave grandmother a sixth. AbuBakr said: Is there anyone with you? Muhammad ibn Maslamah stood and said the same as al-Mughirah ibn Shu'bah had said. So AbuBakr made it apply to her. Another grandmother came to Umar ibn al-Khattab asking him for her share of inheritance. He said: Nothing has been prescribed for you in Allah's Book. The decision made before you was made for a grandmother other than you. I am not going to add in the shares of inheritance; but it is that sixth. If there are two of you, it is shared between you, but whichever of you is the only one left gets it all. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2889"> Book 18, Number 2889: </a> </em>
      <blockquote>
        <p> Narrated Buraydah: </p>
        <p> The Prophet (peace_be_upon_him) appointed a sixth to a grandmother if no mother is left to inherit before her. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2890"> Book 18, Number 2890: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and said: My son has died; what do I receive from his estate? He replied: You receive a sixth. When he turned away he called him and said: You receive another sixth. When he turned away, he called him and said: The other sixth is an allowance (beyond what is due). </p>
        <p> Qatadah said: They (the Companions) did not know the heirs with whom he was given (a sixth). Qatadah said: The minimum share given to the grandfather was a sixth. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2891"> Book 18, Number 2891: </a> </em>
      <blockquote>
        <p> Narrated Ma'qil ibn Yasar: </p>
        <p> Al-Hasan reported that Umar asked: Which of your knows what share the Apostle of Allah (peace_be_upon_him) had given to the grandfather from the estate? Ma'qil ibn Yasar said: The Apostle of Allah (peace_be_upon_him) gave him a sixth. He asked: Along with whom? He replied: I do not know. He said: You do not know; what is the use then? </p>
      </blockquote>
      <hr>
      <em> <a name="018.2892"> Book 18, Number 2892: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Divide the property among those whose share have been prescribed in the Book of Allah, and what remains from the prescribed shares goes to the nearest male heirs. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2893"> Book 18, Number 2893: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam al-Kindi: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone leaves a debt or a  helpless family I shall be responsible-and sometimes the narrator said: Allah and His Apostle  will be responsible-but if anyone leaves property, it goes to his heirs. I am the heirs of him who has none, paying blood-wit for him and inheriting from him; and a maternal uncle is the heir of him who has none, paying blood-wit for him and inheriting from him. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2894"> Book 18, Number 2894: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam al-Kindi: </p>
        <p> The Prophet (peace_be_upon_him) said: I am nearer to every believer than himself, so if anyone leaves a debt or a helpless family, I shall be responsible, but if anyone leaves property, it goes to his heirs. I am patron of him who has none, inheriting his property and freeing him from his liabilities. A maternal uncle is patron of him who has none, inheriting his property and freeing him from his liabilities. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2895"> Book 18, Number 2895: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: I am the heirs of Him who has none, freeing him from his liabilities, and inheriting what he possesses. A maternal uncle is the heir of Him who has none, freeing him from his liabilities, and inheriting his property. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2896"> Book 18, Number 2896: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> A client of the Prophet (peace_be_upon_him) died and left some property, but he left no child or relative. The Apostle of Allah (peace_be_upon_him) said: Give what he has left to a man belonging to his village. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2897"> Book 18, Number 2897: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> A man came to the Apostle of Allah (peace_be_upon_him) and said: I have property left by a man of Azd. I do not find any man of Azd to give it to him. He said: Go and look for man of Azd for a year. He then came to him after one year and said: Apostle of Allah, I did not find any man of Azd to give it to him. He said: Look for a man of Khuza'ah whom you meet first and give it to him. When he turned away, he said; Call the man to me. When he came to him, he said: Look for the leading man of Khuza'ah and give it to him. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2898"> Book 18, Number 2898: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> A man of Khuza'ah died and his estate was brought to the Prophet (peace_be_upon_him). He said: Look for his heir or some relative. But they found neither heir nor relative. The Apostle of Allah (peace_be_upon_him) said: Give it to the leading man of Khuza'ah. The narrator Yahya said: Sometimes I heard him (al-Husayn ibn Aswad) say in this tradition: Look for the greatest man of Khuza'ah. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2899"> Book 18, Number 2899: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A man died leaving no heir but a slave whom he had emancipated. The Apostle of Allah (peace_be_upon_him) asked: Has he any heir? They replied: No, except a slave whom he had emancipated. The Apostle of Allah (peace_be_upon_him) assigned his estate to him (the emancipated slave). </p>
      </blockquote>
      <hr>
      <em> <a name="018.2900"> Book 18, Number 2900: </a> </em>
      <blockquote>
        <p> Narrated Wathilah ibn al-Asqa': </p>
        <p> The Prophet (peace_be_upon_him) said: A woman gets inheritance from the three following: one she has set free, a foundling, and her child about whom she has invoked a curse on herself if she was untrue in declaring he was not born out of wedlock. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2901"> Book 18, Number 2901: </a> </em>
      <blockquote>
        <p> Narrated Makhul: </p>
        <p> The Apostle of Allah (peace_be_upon_him) assigned the estate of a child of a woman about whom she had invoked a curse to her mother, and to her heirs after her. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2905"> Book 18, Number 2905: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: people of two different religions would not inherit from one another. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2906"> Book 18, Number 2906: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Islam increases and does not diminish. He, therefore, appointed a Muslim heir (of a non-Muslim). </p>
      </blockquote>
      <hr>
      <em> <a name="018.2908"> Book 18, Number 2908: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: An estate which was divided in pre-Islamic period may follow the division in force then, but any estate in Islamic times must follow the division laid down by Islam. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2911"> Book 18, Number 2911: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Amr ibn Shu'ayb reported: Rabab ibn Hudhayfah married a woman and three sons were born to him from her. Their mother then died. They inherited her houses and had the right of inheritance of her freed slaves. </p>
        <p> Amr ibn al-'As was the agnate of her sons. He sent them to Syria where they died. Amr ibn al-'As then came. A freed slave of hers died and left some property. Her brothers disputed with him and brought the case to Umar ibn al-Khattab. </p>
        <p> Umar reported the Apostle of Allah (peace_be_upon_him) as saying: Whatever property a son or a father receives as an heir will go to his agnates, whoever they may be. He then wrote a document for him, witnessed by AbdurRahman ibn Awf, Zayd ibn Thabit and one other person. When AbdulMalik became caliph, they presented the case to Hisham ibn Isma'il or Isma'il ibn Hisham (the narrator is doubtful). </p>
        <p> He sent them to AbdulMalik who said: This is the decision which I have already seen. </p>
        <p> The narrator said: So he (AbdulMalik) made the decision on the basis of the document of Umar ibn al-Khattab, and that is still with us today. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2912"> Book 18, Number 2912: </a> </em>
      <blockquote>
        <p> Narrated Tamim ad-Dari: </p>
        <p> Tamim asked: Apostle of Allah), what is the sunnah about a man who accepts Islam by advice and persuasion of a Muslim? He replied: He is the nearest to him in life and in death. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2914"> Book 18, Number 2914: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When an infant has raised its voice (and then dies), it will be treated as an heir. </p>
      </blockquote>
      <hr>
      <em> <a name="018.2921"> Book 18, Number 2921: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Sa'id said: Umar ibn al-Khattab said: Blood-money is meant for the clan of the slain, and she will not inherit from the blood-money of her husband. Ad-Dahhak ibn Sufyan said: The Apostle of Allah (peace_be_upon_him) wrote to me that I should give a share to the wife of Ashyam ad-Dubabi from the blood-money of her husband. So Umar withdrew his opinion. </p>
        <p> Ahmad ibn Salih said: AbdurRazzaq transmitted this tradition to us from Ma'mar, from az-Zuhri on the authority of Sa'id. In this version he said: The Prophet (peace_be_upon_him) made him governor over the bedouins. </p>
      </blockquote>
