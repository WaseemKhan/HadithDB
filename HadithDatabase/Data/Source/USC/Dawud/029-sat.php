      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 29: </a> </em>
      <h1> <em> Divination and Omens (Kitab Al-Kahanah Wa Al-Tatayyur) </em> </h1>
      <hr>
      <em> <a name="029.3895"> Book 29, Number 3895: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone resorts to a diviner and believes in what he says (according) to the version of Musa), or has intercourse with his wife (according to the agreed version) when she is menstruating, or has intercourse with his wife through her anus, he has nothing to do with what has been sent down to Muhammad (peace_be_upon_him) - according to the version of Musaddad. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3896"> Book 29, Number 3896: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone acquires any knowledge of astrology, he acquires a branch of magic of which he gets more as long as he continues to do so. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3898"> Book 29, Number 3898: </a> </em>
      <blockquote>
        <p> Narrated Qabisah: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Augury from the flight of birds, taking evil omens and the practice of pressomancy pertain to divination. Tarq: It is used in the sense of divination in which women threw stones. 'Iyafah: It means geomancy by drawing lines. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3901"> Book 29, Number 3901: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet (peace_be_upon_him) said: Taking omens is polytheism; taking omens is polytheism. He said it three times. Every one of us has some, but Allah removes it by trust (in Him). </p>
      </blockquote>
      <hr>
      <em> <a name="029.3904"> Book 29, Number 3904: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: There is no ghoul. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3907"> Book 29, Number 3907: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) heard a word, and he liked it, he said: We took your omen from your mouth. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3909"> Book 29, Number 3909: </a> </em>
      <blockquote>
        <p> Narrated Urwah ibn Amir al-Qurashi: </p>
        <p> When taking omens was mentioned in the presence of the Prophet (peace_be_upon_him), he said: The best type is the good omen, and it does not turn back a Muslim. If one of you sees anything he dislikes, he should say: O Allah, no one brings good things except Thee, and no one averts evil things except Thee and there is no might and power but in Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3910"> Book 29, Number 3910: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> The Prophet (peace_be_upon_him) did not take omens from anything, but when he sent out an agent he asked about his name. If it pleased him, he was glad about it, and his cheerfulness on that account was visible in his face. If he disliked his name, his displeasure on that account was visible in his face. When he entered a village, he asked about its name, and if it pleased him, he was glad about it, and his cheerfulness on that account was visible in his face. But if he disliked its name, his displeasure on that account was visible in his face. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3911"> Book 29, Number 3911: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: There is no hamah, no infection and no evil omen; if there is in anything an evil omen, it is a house, a horse, and a  woman. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3912"> Book 29, Number 3912: </a> </em>
      <blockquote>
        <p> Narrated Farwah ibn Musayk: </p>
        <p> Yahya ibn Abdullah ibn Buhayr said that he was informed by one who had heard Farwah ibn Musayk tell that he said: Apostle of Allah! we have land called Abyan, which is the land where we have our fields and grow our crops, but it is very unhealthy. The Prophet (peace_be_upon_him) said: Leave it, for destruction comes from being near disease. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3913"> Book 29, Number 3913: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> A man said: Apostle of Allah! we were in an abode in which our numbers and our goods were many and changed to an abode in which our numbers and our goods became few. The Apostle of Allah (peace_be_upon_him) said: Leave it, for it is reprehensible. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3914"> Book 29, Number 3914: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) took a man who was suffering from tubercular leprosy by the hand; he then put it along with his own hand in the dish and said: Eat with confidence in Allah and trust in Him. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3915"> Book 29, Number 3915: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: A slave who has entered into an agreement to purchase his freedom is a slave as long as a dirham of the agreed price remains to be paid. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3916"> Book 29, Number 3916: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: If any slave entered into an agreement to buy his freedom for one hundred uqiyahs and he pays them all but ten, he remains a slave (until he pays the remaining ten); and if a slave entered into an agreement to purchase his freedom for one hundred dinars, and he pays them all but ten dinars, he remains a slave (until he pays the remaining ten). </p>
      </blockquote>
      <hr>
      <em> <a name="029.3917"> Book 29, Number 3917: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to us: If one of you has a slave, and he enters into an agreement to purchase his freedom, and can pay the full price, she must veil herself from him. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3920"> Book 29, Number 3920: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Juwayriyyah, daughter of al-Harith ibn al-Mustaliq, fell to the lot of Thabit ibn Qays ibn Shammas, or to her cousin. She entered into an agreement to purchase her freedom. She was a very beautiful woman, most attractive to the eye. </p>
        <p> Aisha said: She then came to the Apostle of Allah (peace_be_upon_him) asking him for the purchase of her freedom. When she was standing at the door, I looked at her with disapproval. I realised that the Apostle of Allah (peace_be_upon_him) would look at her in the same way that I had looked. </p>
        <p> She said: Apostle of Allah, I am Juwayriyyah, daughter of al-Harith, and something has happened to me, which is not hidden from you. I have fallen to the lot of Thabit ibn Qays ibn Shammas, and I have entered into an agreement to purchase of my freedom. I have come to you to seek assistance for the purchase of my freedom. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Are you inclined to that which is better?  She asked: What is that, Apostle of Allah?  He replied: I shall pay the price of your freedom on your behalf, and I shall marry you. </p>
        <p> She said: I shall do this.  She (Aisha) said: The people then heard that the Apostle of Allah (peace_be_upon_him) had married Juwayriyyah. They released the captives in their possession and set them free, and said: They are the relatives of the Apostle of Allah (peace_be_upon_him) by marriage. We did not see any woman greater than Juwayriyyah who brought blessings to her people. One hundred families of Banu al-Mustaliq were set free on account of her. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3921"> Book 29, Number 3921: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> Safinah said: I was a slave of Umm Salamah, and she said: I shall emancipate you, but I stipulate that you must serve the Apostle of Allah (peace_be_upon_him) as long as you live. I said: Even if you do not make a stipulation, I shall not leave the Apostle of Allah (peace_be_upon_him). She then emancipated me and made the stipulation with me. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3922"> Book 29, Number 3922: </a> </em>
      <blockquote>
        <p> Narrated Usamah ibn Umayr al-Huzali: </p>
        <p> (This is AbulWalid's version): A man emancipated a share in a slave and the matter was mentioned to the Prophet (peace_be_upon_him). He said: Allah has no partner. </p>
        <p> Ibn Kathir added in his version: The Prophet (peace_be_upon_him) allowed his emancipation. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3923"> Book 29, Number 3923: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> A man emancipated his share in a slave. The Prophet (peace_be_upon_him) allowed his (full) emancipation, and required him to pay the rest of his price. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3937"> Book 29, Number 3937: </a> </em>
      <blockquote>
        <p> Narrated ath-Thalabb ibn Tha'labah at-Tamimi: </p>
        <p> A man emancipated his share in a slave. The Prophet (peace_be_upon_him) did not put the responsibility on him to emancipate the rest. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3938"> Book 29, Number 3938: </a> </em>
      <blockquote>
        <p> Narrated Samurah: </p>
        <p> The Prophet (peace_be_upon_him) said: (The narrator Musa said in another place: From Samurah ibn Jundub as presumed by Hammad): If anyone gets possession of a relative who is within the prohibited degrees, that person becomes free. AbuDawud said: A similar tradition has also been transmitted by Samurah from the Prophet (peace_be_upon_him) through a different chain. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3939"> Book 29, Number 3939: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Qatadah reported Umar ibn al-Khattab (may Allah be pleased with him) as saying: If anyone gets possession of a relative who is within the prohibited degrees, that person becomes free. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3942"> Book 29, Number 3942: </a> </em>
      <blockquote>
        <p> Narrated Sulamah bint Ma'qil al-Qasiyyah: </p>
        <p> My uncle brought me (to Medina) in the pre-Islamic days. He sold me to al-Hubab ibn Amr, brother of AbulYusr ibn Amr. I bore a child, AbdurRahman ibn al-Hubab, to him and he (al-Hubab) then died. </p>
        <p> Thereupon his wife said: I swear by Allah, now you will be sold (as a repayment) for his loan. </p>
        <p> So I came to the Apostle of Allah (peace_be_upon_him) and said: Apostle of Allah! I am a woman of Banu Kharijah Qays ibn Aylan. My uncle had brought me to Medina in pre-Islamic days. He sold me to al-Hubab ibn Amr, brother of AbulYusr ibn Amr. I bore AbdurRahman ibn al-Hubab to him. His wife said: I swear by Allah, you will be sold for his loan. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Who is the guardian of al-Hubab? </p>
        <p> He was told: His brother, AbulYusr ibn Amr. He then sent for him and said: Set her free; when you hear that some slaves have been brought to me, came to me, and I shall compensate you for her. </p>
        <p> She said: They set me free, and when some slaves were brought to the Apostle of Allah (peace_be_upon_him), he gave them a slave in compensation for me. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3943"> Book 29, Number 3943: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> We sold slave-mothers during the time of the Apostle of Allah (peace_be_upon_him) and of AbuBakr. When Umar was in power, he forbade us and we stopped. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3952"> Book 29, Number 3952: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The child of adultery is worst of the three. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3953"> Book 29, Number 3953: </a> </em>
      <blockquote>
        <p> Narrated Wathilah ibn al-Asqa: </p>
        <p> Al-Arif ibn ad-Daylami said: We went to Wathilah ibn al-Asqa and said to him: Tell us a tradition which has not addition or omission. He became angry and replied: One of you recites when his copy of a Qur'an is hung up in his house, and he makes additions and omissions. We said: All we mean is a tradition you have heard from the Apostle of Allah (peace_be_upon_him). He said: We went to the Prophet (peace_be_upon_him) about a friend of ours who deserved. Hell for murder. He said: Emancipate a slave on his behalf; Allah will set free from Hell a member of the body for every member of his. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3954"> Book 29, Number 3954: </a> </em>
      <blockquote>
        <p> Narrated AbuNajih as-Sulami: </p>
        <p> Along with the Apostle of Allah (peace_be_upon_him) we besieged the palace of at-Ta'if. The narrator, Mutadh, said: I heard my father (sometimes) say: "Palace of at-Ta'if," and (sometimes) "Fort of at-Ta'if," which are the same. </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: he who causes an arrow to hit its mark in Allah's cause will have it counted as a degree for him (in Paradise). He then transmitted the rest of the tradition. </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: If a Muslim man emancipates a Muslim man, Allah, the Exalted, will make  every bone of his protection for every bone of his emancipator from Hell; and if a Muslim woman emancipates a Muslim woman, Allah will make every bone of hers protection for every bone of her emancipator from Hell on the Day of Resurrection. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3955"> Book 29, Number 3955: </a> </em>
      <blockquote>
        <p> Narrated Marrah ibn Ka'b: </p>
        <p> Amr ibn Abasah, said that Marrah ibn Ka'b said: I heard the Apostle of Allah (peace_be_upon_him) say: If anyone emancipates a Muslim slave, that will be his ransom from Jahannam. </p>
      </blockquote>
      <hr>
      <em> <a name="029.3957"> Book 29, Number 3957: </a> </em>
      <blockquote>
        <p> Narrated AbudDarda': </p>
        <p> The Prophet (peace_be_upon_him) said: the similitude of a man who emancipates a slave at the time of his death is like that of a man who gives a present after satisfying his appetite. </p>
      </blockquote>
