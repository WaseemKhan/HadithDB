      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 12: </a> </em>
      <h1> <em> Divorce (Kitab Al-Talaq) </em> </h1>
      <hr>
      <em> <a name="012.2170"> Book 12, Number 2170: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Anyone who incites a woman against her husband or a slave against his master is not one of us. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2172"> Book 12, Number 2172: </a> </em>
      <blockquote>
        <p> Narrated Muharib: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah did not make anything lawful more abominable to Him than divorce. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2173"> Book 12, Number 2173: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: Of all the lawful acts the most detestable to Allah is divorce. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2180"> Book 12, Number 2180: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> AbdurRahman ibn Ayman, the client of Urwah, asked Ibn Umar and AbuzZubayr was was listening: What do you think if a man divorces his wife while she is menstruating? He said: Abdullah ibn Umar divorced his wife while she was menstruating during the time of the Apostle of Allah (peace_be_upon_him).So Umar asked the Apostle of Allah (peace_be_upon_him) saying: Abdullah ibn Umar divorced his wife while she was menstruating. Abdullah said: He returned her to me and did not count it (the pronouncement) anything. He said: When she is purified, he may divorce her or keep her with him. Ibn Umar said: The Prophet (peace_be_upon_him) recited the Qur'anic verse: O Prophet, when you divorce women, divorce them in the beginning of their waiting period." </p>
      </blockquote>
      <hr>
      <em> <a name="012.2181"> Book 12, Number 2181: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> Mutarrif ibn Abdullah reported:Imran ibn Husayn was asked about a person who divorces his wife, and then has intercourse with her, but he does not call any witness to her divorce nor to her restoration. He said: You divorced against the sunnah and took her back against the sunnah. Call someone to bear witness to her divorce, and to her return in marriage, and do not repeat it. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2182"> Book 12, Number 2182: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Umar ibn Mu'tab reported: AbuHasan, a client of Banu Nawfal asked Ibn Abbas: A slave had a wife who was a slave-girl. He divorced her by two pronouncements. Afterwards both of them were freed. Is it permissible for him to ask her in marriage again? He said: Yes. This is a decision given by the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="012.2183"> Book 12, Number 2183: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The aforesaid tradition (No. 2182) has also been transmitted by Ali (ibn al-Mubarak) through a different chain of narrators to the same effect. </p>
        <p> This version adds: Ibn Abbas said: There remained one more pronouncement of divorce for you. The Apostle of Allah (peace_be_upon_him) took the same decision. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2184"> Book 12, Number 2184: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) said: The divorce of a slave-woman consists in saying it twice and her waiting period is two menstrual courses (qur') AbuAsim said: A similar tradition has been narrated to me by Muzahir and al-Qasim on the authority of Aisha from the Prophet (peace_be_upon_him), except that he said: And her waiting period ('iddah) is two courses. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2185"> Book 12, Number 2185: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: There is no divorce except in what you possess; there is no possession, there is no sale transaction till you possess. The narrator Ibn as-Sabbah added: There is no fulfilling a vow till you possess. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2188"> Book 12, Number 2188: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Muhammad ibn Ubayd ibn AbuSalih who lived in Ayliya said: I went out with Adi ibn Adi al-Kindi till we came to Mecca. He sent me to Safiyyah daughter of Shaybah who remembered a tradition (that she had heard) from Aisha. She said: I heard Aisha say: I heard the Apostle of Allah (peace_be_upon_him) say: There is no divorce or emancipation in case of constraint or duress (ghalaq). </p>
      </blockquote>
      <hr>
      <em> <a name="012.2189"> Book 12, Number 2189: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: There are three things which, whether undertaken seriously or in jest, are treated as serious: Marriage, divorce and taking back a wife (after a divorce which is not final) </p>
      </blockquote>
      <hr>
      <em> <a name="012.2190"> Book 12, Number 2190: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Divorced women shall wait concerning themselves for three monthly periods. Nor is it lawful for them to hide what Allah hath created in their wombs. This means that if a man divorced his wife he had the right to take her back in marriage though he had divorced her by three pronouncements. This was then repealed (by a Qur'anic verse). Divorce is only permissible twice. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2191"> Book 12, Number 2191: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Abdu Yazid, the father of Rukanah and his brothers, divorced Umm Rukanah and married a woman of the tribe of Muzaynah. She went to the Prophet (peace_be_upon_him) and said: He is of no use to me except that he is as useful to me as a hair; and she took a hair from her head. So separate me from him. The Prophet (peace_be_upon_him) became furious. He called on Rukanah and his brothers. He then said to those who were sitting beside him. Do you see so-and-so who resembles Abdu Yazid in respect of so-and-so; and so-and-so who resembles him in respect of so-and-so? They replied: Yes. The Prophet (peace_be_upon_him) said to Abdu Yazid: Divorce her. Then he did so. He said: Take your wife, the mother of Rukanah and his brothers, back in marriage. He said: I have divorced her by three pronouncements, Apostle of Allah. He said: I know: take her back. He then recited the verse: "O Prophet, when you divorce women, divorce them at their appointed periods." </p>
      </blockquote>
      <hr>
      <em> <a name="012.2194"> Book 12, Number 2194: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Tawus said: AbusSahba' said to Ibn Abbas: Do you know that a divorce by three pronouncements was made a single one during the time of the Prophet (peace_be_upon_him), and of AbuBakr and in the early days of the caliphate of Umar? He replied: Yes. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2198"> Book 12, Number 2198: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> Hammad ibn Zayd said: I asked Ayyub: Do you know anyone who narrates the tradition narrated by Al-Hasan about uttering the words (addressing wife). "Your matter is in your hand"? He replied: No, except something similar transmitted by Qatadah from Kathir, the client of Samurah, from AbuSalamah on the authority of AbuHurayrah from the Prophet (peace_be_upon_him). Ayyub said: Kathir then came to us; so I asked him (about this matter). He replied: I never narrated it. I mentioned it to Qatadah who said: Yes (he narrated it) but he forgot. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2202"> Book 12, Number 2202: </a> </em>
      <blockquote>
        <p> Narrated Rukanah ibn Abdu Yazid: </p>
        <p> (Rukanah) divorced his wife absolutely; so he came to the Apostle of Allah (peace_be_upon_him). He asked (him): What did you intend? He said: A single utterance of divorce. He said: Do you swear by Allah? He replied: I swear by Allah. He said: It stands as you intended. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2204"> Book 12, Number 2204: </a> </em>
      <blockquote>
        <p> Narrated Tamimah al-Hujayni: </p>
        <p> A man said to his wife: O my younger sister! The Apostle of Allah (peace_be_upon_him)said: Is she your sister? He (the Prophet disliked it and prohibited saying so. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2207"> Book 12, Number 2207: </a> </em>
      <blockquote>
        <p> Narrated Salamah ibn Sakhr al-Bayadi: </p>
        <p> I was a man who was more given than others to sexual intercourse with women. When the month of Ramadan came, I feared lest I should have intercourse with my wife, and this evil should remain with me till the morning. So I made my wife like my mother's back to me till the end of Ramadan. But one night when she was waiting upon me, something of her was revealed. Suddenly I jumped upon her. When the morning came I went to my people and informed them about this matter. </p>
        <p> I said: Go along with me to the Apostle of Allah (peace_be_upon_him). </p>
        <p> They said: No, by Allah. So I went to the Prophet (peace be upon him and informed him of the matter. </p>
        <p> He said: Have you really committed it, Salamah?  I said: I committed it twice, Apostle of Allah. I am content with the Commandment of Allah, the Exalted; so take a decision about me according to what Allah has shown you. </p>
        <p> He said: Free a slave.  I said: By Him Who sent you with truth, I do not possess a neck other than this: and I struck the surface of my neck. </p>
        <p> He said: Then fast two consecutive months. I said: Whatever I suffered is due to fasting. </p>
        <p> He said: Feed sixty poor people with a wasq of dates. </p>
        <p> I said: By Him Who sent you with truth, we passed the night hungry; there was no food in our house. </p>
        <p> He said: Then go to the collector of sadaqah of Banu Zurayq; he must give it to you. Then feed sixty poor people with a wasq of dates; and you and your family eat the remaining dates. Then I came back to my people, and said (to them): I found with you poverty and bad opinion; and I found with the Prophet (peace_be_upon_him) prosperity and good opinion. He has commanded me to give alms to you. </p>
        <p> Ibn al-Ala' added: Ibn Idris said: Bayadah is a sub-clan of Banu Zurayq. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2208"> Book 12, Number 2208: </a> </em>
      <blockquote>
        <p> Narrated Khuwaylah, daughter of Malik ibn Tha'labah: </p>
        <p> My husband, Aws ibn as-Samit, pronounced the words: You are like my mother. So I came to the Apostle of Allah (peace_be_upon_him), complaining to him about my husband. </p>
        <p> The Apostle of Allah (peace_be_upon_him) disputed with me and said: Remain dutiful to Allah; he is your cousin. </p>
        <p> I continued (complaining) until the Qur'anic verse came down: "Allah hath heard the words of her who disputeth with thee concerning her husband...." till the prescription of expiation. </p>
        <p> He then said: He should set free a slave.  She said: He cannot afford it.  He said: He should fast for two consecutive months.  She said: Apostle of Allah, he is an old man; he cannot keep fasts.  He said: He should feed sixty poor people.  She said: He has nothing which he may give in alms. At that moment an araq (i.e. date-basket holding fifteen or sixteen sa's) was brought to him. </p>
        <p> I said: I shall help him with another date-basked ('araq).  He said: You have done well. Go and feed sixty poor people on his behalf, and return to your cousin.  The narrator said: An araq holds sixty sa's of dates. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2212"> Book 12, Number 2212: </a> </em>
      <blockquote>
        <p> Narrated Urwah: </p>
        <p> Khawlah was the wife of Aws ibn as-Samit; he was a man immensely given to sexual intercourse. When his desire for intercourse was intensified, he made his wife like his mother's back. So Allah, the Exalted, sent down Qur'anic verses relating to expiation for zihar. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2214"> Book 12, Number 2214: </a> </em>
      <blockquote>
        <p> Narrated Ikrimah: </p>
        <p> A man made his wife like the back of his mother. He then had intercourse with her before he atoned for it. He came to the Prophet (peace_be_upon_him) and informed him of this matter. He asked (him): What moved you to the action you have committed? He replied: I saw the whiteness of her shins in moon light. He said: Keep away from her until you expiate for your deed. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2218"> Book 12, Number 2218: </a> </em>
      <blockquote>
        <p> Narrated Thawban: </p>
        <p> The Prophet (peace_be_upon_him) said: If any woman asks her husband for divorce without some strong reason, the odour of Paradise will be forbidden to her. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2219"> Book 12, Number 2219: </a> </em>
      <blockquote>
        <p> Narrated Habibah, daughter of Sahl al-Ansariyyah: </p>
        <p> Amrah, daughter of AbdurRahman ibn Sa'd ibn Zurarah, reported on the authority of Habibah, daughter of Sahl al-Ansariyyah: She (Habibah) was the wife of Thabit ibn Qays ibn Shimmas. The Apostle of Allah (peace_be_upon_him) came out one morning and found Habibah by his door. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Who is this?  She replied: I am Habibah, daughter of Sahl. He asked: What is your case?  She replied: I and Thabit ibn Qays, referring to her husband, cannot live together. </p>
        <p> When Thabit ibn Qays came, the Apostle of Allah (peace_be_upon_him) said to him: This is Habibah, daughter of Sahl, and she has mentioned (about you) what Allah wished to mention. </p>
        <p> Habibah said: Apostle of Allah, all that he gave me is with me. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to Thabit ibn Qays: Take it from her. So he took it from her, and she lived among her people (relatives). </p>
      </blockquote>
      <hr>
      <em> <a name="012.2220"> Book 12, Number 2220: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Habibah daughter of Sahl was the wife of Thabit ibn Qays Shimmas He beat her and broke some of her part. So she came to the Prophet (peace_be_upon_him) after morning, and complained to him against her husband. The Prophet (peace_be_upon_him) called on Thabit ibn Qays and said (to him): Take a part of her property and separate yourself from her. He asked: Is that right, Apostle of Allah? He said: Yes. He said: I have given her two gardens of mine as a dower, and they are already in her possession. The Prophet (peace_be_upon_him) said: Take them and separate yourself from her. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2221"> Book 12, Number 2221: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The wife of Thabit ibn Qays separated herself from him for a compensation. The Prophet (peace_be_upon_him) made her waiting period a menstrual course. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2228"> Book 12, Number 2228: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Barirah was emancipated, and she was the wife of Mughith, a slave of Aal AbuAhmad. The Apostle of Allah (peace_be_upon_him) gave her choice, and said to her: If he has intercourse with you, then there is no choice for you. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2229"> Book 12, Number 2229: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Al-Qasim said: Aisha intended to set free two slaves of her who were spouses. She, therefore, asked the Prophet (peace_be_upon_him) about this matter. He commanded to begin with the man before the woman. The narrator Nasr said: AbuAli al-Hanafi reported it to me on the authority of Ubaydullah. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2230"> Book 12, Number 2230: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A man came after embracing Islam during the time of the Apostle of Allah (peace_be_upon_him). Afterwards his wife came after embracing Islam. He said: Apostle of Allah, she embraced Islam along with me; so restore her to me. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2231"> Book 12, Number 2231: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A woman embraced Islam during the time of the Apostle of Allah (peace_be_upon_him); she then married. Her (former) husband then came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, I have already embraced Islam, and she had the knowledge about my Islam. The Apostle of Allah (peace_be_upon_him) took her away from her latter husband and restored her to her former husband. </p>
      </blockquote>
      <hr>
      <em> <a name="012.2232"> Book 12, Number 2232: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) restored his daughter Zaynab to Abul'As on the basis of the previous marriage, and he did not do anything afresh. </p>
