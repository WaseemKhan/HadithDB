      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 37: </a> </em>
      <h1> <em> Battles (Kitab Al-Malahim) </em> </h1>
      <hr>
      <em> <a name="037.4278"> Book 37, Number 4278: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah will raise for this community at the end of every hundred years the one who will renovate its religion for it. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4281"> Book 37, Number 4281: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> The Prophet (peace_be_upon_him) said: The flourishing state of Jerusalem will be when Yathrib is in ruins, the ruined state of Yathrib will be when the great war comes, the outbreak of the great war will be at the conquest of Constantinople and the conquest of Constantinople when the Dajjal (Antichrist) comes forth. He (the Prophet) struck his thigh or his shoulder with his hand and said: This is as true as you are here or as you are sitting (meaning Mu'adh ibn Jabal). </p>
      </blockquote>
      <hr>
      <em> <a name="037.4282"> Book 37, Number 4282: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> The Prophet (peace_be_upon_him) said: The greatest war, the conquest of Constantinople and the coming forth of the Dajjal (Antichrist) will take place within a period of seven months. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4283"> Book 37, Number 4283: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Busr: </p>
        <p> The Prophet (peace_be_upon_him) said: The time between the great war and the conquest of the city (Constantinople) will be six years, and the Dajjal (Antichrist) will come forth in the seventh. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4284"> Book 37, Number 4284: </a> </em>
      <blockquote>
        <p> Narrated Thawban: </p>
        <p> The Prophet (peace_be_upon_him) said: The people will soon summon one another to attack you as people when eating invite others to share their dish. Someone asked: Will that be because of our small numbers at that time? He replied: No, you will be numerous at that time: but you will be scum and rubbish like that carried down by a torrent, and Allah will take fear of you from the breasts of your enemy and last enervation into your hearts.  Someone asked: What is wahn (enervation). Apostle of Allah (peace_be_upon_him): He replied: Love of the world and dislike of death. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4285"> Book 37, Number 4285: </a> </em>
      <blockquote>
        <p> Narrated AbudDarda': </p>
        <p> The Prophet (peace_be_upon_him) said: The place of assembly of the Muslims at the time of the war will be in al-Ghutah near a city called Damascus, one of the best cities in Syria. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4287"> Book 37, Number 4287: </a> </em>
      <blockquote>
        <p> Narrated Awf ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah will not gather two swords upon this community: Its own sword and the sword of its enemy. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4288"> Book 37, Number 4288: </a> </em>
      <blockquote>
        <p> Narrated One of the Companions: </p>
        <p> The Prophet (peace_be_upon_him) said: Let the Abyssinians alone as long as they let you alone, and let the Turks alone as long as they leave you alone. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4292"> Book 37, Number 4292: </a> </em>
      <blockquote>
        <p> Narrated AbuBakrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Some of my people will alight on low-lying ground, which they will call al-Basrah, beside a river called Dajjal (the Tigris) over which there is a bridge. Its people will be numerous and it will be one of the capital cities of immigrants (or one of the capital cities of Muslims, according to the version of Ibn Yahya who reported from AbuMa'mar). </p>
        <p> At the end of time the descendants of Qantura' will come with broad faces and small eyes and alight on the bank of the river. The town's inhabitants will then separate into three sections, one of which will follow cattle and (live in) the desert and perish, another of which will seek security for themselves and perish, but a third will put their children behind their backs and fight the invaders, and they will be the martyrs. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4293"> Book 37, Number 4293: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: The people will establish cities, Anas, and one of them will be called al-Basrah or al-Busayrah. If you should pass by it or enter it, avoid its salt-marshes, its Kall, its market, and the gate of its commanders, and keep to its environs, for the earth will swallow some people up, pelting rain will fall and earthquakes will take place in it, and there will be people who will spend the night in it and become apes and swine in the morning. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4294"> Book 37, Number 4294: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> Salih ibn Dirham said: We went on the pilgrimage and met a man who asked us: Is there a town near you called al-Ubullah?  We said: Yes.  He said: Is there any of you who will undertake to pray two or four rak'ahs on my behalf in the mosque of al-Ashshar, stating "they are on behalf of AbuHurayrah"? </p>
        <p> He (AbuHurayrah) said: I heard my friend AbulQasim (peace_be_upon_him) say: On the Day of Resurrection Allah will raise martyrs from the mosque of al-Ashshar, who will be the only ones to rise with the martyrs of Badr. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4295"> Book 37, Number 4295: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: Leave the Abyssinians alone as long as they leave you alone, for it is only the Abyssinian with short legs who will seek to take out the treasure of the Ka'bah. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4305"> Book 37, Number 4305: </a> </em>
      <blockquote>
        <p> Narrated Imran ibn Husayn: </p>
        <p> The Prophet (peace_be_upon_him) said: Let him who hears of the Dajjal (Antichrist) go far from him for I swear by Allah that a man will come to him thinking he is a believer and follow him because of confused ideas roused in him by him. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4306"> Book 37, Number 4306: </a> </em>
      <blockquote>
        <p> Narrated Ubadah ibn as-Samit: </p>
        <p> The Prophet (peace_be_upon_him) said: I have told you so much about the Dajjal (Antichrist) that I am afraid you may not understand. The Antichrist is short, hen-toed, woolly-haired, one-eyed, an eye-sightless, and neither protruding nor deep-seated. If you are confused about him, know that your Lord is not one-eyed. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4310"> Book 37, Number 4310: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: There is no prophet between me and him, that is, Jesus (peace_be_upon_him). He will descent (to the earth). When you see him, recognise him: a man of medium height, reddish fair, wearing two light yellow garments, looking as if drops were falling down from his head though it will not be wet. He will fight the people for the cause of Islam. He will break the cross, kill swine, and abolish jizyah. Allah will perish all religions except Islam. He will destroy the Antichrist and will live on the earth for forty years and then he will die. The Muslims will pray over him. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4311"> Book 37, Number 4311: </a> </em>
      <blockquote>
        <p> Narrated Fatimah, daughter of Qays: </p>
        <p> The Apostle of Allah (peace_be_upon_him) once delayed the congregational night prayer. </p>
        <p> He came out and said: The talk of Tamim ad-Dari detained me. He transmitted it to me from a man who was on of of the islands of the sea. All of a sudden he found a woman who was trailing her hair. He asked: Who are you? </p>
        <p> She said: I am the Jassasah. Go to that castle. So I came to it and found a man who was trailing his hair, chained in iron collars, and leaping between Heaven and Earth. </p>
        <p> I asked: Who are you?  He replied: I am the Dajjal (Antichrist). Has the Prophet of the unlettered people come forth now?  I replied: Yes.  He said: Have they obeyed him or disobeyed him?  I said: No, they have obeyed him.  He said: That is better for them. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4314"> Book 37, Number 4314: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said one day from the pulpit: When some people were sailing in the sea, their food was finished. An island appeared to them. They went out seeking bread. They were met by the Jassasah (the Antichrist's spy). </p>
        <p> I said to AbuSalamah: What is the Jassasah?  He replied: A woman trailing the hair of her skin and of her head.  She said: In this castle. He then narrated the rest of the (No. 4311) tradition. He asked about the palm-trees of Baysan and the spring of Zughar.  He said: He is the Antichrist.  Ibn Salamah said to me: There is something more in this tradition, which I could not remember. He said: Jabir testified that it was he who was Ibn Sayyad. </p>
        <p> I said: He died.  He said: Let him die.  I said: He accepted Islam.  He said: Let him accept Islam.  I said: He entered Medina.  He said: Let him enter Medina. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4316"> Book 37, Number 4316: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Nafi' told that Ibn Umar used to say: I swear by Allah that I do not doubt that Antichrist is Ibn Sayyad. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4317"> Book 37, Number 4317: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> Muhammad ibn al-Munkadir told that he saw Jabir ibn Abdullah swearing by Allah that Ibn as-Sa'id was the Dajjal (Antichrist). I expressed my surprise by saying: You swear by Allah! He said: I heard Umar swearing to that in the presence of the Apostle of Allah (peace_be_upon_him), but the Apostle of Allah (peace_be_upon_him) did not make any objection to it. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4318"> Book 37, Number 4318: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> We saw the last of Ibn Sayyad at the battle of the Harrah. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4319"> Book 37, Number 4319: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The Last Hour will not come before there come forth thirty Dajjals (fraudulents), everyone presuming himself that he is an apostle of Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4320"> Book 37, Number 4320: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The Last Hour will not come before there come forth thirty liar Dajjals (fraudulents) lying on Allah and His Apostle. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4322"> Book 37, Number 4322: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: The first defect that permeated Banu Isra'il was that a man (of them) met another man and said: O so-and-so, fear Allah, and abandon what you are doing, for it is not lawful for you. He then met him the next day and that did not prevent him from eating with him, drinking with him and sitting with him. When they did so. Allah mingled their hearts with each other. </p>
        <p> He then recited the verse: "curses were pronounced on those among the children of Isra'il who rejected Faith, by the tongue of David and of Jesus the son of Mary"...up to "wrongdoers". </p>
        <p> He then said: By no means, I swear by Allah, you must enjoin what is good and prohibit what is evil, prevent the wrongdoer, bend him into conformity with what is right, and restrict him to what is right. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4323"> Book 37, Number 4323: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> A similar tradition (to the No. 4322) has also been transmitted by Ibn Mas'ud through a different chain of narrators to the same effect. </p>
        <p> This version adds: "Or Allah will mingle your hearts together and curse you as He cursed them." </p>
      </blockquote>
      <hr>
      <em> <a name="037.4324"> Book 37, Number 4324: </a> </em>
      <blockquote>
        <p> Narrated AbuBakr: </p>
        <p> You people recite this verse "You who believe, care for yourselves; he who goes astray cannot harm you when you are rightly-guided," and put it in its improper place. </p>
        <p> Khalid's version has: We heard the Prophet (peace_be_upon_him) say: When the people see a wrongdoer and do not prevent him, Allah will soon punish them all. Amr ibn Hushaym's version has: I heard the Apostle of Allah (peace_be_upon_him) say: If acts of disobedience are done among any people and do not change them though the are able to do so, Allah will soon punish them all. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4325"> Book 37, Number 4325: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If any man is among a people in whose midst he does acts of disobedience, and, though they are able to make him change (his acts), they do not change, Allah will smite them with punishment before they die. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4327"> Book 37, Number 4327: </a> </em>
      <blockquote>
        <p> Narrated AbuTha'labah al-Khushani: </p>
        <p> AbuUmayyah ash-Sha'bani said: I asked AbuTha'labah al-Khushani: What is your opinion about the verse "Care for yourselves". </p>
        <p> He said: I swear by Allah, I asked the one who was well informed about it; I asked the Apostle of Allah (peace_be_upon_him) about it. </p>
        <p> He said: No, enjoin one another to do what is good and forbid one another to do what is evil. </p>
        <p> But when you see niggardliness being obeyed, passion being followed, worldly interests being preferred, everyone being charmed with his opinion, then care for yourself, and leave alone what people in general are doing; for ahead of you are days which will require endurance, in which showing endurance will be like grasping live coals. The one who acts rightly during that period will have the reward of fifty men who act as he does. </p>
        <p> Another version has: He said (The hearers asked:) Apostle of Allah, the reward of fifty of them? </p>
        <p> He replied: The reward of fifty of you. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4328"> Book 37, Number 4328: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: How will you do when that time will come? Or he said: A time will soon come when the people are sifted and only dregs of mankind survive and their covenants and guarantees have been impaired and they have disagreed among themselves and become thus, interwining his fingers. They asked: What do you order us to do, Apostle of Allah? He replied: Accept what you approve, abandon what  you disapprove, attend to your own affairs and leave alone the affairs of the generality. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4329"> Book 37, Number 4329: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> When we were around the Apostle of Allah (peace_be_upon_him), he mentioned the period of commotion (fitnah) saying: When you see the people that their covenants have been impaired, (the fulfilling of) the guarantees becomes rare, and they become thus (interwining his fingers). I then got up and said: What should I do at that time, may Allah make me ransom for you? He replied: Keep to your house, control your tongue, accept what you approve, abandon what you disapprove, attend to your own affairs, and leave alone the affairs of the generality. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4330"> Book 37, Number 4330: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: The best fighting (jihad) in the path of Allah is (to speak) a word of justice to an oppressive ruler. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4331"> Book 37, Number 4331: </a> </em>
      <blockquote>
        <p> Narrated Amirah al-Kindi: </p>
        <p> The Prophet (peace_be_upon_him) said: When sin is done in the earth, he who sees it and disapproves of it will be taken like one who was not present, but he who is not present and approves of it will be like him who sees. </p>
      </blockquote>
      <hr>
      <em> <a name="037.4335"> Book 37, Number 4335: </a> </em>
      <blockquote>
        <p> Narrated AbuTha'labah al-Khushani: </p>
        <p> The Prophet (peace_be_upon_him) said: Allah will not fail to detain this community for less than half a day. </p>
      </blockquote>
      <hr>
