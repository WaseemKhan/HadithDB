      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 13: </a> </em>
      <h1> <em> Fasting (Kitab Al-Siyam) </em> </h1>
      <hr>
      <em> <a name="013.2306"> Book 13, Number 2306: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Ibn Abbas explained the following Qur'anic verse: "O ye who believe! fasting is prescribed for you as it was prescribed for those before you" During the lifetime of the Prophet (peace_be_upon_him), when the people offered night prayer, they were asked to abstain from food and drink and (intercourse with) women, they kept fast till the next night. A man betrayed himself and had intercourse with his wife after he had offered the night prayer, and he did not break his fast. So Allah, the Exalted, intended to make it (fasting) easy for those who survived,  thus providing a concession and utility. Allah, the Glorified, said: "Allah knoweth what ye used to do secretly among yourselves." By this Allah benefited the people and provided concession and ease to them. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2311"> Book 13, Number 2311: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Explaining the verse; "For those who can do it (with hard-ship) is a ransom, the feeding of one, that is indigent," he said: This was a concession granted to the aged man and woman who were able to keep fast; they were allowed to leave the fast and instead feed an indigent person for each fast; (and a concession) to pregnant and suckling woman when they apprehended harm (to themselves). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2312"> Book 13, Number 2312: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: The month consists of twenty-nine days, but do not fast till you sight it (the moon) and do not break your fast till you sight it. If the weather is cloudy, calculate it thirty days. When the twenty-ninth of Sha'ban came, Ibn Umar would send someone (who tried) to sight the moon for him. If it was sighted, then well and good; in case it was not sighted, and there was no cloud and dust before him (on the horizon), he would not keep fast the next day. If there appeared (on the horizon) before him cloud or dust, he would fast the following day. Ibn Umar would end his fasting alone with the people, and did not follow this calculation. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2315"> Book 13, Number 2315: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> We kept fast for twenty-nine days along with the Prophet (peace_be_upon_him) more often than we kept fast for thirty days. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2317"> Book 13, Number 2317: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The end of Ramadan is on the day when you end it, and the 'Id (festival) of sacrifice is on the day when you sacrifice. The whole of Arafah is the place of staying, and the whole of Mina is the place of sacrifice, and all the roads of Mecca are the place of sacrifice, and the whole of Muzdalifah is the place of staying. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2318"> Book 13, Number 2318: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to count the days in Sha'ban in a manner he did not count any other month; then he fasted when he sighted the new moon of Ramadan; but if the weather was cloudy he counted thirty days and then fasted. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2319"> Book 13, Number 2319: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not fast (for Ramadan) before the coming of the month until you sight the moon or complete the number (of thirty days); then fast until you sight the moon or complete the number (of thirty days). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2320"> Book 13, Number 2320: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not fast one day or two days just before Ramadan except in the case of a man who has been in the habit or observing a fast (on that day); and do not fast until you sight it (the moon). Then fast until you sight it. If a cloud appears on that day (i.e. 29th of Ramadan) then complete the number thirty (days) and then end the fasting: a month consists of twenty-nine days. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2322"> Book 13, Number 2322: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah: </p>
        <p> AbulAzhar al-Mughirah ibn Farwah said: Mu'awiyah stood among the people at Dayr Mustahill lying at the gate of Hims. He said: O people, we sighted the moon on such-and-such day. We shall fast in advance. Anyone who likes to do so may do it. Malik ibn Hubayrah as-Saba'i stood up and asked: Mu'awiyah, did you hear the Apostle of Allah (peace_be_upon_him) say something (about this matter), or is this something on the basis of your opinion? He replied: I heard the Apostle of Allah (peace_be_upon_him) as saying: Fast the month (in the beginning) and in the last. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2327"> Book 13, Number 2327: </a> </em>
      <blockquote>
        <p> Narrated Ammar: </p>
        <p> AbuIshaq reported on the authority of Silah: We were with Ammar on the day when the appearance of the moon was doubtful. (The meat of) goat was brought to him. Some people kept aloof from (eating) it. Ammar said: He who keeps fast on this day disobeys AbulQasim (i.e. the Prophet) (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2329"> Book 13, Number 2329: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> She never saw the Prophet (peace_be_upon_him) fasting the whole month except Sha'ban which he combined with Ramadan. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2330"> Book 13, Number 2330: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> AbdulAziz ibn  Muhammad said: Abbad ibn Kathir came to Medina and went to the assembly of al-Ala'. He caught hold of his hand and made him stand and said: O Allah, he narrates a tradition from his father on the authority of AbuHurayrah who reported the Apostle of Allah (peace_be_upon_him) as saying: When the middle of Sha'ban comes, do not fast. Al-Ala' said: O Allah, my father narrated this tradition on the authority of AbuHurayrah from the Prophet (peace_be_upon_him) </p>
      </blockquote>
      <hr>
      <em> <a name="013.2331"> Book 13, Number 2331: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Husayn ibn al-Harith al-Jadli from the tribe of Jadilah Qays said: The governor of Mecca delivered a speech and said: The Apostle of Allah (peace_be_upon_him) took a pledge from us that we should perform the rites of hajj after sighting the moon. If we do not sight it and two reliable persons bear witness, we should perform the rites of hajj on the basis of their witness. </p>
        <p> I then asked al-Husayn ibn al-Harith: Who was the governor of Mecca?  He replied: I do not know. He then met me later on and told me: He was al-Harith ibn Hatib, brother of Muhammad ibn Hatib. The governor then said: There is among you a man who is more acquainted with Allah and His Apostle than I. He witnessed this from the Apostle of Allah (peace_be_upon_him). He then pointed with his hand to a man. Al-Husayn said: I asked an old man beside me: Who is that man to whom the governor has alluded? </p>
        <p> He said: "This is Abdullah ibn Umar, and he spoke the truth. He was more acquainted with Allah than he. He (Abdullah ibn Umar) said: For this is what the Apostle of Allah (peace_be_upon_him) commanded us (to do). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2333"> Book 13, Number 2333: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A bedouin came to the Prophet (peace_be_upon_him) and said: I have sighted the moon. Al-Hasan added in his version: that is, of Ramadan. He asked: Do you testify that there is no god but Allah? He replied: Yes. He again asked: Do you testify that Muhammad is the Apostle of Allah? He replied: Yes. and he testified that he had sighted the moon. He said: Bilal, announce to the people that they must fast tomorrow. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2334"> Book 13, Number 2334: </a> </em>
      <blockquote>
        <p> Narrated Ikrimah: </p>
        <p> Once the people doubted the appearance of the moon of Ramadan, and intended neither to offer the tarawih prayer nor to keep fast. A bedouin came from al-Harrah and testified that he had sighted the moon. He was brought to the Prophet (peace_be_upon_him). He asked: Do you testify that there is no god but Allah, and that I am the Apostle of Allah? He said: Yes; and he testified that he had sighted the moon. He commanded Bilal who announced to the people to offer the tarawih prayer and to keep fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2335"> Book 13, Number 2335: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The people looked for the moon, so I informed the Apostle of Allah (peace_be_upon_him) that I had sighted it. He fasted and commanded the people to fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2337"> Book 13, Number 2337: </a> </em>
      <blockquote>
        <p> Narrated Al-Irbad ibn Sariyyah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) invited me to a meal shortly before dawn in Ramadan saying: Come to the blessed morning meal. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2341"> Book 13, Number 2341: </a> </em>
      <blockquote>
        <p> Narrated Talq ibn Ali al-Yamami: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Eat and drink; let not the white and ascending light prevent you from (eating and drinking); so eat and drink until the red light spreads horizontally. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2343"> Book 13, Number 2343: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When any of you hears the summons to prayer while he has a vessel in his hand, he should not lay it down till he fulfils his need. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2346"> Book 13, Number 2346: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Religion will continue to prevail as long as people hasten to break the fast, because the Jews and the Christians delay doing so. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2348"> Book 13, Number 2348: </a> </em>
      <blockquote>
        <p> Narrated Salman ibn Amir: </p>
        <p> The Prophet (peace_be_upon_him) said: When one of you is fasting, he should break his fast with dates; but if he cannot get any, then (he should break his fast) with water, for water is purifying. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2349"> Book 13, Number 2349: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to break his fast before praying with some fresh dates; but if there were no fresh dates, he had a few dry dates, and if there were no dry dates, he took some mouthfuls of water. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2350"> Book 13, Number 2350: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Marwan ibn Salim al-Muqaffa' said: I saw Ibn Umar holding his bread with his hand and cutting what exceeded the handful of it. He (Ibn Umar) told that the Prophet (peace_be_upon_him) said when he broke his fast: Thirst has gone, the arteries are moist, and the reward is sure, if Allah wills. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2351"> Book 13, Number 2351: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Zuhrah: </p>
        <p> The Prophet of Allah (peace_be_upon_him) used to say when he broke his fast: O Allah, for Thee I have fasted, and with Thy provision I have broken my fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2357"> Book 13, Number 2357: </a> </em>
      <blockquote>
        <p> Narrated Amir ibn Rabi'ah: </p>
        <p> I have seen the Apostle of Allah (peace_be_upon_him) using a tooth-stick while he was fasting. Musaddad added in his version: "more often than I could count." </p>
      </blockquote>
      <hr>
      <em> <a name="013.2358"> Book 13, Number 2358: </a> </em>
      <blockquote>
        <p> Narrated A Companion of the Prophet: </p>
        <p> AbuBakr ibn AbdurRahman reported on the authority of a Companion of the Prophet (peace_be_upon_him): I saw the Prophet (peace_be_upon_him) commanding the people while he was travelling on the occasion of the conquest of Mecca not to observe fast. He said: Be strong for your enemy. The Apostle of Allah (peace_be_upon_him) fasted himself. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2359"> Book 13, Number 2359: </a> </em>
      <blockquote>
        <p> Narrated AbuBakr: </p>
        <p> A man who narrated his tradition to me said: I have seen the Apostle of Allah (peace_be_upon_him) in al-Arj pouring water over his head while he was fasting, either because of thirst or because of heat. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2360"> Book 13, Number 2360: </a> </em>
      <blockquote>
        <p> Narrated Laqit ibn Saburah: </p>
        <p> The Prophet (peace_be_upon_him) said: Snuff up water freely unless you are fasting. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2361"> Book 13, Number 2361: </a> </em>
      <blockquote>
        <p> Narrated Thawban: </p>
        <p> The Prophet (peace_be_upon_him) said: A man who cupped and a man  who has himself cupped broke their fast. The narrator Shayban said in his version: AbuQilabah told me that AbuAsma' ar-Rahbi told him that Thawban, the client of the Apostle of Allah (peace_be_upon_him), told him that he heard the Prophet (peace_be_upon_him) say this. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2362"> Book 13, Number 2362: </a> </em>
      <blockquote>
        <p> Narrated Shaddad ibn Aws: </p>
        <p> The tradition mentioned above (No. 2361) has also been transmitted by Shaddad ibn Aws through a different chain of narrators. </p>
        <p> This version adds: While Shaddad ibn Aws was walking along with the Prophet (peace_be_upon_him)....The narrator then transmitted the rest of the tradition to the same effect. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2367"> Book 13, Number 2367: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) had himself cupped when he was fasting and wearing ihram (pilgrim garb). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2370"> Book 13, Number 2370: </a> </em>
      <blockquote>
        <p> Narrated A man from the Companions: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Neither vomiting, nor emission, nor cupping breaks the fast of the one who is fasting. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2374"> Book 13, Number 2374: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: if one has a sudden attack of vomiting while one is fasting, no atonement is required of him, but if he vomits intentionally he must make atonement. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2378"> Book 13, Number 2378: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to kiss me when he was fasting and when I was fasting. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2379"> Book 13, Number 2379: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> I became gay and happy, so I kissed while I was fasting, I then said: Apostle of Allah, I have done a big deed; I kissed while I was fasting. He said: What do you think if you rinse your mouth with water while you are fasting. The narrator Isa ibn Hammad said in his version: I said to him: There is no harm in it. Then both of them agreed on the version: He said: Then what? </p>
      </blockquote>
      <hr>
      <em> <a name="013.2380"> Book 13, Number 2380: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) used to kiss her and suck her tongue when he was fasting. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2381"> Book 13, Number 2381: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> A man  asked the Prophet (peace_be_upon_him) whether one who was fasting could embrace (his wife) and he gave him permission; but when another man came to him, and asked him, he forbade him. The one to whom he gave permission was an old man and the one whom he forbade was a youth. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2390"> Book 13, Number 2390: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone breaks his fast one day in Ramadan without a concession granted to him by Allah, a perpetual fast will not atone for it. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2402"> Book 13, Number 2402: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> A man from Banu Abdullah ibn Ka'b brethren of Banu Qushayr (not Anas ibn Malik, the well-known Companion), said: A contingent from the cavalry of the Apostle of Allah (peace_be_upon_him) raided us. I reached (for he said went) to the Apostle of Allah (peace_be_upon_him) who was taking his meals. He said: Sit down, and take some from this meal of ours. I said: I am fasting, he said: Sit down, I shall tell you about prayer and fasting. Allah has remitted half the prayer to a traveller, and fasting to the traveller, the woman who is suckling an infant and the woman who is pregnant, I swear by Allah, he mentioned both (i.e. suckling and pregnant women) or one of them. I was grieved for not taking the food of the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2404"> Book 13, Number 2404: </a> </em>
      <blockquote>
        <p> Narrated Salamah ibn al-Muhabbaq al-Hudhali: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: If anyone has a riding beast which carries him to where he can get sufficient food, he should keep the fast of Ramadan wherever he is when it comes. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2406"> Book 13, Number 2406: </a> </em>
      <blockquote>
        <p> Narrated AbuBusrah al-Ghifari,: </p>
        <p> Ja'far ibn Jubayr said: I accompanied AbuBusrah al-Ghifari, a Companion of the Apostle of Allah (peace_be_upon_him), in a boat proceeding from al-Fustat (Cairo) during Ramadan. He was lifted (to the boat), then his meal was brought to him. The narrator Ja'far said in his version: He did not go beyond the houses (of the city) but he called for the dining sheet. He said (to me): Come near. I said: Do you not see the houses? AbuBusrah said: Do you detest the sunnah (practice) of the Apostle of Allah (peace_be_upon_him)? The narrator Ja'far said in his version: He then ate (it). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2407"> Book 13, Number 2407: </a> </em>
      <blockquote>
        <p> Narrated Dihyah: </p>
        <p> Mansur al-Kalbi said: Dihyah ibn Khalifah once went out from a village of Damascus at as much distance as it measures between Aqabah and al-Fustat during Ramadan; and that is three miles. He then broke his fast and the people broke their fast along with him. But some of them disliked to break their fast. When he came back to his village, he said: I swear by Allah, today I witnessed a thing of which I could not even think to see. The people detested the way of the Apostle of Allah (peace_be_upon_him) and his Companions. He said this to those who fasted. At this moment he said: O Allah, make me die. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2409"> Book 13, Number 2409: </a> </em>
      <blockquote>
        <p> Narrated AbuBakrah: </p>
        <p> The Prophet (peace_be_upon_him) said: One of you should not say: I fasted the whole of Ramadan, and I prayed during the night in the whole of Ramadan. I do not know whether he disliked the purification; or he (the narrator) said: He must have slept a little and taken rest. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2413"> Book 13, Number 2413: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> The Prophet (peace_be_upon_him) said: The day of Arafah, the day of sacrifice, the days of tashriq are (the days of) our festival, O people of Islam. These are the days of eating and drinking. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2415"> Book 13, Number 2415: </a> </em>
      <blockquote>
        <p> Narrated As-Samma' sister of Abdullah ibn Busr: </p>
        <p> The Prophet (peace_be_upon_him) said: Do not fast on Saturday except what has been made obligatory on you; and if one of you can get nothing but a grape skin or a piece of wood from a tree, he should chew it. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2419"> Book 13, Number 2419: </a> </em>
      <blockquote>
        <p> Narrated AbuQatadah: </p>
        <p> A man came to the Prophet (peace_be_upon_him) and said: How do you fast, Apostle of Allah? The Apostle of Allah (peace_be_upon_him) became angry at what he said. </p>
        <p> When Umar observed this (his anger), he said: We are satisfied with Allah as Lord, with Islam as religion, and with Muhammad as Prophet. We seek refuge in Allah from the anger of Allah, and from the anger of His Apostle. Umar continued to repeat these words till his anger cooled down. He then asked: Apostle of Allah, what is the position of one who observes a perpetual fast? </p>
        <p> He replied: May he not fast or break his fast.  Musaddad said in his version: He has neither fasted nor broken his fast. The narrator, Ghaylan, doubted the actual wordings. </p>
        <p> He asked: What is the position of one who fasts two days and does not fast one day? </p>
        <p> He said: Is anyone able to do that?  He asked: What is the position of one who fasts every second day (i.e. fasts one day and does not fasts the next day)? </p>
        <p> He (the Prophet) said: This is the fast that David observed. </p>
        <p> He asked: Apostle of Allah, what is the position of one who fasts one day and breaks it for two days?  He replied: I wish I were given the power to observe that. Then the Apostle of Allah (peace_be_upon_him) said: The observance of three days' fast every month and of one Ramadan to the other (i.e. the fast of Ramadan every year) is (equivalent to) a perpetual fast. I seek from Allah that fasting on the day of Arafah may atone for the sins of the preceding and the coming year, and I seek from Allah that fasting on the day of Ashura' may atone for the sins of the preceding year. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2422"> Book 13, Number 2422: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn al-Harith ; or Uncle of Mujibah al-Bahiliyyah: </p>
        <p> The father or Uncle of Mujibah al-Bahiliyyah visited the Apostle of Allah (peace_be_upon_him). He then went away and came to him (again) after one year when his condition and appearance had changed. </p>
        <p> He said: Apostle of Allah, do you not recognize me?  He asked: Who are you?  He replied: I am al-Bahili who came to you last year.  He said: What has changed you? You were looking well, then you were good in appearance?  He said: I have only food at night since I departed from you. </p>
        <p> Thereupon the Apostle of Allah (peace_be_upon_him) said: Why did you torment yourself? Fast during Ramadan (the month of patience) and fast for one day every month.  He said: Increase it for me, for I have (more) strength.  He said: Fast two days.  He again said: Increase it for me.  He said: Fast three days.  He again said: Increase it for me. He said: Fast during the inviolable months and then stop; fast during the inviolable months and then stop; fast during the inviolable months and then stop. He indicated by his three fingers, and joined them and then opened them. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2425"> Book 13, Number 2425: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The month most liked by the Apostle of Allah (peace_be_upon_him) for fasting was Sha'ban. He then joined it with Ramadan. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2426"> Book 13, Number 2426: </a> </em>
      <blockquote>
        <p> Narrated Muslim al-Qurashi: </p>
        <p> I asked or someone asked the Prophet (peace_be_upon_him) about perpetual fasting. He replied: You have a duty to your family. Fast during Ramadan and the following month, and every Wednesday and Thursday. You will then have observed a perpetual fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2430"> Book 13, Number 2430: </a> </em>
      <blockquote>
        <p> Narrated Usamah ibn Zayd: </p>
        <p> The client of Usamah ibn Zayd said that he went along with Usamah to Wadi al-Qura in pursuit of his camels. He would fast on Monday and Thursday. His client said to him: Why do you fast on Monday and Thursday, while you are an old man? He said: The Prophet of Allah (peace_be_upon_him) used to fast on Monday and Thursday. When he was asked about it, he said: The works of the servants (of Allah) are presented (to Allah) on Monday and Thursday. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2431"> Book 13, Number 2431: </a> </em>
      <blockquote>
        <p> Narrated One of the wives of the Prophet: </p>
        <p> Hunaydah ibn Khalid narrated from his wife on the authority of one of the wives of the Prophet (peace_be_upon_him) who said: The Apostle of Allah (peace_be_upon_him) used to fast the first nine days of Dhul-Hijjah, Ashura' and three days of every month, that is, the first Monday (of the month) and Thursday. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2434"> Book 13, Number 2434: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> Ikrimah said: We were with AbuHurayrah in his house when he narrated to us: The Apostle of Allah (peace_be_upon_him) prohibited fasting on the day of Arafah at Arafah. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2441"> Book 13, Number 2441: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Maslamah: </p>
        <p> AbdurRahman reported on the authority of his uncle that the people of the tribe Aslam came to the Prophet (peace_be_upon_him). He said (to them): Did you fast on this day? They replied: No. He said: Complete the rest of your day, and make atonement for it. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2443"> Book 13, Number 2443: </a> </em>
      <blockquote>
        <p> Narrated Qatadah Ibn Malhan al-Qaysi: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to command us to fast the days of the white (nights): thirteenth, fourteenth and fifteenth of the month. He said: This is like keeping perpetual fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2444"> Book 13, Number 2444: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to fast three days every month. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2445"> Book 13, Number 2445: </a> </em>
      <blockquote>
        <p> Narrated Hafsah, Ummul Mu'minin: </p>
        <p> The apostle of Allah (peace_be_upon_him) used to fast three days every month: Monday, Thursday and Monday in the next week. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2446"> Book 13, Number 2446: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> Hunaydah al-Khuza'i reported on the authority of her mother who said: I entered upon Umm Salamah and asked her about fasting. She said: The Apostle of Allah (peace_be_upon_him) used to command me to fast three days every month beginning with Monday or Thursday. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2446a"> Book 13, Number 2446a: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> According to the version of an-Nufayli, Aisha said: The Property (peace_be_upon_him) used to pass by a patient while he was observing i'tikaf (in the mosque), but he passed as usual, and did not stay asking about him. According to the version of Ibn Isa she said: The Prophet (peace_be_upon_him) would visit a patient while he was observing i'tikaf. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2448"> Book 13, Number 2448: </a> </em>
      <blockquote>
        <p> Narrated Hafsah, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: He who does not determine to fast before dawn does not fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2450"> Book 13, Number 2450: </a> </em>
      <blockquote>
        <p> Narrated Umm Hani: </p>
        <p> On the days of the  conquest of Mecca, when Mecca was captured, Fatimah came and sat on the left side of the Apostle of Allah (peace_be_upon_him), and Umm Hani was on his right side. A slave-girl brought a vessel which contained some drink; she gave it to him and he drank of it. He then gave it to Umm Hani who drank of it. She said: Apostle of Allah, I have broken my fast; I was fasting. He said to her: Were you making atonement for something? She replied: No. He said: Then it does not harm you if it was voluntary (fast). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2451"> Book 13, Number 2451: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Some food was presented to me and Hafsah. We were fasting, but broke our fast. Then the Apostle of Allah (peace_be_upon_him) entered upon us. We said to him: A gift was presented to us; we coveted it and we broke our fast. The Apostle of Allah (peace_be_upon_him) said: There is no harm to you; keep a fast another day in lieu of it. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2453"> Book 13, Number 2453: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> A woman came to the Prophet (peace_be_upon_him) while we were with him. </p>
        <p> She said: Apostle of Allah, my husband, Safwan ibn al-Mu'attal, beats me when I pray, and makes me break my fast when I keep a fast, and he does not offer the dawn prayer until the sun rises. </p>
        <p> He asked Safwan, who was present, about what she had said.  He replied: Apostle of Allah, as for her statement "he beats me when I pray", she recites two surahs (during prayer) and I have prohibited her (to do so). </p>
        <p> He (the Prophet) said: If one surah is recited (during prayer), that is sufficient for the people. </p>
        <p> (Safwan continued:) As regards her saying "he makes me break my fast," she dotes on fasting; I am a young man, I cannot restrain myself. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said on that day: A woman should not fast except with the permission of her husband. </p>
        <p> (Safwan said:) As for her statement that I do not pray until the sun rises, we are a people belonging to a class, and that (our profession of supplying water) is already known about us. We do not awake until the sun rises.  He said: When you awake, offer your prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2457"> Book 13, Number 2457: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> The Prophet (peace_be_upon_him) used to observe i'tikaf during the last ten days of Ramadan. One year he did not observe i'tikaf. When the next year came, he observed i'tikaf for twenty nights (i.e. days). </p>
      </blockquote>
      <hr>
      <em> <a name="013.2467"> Book 13, Number 2467: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The sunnah for one who is observing i'tikaf (in a mosque) is not to visit a patient, or to attend a funeral, or touch or embrace one's wife, or go out for anything but necessary purposes. There is no i'tikaf without fasting, and there is no i'tikaf except in a congregational mosque. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2468"> Book 13, Number 2468: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Umar (may Allah be pleased with him) took a vow in the pre-Islamic days to spend a night or a day in devotion near the Ka'bah (in the sacred mosque). He asked the Prophet (peace_be_upon_him) about it. He said: Observe i'tikaf (i.e. spend a night or a day near the Ka'bah) and fast. </p>
      </blockquote>
      <hr>
      <em> <a name="013.2469"> Book 13, Number 2469: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The tradition mentioned above (No. 2468) has also been transmitted by Abdullah ibn Budayl through a different chain of narrators in a similar way. </p>
        <p> This version adds: While he (Umar) was observing i'tikaf (in the sacred mosque), the people uttered (loudly):  "Allah is most great." He said: What is this, Abdullah?  He said: These are the captives of the Hawazin whom the Apostle of Allah (peace_be_upon_him) has set free. He said: This slave-girl too? He sent her along with them. </p>
      </blockquote>
