      <em> <a name="019.3060"> Book 19, Number 3060: </a> </em>
      <blockquote>
        <p> Narrated Abyad ibn Hammal: </p>
        <p> He asked the Apostle of Allah (peace_be_upon_him) for giving him some land which had arak trees growing in it. The Apostle of Allah (peace_be_upon_him) said: There is no (permission for) protecting a land which has arak trees growing in it. He said: These arak trees are within the boundaries of my field. The Prophet (peace_be_upon_him) said: There is no (permission for) protecting a land which has arak trees growing in it. </p>
        <p> The narrator Faraj said: By the phrase 'within the boundaries of my field' he meant the land which had crop growing in it and was surrounded on four sides. </p>
      </blockquote>
