      <blockquote>
        <p> Narrated Amr ibn Awf al-Muzani: </p>
        <p> The Prophet (peace_be_upon_him) assigned as a fief to Bilal ibn Harith al-Muzani the mines of al-Qabaliyyah, both those which lay on the upper side those and which lay on the lower side. The narrator, Ibn an-Nadr, added: "also Jars and Dhat an-Nusub." The agreed version reads: "and (the land) which is suitable for cultivation at Quds". He did not assign to Bilal ibn al-Harith the right of any Muslim. The Prophet (peace_be_upon_him) wrote a document to him: </p>
        <p> "This is what the Apostle of Allah (peace_be_upon_him) assigned to Bilal ibn al-Harith al-Muzani. He gave him the mines of al-Qabaliyyah both those which lay on the upper and lower side, and that which is fit for cultivation at Quds. He did not give him the right of any Muslim." </p>
        <p> The narrator AbuUways said: A similar tradition has been transmitted to me by Thawr ibn Zayd from Ikrimah on the authority of Ibn Abbas from the Prophet (peace_be_upon_him). Ibn an-Nadr added: Ubayy ibn Ka'b wrote it. </p>
      </blockquote>
