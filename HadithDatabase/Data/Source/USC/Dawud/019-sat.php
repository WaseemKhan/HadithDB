      <blockquote>
        <p> Narrated Qaylah bint Makhramah: </p>
        <p> Abdullah ibn Hasan al-Anbari said: My grandmothers, Safiyyah and Duhaybah, narrated to me, that hey were the daughters of Ulaybah and were nourished by Qaylah, daughter of Makhramah. She was the grandmother of their father. </p>
        <p> She reported to them, saying: We came upon the Apostle of Allah (peace_be_upon_him). My companion, Hurayth ibn Hassan, came to him as a delegate from Bakr ibn Wa'il. He took the oath of allegiance of Islam for himself and for his people. </p>
        <p> He then said: Apostle of Allah (peace_be_upon_him), write a document for us, giving us the land lying between us and Banu Tamim at ad-Dahna' to the effect that not one of them will cross it in our direction except a traveller or a passer-by. </p>
        <p> He said: Write down ad-Dahna' for them, boy. When I saw that he passed orders to give it to him, I became anxious, for it was my native land and my home. </p>
        <p> I said: Apostle of Allah, he did not ask you for a true border when he asked you. This land of Dahna' is a place where the camels have their home, and it is a pasture for the sheep. The women of Banu Tamim and their children are beyond it. </p>
        <p> He said: Stop, boy! A poor woman spoke the truth: a Muslim is a brother of a Muslim. Each one of them may benefit from water and trees, and they should cooperate with each other against Satan. </p>
      </blockquote>
