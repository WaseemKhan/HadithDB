      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 19: </a> </em>
      <h1> <em> Tribute, Spoils, and Rulership (Kitab Al-Kharaj, Wal-Fai' Wal-Imarah) </em> </h1>
      <hr>
      <em> <a name="019.2926"> Book 19, Number 2926: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: When Allah has a good purpose for a ruler, He appoints for him a sincere minister who reminds him if he forgets and helps him if he remembers; but when Allah has a different purpose from that for him. He appoints for him an evil minister who does not remind him if he forgets and does not help him if he remembers. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2927"> Book 19, Number 2927: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam ibn Ma'dikarib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) struck him on his shoulders and then said: You will attain success, Qudaym, if you die without having been a ruler, a secretary, or a chief. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2928"> Book 19, Number 2928: </a> </em>
      <blockquote>
        <p> Narrated Ghalib al-Qattan: </p>
        <p> Ghalib quoted a man who stated on the authority of his father that his grandfather reported: They lived at one of the springs. When Islam reached them, the master of the spring offered his people one hundred camels if they embraced Islam. So they embraced Islam, and he distributed the camels among them. But it occurred to him that he should take the camels back from them. </p>
        <p> He sent his son to the Prophet (peace_be_upon_him) and said to him: Go to the Prophet (peace_be_upon_him) and tell him: My father extends his greetings to you. He asked his people to give them one hundred camels if they embraced Islam, and they embraced Islam. He divided the camels among them. But it occurred to him then that he should withdraw his camels from them. Is he more entitled to them or we? If he says: Yes or no, then tell him: My father is an old man, and he is the chief of the people living at the water. He has requested you to make me chief after him. </p>
        <p> He came to him and said: My father has extended his greetings to you.  He replied: On you and you father be peace.  He said: My father asked his people to give them one hundred camels if they embraced Islam. So they embraced Islam, and their belief in Islam is good. Then it occurred to him that he should take his camels back from them. Is he more entitled to them or are they? </p>
        <p> He said: If he likes to give them the camels, he may give them; and if he likes to take them back, he is more entitled to them than his people. If they embraced Islam, then for them is their Islam. If they do not embrace Islam, they will be fought against in the cause of Islam. </p>
        <p> He said: My father is an old man; he is the chief of the people living at the spring. He has asked you to appoint me chief after him. </p>
        <p> He replied: The office of a chief is necessary, for people must have chiefs, but the chiefs will go to Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2930"> Book 19, Number 2930: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: The official who collects sadaqah (zakat) in a just manner is like him who fights in Allah's path till he returns home. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2931"> Book 19, Number 2931: </a> </em>
      <blockquote>
        <p> Narrated Uqbah ibn Amir: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) as saying: One who wrongfully takes an extra tax (sahib maks) will not enter Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2939"> Book 19, Number 2939: </a> </em>
      <blockquote>
        <p> Narrated Al-Mustawrid ibn Shaddad: </p>
        <p> Al-Mustawrid heard the Prophet (peace_be_upon_him) say: He who acts as an employee for us must get a wife; if he has not a servant, he must get one, and if he has not a dwelling, he must get one. He said that AbuBakr reported: I was told that the Prophet (peace_be_upon_him) said: He who takes anything else he is unfaithful or thief. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2941"> Book 19, Number 2941: </a> </em>
      <blockquote>
        <p> Narrated AbuMas'ud al-Ansari: </p>
        <p> The Prophet (peace_be_upon_him) appointed me to collect sadaqah and then said: Go, AbuMas'ud, I should not find you on the Day of Judgment carrying a camel of sadaqah on your back, which rumbles, the one you have taken by unfaithful dealing in sadaqah. He said: If it is so, I will not go. He said: Then I do not force you. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2942"> Book 19, Number 2942: </a> </em>
      <blockquote>
        <p> Narrated AbuMaryam al-Azdi: </p>
        <p> When I entered upon Mu'awiyah, he said: How good your visit is to us, O father of so-and-so. (This is an idiom used by the Arabs on such occasions). I said: I tell you a tradition which I heard (from the Prophet). I heard the Apostle of Allah (peace_be_upon_him) say: If Allah puts anyone in the position of authority over the affairs of the Muslims, and he secludes himself (from them), not fulfilling their needs, wants, and poverty, Allah will keep Himself away from him, not fulfilling his need, want and poverty. He said: He (Mu'awiyah) appointed a man to fulfil the needs of the people. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2943"> Book 19, Number 2943: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: It is not on my own that I give you or withhold from you: I am just a treasure, putting it where I have been commanded. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2944"> Book 19, Number 2944: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Malik ibn Aws ibn al-Hadthan said: One day Umar ibn al-Khattab mentioned the spoils of war and said: I am not more entitled to this spoil of war than you; and none of us is more entitled to it than another, except that we occupy our positions fixed by the Book of Allah, Who is Great and Glorious, and the division made by the Apostle of Allah (peace_be_upon_him), people being arranged according to their precedence in accepting Islam, the hardship they have endured their having children and their need. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2945"> Book 19, Number 2945: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Zayd ibn Aslam said: Abdullah ibn Umar entered upon Mu'awiyah. He asked: (Tell me) your need, AbuAbdurRahman. He replied: Give (the spoils) to those who were set free, for I saw the first thing the Apostle of Allah (peace_be_upon_him) did when anything came to him was to give something to those who had been set free. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2946"> Book 19, Number 2946: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Prophet (peace_be_upon_him) was brought a pouch  containing bead and divided it among free women and slave women. Aisha said: My father used to divide things between free men and slave. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2948"> Book 19, Number 2948: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: I am nearer to the believers than themselves, so if anyone leaves property, it goes to his heirs, and if anyone leaves debt and dependants, let the matter come to me and I shall be responsible. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2952"> Book 19, Number 2952: </a> </em>
      <blockquote>
        <p> Narrated A man: </p>
        <p> Sulaym ibn Mutayr reported on the authority of his father that Mutayr went away to perform hajj. </p>
        <p> When he reached as-Suwaida', a man suddenly came searching for medicine and ammonium anthorhizum extract, and he said: A man who heard the Apostle of Allah (peace_be_upon_him) addressing the people commanding and prohibiting them, told me that he said: O people, accept presents so long as they remain presents; but when the Quraysh quarrel about the rule, and the presents are given for the religion of one of you, then leave them alone. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2953"> Book 19, Number 2953: </a> </em>
      <blockquote>
        <p> Narrated Dhul-Zawa'id: </p>
        <p> Mutayr said: I heard a man say: I heard the Apostle of Allah (peace_be_upon_him) in the Farewell Pilgrimage. He was commanding and prohibiting them (the people). He said: O Allah, did I give full information? They said: Yes. He said: When the Quraysh quarrel about the rule among themselves, and the presents become bribery, them leave them. The people were asked: Who was he (who narrated this tradition)? They said: This was Dhul-Zawa'id, a Companion of the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="019.2955"> Book 19, Number 2955: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> A son of Adi ibn Adi al-Kindi said that Umar ibn AbdulAziz wrote (to his governors): If anyone asks about the places where spoils (fay') should be spent, that should be done in accordance with the decision made by Umar ibn al-Khattab (Allah be pleased with him). The believers considered him to be just, according to the saying of the Prophet (peace_be_upon_him): Allah has placed truth upon Umar's tongue and heart. He fixed stipends for Muslims, and provided protection for the people of other religions by levying jizyah (poll-tax) on them, deducting no fifth from it, nor taking  it as booty. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2956"> Book 19, Number 2956: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Allah, the Exalted, has placed truth on Umar's tongue and he speaks it. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2961"> Book 19, Number 2961: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Malik ibn Aws al-Hadthan said: One of the arguments put forward by Umar was that he said that the Apostle of Allah (peace_be_upon_him) received three things exclusively to himself: Banu an-Nadir, Khaybar and Fadak. The Banu an-Nadir property was kept wholly for his emergent needs, Fadak for travellers, and Khaybar was divided by the Apostle of Allah (peace_be_upon_him) into three sections: two for Muslims, and one as a contribution for his family. If anything remained after making the contribution of his family, he divided it among the poor Emigrants. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2963"> Book 19, Number 2963: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Fatimah was demanding (the property of) sadaqah of the Apostle of Allah (peace_be_upon_him) at Medina and Fadak, and what remained from the fifth of Khaybar. Aisha quoted AbuBakr as saying: The Apostle of Allah (peace_be_upon_him) said: We are not inherited; whatever we leave is sadaqah. The family of Muhammad will eat from this property, that is, from the property of Allah. They will not take more then their sustenance. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2966"> Book 19, Number 2966: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn AbdulAziz: </p>
        <p> Al-Mughirah (ibn Shu'bah) said: Umar ibn AbdulAziz gathered the family of Marwan when he was made caliph, and he said: Fadak belonged to the Apostle of Allah (peace_be_upon_him), and he made contributions from it, showing repeated kindness to the poor of the Banu Hashim from it, and supplying from it the cost of marriage for those who were unmarried. Fatimah asked him to give it to her, but he refused. That is how matters stood during the lifetime of the Apostle of Allah (peace_be_upon_him) till he passed on (i.e. died). </p>
        <p> When AbuBakr was made ruler he administered it as the Prophet (peace_be_upon_him) had done in his lifetime till he passed on. Then when Umar ibn al-Khattab was made ruler he administered it as they had done till he passed on. Then it was given to Marwan as a fief, and it afterwards came to Umar ibn AbdulAziz. </p>
        <p> Umar ibn AbdulAziz said: I consider I have no right to something which the Apostle of Allah (peace_be_upon_him) refused to Fatimah, and I call you to witness that I have restored it to its former condition; meaning in the time of the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="019.2967"> Book 19, Number 2967: </a> </em>
      <blockquote>
        <p> Narrated AbuBakr: </p>
        <p> AbutTufayl said: Fatimah came to AbuBakr asking him for the inheritance of the Prophet (peace_be_upon_him). AbuBakr said: I heard the Apostle of Allah (peace_be_upon_him) say: If Allah, Most High, gives a Prophet some means of sustenance, that goes to his successor. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2969"> Book 19, Number 2969: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> AbulBakhtari said: I heard from a man a tradition which I liked. I said to him: Write it down for me. So he brought it clearly written to me. </p>
        <p> (It says): Al-Abbas and Ali entered upon Umar when Talhah, az-Zubayr, AbdurRahman and Sa'd were with him. They (Abbas and Ali) were disputing. </p>
        <p> Umar said to Talhah, az-Zubayr, AbdurRahman and Sa'd: Do you not know that the Apostle of Allah (peace_be_upon_him) said: All the property of the Prophet (peace_be_upon_him) is sadaqah (alms), except what he provided for his family for their sustenance and their clothing. We are not to be inherited. </p>
        <p> They said: Yes, indeed.  He said: The Apostle of Allah (peace_be_upon_him) used to spend from his property on his family, and give the residue as sadaqah (alms). The Apostle of Allah (peace_be_upon_him) then died, and AbuBakr ruled for two years. He would deal with it in the same manner as the Apostle of Allah (peace_be_upon_him) did. He then mentioned a little from the tradition of Malik ibn Aws. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2976"> Book 19, Number 2976: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Yazid ibn Hurmuz said that when Najdah al-Haruri performed hajj during the rule of Ibn az-Zubayr, he sent someone to Ibn Abbas to ask him about the portion of the relatives (in the fifth). He asked: For whom do you think?  Ibn Abbas replied: For the relatives of the Apostle of Allah (peace_be_upon_him). The Apostle of Allah (peace_be_upon_him) divided it among them. Umar presented it to us but we found it less than our right. We, therefore returned it to him and refused to accept it. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2978"> Book 19, Number 2978: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> I, al-Abbas, Fatimah and Zayd ibn Harithah gathered with the Prophet (peace_be_upon_him) and I said: Apostle of Allah, if you think to assign us our right (portion) in this fifth ( of the booty) as mentioned in the Book of Allah, and this I may divide during your lifetime so that no one may dispute me after you, then do it. He said: He did that. He said: I divided it during the lifetime of the Apostle of Allah (peace_be_upon_him). AbuBakr then assigned it to me. During the last days of the caliphate of Umar a good deal of property came to him and took out our portion. I said to him: We are well to do this year; but the Muslims are needy, so return it to them. He, therefore, returned it to them. No one called me after Umar. I met al-Abbas when I came out from Umar. He said: Ali, today you have deprived us of a thing that will never be returned to us. He was indeed a man of wisdom. </p>
      </blockquote>
      <hr>
      <em> <a name="019.2979"> Book 19, Number 2979: </a> </em>
      <blockquote>
        <p> Narrated AbdulMuttalib ibn Rabi'ah ibn al-Harith: </p>
        <p> AbdulMuttalib ibn Rabi'ah ibn al-Harith said that his father, Rabi'ah ibn al-Harith, and Abbas ibn al-Muttalib said to AbdulMuttalib ibn Rabi'ah and al-Fadl ibn Abbas: Go to the Apostle of Allah (peace_be_upon_him) and tell him: Apostle of Allah, we are now of age as you see, and we wish to marry. Apostle of Allah, you are the kindest of the people and the most skilled in matchmaking. Our fathers have nothing with which to pay our dower. So appoint us collector of sadaqah (zakat), Apostle of Allah, and we shall give you what the other collectors give you, and we shall have the benefit accruing from it. Ali came to us while we were in this condition. </p>
        <p> He said: The Apostle of Allah (peace_be_upon_him) said: No, I swear by Allah, he will not appoint any of you collector of sadaqah (zakat). </p>
        <p> Rabi'ah said to him: This is your condition; you have gained your relationship with the Apostle of Allah (peace_be_upon_him) by marriage, but we did not grudge you that. Ali then put his cloak on the earth and lay on it. </p>
        <p> He then said: I am the father of Hasan, the chief. I swear by Allah, I shall not leave this place until your sons come with a reply (to the question) for which you have sent them to the Prophet (peace_be_upon_him). </p>
        <p> AbdulMuttalib said: So I and al-Fadl went towards the door of the apartment of the Prophet (peace_be_upon_him). We found that the noon prayer in congregation had already started. So we prayed along with the people. I and al-Fadl then hastened towards the door of the apartment of the Prophet (peace_be_upon_him). He was (staying) with Zaynab, daughter of Jahsh, that day. We stood until the Apostle of Allah (peace_be_upon_him) came. He caught my ear and the ear of al-Fadl. </p>
