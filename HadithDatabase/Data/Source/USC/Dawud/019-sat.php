        <p> So he assigned it to him as a fief. When he returned, a man in the meeting asked: Do you know what you have assigned him as a fief? You have assigned him the perennial spring water. So he took it back from him. He asked him about protecting land which had arak trees growing in it. He replied: He could have such as was beyond the region where the hoofs (of camels) went. </p>
      </blockquote>
