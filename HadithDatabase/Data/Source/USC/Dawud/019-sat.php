      <em> <a name="019.3061"> Book 19, Number 3061: </a> </em>
      <blockquote>
        <p> Narrated Sakhr ibn al-Ayla al-Ahmasi: </p>
        <p> The Apostle of Allah (peace_be_upon_him) raided Thaqif. When Sakhr heard this, he proceeded on his horse along with some horsemen to support the Prophet (peace_be_upon_him). He found the Prophet of Allah (peace_be_upon_him) had returned and he did not conquer (Ta'if). </p>
        <p> On that day Sakhr made a covenant with Allah and had His protection that he would not depart from that fortress until they (the inhabitants) surrendered to the command of the Apostle of Allah (peace_be_upon_him). He did not leave them until they had surrendered to the command of the Apostle of Allah (peace_be_upon_him). </p>
        <p> Sakhr then wrote to him: To proceed: Thaqif have surrendered to your command, Apostle of Allah, and I am on my way to them. They have horses with them. </p>
        <p> The Apostle of Allah (peace_be_upon_him) then ordered prayers to be offered in congregation. He then prayed for Ahmas ten times: O Allah, send blessings the horses and the men of Ahmas. </p>
        <p> The people came and Mughirah ibn Shu'bah said to him: Prophet of Allah, Sakhr took my paternal aunt while she embraced Islam like other Muslims. </p>
        <p> He called him and said: Sakhr, when people embrace Islam, they have security of their blood and property. Give back to Mughirah his paternal aunt. </p>
        <p> So he returned his aunt to him and asked the Prophet of Allah (peace_be_upon_him): What about Banu Sulaym who have run away for (fear of) Islam and left that water? He said: Prophet of Allah, allow me and my people to settle there. </p>
        <p> He said: Yes. So he allowed him to settle there. Banu Sulaym then embraced Islam, and they came to Sakhr. They asked him to return their water to them. But he refused. </p>
        <p> So they came to the Prophet (peace_be_upon_him) and said: Prophet of Allah, we embraced Islam and came to Sakhr so that he might return our water to us. But he has refused. </p>
        <p> He (the Prophet) then came to him and said: When people embrace Islam, they secure their properties and blood. Return to the people their water. </p>
        <p> He said: Yes, Prophet of Allah. I saw that the face of the Apostle of Allah (peace_be_upon_him) was reddening at that moment, being ashamed of taking back from him the slave-girl and the water. </p>
      </blockquote>
