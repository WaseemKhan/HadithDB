        <p> Narrated A number of Companions of the Prophet: </p>
        <p> Safwan reported from a number of Companions of the Apostle of Allah (peace_be_upon_him) on the authority of their fathers who were relatives of each other. The Apostle of Allah (peace_be_upon_him) said: Beware, if anyone wrongs a contracting man, or diminishes his right, or forces him to work beyond his capacity, or takes from him anything without his consent, I shall plead for him on the Day of Judgment. </p>
      </blockquote>
      <hr>
      <em> <a name="019.3047"> Book 19, Number 3047: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Jizyah is not to be levied on a Muslim. </p>
      </blockquote>
      <hr>
      <em> <a name="019.3049"> Book 19, Number 3049: </a> </em>
      <blockquote>
        <p> Narrated Abdullah al-Hawzani: </p>
        <p> I met Bilal, the Mu'adhdhin of the Apostle of Allah (peace_be_upon_him) at Aleppo, and said: Bilal, tell me, what was the financial position of the Apostle of Allah (peace_be_upon_him)? </p>
        <p> He said: He had nothing. It was I who managed it on his behalf since the day Allah made him Prophet of Allah (peace_be_upon_him) until he died. When a Muslim man came to him and he found him naked, he ordered me (to clothe him). I would go, borrow (some money), and purchase a cloak for him. I would then clothe him and feed him. </p>
        <p> A man from the polytheists met me and said: I am well off, Bilal. Do not borrow money from anyone except me. So I did accordingly. One day when I performed ablution and stood up to make call to prayer, the same polytheist came along with a body of merchants. </p>
        <p> When he saw me, he said: O Abyssinian.  I said: I am at your service. He met me with unpleasant looks and said harsh words to me.  He asked me: Do you know how many days remain in the completion of this month?  I replied: The time is near.  He said: Only four days remain in the completion of this month. I shall then take that which is due from you (i.e. loan), and then shall return you to tend the sheep as you did before. I began to think in my mind what people think in their minds (on such occasions). When I offered the night prayer, the Apostle of Allah (peace_be_upon_him) returned to his family. I sought permission from him and he gave me permission. </p>
        <p> I said: Apostle of Allah, may my parents be sacrificed for you, the polytheist from whom I used to borrow money said to me such-and-such. Neither you nor I have anything to pay him for me, and he will disgrace me. So give me permission to run away to some of those tribes who have recently embraced Islam until Allah gives His Apostle (peace_be_upon_him) something with which he can pay (the debt) for me. So I came out and reached my house. I placed my sword, waterskin (or sheath), shoes and shield near my head. When dawn broke, I intended to be on my way. </p>
        <p> All of a sudden I saw a man running towards me and calling: Bilal, return to the Apostle of Allah (peace_be_upon_him). So I went till I reached him. I found four mounts kneeling on the ground with loads on them. I sought permission. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to me: Be glad, Allah has made arrangements for the payment (of your debt). He then asked: Have you not seen the four mounts kneeling on the ground? </p>
        <p> I replied: Yes.  He said: You may have these mounts and what they have on them. There are clothes and food on them, presented to me by the ruler of Fadak. Take them away and pay off your debt. I did so. </p>
        <p> He then mentioned the rest of the tradition. I then went to the mosque and found that the Apostle of Allah (peace_be_upon_him) was sitting there. I greeted him. </p>
        <p> He asked: What benefit did you have from your property?  I replied: Allah Most High paid everything which was due from the Apostle of Allah (peace_be_upon_him). Nothing remains now. </p>
        <p> He asked: Did anything remain (from that property)?  I said: Yes.  He said: Look, if you can give me some comfort from it, for I shall not visit any member of my family until you give me some comfort from it. When the Apostle of Allah (peace_be_upon_him) offered the night prayer, he called me and said: What is the position of that which you had with you (i.e. property)? </p>
        <p> I said: I still have it, no one came to me. The Apostle of Allah (peace_be_upon_him) passed the night in the mosque. </p>
        <p> He then narrated the rest of the tradition. Next day when he offered the night prayer, he called me and asked: What is the position of that which you had (i.e. the rest of the property)? </p>
        <p> I replied: Allah has given you comfort from it, Apostle of Allah.  He said: Allah is Most Great, and praised Allah, fearing lest he should die while it was with him. I then followed him until he came to his wives and greeted each one of them and finally he came to his place where he had to pass the night. This is all for which you asked me. </p>
      </blockquote>
