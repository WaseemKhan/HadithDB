      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 10: </a> </em>
      <h1> <em> The Rites of Hajj (Kitab Al-Manasik Wa'l-Hajj) </em> </h1>
      <hr>
      <em> <a name="010.1717"> Book 10, Number 1717: </a> </em>
      <blockquote>
        <p> Narrated Aqra' ibn Habib: </p>
        <p> Ibn Abbas said: Aqra' ibn Habis asked the Prophet (peace_be_upon_him) saying: Apostle of Allah hajj is to be performed annually or only once? He replied: Only once, and if anyone performs it more often, he performs a supererogatory act. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1718"> Book 10, Number 1718: </a> </em>
      <blockquote>
        <p> Narrated AbuWaqid al-Laythi: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) saying to his wives during the Farewell Pilgrimage: This (is the pilgrimage for you); afterwards stick to the surface of the mats (i.e. should stay at home). </p>
      </blockquote>
      <hr>
      <em> <a name="010.1725"> Book 10, Number 1725: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Islam does not allow for failure to perform the hajj. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1727"> Book 10, Number 1727: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Ibn Abbas recited this verse: 'It is no sin for you that you seek the bounty of your Lord', and said: The people would not trade in Mina (during the hajj), so they were commanded to trade when they proceeded from Arafat. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1728"> Book 10, Number 1728: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: He who intends to perform hajj should hasten to do so. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1729"> Book 10, Number 1729: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> AbuUmamah at-Taymi said: I was a man who used to give (riding-beasts) on hire for this purpose (for travelling during the pilgrimage) and the people would tell (me): Your hajj is not valid. So I met Ibn Umar and told him: AbuAbdurRahman, I am a man who gives (riding-beast) on hire for this purpose (i.e. for hajj), and the people tell me: Your hajj is not valid. Ibn Umar replied: Do you not put on ihram (the pilgrim dress), call the talbiyah (labbayk), circumambulate the Ka'bah, return from Arafat and lapidate jamrahs? I said: Why not? Then he said: Your hajj is valid. a man came to the Prophet (peace_be_upon_him) and asked him the same question you have asked me. The Apostle of Allah (peace_be_upon_him) kept silence and did not answer him till this verse came down: "It is no sin for you that you seek the bounty of your Lord." The Apostle of Allah (peace_be_upon_him) sent for him and recited this verse to him and said: Your hajj is valid. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1730"> Book 10, Number 1730: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The people used to trade, in the beginning, at Mina, Arafat, the market place of Dhul-Majaz, and during the season of hajj. But (later on) they became afraid of trading while they were putting on ihram. So Allah, glory be to Him, sent down this verse: "It is no sin for you that you seek the bounty of your Lord during the seasons of hajj." Ubayd ibn Umayr told me that he (Ibn Abbas) used to recite this verse in his codex. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1735"> Book 10, Number 1735: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) appointed Dhat Irq as the place for putting on ihram for the people of Iraq. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1736"> Book 10, Number 1736: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) appointed al-Aqiq as the place for putting on ihram for the people of East. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1737"> Book 10, Number 1737: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> She heard the Apostle of Allah (peace_be_upon_him) say: If anyone puts on ihram for hajj or umrah from the Aqsa mosque  to the sacred mosque , his former and latter sins will be forgiven, or he will be guaranteed Paradise. The narrator Abdullah doubted which of these words he said. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1738"> Book 10, Number 1738: </a> </em>
      <blockquote>
        <p> Narrated Al-Harith ibn Amr as-Sahmi: </p>
        <p> I came to the Apostle of Allah (peace_be_upon_him) when he was at Mina, or at Arafat. He was surrounded by the people. When the bedouins came and saw his face, they would say: This is a blessed face. He said: He (the Prophet) appointed Dhat Irq as the place of putting on ihram for the people of Iraq. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1740"> Book 10, Number 1740: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: A menstruating woman and the one who delivered a child should take a bath, put on ihram and perform all the rites of hajj except circumambulation of the House (Ka'bah) when they came to the place of wearing ihram. AbuMa'mar said in his version: "till she is purified". The narrator Ibn Isa did not mention the names of Ikrimah and Mujahid, but he said: from Ata on the authority of Ibn Abbas. Ibn Isa also did not mention the word "all (rites of hajj)." He said in his version: All the rites of hajj except circumambulation of the House (the Ka'bah). </p>
      </blockquote>
      <hr>
      <em> <a name="010.1745"> Book 10, Number 1745: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> In the year of al-Hudaybiyyah, the Apostle of Allah (peace_be_upon_him) included among his sacrificial animals a camel with a silver nose-ring (Ibn Minhal's version has gold) which had belonged to AbuJahl (the version of an-Nufayli added) "thereby enraging the polytheists". </p>
      </blockquote>
      <hr>
      <em> <a name="010.1746"> Book 10, Number 1746: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sacrificed a cow for his wives at the Farewell Pilgrimage. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1747"> Book 10, Number 1747: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sacrificed a cow for his wives who had performed umrah. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1752"> Book 10, Number 1752: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Umar ibn al-Khattab named a bukhti camel for sacrifice (at hajj). He was offered three hundred dinars for it (as its price). He came to the Prophet (peace_be_upon_him) and said: Apostle of Allah, I named a bukhti camel for sacrifice and I was offered for it three hundred dinars. May I sell it and purchase another one for its price? No, sacrifice it. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1758"> Book 10, Number 1758: </a> </em>
      <blockquote>
        <p> Narrated Najiyah al-Aslami: </p>
        <p> The Apostle of Allah (peace_be_upon_him) sent sacrificial camels with him (as offering to the Ka'bah). He then said: If any one of them becomes fatigued, slaughter it, dip its shoes in its blood, and leave it for the people (to eat). </p>
      </blockquote>
      <hr>
      <em> <a name="010.1760"> Book 10, Number 1760: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) sacrificed the camels, he sacrificed thirty of them with his own hand, and then commanded me (to sacrifice them), so I sacrificed the rest of them. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1761"> Book 10, Number 1761: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Qurt: </p>
        <p> The Prophet (peace_be_upon_him) said: The greatest day in Allah's sight is the day of sacrifice and next the day of resting which Isa said on the authority of Thawr is the second day. Five or six sacrificial camels were brought to the Apostle of Allah (peace_be_upon_him) and they began to draw near to see which he would sacrifice first. When they fell down dead, he said something in a low voice, which I could not catch. So I asked: What did he say? He was told that he had said: Anyone who wants can cut off a piece. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1762"> Book 10, Number 1762: </a> </em>
      <blockquote>
        <p> Narrated Arfah ibn al-Harith al-Kandi: </p>
        <p> I was present with the Apostle of Allah (peace_be_upon_him) at the Farewell Pilgrimage. When the sacrificial camels were brought to him, he said: Call AbulHasan (Ali) to me. Ali was then called for and he (the Prophet) said to him: Catch hold of the lower end of the lance, and the Apostle of Allah (peace_be_upon_him) himself caught hold of the upper end. He then pierced the camels with it. When he finished slaughtering, he rode on his mule and mounted Ali behind him. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1766"> Book 10, Number 1766: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Sa'id ibn Jubayr said: I said to Abdullah ibn Abbas: AbulAbbas, I am surprised to see the difference of opinion amongst the companions of the Apostle (peace_be_upon_him) about the wearing of ihram by the Apostle of Allah (peace_be_upon_him) when he made it obligatory. </p>
        <p> He replied: I am aware of it more than the people. The Apostle of Allah (peace_be_upon_him) performed only one hajj. Hence the people differed among themselves. The Apostle of Allah (peace_be_upon_him) came out (from Medina) with the intention of performing hajj. When he offered two rak'ahs of prayer in the mosque at Dhul-Hulayfah, he made it obligatory by wearing it. </p>
        <p> At the same meeting, he raised his voice in the talbiyah for hajj, when he finished his two rak'ahs. Some people heard it and I retained it from him. He then rode (on the she-camel), and when it (the she-camel) stood up, with him on its back, he raised his voice in the talbiyah and some people heard it at that moment. This is because the people were coming in groups, so they heard him raising his voice calling the talbiyah when his she-camel stood up with him on its back, and they thought that the Apostle of Allah (peace_be_upon_him) had raised his voice in the talbiyah when his she-camel stood up with him on its back. </p>
        <p> The Apostle of Allah (peace_be_upon_him) proceeded further; when he ascended the height of al-Bayda' he raised his voice in the talbiyah. Some people heard it at that moment. They thought that he had raised his voice in the talbiyah when he ascended the height of al-Bayda'. I swear by Allah, he raised his voice in the talbiyah at the place where he prayed, and he raised his voice in the talbiyah when his she-camel stood up with him on its back, and he raised his voice in the talbiyah when he ascended the height of al-Bayda'. </p>
        <p> Sa'id (ibn Jubayr) said; He who follows the view of Ibn Abbas raises his voice in talbiyah (and ihram) at the place of is prayer after he finishes two rak'ahs of his prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1768"> Book 10, Number 1768: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Ubayd ibn Jurayj said to Abdullah ibn Umar: AbuAbdurRahman, I saw you doing things which I did not see being done by your companions. </p>
        <p> He asked: What are they, Ibn Jurayj?  He replied: I saw you touching only the two Yamani corners; and I saw you wearing shoes having no hair; I saw you dyeing in yellow colour; and I saw you wearing ihram on the eighth of Dhul-Hijjah, whereas the people had worn ihram when they sighted the moon. </p>
        <p> Abdullah ibn Umar replied: As regards the corners, I have not seen the Apostle of Allah (peace_be_upon_him) touching anything (in the Ka'bah) but the two Yamani corners. As for the tanned leather shoes, I have seen the Apostle of Allah (peace_be_upon_him) wearing tanned leather shoes, and he would wear them after ablution. Therefore I like to wear them. As regards wearing yellow, I have seen the Apostle of Allah (peace_be_upon_him) wearing yellow, so I like to wear with it. As regards shouting the talbiyah, I have seen the Apostle of Allah (peace_be_upon_him) raising his voice in talbiyah when his she-camel stood up with him on its back. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1770"> Book 10, Number 1770: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) offered the noon prayer, and then rode on his mount. When he came to the hill of al-Bayda', he raised his voice in talbiyah. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1771"> Book 10, Number 1771: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> When the Prophet of Allah (peace be upon him0 undertook his journey by the way of al-Far', he shouted talbiyah when his mount stood up with him on its back. But when he travelled by the way of Uhud, he raised his voice in Talbiyah when he ascended the hill of al-Bayda'. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1788"> Book 10, Number 1788: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) raised his voice in talbiyah for hajj. When he came (to Mecca) he went round the House (the Ka'bah) and ran between as-Safa and al-Marwah. The narrator Ibn Shawkar said: He did not clip his hair, nor did he take off his ihram due to sacrificial animals. But he commanded those who did not bring sacrificial animals with them to go round the Ka'bah, to run between as-Safa and al-Marwah, to clip their hair, and then put off their ihram. The narrator Ibn Mani' added: Or shave their heads, then take off their ihram." </p>
      </blockquote>
      <hr>
      <em> <a name="010.1789"> Book 10, Number 1789: </a> </em>
      <blockquote>
        <p> Narrated Sa'id ibn al-Musayyab: </p>
        <p> A man from the Companions of the Prophet (peace_be_upon_him) came to Umar ibn al-Khattab (may Allah be pleased with him). He bore witness before him that when he (the Prophet) was suffering from a disease of which he died he heard the Apostle of Allah (peace_be_upon_him) prohibiting performing of umrah before hajj. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1790"> Book 10, Number 1790: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn AbuSufyan: </p>
        <p> Mu'awiyah said to the Companions of the Prophet (peace_be_upon_him): Do you know that the Apostle of Allah (peace_be_upon_him) prohibited from doing so and so (and he prohibited from) riding on the skins of leopards? They said: Yes. </p>
        <p> He again said: You know that he prohibited combining hajj and umrah. They replied: This we do not (know). He said: This was prohibited along with other things, but you forgot. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1793"> Book 10, Number 1793: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> I was with Ali (may Allah be pleased with him) when the Apostle of Allah (peace_be_upon_him) appointed him to be the governor of the Yemen. I collected some ounces of gold during my stay with him. </p>
        <p> When Ali returned from the Yemen to the Apostle of Allah (peace_be_upon_him) he said: I found that Fatimah had put on coloured clothes and the smell of the perfume she had used was pervading the house. (He expressed his amazement at the use of coloured clothes and perfume.) </p>
        <p> She said: What is wrong with you? The Apostle of Allah (peace_be_upon_him) has ordered his companions to put off their ihram and they did so. </p>
        <p> Ali said: I said to her: I raised my voice in talbiyah for which the Prophet (peace_be_upon_him) raised his voice (i.e. I wore ihram for qiran). Then I came to the Prophet (peace_be_upon_him). </p>
        <p> He asked (me): How did you do?  I replied: I raised my voice in talbiyah, for which the Prophet (peace_be_upon_him) raised his voice.  He said: I have brought the sacrificial animals with me and combined umrah and hajj. He said to me: Sacrifice sixty-seven or sixty-six camels (for me) and withhold for yourself thirty-three or thirty-four, and withhold a piece (of flesh) for me from every camel. </p>
      </blockquote>
      <hr>
      <em> <a name="010.1794"> Book 10, Number 1794: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> As-Subayy ibn Ma'bad said: I raised my voice in talbiyah for both of them (i.e. umrah and hajj). Thereupon Umar said: You were guided to the practice (sunnah) of your Prophet (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="010.1795"> Book 10, Number 1795: </a> </em>
