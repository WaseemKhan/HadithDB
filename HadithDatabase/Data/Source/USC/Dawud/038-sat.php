      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 38: </a> </em>
      <h1> <em> Prescribed Punishments (Kitab Al-Hudud) </em> </h1>
      <hr>
      <em> <a name="038.4339"> Book 38, Number 4339: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) Said:  The blood of a Muslim man who testifies that there is no god but Allah and that Muhammad is Allah's Apostle  should not lawfully be shed except only for one of three reasons: a man who committed fornication after marriage, in which case he should be stoned; one who goes forth to fight with Allah and His Apostle, in which case he should be killed or crucified or exiled from the land; or one who commits murder for which he is killed. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4341"> Book 38, Number 4341: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> AbuMusa said: Mu'adh came to me when I was in the Yemen. A man who was Jew embraced Islam and then retreated from Islam. When Mu'adh came, he said: I will not come down from my mount until he is killed. He was then killed. One of them said: He was asked to repent before that. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4345"> Book 38, Number 4345: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Abdullah ibn AbuSarh used to write (the revelation) for the Apostle of Allah (peace_be_upon_him). Satan made him slip, and he joined the infidels. The Apostle of Allah (peace_be_upon_him) commanded to kill him on the day of Conquest (of Mecca). Uthman ibn Affan sought protection for him. The Apostle of Allah (peace_be_upon_him) gave him protection. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4346"> Book 38, Number 4346: </a> </em>
      <blockquote>
        <p> Narrated Sa'd ibn AbuWaqqas: </p>
        <p> On the day of the conquest of Mecca, Abdullah ibn Sa'd ibn AbuSarh hid himself with Uthman ibn Affan. </p>
        <p> He brought him and made him stand before the Prophet (peace_be_upon_him), and said: Accept the allegiance of Abdullah, Apostle of Allah! He raised his head and looked at him three times, refusing him each time, but accepted his allegiance after the third time. </p>
        <p> Then turning to his companions, he said: Was not there a wise man among you who would stand up to him when he saw that I had withheld my hand from accepting his allegiance, and kill him? </p>
        <p> They said: We did not know what you had in your heart, Apostle of Allah! Why did you not give us a signal with your eye? </p>
        <p> He said: It is not advisable for a Prophet to play deceptive tricks with the eyes. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4348"> Book 38, Number 4348: </a> </em>
      <blockquote>
        <p> Narrated Abdullah Ibn Abbas: </p>
        <p> A blind man had a slave-mother who used to abuse the Prophet (peace_be_upon_him) and disparage him. He forbade her but she did not stop. He rebuked her but she did not give up her habit. One night she began to slander the Prophet (peace_be_upon_him) and abuse him. So he took a dagger, placed it on her belly, pressed it, and killed her. A child who came between her legs was smeared with the blood that was there. When the morning came, the Prophet (peace_be_upon_him) was informed about it. </p>
        <p> He assembled the people and said: I adjure by Allah the man who has done this action and I adjure him by my right to him that he should stand up. Jumping over the necks of the people and trembling the man stood up. </p>
        <p> He sat before the Prophet (peace_be_upon_him) and said: Apostle of Allah! I am her master; she used to abuse you and disparage you. I forbade her, but she did not stop, and I rebuked her, but she did not abandon her habit. I have two sons like pearls from her, and she was my companion. Last night she began to abuse and disparage you. So I took a dagger, put it on her belly and pressed it till I killed her. </p>
        <p> Thereupon the Prophet (peace_be_upon_him) said: Oh be witness, no retaliation is payable for her blood. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4349"> Book 38, Number 4349: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> A Jewess used to abuse the Prophet (peace_be_upon_him) and disparage him. A man strangled her till she died. The Apostle of Allah (peace_be_upon_him) declared that no recompense was payable for her blood. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4350"> Book 38, Number 4350: </a> </em>
      <blockquote>
        <p> Narrated AbuBakr: </p>
        <p> AbuBarzah said: I was with AbuBakr. He became angry at a man and uttered hot words. I said: Do you permit me, Caliph of the Apostle of Allah (peace_be_upon_him), that I cut off his neck? These words of mine removed his anger; he stood and went in. He then sent for me and said: What did you say just now? I said: (I had said:) Permit me that I cut off his neck. He said: Would you do it if I ordered you? I said: Yes. He said: No, I swear by Allah, this is not allowed for any man after Muhammad (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="038.4356"> Book 38, Number 4356: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Some people raided the camels of the Prophet (peace_be_upon_him), drove them off, and apostatised. They killed the herdsman of the Apostle of Allah (peace_be_upon_him) who was a believer. He (the Prophet) sent (people) in pursuit of them and they were caught. He had their hands and feet cut off, and their eyes put out. The verse regarding fighting against Allah and His Prophet (peace_be_upon_him) was then revealed. These were the people about whom Anas ibn Malik informed al-Hajjaj when he asked him. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4357"> Book 38, Number 4357: </a> </em>
      <blockquote>
        <p> Narrated AbuzZinad: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) cut off (the hands and feet of) those who had stolen his camels and he had their eyes put out by fire (heated nails), Allah reprimanded him on that (action), and Allah, the Exalted, revealed: "The punishment of those who wage war against Allah and His Apostle  and strive with might and main for mischief through the land is execution or crucifixion." </p>
      </blockquote>
      <hr>
      <em> <a name="038.4359"> Book 38, Number 4359: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The verse "The punishment of those who wage war against Allah and His Apostle, and strive with might and main for mischief through the land is execution, or crucifixion, or the cutting off of hands and feet from opposite side or exile from the land...most merciful" was revealed about polytheists. If any of them repents before they are arrested, it does not prevent from inflicting on him the prescribed punishment which he deserves. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4362"> Book 38, Number 4362: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) Said: Forgive the people of good qualities their slips, but not faults to which prescribed penalties apply. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4363"> Book 38, Number 4363: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: Forgive the infliction of prescribed penalties among yourselves, for any prescribed penalty of which I hear must be carried out. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4364"> Book 38, Number 4364: </a> </em>
      <blockquote>
        <p> Narrated Nu'aym: </p>
        <p> Ma'iz came to the Prophet (peace_be_upon_him) and admitted (having committed adultery) four times in his presence so he ordered him to be stoned to death, but said to Huzzal: If you had covered him with your garment, it would have been better for you. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4366"> Book 38, Number 4366: </a> </em>
      <blockquote>
        <p> Narrated Wa'il ibn Hujr: </p>
        <p> When a woman went out in the time of the Prophet (peace_be_upon_him) for prayer, a man attacked her and overpowered (raped) her. </p>
        <p> She shouted and he went off, and when a man came by, she said: That (man) did such and such to me. And when a company of the Emigrants came by, she said: That man did such and such to me. They went and seized the man  whom they thought had had intercourse with her and brought him to her. </p>
        <p> She said: Yes, this is he. Then they brought him to the Apostle of Allah (peace_be_upon_him). </p>
        <p> When he (the Prophet) was about to pass sentence, the man who (actually) had assaulted her stood up and said: Apostle of Allah, I am the man who did it to her. </p>
        <p> He (the Prophet) said to her: Go away, for Allah has forgiven you. But he told the man some good words (AbuDawud said: meaning the man who was seized), and of the man who had had intercourse with her, he said: Stone him to death. </p>
        <p> He also said: He has repented to such an extent that if the people of Medina had repented similarly, it would have been accepted from them. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4367"> Book 38, Number 4367: </a> </em>
      <blockquote>
        <p> Narrated AbuUmayyah al-Makhzumi: </p>
        <p> A thief who had accepted (having committed theft) was brought to the Prophet (peace_be_upon_him), but no good were found with him. The Apostle of Allah (peace_be_upon_him), said to him: I do not think you have stolen. He said: Yes, I have. He repeated it twice or thrice. So he gave orders. His hand was cut off and he was then brought to him. He said: Ask Allah's pardon and turn to Him in repentance. He said: I ask Allah's pardon and turn to Him in repentance. He (the Prophet) then said: O Allah, accept his repentance. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4369"> Book 38, Number 4369: </a> </em>
      <blockquote>
        <p> Narrated An-Nu'man ibn Bashir: </p>
        <p> Azhar ibn Abdullah al-Harari said: Some goods of the people of Kila' were stolen. They accused some men of the weavers (of theft). They came to an-Nu'man ibn Bashir, the companion of the Prophet (peace_be_upon_him). He confined them for some days and then set them free. </p>
        <p> They came to an-Nu'man and said: You have set them free without beating and investigation. An-Nu'man said: What do you want? You want me to beat them. If your goods are found with them, then it is all right; otherwise, I shall take (retaliation) from your back as I have taken from their backs.  They asked: Is this your decision?  He said: This is the decision of Allah and His Apostle (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="038.4373"> Book 38, Number 4373: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) had a man's hand cut off who had stolen from the place reserved for women a shield whose price was three dirhams. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4374"> Book 38, Number 4374: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Apostle of Allah (peace_be_upon_him) had a man's hand cut off for (stealing) a shield whose price was a dinar or ten dirhams. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4375"> Book 38, Number 4375: </a> </em>
      <blockquote>
        <p> Narrated Rafi' ibn Khadij: </p>
        <p> Muhammad ibn Yahya ibn Hibban said: A slave stole a plant of a palm-tree from the orchard of a man and planted it in the orchard of his master. The owner of the plant went out in search of the plant and he found it. He solicited help against the slave from Marwan ibn al-Hakam who was the Governor of Medina at that time. Marwan confined the slave and intended to cut off his hand. The slave's master went to Rafi' ibn Khadij and asked him about it. </p>
        <p> He told him that he had heard the Apostle of Allah (peace_be_upon_him) say: The hand is not to be cut off for taking fruit or the pith of the palm-tree. </p>
        <p> The man then said: Marwan has seized my slave and wants to cut off his hand. I wish you to go with me to him and tell him that which you have heard from the Apostle of Allah (peace_be_upon_him). So Rafi' ibn Khadij went with him and came to Marwan ibn al-Hakam. </p>
        <p> Rafi' said to him: I heard the Apostle of Allah (peace_be_upon_him) say: The hand is not to be cut off for taking fruit or the pith of the palm-tree. So Marwan gave orders to release the slave and then he was released. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4377"> Book 38, Number 4377: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Apostle of Allah  (peace_be_upon_him) was asked about fruit which was bung up and said: If a needy person takes some with his mouth and does not take a supply away in his garment, there is nothing on him, but he who carries any of it is to be fined twice the value and punished, and he who steals any of it after it has been put in the place where dates are dried to have his hand cut off if their value reaches the value of a shield. If he steals a thing less in value than it, he is to be find twice the value and punished. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4378"> Book 38, Number 4378: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: Cutting of hand is not to be inflicted on one who plunders, but he who plunders conspicuously does not belong to us. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4380"> Book 38, Number 4380: </a> </em>
      <blockquote>
        <p> Narrated Safwan: </p>
        <p> AbuDawud said: Za'idah has also transmitted it from Simak from Ju'ayd ibn Hujayr. He said: Safwan slept. Mujahid and Tawus said: While he was sleeping a thief came and stole the cloak from beneath his head. The version of AbuSalamah ibn AbdurRahman has: He snatched it away from beneath his head and he awoke. He cried and he (the thief) was seized. Az-Zuhri narrated from Safwan ibn Abdullah. His version has: He slept in the mosque and used his cloak as pillow. A thief came and took his cloak. The thief was seized and brought to the Prophet (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="038.4382"> Book 38, Number 4382: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> A woman borrowed jewellery through some known persons and she herself was unknown. She then sold them. She was seized and brought to the Prophet (peace_be_upon_him). He gave orders that her hand should be cut off. It is this woman about whom Usamah interceded and of her the Apostle of Allah (peace_be_upon_him) said whatever he said. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4384"> Book 38, Number 4384: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: There are three (persons) whose actions are not recorded: a sleeper till he awakes, an idiot till he is restored to reason, and a boy till he reaches puberty. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4385"> Book 38, Number 4385: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Ibn Abbas said: A lunatic woman who had committed adultery was brought to Umar. He consulted the people and ordered that she should be stoned. </p>
        <p> Ali ibn AbuTalib passed by and said: What is the matter with this (woman)? They said: This is a lunatic woman belonging to a certain family. She has committed adultery. Umar has given orders that she should be stoned. </p>
        <p> He said: Take her back. He then came to him and said: Commander of the Faithful, do you not know that there are three people whose actions are not recorded: a lunatic till he is restored to reason, a sleeper till he awakes, and a boy till he reaches puberty? </p>
        <p> He said: Yes.  He then asked: Why is it that this woman is being stoned? </p>
        <p> He said: There is nothing.  He then said: Let her go.  He (Umar) let her go and began to utter: Allah is most great. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4387"> Book 38, Number 4387: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Ibn Abbas said: A lunatic woman passed by Ali ibn AbuTalib. He then mentioned the rest of the tradition to the same effect as Uthman mentioned. This version has: Do you not remember that the Apostle of Allah (peace_be_upon_him) has said: There are three whose actions are not recorded: a lunatic whose mind is deranged till he is restored to consciousness, a sleeper till he awakes, and a boy till he reaches puberty? </p>
      </blockquote>
      <hr>
      <em> <a name="038.4388"> Book 38, Number 4388: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> AbuZubyan said: A woman who had committed adultery was brought to Umar. He gave orders that she should be stoned. </p>
        <p> Ali passed by just then. He seized her and let her go. Umar was informed of it.  He said: Ask Ali to come to me.  Ali came to him and said: Commander of the Faithful, you know that the Apostle of Allah (peace_be_upon_him) said: There are three (people) whose actions are not recorded: A boy till he reaches puberty, a sleeper till he awakes, a lunatic till he is restored to reason. This is an idiot (mad) woman belonging to the family of so and so. Someone might have done this action with her when she suffered the fit of lunacy. </p>
        <p> Umar said: I do not know.  Ali said: I do not know. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4389"> Book 38, Number 4389: </a> </em>
