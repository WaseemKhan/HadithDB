      <em> <a name="038.4452"> Book 38, Number 4452: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> A man of Bakr ibn Layth came to the Prophet (peace_be_upon_him) and made confession four times that he had committed fornication with a woman, so he had a hundred lashes administered to him. The man had not been married. He then asked him to produce proof against the woman, and she said: I swear by Allah, Apostle of Allah, that he has lied. Then he was given the punishment of eighty lashes of falsehood. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4457"> Book 38, Number 4457: </a> </em>
      <blockquote>
        <p> Narrated Some Companions: </p>
        <p> AbuUmamah ibn Sahl ibn Hunayf said that some companions of the Apostle of Allah (peace_be_upon_him) told that one of their men suffered so much from some illness that he pined away until he was skin and bone (i.e. only a skeleton). A slave-girl of someone visited him, and he was cheered by her and had unlawful intercourse with her. When his people came to visit the patient, he told them about it. </p>
        <p> He said: Ask the Apostle of Allah (peace_be_upon_him) about the legal verdict for me, for I have had unlawful intercourse with a slave-girl who visited me. </p>
        <p> So they mentioned it to the Apostle of Allah (peace_be_upon_him) saying: We have never seen anyone (so weak) from illness as he is. If we bring him to you, his bones will disintegrate. He is only skin and bone. So the Apostle of Allah (peace_be_upon_him) commanded them to take one hundred twigs and strike him once. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4458"> Book 38, Number 4458: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> A slave-girl belonging to the house of the Apostle of Allah (peace_be_upon_him) committed fornication. He (the Prophet) said: Rush up, Ali, and inflict the prescribed punishment on her. I then hurried up, and saw that blood was flowing from her, and did not stop. So I came to him and he said: Have you finished inflicting (punishment on her)? I said: I went to her while her blood was flowing. He said: Leave her alone till her bleeding stops; then inflict the prescribed punishment on her. And inflict the prescribed punishment on those whom your right hands possess (i.e. slaves). </p>
      </blockquote>
      <hr>
      <em> <a name="038.4459"> Book 38, Number 4459: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> When my vindication came down, the Prophet (peace_be_upon_him) mounted the pulpit and mentioned that, and recited the Qur'an. Then when he came down from the pulpit he ordered regarding the two men and the woman, and they were given the prescribed punishment. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4460"> Book 38, Number 4460: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The tradition mentioned above (No. 4459) has also been transmitted by Muhammad ibn Ishaq through a different chain of narrators. But he did not mention Aisha. </p>
        <p> This version has: He (the Prophet) commanded regarding the two men and the woman who spoke obscenity were Hassan ibn Thabit and Mistah ibn Uthathah. An-Nufayl said: It is said that the woman was Hammah daughter of Jahsh. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4461"> Book 38, Number 4461: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) did not prescribe any punishment for drinking wine. Ibn Abbas said: A man who had drunk wine and become intoxicated was found staggering on the road, so he was taken to the Prophet (peace_be_upon_him). When he was opposite al-Abbas's house, he escaped, and going in to al-Abbas, he grasped hold of him. When that was mentioned to the Prophet (peace_be_upon_him), he laughed and said: Did he do that? and he gave no command regarding him. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4465"> Book 38, Number 4465: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> Hudayn ibn al-Mundhir ar-Ruqashi, who was AbuSasan, said: I was present with Uthman ibn Affan when al-Walid ibn Uqbah was brought to him. Humran and another man bore witness against him (for drinking wine). One of them testified that he had seen him drinking wine, and the other testified that he had seen him vomiting it. </p>
        <p> Uthman said: He could not vomit it, unless he did not drink it. He said to Ali: Inflict the prescribed punishment on him. Ali said to al-Hasan: Inflict the prescribed punishment on him. </p>
        <p> Al-Hasan said: He who has enjoyed its pleasure should also bear its burden.  So Ali said to Abdullah ibn Ja'far: Inflict the prescribed punishment on him. He took a whip and struck him with it while Ali was counting. </p>
        <p> When he reached (struck) forty (lashes), he said: It is sufficient. The Prophet (peace_be_upon_him) gave forty lashes. I think he also said: "And AbuBakr gave forty lashes, and Uthman eighty. This is all sunnah (standard practice). And this is dearer to me." </p>
      </blockquote>
      <hr>
      <em> <a name="038.4466"> Book 38, Number 4466: </a> </em>
      <blockquote>
        <p> Narrated Ali ibn AbuTalib: </p>
        <p> The Apostle of Allah (peace_be_upon_him) and AbuBakr gave forty lashes for drinking wine and Umar made it eighty. And all this is sunnah, the model and standard practice. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4467"> Book 38, Number 4467: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah ibn AbuSufyan: </p>
        <p> The Prophet (peace_be_upon_him) said: If they (the people) drink wine, flog them, again if they drink it, flog them. Again if they drink it, kill them. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4469"> Book 38, Number 4469: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If he is intoxicated, flog him; again if he is intoxicated, flog him; again if he is intoxicated, flog him if he does it again a fourth time, kill him. </p>
        <p> AbuDawud said: And there is a similar tradition of Umar ibn AbuSalamah, from his father, on the authority of AbuHurayrah, from the Prophet (peace_be_upon_him): If he drinks wine, flog him if he does it so again, a fourth time, kill him. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4470"> Book 38, Number 4470: </a> </em>
      <blockquote>
        <p> Narrated Qabisah ibn Dhuwayb: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone drinks wine, flog him; if he repeats it, flog him, and if he repeats it, flog him. If he does it again a third or a fourth time, kill him. A man who had drunk wine was brought (to him) and he gave him lashes. He was again brought to him, and he flogged him. He was again brought to him and he flogged him. He was again brought to him and he flogged him. The punishment of killing (for drinking) was  repealed, and a concession was allowed. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4472"> Book 38, Number 4472: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Azhar: </p>
        <p> I can still picture myself looking at the Apostle of Allah (peace_be_upon_him) who was among the camps of the Companions seeking the camp of Khalid ibn al-Walid, when a man who had drunk wine was brought before him. He asked the people: Beat him. Some struck him with sandals, some with sticks and some with fresh branches of the palm-tree (mitakhah). Ibn Wahb said: This (mitakhah) means green palm fronds. Then the apostle of Allah (peace_be_upon_him) took some dust from the ground and threw it on his face. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4473"> Book 38, Number 4473: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn al-Azhar: </p>
        <p> A man who had drunk wine was brought before the Prophet (peace_be_upon_him) when he was in Hunayn. He threw some dust on his face. He then ordered his Companions and they beat him with their sandals and whatever they had in their hands. He then said to them: Leave him,  and they left him. The Apostle of Allah (peace_be_upon_him) then died, and AbuBakr gave forty lashes for drinking wine, and then Umar in the beginning of his Caliphate inflicted forty stripes and at the end of his Caliphate he inflicted eighty stripes. Uthman (after him) inflicted both punishments, eighty and forty stripes, and finally Mu'awiyah established eighty stripes. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4474"> Book 38, Number 4474: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Azhar: </p>
        <p> I saw the Apostle of Allah (peace_be_upon_him) on the morning of the conquest of Mecca when I was a young boy. He was walking among the people, seeking the camp of Khalid ibn al-Walid. A man who had drunk wine was brought (before him) and he ordered them (to beat him). So they beat him with what they had in their hands. Some struck him with whips, some with sticks and some with sandals. The Apostle of Allah (peace_be_upon_him) threw some dust on his face. </p>
        <p> When a man who had drunk wine was brought before AbuBakr, he asked them (i.e. the people) about the number of beatings which they gave him. They numbered it forty. So AbuBakr gave him forty lashes. </p>
        <p> When Umar came to power, Khalid ibn al-Walid wrote to him: The people have become addicted to drinking wine and they look down upon the prescribed punishment and its penalty. </p>
        <p> He said: They are with you, ask them. The immigrants who embraced Islam in the beginning were with him. He asked them and they agreed on the fact that (a drunkard) should be given eighty lashes. </p>
        <p> Ali said: When a man drinks wine, he tells lies. I, therefore, think that he should be prescribed punishment that is prescribed for telling lies.. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4475"> Book 38, Number 4475: </a> </em>
      <blockquote>
        <p> Narrated Hakim ibn Hizam: </p>
        <p> The Apostle of Allah (peace_be_upon_him) forbade to take retaliation in the mosque, to recite verses in it and to inflict the prescribed punishments in it. </p>
      </blockquote>
      <hr>
      <em> <a name="038.4478"> Book 38, Number 4478: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When one of you inflicts a beating, he should avoid striking the face. </p>
      </blockquote>
