      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 41: </a> </em>
      <h1> <em> General Behavior (Kitab Al-Adab) </em> </h1>
      <hr>
      <em> <a name="041.4756"> Book 41, Number 4756: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> I served the Prophet  (peace_be_upon_him) at Medina for ten years. I was a boy. Every work that I did was not according to the desire of my master, but he never said to me: Fie, nor did he say to me: Why did you do this? or Why did you not do this? </p>
      </blockquote>
      <hr>
      <em> <a name="041.4757"> Book 41, Number 4757: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to sit with us in meetings and talk to us. When he stood up we also used to stand up and see him entering the house of one of his wives. One day he talked to us and we stood up as he stood up and we saw that an Arabi (a nomadic Arab) caught hold of him and gave his cloak a violent tug making his neck red. </p>
        <p> AbuHurayrah said: The cloak was coarse. He turned to him and the Arabi said to him: Load these two camels of mine, for you do not give me anything from your property or from your father's property. </p>
        <p> The Prophet (peace_be_upon_him) said to him: No, I ask Allah's forgiveness; no, I ask Allah's forgiveness; no, I ask Allah's forgiveness. I shall not give you the camel-load until you make amends for the way in which you tugged at me. </p>
        <p> Each time the Arabi said to him: I swear by Allah, I shall not do so. </p>
        <p> He then mentioned the rest of the tradition. He (the Prophet), then called a man and said to him: Load these two camels of his: one camel with barley and the other with dates. He then turned to us and said: Go on your way with the blessing of Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4758"> Book 41, Number 4758: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: Good way, dignified good bearing and moderation are the twenty-fifth part of Prophecy. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4759"> Book 41, Number 4759: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: if anyone suppresses anger when he is in a position to give vent to it, Allah, the Exalted, will call him on the Day of Resurrection over the heads of all creatures, and ask him to choose any of the bright and large eyed maidens he wishes. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4760"> Book 41, Number 4760: </a> </em>
      <blockquote>
        <p> Narrated Son of a Companion: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: He then mentioned a similar tradition described above. This version has: Allah will fill his heart with security and faith. He did not mention the words "Allah will call him". This version further adds: He who gives up wearing beautiful garments when he is able to do so (out of humility, as Bishr's version has) will be clothed by Allah with the robe of honour, and he who marries for Allah's sake will be crowned by Allah with the crown of Kingdom. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4762"> Book 41, Number 4762: </a> </em>
      <blockquote>
        <p> Narrated Mu'adh ibn Jabal: </p>
        <p> Two men reviled each other in the presence of the Prophet (peace_be_upon_him) and one of them became excessively angry so much so that I thought that his nose will break up on account of excess of anger. The Prophet (peace_be_upon_him) said: I know a phrase which, if he repeated, he could get rid of this angry feeling. They asked: What is it, Apostle of Allah? He replied: He should say: I seek refuge in Thee from the accursed devil. Mu'adh then began to ask him to do so, but he refused and persisted in quarrelling, and began to enhance his anger. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4764"> Book 41, Number 4764: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to us: When one of you becomes angry while standing, he should sit down. If the anger leaves him, well and good; otherwise he should lie down. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4766"> Book 41, Number 4766: </a> </em>
      <blockquote>
        <p> Narrated Atiyyah as-Sa'di: </p>
        <p> AbuWa'il al-Qass said: We entered upon Urwah ibn Muhammad ibn as-Sa'di. A man spoke to him and made him angry. So he stood and performed ablution; he then returned and performed ablution, and said: My father told me on the authority of my grandfather Atiyyah who reported the Apostle of Allah (peace_be_upon_him) as saying: Anger comes from the devil, the devil was created of fire, and fire is extinguished only with water; so when one of you becomes angry, he should perform ablution. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4770"> Book 41, Number 4770: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> When the Prophet (peace_be_upon_him) was informed of anything of a certain man, he would not say: What is the matter with so and so that he says? But he would say: What is the matter with the people that they say such and such? </p>
      </blockquote>
      <hr>
      <em> <a name="041.4771"> Book 41, Number 4771: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> A man who had the mark of yellowness on him came to the Apostle of Allah (peace_be_upon_him). The apostle of Allah (peace_be_upon_him) rarely mentioned anything of a man which he disliked before him. When he went out, he said: Would that you asked him to wash it from him. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4772"> Book 41, Number 4772: </a> </em>
      <blockquote>
        <p> Narrated AbuSalamah ; AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The believer is simple and generous, but the profligate is deceitful and ignoble. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4774"> Book 41, Number 4774: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> A man asked permission  to see the Prophet (peace_be_upon_him), and the Prophet (peace_be_upon_him) said: He is a bad member of the tribe. When he entered, the Apostle of Allah (peace_be_upon_him) treated in a frank and friendly way and spoke to him. When he departed , I said: Apostle of Allah! When he asked permission, you said: He is a bad member of the tribe, but when he entered, you treated him in a frank and friendly way. The Apostle of Allah replied: Aisha! Allah does not like the one who is unseemly and lewd in his language. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4776"> Book 41, Number 4776: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> I never said that when any man brought his mouth to the ear of the Apostle of Allah (peace_be_upon_him) and he withdrew his head until the man himself withdrew his head, and I never saw that when any man took him by his hand and he withdrew his hand, until the man himself withdrew his hand. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4780"> Book 41, Number 4780: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: By his good character a believer will attain the degree of one who prays during the night and fasts during the day. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4781"> Book 41, Number 4781: </a> </em>
      <blockquote>
        <p> Narrated AbudDarda': </p>
        <p> The Prophet (peace_be_upon_him) said: There is nothing heavier than good character put in the scale of a believer on the Day of Resurrection. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4782"> Book 41, Number 4782: </a> </em>
      <blockquote>
        <p> Narrated AbuUmamah: </p>
        <p> The Prophet (peace_be_upon_him) said: I guarantee a house in the surroundings of Paradise for a man who avoids quarrelling even if he were in the right, a house in the middle of Paradise for a man who avoids lying even if he were joking, and a house in the upper part of Paradise for a man who made his character good. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4788"> Book 41, Number 4788: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn ash-Shikhkhir: </p>
        <p> I went with a deputation of Banu Amir to the apostle of Allah (peace_be_upon_him), and we said: You are our lord (sayyid). To this he replied: The lord is Allah, the Blessed and Exalted. Then we said: And the one of us most endowed with excellence and superiority. To this he replied: Say what you have to say, or part of what you have to say, and do not let the devil make you his agents. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4790"> Book 41, Number 4790: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Al-Miqdam ibn Shurayh, quoting his father, said: I asked Aisha about living in the desert. She said: The Apostle of Allah (peace_be_upon_him) used to go to the desert to these rivulets. Once he intended to go to the desert and he sent to me a she-camel from the camel of sadaqah which had not been used for riding so far. He said to me: Aisha! show gentleness, for if gentleness is found in anything, it beautifies it and when it is taken out from anything it damages it. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4791"> Book 41, Number 4791: </a> </em>
      <blockquote>
        <p> Narrated Jarir: </p>
        <p> The Prophet (peace_be_upon_him) said: He who is deprived of gentleness is deprived of good. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4792"> Book 41, Number 4792: </a> </em>
      <blockquote>
        <p> Narrated Sa'd: </p>
        <p> The Prophet (peace_be_upon_him) said: There is hesitation in everything except in the actions of the next world. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4793"> Book 41, Number 4793: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who does not thank Allah does not thank people. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4794"> Book 41, Number 4794: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Immigrants (Muhajirun) said: Apostle of Allah! the Helpers (Ansar) got the entire reward. He said: no, so long as you pray to Allah for them and praise them. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4795"> Book 41, Number 4795: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If someone is given something, he should give a return for it provided he can afford; if he cannot afford, he should praise him. He who praises him for it, thanks him, and he who conceals it is ungrateful to him. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4796"> Book 41, Number 4796: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) said: If someone is donated something, and he mentions it, he thanks for it, and if he conceals it, he is ungrateful for it. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4799"> Book 41, Number 4799: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> The Prophet (peace_be_upon_him) said: the same occasion: Help the oppressed (sorrowful) and guide those who have lost their way. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4800"> Book 41, Number 4800: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> A woman came to the Apostle of Allah (peace_be_upon_him) and said: Apostle of Allah: I have some need with you. He said to her: Mother of so and so, sit in the corner of any street you wish and I shall sit with you. So she sat and the Apostle of Allah (peace_be_upon_him) also sat with her till she fulfilled her need. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4802"> Book 41, Number 4802: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) as saying: The best places to sit are those which provide most room. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4803"> Book 41, Number 4803: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> AbulQasim (peace_be_upon_him) said: When one of you is in the sun (Shams)--Makhlad's version has "fay'"--and the shadow withdraws from him so that he is partly in sun and partly in shade, he should get up. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4804"> Book 41, Number 4804: </a> </em>
      <blockquote>
        <p> Narrated AbuHazim al-Bajali: </p>
        <p> AbuHazim came when the Apostle of Allah (peace_be_upon_him) was addressing. He stood in the sun. He ordered him (to shift) and he shifted to the shade. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4807"> Book 41, Number 4807: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Samurah: </p>
        <p> When we came to the Prophet (peace_be_upon_him), each one would sit down where there was room. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4808"> Book 41, Number 4808: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) cursed the one who sat in the middle of a circle. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4809"> Book 41, Number 4809: </a> </em>
      <blockquote>
        <p> Narrated AbuBakrah: </p>
        <p> Sa'id ibn AbulHasan said: When AbuBakrah came to us to give some evidence, a man got up from his place, but he refused to sit in it saying: The Prophet (peace_be_upon_him) forbade this, and the Prophet (peace_be_upon_him) forbade anyone to wipe his hand on the garment of anyone whose clothing he had not himself provided. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4810"> Book 41, Number 4810: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> A man came to the Prophet (peace_be_upon_him), another man got up from his place for him, and when he went to sit in it, the Prophet (peace_be_upon_him) forbade him. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4811"> Book 41, Number 4811: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: A believer who recites the Qur'an is like a citron whose fragrance is sweet and whose taste is sweet, a believer who does not recite the Qur'an is like a date which has no fragrance but has sweet taste, a profligate who recites the Qur'an is like basil whose fragrance is sweet but whose taste is bitter, and the profligate who does not recite the Qur'an is like the colocynth which has a bitter taste and has not fragrance. A good companion is like a man who has musk; if nothing of it goes to you, its fragrance will (certainly) go to you; and a bad companion is like a man who has bellows; if its (black) root does not go to you, its smoke will (certainly) go to you. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4814"> Book 41, Number 4814: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: Associate only with a believer, and let only a God-fearing man eat your meals. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4815"> Book 41, Number 4815: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: A man follows the religion of his friend; so each one should consider whom he makes his friend. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4818"> Book 41, Number 4818: </a> </em>
      <blockquote>
        <p> Narrated As-Sa'ib: </p>
        <p> I came to the Prophet (peace_be_upon_him). The people began to praise me and make a mention of me. The Apostle of Allah (peace_be_upon_him) said: I know you, that is, he knew him. I said: My father and mother be sacrificed for you! you were my partner and how good a partner ; you neither disputed nor quarrelled. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4819"> Book 41, Number 4819: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Salam: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) sat talking (to the people), he would often raise his eyes to the sky. </p>
      </blockquote>
      <hr>
      <em> <a name="041.4820"> Book 41, Number 4820: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) spoke in a distinct and leisurely manner. </p>
