      <blockquote>
        <p> Narrated Umm Atiyyah al-Ansariyyah: </p>
        <p> A woman used to perform circumcision in Medina. The Prophet (peace_be_upon_him) said to her: Do not cut severely as that is better for a woman and more desirable for a husband. </p>
      </blockquote>
