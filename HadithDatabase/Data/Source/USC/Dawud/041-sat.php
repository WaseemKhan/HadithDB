        <p> Narrated Dukayn ibn Sa'id al-Muzani: </p>
        <p> We came to the Prophet (peace_be_upon_him) and asked him for some corn. He said: Go, Umar, and give them. He ascended with us a room upstairs, took a key from his apartment and opened it. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5220"> Book 41, Number 5220: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Habashi: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone cuts the lote-tree, Allah brings him headlong into Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5221"> Book 41, Number 5221: </a> </em>
      <blockquote>
        <p> Narrated Hassan ibn Ibrahim: </p>
        <p> I asked Hisham ibn Urwah about the cutting of a lote-tree when he was leaning against the house of Urwah. He said: Do you not see these doors and leaves? These were made of the lote-tree of Urwah which Urwah used to cut from his hand? He said: There is no harm in it. </p>
        <p> Humayd's version adds: You have brought an innovation, O Iraqi! He said: The innovation is from you. I heard someone say at Mecca: The Apostle of Allah (peace_be_upon_him) cursed him who cuts a lote-tree. He then mentioned the rest of the tradition to the same effect. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5222"> Book 41, Number 5222: </a> </em>
      <blockquote>
        <p> Narrated Buraydah ibn al-Hasib: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: A human being has three hundred and sixty joints for each of which he must give alms. The people asked him: Who is capable of doing this ? He replied: It may be mucus in the mosque which you bury, and something which you remove from the road; but if you do not find such, two rak'ahs in the forenoon will be sufficient for you. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5223"> Book 41, Number 5223: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The Prophet (peace_be_upon_him) said: In the morning alms are due from every bone in man's fingers and toes.  Salutation to everyone he meets is alms; enjoining good is alms; forbidding what is disreputable is alms; removing what is harmful from the road is alms; having sexual intercourse with his wife is alms. The people asked: He fulfils his desire, Apostle of Allah; is  it alms? He replied: Tell me if he fulfilled his desire where he had no right, would he commit a sin ? He then said: Two rak'ahs which one prays in the forenoon serve instead of all that. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5225"> Book 41, Number 5225: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: A man never did a good deed but removed a thorny branch from the road; it was either in the tree and someone cut it and threw it on the road, or it was lying in it, he removed it. Allah accepted this good deed of his and brought him into Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5228"> Book 41, Number 5228: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: We have not made peace with them since we fought with them, so he who leaves any of them alone through fear does not belong to us. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5229"> Book 41, Number 5229: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet (peace_be_upon_him) said: Kill all the snakes, and he who fears their revenge does not belong to me. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5230"> Book 41, Number 5230: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: He who leaves the snakes along through fear of their pursuit, does not belong to us. We have not made peace with them since we have fought with them. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5231"> Book 41, Number 5231: </a> </em>
      <blockquote>
        <p> Narrated Al-Abbas ibn AbdulMuttalib: </p>
        <p> Al-Abbas said to the Apostle of Allah (peace_be_upon_him): We wish to sweep out Zamzam, but in it there are some of these Jinnan, meaning small snakes; so the Apostle of Allah (peace_be_upon_him) ordered that they should be killed. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5236"> Book 41, Number 5236: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> Muhammad ibn AbuYahya said that his father told that he and his companion went to AbuSa'id al-Khudri to pay a sick visit to him. He said: Then we came out from him and met a companion of ours who wanted to go to him. We went ahead and sat in the mosque. He then came back and told us that he heard AbuSa'id al-Khudri say: The Apostle of Allah (peace_be_upon_him) said: Some snakes are jinn; so when anyone sees one of them in his house, he should give it a warning three times. If it return (after that), he should kill it, for it is a devil. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5240"> Book 41, Number 5240: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman Ibn AbuLayla: </p>
        <p> The Apostle of Allah (peace_be_upon_him) was asked about the house-snakes. He said: When you see one of them in your dwelling, say: I adjure you by the covenant which Noah made with you, and I adjure you by the covenant which Solomon made with you not to harm us. Then if they come back, kill them. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5241"> Book 41, Number 5241: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> Kill all the snakes except the little white one which looks like a silver wand. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5243"> Book 41, Number 5243: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone kills a gecko with the first blow, such and such number of good deeds will be recorded for him, if he kills it with the second blow, such and such number  of good deeds will be recorded for him less than the former; and if he kills it with the third blow, such and such number of good deeds will be recorded for him, less than the former. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5244"> Book 41, Number 5244: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: For the first blow seventy good deeds will be recorded. </p>
      </blockquote>
