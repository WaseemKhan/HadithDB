      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) prohibited to kill four creatures: ants, bees, hoopoes, and sparrow-hawks. </p>
      </blockquote>
      <hr>
      <em> <a name="041.5249"> Book 41, Number 5249: </a> </em>
      <blockquote>
        <p> Narrated AbdurRahman ibn Uthman: </p>
        <p> When a physician consulted the Prophet (peace_be_upon_him) about putting frogs in medicine, he forbade him to kill them. </p>
      </blockquote>
      <hr>
