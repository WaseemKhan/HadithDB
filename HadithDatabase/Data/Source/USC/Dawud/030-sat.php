      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 30: </a> </em>
      <h1> <em> Dialects and Readings of the Qur'an (Kitab Al-Huruf Wa Al-Qira'at) </em> </h1>
      <hr>
      <em> <a name="030.3958"> Book 30, Number 3958: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Prophet (peace_be_upon_him) read the Qur'anic verse, "And take ye the Station of Abraham as a place of prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3960"> Book 30, Number 3960: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The verse "And no Prophet could (ever) be false to his trust" was revealed about a red velvet. When it was found missing on the day of Badr, some people said; Perhaps the Apostle of Allah (peace_be_upon_him) has taken it. So Allah, the Exalted, sent down "And no prophet could (ever) be false to his trust" to the end of the verse. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3962"> Book 30, Number 3962: </a> </em>
      <blockquote>
        <p> Narrated Laqit ibn Sabirah: </p>
        <p> I came in the deputation of Banu al-Muntafiq to the Apostle of Allah (peace_be_upon_him). He then narrated the rest of the tradition. The Prophet (peace_be_upon_him) said: la tahsibanna (do not think) and did not say: la tahsabanna (do not think). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3964"> Book 30, Number 3964: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> The Prophet (peace_be_upon_him) used to read: "Not equal are those believers who sit (at home) and receive no hurt (ghayru ulid-darari) but the narrator Sa'id did not say the words "used to read" </p>
      </blockquote>
      <hr>
      <em> <a name="030.3965"> Book 30, Number 3965: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Apostle of Allah (peace_be_upon_him) red the verse: "eye for eye" (al-'aynu bil-'ayn). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3966"> Book 30, Number 3966: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) read the verse: "We ordained therein for them: Life for life and eye for eye (an-nafsa bin-nafsi wal-'aynu bil-'ayn). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3967"> Book 30, Number 3967: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> Atiyyah ibn Sa'd al-Awfi said: I recited to Abdullah ibn Umar the verse: "It is Allah Who created you in a state of (helplessness) weakness (min da'f)."  He said: (Read) min du'f. I recited it to the Apostle of Allah (peace_be_upon_him) as you recited it to me, and he gripped me as I gripped you. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3968"> Book 30, Number 3968: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) read the verse mentioned above, "min du'f." </p>
      </blockquote>
      <hr>
      <em> <a name="030.3970"> Book 30, Number 3970: </a> </em>
      <blockquote>
        <p> Narrated Ibn Abzi: </p>
        <p> Ubayy ibn Ka'b) said: The Prophet (peace_be_upon_him) read the verse: "Say: In the bounty of Allah and in His mercy--in that let you rejoice: that is better than the wealth you hoard." </p>
      </blockquote>
      <hr>
      <em> <a name="030.3971"> Book 30, Number 3971: </a> </em>
      <blockquote>
        <p> Narrated Asma' daughter of Yazid: </p>
        <p> She heard the Prophet (peace_be_upon_him) read the verse: "He acted unrighteously." (innahu 'amila ghayra salih). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3972"> Book 30, Number 3972: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> Shahr ibn Hawshab said: I asked Umm Salamah: How did the Apostle of Allah  (peace_be_upon_him) read this verse: "For his conduct is unrighteous (innahu 'amalun  ghayru salih". She replied: He read it: "He acted unrighteously" (innahu 'amila ghayra salih). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3973"> Book 30, Number 3973: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b:: </p>
        <p> When the Apostle of Allah (peace_be_upon_him) prayed, he began with himself and said: May the mercy of Allah be upon us and upon Moses. If he had patience, he would have seen marvels from his Companion. But he said: "(Moses) said: If ever I ask thee about anything after this, keep me not in they company: then wouldst thou have received (full) excuse from my side". Hamzah lengthened it. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3974"> Book 30, Number 3974: </a> </em>
      <blockquote>
        <p> Narrated Ubayy ibn Ka'b: </p>
        <p> The Prophet (peace_be_upon_him) read the Qur'anic verse: "Thou hast received (full) excuse from me (min ladunni)" and put tashdid (doubling of consonants) on nun (n). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3975"> Book 30, Number 3975: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> Ubayy ibn Ka'b made me read the following verse as the Apostle of Allah (peace_be_upon_him) made him read: "in a spring of murky water" (fi 'aynin hami'atin) with short vowel a after h. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3976"> Book 30, Number 3976: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Prophet (peace_be_upon_him) said: A man from the Illiyyun will look downwards at the people of Paradise and Paradise will be glittering as if it were a brilliant star. </p>
        <p> He (the narrator) said: In this way the word durri (brilliant) occurs in this tradition, i.e. the letter dal (d) has short vowel u and it has no hamzah ('). AbuBakr and Umar will be of them and will have some additional blessings. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3977"> Book 30, Number 3977: </a> </em>
      <blockquote>
        <p> Narrated Farwah ibn Musayk al-Ghutayfi: </p>
        <p> I came to the Prophet (peace_be_upon_him). He then narrated the rest of the tradition. </p>
        <p> A man from the people said: "Apostle of Allah! tell us about Saba'; what is it: land or woman? He replied: It is neither land nor woman; it is a man to whom ten children of the Arabs were born: six of them lived in the Yemen and four lived in Syria. </p>
        <p> The narrator Uthman said al-Ghatafani instead of al-Ghutayfi. He said: It has been transmitted to us by al-Hasan ibn al-Hakam an-Nakha'i. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3980"> Book 30, Number 3980: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) read: "(There is for him) Rest and satisfaction" (faruhun wa rayhan). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3982"> Book 30, Number 3982: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Apostle of Allah (peace_be_upon_him) made me read the verse "It is I who give (all) sustenance, Lord of power, steadfast (for ever). </p>
      </blockquote>
      <hr>
      <em> <a name="030.3983"> Book 30, Number 3983: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Mas'ud: </p>
        <p> The Prophet (peace_be_upon_him) used to read the verse "Is there any that will receive admonition (muddakir)? " that is with doubling of consonant [(dal)(d)]. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3984"> Book 30, Number 3984: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> I saw the Prophet (peace_be_upon_him) reading the verse; "does he think that his wealth would make him last for ever?" </p>
      </blockquote>
      <hr>
      <em> <a name="030.3987"> Book 30, Number 3987: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Apostle of Allah (peace_be_upon_him) related a tradition in which he mentioned the words "Jibril and Mikal" and he pronounced them "Jibra'ila wa Mika'ila." </p>
      </blockquote>
      <hr>
      <em> <a name="030.3988"> Book 30, Number 3988: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> The Apostle of Allah (peace_be_upon_him) mentioned the name of the one who will sound the trumpet (sahib as-sur) and said: On his right will be Jibra'il and on his left will be Mika'il. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3989"> Book 30, Number 3989: </a> </em>
      <blockquote>
        <p> Narrated Ibn al-Musayyab: </p>
        <p> The Prophet (peace_be_upon_him), AbuBakr, Umar and Uthman used to read "maliki yawmid-din (master of the Day of Judgment)". The first to read maliki yawmid-din was Marwan. </p>
      </blockquote>
      <hr>
      <em> <a name="030.3990"> Book 30, Number 3990: </a> </em>
      <blockquote>
        <p> Narrated Umm Salamah, Ummul Mu'minin: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to recite: "In the name of Allah, the Cherisher and Sustainer of the worlds; most Gracious, most Merciful; Master of the Day of Judgment," breaking its recitation into verses, one after another. </p>
      </blockquote>
