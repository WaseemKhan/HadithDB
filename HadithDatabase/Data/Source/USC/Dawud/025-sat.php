      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 25: </a> </em>
      <h1> <em> Knowledge (Kitab Al-Ilm) </em> </h1>
      <hr>
      <em> <a name="025.3634"> Book 25, Number 3634: </a> </em>
      <blockquote>
        <p> Narrated AbudDarda': </p>
        <p> Kathir ibn Qays said: I was sitting with AbudDarda' in the mosque of Damascus. </p>
        <p> A man came to him and said: AbudDarda, I have come to you from the town of the Apostle of Allah (peace_be_upon_him) for a tradition that I have heard you relate from the Apostle of Allah (peace_be_upon_him). I have come for no other purpose. </p>
        <p> He said: I heard the Apostle of Allah (peace_be_upon_him) say: If anyone travels on a road in search of knowledge, Allah will cause him to travel on one of the roads of Paradise. The angels will lower their wings in their great pleasure with one who seeks knowledge, the inhabitants of the heavens and the Earth and the fish in the deep waters will ask forgiveness for the learned man. The superiority of the learned man over the devout is like that of the moon, on the night when it is full, over the rest of the stars. The learned are the heirs of the Prophets, and the Prophets leave neither dinar nor dirham, leaving only knowledge, and he who takes it takes an abundant portion. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3637"> Book 25, Number 3637: </a> </em>
      <blockquote>
        <p> Narrated AbuNamlah al-Ansari: </p>
        <p> When he was sitting with the Apostle of Allah (peace_be_upon_him) and a Jew was also with him, a funeral passed by him. He (the Jew) asked (Him): Muhammad, does this funeral speak? The Prophet (peace_be_upon_him) said: Allah has more knowledge. The Jew said: It speaks. </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: Whatever the people of the Book tell you, do not verify them, nor falsify them, but say: We believe in Allah and His Apostle. If it is false, do not confirm it, and if it is right, do not falsify it. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3638"> Book 25, Number 3638: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> The Apostle of Allah (peace_be_upon_him) ordered me (to learn the writing of the Jews), so I learnt for him the writing of the Jews. He said: I swear by Allah, I do not trust Jews in respect of writing for me. So I learnt it, and only a fortnight passed that . I mastered it. I would write for him when he wrote (to them), and read to him when something was written to him. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3639"> Book 25, Number 3639: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> I used to write everything which I heard from the Apostle of Allah (peace_be_upon_him). I intended (by it) to memorise it. The Quraysh prohibited me saying: Do you write everything  that you hear from him while the Apostle of Allah (peace_be_upon_him) is a human being: he speaks in anger and pleasure? So I stopped writing, and mentioned it to the Apostle of Allah (peace_be_upon_him). He signalled with his finger to him mouth and said: Write, by Him in Whose hand my soul lies, only right comes out from it. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3640"> Book 25, Number 3640: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> Al-Muttalib ibn Abdullah ibn Hantab said: Zayd ibn Thabit entered upon Mu'awiyah and asked him about a tradition. He ordered a man to write it. Zayd said: The Apostle of Allah (peace_be_upon_him) ordered us not to write any of his traditions. So he erased it. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3644"> Book 25, Number 3644: </a> </em>
      <blockquote>
        <p> Narrated Jundub: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone interprets the Book of Allah in the light of his opinion even if he is right, he has erred. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3645"> Book 25, Number 3645: </a> </em>
      <blockquote>
        <p> Narrated A man: </p>
        <p> AbuSalam said on the authority of a man who served the Holy Prophet (peace_be_upon_him) that whenever he talked, he repeated it three times. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3647"> Book 25, Number 3647: </a> </em>
      <blockquote>
        <p> Narrated Aisha, Ummul Mu'minin: </p>
        <p> Are you not surprised at AbuHurayrah? He came and sat beside my apartment, and began to narrate traditions from the Apostle of Allah (peace_be_upon_him) making me hear them. I am saying supererogatory prayer. He got up (and went away) before I finished my prayer. Had I found him, I would have replied to him. The Apostle of Allah (peace_be_upon_him) did not narrate traditions quickly one after another as you narrate quickly. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3648"> Book 25, Number 3648: </a> </em>
      <blockquote>
        <p> Narrated Mu'awiyah: </p>
        <p> The Holy Prophet (peace_be_upon_him) forbade the discussion of thorny  questions. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3649"> Book 25, Number 3649: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone is given a legal decision ignorantly, the sin rests on the one who gave it. Sulayman al-Mahri added in his version: If anyone advises his brother, knowing that guidance lies in another direction, he has deceived him. These are the wordings of Sulayman. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3650"> Book 25, Number 3650: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: He who is asked something he knows and conceals it will have a bridle of fire put on him on the Day of Resurrection. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3651"> Book 25, Number 3651: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Abbas: </p>
        <p> The Prophet (peace_be_upon_him) said: You hear (from me), and others will hear from you; and people will hear from them who heard from you. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3652"> Book 25, Number 3652: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Thabit: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: May Allah brighten a man who hears a tradition from us, gets it by heart and passes it on to others. Many a bearer of knowledge conveys it to one who is more versed than he is; and many a bearer of knowledge is not versed in it. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3654"> Book 25, Number 3654: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: relate traditions from the children of Isra'il; there is no harm. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3655"> Book 25, Number 3655: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) used to relate to us traditions from the children of Isra'il till morning came; he would not get up except for obligatory prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3656"> Book 25, Number 3656: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone acquires knowledge of things by which Allah's good pleasure is sought, but acquires it only to get some worldly advantage, he will not experience the arf, i.e. the odour, of Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3657"> Book 25, Number 3657: </a> </em>
      <blockquote>
        <p> Narrated Awf ibn Malik al-Ashja'i: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: Only a ruler, or one put in charge, or one who is presumptuous, gives instructions. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3658"> Book 25, Number 3658: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri: </p>
        <p> I was sitting in the company of the poor members of the emigrants. Some of them were sitting together because of lack of clothing while a reader was reciting to us. All of a sudden the Apostle of Allah (peace_be_upon_him) came along and stood beside us. When the Apostle of Allah (peace_be_upon_him) stood, the reader stopped and greeted him. </p>
        <p> He asked: What were you doing?  We said: Apostle of Allah! We had a reader who was reciting to us and we were listening to the Book of Allah, the Exalted. </p>
        <p> The Apostle of Allah (peace_be_upon_him) then said: Praise be to Allah Who has put among my people those with whom I have been ordered to stay. The Apostle of Allah (peace_be_upon_him) then sat among us so as to be like one of us, and when he had made a sign with his hand they sat in a circle with their faces turned towards him. </p>
        <p> The narrator said: I think that the Apostle of Allah (peace_be_upon_him) did not recognize any of them except me. </p>
        <p> The Apostle of Allah (peace_be_upon_him) then said: Rejoice, you group of poor emigrants, in the announcement that you will have perfect light on the Day of Resurrection. You will enter Paradise half a day before the rich, and that is five hundred years. </p>
      </blockquote>
      <hr>
      <em> <a name="025.3659"> Book 25, Number 3659: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: That I sit in the company of the people who remember Allah the Exalted from morning prayer till the sun rises is clearer to me than that I emancipate four slaves from the children of Isra'il, and that I sit with the people who remember Allah from afternoon prayer till the sun sets is dearer to me than that I emancipate four slaves. </p>
      </blockquote>
