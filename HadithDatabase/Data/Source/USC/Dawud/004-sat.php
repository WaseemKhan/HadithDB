      <div class="content"><em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 4: </a> </em>
        <h1> <em> Prayer (Kitab Al-Salat): Detailed Rules of Law about the Prayer during Journey </em> </h1>
        <hr>
        <em> <a name="004.1194"> Book 4, Number 1194: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The prayer was prescribed as consisting of two rak'ahs both when one was resident and when travelling. The prayer while travelling was left according to the original prescription and the prayer of one who was resident was enhanced. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1199"> Book 4, Number 1199: </a> </em>
        <blockquote>
          <p> Narrated Uqbah ibn Amir: </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) say: Allah is pleased with a shepherd of goats who calls to prayer at the peak of a mountain, and offers prayer, Allah, the Exalted, says: Look at this servant of Mine; he calls to prayer and offers it and he fears Me. So I forgive him and admit him to paradise. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1201"> Book 4, Number 1201: </a> </em>
        <blockquote>
          <p> Narrated Anas ibn Malik: </p>
          <p> When the Apostle of Allah (peace_be_upon_him) halted at a certain place (while on a journey), he would not leave that place till he offered the noon prayer. A man said to him: Even if in the middle of the day? He replied: Even if in the middle of the day. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1203"> Book 4, Number 1203: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> Ibn Umar was informed about the death of Safiyyah (the wife of the Prophet) when he was at Mecca. He proceeded till the sun set and the stars shined. He said: When the Prophet (peace_be_upon_him) was in a hurry about something while on a journey, he would combine both these prayers. He proceed till twilight had disappeared. He then combined both of them (the prayers). </p>
        </blockquote>
        <hr>
        <em> <a name="004.1204"> Book 4, Number 1204: </a> </em>
        <blockquote>
          <p> Narrated Mu'adh ibn Jabal: </p>
          <p> On the expedition to Tabuk if the sun had passed the meridian before the apostle of Allah (peace_be_upon_him) moved off, he combined the noon and the afternoon prayers; but if he moved off before the sun had passed the meridian, he delayed the noon prayer till he halted for the afternoon prayer. He acted similarly for the sunset prayer; if the sun set before he moved off, he combined the sunset and the night prayers, but if he moved off before sunset, he delayed the sunset prayer till he halted for the night prayer and then combined them. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1208"> Book 4, Number 1208: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> Abdullah ibn Waqid said: The mu'adhdhin of Ibn Umar said: prayer (i.e. the time of prayer has come). He said: Go ahead. He then alighted before the disappearance. He then offered the night prayer. He then said: When the Apostle of Allah (peace_be_upon_him) was in a hurry about something, he would do as I did. Then he travelled and covered a distance of three days' journey on the day. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1211"> Book 4, Number 1211: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> When the sun set at Mecca, the Apostle of Allah (peace_be_upon_him) combined the two prayers at Sarif. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1213"> Book 4, Number 1213: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> Abdullah ibn Dinar said: The sun set when I was with Abdullah ibn Umar. We proceeded, and when we saw that the evening came, we said prayer. He went on travelling until the twilight disappeared and the stars became thick. He then slighted and combined the two prayers. Then he said: I saw the Apostle of Allah (peace_be_upon_him); when he hastened his travelling, he would pray like this prayer of mine. He said: He would combine the two prayers after the passing of a part of night. AbuDawud said: This has been transmitted by Asim ibn Muhammad from his brother on the authority of Salim and this has also been narrated by Ibn AbuNajih from Isma'il ibn AbdurRahman ibn Dhuwayb saying that Ibn Umar would combine the two prayers after the disappearance of twilight. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1216"> Book 4, Number 1216: </a> </em>
        <blockquote>
          <p> Narrated Mu'adh ibn Jabal: </p>
          <p> The Prophet (peace_be_upon_him) was engaged in the Battle of Tabuk. If he moved off before the sun had declined, he would delay the noon prayer till he would combine it with the afternoon prayer and would offer them together. If he moved off after the sun had declined, he would combine the noon and afternoon prayers, and then he proceeded; if he moved off before the evening prayer, he would delay the evening prayer; he would offer it along with the night prayer, he would delay the evening prayer; he would offer it along with the night prayer. If he moved off after the evening prayer, he would offer the night prayer earlier and offer it along with the evening prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1218"> Book 4, Number 1218: </a> </em>
        <blockquote>
          <p> Narrated Al-Bara' ibn Azib: </p>
          <p> I accompanied the Apostle of Allah (peace_be_upon_him) on eighteen journeys and I never saw him fail to pray two rak'ahs when the sun had passed the meridian before offering the noon prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1221"> Book 4, Number 1221: </a> </em>
        <blockquote>
          <p> Narrated Anas ibn Malik: </p>
          <p> When the Apostle of Allah (peace_be_upon_him) was on a journey and wished to say voluntary prayer, he made his she-camel face the qiblah and uttered the takbir (Allah is most great), then prayed in whatever direction his mount made his face. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1223"> Book 4, Number 1223: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) sent me on some business, and when I came to him he was praying on (the back of) his riding beast (moving) towards the east and making the prostration lower than the bowing. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1224"> Book 4, Number 1224: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Ata' ibn AbuRabah asked Aisha: Can women offer prayer on a riding beast? She replied: They were not permitted to do so in hardship or comfort. Muhammad ibn Shu'ayb said: This (prohibition) applies to the obligatory prayers. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1225"> Book 4, Number 1225: </a> </em>
        <blockquote>
          <p> Narrated Imran ibn Husayn: </p>
          <p> I went on an expedition with the Apostle of Allah (peace_be_upon_him), and I was present with him at the conquest. He stayed eighteen days in Mecca and prayed only two rak'ahs (at each time of prayer). And he said: You who live in the town must pray four; we are travellers. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1226"> Book 4, Number 1226: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) had a stop of seventeen days in Mecca and he shortened the prayer (i.e. prayed two rak'ahs at each time of prayer). Ibn Abbas said: He who stays seventeen days should shorten the prayer; and who stays more than that should offer complete prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1227"> Book 4, Number 1227: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) stayed fifteen days in Mecca in the year  of Conquest. Shortening the prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1230"> Book 4, Number 1230: </a> </em>
        <blockquote>
          <p> Narrated Ali ibn AbuTalib ; Anas ibn Malik: </p>
          <p> Muhammad reported from his father, Umar, on the authority of his grandfather, Ali ibn AbuTalib: When Ali travelled, he continued to travel till it became nearly dark. He then alighted and offered the sunset prayer. Then he would call for his dinner and eat it. Then he prayed the night prayer and then moved off. </p>
          <p> He would say: This is how the Apostle of Allah (peace_be_upon_him) used to do. </p>
          <p> Usamah ibn Zayd reported from Hafs ibn Ubaydullah, the son of Anas ibn Malik: Anas would combine them (the evening and night prayer) when the twilight disappeared. </p>
          <p> He said: The Prophet (peace_be_upon_him) used to do so. Az-Zuhri also reported similarly on the authority of Anas from the Prophet (peace_be_upon_him). </p>
        </blockquote>
        <hr>
        <em> <a name="004.1231"> Book 4, Number 1231: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Apostle of Allah (peace_be_upon_him) stayed at Tabuk twenty days; he shortened the prayer (during his stay). </p>
        </blockquote>
        <hr>
        <em> <a name="004.1232"> Book 4, Number 1232: </a> </em>
        <blockquote>
          <p> Narrated AbuAyyash az-Zuraqi: </p>
          <p> We accompanied the Apostle of Allah (peace_be_upon_him) at Usfan, and Khalid ibn al-Walid was the chief of unbelievers. We offered the noon prayer. </p>
          <p> Thereupon, the unbelievers said: We suffered from negligence; we became careless. We should have attacked them while they were praying. Thereupon the verse was revealed, relating to the shortening of the prayer (in time of danger) between the noon and afternoon (prayer). </p>
          <p> When the time of the afternoon prayer came, the Apostle of Allah (peace_be_upon_him) stood facing the qiblah, and the unbelievers were standing in front of him. The people stood in a row behind the Apostle of Allah (peace_be_upon_him) and there was another row behind this row. The Apostle of Allah (peace_be_upon_him) bowed and all of them bowed. He then prostrated and also the row near him prostrated. The other people in the second row remained standing and stood guard over them. When they performed two prostrations and stood up, those who were behind them prostrated. The people in the front row near him then stepped backward taking the place of the people in the second row and the second row took the place of the first row. </p>
          <p> The Apostle of Allah (peace_be_upon_him) then bowed and all of them bowed together. Then he and the row near him prostrated themselves. The other people in the second row remained standing and stood guard over them. When the Apostle of Allah (peace_be_upon_him) and the row near him (i.e. the front row) were seated, the people in the second row behind them prostrated themselves. Then all of them were seated. (He (the Prophet) then uttered the salutation upon all of them. He prayed in his manner at Usfan as well as at the territory of Banu Sulaym. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1236"> Book 4, Number 1236: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> Urwah ibn az-Zubayr reported that Marwan ibn al-Hakam asked AbuHurayrah: Did you pray in time of danger with the Apostle of Allah (peace_be_upon_him)? </p>
          <p> AbuHurayrah replied: Yes.  Marwan then asked: When?  AbuHurayrah said: On the occasion of the Battle of Najd. The Apostle of Allah (peace_be_upon_him) stood up to offer the afternoon prayer. One section stood with him (to pray) and the other was standing before the enemy, and their backs were towards the qiblah. The Apostle of Allah (peace_be_upon_him) uttered the takbir and all of them too uttered the takbir, i.e. those who were with him and those who were facing the enemy. Then the Apostle of Allah (peace_be_upon_him) offered one rak'ah and the section that was with him also prayed one rak'ah. He then prostrated himself and those who were with him also prostrated, while the other section was standing before the enemy. </p>
          <p> The Apostle of Allah (peace_be_upon_him) then stood up and the section with him also stood up. They went and faced the enemy and the section that was previously facing the enemy stepped forward. They bowed and prostrated while the Apostle of Allah (peace_be_upon_him) was standing in the same position. Then they stood up and the Apostle of Allah (may peace be upon) prayed another rak'ah and all of them bowed and prostrated along with him. After that the section that was standing before the enemy came forward and they bowed and prostrated, while the Apostle of Allah (peace_be_upon_him) remained seated and also those who were with him. The salutation then followed. The Apostle of Allah (peace_be_upon_him) uttered the salutation and all of them uttered it together. The Apostle of Allah (peace_be_upon_him) prayed two rak'ahs and each of the two sections prayed one rak'ah with him (and the other by themselves). </p>
        </blockquote>
        <hr>
        <em> <a name="004.1237"> Book 4, Number 1237: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> We went out with the Apostle of Allah (peace_be_upon_him) to Najd. When we reached Dhat ar-Riqa at Nakhl (or in a valley with palm trees) he met a group of the tribe of Ghatafan. The narrator then reported the tradition to the same effect, but his version is other than that of Haywah. He added to the words "when he bowed along with those who were with him and prostrated"  the words "when they stood up, they retraced their footsteps to the rows of their companions". He did not mention the words "their back was towards the qiblah". </p>
        </blockquote>
        <hr>
        <em> <a name="004.1237A"> Book 4, Number 1237A: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Apostle of Allah (peace_be_upon_him) uttered the takbir and the section that was in the same row with him also uttered the takbir. He then bowed and they also bowed, and he prostrated and they also prostrated. Then he raised his head and they also raised (their heads). The Apostle of Allah (peace_be_upon_him) then remained seated. They prostrated alone and stood up and retraced their footsteps and stood behind them. </p>
          <p> Then the other section came; they stood up and uttered the takbir and bowed by themselves. The Apostle of Allah (peace_be_upon_him) prostrated himself and they also prostrated with him. Then the Apostle of Allah (peace_be_upon_him) stood up and they performed the second prostration by themselves. Then both the sections stood up and prayed with the Apostle of Allah (peace_be_upon_him). He bowed and they also bowed, and then he prostrated himself and they also prostrated themselves. Then he returned and performed the second prostration and they also prostrated with him as quickly as possible, showing no slackness in quick prostration. The Apostle of Allah (peace_be_upon_him) then uttered the salutation. After that the Apostle of Allah (peace_be_upon_him) stood up. Thus everyone participated in the entire prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1239"> Book 4, Number 1239: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Mas'ud: </p>
          <p> The Apostle of Allah (peace_be_upon_him) led us in prayer in the time of danger. They (the people) stood in two rows. One row was behind the Apostle of Allah (peace_be_upon_him) and the other faced the enemy. The Apostle of Allah (peace_be_upon_him) led them in one rak'ah,and then the other section came and took their place; they went and faced the enemy. The Prophet (peace_be_upon_him) led them in one rak'ah and uttered the salutation. They stood up and prayed the second rak'ah by themselves and uttered the salutation and went away; they took the place of the other section facing the enemy.  They came back and took their place. They prayed one rak'ah by themselves and then uttered the salutation. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1241"> Book 4, Number 1241: </a> </em>
        <blockquote>
          <p> Narrated Hudhayfah: </p>
          <p> Tha'labah ibn Zahdam said: We accompanied Sa'd ibn al-'As at Tabaristan. He stood and said: Which of you prayed along with the Apostle of Allah (peace_be_upon_him) in time of danger? Hudhayfah said: I then he led one section in one rak'ah and the other section in one rak'ah. They did not pray the second rak'ah by themselves. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1243"> Book 4, Number 1243: </a> </em>
        <blockquote>
          <p> Narrated AbuBakrah: </p>
          <p> The Prophet (peace_be_upon_him) offered the noon prayer in time of danger. Some of the people formed a row behind him and others arrayed themselves against the enemy. He led them in two rak'ahs and then he uttered the salutation. Then those who were with him went away and took the position of their companions before the enemy. Then they came and prayed behind him. He led them in two rak'ahs and uttered the salutation. Thus the Apostle of Allah (peace_be_upon_him) offered four rak'ahs and his companions offered two rak'ahs. </p>
          <p> Al-Hasan used to give legal verdict on the authority of this tradition. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1252"> Book 4, Number 1252: </a> </em>
        <blockquote>
          <p> Narrated Bilal: </p>
          <p> Ziyadah al-Kindi reported on the authority of Bilal that he (Bilal) came to the Apostle of Allah (peace_be_upon_him) to inform him about the dawn prayer. Aisha kept Bilal engaged in a matter which she asked him till the day was bright and it became fairly light. Bilal then stood up and called him to prayer and called him repeatedly. The Apostle of Allah (peace_be_upon_him) did not yet come out. When he came out, he led the people in prayer and he (Bilal) informed him that Aisha had kept him engaged in a matter which she asked him till it became fairly light; hence he became late in reaching him (in time). He (Bilal) said: Apostle of Allah, the dawn became fairly bright. He said: If the dawn became brighter than it is now, I would pray them (the two rak'ahs of the sunnah prayer), offer them well and in a more beautiful manner. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1253"> Book 4, Number 1253: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: Do not omit them (the two rak'ahs before the dawn prayer) even if you are driven away by the horses. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1256"> Book 4, Number 1256: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: If any of you prays two rak'ahs before the dawn prayer, he should lie at his right side. </p>
          <p> Marwan ibn al-Hakam said to him: Is it not enough that one of us walks to the mosque until he lies at his right side? According to the version of Ubaydullah, he (AbuHurayrah) replied: No. </p>
          <p> This statement (of AbuHurayrah) reached Ibn Umar. He said: AbuHurayrah exceed limits on himself. He was asked: Do you look askance at what he says? He replied: No, but he dared and we showed cowardice. This (criticism of Ibn Umar) reached AbuHurayrah. He said: What is my sin if I remembered and they forgot? </p>
        </blockquote>
        <hr>
        <em> <a name="004.1258"> Book 4, Number 1258: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> When the Prophet (peace_be_upon_him) prayed the two rak'ahs of the dawn prayer, he would lie down if I was asleep; in case I was awake, he would talk to me. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1259"> Book 4, Number 1259: </a> </em>
        <blockquote>
          <p> Narrated AbuBakrah: </p>
          <p> I came out with the Prophet (peace_be_upon_him) to offer the dawn prayer. When he passed by a sleeping man he called him for prayer or moved him with his foot. The narrator Ziyad said: This tradition has been reported to us by AbulFadl. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1262"> Book 4, Number 1262: </a> </em>
        <blockquote>
          <p> Narrated Qays ibn Amr: </p>
          <p> The Apostle of Allah (peace_be_upon_him) saw a person praying after the congregational prayer at dawn was over. The Apostle of Allah (peace_be_upon_him) said: There are two rak'ahs of the dawn prayer (i.e. the prescribed rak'ahs). The man replied: I did not pray the two rak'ahs before the dawn prayer. Hence I offered them now. The Apostle of Allah (peace_be_upon_him) kept silent. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1264"> Book 4, Number 1264: </a> </em>
        <blockquote>
          <p> Narrated Umm Habibah: </p>
          <p> The Prophet (peace_be_upon_him) said: If anyone keeps on praying regularly four rak'ahs before and four after the noon prayer, he will not enter the Hell-fire. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1265"> Book 4, Number 1265: </a> </em>
        <blockquote>
          <p> Narrated AbuAyyub: </p>
          <p> The Prophet (peace_be_upon_him) said: The gates of heaven are opened for four rak'ahs containing no taslim (salutation) before the noon prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1266"> Book 4, Number 1266: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> The Prophet (peace_be_upon_him) said: May Allah show mercy to a man who prays four rak'ahs before the afternoon prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1269"> Book 4, Number 1269: </a> </em>
        <blockquote>
          <p> Narrated Ali ibn AbuTalib: </p>
          <p> The Prophet (peace_be_upon_him) prohibited to offer prayer after the afternoon prayer except at the time when the sun is high up in the sky. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1270"> Book 4, Number 1270: </a> </em>
        <blockquote>
          <p> Narrated Ali ibn AbuTalib: </p>
          <p> The Apostle of Allah (peace_be_upon_him) would offer two rak'ahs after every obligatory prayer except the dawn and the afternoon prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1271"> Book 4, Number 1271: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> Some reliable people testified before me, and among them was Umar ibn al-Khattab, and most reliable in my eyes was Umar: The Prophet of Allah (peace_be_upon_him) said: There is no prayer after the dawn prayer until the sun rises; and there is no prayer after the afternoon prayer until the sun sets. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1272"> Book 4, Number 1272: </a> </em>
        <blockquote>
          <p> Narrated Amr ibn Anbasah as-Sulami: </p>
          <p> I asked: Apostle of Allah, in which part of night the supplication is more likely to be accepted? </p>
          <p> He replied: In the last part: Pray as much as you like, for the prayer is attended by the angels and it is recorded till you offer the dawn prayer; then stop praying when the sun is rising till it has reached the height of one or two lances, for it rises between the two horns of the Devil, and the infidels offer prayer for it (at that time). Then pray as much as you like, because the prayer is witnessed and recorded till the shadow of a lance be- comes equal to it. Then cease prayer, for at that time the Hell-fire is heated up and doors of Hell are opened. </p>
          <p> When the sun declines, pray as much as you like, for the prayer is witnessed till you pray the afternoon prayer; then cease prayer till the sun sets, for it sets between the horns of the Devil, and (at that time) the infidels offer prayer for it. He narrated a lengthy tradition. </p>
          <p> Abbas said: AbuSalam narrated this tradition in a similar manner from AbuUmamah. If I have made a mistake unintentionally, I beg pardon of Allah and repent to Him. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1273"> Book 4, Number 1273: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> Yasar, the client of Ibn Umar, said: Ibn Umar saw  me praying after the break of dawn. He said: O Yasar, the Apostle of Allah (peace_be_upon_him) came to us while we were offering this prayer. He (the Prophet) said: Those who are present should inform those who are absent: Do not offer any prayer after (the break of) dawn except two rak'ahs. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1275"> Book 4, Number 1275: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Dhakwan, the client of Aisha, reported on the authority of Aisha: The Apostle of Allah (peace_be_upon_him) used to pray after the afternoon prayer but prohibited others from it; and he would fast continuously but forbid others to do so. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1282"> Book 4, Number 1282: </a> </em>
        <blockquote>
          <p> Narrated Mu'adh ibn Anas al-Juhani: </p>
          <p> The Prophet (peace_be_upon_him) said: If anyone sits in his place of prayer when he finishes the dawn prayer till he prays the two rak'ahs of the forenoon, saying nothing but what is good, his sins will be forgiven even if they are more than the foam of the sea. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1283"> Book 4, Number 1283: </a> </em>
        <blockquote>
          <p> Narrated AbuUmamah: </p>
          <p> The Prophet (peace_be_upon_him) said: Prayer followed by a prayer with no idle talk between the two is recorded in Illiyyun. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1284"> Book 4, Number 1284: </a> </em>
        <blockquote>
          <p> Narrated Nu'aym ibn Hammar: </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) say: Allah, the Exalted, says: Son of Adam, do not be helpless in performing four rak'ahs for Me at the beginning of the day: I will supply what you need till the end of it. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1285"> Book 4, Number 1285: </a> </em>
        <blockquote>
          <p> Narrated Umm Hani ibn AbuTalib: </p>
          <p> The Apostle of Allah (peace_be_upon_him) prayed on the day of the Conquest (of Mecca) eight rak'ahs saluting after every two rak'ahs. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1290"> Book 4, Number 1290: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> The Prophet (peace_be_upon_him) said: Prayer by night and day should consist of pairs of rak'ahs. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1291"> Book 4, Number 1291: </a> </em>
        <blockquote>
          <p> Narrated Muttalib: </p>
          <p> The Prophet (peace_be_upon_him) said: Prayer is to be offered in two rak'ahs; and you should recite the tashahhud at the end of two rak'ahs, and express your distress and humility and raise your hands and say praying: O Allah, O Allah. He who does not do so does not offer a perfect prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1292"> Book 4, Number 1292: </a> </em>
        <blockquote>
          <p> Narrated Abdullah Ibn Abbas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said to al-Abbas ibn AbdulMuttalib: Abbas, my uncle, shall I not give you, shall I not present to you, shall I not donate to you, shall I not produce for you ten things? If you act upon them, Allah will forgive you your sins, first and last, old and new, involuntary and voluntary, small and great, secret and open. </p>
          <p> These are the ten things: you should pray four rak'ahs, reciting in each one Fatihat al-Kitab and a surah. When you finish the recitation of the first rak'ah you should say fifteen times while standing: "Glory be to Allah", "Praise be to Allah", "There is no god but Allah", "Allah is most great". Then you should bow and say it ten times while bowing. Then you should raise your head after bowing and say it ten times. Then you should kneel down in prostration and say it ten times while prostrating yourself. Then you should raise your head after prostration and say it ten times. Then you should prostrate yourself and say it ten times. Then you should raise your head after prostrating and say it ten times in every rak'ah. You should do that in four rak'ahs. </p>
          <p> If you can observe it once daily, do so; if not, then once weekly; if not, then once a month; if not, then once a year; if not, then once in your lifetime. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1293"> Book 4, Number 1293: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Amr: </p>
          <p> AbulJawza' said: A man who attended the company of the Prophet (peace_be_upon_him) narrated to me (it is thought that he was Abdullah ibn Amr): The Prophet (peace_be_upon_him) said to me: Come to me tomorrow; I shall give you something, I shall give you something, I shall reward you something, I shall donate something to you. I thought that he would give me some present. </p>
          <p> He said (to me when I came to him): When the day declines, stand up and pray four rak'ahs. He then narrated something similar. </p>
          <p> This version adds: Do not stand until you glorify Allah ten times, and praise Him ten times, and exalt Him ten times, and say, "There is no god but Allah" ten times. Then you should do that in four rak'ahs. If you are the greatest sinner on earth, you will be forgiven (by Allah) on account of this (prayer). </p>
          <p> I asked: If I cannot pray this the appointed hour, (what should I do)? He replied: Pray that by night or by day (at any time). </p>
        </blockquote>
        <hr>
        <em> <a name="004.1295"> Book 4, Number 1295: </a> </em>
        <blockquote>
          <p> Narrated Ka'b ibn Ujrah: </p>
          <p> The Prophet (peace_be_upon_him) came to the mosque of Banu AbdulAshhal. He prayed the sunset prayer there. When they finished the prayer, he saw them praying the supererogatory prayer after it. He said: This is the prayer to be offered in the houses. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1296"> Book 4, Number 1296: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Abbas: </p>
          <p> The Apostle of Allah (peace_be_upon_him) used to prolong the recitation of the Qur'an in the two rak'ahs after the sunset prayer until the people praying in the mosque dispersed. </p>
        </blockquote>
        <hr>
        <em> <a name="004.1298"> Book 4, Number 1298: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Shurayh ibn Hani said: I asked Aisha about the prayer of the Apostle of Allah (peace_be_upon_him).  She said: The Apostle of Allah (peace_be_upon_him) never offered the night prayer and thereafter came to me but he offered four or six rak'ahs of prayer. One night the rain fell, so we spread a piece of leather (for his prayer), and now I see as if there is a hole in it from which the water is flowing. I never saw him protecting his clothes from the earth (as he did on that occasion). </p>
        </blockquote>
