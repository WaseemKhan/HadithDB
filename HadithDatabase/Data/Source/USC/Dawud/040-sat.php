      <em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 40: </a> </em>
      <h1> <em> Model Behavior of the Prophet (Kitab Al-Sunnah) </em> </h1>
      <hr>
      <em> <a name="040.4579"> Book 40, Number 4579: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The Jews were split up into seventy-one or seventy-two sects; and the Christians were split up into seventy one or seventy-two sects; and my community will be split up into seventy-three sects. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4582"> Book 40, Number 4582: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The Prophet (peace_be_upon_him) said: The best of the actions is to love for the sake of Allah and to hate for the sake of Allah. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4586"> Book 40, Number 4586: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Controverting about the Qur'an is disbelief. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4587"> Book 40, Number 4587: </a> </em>
      <blockquote>
        <p> Narrated Al-Miqdam ibn Ma'dikarib: </p>
        <p> The Prophet (peace_be_upon_him) said: Beware! I have been given the Qur'an and something like it, yet the time is coming when a man replete on his couch will say: Keep to the Qur'an; what you find in it to be permissible treat as permissible, and what you find in it to be prohibited treat as prohibited. Beware! The domestic ass, beasts of prey with fangs, a find belonging to confederate, unless its owner does not want it, are not permissible to you If anyone comes to some people, they must entertain him, but if they do not, he has a right to mulct them to an amount equivalent to his entertainment. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4588"> Book 40, Number 4588: </a> </em>
      <blockquote>
        <p> Narrated AbuRafi': </p>
        <p> The Prophet (peace_be_upon_him) said: Let me not find one of you reclining on his couch when he hears something regarding me which I have commanded or forbidden and saying: We do not know. What we found in Allah's Book we have followed. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4590"> Book 40, Number 4590: </a> </em>
      <blockquote>
        <p> Narrated Irbad ibn Sariyah: </p>
        <p> AbdurRahman ibn Amr as-Sulami and Hujr ibn Hujr said: We came to Irbad ibn Sariyah who was among those about whom the following verse was revealed: "Nor (is there blame) on those who come to thee to be provided with mounts, and when thou saidst: "I can find no mounts for you." </p>
        <p> We greeted him and said: We have come to see you to give healing and obtain benefit from you. </p>
        <p> Al-Irbad said: One day the Apostle of Allah (peace_be_upon_him) led us in prayer, then faced us and gave us a lengthy exhortation at which the eyes shed tears and the hearts were afraid. </p>
        <p> A man said: Apostle of Allah! It seems as if it were a farewell exhortation, so what injunction do you give us? </p>
        <p> He then said: I enjoin you to fear Allah, and to hear and obey even if it be an Abyssinian slave, for those of you who live after me will see great disagreement. You must then follow my sunnah and that of the rightly-guided caliphs. Hold to it and stick fast to it. Avoid novelties, for every novelty is an innovation, and every innovation is an error. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4615"> Book 40, Number 4615: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> A man came to the Apostle of Allah (peace_be_upon_him) and said: I saw (in my dream) a piece of cloud from which ghee and honey were dropping. I saw the people spreading their hands. Some of them took much and some a little. I also saw a rope hanging from Heaven to Earth. I saw, Apostle of Allah, that you caught hold of it and ascended by it. Then another man caught hold of it and ascended it. Then another man caught hold of it and ascended it. Then another man caught hold of it, but it broke, and then it was joined and he ascended it. </p>
        <p> AbuBakr said: May my parents be sacrificed for you, if you allow, I shall interpret it. </p>
        <p> He said: Interpret it.  He said: The piece of cloud is the cloud of Islam; the ghee and honey that were dropping from it are the Qur'an, which contains softness and sweetness. Those who received much or little of it are those who learn much or little of the Qur'an. The rope hanging from Heaven to Earth is the truth which you are following. You catch hold of it and then Allah will raise you to Him. Then another man will catch hold of it and ascend it, Then another man will catch hold of it and it will break. But it will be joined and he will ascend it. Tell me. Apostle of Allah, whether I am right or wrong. </p>
        <p> He said: You are partly right and partly wrong.  He said: I adjure you by Allah, you should tell me where I am wrong. </p>
        <p> The Prophet (peace_be_upon_him) said: Do not take an oath. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4617"> Book 40, Number 4617: </a> </em>
      <blockquote>
        <p> Narrated AbuBakrah: </p>
        <p> One day the Prophet (peace_be_upon_him) said: Which of you had dream? A man said: It is I. I saw as though a scale descended from the sky. You and AbuBakr were weighed and you were heavier; AbuBakr and Umar were weighed and AbuBakr was heavier: Umar and Uthman were weighed and Umar was heavier; than the scale was taken up. we saw signs of dislike on the face of the Apostle of Allah (peace_be_upon_him). </p>
      </blockquote>
      <hr>
      <em> <a name="040.4629"> Book 40, Number 4629: </a> </em>
      <blockquote>
        <p> Narrated Safinah: </p>
        <p> The Prophet (peace_be_upon_him) said: The Caliphate of Prophecy will last thirty years; then Allah will give the Kingdom of His Kingdom to anyone He wills. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4631"> Book 40, Number 4631: </a> </em>
      <blockquote>
        <p> Narrated Sa'id ibn Zayd ibn Amr ibn Nufayl: </p>
        <p> Abdullah ibn Zalim al-Mazini said: I heard Sa'id ibn Zayd ibn Amr ibn Nufayl say: When so and so came to Kufah, and made so and so stand to address the people, Sa'id ibn Zayd caught hold of my hand and said: Are you seeing this tyrant? I bear witness to the nine people that they will go to Paradise. If I testify to the tenth too, I shall not be sinful. </p>
        <p> I asked: Who are the nine?  He said: The Apostle of Allah (peace_be_upon_him) said when he was on Hira': Be still, Hira', for only a Prophet, or an ever-truthful, or a martyr is on you.  I asked: Who are those nine?  He said: The Apostle of Allah, AbuBakr, Umar, Uthman, Ali, Talhah, az-Zubayr, Sa'd ibn AbuWaqqas and AbdurRahman ibn Awf.  I asked: Who is the tenth? He paused a moment and said: it is I. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4632"> Book 40, Number 4632: </a> </em>
      <blockquote>
        <p> Narrated Sa'id ibn Zayd: </p>
        <p> AbdurRahman ibn al-Akhnas said that when he was in the mosque, a man mentioned Ali (may Allah be pleased with him). So Sa'id ibn Zayd got up and said: I bear witness to the Apostle of Allah (peace_be_upon_him) that I heard him say: Ten persons will go to Paradise: The Prophet (peace_be_upon_him) will go to Paradise, AbuBakr will go to Paradise, Umar will go to Paradise, Uthman will go to Paradise, Ali will go to Paradise, Talhah will go to Paradise: az-Zubayr ibn al-Awwam will go to paradise, Sa'd ibn Malik will go to Paradise, and AbdurRahman ibn Awf will go to Paradise. If I wish, I can mention the tenth. The People asked: Who is he: So he kept silence. The again asked: Who is he: He replied: He is Sa'id ibn Zayd. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4633"> Book 40, Number 4633: </a> </em>
      <blockquote>
        <p> Narrated Sa'id ibn Zayd ibn Amr ibn Nufayl: </p>
        <p> Rabah ibn al-Harith said: I was sitting with someone in the mosque of Kufah while the people of Kufah were with him. Then Sa'id ibn Zayd ibn Amr ibn Nufayl came and he welcomed him, greeted him, and seated him near his foot on the throne. Then a man of the inhabitants of Kufah, called Qays ibn Alqamah, came. He received him and began to abuse him. </p>
        <p> Sa'id asked: Whom is this man abusing?  He replied: He is abusing Ali.  He said: Don't I see that the companions of the Apostle of Allah (peace_be_upon_him) are being abused, but you neither stop it nor do anything about it? I heard the Apostle of Allah (peace_be_upon_him) say--and I need not say for him anything which he did not say, and then he would ask me tomorrow when I see him --AbuBakr will go to Paradise and Umar will go to Paradise. He then mentioned the rest of the tradition to the same effect (as in No. 4632). </p>
        <p> He then said: The company of one of their man whose face has been covered with dust by the Apostle of Allah (peace_be_upon_him) is better than the actions of one of you for a whole life time even if he is granted the life-span of Noah. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4635"> Book 40, Number 4635: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: Gabriel came and taking me by the hand showed the gate of Paradise by which my people will enter. AbuBakr then said: Apostle of Allah! I wish I had been with you so that I might have looked at it. The Apostle of Allah (peace_be_upon_him) then said: You, AbuBakr, will be the first of my people to enter Paradise. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4639"> Book 40, Number 4639: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Al-Aqra', the mu'adhdhin (announcer) of Umar ibn al-Khattab said: Umar sent me to a bishop and I called him. </p>
        <p> Umar said to him: Do you find me in the Book?  He said: Yes.  He asked: How do you find me?  He said: I find you (like a) castle.  Then he raised a whip to him, saying: What do you mean by castle?  He replied: An iron castle and severely trustworthy.  He asked: How do you find the one who will come after me?  He said: I find him a pious caliph, except that he will prefer his relatives.  Umar said: May Allah have mercy on Uthman: He said it three times. He then asked: How do you find the one who will come after him? </p>
        <p> He replied: I find him like rusty iron.  Umar then put his hand on his head, and said: O filthy! O filthy!  He said: Commander of the Faithful! He is a pious caliph, but when he is made caliph, the sword will be unsheathed and blood will be shed. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4643"> Book 40, Number 4643: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Zam'ah: </p>
        <p> When the illness of the Apostle of Allah (peace_be_upon_him) became serious while I was with him among a group of people, Bilal called him for prayer. He said: Ask someone to lead the people in prayer. So Abdullah ibn Zam'ah went out and found that Umar was present among the people and AbuBakr was not there. I said: Umar, get up and lead the people in prayer. So he came forward and uttered "Allah is Most Great". When the Apostle of Allah (peace_be_upon_him) heard his voice, as Umar had a loud voice, he said: Where is AbuBakr? Allah does not allow that, and the Muslims too; Allah does not allow that, and the Muslims too. So he sent for AbuBakr. He came after Umar had led the people in that prayer. He then led the people in prayer. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4645"> Book 40, Number 4645: </a> </em>
      <blockquote>
        <p> Narrated AbuBakrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said to al-Hasan ibn Ali. This son of mine is a Sayyid (chief), and I hope Allah may reconcile two parties of my community by means of him. Hammad's version has: And perhaps Allah may reconcile two large parties of Muslims by means of him. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4646"> Book 40, Number 4646: </a> </em>
      <blockquote>
        <p> Narrated Hudhayfah: </p>
        <p> There is no one who will be overtaken by trial regarding whom I do not fear except Muhammad ibn Maslamah, for I heard the Apostle of Allah (peace_be_upon_him) say: Trial will not harm you. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4664"> Book 40, Number 4664: </a> </em>
      <blockquote>
        <p> Narrated AbuUmamah: </p>
        <p> The Prophet (peace_be_upon_him) said: If anyone loves for Allah's sake, hates for Allah's sake, gives for Allah's sake and withholds for Allah's sake, he will have perfect faith. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4665"> Book 40, Number 4665: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: The most perfect believer in respect of faith is he who is best of them in manners. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4673"> Book 40, Number 4673: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Prophet (peace_be_upon_him) said: When a man commits fornication, faith departs from him and there is something like a canvas roof over his head; and when he quits that action, faith returns to him. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4674"> Book 40, Number 4674: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Umar: </p>
        <p> The Prophet (peace_be_upon_him) said: The Qadariyyah are the Magians of this community. If they are ill, do not pay a sick visit to them, and if they die, do not attend their funerals. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4681"> Book 40, Number 4681: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr ; AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) used to sit among his Companions. A stranger would come, but did recognise him (the Prophet) until he asked (about him). So we asked the Apostle of Allah (peace_be_upon_him) to make a place where he might take his seat so that when a stranger came, he might recognise him. So we built a terrace of soil on which he would take his seat, and we would sit beside him. He then mentioned something similar to this Hadith saying: A man came, and he described his appearance. He saluted from the side of the assembly, saying: Peace be upon you, Muhammad. The Prophet (peace_be_upon_him) then responded to him. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4686"> Book 40, Number 4686: </a> </em>
      <blockquote>
        <p> Narrated Umar ibn al-Khattab: </p>
        <p> Muslim ibn Yasar al-Juhani said: When Umar ibn al-Khattab was asked about the verse "When your Lord took their offspring from the backs of the children of Adam" - al-Qa'nabi recited the verse--he said: I heard the Apostle of Allah (peace_be_upon_him) say when he was questioned about it: Allah created Adam, then passed His right hand over his back, and brought forth from it his offspring, saying: I have these for Paradise and these will do the deeds of those who go to Paradise. He then passed His hand over his back and brought forth from it his offspring, saying: I have created these for Hell, and they will do the deeds of those who go to Hell. </p>
        <p> A man asked: What is the good of doing anything, Apostle of Allah?  The Apostle of Allah (peace_be_upon_him) said: When Allah creates a servant for Paradise, He employs him in doing the deeds of those who will go to Paradise, so that his final action before death is one of the deeds of those who go to Paradise, for which He will bring him into Paradise. But when He creates a servant for Hell, He employs him in doing the deeds of those who will go to Hell, so that his final action before death is one of the deeds of those who go to Hell, for which He will bring him into Hell. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4704"> Book 40, Number 4704: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> I heard the Apostle of Allah (peace_be_upon_him) say: He then mentioned a tradition like it. This version adds: When they propound that, say: "Say Allah is one. Allah is He to Whom men repair. He has not begotten and He has not been begotten, and no one is equal to Him." Then one should spit three times on his left side and seek refuge in Allah from Satan. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4705"> Book 40, Number 4705: </a> </em>
      <blockquote>
        <p> Narrated Al-Abbas ibn AbdulMuttalib: </p>
        <p> I was sitting in al-Batha with a company among whom the Apostle of Allah (peace_be_upon_him) was sitting, when a cloud passed above them. </p>
        <p> The Apostle of Allah (peace_be_upon_him) looked at it and said: What do you call this?  They said: Sahab. </p>
        <p> He said: And muzn?  They said: And muzn. He said: And anan?  They said: And anan.  AbuDawud said: I am not quite confident about the word anan.  He asked:  Do you know the distance between Heaven and Earth?  They replied: We do not know.  He then said: The distance between them is seventy-one, seventy-two, or seventy-three years. The heaven which is above it is at a similar distance (going on till he counted seven heavens). Above the seventh heaven there is a sea, the distance between whose surface and bottom is like that between one heaven and the next. Above that there are eight mountain goats the distance between whose hoofs and haunches is like the distance between one heaven and the next. Then Allah, the Blessed and the Exalted, is above that. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4713"> Book 40, Number 4713: </a> </em>
      <blockquote>
        <p> Narrated AbuRazin al-Uqayli: </p>
        <p> I asked: Apostle of Allah! will each one of us see his Lord? Ibn Mu'adh's version has: "being alone with Him, on the Day of Resurrection? And what sign is there is His creation?" He replied: AbuRazin! does each one of you not see the moon? Ibn Mu'adh's version has: "on the night when it is full, being alone with it?"  Then the agreed version goes: I said: Yes. He said: Allah is more great. Ibn Mu'adh's version has: It is only part of Allah's creation, but Allah is more glorious and greater. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4716"> Book 40, Number 4716: </a> </em>
      <blockquote>
        <p> Narrated Jabir ibn Abdullah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) presented himself to the people at Arafat, saying: Is there any man who takes me to his people? The Quraysh have prevented me from preaching the word of my Lord. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4721"> Book 40, Number 4721: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: My intercession will be for those of my people who have committed major sins. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4724"> Book 40, Number 4724: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: The trumpet (sur) which will be blown. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4726"> Book 40, Number 4726: </a> </em>
      <blockquote>
        <p> Narrated AbuHurayrah: </p>
        <p> The Apostle of Allah (peace_be_upon_him) said: When Allah created Paradise, He said to Gabriel: Go and look at it.  He went and looked at it, then came and said: O my Lord! By Thy might, no one who hears of it will fail to enter it. </p>
        <p> He then surrounded it with disagreeable things, and said: Go and look at it, Gabriel.  He went and looked at it, then came and said: O my Lord! By Thy might, I am afraid that no one will enter it. </p>
        <p> When Allah created Hell, He said: Go and look at it, Gabriel.  He went and looked at it, then came and said: O my Lord! By Thy might, no one who hears of it will enter it. </p>
        <p> He then surrounded it with desirable things and said: Go and look at it, Gabriel.  He went, looked at it, then came and said: O my Lord! By Thy might and power, I am afraid that no one will remain who does not enter it. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4728"> Book 40, Number 4728: </a> </em>
      <blockquote>
        <p> Narrated Zayd ibn Arqam: </p>
        <p> We were with the Apostle of Allah (peace_be_upon_him). He said when we arrived at a halting place: You are not a hundred thousandth part of those who will come down to me at the pond. I (the narrator AbuHamzah) asked: What was your number that day? He replied: Seven or eight hundred. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4730"> Book 40, Number 4730: </a> </em>
      <blockquote>
        <p> Narrated Anas ibn Malik: </p>
        <p> When the Prophet of Allah (peace_be_upon_him) was lifted to the heavens (for travelling) in Paradise, or as he said, a river whose banks were of transparent or hollowed pearls was presented to him. The angel who was with him struck it with his hand and took out musk. Muhammad (peace_be_upon_him) then asked the angel who was with him: What is this? He replied: It is al-Kawthar which Allah has given you. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4731"> Book 40, Number 4731: </a> </em>
      <blockquote>
        <p> Narrated AbuBarzah: </p>
        <p> AbdusSalam ibn AbuHazim AbuTalut said: I saw AbuBarzah who came to visit Ubaydullah ibn Ziyad. Then a man named Muslim who was there in the company mentioned it to me. </p>
        <p> When Ubaydullah saw him, he said: This Muhammad of yours is a dwarf and fat. The old man (i.e. AbuBarzah) understood it. </p>
        <p> So he said: I did not think that I should remain among people who would make me feel ashamed of the company of Muhammad (peace_be_upon_him). </p>
        <p> Thereupon Ubaydullah said: The company of Muhammad (peace_be_upon_him) is a honour for you, not a disgrace. He added: I called for you to ask about the reservoir. Did you hear the Apostle of Allah (peace_be_upon_him) mentioning anything about it?  AbuBarzah said: Yes, not once, twice, thrice, four times or five times. If anyone believes it, may Allah not supply him with water from it. He then went away angrily. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4735"> Book 40, Number 4735: </a> </em>
      <blockquote>
        <p> Narrated Al-Bara' ibn Azib: </p>
        <p> We went out with the Apostle of Allah (peace_be_upon_him) accompanying the bier of a man of the Ansar. When we reached his grave, it was not yet dug. So the Apostle of Allah (peace_be_upon_him) sat down and we also sat down around him as if birds were over our heads. He had in his hand a stick with which he was scratching the ground. </p>
        <p> He then raised his head and said: Seek refuge with Allah from the punishment in the grave. He said it twice or thrice. </p>
        <p> The version of Jabir adds here: He hears the beat of their sandals when they go back, and at that moment he is asked: O so and so! Who is your Lord, what is your religion, and who is your Prophet? </p>
        <p> Hannad's version says: Two angels will come to him, make him sit up and ask him: Who is your Lord? </p>
        <p> He will reply: My Lord is Allah.  They will ask him: What is your religion?  He will reply: My religion is Islam.  They will ask him: What is your opinion about the man who was sent on a mission among you?  He will reply: He is the Apostle of Allah (peace_be_upon_him).  They will ask: Who made you aware of this?  He will reply: I read Allah's Book, believed in it, and considered it true; which is verified by Allah's words: "Allah's Book, believed in it, and considered it true, which is verified by Allah's words: "Allah establishes those who believe with the word that stands firm in this world and the next." </p>
        <p> The agreed version reads: Then a crier will call from Heaven: My servant has spoken the truth, so spread a bed for him from Paradise, clothe him from Paradise, and open a door for him into Paradise. So some of its air and perfume will come to him, and a space will be made for him as far as the eye can see. </p>
        <p> He also mentioned the death of the infidel, saying: His spirit will be restored to his body, two angels will come to him, make him sit up and ask him: Who is your Lord? </p>
        <p> He will reply: Alas, alas! I do not know.  They will ask him: What is your religion?  He will reply: Alas, alas! I do not know.  They will ask: Who was the man who was sent on a mission among you?  He will reply: Alas, alas! I do not know. Then a crier will call from Heaven: He has lied, so spread a bed for him from Hell, clothe him from Hell, and open for him a door into Hell. Then some of its heat and pestilential wind will come to him, and his grave will be compressed, so that his ribs will be crushed together. </p>
        <p> Jabir's version adds: One who is blind and dumb will then be placed in charge of him, having a sledge-hammer such that if a mountain were struck with it, it would become dust. He will give him a blow with it which will be heard by everything between the east and the west except by men and jinn, and he will become dust. Then his spirit will be restored to him. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4738"> Book 40, Number 4738: </a> </em>
      <blockquote>
        <p> Narrated AbuUbaydah ibn al-Jarrah: </p>
        <p> I heard the Prophet (peace_be_upon_him) say: There has been no Prophet after Noah who has not warned his people about the antichrist (Dajjal), and I warn you of him. The Apostle of Allah (peace_be_upon_him) described him to us, saying: Perhaps some who have seen me and heard my words will live till his time. The people asked: Apostle of Allah! what will be the condition of our hearts on that day? Like what we are today? He replied: Or better. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4740"> Book 40, Number 4740: </a> </em>
      <blockquote>
        <p> Narrated AbuDharr: </p>
        <p> The Prophet (peace_be_upon_him) said: He who separates from the community within a span takes off the noose of Islam from his neck. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4747"> Book 40, Number 4747: </a> </em>
      <blockquote>
        <p> Narrated AbuSa'id al-Khudri ; Anas ibn Malik: </p>
        <p> The Prophet (peace_be_upon_him) said: Soon there will appear disagreement and dissension in my people; there will be people who will be good in speech and bad in work. They recite the Qur'an, but it does not pass their collar-bones. They will swerve from the religion as an animal goes through the animal shot at. They will not return to it till the arrow comes back to its notch. They are worst of the people and animals. Happy is the one who kills them and they kill him. They call to the book of Allah, but they have nothing to do with it. He who fights against them will be nearer to Allah than them (the rest of the people). The people asked: What is their sign? He replied: They shave the head. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4753"> Book 40, Number 4753: </a> </em>
      <blockquote>
        <p> Narrated Abdullah ibn Amr ibn al-'As: </p>
        <p> The Prophet (peace_be_upon_him) said: If the property of anyone is designed to be taken away without any right and he fights and is killed, he is a martyr. </p>
      </blockquote>
      <hr>
      <em> <a name="040.4754"> Book 40, Number 4754: </a> </em>
      <blockquote>
        <p> Narrated Sa'id ibn Zayd: </p>
        <p> The Prophet (peace_be_upon_him) said: He who is killed while protecting his property is a martyr, and he who is killed while defending his family, or his blood, or his religion is a martyr. </p>
      </blockquote>
