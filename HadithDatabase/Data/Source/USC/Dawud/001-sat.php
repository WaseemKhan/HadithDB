      <div class="content"><em> <a href="satintro.php"> <strong>Partial</strong> Translation of Sunan Abu-Dawud, Book 1: </a> </em>
        <h1> <em> Purification (Kitab Al-Taharah) </em> </h1>
        <hr>
        <em> <a name="001.0001"> Book 1, Number 0001: </a> </em>
        <blockquote>
          <p> Narrated Mughirah ibn Shu'bah: </p>
          <p> When the Prophet (peace_be_upon_him) went (outside) to relieve himself, he went to a far-off place. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0002"> Book 1, Number 0002: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> When the Prophet (peace_be_upon_him) felt the need of relieving himself, he went far off where no one could see him. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0003"> Book 1, Number 0003: </a> </em>
        <blockquote>
          <p> Narrated AbuMusa: </p>
          <p> AbutTayyah reported on the authority of a shaykh (an old man): When Abdullah ibn Abbas came to Basrah, people narrated to him traditions from AbuMusa.  Therefore Ibn Abbas wrote to him asking him about certain things.  In reply AbuMusa wrote to him saying: One day I was in the company of the Apostle of Allah (peace_be_upon_him).  He wanted to urinate.  Then he came to a soft ground at the foot of a wall and urinated.  He (the Prophet) then said: If any of you wants to urinate, he should look for a place (like this) for his urination. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0006"> Book 1, Number 0006: </a> </em>
        <blockquote>
          <p> Narrated Zayd ibn Arqam: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: These privies are frequented by the jinns and devils.  So when anyone amongst you goes there, he should say: "I seek refuge in Allah from male and female devils." </p>
        </blockquote>
        <hr>
        <em> <a name="001.0007"> Book 1, Number 0007: </a> </em>
        <blockquote>
          <p> Narrated Salman al-Farsi: </p>
          <p> It was said to Salman: Your Prophet teaches you everything, even about excrement. He replied: Yes. He has forbidden us to face the qiblah at the time of easing or urinating, and cleansing with right hand, and cleansing with less than three stones, or cleansing with dung or bone. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0010"> Book 1, Number 0010: </a> </em>
        <blockquote>
          <p> Narrated Ma'qil ibn AbuMa'qil al-Asadi: </p>
          <p> The Apostle of Allah (peace_be_upon_him) has forbidden us to face the two qiblahs at the time of urination or excretion. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0011"> Book 1, Number 0011: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> Marwan al-Asfar said: I saw Ibn Umar make his camel kneel down facing the qiblah, then he sat down urinating in its direction. So I said: AbuAbdurRahman, has this not been forbidden? He replied: Why not, that was forbidden only in open country; but when there is something between you and the qiblah that conceals you , then there is no harm. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0013"> Book 1, Number 0013: </a> </em>
        <blockquote>
          <p> Narrated Jabir ibn Abdullah: </p>
          <p> The Prophet of Allah (peace_be_upon_him) forbade us to face the qiblah at the time of making water.  Then I saw him facing it (qiblah) urinating or easing himself one year before his death. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0014"> Book 1, Number 0014: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> When the Prophet (peace_be_upon_him) wanted to relieve himself, he would not raise his garment, until he lowered himself near the ground. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0015"> Book 1, Number 0015: </a> </em>
        <blockquote>
          <p> Narrated AbuSa'id al-Khudri: </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) say: When two persons go together for relieving themselves uncovering their private parts and talking together, Allah, the Great and Majestic, becomes wrathful at this (action). </p>
        </blockquote>
        <hr>
        <em> <a name="001.0017"> Book 1, Number 0017: </a> </em>
        <blockquote>
          <p> Narrated Muhajir ibn Qunfudh: </p>
          <p> Muhajir came to the Prophet (peace_be_upon_him) while he was urinating. He saluted him. The Prophet (peace_be_upon_him) did not return the salutation to him until he performed ablution. He then apologised to him, saying: I disliked remembering Allah except in the state of purification. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0019"> Book 1, Number 0019: </a> </em>
        <blockquote>
          <p> Narrated Anas ibn Malik: </p>
          <p> When the Prophet (peace_be_upon_him) entered the privy, he removed his ring. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0022"> Book 1, Number 0022: </a> </em>
        <blockquote>
          <p> Narrated Amr ibn al-'As: </p>
          <p> AbdurRahman ibn Hasanah reported: I and Amr ibn al-'As went to the Prophet (peace_be_upon_him). He came out with a leather shield (in his hand). He covered himself with it and urinated. Then we said: Look at him. He is urinating as a woman does. The Prophet (peace_be_upon_him), heard this and said: Do you not know what befell a person from amongst Banu Isra'il (the children of Israel)? When urine fell on them, they would cut off the place where the urine fell;  but he (that person) forbade them (to do so), and was punished in his grave. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0024"> Book 1, Number 0024: </a> </em>
        <blockquote>
          <p> Narrated Umaymah daughter of Ruqayqah: </p>
          <p> The Prophet (peace_be_upon_him) had a wooden vessel under his bed in which he would urinate at night. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0026"> Book 1, Number 0026: </a> </em>
        <blockquote>
          <p> Narrated Mu'adh ibn Jabal: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: Be on your guard against three things which provoke cursing: easing in the watering places and on the thoroughfares, and in the shade (of the tree). </p>
        </blockquote>
        <hr>
        <em> <a name="001.0027"> Book 1, Number 0027: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Mughaffal: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: No one of you should make water in his bath and then wash himself there (after urination). </p>
          <p> The version of Ahmad has: Then performs ablution there, for evil thoughts come from it. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0028"> Book 1, Number 0028: </a> </em>
        <blockquote>
          <p> Narrated A Man from the Companions: </p>
          <p> Humayd al-Himyari said: I met a man (Companion of the Prophet) who remained in the company of the Prophet (peace_be_upon_him) just as AbuHurayrah remained in his company. He then added: The Apostle of Allah (peace_be_upon_him) forbade that anyone amongst us should comb (his hair) every day or urinate in the place where he takes a bath. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0029"> Book 1, Number 0029: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Sarjis: </p>
          <p> The Prophet (peace_be_upon_him) prohibited to urinate in a hole. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0030"> Book 1, Number 0030: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> When the Prophet (peace_be_upon_him) came out of the privy, he used to say: "Grant me Thy forgiveness." </p>
        </blockquote>
        <hr>
        <em> <a name="001.0032"> Book 1, Number 0032: </a> </em>
        <blockquote>
          <p> Narrated Hafsah, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) used his right hand for taking his food and drink and used his left hand for other purposes. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0033"> Book 1, Number 0033: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) used his right hand for getting water for ablution and taking food, and his left hand for his evacuation and for anything repugnant. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0035"> Book 1, Number 0035: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: If anyone applies collyrium, he should do it an odd number of times. If he does so, he has done well; but if not, there is no harm. If anyone cleanses himself with pebbles, he should use an odd number. If he does so, he has done well; but if not, there is no harm. </p>
          <p> If anyone eats, he should throw away what he removes with a toothpick and swallow what sticks to his tongue. If he does so, he has done well; if not, there is no harm. If anyone goes to relieve himself, he should conceal himself, and if all he can do is to collect a heap of send, he should sit with his back to it, for the devil makes sport with the posteriors of the children of Adam. If he does so, he has done well; but if not, there is no harm. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0036"> Book 1, Number 0036: </a> </em>
        <blockquote>
          <p> Narrated Ruwayfi' ibn Thabit: </p>
          <p> Shayban al-Qatbani reported that Maslamah ibn Mukhallad made Ruwayfi' ibn Thabit the governor of the lower parts (of Egypt). He added: We travelled with him from Kum Sharik to Alqamah or from Alqamah to Kum Sharik (the narrator doubts) for Alqam. </p>
          <p> Ruwayfi' said: Any one of us would borrow a camel during the lifetime of the Prophet (peace_be_upon_him) from the other, on condition that he would give him half the booty, and the other half he would retain himself. </p>
          <p> Further, one of us received an arrowhead and a feather, and the other an arrow-shaft as a share from the booty. </p>
          <p> He then reported: The Apostle of Allah (peace_be_upon_him) said: You may live for a long time after I am gone, Ruwayfi', so, tell people that if anyone ties his beard or wears round his neck a string to ward off the evil eye, or cleanses himself with animal dung or bone, Muhammad has nothing to do with him. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0039"> Book 1, Number 0039: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Mas'ud: </p>
          <p> A deputation of the jinn came to the Prophet (peace_be_upon_him) and said: O Muhammad, forbid your community to cleans themselves with a bone or dung or charcoal, for in them Allah has provided sustenance for us.  So the Prophet (peace_be_upon_him) forbade them to do so. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0040"> Book 1, Number 0040: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: When any of you goes to relieve himself, he should take with him three stones to cleans himself, for they will be enough for him. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0041"> Book 1, Number 0041: </a> </em>
        <blockquote>
          <p> Narrated Khuzaymah ibn Thabit: </p>
          <p> The Prophet (peace_be_upon_him) was asked about cleansing (after relieving oneself). He said: (One should cleanse oneself) with three stones which should be free from dung. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0042"> Book 1, Number 0042: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) urinated and Umar was standing behind him with a jug of water. He said: What is this, Umar? He replied: Water for you to perform ablution with. He said: I have not been commanded to perform ablution every time I urinate. If I were to do so, it would become a sunnah. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0044"> Book 1, Number 0044: </a> </em>
        <blockquote>
          <p> Narrated AbuHurayrah: </p>
          <p> The Prophet (peace_be_upon_him) said: The following verse was revealed in connection with the people of Quba': "In it are men who love to be purified" (ix.108). He (AbuHurayrah) said: They used to cleanse themselves with water after easing. So the verse was revealed in connection with them. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0047"> Book 1, Number 0047: </a> </em>
        <blockquote>
          <p> Narrated Zayd ibn Khalid al-Juhani: </p>
          <p> I heard the Apostle of Allah (peace_be_upon_him) say: Were it not hard on my ummah, I would order them to use the tooth-stick at the time of every prayer. AbuSalamah said: Zayd ibn Khalid used to attend the prayers in the mosque with his tooth-stick on his ear where a clerk carries a pen, and whenever he got up for prayer he used it. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0048"> Book 1, Number 0048: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Hanzalah ibn AbuAmir: </p>
          <p> Muhammad ibn Yahya ibn Habban asked Abdullah ibn Abdullah ibn Umar about the reason for Ibn Umar's performing ablution for every prayer, whether he was with or without ablution. </p>
          <p> He replied: Asma', daughter of Zayd ibn al-Khattab, reported to me that Abdullah ibn Hanzalah ibn AbuAmir narrated to her that the Apostle of Allah (peace_be_upon_him) was earlier commanded to perform ablution for every prayer whether or not he was with ablution. </p>
          <p> When it became a burden for him, he was ordered to use tooth-stick for every prayer. As Ibn Umar thought that he had the strength (to perform the ablution for every prayer), he did not give up performing ablution for every prayer. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0050"> Book 1, Number 0050: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Apostle of Allah (peace_be_upon_him) was using the tooth-stick, when two men, one older than the other, were with him. A revelation came to him about the merit of using the tooth-stick. He was asked to show proper respect and give it to the elder of the two. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0052"> Book 1, Number 0052: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Apostle of Allah (peace_be_upon_him) said: Ten are the acts according to fitrah (nature): clipping the moustache, letting the beard grow, using the tooth-stick, cutting the nails, washing the finger joints, plucking the hair under the arm-pits, shaving the pubes, and cleansing one's private parts (after easing or urinating) with water. The narrator said: I have forgotten the tenth, but it may have been rinsing the mouth. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0055"> Book 1, Number 0055: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> Ablution water and tooth-stick were placed by the side of the Prophet (peace_be_upon_him). When he got up during the night (for prayer), he relieved himself, then he used the tooth-stick. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0056"> Book 1, Number 0056: </a> </em>
        <blockquote>
          <p> Narrated Aisha, Ummul Mu'minin: </p>
          <p> The Prophet (peace_be_upon_him) did not get up after sleeping by night or by day without using the tooth-stick before performing ablution. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0059"> Book 1, Number 0059: </a> </em>
        <blockquote>
          <p> Narrated AbulMalih: </p>
          <p> The Prophet (peace_be_upon_him) said: Allah does not accept charity from goods acquired by embezzlement as He does not accept prayer without purification. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0061"> Book 1, Number 0061: </a> </em>
        <blockquote>
          <p> Narrated Ali ibn AbuTalib: </p>
          <p> The key to prayer is purification; its beginning is takbir and its end is taslim. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0062"> Book 1, Number 0062: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> AbuGhutayf al-Hudhali reported: I was in the company of Ibn Umar. When the call was made for the noon (zuhr) prayer, he performed ablution and said the prayer. When the call for the afternoon ('asr) prayer was made, he again performed ablution. Thus I asked him (about the reason of performing ablution). He replied: The Apostle of Allah (peace_be_upon_him) said: For a man who performs ablution in a state of purity, ten virtuous deeds will be recorded (in his favour). </p>
          <p> AbuDawud said: This is the tradition narrated by Musaddad, and it is more perfect. </p>
        </blockquote>
        <hr>
        <em> <a name="001.0063"> Book 1, Number 0063: </a> </em>
        <blockquote>
          <p> Narrated Abdullah ibn Umar: </p>
          <p> The Prophet (peace_be_upon_him), was asked about water (in desert country) and what is frequented by animals and wild beasts. He replied: When there is enough water to fill two pitchers, it bears no impurity. </p>
        </blockquote>
