          <p> Thereupon Allah, the Exalted, sent down revelation about it to His Apostle (peace_be_upon_him) granting concession to purify themselves with pure earth. Then the Muslims stood up with the Apostle of Allah (peace_be_upon_him) and struck the ground with their hands and then they raised their hands, and did not take any earth (in their hands). Then they wiped with them their faces and hands up to the shoulders, and from their palms up to the armpits. </p>
