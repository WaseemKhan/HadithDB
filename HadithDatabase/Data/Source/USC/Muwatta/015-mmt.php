        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Ziyad ibn Abi Ziyad said that Abu'd-Darda had said, "Shall I not tell you the best of your deeds, and those that give you the highest rank, and those that are the purest with your King, and are better for you than giving gold and silver, and better for you than meeting your enemy and striking their necks?" They said, "Of course." He said, "Remembrance (dhikr) of Allah ta ala." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.7.25"> Book 15, Number 15.7.25: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nuaym ibn Abdullah ibn al-Mujmir from AIi ibn Yahya az-Zuraqi from his father that Rifaa ibn Rafi said, "One day we were praying behind the Messenger of Allah, may Allah bless him and grant him peace, when the Messenger of Allah, may Allah bless him and grant him peace, raised his head from ruku and said, 'Allah hears the one who praises Him' (Sami Allahu liman hamidah). A man behind him said, 'Our Lord, praise belongs to you - blessed, pure and abundant praise' (Rabbana wa laka'l hamd kathiran tayiban mubarakan fihi). When the Messenger of Allah, may Allah bless him and grant him peace, had finished, he said, 'Who was it who spoke just now?' The man said, 'I did, Messenger of Allah,' and the Messenger of Allah, may Allah bless him and grant him peace, said, 'I saw more than thirty angels rushing to it to see which one of them would record it first.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Dua (Supplication) </h3>
        <hr>
        <em> <a name="015.15.8.26"> Book 15, Number 15.8.26: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Every prophet is given a supplication (dua), and I wish to preserve my dua as intercession for my community in the next world. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.8.27"> Book 15, Number 15.8.27: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, used to say the following dua, "O Allah, it is You who makes the dawn break and makes the night a time for rest and appoints the sun and moon to reckon by. Relieve me of debt and enrich me from poverty and let me enjoy my hearing, my sight and my strength in Your way." </p>
          <p> Allahumma faliqa'l isbah, wa ja ila'l-layli sakana, wa'sh-shamsi wa'l-qamari husbana. Iqda anniy addayna, wa'ghnaniy mina'l faqr. Na'mti aniy bi samiy wa basariy, wa quwwatiy fi sabilik. </p>
        </blockquote>
        <!--EndHadithBody-->
