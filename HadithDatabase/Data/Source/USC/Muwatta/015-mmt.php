        <h1> <em> The Qur'an </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Order to Be In Wudu (When Touching the Qur'an) </h3>
        <hr>
        <em> <a name="015.15.1.1"> Book 15, Number 15.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr ibn Hazm that in a letter that the Messenger of Allah, may Allah bless him and grant him peace, sent to Amr ibn Hazm it said that no-one should touch the Qur'an unless he was pure. </p>
          <p> Malik said, "No-one should carry the Qur'an by its strap, or on a cushion, unless he is pure. If it were permissible to do so, it would also have been permissible to carry it in its cover. This is not because there is something on the hands of the one who carries it by which the Qur'an will be soiled, but because it is disapproved of for someone to carry the Qur'an without being pure out of respect for the Qur'an, and in order to honour it." </p>
          <p> Malik said, "The best thing that I have heard about this is the ayat 'None touch it except the purified.' (Sura 56 ayat 79)  It ranks with the ayat in Surat Abasa (Sura 80), where Allah, the Blessed and Exalted, says, 'No, it is a reminder, and whoever wishes will remember it. Upon honoured pages, exalted and purified, by the hands of scribes, noble and obedient.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Permission to Recite the Qur'an When Not in Wudu </h3>
        <hr>
        <em> <a name="015.15.2.2"> Book 15, Number 15.2.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ayyub ibn Abi Tamima as-Sakhtayani from Muhammad ibn Sirin that Umar ibn al-Khattab was with some people who were reciting Qur'an. He went to relieve himself and then came back and recited Qur'an. One of the men said to him, "Amir al muminin, are you reciting the Qur'an without being in wudu?" Umar replied, "Who gave you a verdict on this? Was it Musaylima?"' </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About the Division of the Qur'an into Sections (Hizbs) </h3>
        <hr>
        <em> <a name="015.15.3.3"> Book 15, Number 15.3.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Dawud ibn al-Husayn from al-Araj from Abd ar-Rahman ibn Abd al-Qari that Umar ibn al Khattab said, "Whoever misses reading his hizb at night and reads it from when the sun has passed the meridian until the dhuhr prayer has not missed it, or it is as if he has caught it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.3.4"> Book 15, Number 15.3.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said, "Once Muhammad ibn Yahya ibn Habban and I were sitting down, and Muhammad called a man over to him and said to him, 'Tell me what you have heard from your father.'  The man replied that his father had told him that he went to Zayd ibn Thabit and asked him, 'What do you think of reciting the whole Qur'an in seven days?'  Zayd said, 'That's good, but I prefer to recite it in two weeks, or ten days. Ask me why that is.' He said, 'I ask you then.' Zayd said, 'So that I can reflect on it and pause in it.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About the Qur'an </h3>
        <hr>
        <em> <a name="015.15.4.5"> Book 15, Number 15.4.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Urwa ibn az-Zubayr that Abd ar-Rahman ibn Abd al-Qari said that he had heard Umar ibn al-Khattab say, "I heard Hisham ibn Hakim ibn Hizam reciting Surat al-Furqan (Sura 25) differently from me, and it was the Messenger of Allah, may Allah bless him and grant him peace, who had recited it to me. I was about to rush up to him but I granted him a respite until he had finished his prayer. Then I grabbed him by his cloak and took him to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Messenger of Allah, I heard this man reciting Surat al-Furqan differently from the way you recited it to me.'  The Messenger of Allah, may Allah bless him and grant him peace, said, 'Let him go.'  Then he said, 'Recite, Hisham,' and Hisham recited as I had heard him recite. The Messenger of Allah, may Allah bless him and grant him peace, said, 'It was sent down like that.'  Then he said to me, 'Recite' and I recited the sura, and he said, 'It was sent down like that. This Qur'an was sent down in seven (different) ways, so recite from it whatever is easy for you .' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.4.6"> Book 15, Number 15.4.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "A man who knows the Qur'an well is like a man who has a hobbled camel. If he takes care of it, he keeps it, and if he lets it go, it gets away." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.4.7"> Book 15, Number 15.4.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that al-Harith ibn Hisham asked the Messenger of Allah, may Allah bless him and grant him peace, "How does the revelation come to you?" and the Messenger of Allah, may Allah bless him and grant him peace, said, "Sometimes it comes to me like the ringing of a bell, and that is the hardest for me, and when it leaves me I remember what it has said. And sometimes the angel appears to me in the likeness of a man and talks to me and I remember what he says." </p>
          <p> A'isha added, "I saw it coming down on him on an intensely cold day, and when it had left him his forehead was dripping with sweat." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.4.8"> Book 15, Number 15.4.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa that his father said that Abasa (Sura 80) was sent down about Abdullah ibn Umm Maktum. He came to the Prophet, may Allah bless him and grant him peace, and began to say, "O Muhammad, show me a place near you (where I can sit)," whilst one of the leading men of the idol worshippers was in audience with the Prophet, may Allah bless him and grant him peace. The Prophet, may Allah bless him and grant him peace, began to turn away from him and give his attention to the other man, and he said to him, "Father of so-and-so, do you see any harm in what I am saying?" and he said, "No, by the blood (of our sacrifices) I see no harm in what you are saying."  And Abasa - "He frowned and turned away when the blind man came" - was sent down. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.4.9"> Book 15, Number 15.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from his father that the Messenger of Allah, may Allah bless him and grant him peace, was on one of his journeys, and one night Umar ibn al-Khattab, who was travelling with him, asked him about something, but he did not answer him. He asked him again, but he did not answer him. Then he asked him again, and again he did not answer him. Umar said, "May your mother be bereaved of you, Umar. Three times you have importuned the Messenger of Allah, may Allah bless him and grant him peace, with a question and he has not answered you at all." </p>
          <p> Umar continued, "I got my camel moving until, when I was in f ront of the people, I feared that a piece of Qur'an was being sent down about me. It was not long before I heard a crier calling for me, and I said that I feared that a piece of Qur'an had been sent down about me." He continued, "I came to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Peace be upon you' to him, and he said, 'A sura has been sent down to me this night that is more beloved to me than anything on which the sun rises.'  Then he recited al-Fath (Sura 48). </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.4.10"> Book 15, Number 15.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Muhammad ibn Ibrahim ibn al-Harith at-Taymi from Abu Salama ibn Abd ar Rahman that Abu Said said that he had heard the Messenger of Allah, may Allah bless him and grant him peace, say, "A group of people will appear among you whose prayer, fasting and deeds will make you think little of your own prayer, fasting and deeds. They will recite the Qur'an, but it wil not get past their throats, and they will pass through the deen like an arrow passes through game. You look at the arrowhead, and you see nothing, and you look at the shaft, and you see nothing, and you look at the flights, and you see nothing. And you are in doubt about the notch." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.4.11"> Book 15, Number 15.4.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that it took Abdullah ibn Umar eight years to learn Surat al-Baqara. (Sura 2) </p>
          <p> 15.5 The Prostration of the Qur'an </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Prostration of the Qur'an </h3>
        <hr>
        <em> <a name="015.15.5.12"> Book 15, Number 15.5.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Yazid, the mawla of al Aswad ibn Sufyan, from Abu Salama ibn Abd ar Rahman that Abu Hurayra recited al-Inshiqaq (Sura 84) to them and prostrated in it. When he had finished he told them that the Messenger of Allah, may Allah bless him and grant him peace, had prostrated in it. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.5.13"> Book 15, Number 15.5.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi, the mawla of Ibn Umar that a man from Egypt told him that Umar ibn al-Khattab recited Surat al-Hajj (Sura 22) and prostrated twice in it, and then said, "This sura has been given special preference by having two prostrations in it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.5.14"> Book 15, Number 15.5.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Abdullah ibn Dinar said, "I saw Abdullah ibn Umar prostrate twice in Surat al-Hajj (Sura 22)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.5.15"> Book 15, Number 15.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from al-Araj that Umar ibn al Khattab recited Surat an-Najm (Sura 53) and prostrated in it, and then got up and recited another sura. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.5.16"> Book 15, Number 15.5.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that Umar ibn al-Khattab once recited a piece of Qur'an requiring a prostration while he was on the mimbar on the day of jumua, and he came down and prostrated, and everyone prostrated with him. Then he recited it again the next jumua and everybody prepared to prostrate but he said, "At your ease. Allah has not prescribed it for us, unless we wish." He did not prostrate, and he stopped them from prostrating. </p>
          <p> Malik said, "The imam does not come down and prostrate when he recites a piece of Qur'an requiring a prostration while he is on the mimbar." </p>
          <p> Malik said, "The position with us is that there are eleven prescribed prostrations in the Qur'an, none of which are in the mufassal." </p>
          <p> Malik said, "No-one should recite any of the pieces of Qur'an that require a prostration after the prayers of subh and asr. This is because the Messenger of Allah, may Allah bless him and grant him peace, forbade prayer after subh until after the sun had risen, and after asr until the sun had set, and prostration is part of the prayer. So no-one should recite any piece of Qur'an requiring a prostration during these two periods of time." </p>
          <p> Malik was asked whether a menstruating woman could prostrate if she heard some-one reciting a passage of Qur'an requiring a prostration, and he said, "Neither a man nor a woman should prostrate unless they are ritually pure." </p>
          <p> Malik was asked whether a man in the company of a woman who was reciting a passage of Qur'an requiring a prostration should prostrate with her, and he said, "He does not have to prostrate with her. The prostration is only obligatory for people who are with a man who is leading them. He recites the piece and they prostrate with him. Some one who hears a piece of Qur'an that requires a prostration being recited by a man who is not leading him in prayer does not have to do the prostration." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About Reciting Surat al-Ikhlas and Surat al-Mulk </h3>
        <hr>
        <em> <a name="015.15.6.17"> Book 15, Number 15.6.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abd ar-Rahman ibn Abdullah ibn Sasaca from his father that Abu Said al-Khudri heard a man reciting Surat al-Ikhlas (Sura 112), repeating it over and over again. In the morning he went to the Messenger of Allah, may Allah bless him and grant him peace, and mentioned it to him, as if he thought little of it. The Messenger of Allah, may Allah bless him and grant him peace, said, "Bythe One in whose hand my self is, it is equal to one third of the Qur'an." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.6.18"> Book 15, Number 15.6.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ubaydullah ibn Abd ar-Rahman that Ubayd ibn Hunayn, the mawla of the family of Zayd ibn al Khattab, said that he had heard Abu Hurayra say, "I was going along with the Messenger of Allah, may Allah bless him and grant him peace, when he heard a man reciting Surat al-Ikhlas (Sura 112). The Messenger of Allah, may Allah bless him and grant him peace, said, 'It is obligatory,' and I asked him, 'What is, Messenger of Allah?' and he said, 'The Garden.' I wanted to tell the man the good news but I was afraid that I would miss the midday meal with the Messenger of Allah, may Allah bless him and grant him peace, and I preferred to eat with the Messenger of Allah, may Allah bless him and grant him peace. When I went to the man afterwards I found that he had gone." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.6.19"> Book 15, Number 15.6.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that Humayd ibn Abd ar-Rahman ibn Awf had told him that Surat al-Ikhlas (Sura 112) was equal to a third of the Qur'an, and that Surat al-Mulk (Sura 67) pleaded for its owner. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Dhikr (Remembrance) ofAllah, the Blessed and Exalted </h3>
        <hr>
        <em> <a name="015.15.7.20"> Book 15, Number 15.7.20: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr, from Abu Salih as-Samman from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever says 'There is no god but Allah, alone, without any partner. The Kingdom and praise belong to Him and He has power over everything' (La ilaha illa'llah, wahdahu la sharika lah, lahu'l mulku wa lahu'l hamd, wa huwa ala kulli shay'in qadir) one hundred times a day, it is the same for him as freeing ten slaves. One hundred good actions are written for him and one hundred wrong actions are erased from him, and it is a protection from Shaytan for that day until the night. No-one does anything more excellent than what he does except someone who does more than that." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.7.21"> Book 15, Number 15.7.21: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr, from Abu Salih as-Samman from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever says, 'Glory be to Allah and with His praise' (Subhana'llah wa bi-hamdihi) one hundred times in a day will have his wrong actions taken away from him, even if they are as abundant as the foam on the sea." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="015.15.7.22"> Book 15, Number 15.7.22: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
