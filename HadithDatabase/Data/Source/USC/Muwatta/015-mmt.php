          <p> Yahya related to me from Malik from Ibn Shihab from Abu Abdullah al-Agharr and from Abu Salama from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Our Lord, the Blessed and Exalted, descends every night to the heaven of this world when the last third of the night is still to come and says, 'Who will call on Me so that I may answer him? Who will ask Me so that I may give him? Who will ask forgiveness of Me so that I may forgive him?' " </p>
        </blockquote>
        <!--EndHadithBody-->
