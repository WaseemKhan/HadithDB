        <em> <a name="015.15.8.28"> Book 15, Number 15.8.28: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z  Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, ''When you are making dua do not say; 'O Allah, forgive me if You wish. O Allah, forgive me if you wish.' You should be firm in your asking, for there is no compelling Him." </p>
        </blockquote>
