        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Umara ibn Sayyad had heard Said ibn al Musayyab say about abiding good deeds that they were a slave's saying 'Allah is greater' (Allahu akbar) and 'Glory be to Allah' (Subahana'llah) and 'Praise be to Allah' (al-hamdu lillah) and 'There is no god but Allah and there is no power and no strength except by Allah.' </p>
          <p> La ilaha illa'llah wa la hawla wa la quwwata illa bi'llah. </p>
        </blockquote>
