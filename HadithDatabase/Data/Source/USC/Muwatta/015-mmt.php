          <p> Yahya related to me from Malik from Abu'z Zubayr al-Makki from Tawus al-Yamani from Abdullah ibn Abbas that the Messenger of Allah, may Allah bless him and grant him peace, used to say, when he rose for prayer in the middle of the night, "O Allah, praise belongs to You. You are the light of the heavens and the earth and praise belongs toYou.You are the Sustainer of the heavens and the earth and praise belongs to You. You are the Lord of the heavens and the earth and whoever is in them. You are the Truth, and Your words are true. Your promise is true, and the meeting with You is true. The Garden is true and the Fire is true and the Hour is true. O Allah, I submit toYou and I accept You and I trust in You and I turn to You and I argue by You and I summon toYou for judgement. Forgive me what I have sent before me and what I have left behind, what I have kept secret and what I have proclaimed, You are my god - there is no god but You." </p>
          <p> Allahumma laka'l-hamdu anta nuru's-samawati wa'l-ardi, wa laka'l-hamdu anta qayamu's-Samawati wa'l-ardi, wa laka'l-hamdu anta rabbu's-Samawati wa'l-ardi,wamanfihina.Anta'l-haqqu,waqawluka'lhaqqu, wa waduka'l-haqqu, wa liqa'uka haqqun, wa jannatu haqqun, wa naru haqqun, wa sactu haqqun. Allahumma laka aslamtu, wa bikaamantu, waalayka tawakaltu, wa ilayka anabtu, wa bika khasamtu, wa ilayka hakamtu, fa'ghfirliy ma qadamtu wa akhartu wa asrartu, wa alantu. Anta ilahiy, la ilaha illa ant. </p>
        </blockquote>
