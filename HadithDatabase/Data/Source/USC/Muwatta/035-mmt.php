      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 35: </a> </em>
        <h1> <em> Pre-emption in Property </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Cases in which Pre-emption is Possible </h3>
        <hr>
        <em> <a name="035.35.1.1"> Book 35, Number 35.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab and from Abu Salama ibn Abd ar-Rahman ibn Awf that the Messenger of Allah, may Allah bless him and grant him peace, decreed for partners the right of preemption in property which had not been divided up. When boundaries had been fixed between them, then there was no right of pre-emption. </p>
          <p> Malik said, "That is the sunna about which there is no dispute among us." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="035.35.1.2"> Book 35, Number 35.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said that he heard that Said ibn al-Musayyab, when asked about pre-emption and whether there was a sunna in it, said, "Yes. Pre-emption is in houses and land, and it is only between partners." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="035.35.1.3"> Book 35, Number 35.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard the like of that from Sulayman ibn Yasar. </p>
          <p> Malik spoke about a man who bought out one of the partners in a shared property, by paying the man with an animal, a slave, a slave-girl, or the equivalent of that in goods. Then another partner decided to exercise his right of pre-emption after that, and he found that the slave or slave-girl had died, and no one knew what her value had been. The buyer claimed, "The value of the slave or slave-girl was 100 dinars." The partner with the right of pre-emption claimed, "The value was 50 dinars." </p>
          <p> Malik said, "The buyer takes an oath that the value of what he payed was 100 dinars. Then if the one with the right of pre-emption wishes, he can compensate him, or else he can leave it, unless he can bring a clear proof that the slave or slave-girl's value is less than what the buyer said. If someone gives away his portion of a shared house or land and the recipient repays him for it by cash or goods, the partners can take it by pre-emption if they wish and pay off the recipient the value of what he gave in dinars or dirhams. If someone makes a gift of his portion of a shared house or land, and does not take any remuneration and does not seek to, and a partner wants to take it for its value, he cannot do so as long as the original partner has not been given recompense for it. If there is any recompense, the one with the right of pre-emption can have it for the price of the recompense." </p>
          <p> Malik spoke about a man who bought into a piece of shared land for a price on credit, and one of the partners wanted to possess it by right of pre-emption . Malik said, "If it seems likely that the partner can meet the terms, he has right of pre-emption for the same credit terms. If it is feared that he will not be able to meet the terms, but he can bring a wealthy and reliable guarantor of equal standing to the one who bought into the land, he can also take possession." </p>
          <p> Malik said, "A person's absence does not sever his right of pre-emption. Even if he is a way for a long time, there is no time limit after which the right of preemption is cut off." </p>
          <p> Malik said that if a man left land to a number of his children, then one of them who had a child died and the child of the deceased sold his right in that land, the brother of the seller was more entitled to pre-empt him than his paternal uncles, the partners of his father. </p>
          <p> Malik said, "This is what is done in our community." </p>
          <p> Malik said, "Pre-emption is shared between partners according to their existing shares. Each of them takes according to his portion. If it is small, he has little. If it is great, it is according to that. That is if they are tenacious and contend with each other about it." </p>
          <p> Malik said, "As for a man who buys out the share of one of his partners, and one of the other partners says, 'I will take a portion according to my share,' and the first partner says, 'If you wish to take all the preemption, I will give it up to you. If you wish to leave it, then leave it.' If the first partner gives him the choice and hands it over to him, the second partner can only take all the pre-emption or give it back. If he takes it, he is entitled to it. If not, he has nothing." </p>
          <p> Malik spoke about a man who bought land, and developed it by planting trees or digging a well etc., and then someone came, and seeing that he had a right in the land, wanted to take possession of it by pre-emption. Malik said "He has no right of preemption unless he compensates the other for his expenditure. If he gives him the price of what he has developed, he is entitled to pre-emption . If not, he has no right in it." </p>
          <p> Malik said that someone who sold off his portion of a shared house or land and then, on learning that some one with a right of pre-emption was to take possession by that right, asked the buyer to revoke the sale, and he did so, did not have the right to do that. The pre-emptor has more right to the property for the price for which he sold it. </p>
          <p> In the case of some one who bought along with a section of a shared house or land, an animal and goods (that were not shared), so that when any one demanded his right of pre-emption in the house or land he said, "Take what I have bought altogether, for I bought it altogether," Malik said, "The pre-emptor need only take possession of the house or land. Each thing the man bought is assessed according to its share of the lump sum the man paid. Then the pre-emptor takes possession of his right for a price which is appropriate on that basis. He does not take any animals or goods unless he wants to do that." </p>
          <p> Malik said, "If someone sells a section of shared land, and one of those who have the right of preemption surrenders it to the buyer and another refuses to do other than take his pre-emption, the one who refuses to surrender has to take all the preemption, and he cannot take according to his right and leave what remains. </p>
          <p> In the case where one of a number of partners in one house sold his share when all his partners were away except for one man, the one present was given the choice of either taking the pre-emption or leaving it, and he said, 'I will take my portion and leave the portions of my partners until they are present. If they take it, that is that. If they leave it, I will take all the pre-emption,' Malik said, 'He can only take it all or leave it. If his partners come, they can take from him or leave it as they wish. If this is offered to him and he does not accept, I think that he has no pre-emption.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Cases in Which Pre-Emption Is Not Possible </h3>
        <hr>
        <em> <a name="035.35.2.4"> Book 35, Number 35.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik related from Muhammad ibn Umara from Abu Bakr ibn Hazm that Uthman ibn Affan said, "When boundaries are fixed in land, there is no pre-emption in it. There is no pre-emption in a well or in male palm trees. " </p>
          <p> Malik said, "This is what is done in our community." </p>
          <p> Malik said, "There is no pre-emption in a road, whether or not it is practical to divide it." </p>
          <p> Malik said, "What is done in our community is that there is no pre-emption in the courtyard of a house, whether or not it is practical to divide it." </p>
          <p> Malik spoke about a man who bought into a shared property provided that he had the option of withdrawal and the partners of the seller wanted to take what their partner was selling by pre-emption before the buyer had exercised his option. Malik said, "They cannot do that until the buyer has taken possession and the sale is confirmed for him. When the sale is confirmed, they have the right of pre-emption." </p>
          <p> Malik spoke about a man who bought land and it remained in his hands for some time. Then a man came and saw that he had a share of the land by inheritance. Malik said, "If the man's right of inheritance is established, he also has a right of preemption. If the land has produced a crop, the crop belongs to the buyer until the day when the right of the other is established, because he has tended what was planted against being destroyed or being carried away by a flood." </p>
          <p> Malik continued, "If the time has been long, or the witnesses are dead or the seller has died, or the buyer has died, or they are both alive and the basis of the sale and purchase has been forgotten because of the length of time, pre-emption is discontinued. A man only takes his right by inheritance which has been established for him. If his situation differs from this, because the sale transaction is recent and he sees that the seller has concealed the price in order to sever his right of pre-emption, the value of the land is estimated, and he buys the land for that price by his right of pre-emption. Then the buildings, plants, or structures which are extra to the land are looked at, so he is in the position of some one who bought the land for a known price, and then after that built on it and planted. The owner of pre-emption takes possession after that is included." </p>
          <p> Malik said, "Pre-emption is applied to the property of the deceased as it is applied to the property of the living. If the family of the deceased fear to break up the property of the deceased, then they share it and sell it, and they have no pre-emption in it." </p>
          <p> Malik said, "There is no pre-emption among us in a slave or a slave-girl or a camel, a cow, sheep, or any animal, nor in clothes or a well which does not have any uncultivated land around it. Pre-emption is in what can be usefully divided, and in land in which boundaries occur. As for what cannot be usefully divided, there is no pre-emption in it." </p>
          <p> Malik said, "Some one who buys land in which people who are present have a right of pre-emption, refers them to the Sultan and either they claim their right or the Sultan surrenders it to him. If he were to leave them, and not refer their situation to the Sultan and they knew about his purchase, and then they left it until a long time had passed and then came demanding their pre-emption, I do not think that they would have it." </p>
        </blockquote>
        <!--EndHadithBody-->
