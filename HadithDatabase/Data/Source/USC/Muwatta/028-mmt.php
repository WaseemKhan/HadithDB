      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 28: </a> </em>
        <h1> <em> Marriage </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Asking for Someone's Hand in Marriage </h3>
        <hr>
        <em> <a name="028.28.1.1"> Book 28, Number 28.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Muhammad ibn Yahya ibn Habban from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Do not ask for a woman in marriage when another muslim has already done so." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.1.2"> Book 28, Number 28.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Do not ask for a woman in marriage when another muslim has already done so." </p>
          <p> Malik said, "The explanation of the statement of the Messenger of Allah, may Allah bless him and grant him peace, according to what we think - and Allah knows best - is that 'Do not ask for a woman in marriage when another muslim has already done so' means that when a man has asked for a woman in marriage, and she has inclined to him and they have agreed on a bride-price, which she has suggested and with which they are mutually satisfied, it is forbidden for another man to ask for that woman in marriage. It does not mean that when a man has asked for a woman in marriage, and his suit does not agree with her and she does not incline to him that no one else can ask for her in marriage. That is a door to misery for people." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.1.3"> Book 28, Number 28.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abd ar-Rahman ibn al-Qasim that his father said about the word of Allah, the Blessed, the Exalted, "There is no fault in you about the proposal you offer to women, or hide in yourselves. Allah knows that you will be mindful of them; but do not make troth with them secretly without honourable words," (Sura 2 ayat 235) that it referred to a man saying to a woman while she was still in her idda after the death of her husband, "You are dear to me, and I desire you, and Allah brings provision and blessing to you," and words such as these. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Asking Consent of Virgins and Women Previously Married for their Persons </h3>
        <hr>
        <em> <a name="028.28.2.4"> Book 28, Number 28.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn al-Fadl from Nafi ibn Jubayr ibn Mutim from Abdullah ibn Abbas that the Messenger of Allah, may Allah bless him and grant him peace, said, "A woman who has been previously married is more entitled to her person than her guardian, and a virgin must be asked for her consent for herself, and her consent is her silence " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.2.5"> Book 28, Number 28.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab had said that Umar ibn al-Khattab said, "A woman is only married with the consent of her guardian, someone of her family with sound judgement or the Sultan. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.2.6"> Book 28, Number 28.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that al-Qasim ibn Muhammad and Salim ibn Abdullah were marrying off their daughters and they did not consult them. </p>
          <p> Malik said, "That is what is done among us about the marriage of virgins." </p>
          <p> Malik said, "A virgin has no right to her property until she enters her house and her state (competence, maturity etc.) is known for sure." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.2.7"> Book 28, Number 28.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that al-Qasim ibn Muhammad and Salim ibn Abdullah and Sulayman ibn Yasar said about the virgin given by her father in marriage without her permission, "That is binding on her." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Bride-Price and Unreturnable Gifts </h3>
        <hr>
        <em> <a name="028.28.3.8"> Book 28, Number 28.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Malik from Abu Hazim ibn Dinar from Sahl ibn Sad as-Saidi that a woman came to the Messenger of Allah, may Allah bless him and grant him peace, and said, "Messenger of Allah! I have given myself to you." She stood for a long time, and then a man got up and said, "Messenger of Allah, marry her to me if you have no need of her." The Messenger of Allah, may Allah bless him and grant him peace, said, "Do you have anything to give her as a bride-price?" He said, "I possess only this lower garment of mine." The Messenger of Allah, may Allah bless him and grant him peace, said, "If you give it to her you will not have a garment to wear so look for something else." He said, "I have nothing else." He said, "Look for something else, even if it is only an iron ring." He looked, and found that he had nothing. The Messenger of Allah, may Allah bless him and grant him peace, said, "Do you know any of the Qur'an?" He said, "Yes. I know such-and-such a sura and such-and-such a sura," which he named. The Messengerof Allah, may Allah bless him and grant him peace, said to him, "I have married her to you for what you know of the Qur'an." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.3.9"> Book 28, Number 28.3.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab had said that Umar ibn al-Khattab said, "If a man marries a woman who is insane, or has leprosy or white leprosy, without being told of her condition by her guardian, and he has sexual relations with her, she keeps her bride-price in its entirety. Her husband has damages against her guardian." </p>
          <p> Malik said, "The husband has damages against her guardian when the guardian is her father, brother, or one who is deemed to have knowledge of her condition. If the guardian who gives her in marriage is a nephew, a mawla or a member of her tribe who is not deemed to have knowledge of her condition, there are no damages against him, and the woman returns what she has taken of her bride-price, and the husband leaves her whatever amount is thought to be fair." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.3.10"> Book 28, Number 28.3.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that the daughter of Ubaydullah ibn Umar whose mother was the daughter of Zayd ibn al-Khattab, married the son of Abdullah ibn Umar. He died and had not yet consummated the marriage or specified her bride-price. Her mother wanted the bride-price, and Abdullah ibn Umar said, "She is not entitled to a bride-price. Had she been entitled to a bride-price, we would not have kept it and we would not do her an injustice. "The mother refused to accept that. Zayd ibn Thabit was brought to adjudicate between them and he decided that she had no bride-price, but that she did inherit. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.3.11"> Book 28, Number 28.3.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Umar ibn Abd al-Aziz during his khalifate, wrote to one of his governors, "Whatever a father, or guardian, who gives someone in marriage, makes a condition in the way of unreturnable gift or of favour, belongs to the woman if she wants it." </p>
          <p> Malik spoke about a woman whose father gave her in marriage and made an unreturnable gift a condition of the bride-price which was to be given. He said, "Whatever is given as a condition by which marriage occurs belongs to the woman if she wants it. If the husband parts from her before the marriage is consummated, the husband has half of the unreturnable gift by which the marriage occurred." </p>
          <p> Malik said about a man who married off his young son and the son had no wealth at all, that the bride-price was obliged of the father if the young man had no property on the day of marriage. If the young man did have property the bride-price was taken from his property unless the father stipulated that he would pay the bride-price. The marriage was affirmed for the son if he was a minor only if he was under the guardianship of his father. </p>
          <p> Malik said that if a man divorced his wife before he had consummated the marriage and she was a virgin, her father returned half of the bride-price to him. That half was permitted to the husband from the father to compensate him for his expenses. </p>
          <p> Malik said that that was because Allah, the Blessed, the Exalted, said in His Book, "Unless they (women with whom he had not consummated marriage) make remission or he makes remission to him in whose hand is the knot of marriage." (Sura 2 ayat 237). (He being the father of a virgin daughter or the master of a female slave.) </p>
          <p> Malik said, "That is what I have heard about the matter, and that is how things are done among us." </p>
          <p> Malik said that a jewish or christian woman who was married to a jew or christian and then became muslim before the marriage had been consummated, did not keep anything from the bride-price. </p>
          <p> Malik said, "I do not think that women should be married for less than a quarter of a dinar. That is the lowest amount for which cutting off the hand is obliged ." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Consummating the Marriage </h3>
        <hr>
        <em> <a name="028.28.4.12"> Book 28, Number 28.4.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Said ibn al-Musayyab that 'Umar ibn al-Khattab decided about the woman who was married by a man and the marriage had been consummated, that the bride-price was obligatory. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.4.13"> Book 28, Number 28.4.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that Zayd ibn Thabit said, "When a man takes his wife to his house and co-habits with her then the bride-price is obliged." </p>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab said, "When a man comes to his wife in her room, he is believed. When she comes to him in his room, she is believed." </p>
          <p> Malik commented, "I think that this refers to sexual intercourse. When he comes in to her in her room and she says, 'He has had intercourse with me' and he says, 'I have not touched her', he is believed. When she comes in to him in his room and he says, 'I have not had intercourse with her' and she says, 'He had intercourse with me', she is believed." </p>
          <p> 28.5 Wedding Nights of Virgins and Women Previously Married </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Wedding Nights of Virgins and Women Previously Married </h3>
        <hr>
        <em> <a name="028.28.5.14"> Book 28, Number 28.5.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr ibn Muhammad ibn Amr ibn Hazm from Abd al-Malik ibn Abi Bakr ibn Abd ar-Rahman ibn al-Harith ibn Hisham al-Makhzumi from his father that when the Messenger of Allah, may Allah bless him and grant him peace, married Umm Salama and then spent the night with her, he said to her, "You are not being humbled in your right. If you wish, I will stay with you for seven nights as I stayed seven nights with the others. If you wish, I will stay with you for three nights, and then visit the others in turn." She said, "Stay three nights." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.5.15"> Book 28, Number 28.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Humayd at-Tawil that Anas ibn Malik said, "A virgin has seven nights, and a woman who has been previously married has three nights." </p>
          <p> Malik affirmed, "That is what is done among us." </p>
          <p> Malik added, "If the man has another wife, he divides his time equally between them after the wedding nights. He does not count the wedding nights against the one he has just married." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Stipulations Not Permitted in Marriage </h3>
        <hr>
        <em> <a name="028.28.6.16"> Book 28, Number 28.6.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab was asked about a woman who made a stipulation on her husband not to take her away from her town. Said ibn al-Musayyab said, "He takes her away if he wishes." </p>
          <p> Malik said, "The custom among us is that when a man marries a woman, and he makes a condition in the marriage contract that he will not marry after her or take a concubine, it means nothing unless there is an oath of divorce or setting-free attached to it. Then it is obliged and required of him." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Marriage of the Muhallil and its Like </h3>
        <hr>
        <em> <a name="028.28.7.17"> Book 28, Number 28.7.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from al-Miswar ibn Rifaa al-Quradhi from az-Zubayr ibn Abd ar-Rahman ibn az-Zubayr that Rifaa ibn Simwal divorced his wife, Tamima bint Wahb, in the time of the Messenger of Allah, may Allah bless him and grant him peace, three times. Then she married Abd ar-Rahman ibn az-Zubayr and he turned from her and could not consummate the marriage and so he parted from her. Rifaa wanted to marry her again and it was mentioned to the Messenger of Allah, may Allah bless him and grant him peace, and he forbade him to marry her. He said, "She is not halal for you until she has tasted the sweetness of intercourse." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.7.18"> Book 28, Number 28.7.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from al-Qasim ibn Muhammad that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said when asked whether it was permissible for a man to marry again a wife he had divorced irrevocably if she had married another man who divorced her before consummating the marriage, "Not until she has tasted the sweetness of intercourse." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.7.19"> Book 28, Number 28.7.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that when asked whether it was permissible for a man to return to his wife if he had divorced her irrevocably and then another man had married her after him and died before consummating the marriage, al-Qasim ibn Muhammad said, "It is not halal for the first husband to return to her." </p>
          <p> Malik said, about the muhallil, that he could not remain in the marriage until he undertook a new marriage. If he had intercourse with her in that marriage, she had her dowry. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Combinations of Women Not to be Married Together </h3>
        <hr>
        <em> <a name="028.28.8.20"> Book 28, Number 28.8.20: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said "One cannot be married to a woman and her paternal aunt, or a woman and her maternal aunt at the same time." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.8.21"> Book 28, Number 28.8.21: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab said, "It is forbidden to be married to a woman and her paternal or maternal aunt at the same time, and for a man to have intercourse with a female slave who is carrying another man's child." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Prohibition against Marrying Mothers of Wives </h3>
        <hr>
        <em> <a name="028.28.9.22"> Book 28, Number 28.9.22: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Zayd ibn Thabit asked whether it was halal for a man who married a woman and then separated from her before he had cohabited with her, to marry her mother. Zayd ibn Thabit said, "No. The mother is prohibited unconditionally. There are conditions, however about foster-mothers." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="028.28.9.23"> Book 28, Number 28.9.23: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from more than one source that when Abdullah ibn Masud was in Kufa, he was asked for an opinion about marrying the mother after marrying the daughter when the marriage with the daughter had not been consummated. He permitted it. When Ibn Masud came to Madina, he asked about it and was told that it was not as he had said, and that this condition referred to foster-mothers. Ibn Masud returnedto Kufa,and he had just reached his dwelling when the man who had asked him for the opinion came to visit and he ordered him to separate from his wife. </p>
          <p> Malik said that if a man married the mother of a woman who was his wife and he had sexual relations with the mother then his wife was haram for him, and he had to separate from both of them. They were both haram to him forever, if he had had sexual relations with the mother. If he had not had relations with the mcther, his wife was not haram for him, and he separated from the mother. </p>
          <p> Malik explained further about the man who married a woman, and then married her mother and cohabited with her, "The mother will never be halal for him, and she is not halal for his father or his son, and any daughters of hers are not halal for him and so his wife is haram for him." </p>
