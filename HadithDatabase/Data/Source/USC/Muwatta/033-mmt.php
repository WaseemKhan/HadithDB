      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 33: </a> </em>
      <h1> <em> Sharecropping </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: Sharecropping </h3>
      <hr>
      <em> <a name="033.33.1.1"> Book 33, Number 33.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab that the Messenger of Allah, may Allah bless him and grant him peace, said to the jews of Khaybar on the day of the conquest of Khaybar, "I confirm you in it as long as Allah, the Mighty, the Majestic, establishes you in it, provided that the fruits are divided between us and you." Said continued, "The Messenger of Allah, may Allah bless him and grant him peace, used to send Abdullah ibn Rawaha, to assess the division of the fruit crop between him and them, and he would say, 'If you wish, you can buy it back, and if you wish, it is mine.' They would take it." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="033.33.1.2"> Book 33, Number 33.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik related to me from Ibn Shihab from Sulayman ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, used to send Abdullah ibn Rawaha to Khaybar, to assess the division of the fruit crop between him and the jews of Khaybar. </p>
        <p> The jews collected for Abdullah pieces of their women's jewellery and said to him, "This is yours. Go light on us and don't be exact in the division!" </p>
        <p> Abdullah ibn Rawaha said, "O tribe of jews! By Allah! You are among the most hateful to me of Allah's creation, but it does not prompt me to deal unjustly with you. What you have offered as a bribe is forbidden. We will not touch it." They said, "This is what supports the heavens and the earth." </p>
        <p> Malik said, "If a share-cropper waters the palms and between them there is some uncultivated land, whatever he cultivates in the uncultivated land is his." </p>
        <p> Malik said, "If the owner of the land makes a condition that he will cultivate the uncultivated land for himself, that is not good because the sharecropper does the watering for the owner of the land and so he increases the owner of the land in property (without any return for himself)." </p>
        <p> Malik said, "If the owner stipulates that the fruit crop is to be shared between them, there is no harm in that if all the maintenance of the property - seeding, watering and case, etc. - are the concern of the sharecropper. </p>
        <p> If the share-cropper stipulates that the seeds are the responsibility of the owner of the property - that is not permitted because he has stipulated an outlay against the owner of the property. Share-cropping is conducted on the basis that all the care and expense is outlayed by the share-cropper, and the owner of the property is not obliged anything. This is the accepted method of share-cropping." </p>
        <p> Malik spoke about a spring which was shared between two men, and then the water dried up and one of them wanted to work on the spring and the other said, "I don't have the means to work on it." He said, "Tell the one who wants to work on the spring, 'Work and expend. All the water will be yours. You will have its water until your companion brings you half of what you have spent. If he brings you half of what you have spent, he can take his share of the water.' The first one is given all the water, because he has spent on it, and if he does not reach anything by his work, the other has not incurred any expense." </p>
        <p> Malik said, "It is not good for a share-cropper not to expend anything but his labour and to be hired for a share of the fruit while all the expense and work is incurred by the owner of the garden, because the share-cropper does not know what the exact wage is going to be for his labour, whether it will be little or great." </p>
        <p> Malik said, "No-one who lends a qirad or grants a share-cropping contract, should exempt some of the wealth, or some of the trees from his agent, because, by that, the agent becomes his hired man. He says, 'I will grant you a share-crop provided that you work for me on such-and-such a palm - water it and tend it. I will give you a qirad for such-and-such money provided that you work for me with ten dinars. They are not part of the qirad I have given you.' That must not be done and it is not good. This is what is done in our community." </p>
        <p> Malik said, "The sunna about what is permitted to an owner of a garden in share-cropping is that he can stipulate to the share-cropper the maintenance of walls, cleaning the spring, sweeping the irrigation canals, pollinating the palms, pruning branches, harvesting the fruit and such things, provided that the share-cropper has a share of the fruit fixed by mutual agreement. However, the owner cannot stipulate the beginning of new work which the agent will start digging a well, raising the source of a well, instigating new planting, or building a cistern whose cost is great. That is as if the owner of the garden said to a certain man, 'Build me a house here or dig me a well or make a spring flow for me or do some work for me for half the fruit of this garden of mine,' before the fruit of the garden is sound and it is halal to sell it. This is the sale of fruit before its good condition is clear. The Messenger of Allah, may Allah bless him and grant him peace, forbade fruit to be sold before its good condition became clear." </p>
        <p> Malik said, "If the fruits are good and their good condition is clear and selling them is halal and then the owner asks a man to do one of those jobs for him, specifying the job, for half the fruit of his garden, for example, there is no harm in that. He has hired the man for something recognised and known. The man has seen it and is satisfied with it. </p>
        <p> "As for share-cropping, if the garden has no fruit or little or bad fruit, he has only that. The labourer is only hired for a set amount, and hire is only permitted on these terms. Hire is a type of sale. One man buys another man's work from him. It is not good if uncertainty enters into it because the Messenger of Allah, may Allah bless him and grant him peace, forbade uncertain transactions." </p>
        <p> Malik said, "The sunna in share-cropping with us is that it can be practised with any kind of fruit tree, palm, vine, olive tree, pomegranate, peach, and soon. It is permitted, and there is no harm in it provided that the owner of the property has a share of the fruit: a half or a third or a quarter or whatever." </p>
        <p> Malik said, "Share-cropping is also permitted in any crop which emerges from the earth if it is a crop which is picked, and its owner cannot water, work on it and tend it. </p>
        <p> "Share-cropping becomes reprehensible in anything in which share-cropping is normally permitted if the fruit is sound and the good condition is clear and it is halal to sell it. He must share-crop in it the next year. If a man waters fruit whose good condition is clear and it is halal to sell it, and he picks it for the owner, for a share of the crop, it is not sharecropping. It is similar to him being paid in dirhams and dinars. Share-cropping is what is between pruning the palms and when the fruit becomes sound and its sale is halal." </p>
        <p> Malik said, "If some one makes a share-cropping contract for fruit trees before the condition becomes clear and its sale is halal, it is share-cropping and is permitted . " </p>
        <p> Malik said, "Uncultivated land must not be involved in a share-cropping contract. That is because it is halal for the owner to rent it for dinars and dirhams or the equivalent for an accepted price." </p>
        <p> Malik said, "As for a man who gives his uncultivated earth for a third or a fourth of what comes out of it, that is an uncertain transaction because crops may be scant one time and plentiful another time. It may perish completely and the owner of the land will have abandoned a set rent which would have been good for him to rent the land for. He takes an uncertain situation, and does not know whether or not it will be satisfactory. This is disapproved. It is like a man having someone travel for him for a set amount, and then saying, 'Shall I give you a tenth of the profit of the journey as your wage?' This is not halal and must not be done." </p>
        <p> Malik summed up,"A man must not hire out himself or his land or his ship unless for a set amount." </p>
        <p> Malik said, "A distinction is made between sharecropping in palms and in cultivated land because the owner of the palms cannot sell the fruit until its good condition is clear. The owner of the land can rent it when it is uncultivated with nothing on it." </p>
        <p> Malik said, "What is done in our community about palms is that they can also be share-cropped for three and four years, and less or more than that." </p>
        <p> Malik said, "That is what I have heard. Any fruit trees like that are in the position of palms. Contracts for several years are permissible for the sharecropper as they are permissible in the palms." </p>
        <p> Malik said about the owner, "He does not take anything additional from the share-cropper in the way of gold or silver or crops which increases him. That is not good. The share-cropper also must not take from the owner of the garden anything additional which will increase him of gold, silver, crops or anything. Increase beyond what is stipulated in the contract is not good. It is also not good for the lender of a qirad to be in this position. If such an increase does enter share-cropping or quirad, it becomes by it hire. It is not good when hire enters it. Hire must never occur in a situation which has uncertainty in it." </p>
        <p> Malik spoke about a man who gave land to another man in a share-cropping contract in which there were palms, vines, or the like of that of fruit trees and there was also uncultivated land in it. He said, "If the uncultivated land is secondary to the fruit trees, either in importance or in size of land, there is no harm in share-cropping. That is if the palms take up two-thirds of the land or more, and the uncultivated land is a third or less. This is because when the land that the fruit trees take up is secondary to the uncultivated land and the cultivated land in which the palms, vines or the like is a third or less, and the uncultivated land is two-thirds or more, it is permitted to rent the land and share-cropping in it is haram." </p>
        <p> "One of the practices of people is to give out sharecropping contracts on property with fruit trees when there is uncultivated land in it, and to rent land while there are fruit trees on it, just as a Qur'an or sword which has some embellishment on it of silver is sold for silver, or a necklace or ring which have stones and gold in them are sold for dinars. These sales continue to be permitted. People buy and sell by them. Nothing described or instituted has come on that which if exceeded, makes it haram, and if fallen below makes it halal. What is done in our community about that is what people practise and permit among themselves. That is, if the gold or silver is secondary to what it is incorporated in, it is permitted to sell it. That is, if the value of the blade, the Qur'an, or the stones is two-thirds or more, and the value of the decoration is one-third or less." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Condition About Slaves in Sharecropping </h3>
      <hr>
      <em> <a name="033.33.2.3"> Book 33, Number 33.2.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya said that Malik said, "The best of what has been heard about a sharecropper stipulating on the owner of the property the inclusion of some slave workers, is that there is no harm in that if they are workers that come with the property. They are like the property. There is no profit in them for the share-cropper except to lighten some of his burden. If they did not come with the property, his toil would be harder. It is like share-cropping land with a spring or land with a watering trough. You will not find anyone who receives the same share for share-cropping two lands which are equal in property and yield, when one property has a constant plentiful spring and the other has a watering trough, because of the lightness of working land with a spring, and the hardship of working land with a watering trough." </p>
        <p> Malik added, "That is what is done in our community." </p>
        <p> Malik said, "A share-cropper cannot employ workers from the property in other work, and he cannot make that a stipulation with the one who gives him the share-cropping contract. Nor is it permitted to one who share-crops to stipulate on the owner of the property inclusion of slaves for use in the garden who are not in it when he makes the share-cropping contract." </p>
        <p> "Nor must the owner of the property stipulate on the one who uses his property for share-cropping that he take any of the slaves of the property and remove him from the property. The share-cropping of property is based on the state which it is currently in." </p>
        <p> "If the owner of the property wants to remove one of the slaves of the property, he removes him before the share-cropping, or if he wants to put someone into the property, he does it before the share-cropping. Then he grants the share-cropping contract after that if he wishes. If any of the slaves die or go off or become ill, the owner of the property must replace them." </p>
      </blockquote>
      <!--EndHadithBody-->
