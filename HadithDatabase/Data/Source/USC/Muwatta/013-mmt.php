      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 13: </a> </em>
        <h1> <em> Asking for Rain </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: How to Ask for Rain </h3>
        <hr>
        <em> <a name="013.13.1.1"> Book 13, Number 13.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr ibn Amr ibn Hazm that he had heard Abbad ibn Tamim say that he had heard Abdullah ibn Zayd al-Mazini say, "The Messenger of Allah, may Allah bless him and grant him peace, came out to the place of prayer and asked for rain, and when he faced the qibla he turned his cloak inside out." </p>
          <p> Malik was asked how many rakas there were in the prayer of asking for rain and he said, "Two rakas, and the imam does the prayer before he gives the khutba. He prays two rakas, and then he gives a khutba and makes dua, facing the qibla and turning his cloak inside out. He recites out loud in both rakas, and when he turns his cloak inside out he puts what is on his right on his left, and what is on his left on his right, and all the people turn their cloaks inside out when the imam does so, and face the qibla, sitting." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About Asking for Rain </h3>
        <hr>
        <em> <a name="013.13.2.2"> Book 13, Number 13.2.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Amr ibn Shuayb that the Messenger of Allah, may Allah bless him and grant him peace, said, when he asked for rain, "O Allah, give water to Your slaves and Your animals, and spread Your mercy, and give life to Your dead land." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="013.13.2.3"> Book 13, Number 13.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Sharik ibn Abdullah ibn Abi Namir that Anas ibn Malik said, "A man came to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Messenger of Allah, our animals are dying and our camels are too weak to travel, so make dua to Allah.' The Messenger of Allah, may Allah bless him and grant him peace, made dua, and it rained on us from one jumua to the next." </p>
          <p> Anas continued, "Then a man came to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Messenger of Allah, our houses have fallen down, the paths are blocked, and our flocks are dying.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'O Allah, (only) the mountain and hill-tops, the valley bottoms, and the places where trees grow.' " </p>
          <p> Anas added, "It cleared away from Madina like a garment being removed." </p>
          <p> Malik said, about a man who missed the prayer of asking for rain but caught the khutba, and wished to pray in the mosque, or in his house when he returned, "He is free to do so, or not, as he wishes." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About Asking the Stars for Rain </h3>
        <hr>
        <em> <a name="013.13.3.4"> Book 13, Number 13.3.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Salih ibn Kaysan from Ubaydullah ibn Abdullah ibn Utba ibn Masud that Zayd ibn Khalid al-Juhani said, "The Messenger of Allah, may Allah bless him and grant him peace, took the subh prayer with us at Hudaybiyya after it had rained in the night. When he had finished he went up to the people and said, 'Do you know what your Lord has said?' They said, 'Allah and His Messenger know best.' He said, 'Some of my slaves have begun the morning believing in Me, and others have begun it rejecting Me. As for those who say, 'We were rained on by the overflowing favour of Allah and His mercy,' they believe in Me and reject the stars. But as for those who say, 'We were rained on by such and such a star, they reject Me and believe in the stars.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="013.13.3.5"> Book 13, Number 13.3.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, used to say, "When a cloud appears from the direction of the sea and then goes towards ash-Sham, it will be an abundant source of rain." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="013.13.3.6"> Book 13, Number 13.3.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Abu Hurayra used to say, when morning came after it had rained on the people, "We have been rained upon by the rain of Allah's opening," and would then recite the ayat, "Whatever Allah opens to man of His mercy no-one can withhold, and whatever He withholds no-one can send forward after Him. " (Sura 35 ayat 2). </p>
        </blockquote>
        <!--EndHadithBody-->
