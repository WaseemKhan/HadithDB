        <p> Yahya related to me from Malik from Ayyub ibn Abi Tamima as-Sakhtayani from Muhammad ibn Sirin from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, finished the prayer after two rakas and Dhu'l-Yadayn said to him, "Has the prayer been shortened or have you forgotten, Messenger of Allah?" The Messenger of Allah, may Allah bless him and grant him peace, said, "Has Dhu'l-Yadayn spoken the truth?"  The people said, "Yes," and the Messenger of Allah, may Allah bless him and grant him peace, stood and prayed the other two rakas and then said, "Peace be upon you." Then he said, "Allah is greater" and went into a sadja as long as his usual prostrations or longer. Then he came up and said, "Allah is greater" and went into a sajda as long as his usual prostrations or longer and then came up. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.16.63"> Book 3, Number 3.16.63: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Da'ud ibn al-Husayn that Abu Sufyan, the mawla of Ibn Abi Ahmad, said that he heard Abu Hurayra say, "The Messenger of Allah, may Allah bless him and grant him peace, prayed asr and said the taslim after two rakas. Dhu'l-Yadayn stood up and said, 'Has the prayer been shortened, Messenger of Allah, or have you forgotten?' The Messenger of Allah, may Allah bless him and grant him peace, stood up and completed what remained of the prayer, and then, remaining sitting after saying the taslim, he made two prostrations." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.16.64"> Book 3, Number 3.16.64: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Abu Bakr ibn Sulayman ibn Abi Hathma said, "I have heard that the Messenger of Allah, may Allah bless him and grant him peace, prayed two rakas of one of the two day-ti me prayers, dhuhr or asr, and said the taslim after two rakas. Dhu'sh-Shamalayn said to him, 'Has the prayer been shortened, Messenger of Allah, or have you forgotten?' The Messenger of Allah, may Allah bless him and grant him peace, said, 'The prayer has not been shortened and I have not forgotten.' Dhu'shShamalayn said, 'It was certainly one of those, Messenger of Allah.' The Messenger of Allah, may Allah bless him and grant him peace, approached the people and said, 'Has Dh u'sh-Shamalayn spoken the truth?' They said, 'Yes, Messenger of Allah,' and the Messenger of Allah, may Allah bless him and grant him peace, completed what remained of the prayer, and then said, 'Peace be upon you.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.16.65"> Book 3, Number 3.16.65: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab, and from Abu Salama ibn Abd ar-Rahman, the same as that. </p>
        <p> Malik said, "Every forgetfulness which decreases from the prayer, prostrations for it come before the greeting, and every forgetfulness which is an addition to the prayer, prostrations for it come after the greeting." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Completing what is Recalled When Uncertain how much has been Prayed </h3>
      <hr>
      <em> <a name="003.3.17.66"> Book 3, Number 3.17.66: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, said, "If you are uncertain in the prayer and do not know whether you have prayed three or four rakas, then pray a raka and make two prostrations from the sitting position before the taslim. If the raka that you prayed was the fifth, then you make it even by these two sajdas, and if it was the fourth, then the two prostrations spite Shaytan." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.17.67"> Book 3, Number 3.17.67: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Umar ibn Muhammad ibn Zayd from Salim ibn Abdullah that Abdullah ibn Umar used to say, "If you are uncertain in the prayer, estimate what you think you have forgotten of the prayer and repeat it, then do the two sajdas of forgetfulness from the sitting position." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.17.68"> Book 3, Number 3.17.68: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Afif ibn Amr as-Sahmi that Ata ibn Yasar said, "I asked Abdullah ibn Amr ibn al-As and Kab al Ahbar about someone who was uncertain in his prayer, and did not know whether he had prayed three or four rakas. Both of them said, 'He should pray another raka and then do two sajdas from the sitting position.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.17.69"> Book 3, Number 3.17.69: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar, when questioned about forgetfulness in the prayer, said, "If you think that you have forgotten part of the prayer, then pray it." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: What to Do if One Stands After the Completion of the Prayer or After Two Rakas </h3>
      <hr>
      <em> <a name="003.3.18.70"> Book 3, Number 3.18.70: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from al-Araj that Abdullah ibn Buhayna said, "The Messenger of Allah, may Allah bless him and grant him peace, prayed two rakas with us and then stood without sitting and the people stood with him. When he had finished the prayerand we had seen him say the taslim, he said 'Allah is greater' and did two sajdas from the sitting position and then said the taslim again." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.18.71"> Book 3, Number 3.18.71: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said from Abd ar-Rahman ibn Hurmuz that Abdullah ibn Buhayna said, "The Messengerof Allah, may Allah bless him and grant him peace, prayed dhuhr with us and he stood straight up after two rakas without sitting. When he had finished the prayer, he did two sajdas and then said the taslim after that." </p>
        <p> Malik said, concerning someone who forgot in his prayer and stood up after he had completed four rakas and recited and then went into ruku and then, when he raised his head from ruku, remembered that he had already completed (his prayer), "He returns to a sitting position and does not do any sajda. If he has already done one sajda I do not think he should do the other. Then when his prayer is finished he does two sajdas from the sitting position after saying the taslim." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Looking in the Prayer at What Distracts You from It </h3>
      <hr>
      <em> <a name="003.3.19.72"> Book 3, Number 3.19.72: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from AIqama ibn Abi AIqama from his mother that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "Abu Jahm ibn Hudhayfa gave the Messenger of Allah, may Allah bless him and grant him peace, a fine striped garment from Syria and he did the prayer in it. When he had finished he said, 'Give this garment back to Abu Jahm. I lookedat its stripes in the prayer and they almost distracted me.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.19.73"> Book 3, Number 3.19.73: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik related to me from Hisham ibn Urwa from his father that the Messenger of Allah, may Allah bless him and grant him peace, wore a fine striped garment f rom Syria, and then gave it to Abu Jahm and took a plain, rough, garment in return. Abu Jahm asked, "Messenger of Allah! Why?" He said, "I looked at its stripes in the prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.19.74"> Book 3, Number 3.19.74: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik related to me from Abdullah ibn Abi Bakr that Abu Talha al-Ansari was praying in his garden when a wild pigeon flew in and began to fly to and fro trying to find a way out. The sight was pleasing to him and he let his eyes follow the bird for a time and then he went back to his prayer but could not remember how much he had prayed. He said, "A trial has befallen me in this property of mine." So he came to the Messenger of Allah, may Allah bless him and grant him peace, and mentioned the trial that had happened to him in his garden and said, "Messenger of Allah, it is a sadaqa for Allah, so dispose of it wherever you wish." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.19.75"> Book 3, Number 3.19.75: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr that a man from the Ansar was praying in a garden of his in Quff, one of the valleys of Madina, during the date season and the palms' branches were weighed down with fruit on all sides. He looked at them and what he saw of their fruits amazed him. Then he went back to his prayer and he did not know how much he had prayed. He said, "A trial has befallen me in this property of mine." So he went toUthman ibn Affan, who was the khalifa at the time, and mentioned it to him and said, "It is sadaqa, so give it away in the paths of good." Uthman ibn Affan sold it for fifty thousand and so that property became known as the Fifty. </p>
      </blockquote>
