      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 3: </a> </em>
      <h1> <em> Prayer </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: The Call to Prayer </h3>
      <hr>
      <em> <a name="003.3.1.1"> Book 3, Number 3.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Yahya ibn Said said, "The Messenger of Allah, may Allah bless him and grant him peace, had wanted to take two pieces of wood to strike them together to gather people for the prayer, and Abdullah ibn Zayd al-Ansari, then of the tribe of Harith ibn al-Khazraj, was shown two pieces of wood in his sleep. He said, 'These are close to what the Messenger of Allah, may Allah bless him and grant him peace, wants.'  Then it was said, 'Do you not call to the prayer?', so when he woke up he went to the Messenger of Allah, may Allah bless him and grant him peace, and mentioned the dream to him. The Messenger of Allah, may Allah bless him and grant him peace, ordered the adhan." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.2"> Book 3, Number 3.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Ata ibn Yazid al-Laythi from Abu Said al-Khudri that the Messenger of Allah, may Allah bless him and grant him peace, said, "When you hear the adhan, repeat what the muadhdhin says." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.3"> Book 3, Number 3.1.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr ibn Abd ar-Rahman, from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "If people knew what was in the adhan and the first row of the prayer and could only draw lots for it, they would draw lots. And if they knew what was in doing dhuhr early, they would race each other to it. And if they knew what was in isha and subh, they would go to them even if they had to crawl." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.4"> Book 3, Number 3.1.4: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from al-Ala ibn Abd ar-Rahman ibn Yaqub from his father and Ishaq ibn Abdullah that they informed him that they heard Abu Hurayra say, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'When the iqama is called for prayer, do not come to it running, but come with calmness. Pray what you catch and complete what you miss. You are in prayer as long as your aim is the prayer.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.5"> Book 3, Number 3.1.5: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abd arRahman ibn Abdullah ibn Abd ar-Rahman ibn Abu Sasaca al-Ansari, and later al-Mazini, that his father told him that Abu Said al-Khudri had said to him, "I see that you love sheep and the desert. When you are among your sheep or in your desert, call the prayer and raise your voice in the adhan, because I heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'No jinn or man oranything within range hears the voice of the muadhdhin except that it bears witness for him on the day of rising.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.6"> Book 3, Number 3.1.6: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "When the call to prayer is made Shaytan retreats, passing wind, so that he will not hear the adhan. When the adhan is completed he comes back, until, when the iqama is said, he retreats again. When the iqama is completed, he comes back, until he comes between a man and his self and says, 'Think of such and such, think of such and such,' which he was not thinking about before, until the man does not know how much he has prayed." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.7"> Book 3, Number 3.1.7: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu Hazim ibn Dinar that Sahl ibn Sad as-Saidi said, "There are two times when the gates of heaven are opened, and few who make supplication have it returned to them unanswered. They are at the timeof the adhan, and in a rank of people fighting in the way of Allah." </p>
        <p> Malik was asked whether the adhan on the day of jumua was called before the time had come for the prayer and he said, "It is not called until after the sun has passed the meridian." </p>
        <p> Malik was asked about doubling the adhan and the iqama, and at what point people had to stand when the iqama for the prayer was called. He said, "I have heard nothing about the adhan and iqama except what I have seen people do. As for the iqama, it is not doubled. That is what the people of knowledge in our region continue to do. As for people standing up when the iqama for the prayer is called, I have not heard of any definite point at which it is begun, and I consider it rather to be according to people's (individual) capacity, for some people are heavy and some are light, and they are not able to be as one man ." </p>
        <p> Malik was asked about a gathering of people who wished to do the prescribed prayer calling the iqama and not the adhan, and he said, "lt is enough for them. The adhan is only obligatory in mosques where the prayer is said in congregation." </p>
        <p> Malik was asked about the muadhdhin saying "Peace be upon you" to the imam and calling him to the prayer, and he was asked who was the first person to whom such a greeting was made. He replied, "I have not heard that this greeting occurred in the first community." </p>
        <p> Yahya said that Malik was asked whether a muadhdhin who called the people to prayer and then waited to see if anyone would come and no one did, so he said the iqama and did the prayer by himself and then people came after he had finished, should repeat the prayer with them. Malik said, "He does not repeat the prayer, and whoever comes after he has finished should do the prayer by himself." </p>
        <p> Yahya said that Malik was asked about a muadhdhin who called the adhan for a group of people, did voluntary prayers, and then the group of people wanted to do the prayer with some one else saying the iqama. He said, "There is no harm in that. His iqama or somebody else's are the same." </p>
        <p> Yahya said that Malik said, "The subh prayer is still called before dawn. As for the other prayers, we believe that they should only be called after the time has started." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.8"> Book 3, Number 3.1.8: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that the muadhdhin came to Umar ibn al-Khattab to call him to the subh prayer and found him sleeping, so he said, "Prayer is better than sleep," and Umar ordered him to put that in the adhan for subh . </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.9"> Book 3, Number 3.1.9: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from his paternal uncle Abu Suhayl ibn Malik that his uncle's father said, "I recognise nothing nowadays of what I saw the people (i.e. the companions of the Messenger, may Allah bless him and grant him peace ) doing except the call to prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.1.10"> Book 3, Number 3.1.10: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Abdullah ibn Umar heard the iqama while he was in Baqi, so he increased his pace of walking to the mosque. </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Adhan During a Journey and Without Wudu </h3>
      <hr>
      <em> <a name="003.3.2.11"> Book 3, Number 3.2.11: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar called the adhan on a cold and windy night and included the phrase, "Do the prayer in shelter." Then he said, "The Messenger of Allah, may Allah bless him and grant him peace, used to orderthe muadhdhin to say, 'Do the prayerin shelter' when it was a cold, rainy night " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.2.12"> Book 3, Number 3.2.12: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that on a journey Abdullah ibn Umar did no more than the iqama, except for subh, when he called both the adhan and the iqama. Abdullah ibn Umar used to say, "The adhan is for an imam whom people join ." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.2.13"> Book 3, Number 3.2.13: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa that his father said to him, "When you are on a journey you can, if you wish, call the adhan and the iqama, or, if you wish, the iqama and not the adhan." </p>
        <p> Yahya said that he heard Malik say, "There is no harm in a man calling the adhan while riding." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.2.14"> Book 3, Number 3.2.14: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab used to say, "Whoever prays on waterless, desolate land - an angel prays on his right and an angel prays on his left. When he calls both the adhan and the iqama for the prayer, or calls out the iqama, angels like mountains pray behind him." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Duration of the Meal before Dawn (Sahur) in Relation to the Adhan </h3>
      <hr>
      <em> <a name="003.3.3.15"> Book 3, Number 3.3.15: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abdullah ibn Dinar from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Bilal calls the adhan whilst it is still night so eat and drink until Ibn Umm Maktum calls the adhan." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Adhan During a Journey and Without Wudu </h3>
      <hr>
      <em> <a name="003.3.4.16"> Book 3, Number 3.4.16: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Salim ibn Abdullah that the Messenger of Allah, may Allah bless him and grant him peace, said, "Bilal calls the adhan in the night, so eat and drink until Ibn Umm Maktum calls the adhan." Ibn Umm Maktum was a blind man who did not call the adhan until someone said to him, "The morning has come. The morning has come." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Opening of the Prayer </h3>
      <hr>
      <em> <a name="003.3.5.17"> Book 3, Number 3.5.17: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Salim ibn Abdullah from Abdullah ibn Umar that the Messengerof Allah, may Allah bless him and grant him peace, used to raise his hands to the level of his shoulders when he began the prayer and when he raised his head from the ruku he raised them in the same way, saying, "Allah hears whoever praises him, our Lord and praise belongs to You." He did not raise them in the sujud. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.5.18"> Book 3, Number 3.5.18: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that AIi ibn Husayn ibn Ali ibn Abi Talib said, "The Messenger of Allah, may Allah bless him and grant him peace, used to say, 'Allah is greater' whenever he lowered himself and raised himself, and he continued to pray like that until he met Al lah ." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.5.19"> Book 3, Number 3.5.19: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Sa'id from Sulayman ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, used to raise his hands in the prayer. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.5.20"> Book 3, Number 3.5.20: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Abu Salama ibn Abdar-Rahman ibn Awf that Abu Hurayra used to lead them in prayer and would say "Allah is greater" whenever he lowered himself and raised himself. When he had finished he would say, "By Allah, I am the person whose prayer most resembles the prayer of the Messenger of Allah, may Allah bless him and grant him peace." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.5.21"> Book 3, Number 3.5.21: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Salim ibn Abdullah that Abdullah ibn Umar used to say "Allah is greater" in the prayer whenever he lowered himself and raised himself. </p>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to raise his hands to the level of his shoulders when he began the prayer and when he raised his head from the ruku he would raise them less than that. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.5.22"> Book 3, Number 3.5.22: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu Nuaym Wahb ibn Kaysan that Jabir ibn Abdullah used to teach them the takbir in the prayer. Abu Nuaym said, "He used to tell us to say 'Allah is greater' whenever we lowered or raised ourselves." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.5.23"> Book 3, Number 3.5.23: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Ibn Shihab used to say, "When a man catches the raka he says, 'Allah is greater' once, and that takbir is enough for him." </p>
        <p> Malik added, "That is if he intended to begin the prayer by that takbir " </p>
        <p> Malik was asked about a man who began with the imam but forgot the opening takbir and the takbir of the ruku until he had done one raka. Then he remembered that he had not said the takbir at the opening nor in the ruku,so he said the takbir in the second raka. He said, "I prefer that he start his prayer again, but if he forgets the opening takbir with the imam and says the takbir in the first ruku, I consider that enough for him if he intends by it the opening takbir." </p>
        <p> Malik said, about some one who prayed by himself and forgot the opening takbir, "He begins his prayer afresh ." </p>
        <p> Malik said, about an imam who forgot the opening takbir until he had finished his prayer, "I think that he should do the prayer again, and those behind him, even if they have said the takbir." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Recitation of Qur'an in the Maghrib and lsha Prayers </h3>
      <hr>
      <em> <a name="003.3.6.24"> Book 3, Number 3.6.24: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Muhammad ibn Jubayr ibn Mutim that his father said, "I heard the Messenger of Allah, may Allah bless him and grant him peace, recite at-Tur (Sura 52) in the maghrib prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.6.25"> Book 3, Number 3.6.25: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Ubaydullah ibn Abdullah ibn Utba ibn Masud from Abdullah ibn Abbas that Umm al-Fadl bint al-Harith heard him reciting al Mursalat (sura 77) and she said to him, "My son, you have reminded me by reciting this sura that it was what I last heard the Messenger of Allah, may Allah bless him and grant him peace, recite in the maghrib prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.6.26"> Book 3, Number 3.6.26: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> 26 Yahya related to me from Malik from Abu Ubayd, the mawla of Sulayman ibn Abd alMalik, from Ubada ibn Nusayy from Qays ibn al Harith that Abu Abdullah as-Sunabihi said, "I arrived in Madina in the khalifate of Abu Bakr as-Siddiq, and I prayed maghrib behind him. He recited the umm al Qur'an and two suras from the shorter ones of the mufassal in the first two rakas. Then he stood up in the third and I drew so near to him that my clothes were almost touching his clothes. I heard him reciting the umm al-Qur'an and this ayat, 'Our Lord, do not make our hearts go astray after You have guided us, and give us mercy from Your presence. Surely You are the Giver. ' " (Sura 3 ayat 8) </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="003.3.6.27"> Book 3, Number 3.6.27: </a> </em>
