      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 56: </a> </em>
        <h1> <em> Speech </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Disliked Speech </h3>
        <hr>
        <em> <a name="056.56.1.1"> Book 56, Number 56.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "If a man says to his muslim brother, 'O kafir!' it is true about one of them." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.1.2"> Book 56, Number 56.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Suhayl ibn Abu Salih from his father from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "When you hear a man say, 'The people are ruined,' he himself is the most ruined of them all." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.1.3"> Book 56, Number 56.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Let none of you complain about time, for Allah is time.' </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.1.4"> Book 56, Number 56.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said that Isa ibn Maryam encountered a pig on the road. He said to it, "Go in peace." Somebody asked, "Do you say this to a pig?" Isa said, "I fear lest I accustom my tongue to evil speech." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Order to be Mindful in Speech </h3>
        <hr>
        <em> <a name="056.56.2.5"> Book 56, Number 56.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Muhammad ibn Amr ibn Alqama from his father from Bilal ibn al-Harith al-Muzani that the Messenger of Allah, may Allah bless him and grant him peace, said, "A man speaks what is pleasing to Allah and he does not suspect that it will have the result that it does, and Allah will write for him His good pleasure for it until the day when he meets Him. And a man speaks what excites the wrath of Allah and he does not suspect that it will have the result that it does, and Allah will write His wrath for him for it until the day when he meets Him." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.2.6"> Book 56, Number 56.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar that Abu Salih as-Samman informed him that Abu Hurayra said, "Truly a man utters words to which he attaches no importance, and by them he falls into the fire of Jahannam, and truly a man utters words to which he attaches no importance, and by them Allah raises him into the Garden." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Disliked Speech and Speech Without the Mention of Allah </h3>
        <hr>
        <em> <a name="056.56.3.7"> Book 56, Number 56.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Zayd ibn Aslam that Abdullah ibn Umar said, "Two men from the east stood up and spoke, and people were amazed at their eloquence. The Messenger of Allah, may Allah bless him and grant him peace, said, 'Some eloquence is sorcery,' or he said, 'Part of eloquence is sorcery.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.3.8"> Book 56, Number 56.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Isa ibn Maryam used to say, "Do not speak much without the mention of Allah for you will harden your hearts. A hard heart is far from Allah, but you do not know. Do not look at the wrong actions of people as if you were lords. Look at your wrong actions as if you were slaves. Some people are afflicted by wrong action and some people are protected from it. Be merciful to the people of affliction and praise Allah for His protection." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.3.9"> Book 56, Number 56.3.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, sent a message to one of her family after the evening prayer, saying, "Will you not allow the recording angel to rest?" </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Backbiting </h3>
        <hr>
        <em> <a name="056.56.4.10"> Book 56, Number 56.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from al-Walid ibn Abdullah ibn Sayyad that al-Muttalib ibn Abdullah ibn Hantab al-Makhzumi informed him that a man asked the Messenger of Allah, may Allah bless him and grant him peace, "What is backbiting?" The Messenger of Allah, may Allah bless him and grant him peace, said, "It is to mention about a man what he does not want to hear." He said, "Messenger of Allah! Even if it is true?" The Messenger of Allah, may Allah bless him and grant him peace, said, "If you utter something false, then it is slander." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What is Feared from the Tongue </h3>
        <hr>
        <em> <a name="056.56.5.11"> Book 56, Number 56.5.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whomever Allah protects from the evil of two things will enter the Garden." A man said, "Messenger of Allah, do not tell us!" The Messenger of Allah, may Allah bless him and grant him peace, was silent. Then the Messenger of Allah, may Allah bless him and grant him peace, repeated what he had said the first time. The man said to him, "Do not tell us, Messenger of Allah!" The Messenger of Allah, may Allah bless him and grant him peace, was silent. Then the Messenger of Allah, may Allah bless him and grant him peace, said the same thing again. The man said, "Do not tell us, Messenger of Allah!" Then the Messenger of Allah, may Allah bless him and grant him peace, said the same thing again. Then the man began to say what he had said previously and a man at his side silenced him. The Messenger of Allah, may Allah bless him and grant him peace, said, "Whomever Allah protects from the evil of two things will enter the Garden. They are what is between his jaws and what is between his legs, what is between his jaws and what is between his legs, what is between his jaws and what is between his legs." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.5.12"> Book 56, Number 56.5.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Zayd ibn Aslam from his father that Umar ibn al-Khattab came upon Abu Bakr as-Siddiq pulling his tongue. Umar said to him, "Stop, may Allah forgive you!" Abu Bakr replied, "This has brought me to dangerous places." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Two People Conversing to the Exclusion of Another </h3>
        <hr>
        <em> <a name="056.56.6.13"> Book 56, Number 56.6.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar saying, "Abdullah ibn Umar and I were at the house of Khalid ibn Uqba who was away at the market. A man came who wanted to speak to Abdullah ibn Umar and I was the only other person present Abdullah ibn Umar called another man so that we were four and said to me and the man whom he had called, 'Go a little way off together, because I heard the Messenger of Allah, may Allah bless him and grant him peace, say, "Two do not converse secretly to the exclusion of another.' "' </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.6.14"> Book 56, Number 56.6.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Two must not converse secretly to the exclusion of another person." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Truthfulness and Lying </h3>
        <hr>
        <em> <a name="056.56.7.15"> Book 56, Number 56.7.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Safwan ibn Sulaym that a man asked the Messenger of Allah, may Allah bless him and grant him peace, "Can I lie to my wife, Messenger of Allah?" The Messenger of Allah, may Allah bless him and grant him peace, said, "There is no good in lying." The man said, "Messenger of Allah! Shall I make her a promise and tell her?" The Messenger of Allah, may Allah bless him and grant him peace, said, "It will not be held against you." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.7.16"> Book 56, Number 56.7.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Abdullah ibn Masud used to say, "You must tell the truth. Truthfulness leads to right action. Right action leads to the Garden. Beware of lying. Lying leads to corruption, and corruption leads to the Fire. Don't you see that it is said, 'He speaks the truth and acts rightly,' and, 'He lies and is corrupt.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.7.17"> Book 56, Number 56.7.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that someone said to Luqman, "What has brought you to what we see?" meaning his high rank. Luqman said, "Truthful speech, fulfilling the trust, and leaving what does not concern me." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.7.18"> Book 56, Number 56.7.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Abdullah ibn Masud used to say, "The slave continues to lie and a black spot grows in his heart until all his heart becomes black. Then he is written, in Allah's sight, among the liars." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.7.19"> Book 56, Number 56.7.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Safwan ibn Sulaym said, "The Messenger of Allah, may Allah bless him and grant him peace, was asked, 'Can the mumin be a coward?' He said, 'Yes.' He was asked, 'Can the mumin be a miser?' He said, 'Yes.' He was asked, 'Can the mumin be a liar?' He said, 'No.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Squandering Property and Being Two-faced </h3>
        <hr>
        <em> <a name="056.56.8.20"> Book 56, Number 56.8.20: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Suhayl ibn Abi Salih from his father from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Allah is pleased with three things from you, and He is angry with three things from you. He is pleased that you worship Him and do not associate anything with Him, and that you take hold of the rope of Allah altogether, and that you give good counsel to the one to whom Allah gives command over you. He is angry with you for gossip, squandering property, and asking too many questions." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.8.21"> Book 56, Number 56.8.21: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "One of the most evil of people is the two-faced person who shows one face to these people and another face to those people." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Punishing the Many for the Actions of the Few </h3>
        <hr>
        <em> <a name="056.56.9.22"> Book 56, Number 56.9.22: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he had heard that Umm Salama, the wife of the Prophet, may Allah bless him and grant him peace, said, "Messenger of Allah! Shall we be destroyed while there are people who are salih among us?" The Messenger of Allah, may Allah bless him and grant him peace, said, "Yes, if there is much wickedness." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.9.23"> Book 56, Number 56.9.23: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Ismail ibn Abi Hakim heard Umar ibn Abd al-Aziz say, "Some say that Allah the Blessed, the Exalted, will not punish the many for the wrong action of the few. However, when the objectionable action is committed openly, then they all deserve to be punished." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About People with Taqwa </h3>
        <hr>
        <em> <a name="056.56.10.24"> Book 56, Number 56.10.24: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ishaq ibn Abdullah ibn Abi Talha that Anas ibn Malik said, "I heard Umar ibn al-Khattab, when I was going out with him to visit an orchard, say to himself, and there was a wall between him and me and he was inside the garden, 'Umar ibn al-Khattab, amir al-muminin! Well done! Well done! By Allah, fear Allah or he will punish you.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.10.25"> Book 56, Number 56.10.25: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said, "I heard that al-Qasim ibn Muhammad used to say, 'I have seen the people, (i.e. the companions), and they were not impressed by speech.' " Malik said, "He meant that only action and deeds would be looked at, not words." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What to Say when it Thunders </h3>
        <hr>
        <em> <a name="056.56.11.26"> Book 56, Number 56.11.26: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Amir ibn Abdullah ibn az-Zubayr would stop speaking when he heard thunder and say, "Glory be to Allah whom the thunder glorifies with His praise and the angels from the fear of Him." (Subhana-aladhee yusabihu ar-radu bi hamdihi wa mala'ikatu min khiyfatihi.) Then he would say, "This is a severe warning to the people of the earth." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Legacy of the Prophet, may Allah Bless Him and Grant Him Peace </h3>
        <hr>
        <em> <a name="056.56.12.27"> Book 56, Number 56.12.27: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Urwa ibn az-Zubayr from A'isha, umm al-muminin that when the Messenger of Allah, may Allah bless him and grant him peace, died, the wives of the Prophet, may Allah bless him and grant him peace, wanted to send Uthman ibn Affan to Abu Bakr as-Siddiq to ask him about their inheritance from the Messenger of Allah, may Allah bless him and grant him peace A'isha said to them, "Didn't the Messenger of Allah, may Allah bless him and grant him peace, say, 'No one inherits from us. What we leave is sadaqa.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="056.56.12.28"> Book 56, Number 56.12.28: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "My inheritance is not divided up by the dinar. What I leave apart from the maintenance of my wives and provision for my servant is sadaqa." </p>
        </blockquote>
        <!--EndHadithBody-->
