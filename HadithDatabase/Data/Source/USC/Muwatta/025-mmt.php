      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 25: </a> </em>
        <h1> <em> Game </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Eating Game Killed with Throwing Sticks and by Stones </h3>
        <hr>
        <em> <a name="025.25.1.1"> Book 25, Number 25.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> ahya related to me from Malik that Nafi said, "I was at al-Juruf (near Madina) and threw a stone at two birds, and hit them. One of them died, and Abdullah ibn Umar threw it away, and then went to slaughter the other one with an adze. It died before he could slaughter it, so Abdullah threw that one away as well." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.1.2"> Book 25, Number 25.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that al-Qasim ibn Muhammad disapproved of eating game that had been killed with throwing sticks and by clay pellets. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.1.3"> Book 25, Number 25.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab disapproved of killing domestic animals that had become wild by any means that game was slain such as arrows and the like. </p>
          <p> Malik said, "I do not see any harm in eating game which is pierced by a throwing stick in a vital organ. </p>
          <p> Allah, the Blessed, the Exalted! said, 'Oh you who believe! Allah will surely try you with something of the game that your hands and spears attain.' " (Sura 5 ayat 97). </p>
          <p> Yahya said, "Any game that man obtains by his hand or by his spear or by any weapon which pierces it and reaches a vital organ, is acceptable as Allah, the Exalted, has said." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.1.4"> Book 25, Number 25.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard the people of knowledge say that when a man hit game and something else might have contributed to death, like water or an untrained dog, that game was not to be eaten unless it was beyond doubt that it was the arrow of the hunter that had killed it by reaching a vital organ, so that it did not have any life after that. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.1.5"> Book 25, Number 25.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say that there was no harm in eating game when you did not see it die if you found the mark of your dog on it or your arrow in it as long as it had not remained overnight. If it had remained overnight, then it was disapproved of to eat it. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Game Caught by Trained Dogs </h3>
        <hr>
        <em> <a name="025.25.2.5a"> Book 25, Number 25.2.5a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar said about a trained dog, "Eat whatever it catches for you whether it eats from it or not." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.2.6"> Book 25, Number 25.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard Nafi say that Abdullah ibn Umar said, "Whether it eats from it or not." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.2.7"> Book 25, Number 25.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Sad ibn Abi Waqqas had said, when asked about a trained dog killing game, "Eat, even if only one piece of it remains." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.2.8"> Book 25, Number 25.2.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard some of the people of knowledge say that when falcons, eagles, and hawks and their like, understood as trained dogs understood, there was no harm in eating what they had killed in the course of hunting, if the name of Allah had been mentioned when they were sent out. </p>
          <p> Malik said, "The best of what I have heard about retrieving game from the falcon's talons or from the dog's fangs and then waiting until it dies, is that it is not halal to eat it." </p>
          <p> Malik said, "The same applies to anything which could have been slaughtered by the hunter when it was in the talons of the falcon or the fangs of the dog. If the hunter leaves it until the falcon or dog has killed it, it is not halal to eat it either". He continued, "The same thing applies to any game hit by a hunter and caught while still alive, which he neglects to slaughter before it dies." </p>
          <p> Malik said, "It is generally agreed among us that it is halal to eat the game that a hunting-dog belonging to magians hunts or kills, if it is sent out by a muslim and the animal is trained. There is no harm in it even if the muslim does not actually slaughter it. </p>
          <p> It is the same as a muslim using a magian's knife to slaughter with or using his bow and arrows to shoot and kill with. The game he shot and the animal he slaughters are halal. There is no harm in eating them. If a magian sends out a muslim's hunting dog for game, and it catches it, the game is not to be eaten unless it is slaughtered by a muslim. That is like a magian using a muslim's bow and arrow to hunt game with, or like his using a muslim's knife to slaughter with. It is not halal to eat anything killed like that. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Game of the Sea </h3>
        <hr>
        <em> <a name="025.25.3.9"> Book 25, Number 25.3.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abd ar-Rahman ibn Abi Hurayra asked Abdullah ibn Umar about eating what was cast up by the sea and he forbade him to eat it. Then Abdullah turned and asked for a Qur'an, and read, "The game of the sea and its flesh are halal for you." Nafi added, "Abdullah ibn Umar sent me to Abdar-Rahman Ibn Abi Hurayra to say that there was no harm in eating it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.3.10"> Book 25, Number 25.3.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam that Sad al-Jari, the mawla of Umar ibn al-Khattab asked Abdullah ibn Umar about fish which had killed each other or which had died from severe cold . He said, "There is no harm in eating them.'' Sad said,' 'I then asked Abdullah ibn Amr ibn al As and he said the same." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.3.11"> Book 25, Number 25.3.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from Abu Salama ibn Abd ar-Rahman from Abu Hurayra and Zayd ibn Thabit that they saw no harm in eating what was cast up by the sea. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.3.12"> Book 25, Number 25.3.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from Abu Salama ibn Abd ar-Rahman that some people from al-Jar came to Marwan ibn al-Hakam and asked him about eating what was cast up by the sea. He said, "There is no harm in eating it." Marwan said, "Go to Zayd ibn Thabit and Abu Hurayra and ask them about it, then come to me and tell me what they say." They went to them and asked them, and they both said, "There is no harm in eating it " They returned to Marwan and told him. Marwan said, "I told you." </p>
          <p> Malik said that there was no harm in eating fish caught by magians, because the Messenger of Allah, may Allah bless him and grant him peace, said, "In the sea's water is purity, and that which is dead in it is halal. " </p>
          <p> Malik said, "If it is eaten when it is dead, there is no harm in who catches it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Prohibition Against Eating Animals with Fangs </h3>
        <hr>
        <em> <a name="025.25.4.13"> Book 25, Number 25.4.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shibab from Abu Idris al-Khawlani from Abu Tha~laba al-Khushani that the Messenger of Allah, may Allah bless him and grant him peace, said, "It is haram to eat animals with fangs " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.4.14"> Book 25, Number 25.4.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ismail ibn Abi Hakim from Abiyda ibn Sufyan al-Hadrami from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Eating animals with fangs is haram. " </p>
          <p> Malik said, "This is the custom among us." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What is Disapproved of Regarding Eating Riding Animals </h3>
        <hr>
        <em> <a name="025.25.5.15"> Book 25, Number 25.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that the best of what he had heard about horses, mules, and donkeys was that they were not eaten because Allah, the Blessed, the Exalted,said, "And horses, and mules and asses, for you to ride, and as an adornment. " (Sura 16 ayat 8) . He said, may He be Blessed and Exalted, "In cattle, some of them you ride, and some of them you eat." (Sura 6 ayat 79). He said, the Blessed, the Exalted, "Mention Allah's name over what He has provided you of cattle, and eat of them and feed the beggar (al-qani) and the suppliant (al-mutarr). (Sura 22 ayat 34). </p>
          <p> Malik said "Allah mentioned horses, mules, and donkeys for riding and adornment, and He mentioned cattle for riding and eating." </p>
          <p> Malik said, "Al-qani also means the poor." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Using the Skin of Animals Found Dead </h3>
        <hr>
        <em> <a name="025.25.6.16"> Book 25, Number 25.6.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Ubaydullah ibn Abdullah ibn Utba ibn Masud that Abdullah ibn Abbas said, "The Messenger of Allah, may Allah bless him and grant him peace, passed by a dead sheep which had been given to a mawla of his wife, Maimuna. He said, ' Aren't you going to use its skin?' They said, 'Messenger of Allah, but it is carrion. 'The Messenger of Allah, may Allah bless him and grant him peace, said, 'Only eating it is haram.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.6.17"> Book 25, Number 25.6.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Zayd ibn Aslam from Ibn Wala al-Misri from Abdullah ibn Abbas that the Messenger of Allah, may Allah bless him and grant him peace, said, "A skin when it is tanned is pure." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="025.25.6.18"> Book 25, Number 25.6.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yazid ibn Abdullah ibn Qusayt from Muhammad ibn Abd ar-Rahman ibn Thawban from his mother that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said that the Messenger of Allah, may Allah bless him and grant him peace, ordered that the skins of carrion be used after they had been tanned. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Eating Carrion when Forced to, out of Necessity </h3>
        <hr>
        <em> <a name="025.25.7.19"> Book 25, Number 25.7.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that the best of what he had heard about a man who is forced by necessity to eat carrion is that he ate it until he was full and then he took provision from it. If he found something which would enable him to dispense with it, he threw it away. </p>
          <p> Malik when asked whether or not a man who had been forced by necessity to eat carrion, should eat it when he also found the fruit, crops or sheep of a people in that place, answered, "If he thinks that the owners of the fruit, crops, or sheep will believe his necessity so that he will not be deemed a thief and have his hand cut off, then I think that he should eat from whatever he finds that which will remove his hunger but he should not carry any of it away. I prefer that he does that than that he eat carrion. If he fears that he will not be believed, and will be deemed a thief for what he has taken, then I think that it is better for him to eat the carrion, and he has leeway to eat carrion in this respect. Even so, I fear that someone who is not forced by necessity to eat carrion might exceed the limits out of a desire to consume other peoples' property, crops or fruit." </p>
          <p> Malik said, "That is the best of what I have heard." </p>
        </blockquote>
        <!--EndHadithBody-->
