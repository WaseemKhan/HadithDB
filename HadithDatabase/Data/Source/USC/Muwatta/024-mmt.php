      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 24: </a> </em>
        <h1> <em> Slaughtering Animals </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Saying the Name of Allah over the Slaughtered Animal </h3>
        <hr>
        <em> <a name="024.24.1.1"> Book 24, Number 24.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa that his father said, "The Messenger of Allah, may Allah bless him and grant him peace, was asked, 'Messenger of Allah! Some people from the desert bring us meat, and we do not know whether the name of Allah has been mentioned over it or not.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'Mention the name of Allah over it and eat.' " </p>
          <p> Malik said, "That was in the beginning of Islam." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="024.24.1.2"> Book 24, Number 24.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Abdullah ibn Ayyash ibn Abi Rabia al-Makhzumi ordered one of his slaves to slaughter an animal. When he wanted to slaughter it, he said to him, "Mention Allah's name." The slave said to him, "I have mentioned the name!" He said to him, "Mention the name of Allah, bother you!" He said to him, "I have mentioned the name of Allah " Abdullah ibn Ayyash said, "By Allah, I shall never eat it!" </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Methods of Slaughter Permitted in Necessity </h3>
        <hr>
        <em> <a name="024.24.2.3"> Book 24, Number 24.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that a man from the Ansar of the tribe of Banu Haritha was herding a pregnant she-camel at Uhud. It was about to die, so he slaughtered it with a sharp stake. The Messenger of Allah was asked about that, and he said, "There is no harm in it, eat it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="024.24.2.4"> Book 24, Number 24.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from a man of the Ansar from Muadh ibn Sad or Sad ibn Muadh that a slave-girl of Kab ibn Malik was herding some sheep at Sal (a mountain near Madina). One of the sheep was about to die, so she went over to it and slaughtered it with a stone. The Messenger of Allah, may Allah bless him and grant him peace, was asked about that, and he said, "There is no harm in it, so eat it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="024.24.2.5"> Book 24, Number 24.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Thawr ibn Zayd ad-Dili that Abdullah ibn Abbas was asked about animals slaughtered by the </p>
          <p> Christian Arabs. He said, "There is no harm in them," but he recited this ayat, "Whoever takes them as friends is from them." (Sura 5 ayat 54). </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="024.24.2.6"> Book 24, Number 24.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Abdullah ibn Abbas used to say, "You can eat anything that has had its jugular vein cut." </p>
          <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab said, "There is no harm in whatever you slaughter with a cutting edge, as long as you are forced to do it by necessity." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What is Disapproved of in Slaughtering Animals </h3>
        <hr>
        <em> <a name="024.24.3.7"> Book 24, Number 24.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Abu Murra, the mawla of Aqil ibn Abi Talib asked Abu Hurayra about a sheep which was slaughtered and then part of it moved. He ordered him to eat it. Then he asked Zayd ibn Thabit about it, and he said, "Does a corpse move?" and he forbade eating its meat. </p>
          <p> Malik was asked about a sheep which fell down and injured itself badly and then its master reached it and slaughtered it. Blood flowed from it but it did not move. Malik said, "If he kills it and blood flows from it and its eyes blink, he should eat it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Slaughtering What is in the Womb of a Slaughtered Animal </h3>
        <hr>
        <em> <a name="024.24.4.8"> Book 24, Number 24.4.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar said, "When a she-camel is slaughtered, what is in its womb is included in the slaughter if it is perfectly formed and its hair has begun to grow. If it comes out of its mother's womb, it is slaughtered so that blood flows from its heart." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="024.24.4.9"> Book 24, Number 24.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yazid ibn Abdullah ibn Qusayt al-Laythi that Said ibn al- Musayyab said, "The slaughter of what is in the womb is included in the slaughter of the mother if it is perfectly formed and its hair has begun to grow." </p>
        </blockquote>
        <!--EndHadithBody-->
