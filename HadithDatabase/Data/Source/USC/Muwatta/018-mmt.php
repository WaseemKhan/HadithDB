        <h3> Section: Making Up Days Missed in Ramadan, and the Kaffara </h3>
        <hr>
        <em> <a name="018.18.17.44"> Book 18, Number 18.17.44: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from his brother Khalid ibn Aslam that Umar ibn al-Khattab once broke thefast on a cloudy day thinking that evening had come and the sun had set. Then a man came to him and said, "Amir al-muminin, the sun has come out,'' and Umar said, "That's an easy matter. It was our deduction (ijtihad)." </p>
          <p> Malik said, "According to what we think, and Allah knows best, what he was referring to when he said, 'That's an easy matter' was making up the fast, and how slight the effort involved was and how easy it was. He was saying (in effect), 'We will fast another day in its place.' " </p>
