      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 18: </a> </em>
        <h1> <em> Fasting </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Sighting the New Moon for Beginning and Ending the Fast of Ramadan </h3>
        <hr>
        <em> <a name="018.18.1.1"> Book 18, Number 18.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, once mentioned Ramadan and said, "Do not begin the fast until you see the new moon, and do not break the fast (at the end of Ramadan) until you see it. If the new moon is obscured from you, then work out (when it should be)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.1.2"> Book 18, Number 18.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Dinar from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "A month has twenty-nine days in it. Do not start the fast or break it until you see the new moon. If the new moon is obscured from you, then work out (when it should be)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.1.3"> Book 18, Number 18.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Thawr ibn Zayd ad-Dili from Abdullah ibn Abbas that the Messenger of Allah, may Allah bless him and grant him peace, once mentioned Ramadan and said, "Do not start the fast or break it until you see the new moon. If the new moon is obscured from you, then complete a full thirty days." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.1.4"> Book 18, Number 18.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that once in the time of Uthman ibn Affan the new moon had been seen in the afternoon and Uthman did not break his fast until evening had come and the sun had set. </p>
          <p> Yahya said that he had heard Malik say that some one who sees the new moon of Ramadan when he is on his own should start the fast and not break it if he knows that that day is part of Ramadan. He added, "Some one who sees the new moon of Shawwal when he is on his own does not break the fast, because people suspect the reliability of someone among them who breaks the fast. Such people should say, when they sight the new moon, 'We have seen the new moon.' Whoever sees the new moon of Shawwal during the day should not break his fast but should continue fasting for the rest of that day. This is because it is really the new moon of the night that is coming ." </p>
          <p> Yahya said that he heard Malik say, "If people are fasting on the day of Fitr thinking that it is still Ramadan and then definite evidence comes to them that the new moon of Ramadan had been seen one day before they began to fast and that they are now into the thirty-first day, then they should break the fast on that day at whatever time the news comes to them. However, they do not pray the id prayer if they hear the news after the sun has begun to decline." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Making the Intention to Fast Before Dawn </h3>
        <hr>
        <em> <a name="018.18.2.5"> Book 18, Number 18.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to say, "Only some one who makes the intention to fast before dawn (actually) fasts." </p>
          <p> Yahya related to me from Malik from Ibn Shihab that A'isha and Hafsa, the wives of the Prophet, may Allah bless him and grant him peace, also said that. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Being Quick to Break the Fast </h3>
        <hr>
        <em> <a name="018.18.3.6"> Book 18, Number 18.3.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu Hazim ibn Dinar from Sahl ibn Sad as Saidi that the Messenger of Allah, may Allah bless him and grant him peace, said, "People will remain in good as long as they are quick to break the fast." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.3.7"> Book 18, Number 18.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abd ar-Rahman ibn Harmala al-Aslami from Said ibn al-Musayyab that the Messenger of Allah, may Allah bless him and grant him peace, said, "People will remain in good as long as they are quick to break the fast." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.3.8"> Book 18, Number 18.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Humayd ibn Abd ar-Rahman that Umar ibn al-Khattab and Uthman ibn Affan would pray maghrib when they saw the night darkening, before they broke their fast, and that was during Ramadan. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Fasting When Junub in the Morning during Ramadan </h3>
        <hr>
        <em> <a name="018.18.4.9"> Book 18, Number 18.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abd ar-Rahman ibn Mamar al-Ansari from Abu Yunus, the mawla of A'isha, from  A'isha that she overheard a man standing at the door saying to the Messenger of Allah, may Allah bless him and grant him peace, "Messenger of Allah, I get up in the morning junub, in a state of major ritual impurity, and want to fast," and the Messenger of Allah, may Allah bless him and grant him peace, said, "I too get up in the morning junub and want to fast, so I do ghusl and fast."  The man said to him, "You are not the same as us. Allah has forgiven you all your wrong actions that have gone before and those that have come after."  The Messenger of Allah, may Allah bless him and grant him peace, got angry and said, "By Allah, I hope that I am the most fearful of you with respect to Allah and the most knowledgeable of you in how I have taqwa." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.4.10"> Book 18, Number 18.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abd Rabbih ibn Said from Abu Bakr ibn Abd ar-Rahman ibn al-Harith ibn Hisham from A'isha and Umm Salama, the wives of the Prophet, may Allah bless him and grant him peace, that the Prophet, may Allah bless him and grant him peace, used to get up in the morning  junub from intercourse, not a dream, in Ramadan, and then he would fast." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.4.11"> Book 18, Number 18.4.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr ibn Abd ar-Rahman ibn al-Harith ibn Hisham that he heard Abu Bakr ibn Abd ar-Rahman ibn al-Harith ibn Hisham say, "My father and I were with Marwan ibn al Hakam at the time when he was amir of Madina, and someone mentioned to him that Abu Hurayra used to say, 'If someone begins the morning junub, he has broken the fast for that day.'  Marwan said, 'I swear to you, Abdar-Rahman, you must go to the two umm al muminin, A'isha and Umm Salama, and ask them about it.' </p>
          <p> ''Abd ar-Rahman went to visit A'isha and I accompanied him. He greeted her and then said, 'Umm al-muminin, we were with Marwan ibn al Hakam and someone mentioned to him that Abu Hurayra used to say that if some one had begun the morning junub, he had broken the fast for that day.' A'isha said, 'It is not as Abu Hurayra says Abd ar-Rahman. Do you dislike what the Messenger of Allah, may Allah bless him and grant him peace, used to do?', and Abd ar-Rahman said, 'No, by Allah.' A'isha said, 'I bear witness that the Messenger of Allah, may Allah bless him and grant him peace, used to get up in the morning junub from intercourse, not a dream, and would then fast for that day.' " </p>
          <p> He continued, "Then we went and visited Umm Salama, and Abd ar-Rahman asked her about the same matter and she said the same as A'isha had said. Then we went off until we came to Marwan ibn al-Hakam Abd ar-Rahman told him what they had both said and Marwan said, 'I swear to you, Abu Muhammad, you must use the mount which is at the door, and go to Abu Hurayra, who is on his land at al Aqiq, and tell him this.' So Abd ar-Rahman rode off, and I went with him, until we came to Abu Hurayra. Abd ar-Rahman talked with him for a while, and then mentioned the matter to him, and Abu Hurayra said, 'I don't know anything about it. I was just told that by someone.'" </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.4.12"> Book 18, Number 18.4.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr, from Abu Bakr ibn Abd ar-Rahman that A'isha and Umm Salama, the wives of the Prophet, may Allah bless him and grant him peace, said, "The Messengerof Allah, may Allah bless him and grant him peace, used to get up in the morning junub from intercourse, not a dream, and would then fast." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Permission for a Fasting Man to Kiss </h3>
        <hr>
        <em> <a name="018.18.5.13"> Book 18, Number 18.5.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that a certain man kissed his wife while he was fasting in Ramadan. This made him very anxious, and so he sent his wife to the Prophet, may Allah bless him and grant him peace, to ask him about that for him. She went in and saw Umm Salama, the wife of the Prophet, may Allah bless him and grant him peace, and mentioned the matter to her, and Umm Salama told her that the Messenger of Allah, may Allah bless him and grant him peace, used to kiss while he was fasting. So she went back and told her husband that, but it only made him find fault all the more and he said, "We are not like the Messenger of Allah, may Allah bless him and grant him peace. Allah makes permissible for the Messenger of Allah, may Allah bless him and grant him peace, whatever He wishes." </p>
          <p> His wife then went back to Umm Salama and found the Messenger of Allah, may Allah bless him and grant him peace, with her. The Messenger of Allah, may Allah bless him and grant him peace, said, "What's the matter with this woman?", and Umm Salama told him. The Messenger of Allah, may Allah bless him and grant him peace, said, "Didn't you tell her that I do that myself?" and she said, "I told her, and she went to her husband and told him, but it only made him find fault all the more and say, 'We are not like the Messenger of Allah, may Allah bless him and grant him peace. Allah makes permissible for His Messenger, may Allah bless him and grant him peace, whatever He wishes.' " The Messenger of Allah, may Allah bless him and grant him peace, got angry and said, "By Allah, I am the one with the most taqwa of Allah of you all, and of you all the one who best knows His limits." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.5.14"> Book 18, Number 18.5.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, umm al-muminin, may Allah be pleased with her, said, "The Messenger of Allah, may Allah bless him and grant him peace, used to kiss certain of his wives when fasting," and then she laughed. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.5.15"> Book 18, Number 18.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Atika bint Zayd ibn Amr ibn Nufayl, the wife of Umar ibn al-Khattab, used to kiss Umar ibn al-Khattab's head while he was fasting, and he did not tell her not to. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.5.16"> Book 18, Number 18.5.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'n-Nadr, the mawla of Umar ibn Ubaydullah that A'isha bint Talha told him that she was once with A'isha, the wife of the Prophet, may Allah bless him and grant him peace, and her husband, who was fasting, came and visited her there. (He was Abdullah ibn Abd ar-Rahman ibn Abi Bakras-Siddiq.) A'isha said to him, "What's stopping you from coming close to your wife and kissing her and joking with her?"  He said, "Can I kiss her when I am fasting?"  She said, "Yes." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.5.17"> Book 18, Number 18.5.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam that Abu Hurayra and Sad ibn Abi Waqqas used to say that someone who was fasting was allowed to kiss. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Restraint in Kissing when Fasting </h3>
        <hr>
        <em> <a name="018.18.6.18"> Book 18, Number 18.6.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, would say, when she mentioned that the Messenger of Allah, used to kiss while fasting, "And who among you is more able to control himself than the Messenger of Allah, may Allah bless him and grant him peace?" </p>
          <p> Yahya said that Malik said that Hisham ibn Urwa ibn az-Zubayr had said, "I do not think that kissing invites to good for people who are fasting." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.6.19"> Book 18, Number 18.6.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that Abdullah ibn Abbas was asked about people kissing while fasting and he said that he allowed it for old men but disapproved of it for young men. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.6.20"> Book 18, Number 18.6.20: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to forbid kissing and fondling for people who were fasting . </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Fasting while Travelling </h3>
        <hr>
        <em> <a name="018.18.7.21"> Book 18, Number 18.7.21: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Ubaydullah ibn Abdullah ibn Utba ibn Masud from Abdullah ibn Abbas that the Messenger of Allah, may Allah bless him and grant him peace, left for Makka in Ramadan during the year of the conquest, and fasted until he reached al-Kadid. He then broke the fast, and so everyone else did so as well. What people used to do was act according to whatever the Messenger of Allah, may Allah bless him and grant him peace, had done most recently. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.7.22"> Book 18, Number 18.7.22: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr ibn Abd ar-Rahman, from Abu Bakr ibn Abd ar-Rahman from one of the companions of the Messenger of Allah, that the Messenger of Allah, may Allah bless him and grant him peace, ordered everyone to break the fast on the journey he made in the year of the conquest saying, "Be strong for your enemy," while the Messenger of Allah, may Allah bless him and grant him peace, kept on fasting. Abu Bakr said that the one who related this to him said, "I saw the Messenger of Allah, may Allah bless him and grant him peace, pouring water over his head at al-Arj, either from thirst or from the heat. Then some one said to the Messenger of Allah, may Allah bless him and grant him peace, 'Messenger of Allah, a group of people kept on fasting when you did.'  Then when the Messenger of Allah was at al-Kadid, he asked for a drinking-bowl and drank, and everyone broke the fast." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.7.23"> Book 18, Number 18.7.23: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Humayd at-Tawil that Anas ibn Malik said, "We once travelled with the Messenger of Allah, may Allah bless him and grant him peace, in Ramadan, and those who were fasting did not find fault with those who were not, and those who were not fasting did not find fault with those who were." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.7.24"> Book 18, Number 18.7.24: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that Hamza ibn Amr al-Aslami once said to the Messenger of Allah, may Allah bless him and grant him peace, "Messenger of Allah, I am a man who fasts. Can I fast when travelling?"  The Messenger of Allah, may Allah bless him and grant him peace, said, "If you want you can fast, and if you want you can break the fast." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.7.25"> Book 18, Number 18.7.25: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used not to fast while travelling. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="018.18.7.26"> Book 18, Number 18.7.26: </a> </em>
