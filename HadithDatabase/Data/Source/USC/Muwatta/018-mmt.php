        <h3> Section: The Vow to Fast, and Fasting for the Dead </h3>
        <hr>
        <em> <a name="018.18.16.42"> Book 18, Number 18.16.42: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab was asked whether a man who had vowed to fast a month could fast voluntarily, and Said said, "He should fulfil his vow before he does any voluntary fasting." </p>
          <p> Malik said, "I have heard the same thing from Sulayman ibn Yasar." </p>
          <p> Malik said, "If someone dies with an unfulfilled vow to free a slave or to fast or to give sadaqa or to give away a camel, and makes a bequest that his vow should be fulfilled from his estate, then the sadaqa or the gift of the camel are taken from one third of his estate. Preference is given to it over other bequests, except things of a similar nature, because by his vow it has become incumbent on him, and this is not the case with something he donates voluntarily. They (vows and voluntary donations) are settled from a limited one-third of his estate, and not from the whole of it, since if the dying man were free to dispose of all of his estate, he might delay settling what had become incumbent on him (i.e. his vows), so that when death came and the estate passed into the hands of his heirs, he would have bequeathed such things (i.e. his vows) that were not claimed by anyone (like debts). If that (i.e. to dispose freely of his property) were allowed him, he would delay these things (i.e. his vows) until when he was near death, he would designate them and they might take up all of his estate. He must not do that." </p>
        </blockquote>
