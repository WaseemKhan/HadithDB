        <em> <a name="018.18.17.49"> Book 18, Number 18.17.49: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Humayd ibn Oays al-Makki told him, "I was with Mujahid while he was performing tawaf around the Kaba, and a man came to him and asked whether the days (of fasting) for kaffara had to be fasted consecutively, or could they be split up. I said to him, 'Yes, they can be split up, if the person so wishes.' Mujahid said, 'He should not split them up, because in Ubayy ibn Kab's recitation they are referred to as three consecutive days.' " </p>
          <p> Malik said, "What I like most is what Allah has specified in the Qur'an, that is, that they are fasted consecutively." </p>
          <p> Malik was asked about a woman who began the day fasting in Ramadan and though it was outside of the time of her period, fresh blood (i.e. not menstrual blood) flowed from her. She then waited until evening to see the same, but did not see anything.Then, on the next day in the morning she had anotherflow, though less than the first. Then, some days before her period, the flow stopped completely. Malik was asked what she should do about her fasting and prayer, and he said, "This blood is like menstrual blood. When she sees it she should break her fast, and then make up the days she has missed. Then, when the blood has completely stopped, she should do ghusl and fast." </p>
          <p> Malik was asked whether someone who became muslim on the last day of Ramadan had to make up all of Ramadan or whether he just had to make up the day when he became muslim, and he said, "He does not have to make up any of the days that have passed. He begins fasting from that day onwards. What I like most is that he makes up the day on which he became muslim." </p>
        </blockquote>
