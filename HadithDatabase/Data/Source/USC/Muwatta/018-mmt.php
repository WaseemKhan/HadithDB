        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to say, "If some one makes himself vomit while he is fasting he has to make up a day, but if he cannot help vomiting he does not have to make up anything." </p>
