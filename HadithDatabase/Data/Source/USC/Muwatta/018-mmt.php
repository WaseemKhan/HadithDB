        <em> <a name="018.18.17.48"> Book 18, Number 18.17.48: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that he heard Said ibn al Musayyab being asked about making up days missed in Ramadan, and Said said, "What I like best is for days missed in Ramadan to be made up consecutively, and not separately." </p>
          <p> Yahya said that he had heard Malik say, about some one who made up the days he had missed in Ramadan separately, that he did not have to repeat them. (What he had done) was enough for him.  It was, however, preferable, if he did them consecutively. </p>
          <p> Malik said, "Whoever eats or drinks thoughtlessly or forgetfully in Ramadan or during any other obligatory fast that he must do, has to fast another day in its place." </p>
        </blockquote>
