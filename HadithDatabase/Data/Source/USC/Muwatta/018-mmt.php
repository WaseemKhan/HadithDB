          <p> Yahya said that he heard Malik say, about fasting for six days after breaking the fast at the end of Ramadan, that he had never seen any of the people of knowledge and fiqh fasting them. He said, "I have not heard that any of our predecessors used to do that, and the people of knowledge disapprove of it and they are afraid that it might become a bida and that common and ignorant people might join to Ramadan what does not belong to it, if they were to think that the people of knowledge had given permission for that to be done and were seen doing it. </p>
