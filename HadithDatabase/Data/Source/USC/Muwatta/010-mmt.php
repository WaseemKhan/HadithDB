      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 10: </a> </em>
        <h1> <em> The Two 'Ids </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Ghusl of the Two Ids, the Call to Prayer for Them, and the Iqama </h3>
        <hr>
        <em> <a name="010.10.1.1"> Book 10, Number 10.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard more than one of their men of knowledge say, "There has been no call to prayer or iqama for the id al-Fitr or the id al-Adha since the time of the Messenger of Allah, may Allah bless him and grant him peace." </p>
          <p> Malik said, "That is the sunna about which there is no disagreement among us." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.1.2"> Book 10, Number 10.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to do ghusl on the day of Fitr before going to the place of prayer. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Order to Pray Before the Khutba on the Two Ids </h3>
        <hr>
        <em> <a name="010.10.2.3"> Book 10, Number 10.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that the Messenger of Allah, may Allah bless him and grant him peace, used to pray on the day of Fitr and the day of Adha before the khutba. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.2.4"> Book 10, Number 10.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Abu Bakr and Umar used to do that. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.2.5"> Book 10, Number 10.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that Abu Ubayd, the mawla of Ibn Azhar said, "I was present at an id with Umar ibn al-Khattab. He prayed, and then after he had prayed he gave a khutba to the people and said, 'The Messenger of Allah, may Allah bless him and grant him peace, forbade fasting on these two days - the day you break your fast (after Ramadan), and the day you eat from your sacrifice (after Hajj) .' " </p>
          <p> Abu Ubaydcontinued,"Then I was present at an id with Uthman ibn Affan. He came and prayed, and when he had finished he gave a khutba and said, 'Two ids have been joined together for you on this day of yours. If any of the people of al-Aliyya (the hills outlying Madina) want to wait for the jumua they can do so, and if any of them want to return, I have given them permission.' Abu Ubayd continued, "Then I was present at an id with AIi ibn Abi Talib (at the time when Uthman was being detained). He came and prayed, and then after he had prayed he gave a khutba." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Order to Eat Before Going Out on the Morning of the Id </h3>
        <hr>
        <em> <a name="010.10.3.6"> Book 10, Number 10.3.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that he used to eat on the day of the id al-Fitr before going out. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.3.7"> Book 10, Number 10.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that Said al-Musayyab told him that people used to be told to eat on the day of Fitr before setting out. Malik said that he did not consider that people had to do that for Adha. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Takbir and the Recitation in the Prayer of the Two Ids </h3>
        <hr>
        <em> <a name="010.10.4.8"> Book 10, Number 10.4.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Damra ibn Said al-Mazini from Ubaydullah ibn Abdullah ibn Utba ibn Masud that Umar ibn al-Khattab asked Abu Waqid al-Laythi what the Messenger of Allah, may Allah bless him and grant him peace, used to recite in the prayers of Adha and Fitr. He said, "He used to recite Qaf (Sura 50) and al-lnshiqaq (Sura84)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.4.9"> Book 10, Number 10.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Nafi, the mawla of Abdullah ibn Umar said, "I was at Adha and Fitr with Abu Hurayra and he said 'Allah is greater' seven times in the first raka, before the recitation, and five times in the second, before the recitation." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Refraining from Prayer Before and After the Two Id Prayers </h3>
        <hr>
        <em> <a name="010.10.5.10"> Book 10, Number 10.5.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar did not pray either before the prayer or after it on the day of Fitr. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.5.11"> Book 10, Number 10.5.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab used to go to the place of prayer after praying subh, and before the sun rose. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Permission to Pray Before and After the Two Id Prayers </h3>
        <hr>
        <em> <a name="010.10.6.12"> Book 10, Number 10.6.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abd ar-Rahman ibn al-Qasim that his father used to pray four rakas before he went to the place of prayer. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="010.10.6.13"> Book 10, Number 10.6.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Hisham ibn Urwa that his father used to pray on the day of Fitr before the prayer in the mosque. </p>
          <p> 10.7 The Coming of the Imam on the Day of the Id and Waiting for the Khutba </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Coming of the Imam on the Day of the Id and Waiting for the Khutba </h3>
        <hr>
        <em> <a name="010.10.7.14"> Book 10, Number 10.7.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me that Malik said, "The sunna concerning the time of prayer on the ids of Fitr and Adha - and there is no disagreement amongst us about it - is that the imam leaves his house and as soon as he has reached the place of prayer the prayer falls due." </p>
          <p> Yahya said that Malik was asked whether a man who prayed with the imam could leave before the khutba, and he said, "He should not leave until the imam leaves." </p>
        </blockquote>
        <!--EndHadithBody-->
