      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 12: </a> </em>
        <h1> <em> The Eclipse Prayer </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: How to Pray the Eclipse Prayer </h3>
        <hr>
        <em> <a name="012.12.1.1"> Book 12, Number 12.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "There was an eclipse of the sun in the time of the Messenger of Allah, may Allah bless him and grant him peace, and the Messenger of Allah, may Allah bless him and grant him peace, led the people in prayer. He stood, and did so for a long time. Then he went into ruku, and made the ruku long. Then he stood again, and did so for a long time, though not as long as the first time. Then he went into ruku, and made the ruku long, though not as long as thefirst time. Then he rose, and went down into sajda. He then did the same in the second raka, and by the time he had finished the sun had appeared. He then gave a khutba to the people, in which he praised Allah and then said, 'The sun and the moon are two of Allah's signs. They do not eclipse for anyone's death nor for anyone's life. When you see an eclipse, call on Allah and say, "Allah is greater" and give sadaqa.'  Then he said, 'O community of Muhammad! ByAllah, there is no-one more jealous than Allah of a male or female slave of his who commits adultery. O community of Muhammad! By Allah, if you knew what I knew, you would laugh little and weep much'." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="012.12.1.2"> Book 12, Number 12.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that Abdullah ibn Abbas said, "There was an eclipse of the sun and the Messenger of Allah, may Allah bless him and grant him peace, prayed, and the people prayed with him. He stood for a long time, nearly as long as (it takes to recite) Surat al-Baqara (Sura 2), and then went into ruku for a long time. Then he rose and stood for a long time, though less than the first time.Then he went into ruku for a long time, though less than the first time. Then he went down into sajda. Then he stood for a long time, though less than the first time. Then he went into ruku for a long time, though less than the first time. Then he rose and stood for a long time, though less than the firsttime. Then he went into ruku for a long time, though less than the first time. Then he went down into sajda, and by the time he had finished the sun had appeared. Then he said, 'The sun and the moon are two of Allah's signs. They do not eclipse for anyone's death nor for anyone's life. When you see an eclipse, remember Allah.' They said, 'Messenger of Allah, we saw you reach out for something while you were standing here and then we saw you withdraw.' He said, 'I saw the Garden and I reached out for a bunch of grapes from it, and if I had taken it you would have been able to eat from it for as long as this world lasted. Then I saw the Fire - and I have never seen anything more hideous than what I saw today - and I saw that most of  its people were women.'  They said, 'Why, Messenger of Allah?'  He said, 'Because of their ungratefulness (kufr).' Someone said, 'Are they ungrateful toAllah?'  He said, 'They are ungrateful to their husbands and they are ungrateful for good behaviour (towards them) . Even if you were to behave well towards one of them for a whole lifetime and then she were to see you do something (that she did not like) she would say that she had never seen anything good from you.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="012.12.1.3"> Book 12, Number 12.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from 'Amra bint Abd ar-Rahman from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that a jewish woman came to beg from her and said, "May Allah give you refuge from the punishment of the grave."  So A'isha asked the Messenger of Allah, may Allah bless him and grant him peace, "Are people punished in their graves?", and the Messenger of Allah, may Allah bless him and grant him peace, took refuge in Allah from that. Then one morning the Messenger of Allah,  may Allah bless him and grant him peace, went out on a journey and there was an eclipse of the sun, and he returned in the late morning and passed through his apartments. Then he stood and prayed, and the people stood behind him. He stood for a long time, and then went into ruku for a long time. Then he rose and stood for a long time, though less than the first time, and then went into ruku for a long time, though less than the first time. Then he rose, and went down into sajda. Then he stood for a long time, though less than the time before, and then went into ruku for a long time, though less than the time before. Then he rose and stood for a long time, though less than the time before, and then went into ruku for a long time, though less than the time before. Then he rose, and went down into sajda. When he had finished he said what Allah willed him to say, and then he told them to seek protection for themselves from the punishment of the grave." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About the Eclipse Prayer </h3>
        <hr>
        <em> <a name="012.12.2.4"> Book 12, Number 12.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from Fatima bint al Mundhir that Asma bint Abi Bakr as-Siddiq said, "I went to A'isha, the wife of the Prophet, may Allah bless him and grant him peace, during an eclipse of the sun, and everybody was standing in prayer, and she too was standing praying. I said, 'What is everybody doing?'  She pointed towards the sky with her hand and said, 'Glory be to Allah.' I said,  'A sign?'  She nodded  'Yes'  with her head." </p>
          <p> She continued, "I stood until I had almost fainted, and I began to pour water over my head. The Messenger of Allah, may Allah bless him and grant him peace, praised Allah and spoke well of Him, and then said, 'There is nothing which I had previously not seen beforehand that I have not now seen while standing - even the Garden and the Fire. It has been revealed to me that you will be tried in your graves with a trial, like, or near to, the trial of the Dajjal (I do not know which one Asma said). Every one of you will have someone who comes to him and asks him, 'What do you know about this man?' A mumin, or one who has certainty (muqin)  (I do not know which one Asma said), will say, 'He is Muhammad, the Messenger of Allah, may Allah bless him and grant him peace, who came to us with clear proofs and guidance, and we answered and believed and followed.' He will then be told, 'Sleep in a good state. We know now that you were a mumin.' A hypocrite, however, or one who has doubts (l do not know which one Asma said), will say, 'I do not know, I heard everybody saying something and I said it.' " </p>
        </blockquote>
        <!--EndHadithBody-->
