        <h1> <em> The 'Aqiqa </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: About The Aqiqa </h3>
        <hr>
        <em> <a name="026.26.1.1"> Book 26, Number 26.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Zayd ibn Aslam from a man of the Banu Damra that his father said, "The Messenger of Allah, may Allah bless him and grant him peace, was asked about the Aqiqa. He said, 'I do not like disobedience (uquq),' as if he disliked the name. He said, 'If anyone has a child born to him, and wants to sacrifice for his child, then let him do it.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="026.26.1.2"> Book 26, Number 26.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Jafar ibn Muhammad that his father said, "Fatima, the daughter of the Messenger of Allah, may Allah bless him and grant him peace, weighed the hair of Hasan, Husayn, Zaynab and Umm Kulthum, and gave away in sadaqa an equivalent weight of silver." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="026.26.1.3"> Book 26, Number 26.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Rabia ibn Abi Abd ar-Rahman that Muhammad ibn Ali ibn al-Husayn said, "Fatima, the daughter of the Messenger of Allah, may Allah bless him and grant him peace, weighed the hair of Hasan and Husayn, and gave away in sadaqa the equivalent weight in silver." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Behaviour in the Aqiqa </h3>
        <hr>
        <em> <a name="026.26.2.4"> Book 26, Number 26.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that if any of Abdullah ibn Umar's family asked him for an aqiqa, he would give it to them. He gave a sheep as aqiqa for both his male and female children. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="026.26.2.5"> Book 26, Number 26.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Rabia ibn Abi Abd ar-Rahman that Muhammad ibn Ibrahim ibn al-Harith at-Taymi said, "I heard my father say that the aqiqa was desirable, even if it was only a sparrow." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="026.26.2.64"> Book 26, Number 26.2.64: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard that there had been an aqiqa for Hasan and Husayn, the sons of Ali ibn Abi Talib. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="026.26.2.7"> Book 26, Number 26.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa that his father, Urwa ibn az-Zubayr made an aqiqa for his male and female children of a sheep each. </p>
          <p> Malik said, "What we do about the aqiqa is that if someone makes an aqiqa for his children, he gives a sheep for both male and female. The aqiqa is not obligatory but it is desirable to do it, and people continue to come to us about it. If someone makes an aqiqa for his children, the same rules apply as with all sacrificial animals - one-eyed, emaciated, injured, or sick animals must not be used, and neither the meat or the skin is to be sold. The bones are broken and the family eat the meat and give some of it away as sadaqa. The child is not smeared with any of the blood .'' </p>
        </blockquote>
        <!--EndHadithBody-->
