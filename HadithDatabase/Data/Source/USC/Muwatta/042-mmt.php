      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 42: </a> </em>
        <h1> <em> Drinks </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Hadd for Drinking Wine </h3>
        <hr>
        <em> <a name="042.42.1.1"> Book 42, Number 42.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that as-Sa'ib ibn Yazid informed him that Umar ibn al-Khattab came out to them. He said, "I have found the smell of wine on so-and-so, and he claimed that it was the drink of boiled fruit juice, and I am inquiring about what he has drunk. If it intoxicates, I will flog him." Umar then flogged him with the complete hadd. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.1.2"> Book 42, Number 42.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Thawr ibn Zayd ad-Dili that Umar ibn al-Khattab asked advice about a man drinking wine. Ali ibn Abi Talib said to him, "We think that you flog him for it with eighty lashes. Because when he drinks, he becomes intoxicated, and when he becomes intoxicated, he talks confusedly, and when he talks confusedly, he lies." (80 lashes is the same amount as for slandering) Umar gave eighty lashes for drinking wine. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.1.3"> Book 42, Number 42.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that he was asked about the hadd of the slave for wine. He said, "I heard that he has half the hadd of a freeman for drinking wine. Umar ibn al-Khattab, Uthman ibn Affan, and Abdullah ibn Umar flogged their slaves with half of the hadd of a freeman when they drank wine." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.1.4"> Book 42, Number 42.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that he heard Said ibn al-Musayyab say, "There is nothing that Allah does not like to be pardoned as long as it is not a hadd." </p>
          <p> Yahya said that Malik said, "The sunna with us is that the hadd is obliged against anyone who drinks something intoxicating whether or not he becomes drunk." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Containers Forbidden for Preparation of Nabidh </h3>
        <hr>
        <em> <a name="042.42.2.5"> Book 42, Number 42.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, addressed the people in one of his raids. Abdullah ibn Umar said, "I went towards him, but he finished before I reached him. I asked about what he had said. Someone said to me, 'He forbade preparing nabidh in a gourd or in a jug smeared with pitch.'" </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.2.6"> Book 42, Number 42.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from al-Ala ibn Abd ar-Rahman ibn Yaqub from his father from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, forbade preparing nabidh in a gourd or in a jug smeared with pitch. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Mixtures of Fruit Disapproved for Making Nabidh </h3>
        <hr>
        <em> <a name="042.42.3.7"> Book 42, Number 42.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, forbade preparing nabidh from nearly ripe dates and fresh dates together, and from dates and raisins together. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.3.8"> Book 42, Number 42.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from a reliable source from Bukayr ibn Abdullah ibn al-Ashajj from Abd ar-Rahman ibn al-Hubab al-Ansari from Abu Qatada al-Ansari that the Messenger of Allah, may Allah bless him and grant him peace, forbade making nabidh from dates and raisins together, and nearly ripe dates and fresh dates together. </p>
          <p> Malik said, "That is the way of doing things among us in which the people of knowledge in our city continue. It is disapproved of because the Messenger of Allah, may Allah bless him and grant him peace, forbade it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Wine Made Haram </h3>
        <hr>
        <em> <a name="042.42.4.9"> Book 42, Number 42.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from ibn Shihab from Abu Salama ibn Abd ar-Rahman that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "The Messenger of Allah, may Allah bless him and grant him peace, was asked about mead, and he replied, 'Every drink which intoxicates is haram.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.4.10"> Book 42, Number 42.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, was asked about al-Ghubayra. He said, "There is no good in it," and forbade it. Malik said, "I asked Zayd ibn Aslam, 'What is al-Ghubayra?' He said, 'It is an intoxicant.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.4.11"> Book 42, Number 42.4.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said "Whoever drinks wine in this world and does not turn from it in tawba, it is haram for him in the Next World." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: General Section on Wine being Made Haram </h3>
        <hr>
        <em> <a name="042.42.5.12"> Book 42, Number 42.5.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam that Ibn Wala al-Misri asked Abdullah ibn Abbas about what is squeezed from the grapes. Ibn Abbas replied, "A man gave the Messenger of Allah, may Allah bless him and grant him peace, a small water-skin of wine. The Messenger of Allah, may Allah bless him and grant him peace, said to him, 'Don't you know that Allah has made it haram?' He said, 'No.' Then a man at his side whispered to him. The Messenger of Allah, may Allah bless him and grant him peace, asked what he had whispered, and the man replied, 'I told him to sell it.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'The One who made drinking it haram has made selling it haram.' The man then opened the water-skins and poured out what was in them ." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.5.13"> Book 42, Number 42.5.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ishaq ibn Abdullah ibn Abi Talha that Anas ibn Malik said, "I was serving wine to Abu Ubayda ibn al-Jarrah and Abu Talha al-Ansari and Umayy ibn Kab. The wine had been prepared from crushed ripe dates and dried dates. Someone came to them and said, 'Wine has been made haram.' Abu Talha ordered me to go and take the jugs and break them. I stood up and went to a mortar of ours and I struck them with the bottom of it until they broke." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.5.14"> Book 42, Number 42.5.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Da'ud ibn al-Husayn that Waqid ibn Amr ibn Sad ibn Muadh informed him from Mahmud ibn Labid al-Ansari that when Umar ibn al-Khattab went to ash-Sham, the people of ash-Sham complained to him about the bad air of their land and its heaviness. They said, "Only this drink helps." Umar said, "Drink this honey preparation." They said, "Honey does not help us." A man from the people of that land said, "Can we give you something of this drink which does not intoxicate?" He said, "Yes." They cooked it until two-thirds of it evaporated and one-third of it remained. Then they brought it to Umar. Umar put his finger in it and then lifted his head and extended it. He said, "This is fruit juice concentrated by boiling. This is like the distillation with which you smear the camel's scabs." Umar ordered them to drink it. Ubada ibn as-Samit said to him, "You have made it halal, by Allah!" Umar said, "No, by Allah! O Allah! I will not make anything halal for them which You have made haram for them! I will not make anything haram for them which You have made halal for them." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="042.42.5.15"> Book 42, Number 42.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that some men from Iraq said to him, "Abu Abd ar-Rahman, we buy the fruit of the palm and grapes and we squeeze them into wine and we sell it." Abdullah ibn Umar said, "I call on Allah and His angels and whoever hears of jinn and men to testify to you that I order you not to buy it nor sell it nor to press it nor to drink it nor to give it to people to drink. It is something impure from the work of Shaytan." </p>
        </blockquote>
        <!--EndHadithBody-->
