      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 22: </a> </em>
        <h1> <em> Vows and Oaths </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Fulfilling Vows to Walk </h3>
        <hr>
        <em> <a name="022.22.1.1"> Book 22, Number 22.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Ubaydullah ibn Abdullah ibn Utba ibn Masud from Abdullah ibn Abbas that Sad ibn Ubada questioned the Messenger of Allah, may Allah bless him and grant him peace, and said,"My mother died while she still had a vow which she had not fulfilled." The Messenger of Allah, may Allah bless him and grant him peace, said, "Fulfill it for her." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.1.2"> Book 22, Number 22.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from </p>
          <p> Abdullah ibn Abi Bakr that his paternal aunt related that her grandmother made a vow to walk to the Quba mosque. She died, and did not fulfill it, so Abdullah ibn Abbas asked her daughter to walk for her. </p>
          <p> Yahya said that he had heard Malik say, "No one walks for anyone else." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.1.3"> Book 22, Number 22.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Abdullah ibn Abi Habiba said, "I said to a man, when I was young, 'A man who only says that he must walk to the House of Allah and does not say that he has vowed to walk, does not have to walk.' A man said, 'Shall I give you this small cucumber?' and he had a small cucumber in his hand and you will say, 'I must walk to the house of Allah?' I said, 'Yes' and I said it, for at that time I was still immature. Then, when I came of age, some one said to me that I had to fulfill my vow. I went and asked Said ibn al-Musayyab about it, and he said to me, 'You must walk.' So I walked." </p>
          <p> Malik said, "That is the custom among us." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Making Vows to Walk to the House and Not Succeeding </h3>
        <hr>
        <em> <a name="022.22.2.4"> Book 22, Number 22.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Urwa ibn Udhayna al-Laythi said, "I went out with my grandmother who had vowed to walk to the House of Allah. When we had gone part of the way, she could not go on. I sent one of her mawlas to question Abdullah ibn Umar and I went with him. He asked Abdullah ibn Umar, and Abdullah ibn Umar said to him, 'Take her and let her ride, and when she has the strength let her ride back, and start to walk from the place from which she was unable to go on.'~ </p>
          <p> Yahya said that he had heard Malik say, "I think that she must sacrifice an animal." </p>
          <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab and Abu Salama ibn Abd ar-Rahman said the same as Abdullah ibn Umar. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.2.5"> Book 22, Number 22.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said, "I vowed to walk, but I was struck by a pain in the kidney, so I rode until I came to Makka. I questioned Ata ibn Abi Rabah and others, and they said, 'You must sacrifice an animal.' When I came to Madina I questioned the ulama there, and they ordered me to walk again from the place from which I was unable to go on. So I walked." </p>
          <p> Yahya said that he had heard Malik say, "What is done among us regarding someone who makes a vow to walk to the House of Allah, and then cannot do it and so rides, is that he must return and walk from the place from which he was unable to go on. If he cannot walk, he should walk what he can and then ride, and he must sacrifice a camel, a cow, or a sheep if that is all that he can find." </p>
          <p> Malik, when asked about a man who said to another, "I will carry you to the House of Allah", answered, "If he intended to carry him on his shoulder, by that he meant hardship and exhaustion to himself, and he does not have to do that. Let him walk by foot and make sacrifice. If he did not intend anything, let him do hajj and ride, and take the man on hajj with him. That is because he said, 'I will carry you to the house of Allah.' If the man refuses to do hajj with him, then there is nothing against him, and what is demanded of him is cancelled." </p>
          <p> Yahya said that Malik was asked whether it was enough for a man who had made a vow that he would walk to the House of Allah a certain (large) number of times, or who had forbidden himself from talking to his father and brother, if he did not fulfil a certain vow, and he had taken upon himself, by the oath, something which he was incapable of fulfilling in his lifetime, even though he were to try every year, to fulfil only one or a (smaller) number of vows by Allah? Malik said, "The only satisfaction for that that I know is fulfilling what he has obliged himself to do. Let him walk for as long as he is able and draw near Allah the Exalted by what he can of good." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: How to Fulfill the Oath of Walking to the Kaba </h3>
        <hr>
        <em> <a name="022.22.3.5a"> Book 22, Number 22.3.5a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that what he preferred of what he had heard from the people of knowledge about a man or woman who vowed to walk to the House of Allah, was that they fulfilled the oath when performing umra, by walking until they had done say between Safa and Marwa. When they had done say it was finished. If they vowed to walk in the hajj, they walked until they came to Makka, then they walked until they had finished all the rites. </p>
          <p> Malik said, "Walking is only for hajj or umra." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Vows Not Permitted in Disobedience to Allah </h3>
        <hr>
        <em> <a name="022.22.4.6"> Book 22, Number 22.4.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Humayd ibn Qays and Thawr ibn Zayd adDili both informed him that the Messenger of Allah, may Allah bless him and grant him peace, (and one of them gave more detail than the other),saw a man standing in the sun. The Messenger asked, "What's wrong with him?" The people said, "He has vowed not to speak or to seek shade from the sun or to sit and to fast." The Messenger of Allah, may Allah bless him and grant him peace, said, "Go and tell him to speak, seek shade, and sit, but let him complete his fast." </p>
          <p> Malik said, "I have not heard that the Messenger of Allah, may Allah bless him and grant him peace, ordered the man in question to do any kaffara. The Messenger of Allah, may Allah bless him and grant him peace, only ordered him to complete that in which there was obedience to Allah and to abandon that in which there was disobedience to Allah." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.4.7"> Book 22, Number 22.4.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said heard al-Qasim ibn Muhammad say, "A woman came to Abdullah ibn Abbas and said, 'I have vowed to sacrifice my son.' Ibn Abbas said, 'Do not sacrifice your son. Do kaffara for your oath.' An old man with Ibn Abbas said, 'What kaffara is there for this?' Ibn Abbas said, 'Allah the Exalted said, "Those of you who say, regarding their wives.'Be as my mother's back' (Sura58 ayat 2) and then He went on to oblige the kaffara for it as you have seen.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.4.8"> Book 22, Number 22.4.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Talha ibn Abi al-Malik al-Ayli from al-Qasim ibn Muhammad ibn as-Siddiq from A'isha that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever vows to obey Allah, let him obey Him. Whoever vows to disobey Allah, let him not disobey Him." </p>
          <p> Yahya said that he had heard Malik say, "The meaning of the statement of the Prophet, may Allah bless him and grant him peace, 'Whoever vows to disobey Allah, let him not disobey Him' is that for instance a man who vows that, if he speaks to such-and-such a person, he will walk to Syria, Egypt, or any other such things which are not considered as ibada, is not under any obligation by any of that, even if he did speak to the man or did break whatever it was he swore, because Allah does not demand obedience in such things. He should only fulfill those things in which there is obedience to Allah." </p>
          <p> 22.5 Rashness in Oaths </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.4.9"> Book 22, Number 22.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, umm al-muminin said, "Rashness in oaths is that a man says, 'By Allah, No! by Allah!' " i.e. out of habit. </p>
          <p> Malik said, "The best of what I have heard on the matter is that rashness in oaths is that a man take an oath on something to show that he is certain that it is like he said, only to find that it is other than what he said. This is rashness." </p>
          <p> Malik said, "The binding oath is for example, that a man says that he will not sell his garment for ten dinars, and then he sells it for that, or that he will beat his young slave and then does not beat him, and so on. One does kaffara for making such an oath, and there is no kaffara in rashness." </p>
          <p> Malik said, "As for the one who swears to a thing which he knows is wicked, and he swears to a lie he knows to be a lie, in order to please someone with it or to excuse himself to someone by it or to gain money by it, no kaffara that he does for it can cover it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Oaths for which Kaffara Not Obligatory </h3>
        <hr>
        <em> <a name="022.22.5.10"> Book 22, Number 22.5.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar said, "Whoever swears by Allah and then says, 'Allah willing' and then does not do what he has sworn to, has not broken his oath." </p>
          <p> Malik said, "The best I have heard on this reservation is that it belongs to the statement made if the speaker does not break the normal flow of speech before he is silent. If he is silent and breaks the flow of speech, he has no exception." </p>
          <p> Yahya said, "Malik said that a man who said that he had disbelieved or associated something with Allah and then he broke his oath, had no kaffara, and he was not a disbeliever or one who associated something with Allah unless his heart concealed something of either of those. He should ask forgiveness of Allah and not return to it - for what he did was evil." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Oaths for Which Kaffara is Obligatory </h3>
        <hr>
        <em> <a name="022.22.6.11"> Book 22, Number 22.6.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Suhayl ibn Abi Salih from his father from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever makes an oath and then sees that something else would be better than it, should do kaffara for his oath and do what is better." </p>
          <p> Yahya said that he heard Malik say, "Anyone who says that he has a vow but does not mention the name of Allah, is still obliged to make the kaffara for an oath (if he breaks it)". </p>
          <p> Malik said, "Emphasis is when a man swears one thing several times, repeating the oath in his speech time after time. For instance, the statement, 'By Allah, I will not decrease it from such-and-such,' sworn three times or more. The kaffara of that is like the kaffara of one oath. If a man swears, 'I will not eat this food or wear these clothes or enter this house,' that is all in one oath, and he is only obliged to do one kaffara. It is the same for a man who says to his wife, 'You are divorced if I clothe you in this garment or let you go to the mosque,' and it is one entire statement in the normal pattern of speech. If he breaks any of that oath, divorce is necessary, and there is no breaking of oath after that in whatever he does. There is only one oath to be broken in that." </p>
          <p> Malik said, "What we do about a woman who makes a vow without her husband's permission is that she is allowed to do so and she must fulfill it, if it only concerns her own person and will not harm her husband. If, however, it will harm her husband, he may forbid her to fulfill it, but it remains an obligation against her until she has the opportunity to complete it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Behaviour in the Kaffara of the Broken Oath </h3>
        <hr>
        <em> <a name="022.22.7.12"> Book 22, Number 22.7.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar said, "If someone breaks an oath which he has stressed, he has to free a slave, or clothe ten poor people. If someone breaks an oath, but has not stressed it, he only has to feed ten poor people and each poor person is fed a mudd of wheat. Some one who does not have the means for that, should fast for three days." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.7.13"> Book 22, Number 22.7.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to do kaffara for a broken oath by feeding ten poor people. Each person got a mudd of wheat. He sometimes freed a slave if he had repeated the oath. </p>
          <p> Yahya related to me from Malik from Yahya ibn Said that Sulayman ibn Yasar said, "I understood from people that when they made the kaffara for a broken oath, they gave a mudd of wheat according to the smaller mudd. They thought that that would compensate for them." </p>
          <p> Malik said, "The best of what I have heard about the one who does kaffara for breaking his oath by clothing people is that if he clothes men he clothes them each in one garment. If he clothes women, he clothes them each in two garments, a long shift and a long scarf, because that is what is satisfactory for each of them in the prayer." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Oaths in General </h3>
        <hr>
        <em> <a name="022.22.8.14"> Book 22, Number 22.8.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that one time the Messenger of Allah, may Allah bless him and grant him peace, was speaking to Umar ibn al-Khattab while he was travelling with a troop and Umar swore by his father and he (the Messenger) said, "Allah forbids you to swear by your fathers. If anyone swears, let him swear by Allah or keep silent." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.8.15"> Book 22, Number 22.8.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, used tosay, "No, by the Overturner of hearts." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.8.16"> Book 22, Number 22.8.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Uthman ibn Hafs ibn Umar ibn Khalda that Ibn Shihab had heard that Abu Lubaba ibn Abd al-Mundhir, when Allah turned to him said, "Messenger of Allah, should I leave my people's house in which I committed wrong action and keep your company, and give away all my property as sadaqa for Allah and His Messenger? "The Messenger of Allah, may Allah bless him and grant him peace, said, "Giving away a third of it is enough for you." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="022.22.8.17"> Book 22, Number 22.8.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ayyub ibn Musa from Mansur ibn Abd ar-Rahman al-Hajabi from his mother that A'isha, umm al-muminin, may Allah be pleased with her, was asked about a man who devoted his property to the door of Kaba. She said, "Let him do kaffara for it with the kaffara of the oath." </p>
          <p> Malik said, that someone who devoted all his property in the way of Allah, and then broke his oath, should put a third of his property in the way of Allah, as that was what the Messenger of Allah, may Allah bless him and grant him peace, did in the case of Abu Lubaba. </p>
        </blockquote>
        <!--EndHadithBody-->
