      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 11: </a> </em>
        <h1> <em> The Fear Prayer </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Fear Prayer </h3>
        <hr>
        <em> <a name="011.11.1.1"> Book 11, Number 11.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yazid ibn Ruman from Salih ibn Khawwat from someone who had prayed (the prayer of fear) with the Messenger of Allah, may Allah bless him and grant him peace, on the day of Dhat ar-Riqa that one group had formed a row with him and one group had formed a row opposite the enemy. He then prayed one raka with the group he was with, and then remained standing while they finished by themselves. They then left and formed a row opposite the enemy, and then the other group came and he prayed the remaining raka of his prayer with them, and then remained sitting while they finished by themselves. Then he said the taslim with them. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="011.11.1.2"> Book 11, Number 11.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from al-Qasim ibn Muhammad from Salih ibn Khawwat that Sahl ibn Abi Hathma related to him that the form of the prayer of fear was that the imam stood with a group of his companions, while another group faced the enemy. The imam prayed one raka with them, including the prostration, and then stood. He remained standing while they completed the remaining raka by themselves. They then said the taslim, left, and formed up opposite the enemy while the imam remained standing. Then the others who had not prayed came forward and said the takbir behind the imam and he prayed one raka with them, including the prostration. He then said the taslim, while they stood up and prayed the remaining raka by themselves. Then they said the taslim. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="011.11.1.3"> Book 11, Number 11.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar, when asked about the fear prayer said, "The imam and a group of people go forward and the imam prays a raka with them, while another group, who have not yet prayed, position themselves between him and the enemy. When those who are with him have prayed a raka they draw back to where those who have not prayed are, and do not say the taslim. Then those who have not prayed come forward and pray a raka with him. Then the imam leaves, as he has now prayed two rakas. Everyone else in the two groups stands and prays a raka by himself after the imam has left. In this way each of the two groups will have prayed two rakas. If the fear is greater than that, then the men pray standing on their feet or mounted, either facing the qibla or otherwise." </p>
          <p> Malik said that Nafi said, "I do not believe that Abdullah ibn Umar related it from anyone other than the Messenger of Allah, may Allah bless him and grant him peace." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="011.11.1.4"> Book 11, Number 11.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab said, "The Messenger of Allah, may Allah bless him and grant him peace, did not pray dhuhr and asr on the day of the Trench until after the sun had set." </p>
          <p> Malik said, "The hadith of al-Qasim ibn Muhammad from Salih ibn Khawwat is the one I like most out of what I have heard about the fear prayer." </p>
        </blockquote>
        <!--EndHadithBody-->
