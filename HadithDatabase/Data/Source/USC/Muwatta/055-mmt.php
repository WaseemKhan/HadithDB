        <h1> <em> The Oath of Allegiance </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: About the Oath of Allegiance </h3>
        <hr>
        <em> <a name="055.55.1.1"> Book 55, Number 55.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar that Abdullah ibn Umar said, "When we took an oath of allegiance with him to hear and obey, the Messenger of Allah, may Allah bless him and grant him peace, said to us, 'In what you are able.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="055.55.1.2"> Book 55, Number 55.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Muhammad ibn al-Munkadir that Umayma bint Ruqayqa said, "I went to the Messenger of Allah, may Allah bless him and grant him peace, with the women who took an oath of allegiance with him in Islam. They said, 'Messenger of Allah! We take a pledge with you not to associate anything with Allah, not to steal, not to commit adultery, not to kill our children, nor to produce any lie that we have devised between our hands and feet, and not to disobey you in what is known.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'In what you can do and are able.' " </p>
          <p> Umayma continued, "They said, 'Allah and His Messenger are more merciful to us than ourselves. Come, let us give our hands to you, Messenger of Allah!' The Messenger of Allah, may Allah bless him and grant him peace, said, 'I do not shake hands with women. My word to a hundred women is like my word to one woman.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="055.55.1.3"> Book 55, Number 55.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar that Abdullah ibn Umar wrote to Abd al-Malik ibn Marwan, making an oath of allegiance. He wrote, "In the name of Allah, the Merciful, the Compassionate. To the slave of Allah, Abd al-Malik, the amir al-muminin, Peace be upon you. I praise Allah to you. There is no god but Him. I acknowledge your right to my hearing and my obedience according to the sunna of Allah and the sunna of His Prophet, in what I am able." </p>
        </blockquote>
        <!--EndHadithBody-->
