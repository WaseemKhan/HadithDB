      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 58: </a> </em>
        <h1> <em> Sadaqa </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Stimulation of Desire for Sadaqa </h3>
        <hr>
        <em> <a name="058.58.1.1"> Book 58, Number 58.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said from Abu'l-Hubab Said ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever gives sadaqa from good earning - and Allah only accepts the good - it is as if he placed it in the palm of the Merciful to raise it, as one of you raises his foal or young camel until it is like the mountain " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.1.2"> Book 58, Number 58.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Ishaq ibn Abdullah ibn Abi Talha heard Anas ibn Malik say, "Abu Talha had the greatest amount of property in palm-trees among the Ansar in Madina. The dearest of his properties to him was Bayruha which was in front of the mosque. The Messenger of Allah, may Allah bless him and grant him peace, used to go into it and drink from the pleasant water which was in it." </p>
          <p> Anas continued, "When this ayat was sent down 'You will not obtain rightness of action until you expend of what you love,' (Sura 2 ayat l76), Abu Talha went to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Messenger of Allah! Allah, the Blessed, the Exalted, has said, "You will not obtain until you expend of what you love." The property which I love the best is Bayruha. It is sadaqa for Allah. I hope for its good and for it to be stored up with Allah. Place it wherever you wish, Messengerof Allah. ' " </p>
          <p> "The Messenger of Allah, may Allah bless him and grant him peace, said, 'Well done! That is property which profits! That is property which profits. I have heard what you have said about it and I think that you should give it to your relatives.' Abu Talha said, 'I will do it, Messenger of Allah!' Abu Talha therefore divided it among his relatives and the children of his paternal uncle." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.1.3"> Book 58, Number 58.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Zayd ibn Aslam that the Messenger of Allah, may Allah bless him and grant him peace, said, "Give to a beggar even if he comes on a horse." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.1.4"> Book 58, Number 58.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Zayd ibn Aslam from Amr ibn Muadh al-Ashali al-Ansari that his grandmother said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'O trusting women! Let none of you despise giving to her neighbour even if it is only a roasted sheep's trotter.' </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.1.5"> Book 58, Number 58.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard that a beggar asked A'isha, the wife of the Prophet, may Allah bless him and grant him peace, for something while she was fasting and there was only a loaf of bread in her house. She said to her female mawla, "Give it to him." The mawla protested, "You will not have anything to break your fast with." A'isha repeated, "Give it to him," so she did so. When evening came, the people of a house or a man who did not usually give to them, gave them a sheep and some food to go with it. A'isha, umm al-muminin, called her mawla and said, "Eat from this. This is better than your loaf of bread." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.1.6"> Book 58, Number 58.1.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me that Malik said, "I heard that a beggar asked for food from A'isha, umm al-muminin, while she had some grapes. She told some one to take him one grape. He began to look in amazement. A'isha said, 'Are you amazed? How many atoms' weights do you see in this grape?' " (referring to Sura 99 ayat 7). </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Refraining from Asking </h3>
        <hr>
        <em> <a name="058.58.2.7"> Book 58, Number 58.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Malik from Ibn Shihab from Ata ibn Yazid al-Laythi from Abu Said al-Khudri that some people of the Ansar asked the Messenger of Allah, may Allah bless him and grant him peace, and he gave to them. Then they asked him again, and he gave to them until he used up what he had. Then he said, "What wealth I have, I will not hoard from you. Whoever has forbearance, Allah will help him. Whoever tries to be independent, Allah will enrich him. Whoever tries to be patient, Allah will give him patience, and no one is given a better or vaster gift than patience." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.2.8"> Book 58, Number 58.2.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said from the mimbar when mentioning sadaqa and refraining from asking, "The upper hand is better than the lower hand. The upper hand is the one which expends, and the lower one is the one which asks." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.2.9"> Book 58, Number 58.2.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, sent a gift to Umar ibn al-Khattab, and Umar returned it. The Messenger of Allah, may Allah bless him and grant him peace, said, "Why did you return it?" He said, "Messenger of Allah, didn't you tell us that it is better for us not to take anything from anyone?" The Messenger of Allah, may Allah bless him and grant him peace, said, "That is by asking. Provision which Allah gives you is different from asking." Umar ibn al-Khattab said, "By the One in whose hand my self is, I will not ask anything from anyone, and anything that comes to me without my asking for it, I will accept." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.2.10"> Book 58, Number 58.2.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "By Him in whose hand myself is! To take your rope and gather firewood on your back is better for you than that you come to a man to whom Allah has given some of His favour and ask him, so he gives to you or refuses." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.2.11"> Book 58, Number 58.2.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that a man of the Banu Asad said, "My family and I dismounted to rest at Baqi. My family said to me, 'Go to the Messenger of Allah, may Allah bless him and grant him peace, and ask him for something that we can eat,' and they began to mention their need. I went to the Messenger of Allah, may Allah bless him and grant him peace, and found that a man was asking for something, and the Messenger of Allah, may Allah bless him and grant him peace, was saying, 'I do not have anything to give you.' The man turned away from him in anger, saying, 'By my life! You give to whomever you wish.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'He is angry with me because I do not have anything to give him. Whoever asks of you while he has an uqiya or its like, has asked with importunity.' " </p>
          <p> The man continued, "I said to myself about a camel that we had, 'It is better than an uqiya.' (Malik explained that an uqiya was forty dirhams.) So I returned and did not ask him for anything, and the Messenger of Allah, may Allah bless him and grant him peace, sent me barley and raisins after that. He gave us from his share until Allah, the Mighty, the Majestic gave us relief." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.2.12"> Book 58, Number 58.2.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related that Malik heard al-Ala ibn Abd ar-Rahman say, "Sadaqa does not decrease property, and Allah only increases a slave in worth for his restraint, and no slave is humble but that Allah raises him." </p>
          <p> Malik said, "I do not know whether this hadith goes back to the Prophet, may Allah bless him and grant him peace, or not." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What Is Disliked in Sadaqa </h3>
        <hr>
        <em> <a name="058.58.3.13"> Book 58, Number 58.3.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard that the Messenger of Allah, may Allah bless him and grant him peace, said, "Sadaqa to the family of Muhammad is not halal. It is only people's impurities." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.3.14"> Book 58, Number 58.3.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr from his father that the Messenger of Allah, may Allah bless him and grant him peace, gave a man from the Banu Abd al-Ashal charge over some sadaqa. When he came to ask him for some camels from the sadaqa, the Messenger of Allah, may Allah bless him and grant him peace, was so angry that the anger showed in his face. One way in which anger could be recognised in his face was that his eyes became red. Then he said, "This man has asked me for what is not good for me or him. If I refuse it, I hate to refuse. If I give it to him, I will give him what is not good for me or him." The man said, "Messenger of Allah! I will never ask you for any of it!" </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="058.58.3.15"> Book 58, Number 58.3.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam that his father said, "Abdullah ibn al-Arqam said, 'Show me a riding-camel which the amir al-muminim can give me to use.' I said, 'Yes. One of the sadaqa camels.' Abdullah ibn al-Arqam said, 'Would you want a stout man on a hot day to wash for you what is under his lower garment and its folds, and then give it to you to drink?' I was angry and said, 'May Allah forgive you! Why do you say such things to me?' Abdullah ibn al-Arqam said, 'Sadaqa is the impurities of people which they wash off themselves.' " </p>
        </blockquote>
        <!--EndHadithBody-->
