      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 14: </a> </em>
        <h1> <em> The Qibla </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Prohibition against Relieving Oneself Facing the Qibla </h3>
        <hr>
        <em> <a name="014.14.1.1"> Book 14, Number 14.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ishaq ibn Abdullah ibn Abi Talha that Rafiibn Ishaq, a mawla of the family of ash-Shifa who was known as the mawla of Abu Talha, heard Abu Ayyub al-Ansari, one of the companions of the Messenger of Allah, may Allah bless him and grant him peace, say, while he was in Egypt, "By Allah! I don't know how to deal with these lavatories." The Messenger of Allah, may Allah bless him and grant him peace, said, "When you go to defecate or urinate, do not expose your genitals towards the qibla, and do not put your back to it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.1.2"> Book 14, Number 14.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from one of the Ansar that the Messenger of Allah, may Allah bless him and grant him peace, forbade defecating or urinating while facing the qibla. </p>
          <p> 14.2 Permission to Face the Qibla when Urinating or Defecating </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Permission to Face the Qibla when Urinating or Defecating </h3>
        <hr>
        <em> <a name="014.14.2.3"> Book 14, Number 14.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Muhammad ibn Yahya ibn Habban from his paternal uncle, Wasi ibn Habban, that Abdullah ibn Umar said, "People say, 'When you sit to relieve yourself, do not face the qibla or the Bayt al-Maqdis.' " </p>
          <p> Abdullah continued, "I went upon top of a house of ours and saw the Messenger of Allah, may Allah bless him and grant him peace, (squatting) on two unfired bricks facing the Bayt al-Maqdis, relieving himself." </p>
          <p> Ibn Umar added, "Perhaps you are one of those who pray folded on their haunches." </p>
          <p> Wasi replied, "I don't know, by Allah!" </p>
          <p> Malik said that he meant som one who, when he prostrated, kept his body close to the ground. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Prohibition against Spitting towards the Qibla </h3>
        <hr>
        <em> <a name="014.14.3.4"> Book 14, Number 14.3.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, saw spittle on the wall of the qibla and scraped it off. Then he went up to the people and said, "Do not spit in front of you when you are praying, because Allah, the Blessed and Exalted, is in front of you when you pray." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.3.5"> Book 14, Number 14.3.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father from A'isha, the wife of the Prophet, mayAllah bless him and grant him peace, that the Prophet, may Allah bless him and grant him peace, saw spittle, or mucus or phlegm, on the wall of the qibla and scraped it off. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.3.6"> Book 14, Number 14.3.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Dinar that Abdullah ibn Umar said, "On one occasion when the people were praying subhat Quba a man came to them and said, 'A piece of Qur'an was sent down to the Messenger of Allah, may Allah bless him and grant him peace, last night, and he was ordered to face the Kaba, so face it.'  They had been facing ash-Sham, so they turned round and faced the Kaba.'' </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.3.7"> Book 14, Number 14.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab said, "The Messenger of Allah, may Allah bless him and grant him peace, prayed towards the Baytal-Maqdis for sixteen months after arriving in Madina. Then the qibla was moved, two months before the battle of Badr. " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.3.8"> Book 14, Number 14.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Umar ibn al-Khattab said, "Any direction that is between east and west can be taken as a qibla if the person praying is face-on to the House." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Concerning the Mosque of the Prophet, may Allah Bless Him and Grant Him Peace </h3>
        <hr>
        <em> <a name="014.14.4.9"> Book 14, Number 14.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Rabah and Ubaydullah ibn Abi Abdullah Salman al-Agharr from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "A prayer in this mosque of mine is better than a thousand prayers in any other mosque, except the Masjid al-Haram (in Makka)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.4.10"> Book 14, Number 14.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Khubayb ibn Abd ar-Rahman from Hafs ibn Asim from Abu Hurayra or from Abu Said al-Khudri that the Messenger of Allah, may Allah bless him and grant him peace, said, "What is between my house and my mimbar is one of the meadows of the Garden, and my mimbar is on my watering-place (al-Hawd)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.4.11"> Book 14, Number 14.4.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr from Abbad ibn Tamim from Abdullah ibn Zayd al-Mazini that the Messenger of Allah, may Allah bless him and grant him peace, said, "What is between my house and my mimbar is one of the meadows of the Garden." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Concerning Women Going Out to the Mosque </h3>
        <hr>
        <em> <a name="014.14.5.12"> Book 14, Number 14.5.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Abdullah ibn Umar said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'Do not forbid the female slaves of Allah from (going into) the mosques of Allah.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.5.13"> Book 14, Number 14.5.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard from Busr ibn Said that the Messenger of Allah, may Allah bless him and grant him peace, said, "None of you women should use perfume when you are present at the isha prayer." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.5.14"> Book 14, Number 14.5.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Atika bint Zayd ibn Amr ibn Nufayl, the wife of Umar ibn al-Khattab, used to ask Umar ibn al-Khattab for permission to go to the mosque. He would keep silent, so she would say, "By Allah, I will go out, unless you forbid me," and he would not forbid her. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="014.14.5.15"> Book 14, Number 14.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Amra bint Abd ar-Rahman that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "If the Messenger of Allah, may Allah bless him and grant him peace, had seen what women do now, he would have forbidden them to go into the mosques,  just as the women of the Bani Israil were forbidden." </p>
          <p> Yahya ibn Said said that he asked Amra, "Were the women of the Bani Israil forbidden to go into the mosques?" and she said, "Yes." </p>
        </blockquote>
        <!--EndHadithBody-->
