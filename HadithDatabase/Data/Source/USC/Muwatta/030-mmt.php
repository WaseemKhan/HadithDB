      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 30: </a> </em>
        <h1> <em> Suckling </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Suckling of the Young </h3>
        <hr>
        <em> <a name="030.30.1.1"> Book 30, Number 30.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr from Amra bint Abd ar-Rahman that A'isha, umm al-muminin informed her that the Messenger of Allah, may Allah bless him and grant him peace, was with her and she heard the voice of a man asking permission to enter the room of Hafsa. A'isha said that she had said, "Messenger of Allah! There is a man asking permission to enter your house!" The Messenger of Allah, may Allah bless him and grant him peace, said, "I think it is so-and-so" (referring to a paternal uncle of Hafsa by suckling). A'isha said, "Messenger of Allah! If so-and-so were alive (referring to her paternal uncle by suckling) could he enter where I am?" The Messenger of Allah, may Allah bless him and grant him peace, said, "Yes. Suckling makes haram as birth makes haram." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.2"> Book 30, Number 30.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, umm al-muminin said, "My paternal uncle by suckling came to me and I refused to give him permission to enter until I had asked the Messenger of Allah, may Allah bless him and grant him peace, about it. The Messenger of Allah, may Allah bless him and grant him peace, came and I asked him about it. He said, 'He is your paternal uncle, so give him permission.' So I said, 'Messenger of Allah! The woman nursed me not the man.' He said, 'He is your paternal uncle, so let him enter.' " </p>
          <p> A'isha said, "That was after the veil had been imposed on us." </p>
          <p> A'isha added, "What is haram by birth is made haram by suckling." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.3"> Book 30, Number 30.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Urwa ibn az-Zubayr that A'isha umm al-muminin told him that Aflah, the brother of Abu'l-Quays came and asked permission to visit her after the veil had been lowered, and he was her paternal uncle by suckling. She said, "I refusedto give him permission to enter. When the Messenger of Allah, may Allah bless him and grant him peace, came, I told him about what I had done, and he ordered me to give him permission to enter." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.4"> Book 30, Number 30.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Thawr ibn Zayd ad-Dili that Abdullah ibn Abbas said, "The milk which a child under two years old sucks, even if it is only one suck, makes the foster relatives haram." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.5"> Book 30, Number 30.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Amr ibn ash-Sharid that Abdullah ibn Abbas was asked whether, if a man had two wives, and one of them nursed a slave-boy, and the other had nursed a slave-girl, could the slave-boy marry the slave-girl. He said, "No. The husband is the same." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.6"> Book 30, Number 30.1.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar said, "There is no kinship by suckling except for a person who is nursed when he is small. There is no kinship by suckling over the age of two years." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.7"> Book 30, Number 30.1.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Salim ibn Abdullah ibn Umar informed him that A'isha umm al-muminin sent him away while he was being nursed to her sister Umm Kulthum bint Abi Bakr as-Siddiq and said, "Suckle him ten times so that he can come in to see me." </p>
          <p> Salim said, "Umm Kulthum nursed me three times and then fell ill, so that she only nursed me three times. I could not go in to see A'isha because Umm Kulthum did not finish for me the ten times." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.8"> Book 30, Number 30.1.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Safiyya bint Abi Ubayd told him that Hafsa, umm al-muminin, sent Asim ibn Abdullah ibn Sad to her sister Fatima bint Umar ibn al-Khattab for her to suckle him ten times so that he could come in to see her. She did it, so he used to come in to see her. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.9"> Book 30, Number 30.1.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abd ar-Rahman ibn al-Qasim that his father told him that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, admitted those whom her sisters and the daughters of her brother had nursed, and she did not admit those who were nursed by the wives of her brothers. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.10"> Book 30, Number 30.1.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Ibrahim ibn Uqba asked Said ibn al-Musayyab about suckling. Said said, "All that occurs in the first two years, even if it is only a drop, makes haram. Whatever is after two years, is only food that is eaten." </p>
          <p> Ibrahim ibn Uqba said, ''Then I asked Urwa ibn az-Zubayr and he told me the same as what Said ibn al-Musayyab said." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.1.11"> Book 30, Number 30.1.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said that he heard Said ibn al-Musayyab say, "Suckling is only while the child is in the cradle. If not, it does not cause flesh and blood relations." </p>
          <p> Yahya related to me from Malik from Ibn Shihab that he said, "Suckling however little or much, makes haram. Kinship by suckling makes men mahram." </p>
          <p> Yahya said that he had heard Malik say, "Suckling, however little or much when it is in the first two years, makes haram. As for what is after the first two years, little or much, it does not make anything haram. It is like food." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Suckling of Older People </h3>
        <hr>
        <em> <a name="030.30.2.12"> Book 30, Number 30.2.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that he was asked about the suckling of an older person. He said, ''Urwa ibn az-Zubayr informed me that Abu Hudhayfa ibn Utba ibn Rabia, one of the companions of the Messenger of Allah, may Allah bless him and grant him peace, who was present at Badr, adopted Salim (who is called Salim, the mawla of Abu Hudhayfa) as the Messenger of Allah, may Allah bless him and grant him peace, adopted Zayd ibn Haritha. He thought of him as his son, and Abu Hudhayfa married him to his brother's sister, Fatima bint al-Walid ibn Utba ibn Rabia, who was at that time among the first emigrants. She was one of the best unmarried women of the Quraysh. When Allah the Exalted sent down in His Book what He sent down about Zayd ibn Haritha, 'Call them after their true fathers. That is more equitable in the sight of Allah. If you do not know who their fathers were then they are your brothers in the deen and your mawali,' (Sura 33 ayat 5) people in this position were traced back to their fathers. When the father was not known, they were traced to their mawla. </p>
          <p> "Sahla bint Suhayl who was the wife of Abu Hudhayfa, and one of the tribe of Amr ibn Luayy, came to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Messenger of Allah! We think of Salim as a son and he comes in to see me while I am uncovered. We only have one room, so what do you think about the situation?' The Messenger of Allah, may Allah bless him and grant him peace, said, 'Give him five drinks of your milk and he will be mahram by it.' She then saw him as a foster son. A'isha umm al-muminin took that as a precedent for whatever men she wanted to be able to come to see her. She ordered her sister, Umm Kulthum bint Abi Bakr as-Siddiq and the daughters of her brother to give milk to whichever men she wanted to be able to come in to see her. The rest of the wives of the Prophet, may Allah bless him and grant him peace, refused to let anyone come in to them by such nursing. They said, 'No! By Allah! We think that what the Messenger of Allah, may Allah bless him and grant him peace, ordered Sahla bint Suhayl to do was only an indulgence concerning the nursing of Salim alone. No! By Allah! No one will come in upon us by such nursing!' </p>
          <p> "This is what the wives of the Prophet, may Allah bless him and grant him peace, thought about the suckling of an older person." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.2.13"> Book 30, Number 30.2.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Abdullah ibn Dinar said, "A man came to Abdullah ibn Umar when I waswith him at the place where judgments were given and asked him about the suckling of an older person. Abdullah ibn Umar replied, 'A man came to Umar ibn al-Khattab and said, 'I have a slave-girl and I used to have intercourse with her. My wife went to her and suckled her. When I went to the girl, my wife told me to watch out, because she had suckled her!' Umar told him to beat his wife and to go to his slave-girl because kinship by suckling was only by the suckling of the young.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.2.14"> Book 30, Number 30.2.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that a man said to Abu Musa al-Ashari, "I drank some milk from my wife's breasts and it went into my stomach." Abu Musa said, "I can only but think that she is haram for you." Abdullah ibn Masud said, "Look at what opinion you are giving the man." Abu Musa said, "Then what do you say?" Abdullah ibn Masud said, "There is only kinship by suckling in the first two years." </p>
          <p> Abu Musa said, "Do not ask me about anything while this learned man is among you." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Suckling in General </h3>
        <hr>
        <em> <a name="030.30.3.15"> Book 30, Number 30.3.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Dinar from Sulayman ibn Yasar and from Urwa ibn az-Zubayr from A'isha umm al-muminin, that the Messenger of Allah, may Allah bless him and grant him peace, said, " What is haram by birth is haram by suckling." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.3.16"> Book 30, Number 30.3.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Muhammad ibn Abd ar-Rahman ibn Nawfal said, ''Urwa ibn az-Zubayr informed me from A'isha umm al-muminin that Judama bint Wahb al-Asadiyya informed her that she heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'I intended to prohibit ghila but I remembered that the Greeks and Persians do that without it causing any injury to their children.' " </p>
          <p> Malik explained, "Ghila is that a man has intercourse with his wife while she is suckling." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="030.30.3.17"> Book 30, Number 30.3.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr ibn Hazm from Amra bint Abd ar-Rahman that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "Amongst what was sent down of the Qur'an was 'ten known sucklings make haram' - then it was abrogated by 'five known sucklings'. When the Messenger of Allah, may Allah bless him and grant him peace, died, it was what is now recited of the Qur'an." </p>
          <p> Yahya said that Malik said, "One does not act on this." </p>
        </blockquote>
        <!--EndHadithBody-->
