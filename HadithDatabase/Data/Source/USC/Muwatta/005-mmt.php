      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 5: </a> </em>
      <h1> <em> Jumu'a </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: Doing Ghusl on the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.1.1"> Book 5, Number 5.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr ibn Abd ar-Rahman, from Abu Salih as-Sammani from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "If someone does ghusl for major ritual impurity on the day of jumua and then goes in the first part of the time, it is as if he had offered up a camel. If he goes in the second part of the time, it is as if he had offered up a cow. If he goes in the third part of the time, it is as if he had offered up a horned ram. If he goes in the fourth part of the time, it is as if he had offered up a hen. If he goes in the fifth part of the time, it is as if he had offered up an egg. And when the imam comes out, the angels settle down listening to the dhikr (remembrance of Allah)." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.1.2"> Book 5, Number 5.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Said ibn Abi Said al-Maqburi that Abu Hurayra used to say, "Doing ghusl as prescribed for major ritual impurity is incumbent (wajib) on the day of jumua on every male who has reached puberty." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.1.3"> Book 5, Number 5.1.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Salim ibn Abdullah said, "One of the companions of the Messenger of Allah, may Allah bless him and grant him peace, came into the mosque on the day of jumua and Umar ibn al Khattab was already giving the khutba. Umar said, 'What (kind of) time is this (to arrive)?'  He said, Amir al-muminin, I returned from the market and heard the call to prayer, so I did no more than do wudu.' Umar said, 'You only did wudu as well? You know that the Messenger of Allah, may Allah bless him and grant him peace, used to tell people to do ghusl.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.1.4"> Book 5, Number 5.1.4: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Safwan ibn Sulaym from Ata ibn Yasar from Abu Said al-Khudri that the Messenger of Allah, may Allah bless him and grant him peace, said, "Ghusl on the day of jumua is incumbent on every male who has reached puberty." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.1.5"> Book 5, Number 5.1.5: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi from Ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "When you come to jumua, do ghusl." </p>
        <p> Malik said, "It is not enough for someone to do a ghusl on the day of jumua and intend by it the ghusl for jumua unless he does the ghusl and then sets off. That is because the Messenger of Allah, may Allah bless him and grant him peace, said in the hadith related by Ibn Umar, 'When you come to jumua, do ghusl .' " </p>
        <p> Malik said, "If someone does ghusl on the day of jumua and intends by it the ghusl of the day of jumua and then sets out, whether early or late, and does something which breaks his wudu, he only has to do wudu and his ghusl remains valid for him." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Paying Atlention when the Imam is Giving the Khutba on the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.2.6"> Book 5, Number 5.2.6: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'z Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Even saying to your companion 'Listen' while the imam is giving the khutba on the day of jumua, is to speak foolishly." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.2.7"> Book 5, Number 5.2.7: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Thalaba ibn Abi Malik al Quradhi informed him that in the time of Umar ibn al Khattab they used to pray on the day of jumua until Umar came out, and when Umar came out and sat on the mimbar and the muadhdhins called the adhan, they would sit and talk, and then when the muadhdhins were silent and Umar stood to give the khutba, they would pay attention and no-one would speak . </p>
        <p> Ibn Shihab said, "The imam coming out stops prayer and his speaking stops conversation." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.2.8"> Book 5, Number 5.2.8: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'n Nadr, the mawla of Umar ibn Ubaydullah, from Malik ibn Abi Amir that Uthman ibn Affan used to say in khutbas, and he would seldom omit it if he was giving the khutba, "When the imam stands delivering the khutba on the day of jumua, listen and pay attention, for there is the same portion for someone who pays attention but cannot hear as for someone who pays attention and hears. And when the iqama of the prayer is called, straighten your rows and make your shoulders adjacent to each other, because the straightening of the rows is part of the completion of the prayer." Then he would not say the takbir until some men who had been entrusted with straightening the rows came and told him that they were straight. Then he would say the takbir. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.2.9"> Book 5, Number 5.2.9: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar saw two men talking while the imam was giving the khutba on the day of jumua and he threw pebbles at them to tell them to be quiet. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.2.10"> Book 5, Number 5.2.10: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that a man sneezed on the day of jumua while the imam was giving the khutba, and a man by his side asked Allah to bless him. Said ibn al Musayyab was asked about it and he forbade the man to do what he had done and said, "Don't do it again." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.2.11"> Book 5, Number 5.2.11: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he asked Ibn Shihab about talking in the jumua after the imam had come down from the mimbar but before he had said the takbir. Ibn Shihab said, "There is no harm in that." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Concerning Someone Who Catches a Raka of the Jumua Prayer </h3>
      <hr>
      <em> <a name="005.5.3.12"> Book 5, Number 5.3.12: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Ibn Shihab used to say, "Some one who catches a raka of the jumua prayer should pray another one with it." Ibn Shihab said, "That is the sunna." </p>
        <p> Malik said, "I saw the people of knowledge in our city doing that. That is because the Messenger of Allah, may Allah bless him and grant him peace, said, 'Whoever catches a raka of the prayer has caught the prayer.' " </p>
        <p> Malik said, concerning some one who was in a crowd on the day of jumua and did the ruku but was not able to go into sajda until the imam had risen or finished his prayer, "If he is able to do the sajda and has already done the ruku then he should do the sajda when the people stand up. If he is unable to do thesajda until after the imam has finished the prayer, then I prefer that he begins the prayeragain and does the four rakas of dhuhr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Nose Bleeds on the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.4.13"> Book 5, Number 5.4.13: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik said, "Someone who has a nosebleed on the day of jumua while the imam is giving the khutba and he leaves and does not come back until the imam has finished the prayer, should pray four rakas.'' </p>
        <p> Malik said that someone who prayed a raka with the imam on the day of jumua, then had a nosebleed and left and came back and the imam had prayed both rakas, should complete the prayer with another raka as long as he had not spoken. </p>
        <p> Malik said, "If some one has a nosebleed, or something happens to him and he has no alternative but to leave, he does not have to ask permission of the imam if he wants to leave on the day of jumua." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Making Haste on the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.5.14"> Book 5, Number 5.5.14: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had asked Ibn Shihab about the word of Allah, the Majestic, the Mighty, "O you who accept, when the call is made for the prayer on the day of jumua, make haste to the remembrance of Allah."(Sura 62 ayat 9). Ibn Shihab said, ''Umar ibn al-Khattab used to recite, 'When the call is made for the prayer on the day of jumua, go to the remembrance of Allah.' " </p>
        <p> Malik said, "Making haste in the Book of Allah is only deed and action. Allah the Blessed, the Exalted, says 'and when he turns away, he acts in the land' (Sura 2 ayat 205), and He, the Exalted, said, 'and as for the one who comes to you acting with fear' (Sura 80 ayat 8), and He said, 'then he turned his back, and acted' (Sura 79 ayat 22),and He said, 'Your deeds are diverse' " (Sura 92  ayat 4). Malik said, "Thus making haste which Allah mentions in His Book is not running on the feet or exertion. It only means deed and actions." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Imam's Stopping Off in a Town on the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.6.15"> Book 5, Number 5.6.15: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik said, "If the imam stops off on a journey in a town where jumua is obligatory and he gives a khutba and takes the jumua prayer for them, then the people of the town and any other people present do the jumua prayer with him." </p>
        <p> Malik said, "If the imam gathers people for prayer while he is travelling in a town where the jumua prayer is not obligatory, then there is no jumua for him, nor for the people of the town, nor for anyone else who joins them for the prayer in congregation, and the people of the town and anyone else who is not travelling should complete the prayer." </p>
        <p> Malik added, "A traveller does not have to do jumua " </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Special Time in the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.7.16"> Book 5, Number 5.7.16: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'z Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, mentioned the day of jumua and said, "There is a time in it when Allah gives to a muslim slave standing in prayer whatever he asks for," and the Messenger of Allah, may Allah bless him and grant him peace, indicated with his hand how small it was. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.7.17"> Book 5, Number 5.7.17: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yazid ibn Abdullah ibn al-Had from Muhammad ibn Ibrahim ibn al-Harith at-Taymi from Abu Salama ibn Abd ar-Rahman ibn Awf that Abu Hurayra said, "I went out to at-Tur (Mount Sinai) and met Kab al Ahbar and sat with him. He related to me things from the Tawrah and I related to him things from the Messenger of Allah, may Allah bless him and grant him peace. Among the things I related to him was that the Messenger of Allah, may Allah bless him and grant him peace, said, 'The best of days on which the sun rises is the day of jumua. In it Adam was created, and in it he fell from the Garden. In it he was forgiven, and in it he died. In it the Hour occurs, and every moving thing listens from morning till sunset in apprehension of the Hour except jinn and men. In it is a time when Allah gives toa muslim slave standing in prayer whatever he asks for.' Kab said, 'That is one day in every year.' I said, 'No, in every jumua.' Then Kab recited the Tawrah and said, 'The Messenger of Allah has spoken the truth.' " </p>
        <p> Abu Hurayra continued, "I met Basra ibn Abi Basra al-Ghiffari and he said, 'Where have you come from?' I said, 'From at-Tur.' He said, 'If I had seen you before you left, you would not have gone. I heard the Messenger of Allah, may Allah bless him and grant him peace, say, "Only make a special journey to three mosques:  the mosque of the Haram (Makka), this mosque (Madina), and the mosque of Ilya or the Bait al-Maqdis (two names of Jerusalem)." ' " (He was not sure which expression was used.) </p>
        <p> Abu Hurayra continued, "Then I met Abdullah ibn Salam and I told him that I had sat with Kabal-Ahbar, and I mentioned what I had related to him about the day of jumua, and told him that Kab had said, 'That is one day in every year.' Abdullah ibn Salam said, 'Kab lied,' and I added, 'Kab then recited the Tawrah and said, "No, it is in every jumua.''  ' Abdullah ibn Salam said, 'Kab spoke the truth. 'Then Abdullah ibn Salam said, 'I know what time that is.' " </p>
        <p> Abu Hurayra continued, "I said to him, 'Let me know it - don't keep it from me.' Abdullah ibn Salam said, 'It is the last period of time in the dayof jumua.' " </p>
        <p> Abu Hurayra continued, "I said, 'How can it be the last period of time in the day of jumua, when the Messenger of Allah, may Allah bless him and grant him peace, said, "a muslim slave standing in prayer", and that is a time when there is no prayer?' Abdullah ibn Salam replied, 'Didn't the Messenger of Allah, may Allah bless him and grant him peace, say, "Whoever sits waiting for the prayer is in prayer until he prays?" "' </p>
        <p> Abu Hurayra added, "I said, 'Of course.' He said, 'Then it is that.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Good Appearance and Not Stepping Over People and Facing the Imam on the Day of Jumua </h3>
      <hr>
      <em> <a name="005.5.8.18"> Book 5, Number 5.8.18: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, said, "There is nothing wrong in wearing two garments which are not work-clothes for jumua." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.8.19"> Book 5, Number 5.8.19: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar would never go to jumua without wearing oil and perfume except when it was forbidden (i.e. when he was muhrim). </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.8.20"> Book 5, Number 5.8.20: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi from Abdullah ibn Abi Bakr ibn Hazm from whoever related it to him that Abu Hurayra used to say, "It is better for a man to pray on the surface of al Harra (a rocky area in Madina) than for him to wait until the imam stands to give the khutba and then come and step over people's necks." </p>
        <p> Malik said, "The sunna with us is that the people face the imam on the day of jumua when he intends to give the khutba, whether they are near the qibla or elsewhere." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Recitation in the Jumua Prayer, the Sitting, and Missing the Prayer Without a Reason </h3>
      <hr>
      <em> <a name="005.5.9.21"> Book 5, Number 5.9.21: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Damra ibn Said al-Mazini from Ubaydullah ibn Abdullah ibn Utba ibn Masud that ad-Dahhak ibn Qays asked an-Numan ibn Bashir, "What did the Messenger of Allah, may Allah bless him and grant him peace, use to recite on the day of jumua after suratal-Jumua (Sura 62)?" He said, "He used to recite al-Ghashiya (Sura 88)." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.9.22"> Book 5, Number 5.9.22: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Safwan ibn Sulaym said, "If anyone omits jumua three times without reason or illness, Allah will set a seal on his heart." (Malik said, "I don't know if it was from the Messenger of Allah, may Allah bless him and grant him peace, or not.") </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="005.5.9.23"> Book 5, Number 5.9.23: </a> </em>
