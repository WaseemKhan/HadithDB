      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 45: </a> </em>
        <h1> <em> Madina </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Dua for Madina and Its People </h3>
        <hr>
        <em> <a name="045.45.1.1"> Book 45, Number 45.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya ibn Yahya related to me from Ishaq ibn Abdullah ibn Abi Talha al-Ansari from Anas ibn Malik that the Messenger of Allah, may Allah bless him and grant him peace, said, "O Allah! Bless them in their measure, and bless them in their sa and mudd." He meant the people of Madina. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.1.2"> Book 45, Number 45.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Suhayl ibn Abi Salih from his father that Abu Hurayra said, "When people saw the first fruits of the season, they brought them to the Messenger of Allah, may Allah bless him and grant him peace. The Messenger of Allah, may Allah bless him and grant him peace, took them and said, 'O Allah! Bless us in our fruits. Bless us in our city. Bless us in our sa and bless us in our mudd. O Allah! Ibrahim is Your slave, Your Khalil and Your Prophet. I am Your slave and Your Prophet. He prayed to You for Makka. I pray to You for Madina for the like of what He prayed to You for Makka, and the like of it with it.' Then he called the smallest child he saw and gave him the fruits." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Residing in Madina and Leaving It </h3>
        <hr>
        <em> <a name="045.45.2.3"> Book 45, Number 45.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Qatan ibn Wahb ibn Umayr ibn al-Ajda that Yuhannas, the mawla of az-Zubayr ibn al-Awwam informed him that he was sitting with Abdullah ibn Umar during the troubles (at the time of al-Hajaj ibn Yusuf) . A female mawla of his came and greeted him. She said, "I want to leave, Abu Abd ar-Rahman. The time is harsh for us." Abdullah ibn Umar said to her, "Sit down, O you with little knowledge, for I have heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'No one will be patient in hunger and hardship in it (Madina) except that I will be a witness or intercede for him on the Day of Rising.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.2.4"> Book 45, Number 45.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Muhammad ibn al-Munkadir from Jabir ibn Abdullah that a Bedouin took an oath of allegiance in Islam with the Messenger of Allah, may Allah bless him and grant him peace. A fever befell the Bedouin at Madina. He came to the Messenger of Allah, and said, "Messenger of Allah, release me from my pledge." The Messenger of Allah, may Allah bless him and grant him peace, refused. Then he came to him again and said, "Release me from my pledge." The Messenger of Allah may Allah bless him and grant him peace, refused. Then he came again and said, "Release me from my pledge." He refused. Then he came again and said, "Release me from my pledge." He refused. The Bedouin left and the Messenger of Allah, may Allah bless him and grant him peace, said, "Madina is like the blacksmith's furnace. It removes the impurities and purifies the good." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.2.5"> Book 45, Number 45.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Yahya ibn Said said, "I heard Abu'l-Hubab Said ibn Yasar say that he heard Abu Hurayra say that he heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'I was ordered to a town which will eat up towns. They used to say, 'Yathrib,' but it is Madina. It removes the bad people like the blacksmith's furnace removes impurities from the iron.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.2.6"> Book 45, Number 45.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Hisham ibn Urwa from his father that the Messenger of Allah, may Allah bless him and grant him peace, said, "No one leaves Madina preferring to live elsewhere, but that Allah will give it better than him in place of him ." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.2.7"> Book 45, Number 45.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Hisham ibn Urwa from his father from Abdullah ibn az-Zubayr that Sufyan ibn Abi Zuhayr said, "I heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'Yemen will be conquered and the people will be attracted to it, taking their families and whoever obeys them. Madina would have been better for them, had they but known. Ash-Sham will be conquered and people will be attracted to it, taking their families and whoever obeys them. Madina would have been better for them, had they but known. Iraq will be conquered and people will be attracted to it, taking their families and whoever obeys them. Madina would have been better for them, had they but known.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.2.8"> Book 45, Number 45.2.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Himas from his paternal uncle from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Madina will be left in the best way that it is until a dog or wolf enters it and urinates on one of the pillars of the mosque or on the mimbar." They asked, "Messenger of Allah! Who will have the fruit at that time?" He replied, "Animals seeking food, birds and wild beasts." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.2.9"> Book 45, Number 45.2.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he had heard that when Umar ibn Abd al-Aziz left Madina, he turned towards it and wept. Then he said, "O Muzahim! Do you fear that we might be among those that Madina casts off?" </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Making a Haram of Madina </h3>
        <hr>
        <em> <a name="045.45.3.10"> Book 45, Number 45.3.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Amr, the mawla of al-Muttalib from Anas ibn Malik that the Messenger of Allah, may Allah bless him and grant him peace, saw Uhud and said, "This is a mountain which loves us and we love it. O Allah! Ibrahim made Makka Haram, and I will make what is between the two tracts of black stones (in Madina) a Haram." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.3.11"> Book 45, Number 45.3.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Said ibn al-Musayyab that Abu Hurayra said, "Had I seen a gazelle at Madina, I would have left it to graze and would not have frightened it. The Messenger of Allah, may Allah bless him and grant him peace, said, 'What is between the two tracts of black stones is a Haram.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.3.12"> Book 45, Number 45.3.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yunus ibn Yusuf from Ata ibn Yasar that Abu Ayyub al-Ansari found some boys who had driven a fox into a corner, and he chased them away from it. </p>
          <p> Malik said, "I only know that he said, 'Is this done in the Haram of the Messenger of Allah, may Allah bless him and grant him peace?' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.3.13"> Book 45, Number 45.3.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from a man who said, "Zayd ibn Thabit came across me while I was at al-Aswaf (on the outskirts of Madina). I had captured a hawk. He took it from my hands and set it free." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Epidemic of Madina </h3>
        <hr>
        <em> <a name="045.45.4.14"> Book 45, Number 45.4.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, umm al-muminin said, "When the Messenger of Allah, may Allah bless him and grant him peace, came to Madina, Abu Bakr and Bilal came down with a fever. I visited them and said, 'Father, how are you? Bilal, how are you?'" She continued, "When Abu Bakr's fever worsened he would say, 'Every man is struck down among his people in the morning - death is nearer than the strap of his sandal.'" </p>
          <p> When it left Bilal, he raised his voice and said, 'Would that I knew whether I will spend a night at the valley of Makka with the idhkhir herb and jalil herb around me. Will I go one day to the waters of Majinna? Will the mountains of Shama and Tafil appear to me?' " ' </p>
          <p> A'isha continued, "I went to the Messenger of Allah, may Allah bless him and grant him peace, and informed him. He said, 'O Allah! Make us love Madina as much as we love Makka or even more. Make it sound and bless us in our sa and mudd. Remove its fever and put it in al-Juhfa.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.4.15"> Book 45, Number 45.4.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said that Yahya ibn Said had related to him that A'isha said that Amir ibn Fuhayra had said at the time of the epidemic, "I have seen death before tasting it, the coward's destination is from above him." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.4.16"> Book 45, Number 45.4.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nuaym ibn Abdullah al-Mujmir that Abu Hurayra said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'There are angels at the entries of Madina, and neither plague nor the Dajjal will enter it.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Expulsion of the Jews from Madina </h3>
        <hr>
        <em> <a name="045.45.5.17"> Book 45, Number 45.5.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ismail ibn Abi Hakim that he heard Umar ibn Abd al-Aziz say, "One of the last things that the Messenger of Allah, may Allah bless him and grant him peace, said was, 'May Allah fight the jews and the christians. They took the graves of their Prophets as places of prostration . Two deens shall not co-exist in the land of the Arabs.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.5.18"> Book 45, Number 45.5.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that the Messenger of Allah, may Allah bless him and grant him peace, said, "Two deens shall not co-exist in the Arabian Peninsula." </p>
          <p> Malik said that Ibn Shihab said, ''Umar ibn al-Khattab searched for information about that until he was absolutely convinced that the Messenger of Allah, may Allah bless him and grant him peace, had said, 'Two deens shall not co-exist in the Arabian Peninsula,' and he therefore expelled the jews from Khaybar." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.5.19"> Book 45, Number 45.5.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said, ''Umar ibn al-Khattab expelled the jews from Najran (a jewish settlement in the Yemen) and Fadak (a jewish settlement thirty miles from Madina). When the jews of Khaybar left, they did not take any fruit or land. The jews of Fadak took half the fruit and half the land, because the Messenger of Allah, may Allah bless him and grant him peace, had made a settlement with them for that. So Umar entrusted to them the value in gold, silver, camels, ropes and saddle bags of half the fruit and half the land, and handed the value over to them and expelled them." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Concerning Madina </h3>
        <hr>
        <em> <a name="045.45.6.20"> Book 45, Number 45.6.20: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that the Messenger of Allah, may Allah bless him and grant him peace, came in view of Uhud and said, "This is a mountain which loves us and we love it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.6.21"> Book 45, Number 45.6.21: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Abd ar-Rahman ibn al-Qasim that Aslam, the mawla of Umar ibn al-Khattab informed him that he had visited Abdullah ibn Ayyash al-Makhzumi. He saw that he had some nabidh with him and he was at that moment on the way to Makka. Aslam said to him, ''Umar ibn al-Khattab loves this drink." Abdullah ibn Ayyash therefore carried a great drinking bowl and brought it to Umar ibn al-Khattab and placed it before him. Umar brought it near to him and then raised his head. Umar said, "This drink is good," so he drank some of it and then passed it to a man on his right. When Abdullah turned to go, Umar ibn al-Khattab called him and asked, "Are you the person who says that Makka is better than Madina?" Abdullah said, "I said that it was the Haram of Allah, and His place of security, and His House was in it." Umar said, "I am not saying anything about the House of Allah or His Haram." Then Umar repeated "Are you the person who says that Makka is better than Madina?" He replied, "I said that it was the Haram of Allah and His place of security, and His House was in it." Umar said, "I am not saying anything about the House of Allah and His Haram." Then Abdullah left. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: About the Plague </h3>
        <hr>
        <em> <a name="045.45.7.22"> Book 45, Number 45.7.22: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Abd al-Hamid ibn Abd ar-Rahman ibn Zayd ibn al-Khattab from Abdullah ibn Abdullah ibn al-Harith ibn Nawfal from Abdullah ibn Abbas that Umar ibn al-Khattab set out for ash Sham and when he was at Sargh, near Tabuk, the commanders of the army, Abu Ubayda ibn al-Jarrah and his companions, met him and told him that the plague had broken out in ash-Sham. Ibn Abbas said, "Umar ibn al-Khattab said, 'all the first Muhajir unto me.' He assembled them and asked them for advice, informing them that the plague had broken out in ash Sham. They disagreed. Some said, 'You have set out for something, and we do not think that you should leave it.' Others said, 'You have the companions of the Prophet, may Allah bless him and grant him peace, and the rest of the people with you, and we do not think that you should send them towards this plague.' Umar said, 'Leave me.' </p>
          <p> Then he said, 'Summon the Ansar to me.' They were summoned and he asked them for advice. They acted as the Muhajirun had and disagreed as they had disagreed. He said, 'Leave me.' "Then he said, 'Summon to me whoever is here of the aged men of Quraysh from the Muhajirun of the conquest.' He summoned them and not one of them differed. They said, 'We think that you should withdraw the people and not send them towards the plague.' Umar called out to the people, 'I am leaving by camel in the morning,' so they set out. Abu Ubayda said, 'Is it flight from the decree of Allah?' Umar said, 'Better that someone other than you had said it, Abu Ubayda. Yes. We flee from the decree of Allah to the decree of Allah. What would you think if these camels had gone down into a valley which had two slopes, one of them fertile, and the other barren. If you pastured in the fertile part, wouldn't you pasture them by the decree of Allah? If you pastured them in the barren part, wouldn't you pasture them by the decree of Allah?' </p>
          <p> ''Abd ar-Rahman ibn Awf arrived and he had been off doing something and he said, 'I have some knowledge of this. I heard the Messenger of Allah, may Allah bless him and grant him peace, say, "If you hear about it in a land, do not go forward to it. If it comes upon a land and you are in it, then do not depart in flight from it." ' Umar praised Allah and then set off." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.7.23"> Book 45, Number 45.7.23: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Muhammad ibn al-Munkadir and from Salim ibn Abi'n-Nadr, the mawla of Umar ibn Ubaydullah that Amir ibn Sad ibn Abi Waqqas heard his father ask Usama ibn Zayd, "Have you heard anything from the Messenger of Allah, may Allah bless him and grant him peace, about the plague?" Usama said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'The plague is a punishment that was sent down on a party of the Banu Israil or whoever was before them. When you hear of it in a land, do not enter it. If it comes upon a land and you are in it, do not depart in flight from it.' " </p>
          <p> Malik said that Abu'n-Nadr said, "That is, do not depart with no other intention but flight." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="045.45.7.24"> Book 45, Number 45.7.24: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
