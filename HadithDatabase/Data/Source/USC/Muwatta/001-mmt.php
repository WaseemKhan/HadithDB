      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 1: </a> </em>
      <h1> <em> The Times of Prayer </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: The Times of Prayer </h3>
      <hr>
      <em> <a name="001.1.1.1"> Book 1, Number 1.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> He said, "Yahya ibn Yahya al-Laythi related to me from Malik ibn Anas from Ibn Shihab that one day Umar ibn Abdal-Aziz delayed the prayer. Urwa ibn az-Zubayr came and told him that al-Mughira ibn Shuba had delayed the prayer one day while he was in Kufa and Abu Masud al-Ansari had come to him and said, 'What's this, Mughira? Don't you know that the angel Jibril came down and prayed and the Messenger of Allah, may Allah bless him and grant him peace, prayed.' Then he prayed again, and the Messenger of Allah, may Allah bless him and grant him peace, prayed. Then he prayed again, and the Messenger of Allah, may Allah bless him and grant him peace, prayed. Then he prayed again, and the Messenger of Allah, may Allah bless him and grant him peace, prayed. Then he prayed again, and the Messenger of Allah, may Allah bless him and grant him peace, prayed. Then Jibril said, 'This is what you have been ordered to do.'  Umar ibn Abd al-Aziz said, 'Be sure of what you relate, Urwa. Was it definitely Jibril who established the time of the prayer for the Messenger of Allah?' " Urwa said, "That's how it was related to Bashir ibn Abi Masud al-Ansari by his father." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.2"> Book 1, Number 1.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Urwa said that A'isha, the wife of the Prophet, may Allah bless him and grant him peace used to pray asr while the sunlight was pouring into her room, before the sun itself had become visible (i.e. because it was still high in the sky). </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.3"> Book 1, Number 1.1.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam that Ata ibn Yasar said, "A man came to the Messenger of Allah, may Allah bless him and grant him peace, and asked him about the time of the subh prayer. The Messenger of Allah, may Allah bless him and grant him peace, did not answer him, but in the morning he prayed subh at first light. The following morning he prayed subh when it was much lighter, and then said, 'Where is the man who was asking about the time of the prayer?' The man replied, 'Here I am, Messenger of Allah.' He said,'The time is between these two.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.4"> Book 1, Number 1.1.4: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said from Amra bint Abd ar-Rahman that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "The Messenger of Allah, may Allah bless him and grant him peace, used to pray subh and the women would leave wrapped in their garments and they could not yet be recognised in the darkness." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.5"> Book 1, Number 1.1.5: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar and from Busr ibn Said and from al-Araj-all of whom related it from Abu Hurayra - that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever manages to do a raka of subh before the sun has risen has done subh in time, and whoever manages to do a raka of asr before the sun has set has done asr in time." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.6"> Book 1, Number 1.1.6: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi from the mawla of Abdullah ibn Umar that Umar ibn al-Khattab wrote to his governors, saying, "The most important of your affairs in my view is the prayer. Whoever protects it and observes it carefully is protecting his deen, while whoever is negligent about it will be even more negligent about other things." Then he added, "Pray dhuhr any time from when the afternoon shade is the length of your forearm until the length of your shadow matches your height. Pray asr when the sun is still pure white, so that a rider can travel two or three farsakhs before the sun sets. Pray maghrib when the sun has set. Pray isha any time from when the redness in the western sky has disappeared until a third of the night has passed - and a person who sleeps, may he have no rest, a person who sleeps, may he have no rest. And pray subh when all the stars are visible and like a haze in the sky." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.7"> Book 1, Number 1.1.7: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik, from his uncle Abu Suhayl from his father that Umar ibn al-Khattab wrote to Abu Musa saying that he should pray dhuhr when the sun had started to decline, asr when the sun was still pure white before any yellowness had entered it maghrib when the sun had set, and to delay isha as long as he did not sleep, and to pray subh when the stars were all visible and like a haze in the sky and to read in it two long suras from the mufassal. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.8"> Book 1, Number 1.1.8: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that Umar ibn al-Khattab wrote to Abu Musa al-Ashari that he should pray asr when the sun was still pure white so that a man could ride threefarsakhs (before maghrib) and that he should pray isha during the first third of the night, or, if he delayed it, then up until the middle of the night, and he warned him not to be forgetful. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.9"> Book 1, Number 1.1.9: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yazid ibn Ziyad that Abdullah ibn Rafi, the mawla of Umm Salama, the wife of the Prophet, may Allah bless him and grant him peace, asked Abu Hurayra about the time of the prayer. Abu Hurayra said, "Let me tell you. Pray dhuhr when the length of your shadow matches your height, asr when your shadow is twice your height, maghrib when the sun has set, isha in the first third of the night, and subh in the very first light of dawn," i.e. when the dawn has definitely come. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.10"> Book 1, Number 1.1.10: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ishaq ibn Abdullah ibn Abi Talha that Anas ibn Malik said, "We would pray asr and anyone who then went to the Bani Amr ibn Awf would find them praying asr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.11"> Book 1, Number 1.1.11: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Anas ibn Malik said, "We would pray asr and anyone who then went to Quba would arrive there while the sun was still high." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.1.12"> Book 1, Number 1.1.12: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Rabia ibn Abi Abd ar-Rahman that al Qasim ibn Muhammad said, "None of the companions that I met prayed dhuhr until well after noon,"(i.e.until when the sun had lost its fierceness). </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Time of the Jumua Prayer </h3>
      <hr>
      <em> <a name="001.1.2.13"> Book 1, Number 1.2.13: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from his uncle Abu Suhayl ibn Malik that his father said, "I used to see a carpet belonging to Aqil ibn Abi Talib spread out on the day of jumua up to the west wall of the mosque. When the shadow of the wall covered the whole carpet, Umar ibn al-Khattab would come out and pray the jumua prayer." </p>
        <p> Malik, Abu Suhayl's father, added, "We would then return after the jumua prayer and take our midday sleep." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.2.14"> Book 1, Number 1.2.14: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Amr ibn Yahya ibn Yahya al-Mazini from Ibn Abi Salit that Uthman ibn Affan prayed jumua in Madina and asr in Malal (a place seventeen miles from Madina). </p>
        <p> Malik commented, "That was by praying jumua just past midday and then travelling fast." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Catching a Raka of the Prayer </h3>
      <hr>
      <em> <a name="001.1.3.15"> Book 1, Number 1.3.15: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Abu Salama ibn Abdar-Rahman from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said "Whoever catches a raka of the prayer has caught the prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.3.16"> Book 1, Number 1.3.16: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar ibn al-Khattab used to say, "If the ruku has passed you by, so has the sajda." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.3.17"> Book 1, Number 1.3.17: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Abdullah ibn Umar and Zayd ibn Thabit used to say, "Whoever catches the ruku has caught the sajda." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.3.18"> Book 1, Number 1.3.18: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Abu Hurayra used to say, "Whoever catches the ruku has caught the sajda and whoever misses the recitation of the umm al-Qur'an has missed much good." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Duluk ash-Shams and Ghasaq al-Layl </h3>
      <hr>
      <em> <a name="001.1.4.19"> Book 1, Number 1.4.19: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to say, "Duluk ash-shams begins from when the sun passes the meridian." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.4.20"> Book 1, Number 1.4.20: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Da'ud ibn al-Husayn said that someone had told him Abdullah ibn Abbas used to say, "Duluk ash-shams begins from when the sun passes the meridian. Ghasaq al-layl is the gathering of the night and its darkness." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Times of Prayer in General </h3>
      <hr>
      <em> <a name="001.1.5.21"> Book 1, Number 1.5.21: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "If someone misses the asr prayer it is as if he has suffered a great misfortune in his family and wealth ." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.5.22"> Book 1, Number 1.5.22: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said that once Umar ibn al-Khattab left after doing the asr prayer and met a man who had not been there. Umar asked him what had kept him from the prayer and eventhough the man gave a good reason, Umar said, "You have given yourself short measure." </p>
        <p> Yahya added that Malik commented, "It is said that everything has a short measure and a full measure." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.5.23"> Book 1, Number 1.5.23: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik thatYahya ibn Said used to say, "Even if someone manages to pray before the time of the prayer has passed, the time that has passed him by is more important, or better, than his family and wealth." </p>
        <p> Yahya said that Malik said, "If the time for a prayer comes and a traveller delays a prayer through neglect or forgetfulness until he reaches his family, he should do that prayer in full if he arrives within the time. But if he arrives when the time has past, he should do the travelling prayer. That way he only repays what he owes." </p>
        <p> Malik said, "This is what I have found the people and men of knowledge doing in our community." Malik explained that shafaq was the redness in the sky after the sun had set, and said, "When the redness has gone then the isha prayer is due and you have left the time of maghrib." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.5.24"> Book 1, Number 1.5.24: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that one time Abdullah ibn Umar fainted and lost his senses and he did not make up the prayer. </p>
        <p> Malik commented, "We consider that that was because, and Allah knows best, the time had passed. Someone who recovers within the time has to pray." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Sleeping Through the Prayer </h3>
      <hr>
      <em> <a name="001.1.6.25"> Book 1, Number 1.6.25: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab that the Messenger of Allah, may Allah bless him and grant him peace, travelled by night on the way back from Khaybar.Towards the end of the night he stopped for a rest and told Bilal to stay awake to keep watch for the subh prayer. The Messenger of Allah, may Allah bless him and grant him peace, and his companions slept. Bilal stayed on guard as long as was decreed for him and then he leant against his riding camel facing the direction of the dawn and sleep overcame him and neither he nor the Messenger of Allah nor any of the party woke up until the sun's rays had struck them. The Messenger of Allah, may Allah bless him and grant him peace, was alarmed. Bilal excused himself, saying, "Messenger of Allah! The One who took your self was the One who took myself. "The Messenger of Allah, may Allah bless him and grant him peace, ordered the party to move on and so they roused thei r mounts and rode on a short distance. The Messenger of Allah, may Allah bless him and grant him peace, ordered Bilal to give the iqama and then led them in the subh prayer. When he had finished he said, "Anyone who forgets a prayer should pray it when he remembers. Allah theBlessed and Exalted says in His book, 'Establish the prayer to remember Me.'" </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="001.1.6.26"> Book 1, Number 1.6.26: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Zayd ibn Aslam said, "The Messenger of Allah, may Allah bless him and grant him peace, stopped for a rest one night on the way to Makka and appointed Bilal to wake them up for the prayer. Bilal slept and everyone else slept and none of them woke up until the sun had risen. When they did wake up they were all alarmed. The Messenger of Allah, may Allah bless him and grant him peace, ordered them to ride out of the valley, saying that there was a shaytan in it. So they rode out of the valley and the Messenger of Allah, may Allah bless him and grant him peace, ordered them to dismount and do wudu and he told Bilal either to call the prayer or to give the iqama. The Messenger of Allah, may Allah bless him and grant him peace, then led them in the prayer. Noticing their uneasiness, he went to them and said, 'O people! Allah seized our spirits (arwah) and if He had wished He would have returned them to us at a time other than this. So if you sleep through the time for a prayer or forget it and then are anxious about it, pray it as if you were praying it in its time.' The Messenger of Allah, may Allah bless him and grant him peace, turned to Abu Bakr and said, 'Shaytan came to Bilal when he was standing in prayer and made him lie down and lulled him to sleep like a small boy.' The Messenger of Allah, may Allah bless him and grant him peace, then called Bilal and told him the same as he had told Abu Bakr. Abu Bakr declared, 'I bear witness that you are the Messenger of Allah.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Prohibition against Doing the Prayer at the Hottest Hour of the Day </h3>
      <hr>
      <em> <a name="001.1.7.27"> Book 1, Number 1.7.27: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Scorching heat is a part of the blast of Jahannam. So, when the heat is fierce, delay the prayer until it gets cooler." </p>
        <p> He added in explanation, "The Fire complained to its Lord and said, 'My Lord, part of me has eaten another part,' so He allowed it two breaths in every year, a breath in winter and a breath in summer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
