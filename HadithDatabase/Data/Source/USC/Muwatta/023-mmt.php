      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 23: </a> </em>
        <h1> <em> Sacrificial Animals </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Animals to Be Avoided as Sacrifices </h3>
        <hr>
        <em> <a name="023.23.1.1"> Book 23, Number 23.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Amr ibn al-Harith from Ubayd ibn Fayruz from al-Bara ibn Azib that the Messenger of Allah, may Allah bless him and grant him peace, was asked what animals should be avoided as sacrifices. He indicated with his hand and said, "Four." Al-Bara pointed with his hand and said, "My hand is shorter than the hand of the Messenger of Allah, may Allah bless him and grant him peace. A lame animal whose lameness is evident, a one-eyed animal which is clearly one-eyed, an animal which is clearly ill, and an emaciated animal with no fat on it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.1.2"> Book 23, Number 23.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar would guard against animals and camels which were young or had physical defects as sacrifices. </p>
          <p> Malik said, "That is what I like best of what I have heard ." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Animals Desirable as Sacrifices </h3>
        <hr>
        <em> <a name="023.23.2.3"> Book 23, Number 23.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that one time Abdullah ibn Umar wanted to sacrifice an animal at Madina. Nafi said, "He told me to buy him an excellent horned ram, then to sacrifice it on the Day of Sacrifice in the place where the people prayed." Nafi continued, "I did so and when the ram was sacrificed, it was carried to Abdullah ibn Umar who shaved his head. He was ill, and did not attend the Id with the people." Nafi added, "Abdullah ibn Umar used to say, 'Shaving the head is not obligatory for someone who sacrifices an animal.' Ibn Umar would do so however." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Prohibition against Sacrificing an Animal Before the Imam Departs </h3>
        <hr>
        <em> <a name="023.23.3.4"> Book 23, Number 23.3.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> ahya related to me from Malik from Yahya ibn Said from Bushayr ibn Yasar that Abu Burda ibn Niyar sacrificed an animal before the Messenger of Allah, may Allah bless him and grant him peace, sacrificed on the Day of Sacrifice. He asserted that the Messenger of Allah, may Allah bless him and grant him peace, ordered him to sacrifice another animal, and he, Abu Burda, said, "What if I can only find an animal less than one year old, Messenger of Allah?" He had said, "If you can only find a young animal, then sacrifice it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.3.5"> Book 23, Number 23.3.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Abbad ibn Tamin that one time Uwaymir ibn Ashqar sacrificed his animal before the prayer on the morning of the Day of Sacrifice, and he mentioned that the Messenger of Allah, may Allah bless him and grant him peace, had ordered him to sacrifice another animal. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Storing Meat from Sacrificial Animals </h3>
        <hr>
        <em> <a name="023.23.4.6"> Book 23, Number 23.4.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zubayr al-Makki from Jabir ibn Abdullah that the Messenger of Allah, may Allah bless him and grant him peace, forbade that the meat from sacrificial animals be eaten after three days. Then later he said, "Eat, give sadaqa, provide for yourselves and store up." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.4.7"> Book 23, Number 23.4.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr that Abdullah ibn Waqid said, "The Messengerof Allah, may Allah bless him and grant him peace, forbade eating the meat from sacrificial animals after three days." </p>
          <p> Abdullah ibn Abi Bakr said, "I mentioned that to Amra bint Abd ar-Rahman, and she affirmed that he had spoken the truth as she had heard A'isha, the wife of the Prophet, may Allah bless him and grant him peace, say, 'Some people from the desert came at the time of the sacrifice in the time of the Messenger of Allah, may Allah bless him and grant him peace, so the Messenger of Allah, may Allah bless him and grant him peace, said, 'Store up for three days, and give what is left over as sadaqa.' " </p>
          <p> She said that afterwards some one said to the Messenger of Allah, may Allah bless him and grant him peace, that people had been accustomed to make use of their sacrificial animals, melting the fat and curing the skins. The Messenger of Allah, may Allah bless him and grant him peace, said, "What about that?" They said, "You have forbidden the meat of sacrificial animals after three days." The Messenger of Allah, may Allah bless him and grant him peace, said, "I only forbade you for the sake of the people who were coming to you. Eat, give sadaqa and store up." </p>
          <p> By these people, he meant the poor people who were coming to Madina. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.4.8"> Book 23, Number 23.4.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Rabia ibn Abi Abd ar-Rahman that Abu Said alKhudri returned from a journey and his family gave him some meat. He asked whether it was meat from the sacrifice. They replied that it was. Abu Said said, "Didn't the Messenger of Allah, may Allah bless him and grant him peace, forbid that?" They said, "There has been a new command from the Messenger of Allah, may Allah bless him and grant him peace, since you went away." Abu Said went out and made enquiries about it and was told that the Messenger of Allah, may Allah bless him and grant him peace, had said,"I forbade you before to eat meat of the sacrifice after three days, but now eat, give sadaqa and store up. I forbade you before to make nabidh (by soaking raisins or dates in water), but now make nabidh, but remember every intoxicant is haram .I forbade you to visit graves, but now visit them, and do not use bad language." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Sharing Sacrificial Animals and How Many Share a Cow or Camel </h3>
        <hr>
        <em> <a name="023.23.5.9"> Book 23, Number 23.5.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zubayr al-Makki that Jabir ibn Abdullah said, "We sacrificed with the Messenger of Allah, may Allah bless him and grant him peace, in the year of Hudaybiya, a camel between seven people, and a cow between seven people." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.5.10"> Book 23, Number 23.5.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Umara ibn Yasar that Ata ibn Yasar told him that Abu Ayyub al-Ansari had told him, "We used to sacrifice one sheep, and a man sacrificed for himself and his family. Then later on people began to compete with each other and it became boasting." </p>
          <p> Malik said, "The best that I have heard about a single camel, cow or sheep, is that a man should sacrifice a camel for himself and his family. He sacrifices a cow or sheep which he owns for his family, and shares with them in it. It is disapproved for a group of people to buy a camel, cow or sheep, to share for the ritual and sacrifices, each man giving a share of its price, and taking a share of its meat. We have heard the tradition that people do not share in the ritual. However, it may be that the people of one household can share." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.5.11"> Book 23, Number 23.5.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Ibn Shihab said, "The Messenger of Allah, may Allah bless him and grant him peace, only sacrificed one camel or one cow for himself and his family." </p>
          <p> Malik said, "I do not know which of them Ibn Shihab said." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Sacrificial Animal for the Child in the Womb and Mention of the Days of Sacrifice </h3>
        <hr>
        <em> <a name="023.23.6.12"> Book 23, Number 23.6.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar said, "The sacrifice can be done up to two days after the Day of Sacrifice." </p>
          <p> Yahya related to me from Malik that he had heard the same from Ali ibn Abi Talib. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="023.23.6.13"> Book 23, Number 23.6.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar did not sacrifice for the child in the womb. </p>
          <p> Malik said, "The sacrifice is sunna, and it is not obligatory. I prefer that anyone who has the price of the animal should not abandon it.'' </p>
        </blockquote>
        <!--EndHadithBody-->
