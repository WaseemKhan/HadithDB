      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 8: </a> </em>
      <h1> <em> Prayer in Congregation </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: The Superiority of Prayer in Congregation over Prayer Done Alone </h3>
      <hr>
      <em> <a name="008.8.1.1"> Book 8, Number 8.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Prayer in congregation is better than the prayer of a man by himself by twenty-seven degrees." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.1.2"> Book 8, Number 8.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Prayer in congregation is better than the prayer of one of you on his own by twenty-five parts." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.1.3"> Book 8, Number 8.1.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'zZinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "By Him in whose hand myself is! I had in mind to order firewood to be collected, then to order the prayer to be called, and to appoint a man to lead the people in prayer, and then to come up behind certain men and burn their houses down about them! By Him in whose hand myself is! If one of them knew that he would find a meaty bone or two good legs of meat, he would be present at isha.'' </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.1.4"> Book 8, Number 8.1.4: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'n-Nadr, the mawla of Umar ibn Ubaydullah from Yusr ibn Said that Zayd ibn Thabit said, "The most excellent prayer is your prayer in your house, except for the prescribed prayers." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Isha and Subh Prayers </h3>
      <hr>
      <em> <a name="008.8.2.5"> Book 8, Number 8.2.5: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abd ar-Rahman ibn Harmala al-Aslami from Said ibn al-Musayyab that the Messenger of Allah, may Allah bless him and grant him peace, said, "What separates us from the hypocrites is being present at isha and subh. They cannot do it," or words to that effect. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.2.6"> Book 8, Number 8.2.6: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Sumayy, the mawla of Abu Bakr ibn Abd ar-Rahman from Abu Salih from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "If a man who is walking along a road finds a branch of thorns on the road and removes it, Allah thanks him for doing it and forgives him." </p>
        <p> He also said, "Martyrs are five:  the one killed by a plague, the one killed by a disease of the belly, the one who drowns, the one killed by a collapsing building, and the martyr in the path of Allah.' </p>
        <p> He also said, "If people knew what there was in the call to prayer and the first row, and they could find no other way except to draw lots for it, they would draw lots for it. And if they knew what there was in doing dhuhr at its time, they would race each other to it. And if they knew what there was in the prayers of isha and maghrib, they would come to them even if they had to crawl . </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.2.7"> Book 8, Number 8.2.7: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Abu Bakr ibn Sulayman ibn Abi Hathma that Umar ibn al-Khattab missed Sulayman ibn Abi Hathma in the subh prayer. In the morning he went to the market, and Sulayman's house was between the market and the Prophet's mosque. He passed ash-Shifa, Sulayman's mother, and said to her, "I did not see Sulayman at subh." She replied, "He spent the night in prayer and his eyes overcame him. Umar said, "I would rather be present at subh than stand the whole night in prayer. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.2.8"> Book 8, Number 8.2.8: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said from Muhammad ibn Ibrahim that Abd ar-Rahman ibn Abi Amra al-Ansari said that Uthman ibn Affan came to the isha prayer and seeing only a few people in the mosque, he lay down at the back of the mosque to wait for the number of people to increase. Ibn Abi Amra went and sat down beside him and Uthman asked him who he was, so he told him. Uthman said, "What have you memorised of the Qur'an?", and he told him. Uthman said, "If someone is present at isha, it is as if he had stood in prayer for half a night, and if some one is present at subh, it is as if he had stood in prayer for a whole night. </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Doing the Prayer Again with the Imam </h3>
      <hr>
      <em> <a name="008.8.3.9"> Book 8, Number 8.3.9: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam from a man of the Bani'd-Dil called Busr ibn Mihjan from his father Mihjan that he was in a gathering with the Messenger of Allah, may Allah bless him and grant him peace, and the call to prayer was made. The Messenger of Allah, may Allah bless him and grant him peace, rose and prayed and then returned. Mihjan remained sitting and did not pray with him. The Messenger of Allah, may Allah bless him and grant him peace, said, "What prevented you from praying with the people? Aren't you a muslim?" He said, "Of course, Messenger of Allah, but I have already prayed with my family." The Messenger of Allah, may Allah bless him and grant him peace, said, "When you come, pray with the people, even if you have prayed already." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.3.10"> Book 8, Number 8.3.10: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that a man asked Abdullah ibn Umar, "Sometimes I pray in my house, and then catch the prayer with the imam. Should I pray with him?" Abdullah ibn Umar said to him, "Yes," and the man said, "Which of them do I make my prayer?" Abdullah ibn Umar said, "Is that up to you? It is up to Allah. He will decide on whichever of them He wishes." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.3.11"> Book 8, Number 8.3.11: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said that a man asked Said ibn al-Musayyab, "I pray in my house, and then I come to the mosque and find the imam praying. Should I pray with him?" Said said, "Yes," and the man said, "Which of them is my prayer?" Said said, "Are you the one to decide that? That is up to Allah." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.3.12"> Book 8, Number 8.3.12: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Afif as-Sahmi that a man from the tribe of Bani Asad asked Abu Ayyub al-Ansari. "Sometimes I pray in my house, and then come to the mosque and find the imam praying. Should I pray with him?" Abu Ayyub said, "Yes, pray with him, for some one who does so has the reward of the group, or the equivalent of the reward of the group." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.3.13"> Book 8, Number 8.3.13: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to say, "Someone who prays maghrib or subh and then catches them with the imam should not repeat them." </p>
        <p> Malik said, "I do not see any harm in someone who has already prayed in his house praying with the imam, except for maghrib, because if he repeats it, he makes it even." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Praying in a Group of People </h3>
      <hr>
      <em> <a name="008.8.4.14"> Book 8, Number 8.4.14: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'z Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "When you lead people in the prayer, make it short, because there are among them some people who are weak, ill and old. But when you pray on your own, make it as long as you wish." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.4.15"> Book 8, Number 8.4.15: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Nafi said, "I stood behind Abdullah ibn Umar in one of the prayers when there was no one else with him, and he reached behind with his hand and placed me beside him." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.4.16"> Book 8, Number 8.4.16: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said that a man used to lead the people in prayer in al-Aqiq (a place near Madina), and Umar ibn Abd al-Aziz sent a message to him forbidding him to do so. </p>
        <p> Malik said, "He only forbade him because his father was not known." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Prayer Behind an Imam when He Prays Sitting </h3>
      <hr>
      <em> <a name="008.8.5.17"> Book 8, Number 8.5.17: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Anas ibn Malik that the Messenger of Allah, may Allah bless him and grant him peace, fell off his horse when riding, and his right side was scraped, so he did one of the prayers sitting, and we prayed behind him sitting. When he left, he said, "The imam is appointed to be followed. If he prays standing, then pray standing, and when he goes into ruku, go into ruku, and when he rises, rise, and when he says, 'Allah hears whoever praises him,' say, 'Our Lord, praise belongs to You,' and if he prays sitting, then all of you pray sitting." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.5.18"> Book 8, Number 8.5.18: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "The Messenger of Allah, may Allah bless him and grant him peace, prayed while he was ill. He prayed sitting, and some people prayed behind him standing, and he indicated to them to sit down. When he left, he said, 'The imam is only appointed to be followed. When he goes into ruku, go into ruku and when he rises, rise and if he prays sitting, pray sitting .' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.5.19"> Book 8, Number 8.5.19: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that the Messenger of Allah, may Allah bless him and grant him peace, came out during his illness, and approached and found Abu Bakr standing, leading the people in prayer. Abu Bakr began to go back, but the Messenger of Allah, may Allah bless him and grant him peace, indicated to him to stay where he was. The Messenger of Allah, may Allah bless him and grant him peace, sat by the side of Abu Bakr, and Abu Bakr prayed following the prayer of the Messenger of Allah, may Allah bless him and grant him peace, who was sitting, and the people prayed following the prayer of Abu Bakr. </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Excellence of the Prayer of Someone Standing over the Prayer of Someone Sitting </h3>
      <hr>
      <em> <a name="008.8.6.20"> Book 8, Number 8.6.20: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ismail ibn Muhammad ibn Said ibn Abi Waqqas from a mawla of Amr ibn al-As or of Abdullah ibn Amr ibn al-As that the Messenger of Allah, may Allah bless him and grant him peace, said, "The prayer of one of you sitting down is only equal to half the prayer of one of you when he is standing." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.6.21"> Book 8, Number 8.6.21: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Abdullah ibn Amr ibn al As said, "When we arrived at Madina we were struck down by a severe epidemic which debilitated us greatly. The Messenger of Allah, may Allah bless him and grant him peace, came out to the people while they were praying nawafil prayers sitting down. The Messenger of Allah, may Allah bless him and grant him peace, said, 'The prayer of the one sitting is equal to only half the prayer of the one standing.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Praying Voluntary Prayers (Nawafil) Sitting </h3>
      <hr>
      <em> <a name="008.8.7.22"> Book 8, Number 8.7.22: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from as-Sa'ib ibn Yazid from al Muttalib ibn Abi Wadaa as-Sahmi that Hafsa, the wife of the Prophet, may Allah bless him and grant him peace, said, "I never saw the Messenger of Allah, may Allah bless him and grant him peace, praying nawafil sitting, until a year before his death, when he began to pray them sitting. He would recite the sura with a measured slowness so that it would seem to be longer than other suras which were actually longer than it." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.7.23"> Book 8, Number 8.7.23: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, told him that she had never seen the Messenger of Allah, may Allah bless him and grant him peace, doing night prayers sitting down until he was getting on in years. He would recite sitting down until when he wanted to go into ruku, he would stand up and recite about thirty or forty ayats and then go into ruku. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.7.24"> Book 8, Number 8.7.24: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abdullah ibn Yazid al-Madani and from Abu'n Nadr from Abu Salama ibn Abd ar-Rahman from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that the Messenger of Allah, may Allah bless him and grant him peace, used to pray sitting. He would recite sitting, and then, when about thirty or forty ayats of what he was reciting remained, he would stand up and recite standing and then go into ruku and sajda. He would do the same in the second raka. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.7.25"> Book 8, Number 8.7.25: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Urwa ibn az-Zubayr and Said ibn al-Musayyab used to pray voluntary prayers sitting. </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Middle Prayer </h3>
      <hr>
      <em> <a name="008.8.8.26"> Book 8, Number 8.8.26: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam from al-Qaqa ibn Hakim that Abu Yunus, the mawla of A'isha, umm al-muminin said, ''A'isha ordered  me to write out a Qur'an for her. She said, 'When you reach this ayat, let me know, "Guard the prayers carefully and the middle prayer and stand obedient to Allah." ' When I reached it I told her, and she dictated to me, 'Guard the prayers carefully and the middle prayer and the asr prayer and stand obedient to Allah.'  A'isha said, 'I heard it from the Messenger of Allah, may Allah bless him and grant him peace.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.8.27"> Book 8, Number 8.8.27: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam that Amr ibn Rafi said, "I was writing a Qur'an for Hafsa, umm al-muminin, and she said, 'When you reach this ayat, let me know, "Guard the prayers carefully and the middle prayer and stand obedient to Allah." When I reached it I told her and she dictated to me, 'Guard the prayers carefully and the middle prayer and the asr prayer and stand obedient to Allah.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.8.28"> Book 8, Number 8.8.28: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Da'ud ibn al-Husayn that Ibn Yarbu al-Makhzumi said, "I heard Zayd ibn Thabit say, 'The middle prayer is the prayer of dhuhr.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.8.29"> Book 8, Number 8.8.29: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that AIi ibn Abi Talib and Abdullah ibn Abbas used to say, "The middle prayer is the prayer of subh." </p>
        <p> Malik said, "Out of all that I have heard about the matter, I prefer what Ali ibn Abi Talib and Abdullah ibn Abbas said." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Permission to Pray in One Garment </h3>
      <hr>
      <em> <a name="008.8.9.30"> Book 8, Number 8.9.30: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that Umar ibn Abi Salama saw the Messenger of Allah, may Allah bless him and grant him peace, praying in one garment in the house of Umm Salama. He was completely covered by it, and had put both ends over his shoulders. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.9.31"> Book 8, Number 8.9.31: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab from Abu Hurayra that some one asked the Messenger of Allah, may Allah bless him and grant him peace about praying in one garment. The Messenger of Allah, may Allah bless him and grant him peace, said, "Do you all have two garments?" </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.9.32"> Book 8, Number 8.9.32: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Said ibn al-Musayyab said that Abu Hurayra was asked, "May a man pray in one garment?" He said, "Yes." The man then said to him "Do you do that?" and he replied, "Yes, I pray in one garment while my clothes are on the clothes-rack." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.9.33"> Book 8, Number 8.9.33: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Jabir ibn Abdullah used to pray in one garment. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.9.34"> Book 8, Number 8.9.34: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Rabia ibn Abi Abd ar-Rahman that Muhammad ibn Amr ibn Hazm used to pray in a single long shirt. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.9.35"> Book 8, Number 8.9.35: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard from Jabir ibn Abdullah that the Messenger of Allah, may Allah bless him and grant him peace, said, "Let anyone who cannot find two garments pray in one garment and wrap himself in it, and if the garment is short, let him wrap it around his waist." </p>
        <p> Malik said, "In my view it is preferable for someone who prays in a single shirt to put a garment or a turban over his shoulders." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Permission for a Woman to Pray in a Shift and Head-Covering </h3>
