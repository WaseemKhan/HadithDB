      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, used to pray in a shift and head-covering. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.10.37"> Book 8, Number 8.10.37: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Muhammad ibn Zayd ibn Qunfudh that his mother asked Umm Salama, the wife of the Prophet, may Allah bless him and grant him peace, "What clothes can a woman wear in prayer?" She said, "She can pray in a shift that reaches down and covers the top of her feet." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="008.8.10.38"> Book 8, Number 8.10.38: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from a reliable source from Bukayr ibn Abdullah ibn al-Ashajj from Busr ibn Said that when Ubaydullah ibn al-Aswad al-Khawlani was in the room of Maimuna, the wife of the Prophet, may Allah bless him and grant him peace, she used to pray in a shift and head-covering, without a waist-wrapper. </p>
      </blockquote>
