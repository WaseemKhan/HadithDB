      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 44: </a> </em>
        <h1> <em> The Oath of Qasama </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Beginning With the People Seeking Blood Revenge in the Oath </h3>
        <hr>
        <em> <a name="044.44.1.1"> Book 44, Number 44.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu Layla ibn Abdullah ibn Abd ar-Rahman ibn Sahl from Sahl ibn Abi Hathma that some of the great men of his people informed him that Abdullah ibn Sahl and Muhayyisa went out to Khaybar because extreme poverty had overtaken them. Muhayyisa returned and said that Abdullah ibn Sahl had been killed and thrown in a shallow well or spring. The jews came and he said, "By Allah! You have killed him." They said, "By Allah! We have not killed him!" Then he made for his people and mentioned that to them. Then he, his brother Huwayyisa, who was older than him, and Abd ar-Rahman, set out. Muhayyisa began to speak, as he had been at Khaybar. The Messenger of Allah, may Allah bless him and grant him peace, said to him, "The greater first, the greater first," meaning in age. So Huwayyisa spoke and then Muhayyisa spoke. The Messenger of Allah, may Allah bless him and grant him peace, said, "Either they pay your companion's blood-money or we will declare war against them." The Messenger of Allah, may Allah bless him and grant him peace, wrote that to them and they wrote, "By Allah, we did not kill him!" The Messenger of Allah, may Allah bless him and grant him peace, said to Huwayyisa, Muhayyisa, and Abd ar-Rahman, "Do you swear and claim the blood of your companion?" They said, "No." He said, "Shall the jews swear to you?" They said, "But they are not muslims." The Messenger of Allah, may Allah bless him and grant him peace, gave blood-money from his own property, and sent them one hundred camels to their house. </p>
          <p> Sahl added, "A red camel among them kicked me." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="044.44.1.2"> Book 44, Number 44.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said from Malik from Yahya ibn Said that Bushayr ibn Yasar informed him that Abdullah ibn Sahl al-Ansari and Muhayyisa ibn Masud went out to Khaybar, and they separated on their various businesses and Abdullah ibn Sahl was killed. Muhayyisa, and his brother Huwayyisa and Abd ar-Rahman ibn Sahl went to the Prophet, may Allah bless him and grant him peace, and Abd ar-Rahman began to speak before his brother. The Messenger of Allah, may Allah bless him and grant him peace, said, "The older first, the older first. </p>
          <p> Therefore Huwayyisa and then Muhayyisa spoke and mentioned the affair of Abdullah ibn Sahl. The Messenger of Allah, may Allah bless him and grant him peace, said to them, "Do you swear with fifty oaths and claim the blood-money of your companion or the life of the murderer?" They said, "Messenger of Allah, we did not see it and we were not present." The Messenger of Allah, may Allah bless him and grant him peace, said, "Will you acquit the jews for fifty oaths?' They said, "Messenger of Allah, how can we accept the oaths of a people who are kafirun?" </p>
          <p> Yahya ibn Said said, "Bushayr ibn Yasar claimed that the Messenger of Allah, may Allah bless him and grant him peace, paid the blood-money from his own property." </p>
          <p> Malik said, "The generally agreed on way of doing things in our community and that which I heard from whoever I am content with, concerning the oath of qasama, and upon which the past and present imams agree, is that those who claim revenge begin with the oaths and swear. The oath for revenge is only obligatory in two situations. Either the slain person says, 'My blood is against so-and-so,' or the relatives entitled to the blood bring a partial proof of it that is not irrefutable against the one who is the object of the blood-claim. This obliges taking an oath on the part of those who claim the blood against those who are the object of the blood-claim. With us, swearing is only obliged in these two situations." </p>
          <p> Malik said, "That is the sunna in which there is no dispute with us and which is still the behaviour of the people. The people who claim blood begin the swearings, whether it is an intentional killing or an accident." </p>
          <p> Malik said, "The Messenger of Allah, may Allah bless him and grant him peace, began with Banu Harith in the case of the killing of their kinsman murdered at Khaybar." </p>
          <p> Malik said, "If those who make the claim swear, they deserve the blood of their kinsman and whoever they swear against is slain. Only one man can be killed in the qasama. Two cannot be killed in it. Fifty men from the blood-relatives must swear fifty oaths. If their number is less or some of them draw back, they can repeat their oaths, unless one of the relatives of the murdered man who deserves blood and who is permitted to pardon it, draws back. If one of these draws back, there is no way to revenge." </p>
          <p> Yahya said that Malik said, "The oaths can be made by those of them who remain if one of them draws back who is not permitted to pardon. If one of the blood-relatives draws back who is permitted to pardon, even if he is only one, more oaths can not be made after that by the blood-relatives. If that occurs, the oaths can be on behalf of the one against whom the claim is made. So fifty of the men of his people swear fifty oaths. If there are not fifty men, more oaths can be made by those of them who already swore. If there is only the defendant, he swears fifty oaths and is acquitted." </p>
          <p> Yahya said that Malik said, "One distinguishes between swearing for blood and oaths for one's rights. When a man has a money-claim against another man, he seeks to verify his due. When a man wants to kill another man, he does not kill him in the midst of people. He keeps to a place away from people. Had there only been swearing in cases where there is a clear proof and had one acted in it as one acts about one's rights (i.e. needing witnesses), the right of blood retribution would have been lost and people would have been swift to take advantage of it when they learned of the decision on it. However, the relatives of the murdered man were allowed to initiate swearing so that people might restrain themselves from blood and the murderer might beware lest he was put into a situation like that (i.e. qasama) by the statement of the murdered man.' " </p>
          <p> Yahya said, "Malik said about a people of whom a certain number are suspected of murder and the relatives of the murdered man ask them to take oaths and they are numerous, so they ask that each man swears fifty oaths on his own behalf. The oaths are not divided out between them according to their number and they are not acquitted unless each man among them swears fifty oaths on his own behalf." </p>
          <p> Malik said, "This is the best I have heard about the matter." </p>
          <p> He said, "Swearing goes to the paternal relatives of the slain. They are the blood-relatives who swear against the killer and by whose swearing he is killed." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Blood-Relatives Who are Permitted to Swear in the Intentional Act </h3>
        <hr>
        <em> <a name="044.44.2.2a"> Book 44, Number 44.2.2a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik said, "The way of doing things in our community about which there is no dispute is that women do not swear in the swearing for the intentional act. If the murdered man only has female relatives, the women have no right to swear for blood and no pardon in murder." </p>
          <p> Yahya said that Malik said about a man who is murdered, "If the paternal relatives of the murdered man or his mawali say, 'We swear and we demand our companion's blood,' that is their right." </p>
          <p> Malik said, "If the women want to pardon him, they cannot do that. The paternal relatives and mawali are entitled to do that more than them because they are the ones who demand blood and swear for it." </p>
          <p> Malik said, "If the paternal relatives or mawali pardon after they demand blood and the women refuse and say, 'We will not abandon our right against the murderer of our companion,' the women are more entitled to that because whoever takes retaliation is more entitled than the one who leaves it among the women and paternal relatives when the murder is established and killing obliged." </p>
          <p> Malik said, "At least two claimants must swear in murder. The oaths are repeated by them until they swear fifty oaths, then they have the right to blood. That is how things are done in our community." </p>
          <p> Malik said, "When people beat a man and he dies in their hands, they are all slain for him. If he dies after their beating, there is swearing. If there is swearing, it is only against one man and only he is slain. We have never known the swearing to be against more than one man." </p>
          <p> Malik spoke about a slave who had his hand or foot broken and then the break mended . He said, "The one who injured him is not obliged to pay anything. If that break causes him loss or scar, the one who injured him must pay according to what he diminished of the value of the slave." </p>
          <p> Malik said, "What is done in our community about retaliation between slaves is that it is like retaliation between freemen. The life of the slave-girl for the life of the slave, and her injury for his injury. When a slave intentionally kills a slave, the master of the murdered slave has a choice. If he wishes, he kills him, and if he wishes, he takes the blood-money. If he takes the blood-money, he takes the value of his slave. If the owner of the slave who killed wishes to give the value of the murdered slave, he does it. If he wishes, he surrenders his slave. If he surrenders him, he is not obliged to do anything other than that. When the owner of the murdered slave takes the slave who murdered and is satisifed with him, he must not kill him. All retaliations between slaves for cutting off of the hand and foot and such things are dealt with in the same way as in the murder." </p>
          <p> Malik said about a muslim slave who injures a jew or christian, "If the master of the slave wishes to pay blood-money for him according to the injury, he does it. Or else he surrenders him and he is sold, and the jew or christian is given the blood-money of the injury or all the price of the slave if the blood-money is greater than his price. The jew or christian is not given a muslim slave." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Swearing in Manslaughter </h3>
        <hr>
        <em> <a name="044.44.3.2b"> Book 44, Number 44.3.2b: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik said, "The way of doing things in our community about Yahya said that Malik said, "The procedure in swearing in manslaughter is that those who claim blood swear and it becomes due by their swearing. They swear fifty oaths, and there is blood-money for them according to the division of their inheritances. If it is not possible to divide up the oaths which they swear between them evenly, one looks to the one who has most of those oaths against him, and that oath is obliged against him." </p>
          <p> Malik said, "If the slain man only has female heirs, they swear and take the blood-money. If he only has one male heir, he swears fifty oaths and takes the blood-money. That is only in the accidental killing, not in the intentional one." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Inheritance in Cases of Qasama </h3>
        <hr>
        <em> <a name="044.44.4.2c"> Book 44, Number 44.4.2c: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik said, "When the relatives of the deceased accept the blood-money then it is inherited according to the Book of Allah. Daughters of the dead man inherit and so do sisters, and whichever women would inherit from him ordinarily. </p>
          <p> If the women do not take all his inheritance, then what remains goes to the agnatic relations who most deserve to inherit from him in conjunction with the women." </p>
          <p> Malik said, "When one of the heirs of a man killed by mistake attempts to take his due from the blood-money while his companions are absent, he may not do that, and he has no right to any of the blood-money, however large or small, unless the qasama has been completed by him. If he swears fifty oaths then he has the right to his portion of the blood-money. That is because the blood-money is not established as due without there being fifty oaths, and the blood-money is not established as due unless the responsibility for the blood is established. If any one of the heirs comes after that he swears a number of the oaths commensurate with his fraction of the inheritance and takes his right until all the heirs exact their complete right. If a maternal uncle comes he has one sixth and must swear one sixth of the fifty oaths. So whoever swears may take his due from the blood-money and whoever abstains annuls his right. If one of the heirs is absent or is a child who has not reached puberty, those who are present swear fifty oaths and if the one who was absent comes after that or the child reaches puberty, they swear. and they swear according to their due of the blood-money and according to their shares of inheritance from it." </p>
          <p> Yahya said that Malik said, "This is the best I have heard on the matter." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Swearing for Blood-Revenge in Cases Involving Slaves </h3>
        <hr>
        <em> <a name="044.44.5.2d"> Book 44, Number 44.5.2d: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik said, "What is done in our community about slaves is that when a slave is struck intentionally or accidentally and the master brings a witness, he swears with his witness one oath and then he has the value of the slave. There is no swearing for revenge in slaves, accidentally or intentionally, and I have not heard any of the people of knowledge say that there was." </p>
          <p> Malik said, "If a slave is killed intentionally or accidentally, the master of the slave who is slain has no swearing or oath. The master cannot demand his right except with a fair proof or a witness if he swears with one witness." </p>
          <p> Yahya said that Malik said, "This is the best of what I have heard on the matter.'' </p>
        </blockquote>
        <!--EndHadithBody-->
