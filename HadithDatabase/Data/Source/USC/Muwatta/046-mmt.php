      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 46: </a> </em>
        <h1> <em> The Decree </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Prohibition against Talking about the Decree </h3>
        <hr>
        <em> <a name="046.46.1.1"> Book 46, Number 46.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Adam and Musa argued and Adam got the better of Musa. Musa rebuked Adam, 'You are Adam who led people astray and brought them out of the Garden.' Adam said to him, 'You are Musa to whom Allah gave knowledge of everything and whom he chose above people with His message.' He said, 'Yes.' He said, 'Do you then censure me for a matter which was decreed for me before I was created?' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.1.2"> Book 46, Number 46.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Abi Unaysa that Abd al-Hamid ibn Abd ar-Rahman ibn Zayd ibn al-Khattab informed him from Muslim ibn Yasar al-Juhani that Umar ibn al-Khattab was asked about this ayat - "When your Lord took their progeny from the Banu Adam from their backs and made them testify against themselves. 'Am I not your Lord?' They said, 'Yes, we bear witness' </p>
          <p> Lest you should say on the Day of Rising, 'We were heedless of that.'" (Sura 7 ayat 172) Umar ibn al-Khattab said, "I heard the Messenger of Allah, may Allah bless him and grant him peace, being asked about it. The Messenger of Allah, may Allah bless him and grant him peace, said, 'Allah, the Blessed, the Exalted, created Adam. Then He stroked his back with His right hand, and progeny issued from it. He said, "I created these for the Garden and they will act with the behaviour of the people of the Garden." Then He stroked his back again and brought forth progeny from him. He said, "I created these for the Fire and they will act with the behaviour of the people of the Fire." 'A man said, 'Messenger of Allah! Then of what value are deeds?' The Messenger of Allah, may Allah bless him and grant him peace, answered, 'When Allah creates a slave for the Garden, he makes him use the behaviour of the people of the Garden, so that he dies on one of the actions of the people of the Garden and by it He brings him into the Garden. When He creates a slave for the Fire, He makes him use the behaviour of the people of the Fire, so that he dies on one of the actions of the people of the Fire, and by it, He brings him into the Fire.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.1.3"> Book 46, Number 46.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard that the Messenger of Allah, may Allah bless him and grant him peace, said, "I have left two matters with you. As long as you hold to them, you will not go the wrong way. They are the Book of Allah and the Sunna of His Prophet." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.1.4"> Book 46, Number 46.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ziyad ibn Sad from Amr ibn Muslim that Tawus al-Yamani said, "I found some of the companions of the Messenger of Allah, may Allah bless him and grant him peace, saying 'Everything is by decree.' " Tawus added, "I heard Abdullah ibn Umar say that The Messenger of Allah, may Allah bless him and grant him peace. said, 'Everything is by decree - even incapacity and ability,' (or 'ability and incapacity')." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.1.5"> Book 46, Number 46.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ziyad ibn Sad that Amr ibn Dinar said, "I heard Abdullah ibn az-Zubayr say in a khutba, 'Allah - He is the Guider and the One Who Tempts Away.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.1.6"> Book 46, Number 46.1.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that his paternal uncle, Abu Suhayl ibn Malik said, "I was a prisoner with Umar ibn Abd al-Aziz. He said, 'What do you think about these Qadariyya (fatalists)?' I said, 'My opinion is that one should ask them to turn from wrong action, if they will do so. If not, subject them to the sword.' Umar ibn Abd al-Aziz said, 'That is my opinion. </p>
          <p> Malik added, "That is my opinion also." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: General Section on the People of the Decree </h3>
        <hr>
        <em> <a name="046.46.2.7"> Book 46, Number 46.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "A woman should not ask for the divorce of her sister to make her plate empty and in order to marry. She has what is decreed for her." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.2.8"> Book 46, Number 46.2.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yazid ibn Ziyad that Muhammad ibn Kab al-Quradhi said, ''Muawiya ibn Abi Sufyan said from the mimbar, 'O people! Nothing keeps away what Allah gives and nothing gives what Allah keeps away. The earnestness of the earnest one does not profit him. When Allah desires good for him, he gives him understanding in the deen.' Then Muawiya said, 'I heard these words from the Messenger of  Allah, may Allah bless him and grant him peace, on these blocks of wood.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.2.9"> Book 46, Number 46.2.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that this was said "Praise be to Allah who created everything as is necessary, who does not hasten anything He defers and determines. Allah is enough for me and sufficient. Allah hears whoever makes dua to him. Allah does not have a goal." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="046.46.2.10"> Book 46, Number 46.2.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard that this was said "No one will die until his provision is completed for him, so behave correctly in your seeking it." </p>
        </blockquote>
        <!--EndHadithBody-->
