      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 47: </a> </em>
        <h1> <em> Good Character </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Good Character </h3>
        <hr>
        <em> <a name="047.47.1.1"> Book 47, Number 47.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Muadh ibn Jabal said, "The last advice the Messenger of Allah, may Allah bless him and grant him peace, gave me when I put my foot in the stirrup was that he said, 'Make your character good for the people, Muadh ibn Jabal!' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.2"> Book 47, Number 47.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Urwa ibn az-Zubayr that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "The Messenger of Allah, may Allah bless him and grant him peace, did not have to choose between two matters, but that he chose the easier of them as long as it was not a wrong action. If it was a wrong action, he was the furthest of people from it. The Messenger of Allah, may Allah bless him and grant him peace, did not take revenge for himself unless the limits of Allah were violated. Then he took revenge for it for Allah." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.3"> Book 47, Number 47.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Ali ibn Husayn ibn Ali ibn Abi Talib that the Messenger of Allah, may Allah bless him and grant him peace, said, "Part of the excellence of a man's Islam is that he leaves what does not concern him." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.4"> Book 47, Number 47.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "A man asked permission to come in to see the Messenger of Allah, may Allah bless him and grant him peace. I was with him in the house and the Messenger of Allah, may Allah bless him and grant him peace, said, 'He is an evil member of his tribe.' Then the Messenger of Allah, may Allah bless him and grant him peace, gave him permission." </p>
          <p> A'isha continued, "It was not long before I heard the Messenger of Allah, may Allah bless him and grant him peace, laughing with him. When the man left, I said, 'Messenger of Allah, you said what you said about him, and then before long you were laughing with him.' The Messenger of Allah, may Allah bless him and grant him peace, replied, 'Among the most evil of people is the one whom people are cautious with because of his evil.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.5"> Book 47, Number 47.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from his paternal uncle, Abu Suhayl ibn Malik from his father that Kab al-Ahbar said, "If you want to know what a slave has with his Lord, then look at whatever good praise follows him." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.6"> Book 47, Number 47.1.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said, "I have heard that by his good character a man can reach the degree of the one who stands in prayer at night and the one who is thirsty from fasting in the heat of the day." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.7"> Book 47, Number 47.1.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said that he heard Said ibn al-Musayyab say, "Shall I tell you what is better than much prayer and sadaqa?" They said, "Yes." He said, "Mending discord. And beware of hatred - it strips you (of your deen)." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.1.8"> Book 47, Number 47.1.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, said, "I was sent to perfect good character." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Modesty </h3>
        <hr>
        <em> <a name="047.47.2.9"> Book 47, Number 47.2.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Salama ibn Safwan ibn Salama az-Zuraqi that Zayd ibn Talha ibn Rukana, who attributed it to the Prophet, may Allah bless him and grant him peace, said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'Every deen has an innate character. The character of Islam is modesty.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.2.10"> Book 47, Number 47.2.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Salim ibn Abdullah from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, passed by a man who was chiding his brother about modesty. The Messenger of Allah, may Allah bless him and grant him peace, said, "Leave him. Modesty is part of iman." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Anger </h3>
        <hr>
        <em> <a name="047.47.3.11"> Book 47, Number 47.3.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Humayd ibn Abd ar-Rahman ibn Awf that a man came to the Messenger of Allah, may Allah bless him and grant him peace, and said, "Messenger of Allah, teach me some words which I can live by. Do not make them too much for me, lest I forget." The Messenger of Allah, may Allah bless him and grant him peace, said, "Do not be angry." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.3.12"> Book 47, Number 47.3.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Said ibn al-Musayyab from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "A strong person is not the person who throws his adversaries to the ground. A strong person is the person who contains himself when he is angry." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Shunning People </h3>
        <hr>
        <em> <a name="047.47.4.13"> Book 47, Number 47.4.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Ata ibn Yazid al-Laythi from Abu Ayyub al-Ansari that the Messenger of Allah, may Allah bless him and grant him peace, said, "It is not halal for a muslim to shun his brother for more than three nights, that is they meet, and this one turns away and that one turns away. The better of the two is the one who says the greeting first." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.4.14"> Book 47, Number 47.4.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Anas ibn Malik that the Messenger of Allah, may Allah bless him and grant him peace, said, "Do not be angry with each other and do not envy each other and do not turn away from each other, and be slaves of Allah, brothers. It is not halal for a muslim to shun his brother for more than three nights." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.4.15"> Book 47, Number 47.4.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Beware of suspicion. Suspicion is the most untrue speech. Do not spy and do not eavesdrop. Do not compete with each other and do not envy each other and do not hate each other and do not shun each other. Be slaves of Allah, brothers." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.4.16"> Book 47, Number 47.4.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ata ibn Abi Muslim that Abdullah al-Khurasani said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'Shake hands and rancour will disappear. Give presents to each other and love each other and enmity will disappear.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.4.17"> Book 47, Number 47.4.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Suhayl ibn Abi Salih from his father from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "The doors of the Garden are opened on Monday and Thursday. Every muslim slave who does not associate anything with Allah is forgiven except for the man who has enmity between him and his brother. It is said, 'Leave these two until they have made a reconciliation. Leave these two until they have made a reconciliation.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="047.47.4.18"> Book 47, Number 47.4.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Muslim ibn Abi Maryam from Abu Salih as-Samman that Abu Hurayra said, "The actions of people are presented twice each week, on Monday and Thursday. Every trusting slave is forgiven except for a slave who has enmity between him and his brother. It is said, 'Leave these two until they turn in tawba. Leave these two until they turn in tawba.' '' </p>
        </blockquote>
        <!--EndHadithBody-->
