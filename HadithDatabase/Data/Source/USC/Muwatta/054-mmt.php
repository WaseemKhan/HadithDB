      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 54: </a> </em>
        <h1> <em> General Subjects </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Asking Permission to Enter </h3>
        <hr>
        <em> <a name="054.54.1.1"> Book 54, Number 54.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Safwan ibn Sulaym from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, was questioned by a man who said, "Messenger of Allah, shall I ask permission of my mother to enter?" He said, "Yes " The man said, "I live with her in the house". The Messenger of Allah, may Allah bless him and grant him peace, said "Ask her permission." The man said, "I am her servant." The Messenger of Allah. may Allah bless him and grant him peace, said, "Ask her permission. Do you want to see her naked?" He said, "No." He said, "Then ask her permission." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.1.2"> Book 54, Number 54.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from a reliable source of his from Bukayr ibn Abdullah ibn al-Ashajj from Basr ibn Said from Abu Said al-Khudri that Abu Musa al-Ashari said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'One asks permission three times. If you are given permission, then enter. If not, go away.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.1.3"> Book 54, Number 54.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Rabia ibn Abi Abd ar-Rahman from another of the ulama of that time that Abu Musa al-Ashari came and asked permission from Umar ibn al-Khattab to enter. He asked permission three times, and then went away Umar ibn al-Khattab sent after him and said, "What's wrong with you? Why didn't you come in?" Abu Musa said, "I heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'Ask permission to enter three times. If you are given permission, then enter. If not, go away.' ''Umar said, "Who can confirm this? If you do not bring me someone to confirm it, I will do such-and-such to you." </p>
          <p> Abu Musa went out until he came to an assembly in the mosque which was called the Majlis-al-Ansar. He said, "I told Umar ibn al-Khattab that I heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'Ask permission three times. If you are given permission, then enter. If not, go away.' Umar said, 'If you do not bring me someone who can confirm it, I will do such-and-such to you'. If any of you have heard that, let him come with me.' " They said to Abu Said al-Khudri, "Go with him". Abu Said was the youngest of them. He went with him and told Umar ibn al-Khattab about that." </p>
          <p> Umar ibn al-Khattab said to Abu Musa, "I did not suspect you, but I feared lest people forge sayings of the Messenger of Allah, may Allah bless him and grant him peace." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Blessing the Person who Sneezes </h3>
        <hr>
        <em> <a name="054.54.2.4"> Book 54, Number 54.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Abi Bakr from his father that the Messenger of Allah, may Allah bless him and grant him peace, said, "If a man sneezes, invoke a blessing on him. Then if he sneezes, invoke a blessing on him. Then if he sneezes, invoke a blessing on him. Then if he sneezes, say, 'You have a cold'." Abdullah ibn Abi Bakr said, "I don't know whether it was after the third or fourth." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.2.5"> Book 54, Number 54.2.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi that when Abdullah ibn Umar sneezed and someone said to him, "May Allah have mercy on you," (Yarhamuka'llah), he said, "May Allah have mercy on us and you, and forgive us and you." (Yarhamuna'llah wa yaghfirlana wa lakum). </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Pictures and Images </h3>
        <hr>
        <em> <a name="054.54.3.6"> Book 54, Number 54.3.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ishaq ibn Abdullah ibn Abi Talha that Rafi ibn Ishaq, the mawla of ash-Shifa informed him that he and Abdullah ibn Abi Talha had gone to visit Abu Said al-Khudri while he was ill. Abu Said said to them, "The Messenger of Allah, may Allah bless him and grant him peace, informed us, 'The angels do not enter a house which contains pictures or images.' " Ishaq was not sure which of them Abu Said said. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.3.7"> Book 54, Number 54.3.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu'n-Nasr that Ubaydullah ibn Abdullah ibn Utba ibn Masud went to visit Abu Talha al-Ansari when he was ill. He said, "I found Sahl ibn Hunayf with him. Abu Talha summoned a man and removed a rug which was under him. Sahl ibn Hunayf said to him, 'Why did you remove it?' He said, 'Because there were pictures on it, and the Messenger of Allah, may Allah bless him and grant him peace, said what you know about them.' Sahl replied, 'Didn't the Messenger of Allah, may Allah bless him and grant him peace, say, "except for markings on a garment?"' (A rug was considered a garment). He said, 'Yes, but it is more pleasing to myself.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.3.8"> Book 54, Number 54.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from al-Qasim ibn Muhammad from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that she bought a cushion which had pictures on it. When the Messenger of Allah, may Allah bless him and grant him peace, saw it, he stopped at the door and did not enter. She recognised disapproval on his face and said, "Messenger of Allah, I turn in repentance to Allah and His Messenger. What have I done wrong?" The Messenger of Allah, may Allah bless him and grant him peace, said, "What is the meaning of this cushion?" She said, "I bought it for you to sit and recline on." The Messenger of Allah, may Allah bless him and grant him peace, said, "Those who make such pictures will be punished on the Day of Rising. It will be said to them, 'Bring to life what you have created'. Then he said, 'The angels do not enter a house in which there are pictures.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Eating Lizards </h3>
        <hr>
        <em> <a name="054.54.4.9"> Book 54, Number 54.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abd ar-Rahman ibn Abdullah ibn Abd ar-Rahman ibn Abi Sasaca that Sulayman ibn Yasar said, "The Messenger of Allah, may Allah bless him and grant him peace, entered the house of Maimuna bint al-Harith and there was a lizard in which there were eggs to eat. Abdullah ibn Abbas and Khalid ibn al-Walid were with him. He said, 'From where did you get this?' She replied, 'My sister, Huzayla bint al-Harith, gave it to me.' He then told Abdullah ibn Abbas and Khalid to eat. They said, 'Won't you eat, Messenger of Allah?' He said, 'There are those who visit me from Allah.' Maimuna said, 'Messenger of Allah, shall we give you some milk to drink which we have?' He said, 'Yes.' When he drank, he said, 'From where did you get this?' She said, 'My sister, Huzayla gave it to me.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'Do you see your slave-girl whom you asked me for permission to free? Give her to your sister and bring her to your maternal relatives to take care of her. That is better for you.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.4.10"> Book 54, Number 54.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Abu Umama ibn Sahl ibn Hunayf from Abdullah ibn Abbas that Khalid ibn al-Walid ibn al-Mughira entered the house of Maimuna, the wife of the Prophet, may Allah bless him and grant him peace, with the Messenger of Allah, may Allah bless him and grant him peace, and he was brought a roasted lizard. The Messenger of Allah, may Allah bless him and grant him peace, stretched his hand toward it. One of the women who was in Maimuna's house said, "Tell the Messenger of Allah, may Allah bless him and grant him peace, what he means to eat." Someone said, "It is a lizard, Messenger of Allah." He withdrew his hand. Khalid said, "Is it haram, Messenger of Allah?" He said, "No, but there were none in my people's land, and I find that I dislike them." </p>
          <p> Khalid added, "I chewed and ate it while the Messenger of Allah, may Allah bless him and grant him peace, was looking." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.4.11"> Book 54, Number 54.4.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar from Abdullah ibn Umar that a man called the Messenger of Allah and said, "Messenger of Allah, what do you think about lizards?" The Messenger of Allah, may Allah bless him and grant him peace, said, "I do not eat them, and I do not forbid them." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Concerning Dogs </h3>
        <hr>
        <em> <a name="054.54.5.12"> Book 54, Number 54.5.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yazid ibn Khusayfa that as-Sa'ib ibn Yazid informed him that he heard Sufyan ibn Abi Zuhayr who was from the Azd Shanua tribe and among the companions of the Messenger of Allah, may Allah bless him and grant him peace, speaking with some people who were with him at the door of the mosque. He said, "I heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'If anyone acquires a dog which he does not use as a sheepdog or for hunting, a qirat will be deducted from the reward of his good deeds each day.' " He was asked, "Did you hear this from the Messenger of Allah, may Allah bless him and grant him peace?" He said, "Yes, by the Lord of this mosque." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.5.13"> Book 54, Number 54.5.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever acquires a dog other than a sheepdog or hunting dog, will have two qirats deducted from the reward of his good actions every day." </p>
          <p> Malik related to me from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, ordered dogs to be killed. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Concerning Sheep </h3>
        <hr>
        <em> <a name="054.54.6.15"> Book 54, Number 54.6.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "The head of kufr is towards the east. Boasting and price is among people who have horses and camels. The loud-voiced people are the people of tents (the Bedouins). Tranquillity is with the people who have sheep." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.6.16"> Book 54, Number 54.6.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abd ar-Rahman ibn Abd ar-Rahman ibn Sasaca from his father that Abu Said al-Khudri said that the Messenger of Allah, may Allah bless him and grant him peace, said, "It will soon happen that the best property of a muslim will be sheep which he takes to the peaks of the mountains and the valleys, fleeing with his deen from trials." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.6.17"> Book 54, Number 54.6.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from Ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "No one should milk someone else's cow without his permission. Would any of you like someone to come to his apartment, break into his larder, and take his food? The udders of cows guard their food for their owners, so no one should milk someone else's cow without his permission." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.6.18"> Book 54, Number 54.6.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, said, "There is no Prophet who has not herded sheep," and someone asked, "You as well, Messenger of Allah?" He said, "Myself as well." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Mice Falling into Clarified Butter, and giving Precedence to Food over the Prayer </h3>
        <hr>
        <em> <a name="054.54.7.19"> Book 54, Number 54.7.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi that one time as Ibn Umar was served his evening meal, he heard the recitation of the Imam while he was in his house and he did not hurry from his food until he had finished what he needed. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.7.20"> Book 54, Number 54.7.20: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Ubaydullah ibn Abdullah ibn Utba ibn Masud from Abdullah ibn Abbas from Maimuna, the wife of the Prophet, may Allah bless him and grant him peace, that the Messenger of Allah, may Allah bless him and grant him peace, was asked about a mouse falling into clarified butter. He said, "Remove it and throw away what is around it " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Guarding against Ill Luck </h3>
        <hr>
        <em> <a name="054.54.8.21"> Book 54, Number 54.8.21: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu Hazim ibn Dinar from Sahl ibn Sad as-Saidi that the Messenger of Allah, may Allah bless him and grant him peace, said, "If it exists, it is in a horse, a woman, and a house," meaning ill luck. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.8.22"> Book 54, Number 54.8.22: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Malik from Ibn Shihab from Hamza and Salim the sons of Abdullah ibn Umar from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "Ill luck is in a house, a woman and a horse." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.8.23"> Book 54, Number 54.8.23: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Yahya ibn Said said, "A woman came to the Messenger of Allah, may Allah bless him and grant him peace, and said, 'Messenger of Allah, we moved into a house when our number was great and our wealth was abundant. Now our number has dwindled and the wealth has gone.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'Leave it as blameworthy.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Names that are Disliked </h3>
        <hr>
        <em> <a name="054.54.9.24"> Book 54, Number 54.9.24: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said that the Messenger of Allah, may Allah bless him and grant him peace, asked about a milk camel about to be milked, "Who milks this camel?" A man stood up. The Messenger of Allah, may Allah bless him and grant him peace, said, "What is your name?" The man said, "Murra (bitterness)." The Messenger of Allah, may Allah bless him and grant him peace, said to him, "Sit down." Then he said, "Who milks this one?" A man stood up and the Messenger of Allah, may Allah bless him and grant him peace, said, "What is your name?" He said, "Harb (war)." The Messenger of Allah, may Allah bless him and grant him peace, said, "Sit down." Then he said, "Who milks this camel?" A man stood up, and the Messenger of Allah, may Allah bless him and grant him peace, said to him, "What is your name?" The man said, ''Yaish (he lives)." The Messenger of Allah, may Allah bless him and grant him peace, said to him, "Milk!" </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.9.25"> Book 54, Number 54.9.25: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Yahya ibn Said that Umar ibn al-Khattab asked a man what his name was. He said, "Jamra (live coal)." "The son of who?" He said, "Ibn Shihab (meteor, flame)." "From whom?" He said, "From al-Huraqa (burning)." "Where do you live?" He said, "At Harrat an-Nar (lava field of the fire). "At which one of them?" He said, "At Dhati Ladha (one with flames)." Umar said, "Go and look at your family - they have been burned." </p>
          <p> Yahya added, "It was as Umar ibn al-Khattab, may Allah be pleased with him, said." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Cupping and the Reward of the Cupper </h3>
        <hr>
        <em> <a name="054.54.10.26"> Book 54, Number 54.10.26: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Humayd at-Tawil that Anas ibn Malik said, "The Messenger of Allah, may Allah bless him and grant him peace, was cupped. Abu Tayba cupped him, and the Messenger of Allah, may Allah bless him and grant him peace, ordered him to be given a sa of dates and ordered his family to lessen what he paid them for his kitaba or kharaj." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.10.27"> Book 54, Number 54.10.27: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that the Messenger of Allah, may Allah bless him and grant him peace, said, "If there is a remedy that will reach the disease, then cupping will reach it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.10.28"> Book 54, Number 54.10.28: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Ibn Muhayyisa al-Ansari, one of the Banu Haritha, that he asked permission from the Messenger of Allah, may Allah bless him and grant him peace, to give payment for cupping, and he forbade him to do it. He continued to ask and seek his permission until he said, "Feed the ones who drive your water-carrying camels," meaning "your slaves." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Concerning the East </h3>
        <hr>
        <em> <a name="054.54.11.29"> Book 54, Number 54.11.29: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abdullah ibn Dinar that Abdullah ibn Umar said, "I saw the Messenger of Allah, may Allah bless him and grant him peace, pointing at the east and saying, 'The cause of dissension is here. The cause of dissension is here, from where the helpers of shaytan arise.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.11.30"> Book 54, Number 54.11.30: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Umar ibn al-Khattab wanted to go to Iraq, and Kabal-Ahbar said to him, "Do not go there, amir al-muminin. There is nine-tenths of sorcery there and it is the place of the rebellious jinn and the disease which the doctors are unable to cure." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Killing Snakes and what is Said about them </h3>
        <hr>
        <em> <a name="054.54.12.31"> Book 54, Number 54.12.31: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from Abu Lubaba that the Messenger of Allah, may Allah bless him and grant him peace, forbade killing snakes which were in the houses. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.12.32"> Book 54, Number 54.12.32: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from Sa'iba, the female mawla of A'isha, that the Messenger of Allah, may Allah bless him and grant him peace, forbade killing the snakes which were in the houses except those with two white stripes on their back and the short ones. They made one go blind and caused miscarriages in women. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.12.33"> Book 54, Number 54.12.33: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Safiyy, the mawla of Ibn Aflah that Abu's-Saib, the mawla of Hisham ibn Zuhra said, "I went to Abu Said al-Khudri and found him praying. I sat to wait for him until he finished the prayer. I heard a movement under a bed in his room, and it was a snake. I stood up to kill it, and Abu Said gestured to me to sit. When he was finished he pointed to a room in the house and said, 'Do you see this room?' I said, 'Yes.' He said, 'There was a young boy in it who had just got married. He went out with the Messenger of Allah, may Allah bless him and grant him peace, to al-Khandaq, (the ditch which the muslims dug in the 5th year of the Hijra to defend Madina against the Quraysh and their allies). </p>
          <p> When he was there, the youth came and asked his permission, saying, "Messenger of Allah. Give me permission to return to my family." The Messenger of Allah, may Allah bless him and grant him peace, gave him permission and said, "Take your weapons with you, for I fear the Banu Quraydha tribe. They may harm you." The youth went to his family and found his wife standing between the two doors. He lifted his spear to stab her as jealousy had been aroused in him. She said, "Don't be hasty until you go in and see what is in your house." He entered and found a snake coiled up on his bed. He transfixed it with his spear and then went out with it and pitched it into the house. The snake stirred on the end of the spear and the youth fell dead. No one knew which of them died first, the snake or the youth. That was mentioned to the Messenger of Allah, may Allah bless him and grant him peace, and he said, "There are jinn in Madina who have become muslim. When you see one of them, call out to it for three days. If it appears after that, then kill it, for it is a shaytan." "' </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What to Say on Journeys </h3>
        <hr>
        <em> <a name="054.54.13.34"> Book 54, Number 54.13.34: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that when the Messenger of Allah, may Allah bless him and grant him peace, set foot in the stirrup intending to travel, he would say, "In the name of Allah. O Allah! You are my companion in the journey and the Khalifa of my family. O Allah! Spread out the earth for us and make the journey easy for us. O Allah! I seek refuge with You from the hardship of the journey and from returning to sorrow and a distressing sight regarding property and family." </p>
          <p> Malik related to me from a reliable source of his from Yaqub ibn Abdullah al-Ashajj from Bushr ibn Said from Sad ibn Abi Waqqas from Khawla bint Hakim that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever dismounts to rest in a place should say, 'I seek refuge with the complete words of Allah from the evil of what he created,' (audhu bi kalimati-llahi at-tammati min sharri ma khalaqa), and nothing will harm him until he remounts." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Travelling Alone in the case of Men and Women </h3>
        <hr>
        <em> <a name="054.54.14.35"> Book 54, Number 54.14.35: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abd ar-Rahman ibn Harmala from Amr ibn Shuayb from his father from his father that the Messenger of Allah, may Allah bless him and grant him peace, said, "One rider is a shaytan. Two riders are two shaytans, and three are a riding-party." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.14.36"> Book 54, Number 54.14.36: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abd ar-Rahman ibn Harmala that Said ibn al-Musayyab heard the Messenger of Allah, may Allah bless him and grant him peace, say, "Shaytan concerns himself with one and two. When there are three, he does not concern himself with them." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.14.37"> Book 54, Number 54.14.37: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Said ibn Abi Said al-Maqburi from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "It is not halal for a woman who believes in Allah and the Last Day to travel the distance of a day and night without a man who is her mahram." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: How to Behave on Journeys </h3>
        <hr>
        <em> <a name="054.54.15.38"> Book 54, Number 54.15.38: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Abu Ubayd, the mawla of Sulayman ibn Abd al-Malik from Khalid ibn Madan who attributed it to the Prophet, may Allah bless him and grant him peace, "Allah, the Blessed and Exalted is kind and loves kindness. He is pleased with it and helps you with it as long as it is not misplaced. When you ride dumb beasts, stop them in their stopping places, and quicken their pace when the land is barren. Travel by night, because the land is travelled faster at night than it is during the day. Beware of pitching tents on the road, for it is the path of animals and the abode of snakes." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.15.39"> Book 54, Number 54.15.39: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Sumayy, the mawla of Abu Bakr from Abu Salih from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Travelling is a portion of the torment. It denies you your sleep, food, and drink. When you have accomplished your purpose, you should hurry back to your family." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Command to be Kind to Slaves </h3>
        <hr>
        <em> <a name="054.54.16.40"> Book 54, Number 54.16.40: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Abu Hurayra said that the Messenger of Allah, may Allah bless him and grant him peace, said, "A slave has his food and clothing in the normal manner, and he is only obliged to do such work as he is capable of doing." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.16.41"> Book 54, Number 54.16.41: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Umar ibn al-Khattab went to the villages every Saturday. If he found a slave doing work which he was not capable of doing, he lightened it for him. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.16.42"> Book 54, Number 54.16.42: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from his paternal uncle, Abu Suhayl ibn Malik, that his father heard Uthman ibn Affan say in a khutba, "Do not oblige the slave-girl to earn money unless she has a skill. When you oblige her to do that, she will earn money by prostitution. Do not oblige the child to earn money. If he does not find it, he will steal. Have integrity since Allah has integrity with you, and you must feed them good food." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Slave and his Reward </h3>
        <hr>
        <em> <a name="054.54.17.43"> Book 54, Number 54.17.43: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "When a slave gives good counsel to his master and worships Allah correctly, he has his reward twice over. " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="054.54.17.44"> Book 54, Number 54.17.44: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Umar ibn al-Khattab saw a female slave belonging to Abdullah ibn Umar ibn al-Khattab. She was dressed up in silks. He went to his daughter Hafsa and said, "Didn't I see your brother's slave-girl dressed up in silks walking among the people and causing trouble?" Umar disapproved of that. </p>
        </blockquote>
        <!--EndHadithBody-->
