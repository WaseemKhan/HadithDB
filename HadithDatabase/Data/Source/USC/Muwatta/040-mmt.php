      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 40: </a> </em>
        <h1> <em> Hudud </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Judgement on the Mudabbar </h3>
        <hr>
        <em> <a name="040.40.1.1"> Book 40, Number 40.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me that Malik said, "What is done in our community in the case of a man who makes his slave-girl a mudabbara and she gives birth to children after that, and then the slave-girl dies before the one who gave her a tadbir is that her children are in her position. The conditions which were confirmed for her are confirmed for them. The death of their mother does not harm them. If the one who made her mudabbara dies, they are free if their value is less than one third of his total property." </p>
          <p> Malik said, "For every mother by birth as opposed to mother by suckling, her children are in her position. If she is free and she gives birth after she is free, her children are free. If she is a mudabbara or mukataba, or freed after a number of years in service, or part of her is free or pledged or she is an umm walad, each of her children are in the same position as their mother. They are set free when she is set free and they are slaves when she is a slave." </p>
          <p> Malik said about the mudabbara given a tadbir while she was pregnant, "Her children are in her position. That is also the position of a man who frees his slave-girl while she is pregnant and does not know that she is pregnant." </p>
          <p> Malik said, "The sunna about such women is that their children follow them and are set free by their being set free." </p>
          <p> Malik said, "It is the same as if a man had bought a slave-girl while she was pregnant. The slave-girl and what is in her womb belong to the one who bought her whether or not the buyer stipulates that." </p>
          <p> Malik continued, "It is not halal for the seller to make an exception about what is in her womb because that is an uncertain transaction. It reduces her price and he does not know if that will reach him or not. That is as if one sold the foetus in the womb of the mother. That is not halal because it is an uncertain transaction ." </p>
          <p> Malik said about the mukatab or mudabbar who bought a slave-girl and had intercourse with her and she became pregnant by him and gives birth, "The children of both of them by a slave-girl are in his position. They are set free when he is set free and they are slaves when he is a slave." </p>
          <p> Malik said, "When he is set free, the umm walad is part of his property which is surrendered to him when he is set free." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: General Section on Tadbir </h3>
        <hr>
        <em> <a name="040.40.2.2"> Book 40, Number 40.2.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik spoke about a mudabbar who said to his master, "Free me immediately and I will give fifty dinars which I will have to pay in instalments." His master said, "Yes. You are free and you must pay fifty dinars, and you will pay me ten dinars every year." The slave was satisfied with this. Then the master dies one, two or three days after that. He said, "The freeing is confirmed and the fifty dinars become a debt against him. His testimony is permitted, his inviolability as a free man is confirmed, as are his inheritance and his liability to the full hudud punishments. The death of his master, however, does not reduce the debt for him at all." </p>
          <p> Malik said that if a man who made his slave a mudabbar died and he had some property at hand and some absent property, and in the property at hand there was not enough (in the third he was allowed to bequeath) to cover the value of the mudabbar, the mudabbar was kept there together with this property, and his tax (kharaj) was gathered until the master's absent property was clear. Then if a third of what his master left would cover his value, he was freed with his property and what had gathered of his tax. If there was not enough to cover his value in what his master had left, as much of him was freed as the third would allow, and his property was left in his hands. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Bequests involving Tadbir </h3>
        <hr>
        <em> <a name="040.40.3.3"> Book 40, Number 40.3.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said, "The generally agreed-on way of doing things in our community is that any setting-free which a man makes in a bequest that he wills in health or illness can be rescinded by him when he likes and changed when he likes as long as it is not a tadbir. There is no way to rescind a tadbir once he has made it. </p>
          <p> "As for every child born to him by a slave-girl who he wills to be set free but he does not make mudabbara, her children are not freed with her when she is freed. That is because her master can change his will when he likes and rescind it when he likes, and being set free is not confirmed for her. She is in the position of a slave-girl whose master says, 'If so-and-so remains with me until I die, she is free.' " (i.e. he does not make a definite contract.) </p>
          <p> Malik said, "If she fulfils that, that is hers. If he wishes, before that, he can sell her and her child because he has not entered her child into any condition he has made for her. </p>
          <p> "The bequest in setting free is different from the tadbir. The precedent of the sunna makes a distinction between them. Had a bequest been in the position of a tadbir, no testator would be able to change his will and what he mentioned in it of setting free. His property would be tied up and he would not be able to use it." </p>
          <p> Malik said about a man who made all his slaves mudabbar while he was well and they were his only property, "If he made some of them mudabbar before the others, one begins with the first until the third of his property is reached. (i.e. their value is matched against the third, and those whose value is covered are free.) If he makes the mall mudabbar in his illness, and says in one statement, 'So-and-so is free. So-and-so is free. So-and-so is free if my death occurs in this illness,' or he makes them all mudabbar in one statement, they are matched against the third and one does not begin with any of them before the others. It is a bequest and they have a third of his property divided between them in shares. Then the third of his property frees each of them according to the extent of his share. </p>
          <p> "No single one of them is given preference when that all occurs in his illness." </p>
          <p> Malik spoke about a master who made his slave a mudabbar and then he died and the only property he had was the mudabbar slave and the slave had property. He said, "A third of the mudabbar is freed and his property remains in his possession." </p>
          <p> Malik said about a mudabbar whose master gave him a kitaba and then the master died and did not leave any property other than him, "A third of him is freed and a third of his kitaba is reduced, and he owes two-thirds." </p>
          <p> Malik spoke about a man who freed half of his slave while he was ill and made irrevocable his freeing half of him or all of him, and he had made another slave of his mudabbar before that. He said, "One begins with the slave he made mudabbar before the one he freed while he was ill. That is because the man cannot revoke what he has made mudabbar and cannot follow it with a matter which will rescind it. When this mudabbar is freed, then what remains of the third goes to the one who had half of him freed so as to complete his setting-free entirely in the third of the property of the deceased. If what is left of the third does not cover that, whatever is covered by what is left of the third is freed after the first mudabbar is freed . " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: A Master's Intercourse with His Slave-girl when he has Made her Mudabbara </h3>
        <hr>
        <em> <a name="040.40.4.4"> Book 40, Number 40.4.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi that Abdullah ibn Umar made two of his slave-girls mudabbara, and he had intercourse with them while they were mudabbara. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="040.40.4.5"> Book 40, Number 40.4.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said that Said ibn al-Musayyab used to say, "When a man makes his slave-girl mudabbara, he can have intercourse with her. He cannot sell her or give her away and her children are in the same position as her." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Selling Mudabbars </h3>
        <hr>
        <em> <a name="040.40.5.6"> Book 40, Number 40.5.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said, "The generally agreed on way of doing things in our community about a mudabbar is that the owner cannot sell him or change the position in which he has put him. If a debt overtakes the master, his creditors cannot sell the mudabbar as long as the master is alive. If the master dies and has no debts, the mudabbar is included in the third (of the bequest) because he expected his work from him as long as he lived. He cannot serve him all his life, and then he frees him from his heirs out of the main portion of his property when he dies. If the master of the mudabbar dies and has no property other than him, one third of him is freed, and two thirds of him belong to the heirs. If the master of the mudabbar dies and owes a debt which encompasses the mudabbar, he is sold to meet the debt because he can only be freed in the third (which is allowed for bequest) ." </p>
          <p> He said, "If the debt only includes half of the slave, half of him is sold for the debt. Then a third of what remains after the debt is freed. " </p>
          <p> Malik said, "It is not permitted to sell a mudabbar and it is not permitted for anyone to buy him unless the mudabbar buys himself from his master. He is permitted to do that. Or else some one gives the master of the mudabbar money and his master who made him a mudabbar frees him. That is also permitted for him." </p>
          <p> Malik said, "His wala' belongs to his master who made him a mudabbar." </p>
          <p> Malik said, "It is not permitted to sell the service of a mudabbar because it is an uncertain transaction since one does not know how long his master will live. That is uncertain and it is not good." </p>
          <p> Malik spoke about a slave who was shared between two men, and one of them made his portion mudabbar. He said, "They estimate his value between them. If the one who made him mudabbar buys him, he is all mudabbar. If he does not buy him, his tadbir is revoked unless the one who retains ownership of him wishes to give his partner who made him mudabbar his value. If he gives him to him for his value, that is binding, and he is all mudabbar." </p>
          <p> Malik spoke about the christian man who made a christian slave of his mudabbar and then the slave became muslim. He said, "One separates the master and the slave, and the slave is removed from his christian master and is not sold until his situation becomes clear. If the christian dies and has a debt, his debt is paid from the price of the slave unless he has in his estate what will pay the debt. Then the mudabbar is set free." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Injuries Caused by Mudabbars </h3>
        <hr>
        <em> <a name="040.40.6.7"> Book 40, Number 40.6.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Umar ibn Abd al-Aziz gave a judgement about the mudabbar who did an injury. He said, "The master must surrender what he owns of him to the injured person. He is made to serve the injured person and recompense (in the form of service) is taken from him as the blood-money of the injury. If he completes that before his master dies, he reverts to his master." </p>
          <p> Malik said, "The generally agreed on way of doing things in our community about a mudabbar who does an injury and then his master dies and the master has no property except him is that the third (allowed to be bequeathed) is freed, and then the blood-money for the in jury is divided into thirds. A third of the blood-money is against the third of him which was set free, and two-thirds are against the two-thirds which the heirs have. If they wish, they surrender what they have of him to the party with the injury, and if they wish, they give the injured person two-thirds of the blood-money and keep their portion of the slave. That is because that injury is a criminal action by the slave and it is not a debt against the master by which whatever setting free and tadbir the master had done would be abrogated. If there were a debt to people held against the master of the slave, as well as the criminal action of the slave, part of the mudabbar would be sold in proportion to the blood-money of the injury and according to the debt. Then one would begin with the blood-money which was for the criminal action of the slave and it would be paid from the price of the slave. Then the debt of his master would be paid, and then one would look at what remained after that of the slave. His third would b be set free, and two-thirds of him would belong to the heirs. That is because the criminal action of the slave is more important than the debt of his master. That is because, if the man dies and leaves a mudabbar slave whose value is one hundred and fifty dinars, and the slave strikes a free man on the head with a blow that lays open the skull, and the blood-money is fifty dinars, and the master of the slave has a debt of fifty dinars, one begins with the fifty dinars which are the blood-money of the head wound, and it is paid from the price of the slave. Then the debt of the master is paid. Then one looks at what remains of the slave, and a third of him is set free and two-thirds of him remain for the heirs. The blood-money is more pressing against his person than the debt of his master. The debt of his master is more pressing than the tadbir which is a bequest from the third of the property of the deceased. None of the tadbir is permitted while the master of the mudabbar has a debt which is not paid. It is a bequest. That is because Allah, the Blessed, the Exalted, said, 'After any bequest that is made or any debt.' " (Sura 4 ayat 10) </p>
          <p> Malik said, "If there is enough in the third property that the deceased can bequeath to free all the mudabbar, he is freed and the blood-money due from his criminal action is held as a debt against him which follows him after he is set free even if that blood-money is the full blood-money. It is not a debt on the master." </p>
          <p> Malik spoke about a mudabbar who injured a man and his master surrendered him to the injured party, and then the master died and had a debt and did not leave any property other than the mudabbar, and the heirs said, "We surrender the mudabbar to the party," whilst the creditor said, "My debt exceeds that." Malik said that if the creditor's debt did exceed that at all , he was more entitled to it and it was taken from the one who owed the debt, according to what the creditor was owed in excess of the blood-money of the injury. If his debt did not exceed it at all, he did not take the slave. </p>
          <p> Malik spoke about a mudabbar who did an injury and had property, and his master refused to ransom him. He said, "The injured party takes the property of the mudabbar for the blood-money of his injury. If there is enough to pay it, the injured party is paid in full for the blood-money of his injury and the mudabbar is returned to his master. If there is not enough to pay it, he takes it from the blood-money and uses the mudabbar for what remains of the blood-money." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Injuries Caused by the Umm Walad </h3>
        <hr>
        <em> <a name="040.40.7.8"> Book 40, Number 40.7.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik said in the case of an umm walad who injured someone, "The blood-money of that injury is the responsibility of her master from his property, unless the blood-money of the injury is greater than the value of the umm walad. Her master does not have to pay more than her value. That is because when the master of a slave or slave-girl surrenders his slave or slave-girl for an injury which one of them has done, he does not owe any more than that, even if the blood-money is greater. As the master of the umm walad cannot surrender her because of the precedent of the sunna, when he pays her price, it is as if he had surrendered her. He does not have to pay more than that. This is the best of what I have heard about the matter. The master is not obliged to assume responsibility for more than an umm walad's value because of her criminal action." </p>
        </blockquote>
        <!--EndHadithBody-->
