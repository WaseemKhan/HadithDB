      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 51: </a> </em>
        <h1> <em> Hair </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: The Sunna on Hair </h3>
        <hr>
        <em> <a name="051.51.1.1"> Book 51, Number 51.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu Bakr ibn Nafi from his father Nafi from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, ordered the moustache to be trimmed and the beard to be left. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.1.2"> Book 51, Number 51.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab that Humay ibn Abd ar-Rahman ibn Awf heard Muawiya ibn Abi Sufyan say from the mimbar in the year that he performed the hajj, holding a lock of hair (i.e. a hairpiece) which he took from one of his guards, "People of Madina! Where are your learned men? I heard the Messenger of Allah, may Allah bless him and grant him peace, forbid the like of this, saying, 'The Banu Israil were destroyed when their women started to use this.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.1.3"> Book 51, Number 51.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Ziyad ibn Sad heard Ibn Shihab say, "The Messenger of Allah, may Allah bless him and grant him peace, let his hair hang down his forehead as Allah willed, and afterwards he parted it." </p>
          <p> Malik said, "There is no harm in a man's looking at the hair of his son's wife or the hair of his wife's mother." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.1.4"> Book 51, Number 51.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar disapproved of castration and said, "The completeness of the created form is in the testicles." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.1.5"> Book 51, Number 51.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Safwan ibn Sulaym heard that the Prophet, may Allah bless him and grant him peace, said, "I and the one who guards the orphan, whether for himself or for someone else, will be like these two in the Garden, when he has taqwa," indicating his middle and index fingers. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Caring for the Hair </h3>
        <hr>
        <em> <a name="051.51.2.6"> Book 51, Number 51.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Abu Qatada al-Ansari said to the Messenger of Allah, may Allah bless him and grant him peace, "I have a lot of hair which comes down to my shoulders, shall I comb it?'' The Messenger of Allah. may Allah bless him and grant him peace, said, "Yes, and honour it." Sometimes Abu Qatada oiled it twice in one day because the Messenger of Allah, may Allah bless him and grant him peace, said to him. "Honour it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.2.7"> Book 51, Number 51.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam that Ata ibn Yasar told him that the Messenger of Allah, may Allah bless him and grant him peace, was in the mosque when a man came in with dishevelled hair and beard. The Messenger of Allah. may Allah bless him and grant him peace, motioned with his hand that he should be sent out to groom his hair and beard. The man did so and then returned. The Messenger of Allah, may Allah bless him and grant him peace, said, "Isn't this better than that one of you should come with his head dishevelled, as if he were a shaytan?" </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Dyeing the Hair </h3>
        <hr>
        <em> <a name="051.51.3.8"> Book 51, Number 51.3.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said that Muhammad ibn Ibrahim at-Taymi had informed him that Abu Salama ibn Abd ar-Rahman said "Abd ar-Rahman ibn al-Aswad ibn Abdal-Yaghuth used to sit with us and he had a white beard and hair. One day he came to us and he had dyed them red, and the people said to him, 'This is better.' He said. 'A'isha the wife of the Prophet, may Allah bless him and grant him peace, sent her slave girl Nukhayla to me yesterday. She swore that my hair would be dyed and she informed me that Abu Bakr as-Siddiq used to dye his hair.' " </p>
          <p> Yahya said that he heard Malik say about dyeing the hair black, 'I have not heard anything certain on that, and other colours than that are preferable to me." </p>
          <p> Yahya said, "Not to dye at all is permitted, Allah willing, and there is no constraint on people concerning it." </p>
          <p> Yahya said that he had heard Malik say, "There is no clear indication in this hadith that the Messenger of Allah, may Allah bless him and grant him peace, did not dye his hair. Had the Messenger of Allah, may Allah bless him and grant him peace, dyed his hair, A'isha would have sent a message to that effect to Abd ar-Rahman ibn al-Aswad." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Taking Refuge </h3>
        <hr>
        <em> <a name="051.51.4.9"> Book 51, Number 51.4.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said that he had heard that Khalid ibn al-Walid said to the Messenger of Allah, may Allah bless him and grant him peace, "I have nightmares." The Messenger of Allah, may Allah bless him and grant him peace, said to him, "Say, 'I seek refuge with the complete words of Allah from His anger and His punishment and the evil of His slaves, and from the evil suggestions of the shayatin and from their being present (at death).' " </p>
          <p> Audhu bi kalimati' llahi't-tammati min ghadabihi wa iqabihi wa sharri ibadihi wa min hamazati' sh-shayatin wa an yahdurun. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.4.10"> Book 51, Number 51.4.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Yahya ibn Said said, "When the Messenger of Allah, may Allah bless him and grant him peace, was taken on the Night Journey, he saw an evil jinn seeking him with a torch of fire. Whenever the Messenger of Allah, may Allah bless him and grant him peace, turned, he saw him. Jibril said to him, 'Shall I teach you some words to say? When you say them, his torch will be put out and will fall from him.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'Yes, indeed.' Jibril said, 'Say, 'I seek refuge with the Noble Face of Allah and with the complete words of Allah which neither the good person nor the corrupt can exceed, from the evil of what descends from the sky and the evil of what ascends in it, and from the evil of what is created in the earth and the evil of what comes out of it, and from the trials of the night and day, and from the visitations of the night and day, except for one that knocks with good, O Merciful!" "' </p>
          <p> Audhu bi wajhi'llahi' l-karim wa bi kalimati'llahi't-tammati. Allati la yujawazu hunna barra wa la fajir, min sharri ma yanzil min as-sama, wa sharri ma yaruju fiha, wa sham ma dhara' fi'l-ard, wa sharri ma yakhruju minha, wa min fitani'l-layli wa'n-nahar, wa min tawariqi'l-layli wa'n-nahar illa tariqan yatruq bikhayr ya Rahman! </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.4.11"> Book 51, Number 51.4.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Suhayl ibn Abi Salih from his father from Abu Hurayra that a man of the Aslam tribe said, "I did not sleep last night." The Messenger of Allah, may Allah bless him and grant him peace, said to him, "For what reason?" He said, "A scorpion bit me." The Messenger of Allah, may Allah bless him and grant him peace, said, "Had you said in the evening, 'I seek refuge with the complete words of Allah from the evil of what He has created, 'it would not have happened." </p>
          <p> Audhu bi kalimati'llahi't-tammati min sharri ma khalaq . </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.4.12"> Book 51, Number 51.4.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Summayy, the mawla of Abu Bakr from al-Qa'qa ibn Hakim that Kab al-Ahbar said, "Had it not been for some words which I said, the jews would have made me into a donkey." Someone asked him what they were. He said, "I seek refuge with the immense Face of Allah - there is nothing greater than it - and with the complete words of Allah which neither the good person nor the corrupt can exceed and with all the most beautiful names of Allah, what I know of them and what I do not know, from the evil of what He has created and originated and multiplied." </p>
          <p> Audhu bi wajhi'llahi l-adheem aladhee laysa shay'un adham minh, wa bi kalimati'llahi't-tammati, alatee la yujawizu hunna barra wa la fajir, wa bi asma'illahi'l-husna kulliha ma alamtu minha wa ma lam alam, min sharri ma khalaqa wa bara'a wa dhara'a. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Those who Love Each Other in Allah </h3>
        <hr>
        <em> <a name="051.51.5.13"> Book 51, Number 51.5.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abd ar-Rahman ibn Mamar from Abu'l-Hubab Said ibn Yasar that Abu Hurayra said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'Allah, the Blessed, the Exalted, will say on the Day of Rising, "Where are those who loved each other for My majesty? Today I will shade them in My shade on the day when there is no shade except My shade." "' </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.5.14"> Book 51, Number 51.5.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Khubayb ibn Abd ar-Rahman al-Ansari from Hafs ibn Asim that either Abu Said al-Khudri or Abu Hurayra said, "The Messenger of Allah, may Allah bless him and grant him peace, said, 'There are seven whom Allah will shade in His shade on the day on which there is no shade except His shade: a just imam, a youth who grows up worshipping Allah, a man whose heart is attached to the mosque when he leaves it until he returns to it, two men who love each other in Allah and meet for that and part for that, a man who remembers Allah when he is alone and his eyes overflow with tears, a man who refuses the approaches of a noble, beautiful woman, saying "I fear Allah," and a man who gives sadaqa and conceals it so that his left hand does not know what his right hand gives.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.5.15"> Book 51, Number 51.5.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Suhayl ibn Abi Salih from his father from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "If Allah loves a slave, he says to Jibril, 'I love so-and-so, so love him,' so Jibril loves him and then calls out to the people of heaven, 'Allah loves so-and-so, so love him,' and the people of heaven love him, and then acceptance is placed in the Earth for him." When Allah is angry with a slave, Malik said, "I consider that he says the like of that about His anger." </p>
          <p> Yahya related to me from Malik from Abu Hazim ibn Dinar that Abu Idris al-Khawlani said, "I entered the Damascus mosque and there was a young man with a beautiful mouth and white teeth sitting with some people. When they disagreed about something, they referred it to him and proceeded from his statement. I inquired about him, and it was said, 'This is Muadh ibn Jabal.' The next day I went to the noon-prayer, and I found that he had preceded me to the noon prayer and I found him praying." </p>
          <p> Abu Idris continued, "I waited for him until he had finished the prayer. Then I came to him from in front of him and greeted him and said, 'By Allah! I love you for Allah!' He said, 'By Allah?' I said, 'By Allah.' He said, 'By Allah?' I said, 'By Allah.' He said, 'By Allah?' I said, 'By Allah.' " </p>
          <p> He continued, "He took me by the upper part of my cloak and pulled me to him and said, 'Rejoice! I heard the Messenger of Allah, may Allah bless him and grant him peace, say, "Allah, the Blessed and Exalted, said, 'My love is obliged for those who love each other in Me, and those who sit with each other in Me, and those who visit each other in Me, and those who give to each other generously in Me.' " ' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="051.51.5.16"> Book 51, Number 51.5.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he had heard that Abdullah ibn Abbas said, "Equanimity, gentleness, and good behaviour are one twenty-fifth of prophecy." </p>
        </blockquote>
        <!--EndHadithBody-->
