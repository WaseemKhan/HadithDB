        <h1> <em> Visions </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Visions </h3>
        <hr>
        <em> <a name="052.52.1.1"> Book 52, Number 52.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ishaq ibn Abdullah ibn Abi Talha al-Ansari from Anas ibn Malik that the Messenger of Allah, may Allah bless him and grant him peace, said, "The good dream of a man who is salih is a forty-sixth part of prophecy." </p>
          <p> Yahya related the like of that to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra from the Messenger of Allah, may Allah bless him and grant him peace. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="052.52.1.2"> Book 52, Number 52.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ishaq ibn Abdullah ibn Abi Talha from Zufar ibn Sasaca from his father from Abu Hurayra that when the Messenger of Allah, may Allah bless him and grant him peace, left the morning prayer, he would say, "Has any of you had a dream last night? All that will remain of prophecy after me will be the true dream ." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="052.52.1.3"> Book 52, Number 52.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam from Ata ibn Yasar that the Messenger of Allah, may Allah bless him and grant him peace, said, "All that will be left of prophecy after me are the mubashshirat." They said, "What are the mubashshirat, Messenger of Allah?" He said, "The true dream which a man who is salih sees - or which is shown to him - is a forty-sixth part of prophecy." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="052.52.1.4"> Book 52, Number 52.1.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said that Abu Salama ibn Abd ar-Rahman said, "I heard Abu Qatada ibn Ribiyy say that he heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'The good dream is from Allah, and the bad dream is from shaytan. When you see what you dislike, spit to your left side three times when you wake up, and seek refuge with Allah from its evil. It will not harm you then, Allah willing.' " Abu Salama said, "I would see dreams which weighed on me more heavily than a mountain. When I heard this hadith, I was not concerned about it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="052.52.1.5"> Book 52, Number 52.1.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa that his father said about this ayat, "You have good news in the life of this world and the next world," (Sura 10 ayat 64), that it was the good dream which the man who was salih saw or which was shown to him. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Games of Dice </h3>
        <hr>
        <em> <a name="052.52.2.6"> Book 52, Number 52.2.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Musa ibn Maysara from Said ibn Abi Hind from Abu Musa al-Ashari that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever plays games of dice has disobeyed Allah and His Messenger. " </p>
          <p> Yahya related to me from Malik from Alqama from his mother that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, heard that the people who lived in a room in her house had some dice. She sent a message to them, "If you do not remove them, I will remove you from my house," and she reproached them for it. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="052.52.2.7"> Book 52, Number 52.2.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that when he found one of his family playing dice he beat him and destroyed the dice. </p>
          <p> Yahya said that he heard Malik say, "There is no good in chess, and he disapproved of it." Yahya said, "I heard him disapprove of playing it and other worthless games. He recited this ayat, 'What is there after the truth except going the wrong way.' " (Sura l0 ayat 32). </p>
        </blockquote>
        <!--EndHadithBody-->
