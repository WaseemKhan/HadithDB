        <h1> <em> Judgements </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Stimulation of Desire to Judge Correctly </h3>
        <hr>
        <em> <a name="036.36.1.1"> Book 36, Number 36.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Hisham ibn Urwa from his father from Zaynab bint Abi Salama from Umm Salama, the wife of the Prophet, may Allah bless him and grant him peace, that the Messenger of Allah, may Allah bless him and grant him peace, said, "I am but a man to whom you bring your disputes. Perhaps one of you is more eloquent in his proof than the other, so I give judgement according to what I have heard from him. Whatever I decide for him which is part of the right of his brother, he must not take any of it, for I am granting him a portion of the Fire." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.1.2"> Book 36, Number 36.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said from Said ibn al-Musayyab that Umar ibn al-Khattab had a dispute brought to him between a muslim and a jew. Umar saw that the right belonged to the jew and decided in his favour. The jew said to him, "By Allah! You have judged correctly.'' So Umar ibn al-Khattab struck him with a whip and said, "How can you be sure." The jew said to him, "We find that there is no judge who judges correctly but that there is an angel on his right side and an angel on his left side who guide him and give him success in the truth as long as he is with the truth. When he leaves the truth, they rise and leave him." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Giving Testimony </h3>
        <hr>
        <em> <a name="036.36.2.3"> Book 36, Number 36.2.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr ibn Muhammad ibn Amr ibn Hazm from his father from Abdullah ibn Amr ibn Uthman from Abu Amra al-Ansari from Zayd ibn Khalid al-Juhani that the Messenger of Allah, may Allah bless him and grant him peace, said, "Shall I not tell you who is the best of witnesses? The one who brings his testimony before he is asked for it, or tells his testimony before he is asked for it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.2.4"> Book 36, Number 36.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that Rabia ibn Abi Abd ar-Rahman said, "An Iraqi man came before Umar ibn al-Khattab and said, 'I have come to you because of a matter which has no beginning and no end.' Umar said, 'What is it?' The man said, 'False testimony has appeared in our land.' Umar said, 'Is that so?' He said, 'Yes.' Umar said, 'By Allah! A man is not detained in Islam without just witnesses.' " </p>
          <p> Malik related to me that Umar ibn al-Khattab said, "The testimony of some one known to bear a grudge or to be unreliable is not accepted." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Testimony of Recipients of Hadd-Punishments </h3>
        <hr>
        <em> <a name="036.36.3.4a"> Book 36, Number 36.3.4a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said from Malik that he heard from Sulayman ibn Yasar and others that when they were asked whether the testimony of a man flogged for a hadd crime was permitted, they said, "Yes, when repentance (tawba) appears from him." </p>
          <p> Malik related to me that he heard Ibn Shihab being asked about that and he said the like of what Sulayman ibn Yasar said. </p>
          <p> Malik said, "That is what is done in our community. It is by the word of Allah, the Blessed, the Exalted, 'And those who accuse women who are muhsan, and then do not bring four witnesses, flog them with eighty lashes, and do not accept any testimony of theirs ever. They indeed are evil-doers, save those who turn in tawba after that and make amends. Allah is Forgiving, Merciful.' " (Sura 24 ayat 4). </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement Based on Oaths with One Witness </h3>
        <hr>
        <em> <a name="036.36.4.5"> Book 36, Number 36.4.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said, "Malik said from Jafar ibn Muhammad from his father that the Messenger of Allah, may Allah bless him and grant him peace, pronounced judgement on the basis of an oath with one witness." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.4.6"> Book 36, Number 36.4.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> From Malik from Abu'z-Zinad that Umar ibn Abd al-Aziz wrote to Abd al-Hamid ibn Abd ar-Rahman ibn Zayd ibn al-Khattab who was the governor of Kufa, "Pronounce judgement on the basis of an oath with one witness." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.4.7"> Book 36, Number 36.4.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that Abu Salama ibn Abd ar-Rahman and Sulayman ibn Yasar were both asked, "Does one pronounce judgement on the basis of an oath with one witness?" They both said, "Yes." </p>
          <p> Malik said, "The precedent of the sunna in judging by an oath with one witness is that if the plaintiff takes an oath with his witness, he is confirmed in his right. If he draws back and refuses to take an oath, the defendant is made to take an oath. If he takes an oath, the claim against him is dropped. If he refuses to take an oath, the claim is confirmed against him." </p>
          <p> Malik said, "This procedure pertains to property cases in particular. It does not occur in any of the hadd-punishments, nor in marriage, divorce, freeing slaves, theft or slander. If some one says, 'Freeing slaves comes under property,' he has erred. It is not as he said. Had it been as he said, a slave could take an oath with one witness, if he could find one, that his master had freed him. </p>
          <p> "However, when a slave lays claim to a piece of property, he can take an oath with one witness and demand his right as the freeman demands his right." </p>
          <p> Malik said, "The sunna with us is that when a slave brings somebody who witnesses that he has been set free, his master is made to take an oath that he has not freed him, and the slave's claim is dropped." </p>
          <p> Malik said, "The sunna about divorce is also like that with us. When a woman brings somebody who witnesses that her husband has divorced her, the husband is made to take an oath that he has not divorced her. If he takes the oath, the divorce does not proceed . " </p>
          <p> Malik said, "There is only one sunna of bringing a witness in cases of divorce and freeing a slave. The right to make an oath only belongs to the husband of the woman, and the master of the slave. Freeing is a hadd matter, and the testimony of women is not permitted in it because when a slave is freed, his inviolability is affirmed and the hadd punishments are applied for and against him. If he commits fornication and he is a muhsan, he is stoned. If he kills a slave, he is killed for it. Inheritance is established for him, between him and whoever inherits from him. If somebody disputes this, arguing that if a man frees his slave and then a man comes to demand from the master of the slave payment of a debt, and a man and two women testify to his right, that establishes the right against the master of the slave so that his freeing him is cancelled if he only has the slave as property, inferring by this case that the testimony of women is permitted in cases of setting free. The case is not as he suggests (i.e. it is a case of property not freeing). It is like a man who frees his slave, and then the claimant of a debt comes to the master and takes an oath with one witness, demanding his right. By that, the freeing of the slave would be cancelled. Or else a man comes who has frequent dealings and transactions with the master of the slave. He claims that he is owed money by the master of the slave. Someone says to the master of the slave, 'Take an oath that you don't owe what he claims'. If he draws back and refuses to take an oath, the one making the claim takes an oath and his right against the master of the slave is confirmed. That would cancel the freeing of the slave if it is confirmed that property is owed by the master." </p>
          <p> Malik said, "It is the same case with a man who marries a slave-girl and then the master of the slave-girl comes to the man who has married her and claims, 'You and so-and-so have bought my slave-girl from me for such an amount of dinars. The husband of the slave-girl denies that. The master of the slave-girl brings a man and two women and they testify to what he has said. The sale is confirmed and his claim is considered true. So the slave-girl is haram for her husband and they have to separate, even though the testimony of women is not accepted in divorce." </p>
          <p> Malik said, "It is also the same case with a man who accuses a free man, so the hadd falls on him. A man and two women come and testify that the one accused is a slave. That would remove the hadd from the accused after it had befallen him, even though the testimony of women is not accepted in accusations involving hadd punishments." </p>
          <p> Malik said, "Another similar case in which judgement appears to go against the precedent of the sunna is that two women testify that a child is born alive and so it is necessary for him to inherit if a situation arises where he is entitled to inherit, and the child's property goes to those who inherit from him, if he dies, and it is not necessary that the two women witnesses should be accompanied by a man or an oath even though it may involve vast properties of gold, silver, live-stock, gardens and slaves and other properties. However, had two women testified to one dirham or more or less than that in a property case, their testimony would not affect anything and would not be permitted unless there was a witness or an oath with them." </p>
          <p> Malik said, "There are people who say that an oath is not acceptable with only one witness and they argue by the word of Allah the Blessed, the Exalted, and His word is the Truth, 'And call in to witness two witnesses, men; or if the two be not men, then one man and two women, such witnesses as you approve of.' (Sura 2 ayat 282). Such people argue that if he does not bring one man and two women, he has no claim and he is not allowed to take an oath with one witness." </p>
          <p> Malik said, "Part of the proof against those who argue this, is to reply to them, 'Do you think that if a man claimed property from a man, the one claimed from would not swear that the claim was false?' If he swears, the claim against him is dropped. If he refuses to take an oath, the claimant is made to take an oath that his claim is true, and his right against his companion is established. There is no dispute about this with any of the people nor in any country. By what does he take this? In what place in the Book of Allah does he find it? So if he confirms this, let him confirm the oath with one witness, even if it is not in the Book of Allah, the Mighty, the Majestic! It is enough that this is the precedent of the sunna. However, man wants to recognise the proper course of action and the location of the proof. In this there is a clarification for what is obscure about that, if Allah ta'ala wills." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on a Deceased with a Debt against Him and a Debt for Him, and only One Witness </h3>
        <hr>
        <em> <a name="036.36.5.7a"> Book 36, Number 36.5.7a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik spoke about a man who died and had a debt owing to him and there was one witness, and some people had a debt against him and they had only one witness, and his heirs refused to take an oath on their rights with their witness. He said, "The creditors take an oath and take their rights. If there is anything left over, the heirs do not take any of it. That is because the oaths were offered to them before and they abandoned them, unless they say, 'We did not know that our companion had extra,' and it is known that they only abandoned the oaths because of that. I think that they should take an oath and take what remains after his debt." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Claims </h3>
        <hr>
        <em> <a name="036.36.6.8"> Book 36, Number 36.6.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said, "Malik said about Jamil ibn Abd ar-Rahman al-Muadhdin that he was present with Umar ibn Abd al-Aziz when he was judging between people. If a man came to him with a claim against a man, he examined whether or not there were frequent transactions and dealings between them. If there were, the defendant could make an oath. If there was nothing of that nature he did not accept an oath from him." </p>
          <p> Malik summed up, "What is done in our community is that if some one makes a claim against a man, it is examined. If there are frequent transactions and dealings between them, the defendant is made to take an oath. If he takes an oath, the claim against him is dropped. If the defendant refuses to take an oath, and returns the oath to the claimant, the one claiming his right takes an oath and takes his due." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Testimony of Children </h3>
        <hr>
        <em> <a name="036.36.7.9"> Book 36, Number 36.7.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said, "Malik said from Hisham ibn Urwa that Abdullah ibn az-Zubayr gave judgment based on the testimony of children concerning the injuries between them." </p>
          <p> Malik said, "The generally agreed on way of doing things in our community is that the testimony of children is permitted concerning injuries between them. It is not accepted about anything else. It is only permitted between them if they testify before they leave the scene of the incident and have been deceived or instructed. If they leave the scene, they have no testimony unless they call just witnesses to witness their testimony before they leave." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Perjury on the Mimbar of the Prophet, may Allah Bless Him and Grant Him Peace </h3>
        <hr>
        <em> <a name="036.36.8.10"> Book 36, Number 36.8.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said, Malik related to us from Hisham ibn Hisham ibn Utba ibn Abi Waqqas from Abdullah ibn Nistas from Jabir ibn Abdullah al-Ansari that the Messenger of Allah, may Allah bless him and grant him peace, said, 'If someone swears a false oath near this mimbar of mine, he will take his seat in the fire.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.8.11"> Book 36, Number 36.8.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from al-Ala ibn Abd ar-Rahman from Mabad ibn Kab as-Salami from his brother Abdullah ibn Kab ibn Malik al-Ansari from Abu Umama that the Messenger of Allah, may Allah bless him and grant him peace, said, "Whoever cuts off the right of a muslim man by his oath, Allah forbids him the Garden and obliges the Fire for him." They said, "Even if it is something insignificant, Messenger of Allah?" He said, "Even if it is a tooth-stick, even if it is a tooth-stick," repeating it three times. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Taking Oaths on the Mimbar in General </h3>
        <hr>
        <em> <a name="036.36.9.12"> Book 36, Number 36.9.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that Malik had said from Da'ud ibn al-Husayn that he heard Abu Ghatafan ibn Tarif al-Muriyi say, "Zayd ibn Thabit al-Ansari and Ibn Muti had a dispute about a house which they shared. They went to Marwan ibn al-Hakam who was the Amir of Madina. Marwan decided that Zayd ibn Thabit must take an oath on the mimbar. Zayd ibn Thabit said, 'I swear to it where I am.' Marwan said, 'No, by Allah! only in the place of sorting out claims (i.e. the mimbar).' Zayd ibn Thabit began to take an oath that his right was true, and he refused to take an oath near the mimbar. Marwan ibn al-Hakam began to wonder at that." </p>
          <p> Malik said, "I do not think that anyone should be made to take an oath near the mimbar for less than a fourth of a dinar, and that is three dirhams." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Prohibition against Forfeiting Pledges Given on Security </h3>
        <hr>
        <em> <a name="036.36.10.13"> Book 36, Number 36.10.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said, "Malik related to us from Ibn Shihab from Sa'id ibn al-Musayyab that the Messenger of Allah, may Allah bless him and grant him peace, said, 'The pledge given as security is not forfeited.' " </p>
          <p> Malik said, "The explanation of that according to what we think - and Allah knows best - is that a man gives a pledge to somebody in security for something. The pledge is superior to that for which he pawned it. The pledger says to the pawn-broker, 'I will bring you your due, after such-and-such a time. If not, the pledge is yours for what it was pawned for.' " </p>
          <p> Malik said, "This transaction is not good and it is not halal. This is what was forbidden. If the owner brings what he pledged it for after the period, it is his. I think that the time condition is void." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Pledging Fruit and Animals as Security </h3>
        <hr>
        <em> <a name="036.36.11.13a"> Book 36, Number 36.11.13a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said, "I heard Malik say that if a man pledges his garden for a stated period and the fruits of that garden are ready before the end of that period, the fruits are not included in the pledge with the real estate, unless it is stipulated by the pledger in his pledge. However, if a man receives a slave-girl as a pledge and she is pregnant or she becomes pregnant after his taking her as a pledge, her child is included with her. </p>
          <p> "A distinction is made between the fruit and the child of the slave-girl. The Messenger of Allah, may Allah bless him and grant him peace, said, 'If someone sells a palm which has been pollinated, the fruit belongs to the seller unless the buyer stipulates its inclusion.' The undisputed way of doing things in our community is that if a man sells a slave-girl or an animal with a foetus in its womb, the foetus belongs to the buyer, whether or not the buyer stipulates it. The palm is not like the animal. Fruit is not like the foetus in its mother's womb. Part of what clarifies that is also that it is the usage of people to have a man pawn the fruit of the palm apart from the palm. No one pawns the foetus in its mother's womb whether of slaves or animals." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Pledging Animals as Security </h3>
        <hr>
        <em> <a name="036.36.12.13b"> Book 36, Number 36.12.13b: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he had heard Malik say, "The undisputed way of doing things in our community concerning pledges is that in cases where land or a house or an animal are known to have been destroyed whilst in the possession of the broker of the pledge, and the circumstances of the loss are known, the loss is against the pledger. There is no deduction made from what is due to the broker at all. Any pledge which perishes in the possession of the broker and the circumstances of its loss are only known by his word, the loss is against the broker and he is liable for its value. He is asked to describe whatever was destroyed and then he is made to take an oath about that description and what he loaned on security for it. "Then people of discernment evaluate the description. If the pledge was worth more than what the broker loaned, the pledger takes the extra. If the assessed value of the pledge is less than what he was loaned, the pledger is made to take an oath as to what the broker loaned and he does not have to pay the extra which the broker loaned above the assessed value of the pledge. If the pledger refuses to take an oath, he has to give the broker the extra above the assessed value of the pledge. If the broker says that he doesn't know the value of the pledge, the pledger is made to take an oath on the description of the pledge and that is his if he brings a matter which is not disapproved of." </p>
          <p> Malik said, "All this applies when the broker takes the pledge and does not put it in the hands of another." </p>
