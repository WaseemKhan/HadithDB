        <h3> Section: Judgement on Renting Animals and Going Beyond Specified Destinations </h3>
        <hr>
        <em> <a name="036.36.15.13e"> Book 36, Number 36.15.13e: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "What is done in our community about a man who rents an animal for a journey to a specified place and then he goes beyond that place and further, is that the owner of the animal has a choice. If he wants to take extra rent for his animal to cover the distance overstepped, he is given that on top of the first rent and the animal is returned. If the owner of the animal likes to sell the animal from the place where he over-steps, he has the price of the animal on top of the rent. If, however, the hirer rented the animal to go and return and then he overstepped when he reached the city to which he rented him, the owner of the animal only has half the first rent. That is because half of the rent is going, and half of it is returning. If he oversteps with the animal, only half of the first rent is obliged for him. Had the animal died when he reached the city to which it was rented, the hirer would not be liable and the renter would only have half the rent." </p>
          <p> Malik said, "That is what is done with people who overstep and dispute about what they took the animal for." </p>
          <p> Malik said, "It is also like that with some one who takes qirad-money from his companion. The owner of the property says to him, 'Do not buy such-and-such animals or such-and-such goods.' He names them and forbids them and disapproves of his money being invested in them. The one who takes the money then buys what he was forbidden. By that, he intends to be liable for the money and take the profit of his companion. When he does that, the owner of the money has an option. If he wants to enter with him in the goods according to the original stipulations between them about the profit, he does so. If he likes, he has his capital guaranteed against the one who took the capital and over stepped the mark." </p>
          <p> Malik said, "It is also like that with a man with whom another man invests some goods. The owner of the property orders him to buy certain goods for him which he names. He differs, and buys with the goods something other than what he was ordered to buy. He exceeded his orders. The owner of the goods has an option. If he wants to take what was bought with his property, he takes it. If he wants the partner to be liable for his capital he has that." </p>
        </blockquote>
