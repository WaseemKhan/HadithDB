          <p> Malik related to me from Amr ibn Yahya al-Mazini from his father that ad-Dahhak ibn Khalifa watered his irrigation ditch from a large source of water. He wanted to have it pass through the land of Muhammad ibn Maslama, and Muhammad refused. Ad-Dahhak said to him, "Why do you prevent me? It will benefit you. You can drink from it first and last and it will not harm you." Muhammed refused so ad-Dahhak spoke about it to Umar ibn al-Khattab, and Umar ibn al-Khattab summoned Muhammad ibn Maslama and ordered him to clear the way. Muhammad said, "No." Umar said, "Why do you prevent your brother from what will benefit him and is also useful for you? You will take water from it first and last and it will not harm you." </p>
          <p> Muhammad said, "No, by Allah!" Umar said, "By Allah, he will pass it through, even if it is over your belly!" Umar ordered him to allow its passage and ad-Dahhak did so. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.26.34"> Book 36, Number 36.26.34: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Amr ibn Yahya al-Mazini that his father said, "There was a stream in my grand-father's garden belonging to Abd ar-Rahman ibn Awf Abd ar-Rahman ibn Awf wanted to transfer it to a corner of the garden nearer to his land, and the owner of the garden prevented him. Abd ar-Rahman ibn Awf spoke to Umar ibn al-Khattab about it, and he gave a judgement to Abd ar-Rahman ibn Awf that he should transfer it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Division of Properties </h3>
        <hr>
        <em> <a name="036.36.27.35"> Book 36, Number 36.27.35: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Thawr ibn Zayd ad-Dili said, "I heard that the Messenger of Allah, may Allah bless him and grant him peace, said, 'A house or land that has been divided in the Jahiliyya, it is according to the division of the Jahiliyya. A house or land which has not been divided before the coming of Islam is divided according to Islam.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.27.36"> Book 36, Number 36.27.36: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik speak about a man who died and left properties in Aliya and Safila (outlying districts of Madina). He said, "Unirrigated naturally watered land is not in the same category as irrigated land unless the family are satisfied with that. Unirrigated land is only in the same category as land with a spring when it resembles it. When the properties are in one land, and are close together, each individual property is evaluated and then divided between the heirs. Dwellings and houses are in the same position." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Animals Grazing on Other People's Crops and Animals Stolen from the Herd </h3>
        <hr>
        <em> <a name="036.36.28.37"> Book 36, Number 36.28.37: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ibn Shihab from Haram ibn Sad ibn Muhayyisa that a female camel of al-Bara ibn Azib entered the garden of a man and it did some damage to it. The Messenger of Allah, may Allah bless him and grant him peace, gave a judgement that the people of the garden were responsible for guarding it in the day, and the owner of the animals was liable for what the animals destroyed at night. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.28.38"> Book 36, Number 36.28.38: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Hisham ibn Urwa from his father from Yahya ibn Abd ar-Rahman ibn Hatib that some slaves of Hatib stole a she-camel belonging to a man from the Muzayna tribe and they slaughtered it. The case was brought before Umar ibn al-Khattab, and Umar ordered Kathir ibn as-Salt to cut off their hands. Then Umar said to Habib, "I think you must be starving them," and he added, "By Allah! I will make you pay such a fine that it will be heavy for you." He enquired of the man from the Muzayna tribe, "What was the price of your camel?" The Muzayni said, "By Allah, I refused to sell her for 400 dirhams.'' Umar said, ''Give him 800 dirhams." </p>
          <p> Yahya said that he heard Malik say, "Doubling the price is not the behaviour of our community. What people have settled on among us is that the man is obliged to pay the value of the camel or animal on the day he took it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Injuries to Domestic Animals </h3>
        <hr>
        <em> <a name="036.36.29.38a"> Book 36, Number 36.29.38a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "What is done in our community about injury to a domestic animal, is that the one who injures it must pay the amount by which he has diminished the animal's price." </p>
          <p> Yahya said that he heard Malik speak about a camel who attacked a man and he feared for himself and killed it or hamstrung it. He said, "If he has a clear proof that it was heading for him and had attacked him, there are no damages against him. If there is no clear proof except his word, he is responsible for the camel." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Articles Given to Artisans to Work On </h3>
        <hr>
        <em> <a name="036.36.30.38b"> Book 36, Number 36.30.38b: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related that he heard Malik say that if a man gave a washer a garment to dye and he dyed it, and then the owner of the garment said, "I did not order you to use this dye," and the  washer protested that he had done so, then the washer was to be believed. It was the same with the tailor and the gold-smith. They took an oath about it unless they produced something they would not normally have been employed to do. In that situation their statement was not allowed and the owner of the garment had to take an oath . If he rejected it and refused to swear, then the dyer was made to take an oath. </p>
          <p> Yahya said, "I heard Malik speak about a dyer who was given a garment and he made a mistake and gave it to another man and the one to whom he gave it wore it. He said, 'The one who wore it has no damages against him, and the washer pays damages to the owner of the garment. That is when the man wears the garment which was given him without recognizing that it is not his. If he wears it knowing that it is not his garment, he is responsible for it.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Taking On Debts and Transfers of Debt </h3>
        <hr>
        <em> <a name="036.36.31.38c"> Book 36, Number 36.31.38c: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "What is done in our community about a man who refers a creditor to another man for the debt he owes him is that if the one referred to goes bankrupt or dies, and does not leave enough to pay the debt, then the creditor has nothing against the one who referred him and the debt does not return to the first party." </p>
          <p> Malik said, "This is the way of doing things about which there is no dispute in our community." </p>
          <p> Malik said, "If a man has his debt to somebody taken on for him by another man and then the man who took it on dies or goes bankrupt, then whatever was taken on by him returns to the first debtor." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Garments Bought with Defects </h3>
        <hr>
        <em> <a name="036.36.32.38d"> Book 36, Number 36.32.38d: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "If a man buys a garment which has a defect, a burn or something else, which the seller knows about and that is testified against him or he confirms it, and the man who has bought it causes a new tear which decreases the price of the garment, and then he learns about the original defect, he can return it to the seller and he is not liable for his tearing it. </p>
          <p> "If a man buys a garment which has a defect of a burn or flaw, and the one who sold it to him claims that he did not know about it, and the buyer has cut the garment or dyed it, then the buyer has an option . If he wishes, he can have a reduction according to what the burn or flaw detracts from the price of the garment and he can keep the garment, or if he wishes to pay damages for what the cutting or dyeing has decreased of the price of the garment and return it, he can do so. </p>
          <p> "If the buyer has dyed the garment with a dye which increases the value, the buyer has an option. If he wishes, he has a reduction from the price of the garment according to what the defect diminishes or if he wishes to become a partner with the one who sold the garment he does so. The price of the garment with a burn or flaw is looked at. If the price is ten dirhams, and the amount by which the dyeing increased the value is five dirhams, then they are partners in the garment, each according to his share. In this reckoning is the amount by which the dyeing increases the price of the garment." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What is Not Permitted in Giving Gifts (1) </h3>
        <hr>
        <em> <a name="036.36.33.39"> Book 36, Number 36.33.39: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to us from Malik from Ibn Shihab from Humayd ibn Abd ar-Rah man ibn Awf and from Muhammad ibn an-Numan ibn Bashir that they related to him that an-Numan ibn Bashir said that his father Bashir brought him to the Messenger of Allah, may Allah bless him and grant him peace, and said, "I have given this son of mine one of my slaves." The Messenger of Allah, may Allah bless him and grant him peace, said, "Have you given each of your children the same as this?" He said, "No." The Messenger of Allah, may Allah bless him and grant him peace, said, "Then take the slave back." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.33.40"> Book 36, Number 36.33.40: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Urwa ibn az-Zubayr that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "Abu Bakr as-Siddiq gave me palm trees whose produce was twenty awsuq from his property at al-Ghaba. When he was dying, he said, 'By Allah, little daughter, there is no one I would prefer to be wealthy after I die than you. There is no one it is more difficult for me to see poor after I die than you. I gave you palm-trees whose produce is twenty awsuq. Had you cut them and taken possession of them, they would have been yours, but today they are the property of the heirs, and they are your two brothers and your two sisters, so divide it according to the Book of Allah.' A'isha continued, "I said, 'My father! By Allah, even if it had been more, I would have left it. There is only Asma. Who is my other sister?" Abu Bakr replied, 'What is in the womb of Kharija? (Kharija was the wife of Abu Bakr's 'brother' from the Ansar.) I think that it is going to be a girl.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.33.41"> Book 36, Number 36.33.41: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Urwa ibn az-Zubayr from Abd ar-Rahman ibn Abd al-Qari that Umar ibn al-Khattab said, "What is wrong with men who give their sons gifts and then keep them and if the son dies, they say, 'My property is in my possession and I did not give it to anyone.' But if they themselves are dying, they say, 'It belongs to my son, I gave it to him.' Whoever gives a gift, and does not hand it over to the one to whom it was given, the gift is invalid, and if he dies it belongs to the heirs in general." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: What is Not Permitted in Giving Gifts (2) </h3>
        <hr>
        <em> <a name="036.36.34.41a"> Book 36, Number 36.34.41a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "What is done in our community about some one who gives a gift not intending a reward is that he calls witnesses to it. It is affirmed for the one to whom it has been given unless the giver dies before the one to whom it was given receives the gift." </p>
          <p> He said, "If the giver wants to keep the gift after he has had it witnessed, he cannot. If the recipient claims it from him, he takes it." </p>
          <p> Malik said, "If some one gives a gift and then withdraws it and the recipient brings a witness to testify for him that he was given the gift, be it goods, gold, silver or animals, the recipient is made to take an oath. If he refuses, the giver is made to take an oath. If he also refuses to take an oath, he gives to the recipient what he claims from him if he has at least one witness. If he does not have a witness, he has nothing . " </p>
          <p> Malik said, "If someone gives a gift not expecting anything in return and then the recipient dies, the heirs are in his place. If the giver dies before the recipient has received his gift, the recipient has nothing. That is because he was given a gift which he did not take possession of. If the giver wants to keep it, and he has called witnesses to the gift, he cannot do that. If the recipient claims his right he takes it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Gifts </h3>
        <hr>
        <em> <a name="036.36.35.42"> Book 36, Number 36.35.42: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Da'ud ibn al-Husayn from Abu Ghatafan ibn Tarif al-Muriyi that Umar ibn al-Khattab said, "If someone gives a gift to strengthen ties with a relative or as sadaqa, he cannot have it returned. If some one, however, gives a gift seeking by it favour or reward, he has his gift and can reclaim it if he does not have satisfaction from it." </p>
          <p> Yahya said that he heard Malik say, "The generally agreed-on way of doing things in our community is that if the gift is returned to the one who gave it for recompense, and its value has been either increased or decreased, the one to whom it has been given gives the owner its value on the day he received it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Taking Back Sadaqa </h3>
        <hr>
        <em> <a name="036.36.36.42a"> Book 36, Number 36.36.42a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "The way of doing things in our community about which there is no dispute, is that if a man gives sadaqa to his son - sadaqa which the son takes possession of or which is in the father's keeping and the father has had his sadaqa witnessed, he cannot take back any of it because he cannot reclaim any sadaqa." </p>
          <p> Yahya said that he heard Malik say, "The generally agreed-on way of doing things in our community in the case of someone who gives his son a gift or grants him a gift which is not sadaqa is that he can take it back as long as the child does not start a debt, which people claim from him, and which they trust him for on the strength of the gift his father has given him. The father cannot take back anything from the gift after debts are started against it. </p>
          <p> "If a man gives his son or daughter something and a woman marries the man, and she only marries him for the wealth and the property which his father has given him and so the father wants to take that back, or, if a man marries a woman whose father has given her a gift and he marries her with an increased bride-price because of the wealth and property that her father has given, then the father says, 'I will take that back,' then the father cannot take back any of that from the son or daughter if it is as I have described to you." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Life Pensions </h3>
        <hr>
        <em> <a name="036.36.37.43"> Book 36, Number 36.37.43: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ibn Shihab from Abu Salama ibn Abd ar-Rahman ibn Awf from Jabir ibn Abdullah al-Ansari that the Messenger of Allah, may Allah bless him and grant him peace, said, "If someone is given a life pension, for him and his posterity, it belongs to the person to whom it has been given. It never reverts to the one who gave it because he gave a gift and the rules of inheritance apply to it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.37.44"> Book 36, Number 36.37.44: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said that Abd ar-Rahman ibn al-Qasim ibn Muhammad heard Makhul ad-Dimashqi ask al-Qasim ibn Muhammad about the life pension and what people said about it. Al-Qasim ibn Muhammad said, "I have only come upon people who keep to the conditions they make about their property and what they are given." </p>
          <p> Yahya said that he heard Malik say, "What is done in our community is that the life pension reverts to the one who makes it a life pension unless he says, 'It belongs to you and your posterity.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.37.45"> Book 36, Number 36.37.45: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi that Abdullah ibn Umar inherited the house of Hafsa bint Umar. He said, "Hafsa gave lodging to the daughter of Zayd ibn al-Khattab for as long as she lived. When the daughter of Zayd died, Abdullah ibn Umar took possession of the dwelling and considered that it was his." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: The Judgement on Lost Property </h3>
        <hr>
        <em> <a name="036.36.38.46"> Book 36, Number 36.38.46: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Rabia ibn Abi Abd ar-Rahman from Yazid, the mawla of al-Munbaith that Zayd ibn Khalid al-Juhani said, "A man came to the Messenger of Allah, may Allah bless him and grant him peace, and asked him about finds. He said, 'Memorize the characteristics of the object found, then publicise it for a year. If the owner comes, give it to him. If not, then it is your business.' He said, 'What about lost sheep, Messenger of Allah?' He said, 'They are yours, your brother's or the wolf's.' He said, 'And the lost camel?' He said, 'It's none of your concern. It has its water and its feet. It will reach water and eat trees until its owner finds it.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.38.47"> Book 36, Number 36.38.47: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Ayyub ibn Musa from Muawiya ibn Abdullah ibn Badr al-Juhani that his father informed him that he stopped with a people on the way to Syria and he found a purse which had eighty dinars in it. He mentioned that to Umar ibn al-Khattab. Umar said to him, "Announce it at the doors of the mosques and mention it to everyone who comes from Syria for a year. When a year passes, it is your business." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.38.48"> Book 36, Number 36.38.48: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Nafi that a man found something and went to Abdullah ibn Umar and said to him, "I have found something. What do you think I should do about it?" Abdullah ibn Umar said to him, "Publicise it!" He said, "I have done so." He said, "Do it again." He said, "I have done so." Abdullah said, "I do not order you to use it. If you wished, you could have left it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Slaves Using Finds </h3>
        <hr>
        <em> <a name="036.36.39.48a"> Book 36, Number 36.39.48a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "What is done in our community about a slave who finds something and uses it before the term which is set for finds has been reached, and that is a year, is that it is against his person. Either his master gives the price of what his slave has used, or he surrenders his slave to them as compensation. If he withheld it until the term was reached which is set for finds and he used it, it is a debt against him which follows him and it is not against his person and there is nothing against his master in it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Judgement on Strays </h3>
        <hr>
        <em> <a name="036.36.40.49"> Book 36, Number 36.40.49: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said from Sulayman ibn Yasar that Thabit ibn ad-Dahhak al-Ansari told him that he had found a camel at Harra, so he hobbled it and mentioned it to Umar ibn al-Khattab and Umar ordered him to make it known three times. Thabit said to him, "That would distract me from the running of my estate." Umar said to him, "Then let it go where you found it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.40.50"> Book 36, Number 36.40.50: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Yahya ibn Said from Said ibn al-Musayyab that Umar ibn al-Khattab said while he was leaning his back against the Kaba, "Whoever takes a stray is astray." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.40.51"> Book 36, Number 36.40.51: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard Ibn Shihab say, "The stray camels in the time of Umar ibn al-Khattab were numerous and left alone. No one touched them until the time of Uthman ibn Affan. He ordered that they be publicised and then sold, and if the owner came afterwards, he was given their price." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Sadaqa of the Living for the Dead </h3>
        <hr>
        <em> <a name="036.36.41.52"> Book 36, Number 36.41.52: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Said ibn Amr Shurahbil ibn Said ibn Sad ibn Ubada from his father that his father said, ''Sad ibn Ubada went out with the Messenger of Allah, may Allah bless him and grant him peace, in one of his raids and his mother was dying in Madina. Someone said to her, 'Leave a testament.' She said, 'In what shall I leave a testament? The property is Sad's property.' Then she died before Sad returned. When Sad ibn Ubada returned, that was mentioned to him. Sad said, </p>
          <p> 'Messenger of Allah! Will it help her if I give sadaqa for her?' The Messenger of Allah, may Allah bless him and grant him peace, said, 'Yes' Sad said, 'Such-and-such a garden is sadaqa for her,' naming the garden." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.41.53"> Book 36, Number 36.41.53: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Hisham ibn Urwa from his father from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that a man said to the Messenger of Allah, may Allah bless him and grant him peace, "My mother died suddenly, and I think that had she spoken, she would have given sadaqa. Shall I give sadaqa for her?" The Messenger of Allah, may Allah bless him and grant him peace, said, "Yes." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="036.36.41.54"> Book 36, Number 36.41.54: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me that he heard that a man of the Ansar from the tribe of Banu al-Harith ibn al-Khazraj, gave sadaqa to his parents and then they died. Their son inherited the property he had given them and it was palm-trees. He asked the Messenger of Allah, may Allah bless him and grant him peace, about it and he said, "You are rewarded for your sadaqa, and take it as your inheritance." </p>
        </blockquote>
        <!--EndHadithBody-->
