        <h3> Section: Judgement on Pledges in General </h3>
        <hr>
        <em> <a name="036.36.14.13d"> Book 36, Number 36.14.13d: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik speak about someone who pledged goods as security for a loan, and they perished with the broker. The one who took out the loan confirmed its specification. They agreed on the amount of the loan, but challenged each other about the value of the pledge, the pledger saying that it had been worth twenty dinars, whilst the broker said that it had been worth only ten, and that the amount loaned on security was twenty dinars. Malik said, "It is said to the one in whose hand the pledge is, 'describe it.' If he describes it he is made to take an oath on it and then the people of experience evaluate that description. If the value is more than what was loaned on security for it, it is said to the broker, 'Return the rest of his due to the pledger.' If the value is less than what was loaned on security for it, the broker takes the rest of his due from the pledger. If the value is the exact amount of the loan, the pledge is compensated for by the loan." </p>
          <p> Yahya said that he heard Malik say, "What is done in our community about two men who have a dispute about an amount of money loaned on the security of a pledge - the pledger claiming that he pledged it for ten dinars and the broker insisting that he took the pledge as security for twenty dinars, and the pledge is clearly in the possession of the broker - is that the broker is made to take an oath when the value of the pledge is fully known. If the value of the pledge is exactly what he swore that he had loaned on security for it, the broker takes the pledge as his right. He is more entitled to take precedence with an oath since he has possession of the pledge. If the owner of the pledge wants to give him the amount which he swore that he was owed, he can take the pledge back. If the pledge is worth less than the twenty dinars he loaned, then it is said to the pledger, 'Either you give him what he has sworn to and take your pledge back, or you swear to what you said you pledged it for.' If the pledger takes the oath, then what the broker has increased over the value of the pledge will become invalid. If the pledger does not take an oath, he must pay what the broker swore to." </p>
          <p> Malik said, "If a pledge given on security for a loan perishes, and both parties deny each other's rights, with the broker who is owed the loan saying that he gave twenty dinars, and the pledger who owes the loan saying that he was given only ten, and with the broker who is owed the loan saying the pledge was worth ten dinars, and the broker who owes the loan saying it was worth twenty, then the broker who is owed the loan is asked to describe the pledge. If he describes it, he must take an oath on its description. Then people with experience of it evaluate that description. If the value of the pledge is estimated to be more than what the broker claims it was, he takes an oath as to what he claimed, and the pledger is given what is over from the value of the pledge. If its value is less than what the broker claims of it, he is made to take an oath as to what he claims is his. Then he demands settlement according to the actual value of the pledge. The one who owes the loan is then made to take an oath on the extra amount which remains owing against him to the claimant after the price of the pledge is reached. That is because the broker becomes a claimant against the pledger. If he takes an oath, the rest of what the broker swore to of what he claimed above the value of the pledge is invalidated. If he draws back, he is bound to pay what remains due to the broker after the value of the pledge." </p>
        </blockquote>
