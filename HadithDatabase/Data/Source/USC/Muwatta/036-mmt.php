        <h3> Section: Judgement on Inheritance of Attached Children </h3>
        <hr>
        <em> <a name="036.36.22.23a"> Book 36, Number 36.22.23a: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya said that he heard Malik say, "The way of doing things generally agreed upon in our community in the case of a man who dies and has sons and one of them claims, 'My father confirmed that so-and-so was his son,' is that the relationship is not established by the testimony of one man, and the confirmation of the one who confirmed it is only permitted as regards his own share in the division of his father's property. The one testified for is only given his due from the share of the testifier." </p>
          <p> Malik said, "An example of this is that a man dies leaving two sons, and 600 dinars. Each of them takes 300 dinars. Then one of them testifies that his deceased father confirmed that so-and-so was his son. The one who testifies is obliged to give 100 dinars to the one thus connected. This is half of the inheritance of the one thought to be related, had he been related. If the other confirms him, he takes the other 100 and so he completes his right and his relationship is established. His position is similar to that of a woman who confirms a debt against her father or her husband and the other heirs deny it. She must pay to the person whose debt she confirms, the amount according to her share of the full debt, had it been confirmed against all the heirs. If the woman inherits an eighth, she pays the creditor an eighth of his debt. If a daughter inherits a half, she pays the creditor half of his debt. Whichever women confirm him, pay him according to this. </p>
