      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 6: </a> </em>
      <h1> <em> Prayer in Ramadan </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: Stimulation of the Desire for Prayer in Ramadan </h3>
      <hr>
      <em> <a name="006.6.1.1"> Book 6, Number 6.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Urwa ibn az-Zubayr from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that the Messenger of Allah, may Allah bless him and grant him peace, prayed in the mosque one night and people prayed behind him. Then he prayed the next night and there were more people. Then they gathered on the third or fourth night and the Messenger of Allah, may Allah bless him and grant him peace, did not come out to them. In the morning, he said, "I saw what you were doing and the only thing that prevented me from coming out to you was that I feared that it would become obligatory (fard) for you." This happened in Ramadan. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="006.6.1.2"> Book 6, Number 6.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Abu Salama ibn Abd ar-Rahman ibn Awf from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, used to exhort people to watch the night in prayer in Ramadan but never ordered it definitely. He used to say, "Whoever watches the night in prayer in Ramadan with trust and expectancy, will be forgiven all his previous wrong actions." </p>
        <p> Ibn Shihab said, "The Messenger of Allah, may Allah bless him and grant him peace, died while that wasstill the custom, and it continued to be the custom in the khalifate of Abu Bakr and at the beginning of the khalifate of Umar ibn al-Khattab." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: Watching the Night in Prayer </h3>
      <hr>
      <em> <a name="006.6.2.3"> Book 6, Number 6.2.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik related to me from Ibn Shihab from Urwa ibn az-Zubayr that Abd ar-Rahman ibn Abd al-Qari said, "I went out with Umar ibn alKhattab in Ramadan to the mosque and the people there were spread out in groups. Some men were praying by themselves, whilst others were praying in small groups. Umar said, 'By Allah! It would be better in my opinion if these people gathered behind one reciter.' So he gathered them behind Ubayy ibn Kab. Then I went out with him another night and the people were praying behind their Qur'an reciter. Umar said, 'How excellent this new way is, but what you miss while you are asleep is better than what you watch in prayer.' He meant the end of the night, and people used to watch the beginning of the night in prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="006.6.2.4"> Book 6, Number 6.2.4: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Muhammad ibn Yusuf that as-Sa'ib ibn Yazid said, "Umar ibn al-Khattab ordered Ubayy ibn Kab and Tamim ad-Dari to watch the night in prayer with the people for eleven rakas.The reciterof the Qur'an would recite the Mi'in (a group of medium-sized suras) until we would be leaning on our staffs from having stood so long in prayer. And we would not leave until the approach of dawn." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="006.6.2.5"> Book 6, Number 6.2.5: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Yazid ibn Ruman said, "The people used to watch the night in prayer during Ramadan for twenty-three rakas in the time of Umar ibn al-Khattab." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="006.6.2.6"> Book 6, Number 6.2.6: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Da'ud ibn al-Husayn that he heard al-Araj say, "I never saw the people in Ramadan, but that they were cursing the disbelievers." He added, "The reciter of Qur'an used to recite surat al-Baqara in eight rakas and if he did it in twelve rakas the people would think that he had made it easy." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="006.6.2.7"> Book 6, Number 6.2.7: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Abdullah ibn Abi Bakr said, "I heard my father say, 'We finished praying in Ramadan and the servants hurried with the food, fearing the approach of dawn.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="006.6.2.8"> Book 6, Number 6.2.8: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that Dhakwan Abu Amr (a slave belonging to A'isha, the wife of the Prophet, may Allah bless him and grant him peace, who was freed by her after her death) used tostand in prayer and recite for her in Ramadan. </p>
      </blockquote>
      <!--EndHadithBody-->
