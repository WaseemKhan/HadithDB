      <div class="content"><em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 48: </a> </em>
        <h1> <em> Dress </em> </h1>
        <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
        <hr>
        <h3> Section: Wearing Clothes for Beautification </h3>
        <hr>
        <em> <a name="048.48.1.1"> Book 48, Number 48.1.1: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Zayd ibn Aslam that Jabir ibn Abdullah al-Ansari said, "We went out with the Messenger of Allah, may Allah bless him and grant him peace, in the raid on the Banu Ammar tribe." Jabir said, "I was resting under a tree when the Messenger of Allah, may Allah bless him and grant him peace, came. I said, 'Messenger of Allah; come to the shade.' So the Messenger of Allah, may Allah bless him and grant him peace, sat down, and I stood up and went to a sack of ours. I looked in it for something and found a small cucumber and broke it. Then I brought it to the Messenger of Allah, may Allah bless him and grant him peace. He said, 'From where did you get this?' I said, 'We brought it from Madina, Messenger of Allah.' " </p>
          <p> Jabir continued, "We had a friend of ours with us whom we used to equip to go out to guard our mounts. I gave him what was necessary and then he turned about to go to the mounts and he was wearing two threadbare cloaks of his. The Messenger of Allah, may Allah bless him and grant him peace, looked at him and said, 'Does he have two garments other than these?' I said, 'Yes, Messenger of Allah. He has two garments in the bag.' I gave them to him. He said, 'Let him go and put them on.' I let him go to put them on. As he turned to go, the Messenger of Allah, may Allah bless him and grant him peace, exclaimed, 'May Allah strike his neck. Isn't that better for him?' He said (taking him literally), 'Messenger of Allah, in the way of Allah.' The Messenger of Allah, may Allah bless him and grant him peace, said, 'In the way of Allah.' " Jabir added, "The man was killed in the way of Allah." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.1.2"> Book 48, Number 48.1.2: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that he heard that Umar ibn al-Khattab said, "I love to look at a Qur'an reader in white garments." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.1.3"> Book 48, Number 48.1.3: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Ayyub ibn Abi Tamim that Ibn Sirin said, ''Umar ibn al-Khattab said, 'Allah has been generous to you, so be generous to yourselves. Let a man wear a combination of his garments.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Wearing Dyed Garments and Gold </h3>
        <hr>
        <em> <a name="048.48.2.4"> Book 48, Number 48.2.4: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar wore garments dyed with red earth and dyed with saffron. </p>
          <p> Yahya said that he heard Malik say, "I disapprove of youths wearing any gold because I heard that the Messenger of Allah, may Allah bless him and grant him peace, forbade wearing gold rings, and I disapprove of it for males old or young." </p>
          <p> Yahya said, "I heard Malik say about men wearing wraps dyed with safflower in their houses and courtyards, 'I do not know that any of that is haram but I prefer other garments than that.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Wearing Silk </h3>
        <hr>
        <em> <a name="048.48.3.5"> Book 48, Number 48.3.5: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Malik related to me from Hisham ibn Urwa from his father that A'isha the wife of the Prophet, may Allah bless him and grant him peace, dressed Abdullah ibn az-Zubayr in a shawl of silk which A'isha used to wear. </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Clothes Disapproved for Women to Wear </h3>
        <hr>
        <em> <a name="048.48.4.6"> Book 48, Number 48.4.6: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Alqama ibn Abi Alqama that his mother said, "Hafsa bint Abd ar-Rahman visited A'isha, the wife of the Prophet, may Allah bless him and grant him peace, and Hafsa was wearing a long thin head scarf A'isha tore it in two and made a wide one for her." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.4.7"> Book 48, Number 48.4.7: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Muslim ibn Abi Maryam from Abu Salih that Abu Hurayra said, "Women who are naked even though they are wearing clothes, go astray and make others go astray, and they will not enter the Garden and they will not find its scent, and its scent is experienced from as far as the distance travelled in five hundred years." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.4.8"> Book 48, Number 48.4.8: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Yahya ibn Said from Ibn Shihab that the Messenger of Allah, may Allah bless him and grant him peace, stood up in the night and looked at the horizon of the sky. He said, "What treasures has the night opened? What trials have occurred? How many are dressed in this world and will be naked on the Day of Rising. Warn the women in their rooms." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: A Man Dragging his Garments </h3>
        <hr>
        <em> <a name="048.48.5.9"> Book 48, Number 48.5.9: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abdullah ibn Dinar from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "A person who drags his garment in arrogance will not be looked at by Allah on the Day of Rising." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.5.10"> Book 48, Number 48.5.10: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "On the Day of Rising, Allah the Blessed, the Exalted, will not look at a person who drags his lower garment in arrogance." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.5.11"> Book 48, Number 48.5.11: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi and Abdullah ibn Dinar and Zayd ibn Aslam that all of them informed him from Abdullah ibn Umar that the Messenger of Allah, may Allah bless him and grant him peace, said, "On the Day of Rising, Allah will not look at a person who drags his garment in arrogance." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.5.12"> Book 48, Number 48.5.12: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from al Ala ibn Abd ar-Rahman that his father said, "I asked Abu Said al-Khudri about the lower garment. He said that he would inform me with knowledge and that he had heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'The lower garment of the mumin should reach to the middle of his calves. There is no harm in what is between that and the ankles. What is lower than that is in the Fire. What is lower than that is in the Fire. On the Day of Rising, Allah will not look at a person who trails his lower garment in arrogance.' " </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: A Woman Dragging her Garments </h3>
        <hr>
        <em> <a name="048.48.6.13"> Book 48, Number 48.6.13: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu Bakr ibn Nafi from his father Nafi, the mawla of Ibn Umar that Safiyya bint Abi Ubayd informed him that Umm Salama, the wife of the Prophet, may Allah bless him and grant him peace, said that when the lower garment of women was mentioned to the Messenger of Allah, he said, "She lets it down a handspan." Umm Salama said, "If it leaves her uncovered?" He said, "Then the length of a forearm and let her not increase it." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Wearing Sandals </h3>
        <hr>
        <em> <a name="048.48.7.14"> Book 48, Number 48.7.14: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "Do not wear one sandal. Wear both of them or go with both feet bare." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.7.15"> Book 48, Number 48.7.15: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj from Abu Hurayra that the Messenger of Allah, may Allah bless him and grant him peace, said, "When you put on sandals, begin with the right foot. When you take them off, begin with the left foot. The right foot is the first to be put in the sandal and the last to be taken out." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.7.16"> Book 48, Number 48.7.16: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from his paternal uncle Abu Suhayl ibn Malik from his father that Kab al-Ahbar said to a man who took off his sandals, "Why have you taken off your sandals? Perhaps you have interpreted this ayat, 'Remove your sandals. You are in the pure valley of Tuwa?' (Sura 20 ayat 12) Do you know what the sandals of Musa were?" </p>
          <p> Malik (the father of Abu Suhayl) said, "I do not know what the man answered." Kab said, "They were made from the skin of a dead donkey." </p>
        </blockquote>
        <!--EndHadithBody-->
        <h3> Section: Ways of Dressing </h3>
        <hr>
        <em> <a name="048.48.8.17"> Book 48, Number 48.8.17: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Abu'z-Zinad from al-Araj that Abu Hurayra said, "The Messenger of Allah, may Allah bless him and grant him peace, forbade two sales. Mulamasa, in which a man is obliged to buy whatever he touches without any choice in the matter, and munabadha, in which two men throw their garment to each other without either seeing the other's garment. He also forbade two ways of dressing. One in which a man sits with his legs drawn up to his chest wrapped in one garment that does not cover his genitals, and the other in which a man wraps a single garment over one arm and shoulder restricting them." </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.8.18"> Book 48, Number 48.8.18: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik from Nafi from Abdullah ibn Umar that Umar ibn al-Khattab saw a silk robe at the door of the mosque. He said, "Messenger of Allah, would you buy this robe and wear it on jumua and when envoys come to you?" The Messenger of Allah, may Allah bless him and grant him peace, said, "Only a person who has no portion in the next world wears this." Then the Messenger of Allah, may Allah bless him and grant him peace, was brought some robes of the same material and gave Umar ibn al-Khattab one of the robes. Umar said, "Messenger of Allah, do you clothe me in it when you said what you said about the robe of Utarid?" The Messenger of Allah, may Allah bless him and grant him peace, said, "I did not give it to you to wear." Umar gave it to a brother of his in Makka who was still an idolater. </p>
        </blockquote>
        <!--EndHadithBody-->
        <hr>
        <em> <a name="048.48.8.19"> Book 48, Number 48.8.19: </a> </em>
        <!--BeginHadithBody-->
        <blockquote>
          <p> Yahya related to me from Malik that Ishaq ibn Abdullah ibn Abi Talha said, "Anas ibn Malik said, 'I saw Umar ibn al-Khattab when he was the amir of Madina. Three patches were sewn between his shoulders, one patched over the other.' '' </p>
        </blockquote>
        <!--EndHadithBody-->
