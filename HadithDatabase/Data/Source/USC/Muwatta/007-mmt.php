      <em> <a href="mmtintro.php"> Translation of Malik's Muwatta, Book 7: </a> </em>
      <h1> <em> Tahajjud </em> </h1>
      <p> Courtesy of <a href="http://www.islsoftware.com/">ISL Software</a>, makers of the <strong><a href="http://www.islsoftware.com/winalim.htm">WinAlim Islamic database</a></strong>. </p>
      <hr>
      <h3> Section: Concerning Prayer in the Night </h3>
      <hr>
      <em> <a name="007.7.1.1"> Book 7, Number 7.1.1: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Muhammad ibn al-Munkadir from Said ibn al-Jubayr that a man who has approval (as a relater of hadith), told him that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "If a man prays in the night and sleep overcomes him during it, Allah writes for him the reward of his prayer, and his sleep is sadaqa for him." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.1.2"> Book 7, Number 7.1.2: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu'nNadr, the mawla of Umar ibn 'Ubaydullah, from Abu Salama ibn Abd ar-Rahman that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, said, "I was sleeping in front of the Messenger of Allah, may Allah bless him and grant him peace, and my feet were in his qibla. When he prostrated, he nudged me and I pulled up my feet,and when he stood up I spread them out." She added, "There were no lamps in the house at that time." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.1.3"> Book 7, Number 7.1.3: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that the Messenger of Allah, may Allah bless him and grant him peace, said, "If you are drowsy in prayer, sleep until sleep leaves you, because if you pray while you are drowsy, you do not know whether you may intend to ask for forgiveness but (in fact) ask for harm." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.1.5a"> Book 7, Number 7.1.5a: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ismail from Ibn Abi Hakim that he had heard that the Messenger of Allah, may Allah bless him and grant him peace, heard a woman praying at night. He said, "Who is that?" and someone said to him,"It is al-Hawla bint Tuwayt, she does not sleep in the night." The Messenger of Allah, may Allah bless him and grant him peace, disapproved of that and his disapproval showed in his face. Then he said, "Allah, the Blessed and Exalted, does not become weary, but you become weary. Take on whatever is within your capability." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.1.5b"> Book 7, Number 7.1.5b: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Zayd ibn Aslam from his father that Umar ibn al-Khattab used to pray as much as Allah willed in the night until at the end of the night he would wake his family for the prayer. He used to say to them, "The prayer, the prayer." Then he would recite the ayat, "Enjoin prayer on your family and be constant in it. We do not ask you for your provision. We provide for you. And the end result is for taqwa." (Sura 20 ayat 132) </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.1.6"> Book 7, Number 7.1.6: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Said ibn al-Musayyab used to say, "Sleep is disapproved of before isha and conversation after it." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.1.7"> Book 7, Number 7.1.7: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Abdullah ibn Umar used to say, "(Voluntary) prayer in both the day and night is two at a time with a taslim after every raka.'' </p>
        <p> Malik said, "That is the custom among us. " </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: How the Prophet, may Allah bless him and grant him peace, Prayed the Witr </h3>
      <hr>
      <em> <a name="007.7.2.8"> Book 7, Number 7.2.8: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab from Urwa ibn az-Zubayr from A'isha, the wife of the Prophet, may Allah bless him and grant him peace, that the Messenger of Allah, may Allah bless him and grant him peace, used to pray eleven rakas in the night, making them odd by a single one, and when he had finished he lay down on his right side. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.2.9"> Book 7, Number 7.2.9: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Said ibn Abi Said al-Maqburi from Abu Salama ibn Abd ar-Rahman ibn Awf that he asked A'isha, the wife of the Prophet, may Allah bless him and grant him peace, what the prayer of the Messenger of Allah, may Allah bless him and grant him peace, was like during Ramadan. She said, "The Messenger of Allah, may Allah bless him and grant him peace, did not go above eleven rakas in Ramadan or at any other time. He prayed four - do not ask me about their beauty or length. Then he prayed another four - do not ask me about their beauty and length. Then he prayed three." </p>
        <p> A'isha continued, "I said, 'Messenger of Allah, are you sleeping before you do the witr?' He said, A'isha, my eyes sleep but my heart does not sleep.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.2.10"> Book 7, Number 7.2.10: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that A'isha, umm al-muminin said, "The Messenger of Allah, may Allah bless him and grant him peace, used to pray thirteen rakas in the night and then would pray two rakas when he heard the adhan for the subh prayer." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.2.11"> Book 7, Number 7.2.11: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Makhrama ibn Sulayman from Kurayb, the mawla of Ibn Abbas, that Abdullah ibn Abbas told him that he had spent a night at the house of Maimuna, the wife of the Prophet, may Allah bless him and grant him peace, who was also Ibn Abbas' mother's sister. Ibn Abbas said, "I lay down with my head on the breadth of the cushion, and the Messenger of Allah, may Allah bless him and grant him peace, and his wife lay down with their heads on its length. The Messenger of Allah, may Allah bless him and grant him peace, slept, until, halfway through the night or a little before or after it, he awoke and sat up and wiped the sleep away from his face with his hand. Then he recited the last ten ayats of sura Ali Imran (Sura3). Then he got up and went over to a water-skin which was hanging up and did wudu from it, doing his wudu thoroughly, and then he stood in prayer." </p>
        <p> Ibn Abbas continued, "I stood up and did the same and then went and stood by his side. The Messenger of Allah, may Allah bless him and grant him peace, put his right hand on my head and took my right ear and tweaked it. He prayed two rakas, then two rakas, then two rakas, then two rakas, then two rakas, then two rakas, and then prayed an odd raka. Then he lay down until the muadhdhin came to him, and then prayed two quick rakas, and went out and prayed subh ." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.2.12"> Book 7, Number 7.2.12: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abdullah ibn Abi Bakr from his father that Abdullah ibn Qays ibn Makhrama told him that Zayd ibn Khalid al-Juhani said one night that he was going to observe the prayer of the Messenger of Allah, may Allah bless him and grant him peace. He said, "I rested my head on his threshold. The Messenger of Allah, may Allah bless him and grant him peace, got up and prayed two long, long, long rakas. Then he prayed two rakas which were slightly less long than the two before them. Then he prayed two rakas which were slightly less long than the two before them. Then he prayed two rakas which were slightly less long than the two before them. Then he prayed two rakas which were slightly less long than the two before them. Then he prayed two rakas which were slightly less long than the two before them. Then he prayed an odd raka, making thirteen rakas in all." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Command to Pray the Witr </h3>
      <hr>
      <em> <a name="007.7.3.13"> Book 7, Number 7.3.13: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi and Abdullah ibn Umar that a man asked the Messenger of Allah, may Allah bless him and grant him peace, about night prayers. The Messenger of Allah, may Allah bless him and grant him peace, said, "Night prayers are two by two, and when you are afraid that dawn is approaching, pray one raka to make what you have prayed odd." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.14"> Book 7, Number 7.3.14: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said from Muhammad ibn Yahya ibn Habban from Ibn Muhayriz that a man from the Kinana tribe called al-Mukhdaji heard a man in Syria known as Abu Muhammad saying, "The witr is obligatory (fard)." Al-Mukhdaji said, "I went to Ubada ibn as-Samit and presented myself to him as he was going to the mosque, and told him what Abu Muhammad had said. Ubada said that Abu Muhammad had lied and that he had heard the Messenger of Allah, may Allah bless him and grant him peace, say, 'Allah the Majestic and Mighty has written five prayers for mankind, and whoever does them and does not waste anything of them by making light of what is due to them, there is a pact for him with Allah that He will admit him into the Garden.Whoever does not do them, there is no pact for him with Allah. If He wishes, He punishes him, and if He wishes, He admits him into the Garden.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.15"> Book 7, Number 7.3.15: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abu Bakr ibn Umar that Said ibn Yasar said, ''I was travelling with Abdullah ibn Umar on the road to Makka, and fearing that it was nearly dawn. I dismounted and prayed witr. Abdullah said, 'Is there not a model for you in the Messenger of Allah, may Allah bless him and grant him peace?'  I said, 'Of course, by Allah!'  He said, 'The Messenger of Allah, may Allah bless him and grant him peace, used to pray witr on his camel.' " </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.16"> Book 7, Number 7.3.16: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Yahya ibn Said that Said ibn al-Musayyab said, "Abu Bakr as-Siddiq used to pray witr when he wished to go to bed, and Umar ibn al-Khattab used to pray witr at the end of the night. As for me, I pray witr when I go to bed." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.17"> Book 7, Number 7.3.17: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that a man asked Abdullah ibn Umar whether the witr was obligatory and Abdullah ibn Umar said, "The Messenger of Allah, may Allah bless him and grant him peace, prayed witr, and the muslims prayed witr." The man began repeating his question, and Abdullah ibn Umar kept saying, "The Messenger of Allah, may Allah bless him and grant him peace, prayed witr, and the muslims prayed witr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.18"> Book 7, Number 7.3.18: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that A'isha, the wife of the Prophet, may Allah bless him and grant him peace, used to say, "If someone fears that he will sleep through till the morning, let him pray the witr before he sleeps, and if some one hopes to wake for the last part of the night, let him delay his witr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.19"> Book 7, Number 7.3.19: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Nafi said, "I was with Abdullah ibn Umar in Makka. The sky was clouded over and Abdullah feared that dawn was approaching so he prayed one raka for witr. Then the clouds cleared and he saw that it was still night, so he made his prayers even with one raka. Then he continued to pray two rakas at a time, until when he feared the approach of dawn, he prayed one raka for witr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.20"> Book 7, Number 7.3.20: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Nafi that Abdullah ibn Umar used to say the taslim between the two rakas and the one raka of witr so that he could order something he needed. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.213"> Book 7, Number 7.3.213: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Ibn Shihab that Sad ibn Abi Waqqas used to pray witr after isha with one raka. </p>
        <p> Malik said, "This is not the situation with us. Rather three is the minimum for witr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.22"> Book 7, Number 7.3.22: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abdullah ibn Dinar that Abdullah ibn Umar used to say, "The maghrib prayer is the witr of the daytime prayers." </p>
        <p> Malik said, "If someone prays witr at the beginning of the night, and goes to sleep, and then wakes up and it seems good to him to pray, let him pray, two rakas at a time. That is what I like most of what I have heard." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.23"> Book 7, Number 7.3.23: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Abd al Karim ibn Abi'l-Mukhariq al-Basri from Said ibn Jubayr that Abdullah ibn Abbas slept, and when he woke up, he said to his servant, "Go and see what the people have done," (by that time his sight had gone.) The servant went out and returned saying, "The people have left from subh," so Abdullah ibn Abbas got up and prayed witr and then prayed subh. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.24"> Book 7, Number 7.3.24: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that he had heard that Abdullah ibn Abbas and Ubada ibn as-Samit and al-Qasim ibn Muhammad and Abdullah ibn Amir ibn Rabia had all prayed witr after the break of dawn. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.25"> Book 7, Number 7.3.25: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik from Hisham ibn Urwa from his father that Abdullah ibn Masud said, "I do not mind if the iqama for the subh prayer is called while I am still praying witr." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.26"> Book 7, Number 7.3.26: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Yahya ibn Said said, ''Ubada ibn as-Samit used to lead the people in prayer. One day he came out for subh and the muadhdhin began to give the iqama for the subh prayer Ubada silenced him, prayed witr and then led them in subh." </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.27"> Book 7, Number 7.3.27: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Yahya related to me from Malik that Abd ar-Rahman ibn al-Qasim said, "I heard Abdullah ibn Amir ibn Rabia say, 'I sometimes pray witr while hearing the iqama, or after the break of dawn.' " Abd ar-Rahman was not certain which he said. </p>
      </blockquote>
      <!--EndHadithBody-->
      <hr>
      <em> <a name="007.7.3.28"> Book 7, Number 7.3.28: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
        <p> Malik related to me that Abd ar-Rahman ibn al-Qasim heard his father al-Qasim ibn Muhammad say, "I have prayed witr after dawn." </p>
        <p> Malik said, "Only a person who oversleeps so that he does not do the witr prays it after dawn. No one should intentionally make his witr after dawn." </p>
      </blockquote>
      <!--EndHadithBody-->
      <h3> Section: The Two Rakas of Fajr </h3>
      <hr>
      <em> <a name="007.7.4.29"> Book 7, Number 7.4.29: </a> </em>
      <!--BeginHadithBody-->
      <blockquote>
